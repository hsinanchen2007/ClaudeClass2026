// 檔案：lesson_1_3_shared_memory.cpp
// 說明：展示同一程序內的執行緒共享記憶體

// =============================================================================
//  課程 1.3：執行緒 vs 程序  —  同一位址空間內的共享，與它的代價
// =============================================================================
//
// 【主題資訊 Information】
//   標頭檔：<thread>（C++11 起）
//   本檔示範：同一程序內的兩條 std::thread 共享同一個全域變數 sharedCounter
//   對照的 OS 原語：
//     建立程序：fork() / posix_spawn()（POSIX）、CreateProcess()（Windows）
//     建立執行緒：pthread_create()（POSIX）、CreateThread()（Windows）
//     C++11 之後一律用 std::thread 包裝，跨平台且型別安全
//   ⚠️ 標準本身沒有「程序」的概念。C++ 標準只定義執行緒與記憶體模型；
//      fork()、位址空間、TLB 這些都是 OS 層的事，本課借 OS 觀點來解釋
//      「為什麼執行緒便宜、為什麼共享是雙面刃」。
//
// 【詳細解釋 Explanation】
//
// 【1. 一句話區分：隔離的單位 vs 排程的單位】
//   程序（process）是「資源擁有 + 隔離」的單位：它擁有一份獨立的虛擬位址空間、
//   檔案描述符表、訊號處理表。
//   執行緒（thread）是「排程」的單位：OS 排程器實際挑選來上 CPU 的東西。
//   一個程序至少有一條執行緒（主執行緒），也可以有很多條。
//   所以正確的說法不是「程序比較慢、執行緒比較快」，而是：
//     兩條執行緒共用一張分頁表（page table）；兩個程序各有一張。
//   本課後面所有的成本差異，全部從這一句推導出來。
//
// 【2. 誰共享、誰私有（本檔的關鍵）】
//   同一程序內的執行緒「共享」：
//     * 程式碼段（.text）
//     * 全域 / static 變數（.data、.bss）← 本檔的 sharedCounter 就在這裡
//     * 堆積（heap）：一條執行緒 new 的東西，另一條可以直接讀寫
//     * 開啟的檔案描述符、current working directory、訊號處理設定
//   每條執行緒「私有」：
//     * 自己的堆疊（stack）—— 區域變數、呼叫框架
//     * 程式計數器（PC/RIP）、堆疊指標（SP/RSP）、通用暫存器
//     * thread_local 變數（TLS）、errno（現代 libc 中 errno 是 thread-local 巨集）
//
//   ⚠️ 「私有」的精確意思是「各有一份」，不是「別人看不到」。
//      同一個位址空間裡，只要你把區域變數的位址交出去（例如傳給另一條執行緒的
//      lambda 捕獲、或塞進全域指標），另一條執行緒照樣能讀寫它。
//      執行緒之間沒有記憶體保護；真正的隔離只有程序邊界才有。
//      這正是「懸空指標指向已結束執行緒的 stack」這類 bug 的來源。
//
// 【3. 為什麼本檔的結果是錯的：data race，不只是「不同步」】
//   ++sharedCounter 看起來是一行，在 -O0 的 x86-64 上是三條指令：
//       mov  eax, DWORD PTR sharedCounter[rip]   ; load
//       add  eax, 1                              ; modify
//       mov  DWORD PTR sharedCounter[rip], eax   ; store
//   兩條執行緒可能各自 load 到同一個舊值、各自 +1、再各自寫回 →
//   兩次遞增只生效一次，這叫 lost update。
//   這在 C++ 標準裡不是「可能算錯」而已，而是直接構成 data race → 未定義行為
//   （[intro.races]）。詳見下面【注意事項】第 1 條。
//
// 【4. 建立與切換的成本差在哪（面試最愛問）】
//   建立：
//     fork() 要複製分頁表、fd 表、訊號表等核心結構（現代 Linux 用 copy-on-write，
//     不複製實體資料頁，但分頁表本身還是要建）。
//     pthread_create() 只要配一份 stack + 一個 task_struct，並與呼叫者共用 mm_struct。
//     實務量級：建立執行緒通常是幾十微秒等級，建立程序明顯更貴；
//     ⚠️ 確切數字高度依賴核心版本、CPU、記憶體配置，不要背固定值，要自己量。
//   切換（context switch）——這是最關鍵的一點：
//     * 執行緒 → 同程序的另一條執行緒：
//         換暫存器、換堆疊指標、換 PC。分頁表根指標（x86-64 的 CR3）不變，
//         因此「不需要 flush TLB」，已翻譯好的虛擬→實體位址映射還能繼續用。
//     * 程序 → 另一個程序：
//         必須換掉分頁表根（寫入 CR3）。在沒有硬體標記的年代，這會使整個
//         TLB 失效（TLB flush），切換後的前幾千次記憶體存取都得重走 page walk，
//         成本遠高於「存幾個暫存器」本身。
//     * 現代硬體有緩解機制：x86 的 PCID（Process-Context Identifier）、
//       ARM 的 ASID，讓 TLB entry 帶上位址空間標籤，切換時不必全部丟掉。
//       所以「換程序一定要 flush 整個 TLB」在今天已經不精確；
//       但 cache 汙染（L1/L2 內容被新程序擠掉）依然存在，程序切換仍然比較貴。
//   ⚠️ 上述屬於 OS/硬體實作細節，不同架構與核心版本差異很大。
//
// 【5. 那什麼時候該用程序而不是執行緒？】
//   * 需要故障隔離：一條執行緒踩壞記憶體會拖垮整個程序；一個子程序 crash 不會。
//     瀏覽器把每個分頁放進獨立程序、就是為了這個。
//   * 需要權限隔離：子程序可以降權（drop privileges）、套用 seccomp / namespace。
//   * 需要不同的生命週期或不同的執行檔。
//   反之，需要大量共享資料、且共享成本敏感時，用執行緒（否則得走 IPC：
//   pipe、socket、shared memory、message queue，資料要序列化與複製）。
//
// 【概念補充 Concept Deep Dive】
//   (A) 虛擬記憶體是這一切的基礎
//       每個程序有自己的虛擬位址空間，同一個虛擬位址 0x400000 在兩個程序裡
//       透過各自的分頁表被翻譯到不同的實體頁 → 天然隔離。
//       同程序的兩條執行緒用同一張分頁表 → 同一個虛擬位址必然指到同一個實體位置，
//       這就是「共享」在硬體層的真正意義：不是複製，而是同一份記憶體。
//
//   (B) Linux 的真相：執行緒就是共享得比較多的行程
//       Linux 底層只有一個 clone() 系統呼叫。
//         fork()            ≈ clone(不共享 mm)
//         pthread_create()  ≈ clone(CLONE_VM | CLONE_FILES | CLONE_FS | CLONE_SIGHAND | ...)
//       核心排程的單位叫 task；同一個 thread group（共用 tgid）的多個 task
//       在使用者眼中就是「一個程序的多條執行緒」。
//       這也是為什麼 getpid() 在所有執行緒回傳同一個值，而 gettid() 各不相同。
//
//   (C) 堆疊大小是實作定義的
//       std::thread 沒有提供設定 stack 大小的介面（這是標準刻意留給實作的）。
//       Linux/glibc 預設每條執行緒 8 MiB 虛擬位址空間（可用 ulimit -s 查詢與調整），
//       但那是「保留虛擬位址」，實體記憶體隨用隨配。
//       要精確控制得下沉到 pthread_attr_setstacksize()。
//       ⚠️ 這是實作定義的數值，不同平台不同（例如 musl 預設小得多）。
//
//   (D) 快取一致性：共享不是免費的
//       兩條執行緒在不同核心上猛改同一個變數時，該 cache line 會在核心之間
//       來回彈跳（cache line ping-pong），每次都要走 MESI 之類的一致性協定。
//       即使加了 mutex 或 atomic 把正確性修好，效能仍可能很差。
//       這就是 false sharing 的近親問題，後續課程會處理。
//
// 【注意事項 Pay Attention】
//   1. ★ data race ≠ race condition（最常被混為一談，面試必考）
//      * data race：兩條以上執行緒未同步地存取同一記憶體位置，且至少一方是寫入。
//        C++ 標準直接判定為 undefined behavior（[intro.races]）。
//        本檔的 ++sharedCounter 就是 data race。
//      * race condition：程式的結果取決於事件發生的時序，屬於邏輯錯誤。
//        它可以完全沒有 data race —— 例如兩邊都乖乖用了 mutex，但先做 contains()
//        再做 insert()（check-then-act），中間空窗被插隊，邏輯照樣錯。
//      關係：data race 是記憶體模型層級的違規（UB）；race condition 是設計層級的
//      正確性問題。修掉 data race 不代表修掉 race condition。
//
//   2. ★ 因為是 UB，不能說「結果一定不等於 200000」。
//      未定義行為的意思是標準不保證任何結果，包含「剛好是對的」。
//      本機實測（g++ 15.2, x86-64, 16 硬體執行緒）：
//        -O0（本檔預設編譯方式）：多次執行落在約 100000 ~ 170000 之間跳動，
//                                 每次都不同（實測值例：109705、130290、168399）
//        -O2：連續多次都印出 200000
//      為什麼 -O2 反而「對」？因為編譯器看到迴圈只是把同一個變數加 100000 次，
//      合法地把它優化成單一次加法（在無 data race 的假設下這是等價變換）。
//      race 的視窗因此縮到幾乎為零 —— 但 race 並沒有消失。
//      ⚠️ 所以「開了 -O2 跑出正確答案」永遠不能當作沒有 race 的證據。
//      要證明有沒有 race，請用工具：g++ -fsanitize=thread（ThreadSanitizer）。
//
//   3. 修法不在本課（本檔刻意保留錯誤以供觀察）：
//      std::mutex + std::lock_guard（課程 1.4）、std::atomic<int>（後續課程）。
//      volatile 不是答案 —— 它不提供原子性，也不建立跨執行緒的 happens-before。
//
//   4. 本檔兩條執行緒都有 join()。若忘記 join 又讓 std::thread 解構，
//      解構子會呼叫 std::terminate()，程式直接中止 —— 這不是 UB，
//      而是標準明文規定的行為（[thread.thread.destr]）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】執行緒 vs 程序
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 執行緒和程序最根本的差別是什麼？
//     答：位址空間。同一程序的執行緒共用一張分頁表，因此共用全域區與堆積，
//         彼此之間沒有記憶體保護；不同程序各有一張分頁表，天然隔離，
//         要交換資料必須走 IPC。其他所有差異（建立成本、切換成本、崩潰影響範圍）
//         都是從這一點推導出來的結果，不是獨立的性質。
//     追問：那為什麼「共用一張分頁表」會讓 context switch 便宜這麼多？
//           → 見 Q2。
//
// 🔥 Q2. 為什麼執行緒切換比程序切換便宜？
//     答：程序切換必須更換分頁表根指標（x86-64 寫 CR3），使既有的 TLB 映射
//         失效，切換後大量記憶體存取要重走 page walk；同程序的執行緒切換
//         不動 CR3，TLB 可以繼續用，只需存/回復暫存器與堆疊指標。
//     追問：現代 CPU 還會 flush 整個 TLB 嗎？
//           → 不一定。x86 的 PCID 與 ARM 的 ASID 讓 TLB entry 帶位址空間標籤，
//             可避免全域 flush。但 L1/L2 cache 仍會被新程序的資料擠掉
//             （cache 汙染），所以程序切換依然比較貴，只是差距沒有教科書上那麼誇張。
//
// 🔥 Q3. 這支程式印出 113304 而不是 200000。這是 data race 還是 race condition？
//     答：兩個角度都成立，但精確講它是 data race：對 sharedCounter 的存取
//         沒有任何同步，而且雙方都在寫 → 標準判定為 undefined behavior。
//         race condition 是更廣的概念（結果取決於時序的邏輯錯誤），
//         而且可以在完全沒有 data race 的程式裡發生。
//     追問：舉一個「沒有 data race 但有 race condition」的例子。
//           → if (!set.contains(k)) set.insert(k);  兩個呼叫各自用 mutex 保護，
//             沒有 data race，但在兩次呼叫的空窗被別的執行緒插入，邏輯仍然錯。
//             正解是把 check 與 act 放進同一個臨界區。
//
// ⚠️ 陷阱1. 「執行緒共享所有東西，所以一條執行緒的區域變數別的執行緒讀不到。」
//     答：前半句錯（stack、暫存器、thread_local 是各自一份），
//         後半句也錯（同一位址空間內，只要拿得到位址就讀得到）。
//     為什麼會錯：多數人把「各有一份」誤讀成「有記憶體保護」。
//         執行緒之間完全沒有保護機制 —— 把區域變數的位址傳給另一條執行緒，
//         它就能合法地讀寫；等原執行緒結束、stack 被回收後再存取，就是懸空指標 UB。
//         真正的記憶體隔離只存在於程序邊界。
//
// ⚠️ 陷阱2. 「我加了 -O2 跑，結果都是 200000，所以這段程式其實沒問題。」
//     答：錯。-O2 的 200000 是編譯器把整個迴圈折成一次加法的副作用，
//         race 只是視窗變小到量不到，並沒有被修掉。換一台機器、換個
//         優化等級、或把變數換成編譯器折不掉的形式，立刻復發。
//     為什麼會錯：把「測不到」當成「不存在」。data race 是 UB，
//         而 UB 的表現可以隨編譯器版本、優化等級、CPU 架構任意改變。
//         驗證 race 要用 ThreadSanitizer（-fsanitize=thread），不是靠跑幾次看答案。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <thread>

int sharedCounter = 0;  // 全域變數，所有執行緒共享

void increment() {
    for (int i = 0; i < 100000; ++i) {
        ++sharedCounter;  // 多個執行緒同時修改同一變數
    }
}

int main() {
    std::thread t1(increment);
    std::thread t2(increment);
    
    t1.join();
    t2.join();
    
    // 預期 200000，但結果通常不是！
    std::cout << "預期結果: 200000" << std::endl;
    std::cout << "實際結果: " << sharedCounter << std::endl;
    
    return 0;
}

