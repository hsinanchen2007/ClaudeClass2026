//
// 觀察用（證明 race 真的存在，而不是靠肉眼猜）：
//   g++ -std=c++17 -Wall -Wextra -pthread -fsanitize=thread "課程 1.3：執行緒 vs 程序.cpp" -o lesson_1_3_tsan
//   ./lesson_1_3_tsan        # ThreadSanitizer 會直接指出 data race 的兩個存取點
//
// 對照用（看編譯器如何把 race 視窗優化到量不到）：
//   g++ -std=c++17 -O2 -Wall -Wextra -pthread "課程 1.3：執行緒 vs 程序.cpp" -o lesson_1_3_o2

// ⚠️（每次執行都不同）本程式含 data race，屬於未定義行為，
//    上面那筆 130290 只是本機某一次的實測值，不是規格。

// === 預期輸出 ===
// 預期結果: 200000
// 實際結果: 130290
//
//    標準不保證任何特定結果 —— 包含「剛好正確」。
//
// 本機實測（g++ 15.2.0, x86-64, 16 硬體執行緒），連續 6 次以預設 -O0 編譯執行：
//   實際結果: 130290   exit=0
//   實際結果: 112276   exit=0
//   實際結果: 109705   exit=0
//   實際結果: 168399   exit=0
//   實際結果: 110866   exit=0
//   實際結果: 117455   exit=0
//   → 全部小於 200000，且彼此不同；遺失的更新量每次都不一樣。
//
// 同一份原始碼改用 -O2 編譯，連續 3 次的結果：
//   實際結果: 200000
//   實際結果: 200000
//   實際結果: 200000
//   → 看起來「正確」，但這不代表 race 被修好了。編譯器在「程式無 data race」
//     的前提下，把 100000 次遞增合法地折疊成一次加法，race 視窗因而縮到量不到。
//     換平台、換優化等級、或改成編譯器折不掉的寫法就會復發。
//     這正是為什麼驗證 race 要靠 ThreadSanitizer，而不是靠跑幾次看答案。
//
// 程式一律正常結束（exit=0）：data race 是 UB，但在本機這次並未表現為
// crash 或中止，而是表現為「數值錯誤」。UB 不保證會有明顯症狀，
// 這也是它比 crash 更危險的原因。
