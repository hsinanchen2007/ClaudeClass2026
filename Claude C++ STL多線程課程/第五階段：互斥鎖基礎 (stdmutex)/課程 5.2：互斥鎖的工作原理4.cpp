//### 七、作業系統層面的實現//

//#### Linux：Futex（Fast Userspace Mutex）//

//```
//┌─────────────────────────────────────────────────────────────┐
//│                     Linux Futex                             │
//├─────────────────────────────────────────────────────────────┤
//│                                                             │
//│  設計理念：「無競爭時不需要系統呼叫」                         │
//│                                                             │
//│  ┌─────────────────────────────────────────────────────┐    │
//│  │  無競爭情況（fast path）                             │    │
//│  │  ───────────────────────                             │    │
//│  │  1. 使用原子操作嘗試獲取鎖                           │    │
//│  │  2. 成功 → 直接進入臨界區段（無系統呼叫）            │    │
//│  │  3. 釋放時也只需原子操作                             │    │
//│  └─────────────────────────────────────────────────────┘    │
//│                                                             │
//│  ┌─────────────────────────────────────────────────────┐    │
//│  │  有競爭情況（slow path）                             │    │
//│  │  ───────────────────────                             │    │
//│  │  1. 原子操作失敗                                     │    │
//│  │  2. 呼叫 futex(FUTEX_WAIT) 系統呼叫                  │    │
//│  │  3. 核心將執行緒加入等待佇列                          │    │
//│  │  4. 執行緒睡眠                                       │    │
//│  │  5. 鎖釋放時，呼叫 futex(FUTEX_WAKE) 喚醒等待者      │    │
//│  └─────────────────────────────────────────────────────┘    │
//│                                                             │
//└─────────────────────────────────────────────────────────────┘
//```//

