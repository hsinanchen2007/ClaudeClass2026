//### 二、原子操作：互斥鎖的基石//

//互斥鎖依賴 CPU 提供的**原子指令**來確保狀態改變不會被中斷。//

//#### 什麼是原子操作？//

//```
//┌─────────────────────────────────────────────────────────────┐
//│                      原子操作                                │
//├─────────────────────────────────────────────────────────────┤
//│                                                             │
//│  原子（Atomic）= 不可分割                                    │
//│                                                             │
//│  非原子操作（如 counter++）：                                │
//│                                                             │
//│      ┌─────┐    ┌─────┐    ┌─────┐                         │
//│      │ 讀取 │ → │ +1  │ → │ 寫回 │   ← 三個步驟，可被中斷    │
//│      └─────┘    └─────┘    └─────┘                         │
//│                                                             │
//│  原子操作：                                                  │
//│                                                             │
//│      ┌─────────────────────┐                                │
//│      │   讀取 + 修改 + 寫回  │   ← 一個不可分割的操作        │
//│      └─────────────────────┘                                │
//│                                                             │
//└─────────────────────────────────────────────────────────────┘
//```//

//#### CPU 層級的原子指令//

//現代 CPU 提供特殊指令來實現原子操作：//

//| 指令 | 架構 | 功能 |
//|------|------|------|
//| `LOCK CMPXCHG` | x86/x64 | Compare-And-Swap (CAS) |
//| `LOCK XCHG` | x86/x64 | 原子交換 |
//| `LDREX/STREX` | ARM (舊) | Load-Exclusive / Store-Exclusive |
//| `LDXR/STXR` | ARM (新) | 原子載入/儲存 |//

