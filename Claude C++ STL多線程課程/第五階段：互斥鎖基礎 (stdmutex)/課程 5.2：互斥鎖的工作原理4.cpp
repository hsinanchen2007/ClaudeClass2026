// *         }
// *     }
// * }
// * 
// * void unlock() {
// *     // 如果 state 從 1 變成 0，無等待者
// *     if (state.fetch_sub(1) == 1) {
// *         return;  // 無需喚醒
// *     }
// *     
// *     // 有等待者，需要喚醒
// *     state.store(0);
// *     futex_wake(&state, 1);  // 喚醒一個等待者
// * }
// *///

//int main() {
//    std::cout << "Futex 是 Linux 實現高效互斥鎖的機制" << std::endl;
