//### 五、自旋鎖 vs 睡眠鎖//

//```
//┌─────────────────────────────────────────────────────────────┐
//│                自旋鎖 vs 睡眠鎖                              │
//├─────────────────────────────────────────────────────────────┤
//│                                                             │
//│  自旋鎖（Spinlock）                                         │
//│  ─────────────────                                          │
//│  • 等待時不斷循環檢查鎖狀態                                  │
//│  • 持續佔用 CPU                                             │
//│  • 適合：鎖持有時間極短的情況                                │
//│                                                             │
//│    while (locked) {                                         │
//│        // 忙等待，消耗 CPU                                   │
//│    }                                                        │
//│                                                             │
//│  ─────────────────────────────────────────────────────────  │
//│                                                             │
//│  睡眠鎖（Sleeping Lock / Blocking Lock）                    │
//│  ───────────────────────────────────────                    │
//│  • 等待時讓出 CPU，進入睡眠狀態                              │
//│  • 需要作業系統介入（系統呼叫）                              │
//│  • 適合：鎖持有時間較長的情況                                │
//│                                                             │
//│    if (locked) {                                            │
//│        加入等待佇列;                                         │
//│        睡眠;  // 讓出 CPU                                   │
//│    }                                                        │
//│                                                             │
//└─────────────────────────────────────────────────────────────┘
//```//

//#### 比較表//

//| 特性 | 自旋鎖 | 睡眠鎖 |
//|------|--------|--------|
//| 等待方式 | 忙等待（busy-wait） | 睡眠（block） |
//| CPU 消耗 | 高（持續佔用） | 低（讓出 CPU） |
//| 上下文切換 | 無 | 有（開銷較大） |
//| 適用場景 | 臨界區段極短 | 臨界區段較長 |
//| 適用環境 | 多核心系統 | 任何環境 |//

