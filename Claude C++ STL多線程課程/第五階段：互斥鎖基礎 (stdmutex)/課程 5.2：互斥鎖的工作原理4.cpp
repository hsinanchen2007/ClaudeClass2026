///*
// * 這是 Futex 的概念說明，不是實際可編譯的程式碼
// * 
// * Futex 的核心思想：
// * 
// * struct futex_mutex {
// *     atomic<int> state;  // 0 = unlocked, 1 = locked (無等待者), 2 = locked (有等待者)
// * };
// * 
// * void lock() {
// *     int expected = 0;
// *     
// *     // Fast path：嘗試 0 → 1（無競爭）
// *     if (state.compare_exchange_strong(expected, 1)) {
// *         return;  // 成功獲取，無需系統呼叫
// *     }
// *     
// *     // Slow path：有競爭
// *     while (true) {
