//### 六、std::mutex 的實際實現//

//`std::mutex` 通常採用**混合策略**：先自旋一小段時間，若仍無法獲取鎖，再進入睡眠。//

//```
//┌─────────────────────────────────────────────────────────────┐
//│              std::mutex 的混合策略                           │
//├─────────────────────────────────────────────────────────────┤
//│                                                             │
//│                    lock() 被呼叫                            │
//│                         │                                   │
//│                         ▼                                   │
//│                  ┌─────────────┐                            │
//│                  │ 嘗試獲取鎖  │                            │
//│                  └──────┬──────┘                            │
//│                         │                                   │
//│              ┌──────────┴──────────┐                        │
//│              │                     │                        │
//│         成功獲取                失敗                         │
//│              │                     │                        │
//│              ▼                     ▼                        │
//│         進入臨界區段         ┌───────────┐                   │
//│                             │ 自旋等待   │ ← 短暫自旋        │
//│                             │ (數十到    │   （節省系統呼叫） │
//│                             │  數百次)   │                   │
//│                             └─────┬─────┘                   │
//│                                   │                         │
//│                        ┌──────────┴──────────┐              │
