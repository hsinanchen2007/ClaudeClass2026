// =============================================================================
//  課程 5.1：std::mutex 基本操作 (1)  —  沒有鎖的共享計數器：data race 的原型
// =============================================================================
//
// 【主題資訊 Information】
//   本檔沒有用到 <mutex>，它是「反面教材」：示範不加鎖會發生什麼事。
//   標頭檔：<thread>（std::thread）、<iostream>
//   標準版本：C++11 起（std::thread、記憶體模型）
//   共享物件：int counter（全域，兩個執行緒同時 ++）
//   結論先講：這是 **data race → 未定義行為 (UB)**，不是「少加了幾次」而已。
//
// 【詳細解釋 Explanation】
//
// 【1. ++counter 為什麼不是一個動作】
//   C++ 原始碼寫成一個運算子，但它在機器層面是 read-modify-write 三步：
//       mov  eax, DWORD PTR counter[rip]   ; ① 讀進暫存器
//       add  eax, 1                        ; ② 加一
//       mov  DWORD PTR counter[rip], eax   ; ③ 寫回記憶體
//   兩個執行緒可以在 ① 和 ③ 之間互相插隊：
//       T1 讀到 5 ─┐
//       T2 讀到 5 ─┤   兩邊都算出 6
//       T1 寫回 6 ─┤
//       T2 寫回 6 ─┘   結果 6，不是 7 → 這次遞增「蒸發」了
//   本機 -O0 實測跑三次得到 111658 / 100000 / 100000（每次都不同），
//   正好對應「一半左右的遞增被覆蓋掉」。
//
// 【2. data race 的正式定義（要背起來）】
//   兩個執行緒存取同一記憶體位置、其中至少一方是寫入、
//   且兩者之間沒有任何 happens-before 關係 → 就是 data race。
//   標準規定：程式一旦含有 data race，**整個程式的行為未定義**。
//   注意「未定義」的力道：不是「counter 會少一點」，而是編譯器與 CPU
//   對這段程式的任何假設都不再受約束。
//
// 【3. data race ≠ race condition（面試最常混用的兩個詞）】
//   ┌────────────────┬──────────────────────────┬──────────────────────┐
//   │                │ data race                │ race condition       │
//   ├────────────────┼──────────────────────────┼──────────────────────┤
//   │ 定義           │ 無同步的並行存取，        │ 結果取決於時序        │
//   │                │ 至少一方為寫              │ （如 check-then-act） │
//   │ 是否 UB        │ 是，未定義行為            │ 否，行為都有定義      │
//   │ 加了鎖會消失嗎 │ 會                        │ 不一定！              │
//   └────────────────┴──────────────────────────┴──────────────────────┘
//   本檔是 data race。反例：「先 if (balance >= amount) 再 balance -= amount」
//   即使每一行都上鎖、沒有任何 UB，只要檢查與扣款不在同一個臨界區段，
//   仍然是 race condition（會超額扣款）。兩者要分開處理，不可混為一談。
//
// 【概念補充 Concept Deep Dive】
//   ★ 本機實測：同一支程式，-O0 會出錯，-O2 反而「答對」★
//     $ g++ -std=c++17 -pthread ...   → 實際: 111658 / 100000 / 100000
//     $ g++ -O2 -std=c++17 -pthread ... → 實際: 200000 / 200000 / 200000
//   為什麼？看 -O2 產生的組譯（本機 g++ 15.2 實際輸出）：
//       _Z9incrementv:
//           addl    $100000, counter(%rip)
//           ret
//   GCC 發現迴圈內沒有任何同步，於是「合法地」假設沒有別人會碰 counter，
//   把十萬次迴圈整個摺疊成一條指令。競爭視窗從十萬次縮成一次，
//   兩個執行緒幾乎不可能剛好撞在同一條指令上 → 看起來完全正確。
//
//   這正是 data race 最危險的地方：**開了最佳化反而看不出錯**。
//   你在 -O2 的 CI 上跑一萬次都綠燈，上線後換一台 CPU、換一版編譯器、
//   或只是旁邊多了一個執行緒，它就開始掉數字。
//   「測起來是對的」從來不是 data race 的免罪金牌。
//
// 【注意事項 Pay Attention】
//   1. 上面兩組數字是**本平台某次實際執行的結果，不是標準保證**。
//      UB 沒有「正確答案」，換編譯器／最佳化等級／CPU 都可能不同。
//   2. 不可以說「無鎖遞增一定會少算」。它可能少算、可能剛好正確、
//      也可能出現你完全沒預期的行為 —— 這才是 UB 的定義。
//   3. 不要用 volatile 解決這個問題。volatile 只禁止編譯器最佳化掉存取，
//      **不提供原子性、也不提供跨執行緒的記憶體順序**，data race 依然成立。
//   4. 正確解法有兩種，下一檔（5.1-2）用 std::mutex，
//      更輕量的是 std::atomic<int>（第三階段主題）。
//   5. 想親眼確認這是 data race，可用 ThreadSanitizer：
//        g++ -std=c++17 -fsanitize=thread -g -pthread <本檔> -o tsan && ./tsan
//      它會直接報 "WARNING: ThreadSanitizer: data race"。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】data race 與非原子遞增
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 兩個執行緒各對同一個 int 做十萬次 ++，最後結果是多少？
//     答：不知道，而且這題本身就是陷阱 —— 這是 data race，屬未定義行為，
//         標準不保證任何結果。實務上常見「小於 200000」，
//         但本機 -O2 實測是 200000（編譯器把迴圈摺成一條 addl 指令）。
//         正確答法是先指出 UB，再說明為什麼實測值會飄。
//     追問：那為什麼開了 -O2 反而「對」？
//         → 因為編譯器合法地假設無人競爭，把十萬次寫入縮成一次，
//           競爭視窗變得極小，不是因為程式變正確了。
//
// 🔥 Q2. ++counter 是原子操作嗎？x86 上呢？
//     答：不是。它是 load / add / store 三步。
//         即使在 x86 上單一的 add [mem], 1 指令，只要沒有 lock 前綴，
//         在多核心下仍然不是原子的（其他核心可在讀與寫之間插入）。
//         要原子必須用 std::atomic<int>，或用 mutex 保護。
//
// ⚠️ 陷阱. 「加上 volatile int counter 就安全了吧？」
//     答：不安全，依然是 data race、依然是 UB。
//     為什麼會錯：多數人把 volatile 當成「跨執行緒關鍵字」，
//         但 volatile 在 C++ 只保證「不要把這個存取最佳化掉」，
//         它既不保證讀-改-寫的原子性，也不建立任何 happens-before 關係。
//         （Java 的 volatile 有記憶體語意，C++ 的沒有 —— 這是最常見的混淆來源。）
//
// ⚠️ 陷阱. 「我跑了一萬次都是 200000，所以沒問題。」
//     答：測試通過不能證明沒有 data race，只能證明這次沒被你抓到。
//     為什麼會錯：把 UB 當成「機率性的錯誤結果」，
//         但 UB 是「編譯器可以基於『不會發生』來重寫你的程式」。
//         要證明沒有 data race 只能靠工具（TSan）或靠設計（鎖／atomic）。
// ═══════════════════════════════════════════════════════════════════════════

// 檔案：lesson_5_1_no_mutex.cpp
// 說明：沒有互斥鎖保護的計數器（data race，刻意保留的錯誤示範，請勿修正）

#include <iostream>
#include <thread>

int counter = 0;  // 共享資源

void increment() {
    for (int i = 0; i < 100000; ++i) {
        ++counter;  // 危險！非原子操作
    }
}

int main() {
    std::cout << "=== 無鎖遞增（data race，結果無保證）===" << std::endl;

    std::thread t1(increment);
    std::thread t2(increment);

    t1.join();
    t2.join();

    std::cout << "預期: 200000" << std::endl;
    std::cout << "實際: " << counter << std::endl;  // 無保證：可能少、可能剛好對

    if (counter != 200000) {
        std::cout << "→ 遺失 " << (200000 - counter)
                  << " 次遞增（被覆蓋掉的 read-modify-write）" << std::endl;
    } else {
        std::cout << "→ 這次剛好正確，但這不代表程式正確（見檔頭 -O2 說明）"
                  << std::endl;
    }

    return 0;
}

