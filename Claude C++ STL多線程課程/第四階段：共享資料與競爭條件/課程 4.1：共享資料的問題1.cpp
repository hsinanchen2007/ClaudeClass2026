// =============================================================================
//  課程 4.1：共享資料的問題1.cpp  —  為什麼 ++counter 不是一個動作
// =============================================================================
//
// 【主題資訊 Information】
//   主題      : 無同步的共享計數器 → data race → 未定義行為 (UB)
//   標準依據  : [intro.races]（C++17 N4659）
//               「Two expression evaluations conflict if one of them modifies
//                 a memory location and the other one reads or modifies the
//                 same memory location.」
//               「...contains a data race if it contains two potentially
//                 concurrent conflicting actions, at least one of which is not
//                 atomic, and neither happens before the other.
//                 Any such data race results in undefined behavior.」
//   標頭檔    : <iostream> <thread> <vector>（正解對照另需 <atomic>）
//   標準版本  : C++11 引入執行緒與記憶體模型；本檔以 -std=c++17 編譯
//   工作量    : 10 執行緒 × 100000 次遞增，期望值 1000000
//   本檔性質  : ⚠️ 這是「刻意保留」的 genuine UB 示範，請勿把它改成正確版。
//               它的教學價值就在於「錯得出來」。
//
// -----------------------------------------------------------------------------
// 【詳細解釋 Explanation】
//
// 【1. ++counter 看起來是一個動作，實際上是三個】
//   C++ 原始碼的「一行」和 CPU 的「一個指令」是兩回事。counter 住在記憶體，
//   而算術運算發生在暫存器，所以 ++counter 必須先把值搬進來、加完、再搬回去：
//
//       (1) load  : 從記憶體讀 counter 到暫存器
//       (2) add   : 在暫存器中 +1
//       (3) store : 把暫存器的值寫回記憶體
//
//   這個「讀-改-寫」(read-modify-write, RMW) 三步之間，作業系統隨時可能把
//   這個執行緒換下 CPU，另一個執行緒就插進來了。
//
//   本機（GCC 15.2.0 / x86-64）以 -O0 編譯，這三步在組譯碼中一字不差地出現：
//
//       mov  eax, DWORD PTR counter[rip]   ← (1) load
//       add  eax, 1                        ← (2) add
//       mov  DWORD PTR counter[rip], eax   ← (3) store
//
//   注意這三個指令「沒有 lock 前綴」。x86 的 `add [mem], 1` 若不加 lock，
//   即使寫成單一指令也不是原子的（多核心下其他 core 仍可能插入）。
//
// 【2. 交錯執行如何弄丟一次遞增 (lost update)】
//
//     時間   執行緒A            執行緒B            記憶體中的 counter
//     ─────────────────────────────────────────────────────────────
//      1     load  → 得到 0                              0
//      2                        load  → 得到 0           0
//      3     add   → 暫存器 1                            0
//      4                        add   → 暫存器 1         0
//      5     store 1                                     1
//      6                        store 1                  1
//     ─────────────────────────────────────────────────────────────
//     兩個執行緒各做了一次 ++，但 counter 只前進了 1 —— 少算一次。
//
//   B 在步驟 2 讀到的 0 在步驟 5 之後就「過期」了，但 B 不知道，仍然把基於
//   過期值算出的 1 寫回去，直接蓋掉 A 的成果。這叫 lost update。
//   丟失的次數沒有上限：本機 -O0 實測 10 執行緒 × 100000 次的結果落在
//   約 13 萬 ~ 34 萬之間（每次執行都不同），也就是最多丟掉了 87% 的遞增。
//
// 【3. 這不只是「算錯」，而是未定義行為】
//   很多人以為 data race 的後果就是「數字不準」。不是。標準說的是
//   undefined behavior —— 整個程式的行為都不再有任何保證，
//   編譯器可以假設「這件事不會發生」而做出各種轉換。下面這點最關鍵。
//
// 【4. 為什麼「我測了 200 次都對」證明不了任何事（本檔實測）】
//   同一份原始碼，只是換個最佳化等級，行為天差地遠：
//
//     -O0  →  實際: 119722 / 144580 / 147204 …  每次都不同、錯得很明顯
//     -O2  →  實際: 1000000                     連跑 200 次，200 次全對！
//
//   -O2 為什麼「看起來對了」？因為編譯器把整個 100000 次的迴圈，
//   直接折疊成一道指令（本機實測的 -O2 組譯碼）：
//
//       add  DWORD PTR counter[rip], 100000    ← 整個迴圈只剩這一行
//
//   編譯器有權這樣做，正是「因為 data race 是 UB」：標準允許它假設
//   沒有別的執行緒會碰 counter，於是把 100000 次 RMW 合併成 1 次。
//   結果是競爭視窗從 100000 次縮到 1 次，小到 200 次執行都撞不到
//   —— 但它「仍然是 UB」，這道 add 一樣沒有 lock 前綴，一樣不是原子的。
//
//   結論：測試通過不能證明沒有 data race。UB 最惡劣之處在於它可以
//   「剛好正確」，而且往往在你換編譯器、換最佳化等級、換機器、
//   或上線遇到不同排程時才第一次爆炸。
//
//   要證明「有沒有 data race」必須用工具，不能靠肉眼與重跑：
//
//       g++ -std=c++17 -fsanitize=thread -g -O1 -pthread 本檔.cpp -o tsan_demo
//
//   本機用 ThreadSanitizer 實測（連 -O2 那個「200 次全對」的版本也照抓）：
//
//       WARNING: ThreadSanitizer: data race (pid=1525269)
//         Read of size 4 at 0x... by thread T2:
//           #0 increment()
//         Previous write of size 4 at 0x... by thread T1:
//           #0 increment()
//         Location is global 'counter' of size 4 at 0x...
//
// -----------------------------------------------------------------------------
// 【概念補充 Concept Deep Dive】
//
// (A) data race 與 race condition 是兩件事（本階段最重要的觀念）
//     ─────────────────────────────────────────────────────────────
//     data race（資料競爭）—— 是一個「記憶體模型層級」的技術名詞：
//       兩個執行緒潛在並行地存取「同一個記憶體位置」，至少一方是寫入，
//       至少一方不是 atomic，且兩者之間沒有 happens-before 關係。
//       後果：未定義行為。這是本檔示範的東西。
//
//     race condition（競爭條件）—— 是一個「程式邏輯層級」的名詞：
//       執行結果取決於事件發生的先後時序。
//       後果：邏輯錯誤、違反不變量，但「不一定」是 UB。
//
//     兩者是交集關係，不是包含關係：
//       • 有 data race，不見得違反你在意的不變量
//         （例如兩執行緒各自把同一個 flag 寫成 true —— 仍是 UB，
//           但你想要的結果剛好還是 true）。
//       • 沒有 data race，照樣可以有 race condition
//         （全程上鎖的 check-then-act：if (m.count(k)) 之後才 m[k]++，
//           每次存取都在鎖內、零 data race，但兩執行緒交錯後仍算錯）。
//         這種情形見「課程 4.1：共享資料的問題2.cpp」的轉帳範例。
//
//     一句話記法：
//       data race 講的是「記憶體被怎麼碰」，race condition 講的是
//       「事情發生的順序對不對」。修掉 data race（加鎖／改 atomic）
//       不會自動修掉 race condition。
//
// (B) 為什麼 std::atomic<int> 就沒事
//     atomic 的 ++ 會編成帶 lock 前綴的指令（x86 上是 lock add / lock xadd）。
//     lock 前綴讓這個「讀-改-寫」在整個快取一致性協定中不可分割：
//     其他 core 無法在中途觀察或插入。此外 atomic 的存取「依定義」
//     不構成 data race —— 標準對 data race 的定義本來就要求
//     「至少一方不是 atomic」。所以正解版永遠印出 1000000。
//
// (C) 代價：atomic 不是免費的
//     lock 前綴會讓該快取行 (cache line) 在核心之間來回搶奪
//     (cache line ping-pong)。10 個執行緒猛打同一個 atomic 計數器時，
//     效能可能比單執行緒還差 —— 這叫 contention。
//     實務上高頻計數器的正解是「每執行緒各自累加自己的區域變數，
//     最後才合併一次」（sharding / thread-local accumulation），
//     見「課程 4.1：共享資料的問題3.cpp」的統計聚合範例。
//
// -----------------------------------------------------------------------------
// 【注意事項 Pay Attention】
//
// 1. UB 不等於「一定會出錯」。本檔 -O2 版連跑 200 次全部印出 1000000，
//    這是「本平台、本編譯器、本次測試」的實作結果，不是標準保證，
//    更不代表程式正確。換台機器、換個 GCC 版本就可能完全不同。
// 2. 本檔的輸出是非決定性的：「實際:」那一行每次執行都不同，
//    不要把任何特定數字當成正確答案或迴歸測試的期望值。
// 3. volatile 不能用來修 data race。volatile 只禁止編譯器最佳化掉存取，
//    它不提供原子性、也不提供跨執行緒的順序保證。
//    在 C++ 中處理執行緒間共享請用 std::atomic 或 mutex，不要用 volatile。
// 4. 「int 的讀寫在我的 CPU 上是原子的，所以沒關係」是錯的。
//    就算硬體不會撕裂 (tearing)，語言層的 UB 依然成立，
//    編譯器仍可依「無 data race」的假設做重排與折疊（第 4 點已實測）。
// 5. 加了 -fsanitize=thread 後程式會變慢約 5~15 倍且吃更多記憶體，
//    這是偵錯／CI 用的組建，不要拿去上線。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】共享資料與 data race
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 10 個執行緒各對同一個 int 做 100000 次 ++，最後不是 1000000。
//        請說明為什麼，並說出你會怎麼修。
//     答：++counter 是 load / add / store 三步的 read-modify-write，
//         不是原子操作。兩執行緒可能讀到同一個舊值、各自 +1、再互相覆蓋，
//         造成 lost update。修法：改用 std::atomic<int> 並用 fetch_add，
//         或用 mutex 保護；高頻情境則改成每執行緒本地累加、最後合併。
//     追問：那把它宣告成 volatile int 可不可以？
//         → 不行。volatile 只保證不被最佳化掉，不提供原子性也不提供
//           跨執行緒順序保證，data race 依然成立、依然是 UB。
//
// 🔥 Q2. 請精確定義 data race，並說出它和 race condition 的差別。
//     答：data race 是兩個潛在並行的衝突存取（同一記憶體位置、至少一方寫入、
//         至少一方非 atomic、彼此無 happens-before），後果是未定義行為。
//         race condition 是「結果取決於時序」的邏輯錯誤，不一定是 UB。
//         兩者是交集：有鎖保護、完全沒有 data race 的 check-then-act
//         仍然可能有 race condition。
//     追問：舉一個「沒有 data race 但有 race condition」的例子。
//         → 全程持鎖的 if (balance >= amount) 後才扣款，
//           兩個轉帳交錯檢查後才各自扣款，餘額可能變負數。
//
// ⚠️ 陷阱. 我把最佳化開到 -O2，跑 200 次結果都正好是 1000000，
//         是不是代表這段程式其實沒問題？
//     答：不代表。本機實測 -O2 下 GCC 把整個迴圈折疊成單一
//         `add DWORD PTR counter[rip], 100000`，競爭視窗從 100000 次
//         縮到 1 次，所以 200 次都撞不到。但那道 add 沒有 lock 前綴、
//         仍非原子，data race 與 UB 都還在，ThreadSanitizer 一跑就抓到。
//     為什麼會錯：多數人腦中的模型是「有 bug 就會測得出來」。
//         但 UB 的定義是「標準不保證任何行為」，其中完全包含
//         「剛好給出你想要的答案」。而且編譯器能做這種折疊，
//         正是因為它被允許假設 data race 不存在 —— 你等於是
//         拿「UB 的後果」去證明「沒有 UB」，循環論證。
//         正確做法是用 ThreadSanitizer 之類的工具做因果分析，
//         而不是靠重跑次數。
//
// ⚠️ 陷阱2. 「x86 是強記憶體模型 (TSO)，int 的讀寫本來就原子，
//           所以這段在 x86 上是安全的。」
//     答：錯。硬體不撕裂只解決了「值會不會壞掉」，沒解決
//         「read-modify-write 中間會不會被插入」（本檔就是被插入）。
//         而且語言層的 UB 與硬體無關 —— 編譯器在 UB 前提下做的重排、
//         折疊、暫存器快取，在任何架構上都可能咬你。
//     為什麼會錯：把「硬體記憶體模型」和「語言記憶體模型」混為一談。
//         C++ 的保證來自 C++ 標準，不是來自你的 CPU。
// ═══════════════════════════════════════════════════════════════════════════

#include <atomic>
#include <iostream>
#include <thread>
#include <vector>

int counter = 0;

void increment() {
    for (int i = 0; i < 100000; ++i) {
        ++counter;
    }
}

int main() {
    std::vector<std::thread> threads;
    
    for (int i = 0; i < 10; ++i) {
        threads.emplace_back(increment);
    }
    
    for (auto& t : threads) {
        t.join();
    }
    
    std::cout << "預期: 1000000" << std::endl;
    std::cout << "實際: " << counter << std::endl;
    
    return 0;
}

