// 檢測: g++ -std=c++17 -fsanitize=thread -g -O1 -pthread 本檔.cpp -o tsan_demo

// 註 1:【本檔是 data race,屬未定義行為(UB),沒有「預期輸出」可言。】
//      下方數字是本機某一次實測,不是保證,也不該被當成驗收標準。
//
// 註 2:本機實測(g++ 15.2.0 / x86-64 / 10 條執行緒 × 100000 次):
//        連跑 40 次,得到 40 個【互不相同】的數值 ——
//        沒有任何兩次跑出一樣的結果。
//        觀察到的範圍約在 135185 ~ 326597 之間,
//        40 次裡【沒有任何一次】等於預期的 1000000。
//      請注意這個範圍本身也不是保證:換 CPU 核心數、換最佳化等級、
//      換編譯器,甚至只是機器負載不同,數值都會落在別的區間。
//
// 註 3:為什麼會少這麼多?因為 ++counter 是 read-modify-write 三步驟,
//      不是單一不可分割的動作。當 10 條執行緒同時做,
//      大量的「讀到同一個舊值 → 各自加一 → 寫回同一個新值」互相覆蓋,
//      每一次覆蓋就吃掉一次遞增。丟失的比例取決於執行緒實際的重疊程度。
//
// 註 4:【不要說「一定會少」或「一定不等於 1000000」。】
//      在核心數少、或執行緒被排成幾乎不重疊的機器上,
//      跑出剛好 1000000 是完全可能的 —— 那也不代表程式正確,
//      只代表這次沒被抓包。UB 的危險正在於它可以「看起來正常」。
//
// 註 5:對照組是 5.1(裸 mutex)與 5.5(封裝成執行緒安全類別),
//      那兩支連跑 20~25 次都是位元組完全相同的輸出。
//      「輸出是否可重現」本身就是有沒有正確同步最直接的訊號。

// === 預期輸出 ===
// （UB：實際值每次執行都不同，以下僅為某一次實測）
// 預期: 1000000
// 實際: 190330
