// =============================================================================
//  課程 4.3：臨界區段概念1.cpp  —  哪幾行才是臨界區段？
// =============================================================================
//
// 【本檔性質】原始版本刻意「不加鎖」，用來標示哪些行【屬於】臨界區段。
//   其中 `sharedData = localVar;` 與 `int temp = sharedData;` 兩行
//   在兩條執行緒下沒有任何同步 → genuine data race → 【未定義行為 (UB)】。
//   本檔保留該示範以維持教學對照，並在後面補上加鎖後的正確版本。
//
// 【主題資訊 Information】
//   主題：    臨界區段 (critical section) 的判定 —— 什麼算、什麼不算
//   定義：    存取「共享且可能被修改」之資源的程式碼區段
//   標準版本：std::thread 為 C++11
//   標頭檔：  <thread>、<iostream>
//   判定口訣：① 這份資料被一條以上的執行緒看得到嗎？
//             ② 其中有沒有任何一方會寫入？
//             兩個都「是」→ 這段程式碼就是臨界區段，必須保護。
//
// 【詳細解釋 Explanation】
//
// 【1. 逐行判定 worker() 裡的每一行】
//   void worker() {
//       int localVar = 0;        // ✗ 不是。localVar 在每條執行緒的堆疊上，
//                                //    兩條執行緒各有一份，位址不同，不可能衝突。
//       localVar = 42;           // ✗ 不是。同上，純區域操作。
//       sharedData = localVar;   // ✓ 是。寫入全域變數 → 兩條執行緒同時寫。
//       int temp = sharedData;   // ✓ 是。讀取全域變數，而且另一方正在寫。
//       std::cout << temp;       // △ 視情況。cout 是共享資源，但情況特殊，見【4】。
//   }
//   → 關鍵洞見：臨界區段不是「函式」的屬性，而是「某幾行」的屬性。
//     同一個函式裡，大部分程式碼可能都不需要保護。
//
// 【2. 為什麼區域變數天生免疫】
//   每條執行緒都有自己獨立的堆疊（本機 Linux 預設每執行緒 8 MB，
//   可用 `ulimit -s` 查看）。`int localVar` 配置在呼叫端的堆疊框架上，
//   兩條執行緒執行同一個函式時，兩份 localVar 位於完全不同的位址，
//   彼此看不見對方 → 不存在「共享」，自然不存在競爭。
//   這也是並行程式設計最重要的效能原則：
//   【能放區域變數就別放共享變數】。不需要同步的資料是最快的資料。
//
// 【3. 「讀取」為什麼也算臨界區段】
//   直覺會認為讀不會弄壞東西。但 data race 的定義是
//   「至少一方寫入」，不是「雙方都寫入」。
//   只要同時存在一個寫入者，所有讀取者就都在臨界區段內，都必須加鎖。
//   反過來說，如果整個生命週期都沒有人寫（純唯讀常數、
//   初始化完成後就不再改的設定），那麼讀取【不需要】任何保護 ——
//   這正是 immutable 設計在並行系統中特別受歡迎的原因。
//
// 【4. std::cout 的特殊地位】
//   std::cout 確實是共享資源，但它與 sharedData 有本質差異：
//     * C++11 起，標準要求對標準串流物件的並行「格式化輸出」
//       不會造成 data race（[iostream.objects] 的執行緒安全保證）。
//       libstdc++ 的實作在 streambuf 層有內部鎖。
//     * 因此多執行緒同時 `std::cout << x` 【不是 UB】，
//       但【輸出內容會交錯】—— 因為每個 `<<` 是獨立的一次操作，
//       兩個 `<<` 之間別的執行緒可以插進來。
//   → 結論：cout 需要保護的理由是「輸出可讀性」而非「避免 UB」。
//     若要讓一整行不被打斷，得自己加鎖，或先組成完整字串再一次輸出。
//     （課程 5.5 的 ThreadSafeLogger 就是這個做法。）
//
// 【5. 臨界區段的四個設計原則】
//   ① 最小化：只把「必須」的那幾行放進去，計算留在鎖外。
//   ② 快進快出：不要在鎖內做 I/O、睡眠、網路呼叫。
//   ③ 避免巢狀：巢狀加鎖是死結的主要來源；必要時用 std::scoped_lock 一次鎖多把。
//   ④ 不要在鎖內呼叫外部回呼：你不知道使用者的回呼會不會反過來取用同一把鎖。
//   （②③④ 的量化與實例見課程 5.6。）
//
// 【概念補充 Concept Deep Dive】
//
// (A) 「共享」的真正判準是位址，不是關鍵字
//   判斷一份資料是否共享，看的是「有沒有兩條執行緒能取得同一個位址」：
//     * 全域 / static / thread-local 以外的靜態儲存期物件 → 共享
//     * 堆積上的物件，只要指標被傳給多條執行緒       → 共享
//     * 類別成員，只要同一個物件被多條執行緒使用     → 共享
//     * 區域變數                                     → 不共享
//     * 但區域變數若被 lambda 以【引用】捕捉並傳給別的執行緒 → 立刻變成共享！
//   最後一條是初學者最常踩的坑：`[&]` 捕捉讓堆疊上的變數瞬間變成共享資料，
//   而且原函式一旦回傳，那個變數還會變成懸空引用。
//
// (B) 為什麼臨界區段越短越好（不只是效能）
//   * 效能面：臨界區段是序列化的，依 Amdahl 定律，序列比例決定了
//     多核心能帶來的加速上限。鎖內的時間越長，加再多核心也沒用。
//   * 正確性面：臨界區段（= 不變量的破壞期）越長，被撞見的機率越高，
//     死結與優先權反轉的風險也越高。
//   → 短臨界區段同時買到效能與正確性，這是少見的雙贏。
//
// (C) 為什麼不能用「一個函式一把鎖」這種粗暴規則
//   會把大量不需要保護的計算也序列化，白白損失並行度；
//   而且容易演變成巢狀加鎖（函式互相呼叫），埋下死結。
//   正確做法是逐行判定，只保護真正共享且會被修改的存取。
//
// 【注意事項 Pay Attention】
// 1. 臨界區段是「某幾行」的屬性，不是「某個函式」的屬性。
// 2. 讀取也算臨界區段 —— 只要同時存在寫入者。
// 3. 區域變數天生安全，但被 `[&]` 引用捕捉傳給別的執行緒後就不再安全。
// 4. std::cout 並行輸出不是 UB（標準有保證），但會交錯；
//    要保持整行完整必須自己加鎖或先組字串。
// 5. 本檔第一段是 UB 示範，不可宣稱「一定印出 4242」或任何固定結果。
//
// 【LeetCode 說明】本檔不附 LeetCode 範例。
//   本檔的主題是「判定哪幾行需要保護」，屬於分析與設計的思考方法，
//   沒有輸入輸出可判題。允許使用的設計題（146/155/705/707/1603）
//   與並行題（1114～1117/1195）都無法誠實對應「臨界區段判定」這件事，
//   硬湊一題只會稀釋重點，故從缺，改以下方的實務範例呈現。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】臨界區段的判定
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 怎麼判斷一行程式碼是不是臨界區段?
//     答：兩個條件同時成立才是：① 這份資料有一條以上的執行緒看得到；
//         ② 其中至少有一方會寫入。
//         全域 / static / 被多執行緒共用的堆積物件與類別成員都符合①；
//         區域變數與 thread_local 不符合①，天生安全。
//     追問：那唯讀的全域常數需要加鎖嗎?
//         → 不需要。沒有任何寫入者就不構成 data race，
//           這也是 immutable / const 設計在並行系統中受歡迎的原因。
//
// 🔥 Q2. 多執行緒同時 std::cout << x 會是未定義行為嗎?
//     答：不會。C++11 起標準保證對標準串流物件的並行格式化輸出不構成 data race
//         （libstdc++ 在 streambuf 層有內部鎖）。
//         但【輸出會交錯】，因為每個 << 是獨立操作，中間可以被插入。
//         需要整行完整就要自己加鎖，或先組成一個字串再一次輸出。
//     追問：那 printf 呢?
//         → POSIX 要求 stdio 函式對 FILE* 的操作是執行緒安全的（內部有鎖），
//           所以單次 printf 通常不會被切斷，這也是它在多執行緒 log 中
//           反而比 cout 的多個 << 更「不容易亂」的原因。
//
// ⚠️ 陷阱. 「這個變數是函式裡的區域變數，所以一定不需要加鎖」——什麼時候會錯?
//     答：當它被 lambda 以引用捕捉 `[&]` 並傳給另一條執行緒時。
//         那一刻它的位址就被兩條執行緒共享了，和全域變數沒有兩樣；
//         若原函式先回傳，還會多一個懸空引用的 UB。
//     為什麼會錯：把「宣告位置」當成安全性的判準。
//         真正的判準是「有沒有第二條執行緒能取得同一個位址」——
//         `[&]` 捕捉、取位址傳出去、放進 std::ref，都會讓區域變數變成共享資料。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <thread>
#include <mutex>
#include <vector>
#include <string>
#include <sstream>

// -----------------------------------------------------------------------------
// 【原始示範】標示哪幾行是臨界區段（刻意不加鎖 → 該兩行構成 data race）
// -----------------------------------------------------------------------------
int sharedData = 0;

void worker() {
    int localVar = 0;        // 不是臨界區段：區域變數

    localVar = 42;           // 不是臨界區段：只存取區域變數

    sharedData = localVar;   // ← 臨界區段：寫入共享資料

    int temp = sharedData;   // ← 臨界區段：讀取共享資料（如有寫入者）

    std::cout << temp;       // 可能是臨界區段：cout 也是共享資源
}

// -----------------------------------------------------------------------------
// 【正確版】只把真正需要保護的兩行放進臨界區段
//   注意計算 (localVar = 42) 仍然留在鎖外 —— 這就是「最小化原則」。
// -----------------------------------------------------------------------------
std::mutex dataMtx;
std::mutex coutMtx;

void safeWorker(int id) {
    int localVar = 0;            // 鎖外：區域變數
    localVar = 42 + id;          // 鎖外：純計算，不碰共享資料

    int temp;
    {
        std::lock_guard<std::mutex> lock(dataMtx);   // ← 臨界區段開始
        sharedData = localVar;
        temp = sharedData;
    }                                                 // ← 臨界區段結束

    std::ostringstream oss;                          // 鎖外：組字串
    oss << "[thread " << id << "] temp = " << temp << "\n";

    {
        std::lock_guard<std::mutex> lock(coutMtx);   // 只保護「輸出」這一下
        std::cout << oss.str();
    }
}

// -----------------------------------------------------------------------------
// 【日常實務範例 1】Web 伺服器的請求處理：一個函式裡只有兩行需要鎖
//   情境：每個請求都要做「解析 → 驗證 → 計算 → 累計統計」。
//         前三步全是區域運算（各執行緒獨立），只有最後的統計是共享的。
//   常見錯誤：直接在函式最上面 lock_guard 一路鎖到 return，
//         把解析與計算也序列化，多核心完全失去意義。
//   正確做法：只鎖最後那一下累計。本例示範兩者的差別。
// -----------------------------------------------------------------------------
struct ServerStats {
    mutable std::mutex mtx;
    long handled = 0;
    long totalBytes = 0;

    void record(long bytes) {
        std::lock_guard<std::mutex> lock(mtx);   // ← 唯一的臨界區段
        ++handled;
        totalBytes += bytes;
    }

    std::pair<long, long> snapshot() const {
        std::lock_guard<std::mutex> lock(mtx);
        return {handled, totalBytes};
    }
};

// 模擬解析與驗證：純區域運算，完全不需要鎖
long parseAndValidate(const std::string& request) {
    long weight = 0;
    for (char ch : request) weight += static_cast<unsigned char>(ch);
    return weight % 1000;
}

void handleRequest(ServerStats& stats, const std::string& request) {
    long bytes = parseAndValidate(request);   // 鎖外：可完全並行
    stats.record(bytes);                      // 鎖內：只有這一行
}

// -----------------------------------------------------------------------------
// 【日常實務範例 2】把「區域變數用 [&] 捕捉」變成共享資料的經典事故
//   這是「區域變數一定安全」這個直覺唯一會失效的地方，
//   本例用【正確】的寫法示範：既然被共享了，就必須當共享資料保護。
// -----------------------------------------------------------------------------
long sumWithCapture(const std::vector<int>& data, int numThreads) {
    long total = 0;                  // ← 區域變數，但下面被 [&] 捕捉 → 變成共享！
    std::mutex totalMtx;             // 所以必須保護
    std::vector<std::thread> ths;
    size_t chunk = data.size() / static_cast<size_t>(numThreads);

    for (int i = 0; i < numThreads; ++i) {
        size_t begin = chunk * static_cast<size_t>(i);
        size_t end = (i == numThreads - 1) ? data.size() : begin + chunk;
        ths.emplace_back([&data, &total, &totalMtx, begin, end]() {
            long local = 0;                       // 真正的區域變數，鎖外累加
            for (size_t k = begin; k < end; ++k) local += data[k];
            std::lock_guard<std::mutex> lock(totalMtx);
            total += local;                       // 只有這一行需要保護
        });
    }
    for (auto& t : ths) t.join();    // join 之後才讀 total，安全
    return total;
}

int main() {
    std::cout << "=== 原始示範：標示臨界區段（該兩行是 data race / UB）===\n";
    {
        std::thread t1(worker);
        std::thread t2(worker);
        t1.join();
        t2.join();
        std::cout << "\n（上面那串數字每次執行都可能不同，且不受標準保證）\n";
    }

    std::cout << "\n=== 正確版：只鎖真正需要的兩行 ===\n";
    {
        std::thread t1(safeWorker, 1);
        std::thread t2(safeWorker, 2);
        t1.join();
        t2.join();
        std::cout << "（每行輸出完整不交錯；兩行的先後順序仍取決於排程）\n";
    }

    std::cout << "\n=== 日常實務 1：只鎖統計，不鎖解析與計算 ===\n";
    {
        ServerStats stats;
        std::vector<std::thread> ths;
        for (int i = 0; i < 4; ++i) {
            ths.emplace_back([&stats, i] {
                for (int k = 0; k < 5000; ++k) {
                    handleRequest(stats, "GET /api/v1/items?page=" + std::to_string(i * k));
                }
            });
        }
        for (auto& t : ths) t.join();

        auto snap = stats.snapshot();
        std::cout << "處理請求數: " << snap.first << " (必定為 20000)\n";
        std::cout << "解析與驗證完全在鎖外 → 4 核心可真正並行\n";
        std::cout << "只有 record() 的兩行是臨界區段\n";
    }

    std::cout << "\n=== 日常實務 2：被 [&] 捕捉的區域變數必須當共享資料保護 ===\n";
    {
        std::vector<int> data(100000);
        for (size_t i = 0; i < data.size(); ++i) data[i] = static_cast<int>(i % 100);

        long expected = 0;
        for (int v : data) expected += v;

        long got = sumWithCapture(data, 8);
        std::cout << "單執行緒總和: " << expected << "\n";
        std::cout << "8 執行緒總和: " << got << "\n";
        std::cout << "結果一致: " << (expected == got ? "是" : "否")
                  << " (有鎖保護，必定一致)\n";
    }

    return 0;
}

