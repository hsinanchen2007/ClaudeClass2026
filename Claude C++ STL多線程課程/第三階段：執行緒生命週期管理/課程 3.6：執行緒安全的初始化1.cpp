// =============================================================================
//  課程 3.6：執行緒安全的初始化1.cpp  —  未同步的延遲初始化 = data race（UB）
// =============================================================================
//
// 【本檔是「刻意示範錯誤」的範例】
//   ⚠️ 分類：genuine data race → 未定義行為（Undefined Behavior, UB）
//   本檔的 `if (db == nullptr) db = new Database();` 是**沒有任何同步**的
//   check-then-act。兩條執行緒同時讀寫全域指標 `db`，其中至少一方是寫入
//   → 依 [intro.races]/21 構成 data race → **整個程式的行為未定義**。
//   請**不要**把它「修好」；它的價值就在於呈現錯誤本身。
//   正確作法見同課的 2〜5 號檔（call_once / magic static）。
//
// =============================================================================
//
// 【主題資訊 Information】
//   語法（錯誤示範）：
//       T* p = nullptr;                    // 全域，未受保護
//       if (p == nullptr) p = new T();     // 多執行緒同時進入 → data race
//   標準版本：本檔語法自 C++11 起可編譯（<thread> 是 C++11）
//   標頭檔：<iostream>、<thread>
//   標準依據：
//       [intro.races]/21 —— 兩個動作若存取同一記憶體位置、其中至少一個是
//       修改（modification）、且彼此沒有 happens-before 關係，
//       則該程式含有 data race，**行為未定義**。
//   複雜度：不適用（本檔談的是正確性，不是效能）
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼「先檢查再建立」在多執行緒下會壞掉】
//   單執行緒看，這段程式無懈可擊：沒建過就建，建過就用。
//   但 `if (db == nullptr)` 與 `db = new Database()` 之間存在一個**時間窗**。
//   兩條執行緒可以同時通過檢查（都看到 nullptr），然後**都**去 new：
//
//       執行緒 A                        執行緒 B
//       ────────────────────────────    ────────────────────────────
//       讀 db → nullptr
//                                       讀 db → nullptr      ← 也看到 null
//       db = new Database()   (寫)
//                                       db = new Database()  (寫，覆蓋 A 的)
//       db->query()                     db->query()
//
//   後果有三層，一層比一層嚴重：
//     (a) 建構子跑兩次 —— 若建構子有副作用（開檔、連 DB、註冊 callback、
//         遞增全域計數），副作用就發生了兩次。
//     (b) 記憶體流失 —— A 配置的物件位址被 B 覆蓋掉，再也沒人持有它；
//         main() 末尾的 `delete db` 只釋放最後寫入的那一個。
//     (c) 「單例」的語意徹底崩潰 —— A 和 B 手上其實是**兩個不同物件**。
//         之後任何「大家共用同一份狀態」的假設都不成立。
//
// 【2. 這裡同時存在 data race 與 race condition —— 兩者不是同義詞】
//   這是本課最容易混淆、面試也最愛問的一點：
//
//     • **data race**（資料競爭）＝ 語言層級的定義：未同步的並行存取，
//       且至少一方是寫入 → **UB**。這是「編譯器可以對你為所欲為」的等級。
//       本檔的 `db` 正是如此。
//     • **race condition**（競爭條件）＝ 邏輯層級的問題：結果取決於時序。
//       可以**完全沒有 data race** 卻仍是 race condition
//       （例如兩條執行緒都用 mutex 保護、但「先查餘額再扣款」分兩次上鎖，
//         同步完全正確，邏輯依然錯）。
//
//   本檔兩者都有：底層是 data race（UB），表面症狀是 race condition
//   （初始化幾次取決於時序）。修掉同步可以消滅 data race；
//   但要消滅 race condition，必須讓「檢查 + 建立」成為**單一不可分割**的動作
//   —— 這正是 std::call_once 與 magic static 在做的事。
//
// 【3. 為什麼不可以說「一定會初始化兩次」】
//   本課文件（7 號檔第一節）寫「Database 被初始化了兩次！」——
//   那只是**其中一種**可能的結果，不是保證。本機實測（見檔尾）：
//   同一支執行檔連跑兩批各 200 次，結果是
//   **第一批 22 次只初始化一次 / 178 次兩次；第二批 10 次一次 / 190 次兩次**。
//   注意連「比例」本身都不穩定——這正說明它沒有任何可依賴的行為。
//   因為 UB 沒有「預期輸出」可言，任何「一定會 X」的敘述都是錯的。
//   之所以雙重初始化佔多數，只是因為兩條執行緒幾乎同時啟動、
//   而 `new` + iostream 的耗時遠大於兩次讀取的間隔；
//   換一台機器、換編譯器最佳化等級、加上排程抖動，比例都會變。
//
// 【概念補充 Concept Deep Dive】
//
// (A) UB 不只是「結果不確定」——編譯器有權假設 race 不存在
//   很多人以為 data race 最壞就是「拿到舊值」。實際上更嚴重：
//   C++ 記憶體模型允許編譯器**假設程式無 data race**，並據此最佳化。
//   於是它可以：
//     • 把 `db` 快取進暫存器，迴圈中永遠不重讀（其他執行緒的寫入永遠看不到）；
//     • 重排 `db` 的讀寫順序；
//     • 發明額外的讀取（invented load），讓你在兩次讀之間看到不一致的值。
//   本檔規模小、又有 iostream 這種「編譯器看不穿」的呼叫當屏障，
//   所以症狀溫和；把它放進一個熱迴圈裡，症狀會截然不同。
//
// (B) 「非空但尚未建構完成」的指標
//   `db = new Database()` 在機器層級是三件事：
//     ① 配置記憶體 → ② 在該記憶體上執行建構子 → ③ 把位址寫進 db
//   在沒有同步（也就沒有 happens-before）的情況下，
//   ②③ 的順序對**其他執行緒**而言是不保證的。
//   另一條執行緒可能讀到「非 nullptr」而跳過檢查，卻對一個**建構尚未完成**
//   的物件呼叫 query()。這正是雙重檢查鎖定（DCLP）在 C++11 之前壞掉的根因，
//   詳見 3 號檔與 7 號檔第六節。
//
// (C) 用工具抓，不要用眼睛抓
//   這類 bug 在測試機上常常「跑一萬次都對」，上線才炸。請用 sanitizer：
//       g++ -std=c++17 -fsanitize=thread -O1 -g -pthread 本檔.cpp -o /tmp/tsan
//   ThreadSanitizer 會直接指出 `db` 上的 read/write race 與雙方的堆疊。
//   注意 TSan 是**執行期**偵測，只能報告實際跑到的交錯；沒報不等於沒有。
//
// 【注意事項 Pay Attention】
//   1. UB 沒有「預期輸出」。本檔檔尾附的是**某一次實測**與統計分布，
//      不是保證；換機器、換最佳化等級結果都會變。
//   2. 「只初始化一次」的那 22/200 次**不代表程式是對的**——
//      它依然是 UB，只是這次沒被抓包。這正是 UB 最危險的地方。
//   3. 加 `volatile` 救不了。volatile 只管「不要把存取最佳化掉」，
//      **不建立 happens-before、不保證原子性**，對 data race 完全無效。
//      （這是 Java 的 volatile 語意，C++ 的 volatile 不同。）
//   4. 本檔在 `db` 被寫兩次時會流失一個 Database 物件；
//      用 valgrind 或 -fsanitize=address 可以看到 leak。
//   5. 修法**不是**在外面包一把 mutex 就好（那樣每次呼叫都要上鎖，
//      熱路徑很貴）。正解是 call_once（2、3 號檔）或 magic static（5 號檔），
//      它們在初始化完成後是近乎免費的 fast path。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】未同步的延遲初始化 / data race
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 這段 `if (db == nullptr) db = new Database();` 在多執行緒下有什麼問題？
//     答：它是未同步的 check-then-act。兩條執行緒可能同時通過 nullptr 檢查而
//         各自 new 一次，造成建構子副作用重複、記憶體流失、以及「單例」失效
//         （兩邊拿到不同物件）。更根本地說，`db` 被並行讀寫且無同步，
//         依 [intro.races]/21 這是 data race → 整個程式 UB。
//     追問：那我把它改成 `std::atomic<Database*>` 就好了嗎？
//         （光把型別換成 atomic 只消滅 data race（UB），check-then-act 的
//           race condition 還在——兩條執行緒仍可能都讀到 nullptr 而各建一個。
//           要真正修好，必須讓「檢查＋建立」成為單一不可分割的動作：
//           call_once、magic static，或 compare_exchange + 敗者釋放自己那份。）
//
// 🔥 Q2. data race 和 race condition 差在哪？
//     答：data race 是語言層級的定義——未同步的並行存取且至少一方是寫入，
//         結果是 UB。race condition 是邏輯層級的問題——結果取決於時序。
//         兩者獨立：可以有 data race 而邏輯剛好對；也可以完全沒有 data race
//         （例如全程 mutex 保護）卻仍有 race condition（例如「先查後扣」
//         分兩次上鎖）。本檔兩者兼具。
//     追問：把所有共享變數都改成 atomic，是不是就沒有並行 bug 了？
//         （不是。atomic 只消滅 data race 這個 UB 來源，不會自動讓
//           多步驟操作變成不可分割的交易，race condition 依然可能存在。）
//
// ⚠️ 陷阱. 「我跑了很多次，Database 都只印一次，所以這段沒問題吧？」
//     答：錯。本機實測 200 次中就有 22 次「只初始化一次」——如果你剛好抽到
//         那 22 次，會誤判成正確。但它自始至終都是 UB，只是沒被抓包。
//     為什麼會錯：多數人腦中的模型是「有 bug 就會表現出來」。但 data race 的
//         症狀取決於排程、快取狀態、最佳化等級與硬體記憶體模型，
//         在 x86 這種強記憶體序（TSO）的機器上尤其容易「看起來正常」，
//         換到 ARM/POWER 這種弱記憶體序平台就原形畢露。
//         **測試通過不能證明沒有 data race**；要證明請用 ThreadSanitizer
//         或直接改用有同步保證的寫法。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <thread>

class Database {
public:
    Database() { std::cout << "Database 初始化" << std::endl; }
    void query() { std::cout << "查詢中..." << std::endl; }
};

Database* db = nullptr;

void initAndUse() {
    if (db == nullptr) {           // 執行緒 A 檢查
        db = new Database();       // 執行緒 A 和 B 都可能執行這行！
    }
    db->query();
}

int main() {
    std::thread t1(initAndUse);
    std::thread t2(initAndUse);
    
    t1.join();
    t2.join();
    
    delete db;
    return 0;
}

