// 注意:以下為某一次實際執行的結果。
//   * 行的先後順序、以及「哪一條執行緒拿到第幾次嘗試」每次執行都不同。
//   * 實務段的「實際嘗試連線次數」可能是 1,也可能大於 1 ——
//     因為五條執行緒可能在旗標被設起來之前就同時通過了快速失敗檢查。
//     這是刻意保留的誠實示範:快速失敗能大幅削減重試,但無法消滅
//     啟動瞬間的競爭視窗;要完全杜絕就得改用 mutex 或狀態機。
//   * 不變的是:「初始化成功!」恰好一行,「嘗試 #」恰好三行且編號為 1、2、3。

// === 預期輸出 ===
// === 原始示範:四條執行緒,初始化前兩次失敗 ===
//   嘗試 #1
//   嘗試 #2
//   捕獲例外: 初始化失敗
//   捕獲例外: 初始化失敗
//   嘗試 #3
//   初始化成功!
//   繼續執行
//   繼續執行
//   ── 統計 ──
//   mayFail() 實際被執行 3 次(前 2 次拋例外不算完成,第 3 次才是 effective call)
//
// === 實務:永久性失敗不應該讓每個請求都重試 ===
//     [請求 4] 失敗: 先前已判定為永久性失敗,直接拒絕(未重試)
//     [請求 1] 失敗: API 金鑰格式錯誤(永久性失敗)
//     [請求 3] 失敗: 先前已判定為永久性失敗,直接拒絕(未重試)
//     [請求 5] 失敗: 先前已判定為永久性失敗,直接拒絕(未重試)
//     [請求 2] 失敗: 先前已判定為永久性失敗,直接拒絕(未重試)
//   實際嘗試連線次數 = 1 (5 個請求,但只真的連了 1 次;其餘被快速失敗擋下)
