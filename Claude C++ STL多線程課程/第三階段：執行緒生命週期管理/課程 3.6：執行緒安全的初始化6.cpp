// =============================================================================
//  課程 3.6：執行緒安全的初始化6.cpp  —  call_once 遇到例外:重試語意
// =============================================================================
//
// 【主題資訊 Information】
//   簽名      : void call_once(std::once_flag& flag, Callable&& f, Args&&... args);
//   標準版本  : C++11
//   標頭檔    : <mutex>(本檔另用 <atomic>、<stdexcept>)
//   例外語意  : f 拋出例外 → 例外傳播給「當次呼叫者」,且 flag「不」標記完成
//               → 下一個呼叫 call_once 的執行緒會再執行一次 f
//   關鍵術語  : 標準稱成功完成的那一次為 effective call(有效呼叫)
//
// 【詳細解釋 Explanation】
//
// 【1. 「只執行一次」其實是「只成功一次」】
// call_once 最容易被誤記的地方,就是把它想成「這個函式只會被呼叫一次」。
// 標準真正規定的是:對同一個 once_flag,「恰好一次 effective call」——
// 也就是「恰好成功完成一次」。
//
// 若可呼叫物件拋出例外:
//   (a) 例外會傳播給「這一次」呼叫 call_once 的那個執行緒;
//   (b) once_flag「不會」被標記為已完成;
//   (c) 因此下一個(或下一次)呼叫 call_once 的執行緒會再執行一次。
//
// 這正是本檔的示範:mayFail() 前兩次拋例外,第三次才成功。
// 於是「嘗試 #」出現三次,而「初始化成功!」只出現一次。
//
// 【2. 為什麼標準要這樣設計】
// 如果初始化失敗也把旗標標記成完成,結果會是災難性的:
// 資源永遠處於「沒建立好,但系統認為已經建立好」的狀態,
// 之後每一個使用者都會拿到半成品或空指標,而且再也沒有機會修復。
// 對於「連不上資料庫」「檔案暫時被鎖住」這類暫時性失敗,
// 讓下一個執行緒重試才是唯一合理的行為。
//
// 【3. 但重試語意也有它的風險】
// 這個設計的另一面是:如果失敗是「永久性」的(設定檔根本不存在、
// 密碼就是錯的),那麼每一個進來的執行緒都會重跑一次昂貴的初始化、
// 各自拋一次例外。高流量下這會變成一台「例外產生器」,
// 拖垮效能還洗爆 log。
// 實務上的處理方式:在初始化函式內自行區分「暫時性失敗」與「永久性失敗」,
// 永久性失敗就記錄下來、快速失敗,不要每次都重試(見【實務範例】)。
//
// 【4. 呼叫端一定要處理例外】
// call_once 會把例外原封不動往外丟。若呼叫端沒有 try/catch,
// 例外會離開執行緒函式 —— 而執行緒函式若讓例外逸出,
// 標準規定會呼叫 std::terminate(),整個程式中止。
// 本檔的 worker() 因此用 try/catch 包住,這不是可選的裝飾。
//
// 【概念補充 Concept Deep Dive】
//
// (A) once_flag 的三個狀態
//   要支援重試語意,once_flag 不可能只是一個布林值,至少要能表達:
//     未執行 / 執行中(其他人要等)/ 已完成
//   當執行中的那一次拋出例外,狀態會從「執行中」回到「未執行」,
//   並喚醒正在等待的執行緒,讓其中一個接手重試。
//
// (B) 和 magic static 的對照
//   函式內 static 變數的建構子若拋例外,行為完全一致:
//   該次初始化不算完成,下次進入該宣告時會再試一次。
//   本機實測反組譯可見編譯器產生 __cxa_guard_abort ——
//   它的職責正是「把 guard 還原成未初始化」。
//   兩套機制在例外處理上的設計哲學是一致的。
//
// (C) 計數為什麼是 1、2、3 而不會跳號
//   attempt 是 std::atomic<int>,而且 call_once 保證同一時間只有一個
//   執行緒能執行 mayFail(),所以 ++attempt 是被序列化的,
//   必定依序拿到 1、2、3。但「哪一條執行緒拿到哪一號」則不確定。
//
// 【注意事項 Pay Attention】
// 1. 「只執行一次」的精確說法是「只成功完成一次」;失敗會重試。
// 2. 呼叫端必須處理例外;讓例外逸出執行緒函式會導致 std::terminate()。
// 3. 永久性失敗會讓每個執行緒都重試一次,造成效能與 log 災難 ——
//    需要自行區分暫時性/永久性失敗。
// 4. 若所有執行緒都失敗了,資源就處於「未初始化」狀態,
//    後續任何呼叫仍會嘗試初始化。使用前務必檢查。
// 5. 例外從 call_once 傳出時,已經完成的部分副作用「不會」被回復 ——
//    初始化函式最好寫成要嘛全部成功、要嘛不留痕跡(強例外保證)。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】call_once 的例外與重試語意
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. call_once 的初始化函式拋出例外會怎樣?
//     答：三件事。例外會傳播給當次呼叫 call_once 的執行緒;once_flag
//         不會被標記為完成;因此下一個呼叫的執行緒會再執行一次。
//         標準的精確說法是「恰好一次 effective call(成功完成的呼叫)」,
//         而不是「函式只被呼叫一次」。
//     追問：為什麼不乾脆標記成完成就好?
//         → 那會讓資源永遠停在「沒建好卻被認為建好了」的狀態,
//           之後所有使用者都拿到半成品,而且再也沒有修復機會。
//
// 🔥 Q2. 這個重試語意有什麼風險?實務上怎麼處理?
//     答：如果失敗是永久性的(設定檔不存在、密碼錯誤),每個進來的執行緒
//         都會重跑一次昂貴的初始化並各拋一次例外,變成效能黑洞兼 log 炸彈。
//         做法是在初始化函式內區分暫時性與永久性失敗:永久性失敗就記錄
//         狀態、之後快速失敗,不要每次真的重試。
//     追問：那 magic static 的建構子拋例外呢?
//         → 行為一致,下次進入宣告會再試一次。本機反組譯可看到編譯器
//           產生 __cxa_guard_abort,職責就是把 guard 還原成未初始化。
//
// ⚠️ 陷阱. 「worker() 裡的 try/catch 只是為了讓輸出好看,拿掉也只是
//         少印一行訊息而已。」哪裡錯了?
//     答：拿掉會讓整個程式中止。call_once 把例外原封不動往外丟,
//         若 worker() 不接住,例外就會逸出執行緒的進入點函式 ——
//         標準規定這種情況直接呼叫 std::terminate()。
//         這不是「少印一行」,而是 exit code 134、程式當場死亡。
//     為什麼會錯：用單執行緒的直覺推論,以為沒接住的例外最多就是往上冒
//         到 main 然後結束。但執行緒函式沒有「上一層」可以冒 ——
//         例外無法跨執行緒邊界傳播,標準只好規定直接中止。
//         (想把例外帶回主執行緒,要用 std::promise/std::future 或
//          std::exception_ptr。)
// ═══════════════════════════════════════════════════════════════════════════

#include <atomic>
#include <iostream>
#include <mutex>
#include <stdexcept>
#include <string>
#include <thread>
#include <vector>

// 保護 std::cout,避免多執行緒輸出互相切開
std::mutex g_ioMutex;

void say(const std::string& msg) {
    std::lock_guard<std::mutex> lock(g_ioMutex);
    std::cout << msg << std::endl;
}

// -----------------------------------------------------------------------------
// 原始示範:前兩次失敗,第三次才成功
// -----------------------------------------------------------------------------
std::once_flag flag;
std::atomic<int> attempt{0};

void mayFail() {
    int current = ++attempt;
    say("  嘗試 #" + std::to_string(current));

    if (current < 3) {
        throw std::runtime_error("初始化失敗");
    }

    say("  初始化成功!");
}

void worker() {
    try {
        std::call_once(flag, mayFail);
        say("  繼續執行");
    } catch (const std::exception& e) {
        // 這個 try/catch 是必要的:例外逸出執行緒函式會導致 std::terminate()
        say(std::string("  捕獲例外: ") + e.what());
    }
}

// -----------------------------------------------------------------------------
// 【日常實務範例】區分「暫時性失敗」與「永久性失敗」的延遲初始化
//   情境: 服務啟動時連線授權伺服器。連線逾時屬於暫時性失敗,應該讓下一個
//         請求重試;但「金鑰格式錯誤」是永久性失敗,重試一百萬次也不會好 ——
//         若放任 call_once 的重試語意,每個進來的請求都會重跑一次連線、
//         各拋一次例外,把 log 洗爆也把 CPU 吃光。
//         做法是把永久性失敗記錄在一個原子旗標裡,之後直接快速失敗。
//   為什麼用本主題: 直接對應【詳細解釋 3】,是 call_once 重試語意在
//                   生產環境最重要的一個補強。
// -----------------------------------------------------------------------------
std::once_flag       g_authFlag;
std::atomic<bool>    g_permanentlyFailed{false};
std::atomic<int>     g_connectAttempts{0};
std::string          g_authToken;

// 模擬:金鑰是壞的 → 每次都會失敗,而且是永久性的
void connectAuthServer(const std::string& apiKey) {
    ++g_connectAttempts;
    if (apiKey.rfind("sk-", 0) != 0) {
        g_permanentlyFailed.store(true);          // 記下來,不要再重試
        throw std::invalid_argument("API 金鑰格式錯誤(永久性失敗)");
    }
    g_authToken = "token-for-" + apiKey;
}

// 對外的取得介面:先看快速失敗旗標,再決定要不要真的嘗試
std::string getAuthToken(const std::string& apiKey) {
    if (g_permanentlyFailed.load()) {
        throw std::invalid_argument("先前已判定為永久性失敗,直接拒絕(未重試)");
    }
    std::call_once(g_authFlag, connectAuthServer, apiKey);
    return g_authToken;
}

void authWorker(int id, const std::string& apiKey) {
    try {
        std::string token = getAuthToken(apiKey);
        say("    [請求 " + std::to_string(id) + "] 取得 " + token);
    } catch (const std::exception& e) {
        say("    [請求 " + std::to_string(id) + "] 失敗: " + e.what());
    }
}

// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】—— 本檔從缺,理由如下
//   LeetCode 的並行題(1114 Print in Order、1115 Print FooBar Alternately、
//   1116 Print Zero Even Odd、1117 Building H2O、1195 Fizz Buzz Multithreaded)
//   的評測環境不會在初始化階段注入失敗,也沒有「重試」這個維度;
//   而本檔的主題正是「初始化失敗後會發生什麼」。
//   把重試邏輯硬塞進那些題目只會讓解法偏離題意,因此誠實從缺,
//   改以上面的授權連線情境示範真實系統中最常見的補強做法。
// -----------------------------------------------------------------------------

int main() {
    std::cout << "=== 原始示範:四條執行緒,初始化前兩次失敗 ===" << std::endl;
    std::vector<std::thread> pool;
    for (int i = 0; i < 4; ++i) pool.emplace_back(worker);
    for (std::thread& t : pool) t.join();

    std::cout << "  ── 統計 ──" << std::endl;
    std::cout << "  mayFail() 實際被執行 " << attempt.load() << " 次"
              << "(前 2 次拋例外不算完成,第 3 次才是 effective call)" << std::endl;

    std::cout << "\n=== 實務:永久性失敗不應該讓每個請求都重試 ===" << std::endl;
    std::vector<std::thread> auth;
    for (int i = 1; i <= 5; ++i) auth.emplace_back(authWorker, i, "bad-key-123");
    for (std::thread& t : auth) t.join();
    std::cout << "  實際嘗試連線次數 = " << g_connectAttempts.load()
              << " (5 個請求,但只真的連了 1 次;其餘被快速失敗擋下)" << std::endl;

    return 0;
}

