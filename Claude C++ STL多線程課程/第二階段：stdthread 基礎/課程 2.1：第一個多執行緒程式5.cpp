// 【LeetCode 實戰範例】—— 本檔從缺,理由如下
//   本檔的主題是「lambda 的捕獲語意與閉包生命週期」,屬於語言機制。
//   LeetCode 的並行題(1114 Print in Order、1115 Print FooBar Alternately、
//   1116 Print Zero Even Odd、1117 Building H2O、1195 Fizz Buzz Multithreaded)
//   考的是執行緒之間的同步原語,評測框架甚至不讓你自己建立執行緒 ——
//   用不到「怎麼捕獲」這件事。本課第 6 個範例檔(總整理)會示範
//   真正對應得上的 1114;此處硬湊反而失焦,故從缺。
// -----------------------------------------------------------------------------

int main() {
    std::cout << "=== 原始示範:用 lambda 建立執行緒 ===" << std::endl;
    std::thread t([]() {
        std::cout << "Hello from lambda!" << std::endl;
    });
    t.join();

    std::cout << "\n=== 值捕獲 vs 參考捕獲(安全的那一半) ===" << std::endl;
    {
        int  local = 42;
        std::string name = "設定值";

        // 值捕獲:執行緒拿到的是副本,即使 local 之後改變也不受影響
        std::thread byValue([local, name]() {
            say("  [值捕獲] 看到 " + name + " = " + std::to_string(local));
        });
        local = 999;             // 改動不會影響已經複製進閉包的那一份
        byValue.join();
        say("  [主執行緒] local 現在是 " + std::to_string(local));

        // 參考捕獲:只有在「保證變數活得比執行緒久」時才安全。
        // 這裡我們在同一個區塊內 join,所以 local 一定還活著 —— 這是安全的用法。
        std::thread byRef([&local]() {
            say("  [參考捕獲] 看到最新的 local = " + std::to_string(local));
        });
        byRef.join();            // ← join 在 local 離開作用域之前,所以安全
    }

    std::cout << "\n=== 實務:平行下載,每條執行緒帶自己的任務 ===" << std::endl;
    std::vector<std::string> urls = {"a.com/x", "bb.com/yy", "ccc.com/zzz"};
    std::vector<DownloadResult> results(urls.size());
    downloadAll(urls, results);

    std::cout << "  ── 彙總 ──" << std::endl;
    for (const DownloadResult& r : results) {
        std::cout << "  " << r.url << " → " << r.bytes << " bytes" << std::endl;
    }

    return 0;
}

