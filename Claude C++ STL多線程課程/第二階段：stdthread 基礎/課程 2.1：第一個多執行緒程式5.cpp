// =============================================================================
//  課程 2.1：第一個多執行緒程式5.cpp  —  用 Lambda 建立執行緒
// =============================================================================
//
// 【主題資訊 Information】
//   語法      : std::thread t([捕獲](參數){ 函式本體 });
//   標準版本  : C++11(std::thread 與 lambda 同期加入);
//               generic lambda 是 C++14;init-capture(捕獲時初始化)也是 C++14
//   標頭檔    : <thread>
//   本質      : lambda 會被編譯成一個匿名類別,thread 建構時「複製」它
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼 lambda 是建立執行緒最常見的方式】
// 執行緒的進入點通常是「只在這裡用一次」的一段邏輯。為它另外命名一個
// 全域函式,不但污染命名空間,還讓程式碼被拆到兩個地方閱讀。
// lambda 讓工作內容就寫在建立執行緒的那一行旁邊,是可讀性上的明顯勝利。
// 更關鍵的是:一般函式只能透過參數或全域變數取得外部資訊,
// 而 lambda 可以直接「捕獲」外圍的區域變數。
//
// 【2. 捕獲方式決定了這段程式安全或危險】
// 這是本課最重要的一點。捕獲有兩種基本形式:
//
//   [=] 或 [x]  —— 以「值」捕獲:把變數複製一份存進閉包物件。
//                   執行緒操作的是自己的副本,外面的變數消失也不影響它。
//                   ✅ 對執行緒來說這是安全的預設選擇。
//
//   [&] 或 [&x] —— 以「參考」捕獲:閉包裡存的是指向外面變數的參考。
//                   一旦那個變數的作用域結束、它被銷毀,而執行緒還在跑,
//                   就是在存取已銷毀的物件 —— 未定義行為。
//                   ⚠️ 這是多執行緒最經典的懸空參考錯誤(見課程 2.4)。
//
// 單執行緒下用 [&] 幾乎總是安全的(lambda 通常立刻就被呼叫完),
// 這養成的直覺一旦帶進多執行緒,就會直接踩雷 ——
// 因為執行緒的生命週期和建立它的那個作用域完全脫鉤了。
//
// 【3. thread 是「複製」lambda,不是引用它】
//     std::thread t(myLambda);
// thread 會把 myLambda 這個閉包物件複製(或移動)到執行緒自己的儲存空間。
// 所以即使 myLambda 這個變數本身立刻離開作用域,執行緒手上的副本仍然有效。
// 但請注意:複製的是「閉包物件」,而閉包物件裡若存的是參考,
// 複製之後那個參考指向的仍然是同一個(可能即將被銷毀的)外部變數。
// 也就是說,thread 的複製救不了 [&] 捕獲。
//
// 【概念補充 Concept Deep Dive】
//
// (A) lambda 到底被編譯成什麼
//   編譯器會為每個 lambda 產生一個匿名類別(closure type),
//   捕獲的變數成為它的資料成員,函式本體成為 operator()。
//     [x]() { use(x); }
//   大致等價於:
//     class __Anon { int x; public: void operator()() const { use(x); } };
//   無捕獲的 lambda 沒有資料成員,所以它可以隱式轉換成函式指標;
//   有捕獲的則不行 —— 因為函式指標無處存放那些狀態。
//
// (B) 為什麼 std::thread 接受 lambda 完全不需特別支援
//   std::thread 的建構子接受任何「可呼叫物件」(Callable)。
//   lambda 產生的閉包類別有 operator(),自然就是可呼叫物件。
//   一般函式、函式指標、functor、成員函式指標也都符合 —— 這是同一套機制,
//   本課下一節(課程 2.2)會完整比較這五種形式。
//
// (C) init-capture 讓「移動進執行緒」成為可能(C++14)
//   有些資源只能移動不能複製(如 unique_ptr)。C++14 的 init-capture
//   可以在捕獲時就完成移動:
//       auto p = std::make_unique<int>(42);
//       std::thread t([q = std::move(p)]{ use(*q); });
//   這在 C++11 是做不到的(只能用 shared_ptr 或改傳參數繞過)。
//
// 【注意事項 Pay Attention】
// 1. 傳給執行緒的 lambda,預設應該用「值捕獲」。要用 [&] 之前,
//    必須能明確說出「那個變數保證活得比執行緒久」的理由。
// 2. [this] 捕獲的是指標,和 [&] 一樣危險 —— 物件被銷毀後執行緒仍在跑,
//    就是懸空。C++17 起可用 [*this] 複製整個物件。
// 3. 值捕獲的 lambda 其 operator() 預設是 const,想修改副本要加 mutable。
// 4. 就算捕獲安全,只要多條執行緒同時存取同一份資料,仍需要 mutex ——
//    捕獲方式解決的是「生命週期」,不是「資料競爭」,這是兩個獨立的問題。
// 5. 建立了執行緒就必須 join() 或 detach(),否則解構時 std::terminate()
//    (見本課第 4 個範例檔)。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】用 Lambda 建立執行緒
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 把 lambda 傳給 std::thread 時,值捕獲 [=] 和參考捕獲 [&]
//        在多執行緒下有什麼關鍵差別?
//     答：值捕獲會把變數複製一份存進閉包,執行緒用的是自己的副本,
//         外部變數消失也沒關係。參考捕獲存的是指向外部變數的參考,
//         一旦那個變數的作用域結束而執行緒還在跑,就是存取已銷毀的物件,
//         屬於未定義行為。所以傳給執行緒的 lambda 預設應該用值捕獲。
//     追問：那 [this] 算哪一種?
//         → 算參考那一類,它捕獲的是 this 指標。物件被銷毀後執行緒還在用,
//           一樣是懸空。C++17 起可以用 [*this] 複製整個物件來避開。
//
// ⚠️ 陷阱. 「std::thread 會把 lambda 複製一份到執行緒自己的空間,
//         所以就算我用 [&] 捕獲也沒關係,反正都複製過去了。」哪裡錯了?
//     答：複製的是「閉包物件」本身,不是它所參考的東西。
//         用 [&] 捕獲時,閉包裡存的就是一個參考(實作上是指標);
//         把這個閉包複製一百份,那一百份裡的參考仍然全部指向同一個
//         原始變數 —— 那個變數該被銷毀時還是會被銷毀。
//     為什麼會錯：把「複製閉包」誤以為等於「深層複製它用到的所有東西」。
//         複製的深度只到閉包的資料成員為止,而 [&] 的資料成員就是個參考,
//         複製參考只會得到另一個指向同一處的參考。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <mutex>
#include <string>
#include <thread>
#include <vector>

// 保護 std::cout,避免多執行緒輸出互相切開
std::mutex g_ioMutex;

void say(const std::string& msg) {
    std::lock_guard<std::mutex> lock(g_ioMutex);
    std::cout << msg << std::endl;
}

// -----------------------------------------------------------------------------
// 【日常實務範例】平行下載多個檔案,每條執行緒帶著自己的任務資料
//   情境: 下載器要同時處理一批 URL。每條執行緒需要知道「自己負責哪一個」,
//         這份資訊必須用「值捕獲」帶進執行緒 ——
//         因為迴圈變數 i 與區域字串在迴圈結束後就沒了,
//         用參考捕獲會讓所有執行緒讀到已銷毀(或全都相同)的資料。
//   為什麼用本主題: 這是 lambda 捕獲在多執行緒下最典型、也最常出錯的場景。
// -----------------------------------------------------------------------------
struct DownloadResult {
    std::string url;
    int         bytes = 0;
};

void downloadAll(const std::vector<std::string>& urls,
                 std::vector<DownloadResult>&    results) {
    std::vector<std::thread> workers;

    for (std::size_t i = 0; i < urls.size(); ++i) {
        // 值捕獲 i 與 url:每條執行緒都拿到自己的一份副本。
        // results 用參考捕獲是安全的 —— 它由呼叫端擁有,
        // 生命週期明確長於底下所有 join 完成之前。
        // 而且每條執行緒只寫自己的 results[i],沒有兩條寫同一格,
        // 因此不需要額外的 mutex(這是常見且正確的「切割寫入」模式)。
        std::string url = urls[i];
        workers.emplace_back([i, url, &results]() {
            int bytes = static_cast<int>(url.size()) * 1024;   // 模擬下載
            results[i] = DownloadResult{url, bytes};
            say("    [執行緒] 下載完成 " + url + " (" + std::to_string(bytes) +
                " bytes)");
        });
    }

    for (std::thread& t : workers) t.join();
}

