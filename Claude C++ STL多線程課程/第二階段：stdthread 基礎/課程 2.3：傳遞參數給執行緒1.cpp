// =============================================================================
//  課程 2.3：傳遞參數給執行緒1.cpp  —  基本參數傳遞與 decay-copy 的本質
// =============================================================================
//
// 【主題資訊 Information】
//   template<class F, class... Args>
//   explicit thread(F&& f, Args&&... args);                       // C++11 起
//
//   標準語意（[thread.thread.constr]）：新執行緒執行
//       INVOKE( decay-copy(std::forward<F>(f)),
//               decay-copy(std::forward<Args>(args))... )
//   C++11–C++20 的規格文字寫作 DECAY_COPY(x)，C++23 改寫成 auto(x)，語意相同：
//   對每個參數做 std::decay_t 後「複製一份」到執行緒自己的儲存區。
//
//   標頭檔：<thread>（std::thread）、<functional>（std::ref / std::cref）
//   複雜度：建構本身 O(1)，但實際成本是一次 OS 執行緒建立（Linux 上為 clone(2)）
//           加上每個參數一次複製；相對於函式呼叫是「很貴」的操作。
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼參數不是「直接轉發」，而是先複製一份？】
// 直覺上會期待 std::thread t(f, a, b) 就等於「在新執行緒裡呼叫 f(a, b)」。
// 但這兩者有一個根本差異：**時間**。
//   t.join() 之前，建構 thread 的那一行早就返回了，呼叫端可能繼續往下跑、
//   離開作用域、把 a 和 b 銷毀掉——而新執行緒可能這時才真正開始執行 f。
// 如果 std::thread 保留的是「指向 a 的引用」，那就是一個標準的懸空引用。
// 所以標準選擇了唯一安全的預設：**把參數的值複製到執行緒自己的儲存區**，
// 讓新執行緒操作的資料生命週期由執行緒物件自己掌握，而不依賴呼叫端的堆疊。
//
// 【2. decay 到底做了什麼？】
// decay-copy 不是單純的複製，它先套用 std::decay_t，效果是：
//   * 陣列 → 指標：char buf[6] 傳進去，存的是 char*（不是 6 個字元的副本！）
//   * 函式 → 函式指標
//   * 去掉 const / volatile / 引用：const int& → int
// 第一條在課程 2.3 的第 5 個檔案會變成一個真正的懸空 bug（見該檔）：
// 陣列退化成指標之後，執行緒存的只是「一個指向呼叫端堆疊的位址」。
//
// 【3. 複製發生在哪一條執行緒？】
// 複製發生在**呼叫端（建構 thread 的那條執行緒）**，在建構子返回前就完成。
// 標準保證建構子的完成 synchronizes-with 新執行緒中該函式的開始執行，
// 所以「複製好的參數」對新執行緒可見，不需要額外同步。
// 這一點很重要：如果複製是延後到新執行緒才做，那連複製本身都會讀到已銷毀的物件。
//
// 【4. 存起來之後是以 rvalue 傳給函式的】
// 執行緒儲存區裡的副本，在真正呼叫函式時是以 **rvalue** 傳入。
// 這個細節解釋了本課最常見的兩個「怪現象」：
//   * void f(int&)       + thread(f, v) → **編譯失敗**（rvalue 綁不到非 const 左值引用）
//   * void f(const int&) + thread(f, v) → **編譯成功，但綁到的是副本，不是 v**
// 第二種是本課最陰險的陷阱：它安靜地通過編譯，卻沒有引用語意。
// 兩者的實測見本檔 main() 的第二、三段。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 副本存在哪裡？
//   libstdc++ 把 callable 與所有參數包進一個 std::tuple，放在一個 heap 配置的
//   _State 物件中（thread::_State_impl），再把該物件的指標交給 clone/pthread_create。
//   所以副本**不在呼叫端的堆疊上，也不在新執行緒的堆疊上，而是在 heap**。
//   本機實測（見下方 main 第三段）：main 的區域變數位址為 0x7ffc... 這種堆疊位址，
//   而執行緒內經由 const int& 看到的位址是 0x5c6d... 這種 heap 位址——兩者不同區段，
//   一眼就能看出「你引用到的根本不是原變數」。
//   （實際位址每次執行都不同，且是實作定義；此處只用來對比「不同記憶體區段」。）
//
// (B) 為什麼是 heap？
//   因為新執行緒的堆疊在 pthread_create 之後才存在，而呼叫端的堆疊隨時可能消失，
//   兩者都不能安全承載「跨越執行緒邊界、生命週期不確定」的資料。heap 是唯一
//   兩邊都能穩定存取、且由執行緒狀態物件自行管理釋放的地方。
//
// (C) 這個設計的代價
//   參數一律複製，代表傳大物件會有一次額外複製成本。要避免時的選項有三：
//     1) std::move(obj)  → 副本是 move 構造的（見本課第 6 個檔案）
//     2) std::ref(obj)   → 只複製一個指標大小的 reference_wrapper（見第 3 個檔案）
//     3) 傳指標          → 只複製指標（見第 4 個檔案）
//   後兩者都把生命週期責任交還給你，是效能與安全的交換。
//
// 【注意事項 Pay Attention】
// 1. 「參數被複製」對**函式簽名是引用**時同樣成立——這是本課最大的誤解來源。
//    要有引用語意只有 std::ref / std::cref 一條路。
// 2. 陣列與字串常值會 decay 成指標，複製的是位址不是內容；生命週期問題原封不動。
// 3. 複製次數是實作可觀察的：傳左值至少一次複製構造，傳右值則是移動構造。
//    寫有 side effect 的複製構造子去「數複製次數」，數字可能因實作而異。
// 4. 參數型別必須可 decay-copy。傳入不可複製也不可移動的型別會編譯失敗。
// 5. std::thread 的建構子是 explicit，且一旦建構就已經開始跑；沒有「先建好、之後再啟動」
//    的兩階段語意（那是 std::async / 執行緒池才有的概念）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】std::thread 的參數傳遞（decay-copy）
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::thread t(f, a, b) 中，a 和 b 是何時、在哪一條執行緒上被複製的？
//     答：在**呼叫端**執行緒上，且在 thread 建構子返回之前就複製完成，
//         副本存放在 heap 上的執行緒狀態物件中（libstdc++ 是一個 tuple）。
//         標準保證建構子的完成 synchronizes-with 新執行緒中函式的開始，
//         因此副本對新執行緒可見，不必額外同步。
//     追問：那為什麼不能延後到新執行緒再複製？
//         → 因為那時呼叫端的區域變數可能已經銷毀，連「複製」這個動作本身
//           都會讀到已死亡的物件。
//
// 🔥 Q2. void f(int& x) 搭配 std::thread t(f, value)，會發生什麼事？
//     答：**編譯失敗**，不是「印出未修改的值」。執行緒儲存區裡的副本是以
//         rvalue 傳給 f 的，rvalue 無法綁定到非 const 左值引用，
//         libstdc++ 會直接 static_assert：
//         "std::thread arguments must be invocable after conversion to rvalues"。
//     追問：那要怎麼改才真的能改到原值？
//         → std::thread t(f, std::ref(value))。
//
// ⚠️ 陷阱. void f(const int& x) 搭配 std::thread t(f, value) 能編譯嗎？
//         x 綁到的是 value 嗎？
//     答：**能編譯**（const 左值引用可以綁定 rvalue），但 x 綁到的是
//         執行緒內部的那份副本，**不是 value**。本機實測：main 中 value 的位址是
//         0x7ffe37f4e73c（堆疊），執行緒內看到的是 0x58be5db52040（heap 副本）；
//         改用 std::cref(value) 後，執行緒內看到的位址就與原變數完全相同。
//     為什麼會錯：多數人用「能不能編譯」當作「有沒有引用語意」的判準。
//         但 const& 能綁 rvalue，於是「編譯成功」在這裡完全不代表你拿到了原物件——
//         它只是安靜地引用了一份你不知道存在的副本。唯讀時看起來人畜無害，
//         一旦你期待看到別的執行緒對該變數的更新，就會百思不得其解。
//
// ⚠️ 陷阱2. 「參數會被複製，所以傳進執行緒的東西一定是安全的」——對嗎？
//     答：不對。複製的是「decay 之後的值」。char buf[] 傳進去，decay 成 char*，
//         複製的是**指標本身**，指向的字元仍在呼叫端堆疊上。
//         複製一個指向即將消失的物件的指標，得到的只是一個一樣會懸空的指標。
//     為什麼會錯：把「複製參數」誤讀成「深複製整個物件圖」。
//         decay-copy 只複製頂層的值；指標、引用包裝、view 型別（string_view、span）
//         全都只複製「觀察者」，不複製被觀察的資料。本課第 5 個檔案就是這個 bug 的實例。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <thread>
#include <functional>   // std::cref

void printSum(int a, int b) {
    std::cout << a << " + " << b << " = " << (a + b) << std::endl;
}

// -----------------------------------------------------------------------------
// 用來觀察「執行緒實際引用到哪一塊記憶體」。
// 參數刻意用 const int&：這是唯一「傳值也能編譯」的引用寫法，也正是陷阱所在。
// -----------------------------------------------------------------------------
void showBinding(const int& x, const char* tag) {
    std::cout << "  " << tag << " 執行緒內看到的位址 = " << static_cast<const void*>(&x)
              << "，值 = " << x << std::endl;
}

int main() {
    std::cout << "=== 基本參數傳遞 ===" << std::endl;
    std::thread t(printSum, 3, 5);
    t.join();

    std::cout << "\n=== 參數是複製的：函式簽名是 const& 也一樣 ===" << std::endl;
    int value = 42;
    std::cout << "  main 中 value 的位址        = " << static_cast<const void*>(&value)
              << "，值 = " << value << std::endl;

    // 直接傳 value：decay-copy 成一份 int 副本，const int& 綁到那份副本
    std::thread t2(showBinding, value, "直接傳 value:");
    t2.join();

    // std::cref：複製的是 reference_wrapper，還原後綁到原本的 value
    std::thread t3(showBinding, std::cref(value), "用 std::cref:");
    t3.join();

    std::cout << "\n  ↑ 直接傳的位址落在 heap（執行緒的參數副本），與 main 的堆疊位址不同；" << std::endl;
    std::cout << "    std::cref 的位址則與 main 中 value 完全相同。" << std::endl;
    std::cout << "    （位址每次執行都不同，且為實作定義，重點在「是否相同」而非數值本身）" << std::endl;

    return 0;
}

