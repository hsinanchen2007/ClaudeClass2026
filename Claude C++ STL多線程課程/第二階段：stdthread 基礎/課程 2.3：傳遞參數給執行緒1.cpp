// === 預期輸出 ===
// === 基本參數傳遞 ===
// 3 + 5 = 8
//
// === 參數是複製的：函式簽名是 const& 也一樣 ===
//   main 中 value 的位址        = 0x7ffc5e5e9114，值 = 42
//   直接傳 value: 執行緒內看到的位址 = 0x5c6df98f74a0，值 = 42
//   用 std::cref: 執行緒內看到的位址 = 0x7ffc5e5e9114，值 = 42
//
//   ↑ 直接傳的位址落在 heap（執行緒的參數副本），與 main 的堆疊位址不同；
//     std::cref 的位址則與 main 中 value 完全相同。
//
//     上面是某一次實測的結果。可重現的不是數值，而是這個關係：
//         「直接傳 value」的位址 != main 中 value 的位址   （綁到 heap 上的副本）
//         「用 std::cref」的位址 == main 中 value 的位址   （綁到原變數）
//     這兩條關係才是本檔要證明的事。
