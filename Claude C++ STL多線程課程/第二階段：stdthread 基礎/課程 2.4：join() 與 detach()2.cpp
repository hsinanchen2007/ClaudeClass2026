// =============================================================================
//  課程 2.4：join() 與 detach()2.cpp  —  detach():放手之後會發生什麼
// =============================================================================
//
// 【主題資訊 Information】
//   簽名      : void detach();
//   標準版本  : C++11
//   標頭檔    : <thread>
//   前置條件  : joinable() 必須為 true,否則丟出 std::system_error
//   效果      : 切斷 thread 物件與執行緒的關聯;執行緒繼續獨立執行,
//               結束時由執行期系統自動回收資源
//   之後      : joinable() 變成 false;你再也無法等待它、無法知道它結束了沒
//
// 【詳細解釋 Explanation】
//
// 【1. detach() 真正的意思是「放棄所有權」】
// 很多人把 detach() 理解成「讓執行緒在背景跑」,這只說對了一半。
// 更精確的說法是:**你放棄了對這條執行緒的一切控制與知情權。**
// 放棄之後你失去三樣東西:
//   * 無法等待它結束(沒有任何方法可以 join 一條已 detach 的執行緒)
//   * 無法知道它是否還在跑、跑到哪裡、有沒有出錯
//   * 無法取得它的回傳值或它拋出的例外
// 換來的只有一樣:你不必負責回收它 —— 執行期系統會在它結束時自動處理。
//
// 【2. 程式結束時,detach 的執行緒會怎樣】
// 這是本檔示範的核心。從 main() 返回,效果等同呼叫 std::exit:
//   (a) 先銷毀有靜態儲存期的物件、執行 atexit 註冊的函式;
//   (b) 然後行程結束。
// 標準「不會」等待任何已 detach 的執行緒。所以:
//   * 本例的背景執行緒睡 1 秒,而 main 幾乎立刻返回 → 行程在它醒來前
//     就結束了,「背景執行緒完成」永遠來不及印出來。
//   * 更危險的是階段 (a):如果 detach 的執行緒在這段期間還在跑,
//     而它碰到了正在被銷毀(或已經銷毀)的全域物件 —— 包括 std::cout ——
//     那就是未定義行為。
//
// ⚠️ 也就是說,本檔的「什麼都沒印出來」是最幸運的結果。
//    真實世界中這種競爭常常表現為:程式結束時偶爾崩潰、
//    偶爾在 log 尾巴出現亂碼,而且完全無法穩定重現。
//
// 【3. 那 detach() 到底什麼時候該用】
// 合理的使用場景其實不多,共同特徵是「這個工作的生命週期本來就該和
// 建立它的那段程式脫鉤」:
//   * 真正的常駐背景服務(監控、定期清理),它會活到行程結束。
//   * 射後不理的通知/上報,失敗了也無所謂。
// 而且必須滿足兩個嚴格條件:
//   (a) 執行緒需要的所有資料都以「值」或 shared_ptr 完整帶走,
//       絕不碰呼叫端的區域變數(見本課第 3 個範例檔的 UB 示範);
//   (b) 程式結束前有明確的關閉協定,或該執行緒不碰任何全域資源。
//
// 實務上,現代 C++ 的建議是:**優先用 join(),需要背景執行就用執行緒池
// 或 std::jthread(C++20),盡量不要用 detach()。**
// 原因很簡單:detach 讓你失去了觀測能力,而不能觀測的東西就不能除錯。
//
// 【4. detach() 不能取代 join() 來「避免 terminate」】
// 有人發現「忘記 join 會 terminate」之後,乾脆到處寫 detach() 來消災。
// 這是把「一個會立刻爆出來的錯誤」換成「一個隨機出現的未定義行為」——
// 交換的方向完全錯誤。terminate 至少會當場告訴你哪裡有問題。
//
// 【概念補充 Concept Deep Dive】
//
// (A) detach 之後,執行緒的資源由誰回收
//   在 POSIX 上對應 pthread_detach():執行緒結束時由執行期系統
//   自動釋放它的堆疊與相關結構,不需要有人來 join。
//   代價就是沒有任何回收點可以讓你搭便車取得結果或同步。
//
// (B) 為什麼 detach 之後就沒有 happens-before 保證
//   join() 之所以能保證「你看得到執行緒寫的資料」,是因為它是一個
//   明確的同步點。detach 之後根本沒有這樣的點 ——
//   你連它做完了沒都不知道,自然也無從保證看得到它的結果。
//   要跨過這道牆,只能自己用 atomic、mutex 或條件變數建立同步。
//
// (C) 想要「背景執行但仍可觀測」怎麼做
//   * std::async(std::launch::async, f) → 回傳 future,可查詢/等待/取例外。
//   * 執行緒池 + 任務佇列 → 生命週期由池管理,可優雅關閉。
//   * std::jthread(C++20)→ 解構時自動 request_stop() + join(),
//     兼顧方便與可控。
//   這三者都比 detach 更好,detach 幾乎總是最後的選擇。
//
// 【注意事項 Pay Attention】
// 1. detach 之後無法等待、無法查詢狀態、無法取得結果或例外。
// 2. 程式結束時不會等待 detach 的執行緒;它們可能在靜態物件銷毀期間
//    仍在執行,碰到已銷毀的全域物件(含 std::cout)就是 UB。
// 3. detach 的執行緒絕不可存取呼叫端的區域變數 ——
//    所有資料必須以值或 shared_ptr 完整帶走。
// 4. 對 non-joinable 的 thread 呼叫 detach() 會丟 std::system_error。
// 5. 別把 detach 當成「避免忘記 join 導致 terminate」的偷懶解法,
//    那是把明確的錯誤換成隨機的未定義行為。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】detach() 的語意與風險
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. detach() 之後,你失去了什麼?
//     答：失去對那條執行緒的一切控制與知情權 —— 無法等待它結束、
//         無法知道它是否還在跑、無法取得回傳值或它拋出的例外。
//         換來的只有「不必負責回收資源」。也因為沒有同步點,
//         你也失去了 join() 提供的 happens-before 保證。
//     追問：那怎麼「背景執行但仍然可觀測」?
//         → 用 std::async 取得 future,或用執行緒池管理生命週期,
//           或用 C++20 的 std::jthread(解構時自動 request_stop + join)。
//           這三者都比 detach 好。
//
// 🔥 Q2. 程式從 main 返回時,已 detach 且還在跑的執行緒會怎樣?
//     答：標準不會等待它們。從 main 返回等同呼叫 std::exit:
//         先銷毀靜態儲存期物件、執行 atexit,然後行程結束。
//         detach 的執行緒若在這期間還在跑,而且碰到正在被銷毀或
//         已銷毀的全域物件(包括 std::cout),就是未定義行為。
//     追問：本例為什麼「背景執行緒完成」沒印出來?
//         → 因為它睡 1 秒,而 main 幾乎立刻返回,行程在它醒來前就結束了。
//           這其實是最幸運的結果 —— 若時序稍有不同、它剛好醒來去用
//           已被銷毀的 std::cout,就會變成隨機崩潰。
//
// ⚠️ 陷阱. 「忘記 join 會導致 terminate,那我全部改成 detach 不就
//         不會崩潰了嗎?」哪裡錯了?
//     答：這是把「一個一定會發生、當場報錯的問題」,換成「一個隨機出現、
//         無法重現的未定義行為」—— 交換方向完全錯了。
//         terminate 至少會在出錯的那一刻明確地停下來告訴你;
//         而 detach 造成的懸空存取可能三個月後才在正式環境爆一次,
//         症狀還出現在完全無關的地方。
//     為什麼會錯：把「程式沒有中止」當成「程式是對的」。
//         程式沒崩潰不代表它正確,只代表錯誤還沒被觀察到 ——
//         這在未定義行為上尤其致命,因為 UB 最常見的表現形式
//         就是「看起來一切正常」。
// ═══════════════════════════════════════════════════════════════════════════

#include <chrono>
#include <iostream>
#include <memory>
#include <mutex>
#include <string>
#include <thread>

// 保護 std::cout,避免多執行緒輸出互相切開
std::mutex g_ioMutex;

void say(const std::string& msg) {
    std::lock_guard<std::mutex> lock(g_ioMutex);
    std::cout << msg << std::endl;
}

// -----------------------------------------------------------------------------
// 【日常實務範例】射後不理的用量上報 —— detach 少數合理的用法
//   情境: 使用者操作完成後,想順手把一筆匿名用量統計送到分析伺服器。
//         這件事「失敗了也無所謂」,而且絕不能拖慢使用者的操作。
//   為什麼用本主題: 這是 detach 真正站得住腳的場景,同時示範它的兩個
//         必要條件:(a) 所有資料以值完整帶走,不碰呼叫端的區域變數;
//         (b) 主流程在結束前留下足夠時間,或該任務不碰全域資源。
//   ⚠️ 注意本例仍用 sleep 等它一下 —— 那是示範用的權宜之計。
//      正式系統應該用明確的關閉協定(例如一個「等待所有背景任務」的
//      計數器),而不是靠 sleep 猜時間。
// -----------------------------------------------------------------------------
struct UsageEvent {
    std::string feature;
    int         durationMs;
};

void reportUsage(UsageEvent e) {   // 按值接收:執行緒擁有自己的一份
    say("    [上報] feature=" + e.feature + " duration=" +
        std::to_string(e.durationMs) + "ms");
}

void onUserAction(const std::string& feature) {
    UsageEvent e{feature, 128};        // 區域變數……
    std::thread t(reportUsage, e);     // ……但整包「複製」進執行緒,不是借用
    t.detach();
    say("    使用者操作已完成(不等上報結果就返回)");
}   // e 在這裡銷毀,但執行緒手上有自己的副本 → 安全

// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】—— 本檔從缺,理由如下
//   LeetCode 的並行題(1114 Print in Order、1115 Print FooBar Alternately、
//   1116 Print Zero Even Odd、1117 Building H2O、1195 Fizz Buzz Multithreaded)
//   全部要求「所有執行緒的工作都必須完成且被觀察到」,評測框架會等待
//   每一條執行緒結束才比對輸出。detach() 的語意恰恰是「放棄等待與觀測」——
//   在那些題目裡使用 detach 會直接導致輸出不完整而判錯。
//   本課第 7 個範例檔(總整理)會示範真正對應得上的
//   1195 Fizz Buzz Multithreaded,此處從缺。
// -----------------------------------------------------------------------------

int main() {
    std::cout << "=== 原始示範:detach 之後主程式立刻結束 ===" << std::endl;
    {
        std::thread t([]() {
            std::this_thread::sleep_for(std::chrono::seconds(1));
            // 這行幾乎不可能被印出來 —— 行程早就結束了。
            // 更嚴重的是:若此時 std::cout 已被銷毀,這行就是未定義行為。
            std::cout << "背景執行緒完成" << std::endl;
        });

        t.detach();  // 放手,不等待
        std::cout << "  主執行緒立即繼續" << std::endl;
        std::cout << "  detach 後 t.joinable() = " << std::boolalpha
                  << t.joinable() << "  (已放棄所有權)" << std::endl;
    }

    std::cout << "\n=== 實務:射後不理的用量上報 ===" << std::endl;
    onUserAction("export-pdf");
    // 給背景執行緒一點時間 —— 這是示範用的權宜之計,不是正式作法
    std::this_thread::sleep_for(std::chrono::milliseconds(100));

    std::cout << "\n=== 為什麼建議少用 detach ===" << std::endl;
    std::cout << "  1. 無法等待、無法知道它做完沒、無法取得結果或例外"
              << std::endl;
    std::cout << "  2. 程式結束時不會等它,它可能在靜態物件銷毀期間仍在跑"
              << std::endl;
    std::cout << "  3. 更好的替代:std::async + future / 執行緒池 /"
                 " C++20 的 std::jthread" << std::endl;

    std::cout << "\n=== main 即將返回 ===" << std::endl;
    std::cout << "(第一段那條睡 1 秒的執行緒,到現在都還沒醒 ——"
                 "行程結束不會等它)" << std::endl;
    return 0;
}

