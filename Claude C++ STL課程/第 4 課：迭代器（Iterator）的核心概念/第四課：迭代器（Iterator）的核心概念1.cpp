// =============================================================================
//  第四課：迭代器（Iterator）的核心概念 1  —  從指標出發：迭代器的原型
// =============================================================================
//
// 【主題資訊 Information】
//   語法：
//     for (int* ptr = arr; ptr < arr + N; ++ptr) { ... *ptr ... }
//   標準版本：指標算術是 C 就有的；C++98 起 STL 明確承認
//             「原生指標 T* 本身就是一個 Random Access Iterator」。
//   複雜度：走訪 N 個元素 O(N)；++ptr、*ptr、ptr < end 各為 O(1)。
//   標頭檔：本檔只需 <iostream>；把指標當迭代器餵給演算法時需要 <algorithm>。
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼「指標」是理解迭代器的正確起點】
//   STL 的迭代器不是憑空發明的抽象概念，而是「把指標的操作介面抽出來當成規格」
//   的結果。Alexander Stepanov 設計 STL 時的核心觀察是：C 的指標已經提供了一組
//   極簡但完備的走訪操作——
//       *ptr         取值（解參考）
//       ++ptr        前進到下一個元素
//       ptr1 != ptr2 判斷是否走到同一位置
//   只要任何型別「長得像指標」（支援上述運算子），泛型演算法就能對它運作，
//   而不必知道背後是陣列、鏈結串列、還是紅黑樹。
//   所以正確的因果關係是：**迭代器模仿指標**，而不是指標「剛好也算一種迭代器」。
//
// 【2. `arr` 為什麼可以直接當起點？array-to-pointer decay】
//   `int arr[5]` 的型別是「5 個 int 的陣列」，但在大多數運算式中會發生
//   **array-to-pointer decay**（陣列到指標的隱式轉換）：`arr` 退化成
//   `&arr[0]`，型別變成 `int*`。這就是為什麼 `int* ptr = arr;` 不需要寫 `&`。
//   注意 decay 會遺失長度資訊——這正是 C 陣列必須額外傳 size 的根本原因，
//   也是 STL 容器要提供 end() 的原因：長度必須用「另一個位置」來表達。
//
// 【3. `arr + 5` 為什麼合法？one-past-the-end 是標準保證的】
//   `arr + 5` 指向「最後一個元素的再下一個位置」。這個位址上沒有物件，
//   但 C++ 標準**明文允許**計算它並拿來比較。
//   這是整個 STL 半開區間設計的地基：end() 之所以能存在且可移植，
//   正是因為語言層面保證了 one-past-the-end 指標的合法性。
//   但標準同時規定：**解參考它是 UB**。合法的只有「算出來」與「拿來比較」。
//
// 【4. 為什麼這裡用 `<` 而 STL 慣例用 `!=`】
//   對連續記憶體的指標，`ptr < arr + 5` 與 `ptr != arr + 5` 等價。
//   但 STL 統一用 `!=`，因為 `<` 只有 Random Access Iterator 才支援——
//   std::list 的迭代器沒有 `<`（節點在記憶體中無序，比大小沒有意義）。
//   用 `!=` 寫出來的迴圈可以原封不動換到任何容器，用 `<` 則不行。
//   這是「寫給能力最弱的迭代器」的泛型思維。
//
// 【概念補充 Concept Deep Dive】
//   指標算術不是位元組算術。`++ptr` 實際加的是 `sizeof(*ptr)` 個位元組：
//   int* 加 4、double* 加 8。編譯器產生機器碼時會把 `ptr + n` 編成
//   `base + n * sizeof(T)`（x86-64 上常用 lea 指令一次算完）。
//   迭代器把這件事推得更遠：std::list 的 ++it 編出來是 `it = it->next`，
//   std::map 的 ++it 是紅黑樹的中序後繼走訪（可能得往上爬好幾層父節點）。
//   **同樣一行 `++it`，機器碼可以差到十倍以上**——這就是抽象的代價與價值。
//
// 【注意事項 Pay Attention】
//   1. `*(arr + 5)`（也就是解參考 one-past-the-end）是 UB。UB 不保證任何特定
//      結果：可能讀到相鄰變數的值、可能看起來一切正常、也可能被最佳化器當成
//      「不可能發生」而整段刪掉。不要把它想成「一定會 crash」。
//   2. 迴圈寫死的 5 與陣列大小必須一致。手寫數字是常見 bug 來源，
//      C++17 起可用 `std::size(arr)`（<iterator>）取得編譯期長度。
//   3. 陣列一旦以參數傳入函式就會 decay 成指標，`sizeof(arr)/sizeof(arr[0])`
//      在函式內會算出錯誤答案（變成 sizeof(int*)/sizeof(int)）。
//   4. 兩個指標相減（`end - begin`）只在指向同一陣列時有定義，型別是
//      `std::ptrdiff_t`（有號）。指向不同物件的指標相減是 UB。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】指標作為迭代器的原型
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼說「原生指標就是一個迭代器」？它屬於哪一類？
//     答：因為 T* 天生支援迭代器規格要求的全部操作：*、++、--、+n、-n、
//         兩指標相減、以及 == != < > 比較。它滿足 Random Access Iterator
//         的所有需求，因此 std::sort(arr, arr+n)、std::find(arr, arr+n, x)
//         都能直接對 C 陣列運作，不需要任何轉接層。
//     追問：那 std::vector<int>::iterator 是不是就是 int*？
//         → 不保證，這是**實作定義**。本機 libstdc++（g++ 15.2）用的是
//           __gnu_cxx::__normal_iterator<int*, vector<int>>——包一層的 class，
//           為了型別安全與 debug mode；某些實作在 release 下確實直接
//           typedef 成 int*。所以絕對不能寫 `int* p = vec.begin();`。
//
// 🔥 Q2. `arr + 5` 合法嗎？`arr + 6` 呢？
//     答：`arr + 5`（one-past-the-end）合法，標準明文允許計算與比較，
//         但解參考是 UB。`arr + 6` 連「算出來」本身就是 UB——
//         標準只保證到 one-past-the-end 為止，多一格都不行。
//     追問：那 `arr - 1` 呢？→ 同樣是 UB，沒有 "one-before-the-begin"
//         的對應保證。這就是為什麼反向走訪不能寫成
//         `for (p = arr+4; p >= arr; --p)`（最後一次 --p 會產生 arr-1）。
//
// ⚠️ 陷阱. 反向走訪陣列時，下面這個迴圈為什麼是 UB？
//         for (int* p = arr + 4; p >= arr; --p) std::cout << *p;
//     答：迴圈結束前的最後一次 `--p` 會讓 p 變成 `arr - 1`。這個指標值
//         本身就已經是 UB（不只是不能解參考），接著的 `p >= arr` 比較
//         也建立在無效指標上。實務上多數平台「看起來會正常跑完」，
//         但那不是保證，最佳化器有權假設它不會發生。
//     為什麼會錯：多數人把指標當成普通整數，覺得「算出一個小一點的值
//         有什麼關係，反正我沒解參考」。但 C++ 的指標模型是基於物件的，
//         標準只在 [arr, arr+N] 這個閉區間內定義指標值，走出去就沒有語意。
//         正解是用 one-past-the-end 反向寫：
//             for (int* p = arr + 5; p != arr; ) { --p; use(*p); }
//         或直接用 std::reverse_iterator（見本課第 6 檔）。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>

// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】LeetCode 344. Reverse String
//   題目：原地反轉字元陣列，不可額外配置 O(n) 空間。
//   為什麼用到本主題：這是「指標即迭代器」最直接的體現——雙指標一前一後
//                     相向而行，用的正是本檔示範的 ++/-- 與位置比較。
//                     題目給的是 vector<char>，但演算法對「任何一對隨機存取
//                     迭代器」都成立，所以這裡寫成指標版本，同一份程式碼對
//                     C 陣列與 vec.data() 都能直接套用。
//   複雜度：O(n) 時間、O(1) 額外空間。
// -----------------------------------------------------------------------------
void reverseString(char* first, char* last) {  // 半開區間 [first, last)
    while (first != last) {
        --last;                    // 先退一格才指到有效元素
        if (first == last) break;  // 奇數長度時中間那格不用換
        char tmp = *first;
        *first = *last;
        *last = tmp;
        ++first;
    }
}

int main() {
    int arr[] = {10, 20, 30, 40, 50};

    std::cout << "=== 用指標遍歷陣列 ===" << std::endl;
    // ptr 從 arr 走到 arr+5（one-past-the-end），迴圈內絕不解參考終點
    for (int* ptr = arr; ptr < arr + 5; ++ptr) {
        std::cout << *ptr << " ";
    }
    std::cout << std::endl;

    std::cout << "\n=== 指標算術：加的是元素個數，不是位元組 ===" << std::endl;
    int* p = arr;
    std::cout << "*p           = " << *p << std::endl;
    std::cout << "*(p + 2)     = " << *(p + 2) << std::endl;
    std::cout << "sizeof(int)  = " << sizeof(int)
              << " bytes（所以 p+1 實際跳 " << sizeof(int) << " bytes）" << std::endl;
    std::cout << "(arr+5) - arr = " << (arr + 5) - arr
              << "（指標相減得到元素個數，型別是 ptrdiff_t）" << std::endl;

    std::cout << "\n=== 為什麼 STL 用 != 而不用 < ===" << std::endl;
    // 對連續記憶體兩者等價，但只有 != 能原封不動搬到 list/map 上
    for (int* ptr = arr; ptr != arr + 5; ++ptr) {
        std::cout << *ptr << " ";
    }
    std::cout << " <- 與上面 < 版本結果相同" << std::endl;

    std::cout << "\n=== LeetCode 344. Reverse String ===" << std::endl;
    char s[] = "hello";
    std::cout << "反轉前: " << s << std::endl;
    reverseString(s, s + 5);   // 不含結尾 '\0'
    std::cout << "反轉後: " << s << std::endl;

    return 0;
}

