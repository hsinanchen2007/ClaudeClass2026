// ============================================================
// 第 21 課 總結：deque 的內部結構（分段連續空間）
// 編譯：g++ -std=c++17 -o summary summary.cpp
// ============================================================
// 【deque = double-ended queue 雙端佇列】
//
// 【內部結構：分段連續空間】
//   deque 不像 vector 使用一整塊連續記憶體，
//   而是由多個固定大小的「緩衝區（buffer）」組成，
//   透過一個「中控器（map）」陣列來管理指標。
//
//   map: [ptr0][ptr1][ptr2][ptr3]...
//         ↓     ↓     ↓     ↓
//        buf0  buf1  buf2  buf3   ← 每個 buffer 是固定大小的連續記憶體
//
// 【vs vector 的關鍵差異】
//   vector：一整塊連續記憶體
//     → push_back 可能觸發整體搬移
//     → push_front 是 O(n)（要搬移所有元素）
//
//   deque：分段連續
//     → push_back / push_front 都是 O(1) 攤銷
//     → 不需要整體搬移
//     → 但隨機存取需要計算在哪個 buffer（比 vector 稍慢）
//
// 【deque 的基本操作】
//   push_back(val)    尾端插入 O(1)
//   push_front(val)   頭端插入 O(1)  ← vector 做不到！
//   pop_back()        尾端刪除 O(1)
//   pop_front()       頭端刪除 O(1)  ← vector 做不到！
//   operator[]        隨機存取 O(1)
// ============================================================

#include <iostream>
#include <deque>
using namespace std;

int main() {
    deque<int> dq;

    // ============================================================
    // 1. 雙端插入：push_front + push_back
    // ============================================================
    cout << "===== 1. 雙端插入 =====\n";
    dq.push_back(10);     // 尾端加入
    dq.push_back(20);
    dq.push_back(30);
    dq.push_front(5);     // 頭端加入 ← deque 的優勢！
    dq.push_front(1);

    // 此時邏輯順序：1, 5, 10, 20, 30
    cout << "  deque 內容：";
    for (int val : dq) cout << val << " ";
    cout << endl;
    // 輸出：1 5 10 20 30

    // ============================================================
    // 2. 隨機存取：和 vector 一樣方便
    // ============================================================
    cout << "\n===== 2. 隨機存取 =====\n";
    cout << "  dq[0] = " << dq[0] << endl;  // 1
    cout << "  dq[2] = " << dq[2] << endl;  // 10
    cout << "  dq[4] = " << dq[4] << endl;  // 30

    // ============================================================
    // 3. 雙端刪除：pop_front + pop_back
    // ============================================================
    cout << "\n===== 3. 雙端刪除 =====\n";
    dq.pop_front();  // 移除 1
    dq.pop_back();   // 移除 30

    cout << "  pop 後：";
    for (int val : dq) cout << val << " ";
    cout << endl;
    // 輸出：5 10 20

    cout << "  size = " << dq.size() << endl;  // 3

    // ============================================================
    // 4. front() 和 back() 存取首尾
    // ============================================================
    cout << "\n===== 4. front & back =====\n";
    cout << "  front = " << dq.front() << endl;  // 5
    cout << "  back  = " << dq.back() << endl;   // 20

    // ============================================================
    // 重點整理
    // ============================================================
    cout << "\n=== 重點整理 ===\n";
    cout << "  deque 內部由多個固定大小的 buffer 組成\n";
    cout << "  透過 map（指標陣列）管理各 buffer\n";
    cout << "  push_front / push_back 都是 O(1) 攤銷\n";
    cout << "  隨機存取 O(1)，但比 vector 多一次間接尋址\n";
    cout << "  不保證所有元素在同一塊連續記憶體中\n";

    return 0;
}
