// =============================================================================
//  第 12 課 (2)  —  越界存取 operator[]：未定義行為（UB）的真實面貌
// =============================================================================
//
// ⚠️⚠️ 本檔是「刻意觸發未定義行為」的教學範例 ⚠️⚠️
//   本檔可以正常編譯（無警告），但**預期會異常結束**。
//   程式中止是示範的目的，不是本檔的 bug。
//   請把它當成「解剖標本」閱讀，不要把這種寫法帶進任何真實專案。
//
// 【主題資訊 Information】
//   本檔示範：v[pos] 當 pos >= v.size() 時會發生什麼事
//   標準的規定：[vector.access]/1 明訂 operator[] 的前置條件是 pos < size()，
//               違反前置條件 → 未定義行為（Undefined Behavior, UB），
//               標準對後果「完全不做任何規範」
//   對照組：v.at(pos) 越界會擲出 std::out_of_range（見本課第 3 檔）
//   標頭檔：<vector>
//
// 【詳細解釋 Explanation】
//
// 【1. 「未定義行為」到底是什麼意思？】
// 很多初學者以為 UB = 「程式會當掉」。這是錯的，而且是危險的誤解。
// UB 的正確意義是：**標準放棄對這個程式的一切承諾**。編譯器被允許假設
// 「UB 永遠不會發生」，並基於這個假設去最佳化。所以越界之後：
//   * 可能立刻 abort（有檢查時）
//   * 可能印出一個任意值然後「正常」跑完（沒檢查時）
//   * 可能 segfault
//   * 可能默默改寫別的變數，讓一小時後某段無關的程式出錯
//   * 甚至可能整段程式碼被最佳化掉，行為完全出乎意料
// 最可怕的不是當機，而是**「看起來正常」** —— 你在自己機器上測都沒事，
// 上線後在別的編譯器 / 最佳化等級 / 記憶體配置下才爆炸。
//
// 【2. 本機實測：同一份原始碼，兩種完全不同的結果】
// 環境：Ubuntu g++ 15.2.0，libstdc++ 15。
// Ubuntu 的 g++ 在 **-O0（不最佳化）時預設開啟 _GLIBCXX_ASSERTIONS**
// （libstdc++ 的 hardening 檢查），但在 **-O1 以上會關掉它**。實測：
//
//   編譯方式                     結果
//   ─────────────────────────────────────────────────────────────────
//   g++ -std=c++17       (=-O0)  Assertion '__n < this->size()' failed. → abort（結束碼 134）
//   g++ -std=c++17 -O1           不檢查，印出一個任意值，正常結束（結束碼 0）
//   g++ -std=c++17 -O2           不檢查，印出一個任意值，正常結束（結束碼 0）
//   g++ -std=c++17 -O3           不檢查，印出一個任意值，正常結束（結束碼 0）
//
// 換句話說：**只是改了最佳化等級，同一行程式碼就從「明確中止」變成「靜默放行」。**
// 這正是 UB 的定義在真實世界的樣子。
//
// 【3. 更驚人的實測：連「印出什麼值」都不固定】
// 把 -O2 版本執行檔跑起來（同一個執行檔、同一台機器、同一分鐘內）：
//
//   ./probe_o2               → 印出 0
//   stdbuf -o0 ./probe_o2    → 印出 1048592
//
// 兩者唯一的差別只是 stdio 的緩衝模式。緩衝模式改變了 heap 的配置版面，
// 於是「vector 尾端之後那塊記憶體」剛好放著不同的內容。
// 所以任何教材若寫「v[10] 會印出 XXX」都是不可信的 —— 這個值取決於
// 當下整個行程的記憶體狀態，換個環境變數、換個 shell、換台機器都可能不同。
// **這就是為什麼本檔的「預期輸出」不能把越界讀到的值當成事實記錄下來。**
//
// 【4. 為什麼編譯器不幫我報錯？】
// 因為它辦不到（在一般情況下）。索引往往是執行期才決定的（來自輸入、
// 計算結果、迴圈變數）。編譯期唯一能查的只有常數索引的少數情況。
// 真正能抓越界的工具是**執行期偵測器**：
//   * -fsanitize=address（AddressSanitizer）：抓 heap 越界，訊息最完整
//   * -D_GLIBCXX_ASSERTIONS：libstdc++ 的輕量檢查（本機 -O0 預設就開）
//   * -D_GLIBCXX_DEBUG：更嚴格的 debug mode（會改變 ABI，不可混用）
//   * Valgrind：不必重編譯，但慢很多
// 實務建議：**debug / CI 建置一律開 sanitizer，release 才關掉。**
//
// 【概念補充 Concept Deep Dive】
// v[10] 對一個 size==3 的 vector 做了什麼？
//
//   實際配置的空間（capacity 可能只有 3 個 int = 12 bytes）
//   ┌────┬────┬────┐░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░
//   │ 10 │ 20 │ 30 │░ 不屬於這個 vector 的記憶體 ░░░░
//   └────┴────┴────┘░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░
//     ▲              ▲                    ▲
//   v[0]           v[3] 已越界          v[10] = *(start + 10)
//
// v[10] 就是「起點 + 10 * sizeof(int) = 起點 + 40 bytes」。那個位置可能是：
//   * malloc 的 heap metadata（改寫它 → 之後 free() 崩潰）
//   * 另一個物件的資料（改寫它 → 那個物件莫名其妙變值）
//   * 未對映的分頁（讀它 → SIGSEGV）
// 而 `v[10] = 999`（寫入）比讀取危險得多：讀只是拿到垃圾，
// 寫則是**破壞別人的資料**，錯誤現場離真兇可能有十萬八千里。
//
// 【注意事項 Pay Attention】
// 1. 不要因為「我測過，它會 abort」就安心 —— 那只是本機 -O0 的行為。
//    Release 建置（-O2）不會 abort。UB 沒有「本機實測結果」可以依賴。
// 2. 不要用 v[i] 搭配「來自外部的索引」。索引來自使用者、檔案、網路封包時，
//    要嘛先驗證 i < v.size()，要嘛改用 at()。
// 3. libstdc++ 的 hardening assertion 是**實作提供的額外好意**，
//    不是標準保證。MSVC、libc++ 的預設行為都不一樣。
// 4. 本檔會 abort，因此後面的程式碼不會執行。收集輸出時必須用
//    `stdbuf -o0` 關閉緩衝，否則 abort 會讓還留在緩衝區的輸出整批遺失
//    （實測未加 stdbuf 會少掉 2 行）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】越界存取與未定義行為
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::vector 越界存取會發生什麼事？
//     答：operator[] 越界是未定義行為，標準不做任何規範 —— 可能當機、
//         可能印出任意值、可能默默破壞其他記憶體、也可能看起來完全正常。
//         若要有明確定義的行為，必須改用 at()，它保證擲出 std::out_of_range。
//     追問：那你怎麼在開發階段抓到越界？→ debug/CI 建置開 -fsanitize=address
//         或 -D_GLIBCXX_ASSERTIONS；它們能在越界當下就停下來並印出位置。
//
// 🔥 Q2. 為什麼「未定義行為」比「一定會崩潰」更麻煩？
//     答：崩潰至少是即時、可定位的訊號。UB 最常見的形態是「暫時沒事」——
//         你破壞了別人的記憶體，但錯誤要到很久以後、在完全無關的程式碼裡
//         才顯現，堆疊追蹤指向的是受害者而不是加害者。
//         而且編譯器會「假設 UB 不存在」來最佳化，可能讓你寫的檢查被刪掉。
//     追問：舉個編譯器假設 UB 不存在的例子？→ 若程式碼中出現 p 的解參考，
//         編譯器可推論 p 不為 null，進而把後面的 if (p == nullptr) 整段刪除。
//
// ⚠️ 陷阱. 「我在我的機器上跑過 v[10]，它印出垃圾值但沒當機，所以只是讀到髒資料而已，
//          不算嚴重問題」—— 哪裡錯了？
//     答：錯在把「這一次的觀測結果」當成「這段程式碼的行為」。
//         本機實測就足以推翻它：同一個 -O2 執行檔，直接跑印出 0，
//         用 stdbuf -o0 跑卻印出 1048592；而 -O0 建置根本直接 abort。
//         「沒當機」只是這一次剛好沒踩到未對映的分頁而已。
//     為什麼會錯：把 UB 當成「實作定義行為（implementation-defined）」。
//         實作定義行為是「編譯器必須挑一種行為並寫進文件」（例如 sizeof(int)）；
//         UB 是「沒有人有任何義務」—— 兩者天差地遠，不能用同一套直覺推理。
// ═══════════════════════════════════════════════════════════════════════════

#include <vector>
#include <iostream>

int main() {
    std::vector<int> v = {10, 20, 30};

    // ── 先做安全、可預測的事，說明現場狀況 ─────────────────────────────
    std::cout << "=== 現場資訊（這些都是明確定義的行為）===\n";
    std::cout << "v.size()     = " << v.size() << "\n";
    std::cout << "合法索引範圍 = 0 .. " << v.size() - 1 << "\n";
    std::cout << "v[0]=" << v[0] << " v[1]=" << v[1] << " v[2]=" << v[2] << "\n";

    std::cout << "\n=== 安全的做法：存取前先驗證 ===\n";
    const std::size_t want = 10;
    if (want < v.size()) {
        std::cout << "v[" << want << "] = " << v[want] << "\n";
    } else {
        std::cout << "索引 " << want << " 越界（size=" << v.size()
                  << "），已攔截，不做存取\n";
    }

    // ── 以下刻意觸發 UB，程式預期在此中止 ───────────────────────────────
    std::cout << "\n=== 以下刻意越界（未定義行為）===\n";
    std::cout << "即將執行 v[10]，而 v.size() 只有 " << v.size() << "\n";
    std::cout << "本機 -O0 建置會被 libstdc++ hardening assertion 攔下而 abort；\n";
    std::cout << "但 -O1 以上不會攔，會讀到一個無法預測的值後繼續執行。\n";
    std::cout << std::flush;   // 確保上面的訊息在 abort 前一定送出

    std::cout << "v[10] = " << v[10] << std::endl;   // ← 未定義行為

    // 下面這行在 -O0 建置中不會被執行到（前一行已 abort）。
    // 寫入越界比讀取更危險：它會破壞不屬於這個 vector 的記憶體。
    v[10] = 999;                                     // ← 未定義行為

    std::cout << "如果你看到這一行，代表越界沒有被攔下（例如 -O1 以上的建置）\n";
    return 0;
}

