// =============================================================================
//  第 12 課：vector 元素存取 8  —  operator[] vs at()：邊界檢查的代價
// =============================================================================
//
// 【主題資訊 Information】
//   reference       operator[](size_type pos);        // 無邊界檢查
//   const_reference operator[](size_type pos) const;
//   reference       at(size_type pos);                // 有邊界檢查
//   const_reference at(size_type pos) const;
//   標頭檔：<vector>
//   標準版本：兩者皆 C++98。
//   複雜度：兩者皆 O(1)。差別不在複雜度，而在【常數項】。
//   越界行為：
//     operator[] —— 未定義行為（不檢查，不保證任何結果）
//     at()       —— 丟出 std::out_of_range
//   計時工具：std::chrono::steady_clock（C++11）
//   ⚠️ 本檔的耗時走 stderr，可重現的事實走 stdout。
//
// 【詳細解釋 Explanation】
//
// 【1. at() 到底多做了什麼】
//   libstdc++ 的實作大致是：
//       reference at(size_type n) {
//           if (n >= this->size())
//               __throw_out_of_range_fmt(...);   // 不會回來
//           return (*this)[n];
//       }
//   多出來的只有一次比較加一次條件跳躍，外加一層函式呼叫。
//   直覺會說「那就是永遠比較慢一點」——但本機實測的結果
//   比這個直覺有趣得多，見下方【4.】與【概念補充 (C)】。
//   先記住結構上的差異：
//     ① 多一道 cmp + 條件跳躍
//     ② at() 本身是一層包在 operator[] 外面的函式
//     ③ throw 路徑理論上會限制編譯器對迴圈的重排與向量化
//
// 【2. 為什麼標準要提供兩個】
//   這是 C++ 的核心哲學「你不用的東西，不用付錢」的直接體現。
//   若只提供有檢查的版本，寫高效能程式的人無法擺脫成本；
//   若只提供無檢查的版本，寫應用層程式的人必須自己到處寫檢查。
//   於是標準兩個都給，由呼叫端決定要不要買這份保險。
//   對比 Java：ArrayList 的 get() 永遠檢查，沒有選擇。
//
// 【3. 什麼時候該用哪一個（實務準則）】
//   用 operator[]：
//     * 索引由迴圈變數產生，且迴圈上界就是 size()
//     * 索引來自已驗證過的內部資料
//     * 效能敏感的內層迴圈（影像處理、數值計算、遊戲主迴圈）
//   用 at()：
//     * 索引來自外部（使用者輸入、網路封包、設定檔、資料庫）
//     * 索引經過複雜運算，你無法一眼看出它一定合法
//     * 例外會被上層適當處理（例如轉成 HTTP 400）
//   一句話：【索引的合法性由誰保證】。你保證得了就用 []，
//   保證不了就讓 at() 幫你把關。
//
// 【4. 本機實測結果與直覺相反 —— 這才是本檔真正的重點】
//   本機（g++ 15.2 / libstdc++ / 2000 萬次存取）三次量測：
//       -O0 : operator[] ≈ 89~93 ms   at() ≈ 192~196 ms   → at() 慢約 2.2 倍
//       -O2 : operator[] ≈ 13~14 ms   at() ≈ 13~14 ms     → 兩者【幾乎完全相同】
//   注意這個方向：差距在【關掉】最佳化時最大，開了最佳化反而消失。
//   原因分別是：
//     * -O0：什麼都不 inline。at() 是一層包在 operator[] 外面的函式，
//       所以每次存取變成「兩次函式呼叫」對「一次函式呼叫」。
//       這裡量到的是函式呼叫開銷，根本不是邊界檢查的成本。
//     * -O2：兩者都被完全 inline。更關鍵的是，本檔的索引是
//       手動回繞的 idx（0..999 循環），編譯器【推導得出 idx 一定小於 size()】，
//       於是那個 `if (n >= size()) throw` 被判定為永遠不成立而整段刪除。
//       檢查成本歸零，兩者產生的機器碼幾乎一樣。
//   所以正確的結論不是「at() 比較慢」，而是：
//     邊界檢查的成本，取決於【最佳化器能不能證明它不必要】。
//   證明得了就免費；證明不了（索引來自外部、經過複雜運算、
//   跨越函式邊界而無法內聯）才真的要付錢。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 本檔為什麼要用 i % 1000 而不是直接跑 v[i]
//     原本的意圖是把存取限制在一個小陣列上，讓資料全部留在 L1 快取，
//     這樣量到的才是「存取指令本身」的成本，而不是記憶體延遲。
//     代價是每輪多一次除法（% 很貴）。更好的做法是用一個
//     手動回繞的計數器，本檔改用這個方式，讓 % 不再污染量測。
//
// (B) 為什麼一定要把結果印出來
//     若 sum 沒有被使用，最佳化器會發現整個迴圈沒有副作用而直接刪掉，
//     你就會量到「0 毫秒」這種假結果。
//     把 sum 印出來（或用 asm volatile 屏障）是 micro-benchmark 的基本功。
//     這也是原始碼裡那句「防止編譯器優化掉」的用意。
//
// (C) 本機實測數據（g++ 15.2 / libstdc++ / 2000 萬次存取，各跑三次）
//       -O0 : []  89 / 93 / 89 ms      at() 192 / 192 / 196 ms
//       -O2 : []  13 / 14 / 14 ms      at()  14 /  13 /  13 ms
//     這些數字每次執行都會變動，也會隨機器不同，只供理解量級。
//     但兩件事是穩定的：
//       ① -O0 的「2 倍差距」量到的是函式呼叫開銷，不是檢查成本。
//          拿它下結論等於用 debug build 評估 release 效能。
//       ② -O2 下差距消失，因為編譯器證明了檢查恆真而把它刪掉。
//     這也解釋了為什麼「全面禁用 at()」是個沒有根據的規則——
//     真正該做的是量測你自己的熱路徑。
//
// (D) 什麼情況下 at() 的檢查真的刪不掉
//     編譯器要能刪掉檢查，必須「證明」索引一定小於 size()。
//     以下情況它證明不了，檢查就會留在機器碼裡：
//       * 索引來自函式參數，且該函式沒有被 inline
//       * 索引來自 I/O、亂數、或另一個容器的內容
//       * 索引經過取模以外的複雜運算，超出編譯器的值域分析能力
//       * vector 在迴圈中可能被改動（size() 不再是迴圈不變量）
//     反過來說，本檔量到「差距為零」正是因為索引是最容易分析的形式。
//     換成從外部讀進來的索引，結果就會不同。
//
// 【注意事項 Pay Attention】
//   1. v[i] 越界是未定義行為。不保證崩潰、不保證回傳垃圾值，
//      也可能靜默寫壞相鄰記憶體——這是最難除錯的一種 bug。
//   2. 不要為了「安全」就全面改用 at()。at() 只在你【打算處理】
//      那個例外時才有意義；若上層只是讓程式終止，
//      那和 UB 導致的崩潰相比並沒有改善多少（但有明確錯誤訊息，仍較好）。
//   3. 效能量測一定要在 -O2 以上進行。本機 -O0 量到 at() 慢 2 倍，
//      -O2 卻是兩者相同——用 debug build 下結論會得到相反的答案。
//   4. 兩者的複雜度都是 O(1)。差別是常數項，不是漸進複雜度。
//   5. 本檔的耗時數字每次執行都不同，故走 stderr，不列入預期輸出。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】operator[] 與 at()
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. vector 的 operator[] 和 at() 差在哪？複雜度不同嗎？
//     答：複雜度都是 O(1)，差別在常數項與越界行為。
//         at() 多一次 `if (n >= size()) throw out_of_range`；
//         operator[] 完全不檢查，越界是未定義行為。
//         但「at() 一定比較慢」是錯的——本機 -O2 實測兩者耗時相同
//         （各約 13 ms / 2000 萬次），因為編譯器證明了索引恆在範圍內，
//         直接把檢查刪掉。成本取決於最佳化器能不能證明檢查多餘。
//     追問：那什麼時候該用哪個？
//         → 看「索引的合法性由誰保證」。索引來自迴圈變數或已驗證的
//           內部資料就用 []；來自使用者輸入、網路封包、設定檔就用 at()。
//           後者恰好也是編譯器證明不了、檢查真的會留下來的情況——
//           但那個成本正是你需要的。
//
// 🔥 Q2. 為什麼標準要同時提供這兩個，不乾脆統一成安全的版本？
//     答：因為 C++ 的核心原則是「你不用的東西，不用付錢」。
//         只提供檢查版，寫高效能程式的人無法擺脫成本；
//         只提供無檢查版，應用層要到處自己寫檢查。
//         兩個都提供，讓呼叫端自己決定要不要買這份保險。
//         對比 Java 的 ArrayList.get() 永遠檢查，沒有選擇餘地。
//     追問：有沒有「開發時檢查、上線時不檢查」的折衷？
//         → 有。libstdc++ 可以編譯時定義 _GLIBCXX_ASSERTIONS，
//           讓 operator[] 也加上邊界檢查；MSVC 有 _ITERATOR_DEBUG_LEVEL。
//           這樣 debug build 抓得到越界，release build 維持零成本。
//
// ⚠️ 陷阱 A. 「我在 -O0 下量到 at() 比 operator[] 慢兩倍，
//         所以專案裡應該全面禁用 at()」——這個結論哪裡站不住腳？
//     答：兩個問題。① 拿 debug build 的數字下 release 的結論：
//         -O0 什麼都不 inline，at() 是包在 operator[] 外的一層函式，
//         那 2 倍量到的是【函式呼叫開銷】而非邊界檢查成本。
//         ② 同一份程式在 -O2 下實測兩者【耗時相同】（本機各約 13 ms），
//         因為編譯器證明了索引恆小於 size()，把整個檢查刪掉了。
//         正確的做法是在 release 設定下量測你自己的熱路徑，
//         而不是套用一條「at() 很慢」的通則。
//     為什麼會錯：把「多寫了一行檢查」直接等同於「執行期多付一份成本」。
//         現代最佳化器會做值域分析：只要它能證明檢查恆真，
//         那行程式碼就不會產生任何機器碼。抽象的成本不是看原始碼算的，
//         要看編譯器最後吐出什麼。
//
// ⚠️ 陷阱 B. 「v[i] 越界會回傳垃圾值或崩潰，所以我加個
//         `if (v[i] != 0)` 就能偵測出越界」——這個想法錯在哪？
//     答：錯在把未定義行為當成「某種可預測的壞結果」。
//         UB 的意思是【標準對此完全不做任何規定】：
//         可能讀到垃圾、可能崩潰、可能靜默改寫相鄰變數、
//         也可能因為編譯器假設「UB 不會發生」而把你的檢查
//         整段最佳化掉——你的 if 根本不會被執行。
//         越界不可能靠事後檢查偵測，只能事前確保索引合法
//         （用 at()、或先比對 size()）。
//     為什麼會錯：把 UB 想成「執行期會發生的某件壞事」，
//         但它其實是「編譯期的一個假設」。編譯器有權假設你的程式
//         沒有 UB，並基於這個假設重寫程式碼。
//         這就是為什麼 UB 造成的 bug 常常在換編譯器或加了 -O2 之後
//         才突然爆出來。
// ═══════════════════════════════════════════════════════════════════════════

#include <vector>
#include <iostream>
#include <chrono>
#include <stdexcept>
#include <string>

// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】—— 本檔刻意不放
//   理由：LeetCode 保證輸入合法，索引越界一律代表解法寫錯，
//   所以那裡永遠用 operator[]，沒有選擇的餘地——
//   「該用 [] 還是 at()」這個決策在 LeetCode 上根本不存在。
//   而本檔的另一半（micro-benchmark 方法學：最佳化等級、
//   防止 dead code elimination、計時時鐘的選擇）更是離題。
//   這個主題真正的用武之地是下方的實務範例。
// -----------------------------------------------------------------------------

// -----------------------------------------------------------------------------
// 【日常實務範例】同一份程式裡，兩種存取方式各司其職
//   情境：一個影像處理服務。外部送進來的是「未經驗證的參數」
//         （要處理的矩形區域），內部則是「已驗證後的緊迴圈」。
//   為什麼用到本主題：這是 [] 與 at() 分工最典型的樣子——
//     ① 邊界：用 at()（或明確檢查）擋下非法輸入，把錯誤變成
//        可處理的例外，而不是 UB。
//     ② 核心：驗證通過後，迴圈上界已由 size() 保證，
//        內層就該用 operator[]，不必為每個像素重複付檢查成本。
//   把兩者混用不是不一致，而是「在正確的地方付正確的成本」。
// -----------------------------------------------------------------------------
class GrayImage {
public:
    GrayImage(size_t w, size_t h, unsigned char fill)
        : w_(w), h_(h), px_(w * h, fill) {}

    size_t width()  const { return w_; }
    size_t height() const { return h_; }

    // 邊界層：參數來自外部，一律驗證。用 at() 讓越界變成可捕捉的例外。
    unsigned char pixelChecked(size_t x, size_t y) const {
        if (x >= w_ || y >= h_) {
            throw std::out_of_range("pixel (" + std::to_string(x) + "," +
                                    std::to_string(y) + ") 超出影像範圍");
        }
        return px_.at(y * w_ + x);
    }

    // 核心層：迴圈上界由 w_/h_ 保證，索引一定合法 → 用 operator[]
    // 回傳整張圖的亮度總和。這是每張圖跑 w*h 次的熱路徑。
    long long totalBrightness() const {
        long long sum = 0;
        for (size_t y = 0; y < h_; ++y) {
            const size_t row = y * w_;
            for (size_t x = 0; x < w_; ++x) {
                sum += px_[row + x];     // 不檢查：索引已由迴圈條件保證
            }
        }
        return sum;
    }

    // 外部 API：先驗證整個矩形，通過後內層就能安全地用 []
    long long regionBrightness(size_t x0, size_t y0, size_t w, size_t h) const {
        // 一次把邊界檢查做完（注意用加法避免無號環繞）
        if (w == 0 || h == 0) return 0;
        if (x0 >= w_ || y0 >= h_ || x0 + w > w_ || y0 + h > h_) {
            throw std::out_of_range("region 超出影像範圍");
        }
        long long sum = 0;
        for (size_t y = y0; y < y0 + h; ++y) {
            const size_t row = y * w_;
            for (size_t x = x0; x < x0 + w; ++x) {
                sum += px_[row + x];     // 已驗證，這裡不必再檢查
            }
        }
        return sum;
    }

private:
    size_t w_, h_;
    std::vector<unsigned char> px_;
};

int main() {
    // 2000 萬次存取。原版用 1 億次，在 -O0 下要跑很久；
    // 縮小規模不影響結論（我們看的是兩者的相對關係）。
    const int N = 20000000;
    std::vector<int> v(1000);

    // 填入資料
    for (int i = 0; i < 1000; ++i) {
        v[i] = i;
    }

    long long sum1 = 0, sum2 = 0;

    // 測試 operator[]
    // 用手動回繞的索引取代 i % 1000：取模是除法，很貴，
    // 會把我們真正想量的東西淹沒掉。
    auto start1 = std::chrono::steady_clock::now();
    {
        size_t idx = 0;
        for (int i = 0; i < N; ++i) {
            sum1 += v[idx];
            if (++idx == 1000) idx = 0;
        }
    }
    auto end1 = std::chrono::steady_clock::now();

    // 測試 at()
    auto start2 = std::chrono::steady_clock::now();
    {
        size_t idx = 0;
        for (int i = 0; i < N; ++i) {
            sum2 += v.at(idx);
            if (++idx == 1000) idx = 0;
        }
    }
    auto end2 = std::chrono::steady_clock::now();

    auto duration1 = std::chrono::duration_cast<std::chrono::milliseconds>(end1 - start1);
    auto duration2 = std::chrono::duration_cast<std::chrono::milliseconds>(end2 - start2);

    // 耗時是非決定性的 → 走 stderr，不列入預期輸出
    std::cerr << "[計時 | 每次執行都不同，故不列入預期輸出]\n";
    std::cerr << "  operator[]: " << duration1.count() << " ms\n";
    std::cerr << "  at():       " << duration2.count() << " ms\n";
    std::cerr << "  本機參考值：-O0 約 90 / 190 ms（量到的是函式呼叫開銷），\n";
    std::cerr << "              -O2 約 13 / 13 ms（檢查被最佳化器證明多餘而刪除）。\n";

    std::cout << "=== 量測設定（可重現）===\n";
    std::cout << "存取次數: " << N << "\n";
    std::cout << "陣列大小: " << v.size() << "（刻意很小，讓資料留在 L1 快取）\n";
    std::cout << "耗時已輸出到 stderr —— 它每次執行都不同，不適合當預期輸出\n";

    // 防止編譯器優化掉：結果必須被使用
    std::cout << "\n=== 結果驗證（決定性）===\n";
    std::cout << "sum1 (operator[]) = " << sum1 << "\n";
    std::cout << "sum2 (at())       = " << sum2 << "\n";
    std::cout << "兩者相同: " << std::boolalpha << (sum1 == sum2) << "\n";
    std::cout << "（把 sum 印出來是必要的：若結果沒被使用，\n";
    std::cout << "  最佳化器會直接刪掉整個迴圈，量到假的 0 毫秒）\n";

    std::cout << "\n=== 越界時兩者的行為 ===\n";
    {
        std::cout << "v.at(99999) -> ";
        try {
            std::cout << v.at(99999) << "\n";
        } catch (const std::out_of_range& e) {
            std::cout << "丟出 std::out_of_range\n";
            std::cout << "  訊息: " << e.what() << "\n";
        }
        std::cout << "v[99999]    -> 未定義行為：可能讀到垃圾、可能崩潰、\n";
        std::cout << "               也可能因為編譯器假設 UB 不存在而讓你的\n";
        std::cout << "               檢查程式碼被整段最佳化掉。本檔不示範。\n";
    }

    std::cout << "\n=== 日常實務：邊界用 at()，核心迴圈用 [] ===\n";
    {
        GrayImage img(64, 48, 100);      // 64x48 全部填 100
        std::cout << "影像尺寸: " << img.width() << "x" << img.height() << "\n";
        std::cout << "像素總數: " << (img.width() * img.height()) << "\n";

        // 核心：熱路徑，用 operator[]
        long long total = img.totalBrightness();
        std::cout << "全圖亮度總和 (內層用 []): " << total << "\n";
        std::cout << "  驗算 64*48*100 = " << (64 * 48 * 100)
                  << " -> 相符: " << (total == 64 * 48 * 100) << "\n";

        // 邊界：合法請求
        long long region = img.regionBrightness(10, 10, 8, 8);
        std::cout << "區域 (10,10,8x8) 亮度總和: " << region
                  << "（= 8*8*100 = " << (8 * 8 * 100) << "）\n";

        // 邊界：非法請求 —— 變成可捕捉的例外，不是 UB
        std::cout << "\n非法請求的處理：\n";
        try {
            img.pixelChecked(100, 10);
        } catch (const std::out_of_range& e) {
            std::cout << "  pixelChecked(100,10) -> " << e.what() << "\n";
        }
        try {
            img.regionBrightness(60, 40, 20, 20);
        } catch (const std::out_of_range& e) {
            std::cout << "  regionBrightness(60,40,20x20) -> " << e.what() << "\n";
        }
        std::cout << "\n→ 外部參數在邊界層被擋下並轉成例外（上層可回 HTTP 400）；\n";
        std::cout << "  驗證通過後，內層每個像素都用 [] 存取，不重複付檢查成本。\n";
        std::cout << "  這不是「不一致」，而是在正確的地方付正確的成本。\n";
    }

    return 0;
}

