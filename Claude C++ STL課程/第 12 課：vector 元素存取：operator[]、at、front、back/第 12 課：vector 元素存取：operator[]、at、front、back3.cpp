// =============================================================================
//  第 12 課 (3)  —  vector::at()：唯一有明確定義的越界行為
// =============================================================================
//
// 【主題資訊 Information】
//   reference       at(size_type pos);        // 非 const 版本
//   const_reference at(size_type pos) const;  // const 版本
//
//   標頭檔：<vector>（例外型別定義在 <stdexcept>）
//   標準版本：C++98 起即有；C++20 起加上 constexpr
//   複雜度：O(1)
//   越界行為：若 pos >= size()，**保證**擲出 std::out_of_range
//   回傳：元素的參考（與 operator[] 相同）
//
// 【詳細解釋 Explanation】
//
// 【1. at() 存在的唯一理由：把 UB 換成可預測的錯誤】
// operator[] 越界是未定義行為 —— 標準不做任何承諾（見本課第 2 檔）。
// at() 則相反：它是整個 vector 介面中，**唯一對越界給出明確保證的存取方式**。
// 這個保證讓錯誤變成「可處理的事件」而不是「不可知的災難」：
//   * 它一定會被偵測（不受最佳化等級影響）
//   * 它一定擲出 std::out_of_range（可以 catch、可以記 log、可以回覆錯誤訊息）
//   * 它一定發生在越界的那一刻（不會延後到別的地方才爆炸）
//
// 【2. 什麼時候該用 at()？判準是「索引的來源」】
// 不是「安全的用 at()、快的用 []」這麼粗糙，真正的判準是：
//   **這個索引是我自己算出來的，還是別人給我的？**
//
//   索引來源                                   建議
//   ──────────────────────────────────────────────────────────
//   for (i = 0; i < v.size(); ++i) 的迴圈變數   operator[]（已由迴圈保證）
//   剛做完 if (i < v.size()) 的分支內           operator[]（已驗證，別重複檢查）
//   使用者輸入 / 命令列參數                      at()
//   設定檔、JSON、CSV 欄位編號                   at()
//   網路封包裡的 offset / index                  at()（這類欄位是攻擊面）
//   另一個函式回傳、你沒把握其範圍的值            at()
//
// 【3. at() 的成本：一次比較 + 一個「幾乎不會走」的分支】
// libstdc++ 的實作大致是：
//   if (__n >= this->size())
//       __throw_out_of_range_fmt(...);      // 冷路徑
//   return (*this)[__n];
// 成本是一次比較與一個條件跳轉。因為越界幾乎不會發生，
// 分支預測器（branch predictor）幾乎 100% 命中，**單次呼叫的實際開銷極低**。
//
// 但在「熱迴圈」中它有一個較隱晦的代價：那個 throw 分支讓迴圈主體有了
// 額外的出口，可能**阻礙編譯器做自動向量化（auto-vectorization）與展開**。
// 這通常才是 at() 在數值密集迴圈中變慢的主因，而不是那一次比較本身。
// 詳細的量測與討論見本課第 8 檔。
//
// 【4. 例外訊息的內容】
// libstdc++ 產生的 what() 訊息包含實際數值，對除錯非常有用：
//   vector::_M_range_check: __n (which is 10) >= this->size() (which is 3)
// 注意這段文字是**實作定義**的：標準只要求擲出 std::out_of_range，
// 並未規定訊息內容。libc++ 與 MSVC 的訊息都不一樣，
// 因此**絕對不要用字串比對去解析它**，只能拿來給人看。
//
// 【概念補充 Concept Deep Dive】
// 為什麼是 std::out_of_range 而不是回傳錯誤碼？
// at() 的介面必須回傳 reference（才能支援 v.at(0) = 5 的寫入語意），
// 而 reference 沒有「無效值」可以表示失敗 —— 不像指標可以回 nullptr。
// 在「必須回傳參考」與「必須能表示失敗」的夾擊下，例外是唯一的出路。
//
// 這也解釋了為什麼 C++ 後來會出現第三條路：
//   * std::optional<T>（C++17）：回傳「值或空」，呼叫端必須顯式處理 —— 見第 9 檔
//   * std::expected<T,E>（C++23）：回傳「值或錯誤」，帶錯誤原因
// 三者的取捨是：at() 適合「越界=程式邏輯錯誤」，
// optional/expected 適合「查不到是正常業務情況」。
//
// 【注意事項 Pay Attention】
// 1. at() 只檢查「索引越界」，不檢查「容器已被改動」。
//    v.at(0) 取得的參考在 push_back 擴容後一樣會失效（見第 11 檔）。
// 2. 例外訊息文字是實作定義，不可用於程式邏輯判斷。
// 3. at() 不會保護你免於「負數索引」—— 傳入 -1 會先被轉成極大的無號數，
//    然後正確地被判定為越界並擲出例外（結果是對的，但理由和你想的不同）。
// 4. 若專案關閉例外（-fno-exceptions），at() 越界會直接呼叫 std::terminate。
//    嵌入式 / 遊戲主機專案常見此設定，此時 at() 的價值大幅下降。
// 5. 不要在已經驗證過範圍的地方再用 at()（重複檢查），
//    也不要在完全沒驗證的地方用 [] —— 這兩種都是常見的誤用。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】vector::at()
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. at() 和 operator[] 差在哪？你在專案中怎麼選？
//     答：at() 做邊界檢查、越界保證擲出 std::out_of_range；
//         operator[] 不檢查、越界是未定義行為。
//         選擇的判準是「索引的來源」：自己迴圈產生或剛驗證過的用 []；
//         來自使用者輸入、設定檔、網路封包等外部來源的用 at()。
//     追問：at() 慢多少？→ 單次成本是一次比較加一個幾乎不會走的分支，
//         分支預測近乎全中，開銷極小；真正的代價是那個 throw 出口
//         可能阻礙熱迴圈的自動向量化。
//
// 🔥 Q2. 為什麼 at() 越界要用「擲例外」而不是回傳錯誤碼或特殊值？
//     答：因為 at() 必須回傳 reference（才能支援 v.at(0) = 5），
//         而 reference 沒有可用來表示「失敗」的無效值。
//         在「回傳參考」與「表示失敗」的衝突下，例外是唯一選擇。
//     追問：那有沒有不用例外的替代？→ 有，自己包一層回傳
//         std::optional<T>（C++17）或 std::expected<T,E>（C++23），
//         適合「查不到屬於正常業務情況」的場景。
//
// ⚠️ 陷阱. 「at() 有邊界檢查，所以用 at() 拿到的參考就是安全的」—— 哪裡錯了？
//     答：at() 只保證「取得參考的那一瞬間索引合法」，
//         完全不保證這個參考之後還有效。
//             int& r = v.at(0);
//             v.push_back(42);      // 可能擴容 → 整塊記憶體搬家
//             r = 99;               // 未定義行為！at() 一點忙都幫不上
//     為什麼會錯：把「邊界檢查」誤當成「生命週期保護」。
//         這是兩個完全不同的問題：at() 解決的是索引範圍，
//         而參考失效問題要靠「不要在持有參考時修改容器」來解決。
// ═══════════════════════════════════════════════════════════════════════════

#include <vector>
#include <iostream>
#include <stdexcept>
#include <string>

// -----------------------------------------------------------------------------
// 【日常實務範例】以 at() 讀取設定表，把「設定檔寫錯」變成清楚的錯誤訊息
//   情境：服務啟動時讀取設定，其中 retry_level 是使用者在設定檔裡填的數字，
//         我們要用它去查「重試等待秒數表」。使用者完全可能填 0、9、-1，
//         這是**外部輸入**，絕不能直接拿去做 operator[]。
//   為什麼用 at()：越界時我們要的是一句「設定檔第 N 項超出範圍」的清楚訊息，
//         而不是讀到一塊垃圾記憶體、拿著錯誤的等待秒數繼續跑。
//         把 UB 轉成可捕捉的例外，才有辦法給出可行動的錯誤訊息。
// -----------------------------------------------------------------------------
int lookupBackoffSeconds(const std::vector<int>& table, int userLevel) {
    try {
        // userLevel 來自設定檔，先轉成 size_type 再交給 at() 檢查。
        // 注意：負數會轉成極大的無號數，一樣會被 at() 判定為越界。
        return table.at(static_cast<std::vector<int>::size_type>(userLevel));
    } catch (const std::out_of_range&) {
        std::cout << "  [設定錯誤] retry_level = " << userLevel
                  << " 超出範圍（合法值 0.." << table.size() - 1
                  << "），改用預設值 " << table.front() << " 秒\n";
        return table.front();          // 降級成預設值，服務照樣能啟動
    }
}

// -----------------------------------------------------------------------------
// 【日常實務範例 2】解析固定欄位的 CSV 列，用 at() 擋掉「欄位數不足」的髒資料
//   情境：批次匯入 CSV，格式應為「日期,產品,數量,單價」共 4 欄。
//         真實世界的 CSV 一定會有缺欄位的爛資料（多一個換行、少一個逗號）。
//   為什麼用 at()：欄位數量取決於檔案內容（外部輸入），
//         用 fields[3] 遇到只有 2 欄的列就是直接 UB；
//         用 at() 則能明確捕捉，記錄「第幾列格式錯誤」後跳過，繼續處理其他列。
// -----------------------------------------------------------------------------
bool parseOrderRow(const std::vector<std::string>& fields, int lineNo) {
    try {
        const std::string& date    = fields.at(0);
        const std::string& product = fields.at(2);   // 故意跳著取，示範任意索引
        std::cout << "  第 " << lineNo << " 列 OK: 日期=" << date
                  << " 產品=" << product << "\n";
        return true;
    } catch (const std::out_of_range& e) {
        std::cout << "  第 " << lineNo << " 列格式錯誤（只有 " << fields.size()
                  << " 欄，需要至少 3 欄），已跳過\n";
        std::cout << "    底層訊息: " << e.what() << "\n";
        return false;
    }
}

int main() {
    std::cout << "=== at() 正常存取（與 operator[] 行為相同）===\n";
    std::vector<int> v = {10, 20, 30};
    std::cout << "v.at(0) = " << v.at(0) << "\n";
    std::cout << "v.at(2) = " << v.at(2) << "\n";

    std::cout << "\n=== at() 回傳參考，可以寫入 ===\n";
    v.at(1) = 200;
    std::cout << "v.at(1) = 200 之後，v[1] = " << v[1] << "\n";

    std::cout << "\n=== at() 越界：保證擲出 std::out_of_range ===\n";
    try {
        std::cout << v.at(10) << "\n";
        std::cout << "（這行不會被執行）\n";
    } catch (const std::out_of_range& e) {
        std::cout << "捕捉到 std::out_of_range\n";
        std::cout << "e.what() = " << e.what() << "\n";
        std::cout << "（訊息文字屬於實作定義，不可用於程式判斷）\n";
    }

    std::cout << "\n=== 與 operator[] 的關鍵差異 ===\n";
    std::cout << "at()        越界 → 一定擲出例外，不受 -O 等級影響\n";
    std::cout << "operator[]  越界 → 未定義行為，-O0 與 -O2 表現完全不同\n";

    std::cout << "\n=== 日常實務：設定檔 retry_level 查表 ===\n";
    const std::vector<int> backoff = {1, 2, 5, 15, 60};   // 合法 level: 0..4
    std::cout << "level=2 -> 等待 " << lookupBackoffSeconds(backoff, 2) << " 秒\n";
    std::cout << "level=9 -> ";
    std::cout << "等待 " << lookupBackoffSeconds(backoff, 9) << " 秒\n";
    std::cout << "level=-1 -> ";
    std::cout << "等待 " << lookupBackoffSeconds(backoff, -1) << " 秒\n";

    std::cout << "\n=== 日常實務：CSV 匯入的髒資料防禦 ===\n";
    parseOrderRow({"2024-05-18", "widget", "A-100", "30"}, 1);
    parseOrderRow({"2024-05-19", "gadget"}, 2);            // 欄位不足的爛資料
    parseOrderRow({"2024-05-20", "bolt", "B-200", "12"}, 3);

    return 0;
}

