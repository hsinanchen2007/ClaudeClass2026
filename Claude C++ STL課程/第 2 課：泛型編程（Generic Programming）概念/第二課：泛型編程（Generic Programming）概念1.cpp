// =============================================================================
//  第二課：泛型編程（Generic Programming）概念1.cpp
//   —  沒有泛型的世界：為什麼「一種型別寫一個函式」會失控
// =============================================================================
//
// 【主題資訊 Information】
//   本檔沒有用到任何 template，示範的是「泛型出現之前」的寫法：
//
//     void swap_int(int& a, int& b);          // 只服務 int
//     void swap_double(double& a, double& b); // 只服務 double
//
//   標準版本：C++98 起即可編譯（本檔不依賴任何新特性）
//   標頭檔  ：<iostream>
//   複雜度  ：swap 本身 O(1)；真正的成本在「人要重複寫 N 次」
//
//   對照組：std::swap 是一個函式模板，一份定義服務所有型別。
//           它宣告於 <utility>（C++11 起；C++98 時位於 <algorithm>）。
//
// 【詳細解釋 Explanation】
//
// 【1. 這裡真正的問題不是「打字很累」】
// 初學者看到本檔的直覺是「重複寫很煩」。但重複打字只是表面症狀，真正的代價是
// 三個會隨專案長大而放大的問題：
//
//   (a) 修改成本 O(N)：假設之後發現 swap 應該改用 std::move 以避免深拷貝，
//       你必須把「每一個」swap_xxx 都改一次。漏改一個就留下一個效能不一致的
//       角落，而編譯器不會提醒你。
//   (b) 正確性漂移：swap_int 與 swap_double 今天邏輯相同，但它們是兩份獨立的
//       原始碼。人手維護兩份「應該相同」的程式碼，長期必然分岔。
//   (c) 不可擴充：函式庫作者無法預先知道使用者會定義什麼型別。你寫不出
//       swap_UserDefinedMatrix —— 因為那個型別在你發佈函式庫時還不存在。
//
// (c) 才是決定性的。泛型編程的核心價值不是「少打字」，而是
// 「讓演算法能作用在**作者當初不知道其存在**的型別上」。STL 的 sort 能排序你
// 今天下午才定義的 struct，靠的就是這件事。
//
// 【2. 為什麼函式重載（overloading）解決不了】
// 有人會說：把它們都命名為 swap，用重載不就好了？
//
//     void swap(int&, int&);
//     void swap(double&, double&);
//
// 呼叫端確實變乾淨了（都寫 swap(a, b)），但**函式體仍然是 N 份**。重載改善的
// 是「命名」，不是「重複」。上面 (a)(b)(c) 三個問題一個都沒解決 —— 重載讓
// 呼叫者輕鬆，template 讓實作者輕鬆，兩者解決的是不同層面的問題。
//
// 【3. 用 void* 或巨集呢？】
// C 的傳統解法是這兩條路，它們正好凸顯 template 的價值：
//   * void* + memcpy：型別資訊全部丟失，編譯器無法檢查，傳錯大小就是 UB；
//     且對非 trivially-copyable 的型別（如 std::string）會直接破壞物件不變式。
//   * 前處理器巨集 #define SWAP(T,a,b)：純文字替換，沒有型別檢查、不遵守作用域、
//     錯誤訊息指向展開後的位置，且參數可能被求值多次。
// template 常被戲稱為「有型別檢查的巨集」，關鍵差別就在**有型別檢查**：
// 它在編譯器的語意分析階段展開，不是在文字階段。
//
// 【概念補充 Concept Deep Dive】
// 編譯器對本檔做的事非常單純：swap_int 與 swap_double 是兩個獨立函式，各自
// 產生一份機器碼、各自有一個 mangled 名稱（C++ 連結會修飾名稱，本機 g++ 15.2
// 實測形如 _Z8swap_intRiS_）。
//
// 這與下一個檔案的 template 有個關鍵差異：本檔的兩個函式是 **strong symbol**
// （全域唯一，兩個 TU 各定義一次就是 link error）；而 template 實例化出來的
// 函式是 **weak symbol / COMDAT**（允許多個 TU 各產生一份，由 linker 合併留一份）。
// 這個差別正是「template 定義可以放在 header、卻不會違反 ODR」的根本原因，
// 本課 summary.cpp 會完整展開。
//
// 【注意事項 Pay Attention】
// 1. 本檔可以正常編譯執行，它不是錯誤示範，而是「還沒用泛型」的對照組。
// 2. 自己寫 swap 時，正式程式碼應該用 std::move：
//        T temp = std::move(a); a = std::move(b); b = std::move(temp);
//    否則對 std::string / std::vector 這類型別會做三次深拷貝。本檔為了聚焦於
//    「重複」這個主題，維持最單純的寫法。
// 3. 標準函式庫已提供 std::swap，實務上幾乎不需要自己寫。自訂型別要支援高效
//    交換時，慣例是在該型別所屬的 namespace 提供 swap 多載，呼叫端寫
//    `using std::swap; swap(a, b);`（此慣例稱為 two-step swap / ADL swap）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】為什麼需要泛型編程
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 泛型編程解決的核心問題是什麼？「少寫重複程式碼」是完整答案嗎？
//     答：不是完整答案，只答到表層。核心是「讓演算法作用在作者事先不知道的
//         型別上」—— 函式庫作者無法窮舉使用者未來會定義的型別，重載與複製貼上
//         在本質上都做不到這件事。少打字只是附帶效果。
//     追問：那 std::sort 憑什麼能排序我自訂的 struct？
//         → 因為它只要求元素滿足「可移動、可用 < 比較」這組隱含介面，
//           不要求繼承任何基底類別；實際型別在編譯期才被代入。
//
// 🔥 Q2. 函式重載和函式模板差在哪？既然有重載了，為什麼還需要模板？
//     答：重載讓「呼叫端」統一（都叫 swap），但函式體仍是 N 份，維護成本與
//         擴充性問題原封不動。模板讓「實作端」統一 —— 一份定義，編譯器按需
//         為每個實際用到的型別各生成一份程式碼。
//     追問：兩者可以並存嗎？
//         → 可以，而且很常見。overload resolution 的規則是：當非模板函式與
//           模板實例化結果同樣匹配（轉換代價一樣好）時，**非模板函式優先**。
//           這正是「為特例手寫一個更快版本」的標準做法。
//
// ⚠️ 陷阱. 用巨集 #define SWAP(T, a, b) 也能「一份程式碼服務多型別」，
//          那 template 是不是只是語法糖？
//     答：不是。巨集在前處理階段做純文字替換，沒有型別檢查、不遵守作用域、
//         參數可能被求值多次（SWAP(T, i++, j) 就壞了），錯誤訊息指向展開後的
//         位置而難以除錯。template 在語意分析階段展開，完整參與型別檢查、
//         多載決議與 ADL。
//     為什麼會錯：把「都會展開成多份程式碼」當成「機制相同」。
//         兩者展開的**階段**不同（前處理 vs 語意分析），而型別安全正是由後者
//         提供的 —— 這個階段差異就是全部的差別。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>

// 交換兩個 int
void swap_int(int& a, int& b) {
    int temp = a;
    a = b;
    b = temp;
}

// 交換兩個 double
void swap_double(double& a, double& b) {
    double temp = a;
    a = b;
    b = temp;
}

// 交換兩個 string... 還要再寫一次？
// —— 這行註解就是本課的起點：每多一個型別，就要多一份幾乎相同的程式碼。
//    下一個檔案（概念2.cpp）用一個 template 取代全部。

int main() {
    std::cout << "=== 非泛型版本：每種型別各寫一個函式 ===" << std::endl;

    int x = 10, y = 20;
    swap_int(x, y);
    std::cout << "x = " << x << ", y = " << y << std::endl;

    double a = 3.14, b = 2.72;
    swap_double(a, b);
    std::cout << "a = " << a << ", b = " << b << std::endl;

    std::cout << "\n=== 那 std::string 呢？ ===" << std::endl;
    std::cout << "得再寫一個 swap_string，每個新型別都要重來一次。" << std::endl;
    std::cout << "而使用者自訂的型別，函式庫作者根本無法預先寫出來。" << std::endl;

    return 0;
}

