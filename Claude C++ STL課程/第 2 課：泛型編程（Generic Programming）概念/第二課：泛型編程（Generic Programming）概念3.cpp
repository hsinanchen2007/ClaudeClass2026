// =============================================================================
//  第二課：泛型編程（Generic Programming）概念3.cpp
//   —  隱含介面（implicit interface）：模板對型別的真正要求是什麼
// =============================================================================
//
// 【主題資訊 Information】
//   語法：
//     template <typename T>
//     T find_max(T a, T b);          // 兩個參數共用同一個 T
//
//   標準版本：C++98 起（本檔用 -std=c++17 編譯，未依賴新語法）
//   標頭檔  ：<iostream>、<string>
//   複雜度  ：一次比較 O(1)；但對 std::string，operator> 是逐字元比較，
//             最壞 O(min(len1, len2))，且**按值傳參會額外複製字串**
//
//   對照：標準庫的 std::max 定義於 <algorithm>，語意相同但回傳 const T&，
//         另有接受自訂比較器的重載 std::max(a, b, comp)。
//
// 【詳細解釋 Explanation】
//
// 【1. 模板沒有寫出來的那份「合約」】
// find_max 的宣告裡完全沒提到型別 T 需要具備什麼能力，但函式本體寫了
// `a > b`。這句話等於偷偷加上了一條要求：
//
//     「T 必須支援 operator>，且結果要能當條件式用（可轉成 bool）」
//
// 這種「沒有寫在型別系統裡、但實際存在」的要求，叫做**隱含介面**
// （implicit interface）。C++ 的模板是**編譯期的鴨子型別**（duck typing）：
// 不管你是什麼型別，只要「會 > 這個動作」，就能被 find_max 接受。
//
// 對照物件導向的**明確介面**（explicit interface）：那邊要求你先繼承某個
// 基底類別、實作某個虛擬函式，關係寫在型別宣告裡、一眼可見。
// 兩者的取捨是本課後段（概念11 vs 12）的主題：
//   * 明確介面：關係清楚、可執行期抽換，代價是虛擬呼叫與繼承綁定
//   * 隱含介面：零執行期成本、不需侵入型別定義，代價是要求不可見、
//               錯誤訊息在實例化時才爆炸
//
// 【2. 這個隱含要求何時被檢查？（兩階段查找）】
// 關鍵觀念：模板本體**不是**在定義時就完整檢查的。C++ 的模板編譯分兩階段：
//   * 第一階段（定義時）：檢查與 T 無關的語法錯誤。此時 `a > b` 因為 T 未知，
//     編譯器只能先擱著。
//   * 第二階段（實例化時）：T 已確定，才真正檢查 `a > b` 是否合法。
// 所以「模板寫錯了但沒人用它，就不會報錯」是真的。這也解釋了為什麼模板的
// 錯誤訊息總是指向**呼叫的那一行**，並附上一長串 instantiation backtrace ——
// 錯誤是在代入型別之後才被發現的。概念8.cpp 會直接示範這個爆炸現場。
//
// 【3. 為什麼 std::string 可以直接比大小】
// find_max(s1, s2) 能運作，是因為 std::string 有定義 operator>，語意是
// **字典序**（lexicographical order）：逐字元比較字元的數值，第一個不同的
// 字元決定大小；若某一方是另一方的前綴，較短者為小。
// 所以 "apple" < "banana"（'a' < 'b' 就分出勝負了）。
//
// 注意字典序不等於「人類的字母順序」：它比較的是字元編碼值。
// 在 ASCII 中大寫字母（65~90）全部小於小寫字母（97~122），
// 因此 "Zebra" < "apple" 成立。要做不分大小寫或依語系的排序，
// 必須自行提供比較器或使用 <locale> 的定序機制。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 按值傳參 T 的代價
//   本檔簽名是 `T find_max(T a, T b)` —— 兩個參數都按值傳遞。對 int/char
//   這種小型別完全沒問題（放暫存器裡最快）。但傳 std::string 時，每次呼叫
//   都會複製兩個字串，回傳時再複製一次。泛型程式碼的慣用寫法是：
//
//       template <typename T>
//       const T& find_max(const T& a, const T& b) { return a > b ? a : b; }
//
//   這正是 std::max 的簽名形式（回傳 const T&）。但回傳參考有個著名陷阱：
//   若拿它去接一個暫時物件，例如 `const auto& m = std::max(f(), g());`，
//   當引數是暫時物件時，回傳的參考在敘述結束後就懸空了（dangling）。
//   本檔維持按值版本，是為了讓初學者聚焦在「隱含介面」而非生命週期。
//
// (B) 為什麼是 a > b 而不是 b < a？
//   標準庫的慣例是**只要求 operator<**（strict weak ordering），
//   std::max 內部寫成 `return a < b ? b : a;`。這樣自訂型別只需要提供一個
//   operator<，就能用上 sort、max、min、set、map 等一整套設施。
//   本檔用 > 是為了直觀，但實務上「只依賴 <」是更好的泛型設計習慣 ——
//   對型別作者的要求越少，你的模板適用面越廣。
//
// (C) 為什麼 find_max(10, 20) 回傳的是 int 而不是 const int&
//   T 被推導成 int，回傳型別 T 就是 int，值被複製出來。這裡沒有懸空問題。
//   但如果簽名是 const T&，回傳的是對參數的參考 —— 而參數本身是區域變數，
//   函式結束就消失。這就是為什麼 std::max 的參數也必須是 const T&
//   （參考綁到呼叫端的物件），三者必須一致設計，不能只改回傳型別。
//
// 【注意事項 Pay Attention】
// 1. 兩個參數共用同一個 T，代表兩邊型別必須推導出**完全一致**的結果。
//    find_max(10, 3.14) 會編譯失敗 —— 這是概念4.cpp 的主題。
// 2. 對 C 字串（const char*）呼叫 find_max 是個經典陷阱：
//    T 會被推導成 const char*，`a > b` 比較的是**指標位址**而非字串內容，
//    結果沒有意義（兩個無關物件的指標比較，其結果標準未指定）。
//    要比字串內容，請確保傳入的是 std::string。
// 3. 對浮點數比較要留意 NaN：涉及 NaN 的比較全部回傳 false，
//    因此 find_max(nan, x) 會回傳 x，而 find_max(x, nan) 會回傳 nan。
//    這不是 bug，是 IEEE 754 的規定；需要處理 NaN 請改用 std::fmax。
// 4. 隱含介面的要求不可見，是 C++20 concepts 要解決的問題：
//    concepts 讓你把「T 必須支援 >」寫成程式碼的一部分（見概念10.cpp）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】隱含介面與兩階段編譯
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. template <typename T> T find_max(T a, T b) 對型別 T 有什麼要求？
//        這個要求寫在哪裡？
//     答：要求 T 支援 operator> 且結果可轉成 bool（也需可複製建構，因為按值
//         傳參與回傳）。這個要求**沒有寫在任何地方** —— 它只隱含在函式本體
//         的 `a > b` 裡，稱為隱含介面（implicit interface），
//         是編譯期的鴨子型別。
//     追問：那要怎麼把這個要求明講出來？
//         → C++20 用 concepts（見概念10.cpp）；C++20 之前用 static_assert
//           搭配 type traits，或用 std::enable_if 做 SFINAE 約束。
//
// 🔥 Q2. 模板本體裡的錯誤，是在定義模板時就被發現，還是在使用時？
//     答：分兩階段。與 T 無關的語法錯誤在定義時就會報；牽涉 T 的部分
//         （如 a > b）必須等到實例化、T 被確定後才檢查。所以一個從未被
//         呼叫的模板，即使本體對某些型別不成立，也不會產生任何錯誤。
//     追問：這會造成什麼實務問題？
//         → 錯誤在使用端才爆炸，訊息指向呼叫點並附帶整條實例化鏈，
//           往往幾十行且與真正的原因隔了好幾層。這正是 concepts 的動機。
//
// ⚠️ 陷阱. find_max("apple", "banana") 傳兩個字串字面量，會發生什麼事？
//     答：T 被推導成 const char*（陣列會 decay 成指標），所以 `a > b` 比較的
//         是兩個**指標位址**，不是字串內容。它能編譯、能執行，但結果沒有意義
//         —— 比較兩個無關物件的指標，其結果標準並未指定。
//         必須明確傳 std::string（如本檔的 s1、s2）才會做字典序比較。
//     為什麼會錯：以為「字串字面量就是字串」。在 C++ 裡它是 const char[N]，
//         傳入按值參數時 decay 成 const char*，而指標的 > 就只是位址比大小。
//         這正是隱含介面的危險之處：**它沒有報錯，只是安靜地做了另一件事**。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>

// T 的隱含要求：可複製、支援 operator>
template <typename T>
T find_max(T a, T b) {
    return (a > b) ? a : b;
}

int main() {
    std::cout << "=== 同一個模板，四種型別 ===" << std::endl;
    std::cout << "max(10, 20) = " << find_max(10, 20) << std::endl;
    std::cout << "max(3.14, 2.72) = " << find_max(3.14, 2.72) << std::endl;
    std::cout << "max('a', 'z') = " << find_max('a', 'z') << std::endl;

    // 字串比較（字典序）
    std::string s1 = "apple", s2 = "banana";
    std::cout << "max(\"apple\", \"banana\") = " << find_max(s1, s2) << std::endl;

    std::cout << "\n=== 字典序不是人類的字母順序 ===" << std::endl;
    std::string upper = "Zebra", lower = "apple";
    // ASCII 中大寫 'Z'(90) < 小寫 'a'(97)，所以 "apple" 反而較大
    std::cout << "max(\"Zebra\", \"apple\") = " << find_max(upper, lower)
              << "   <- 比的是字元編碼值，不是字母先後" << std::endl;

    std::cout << "\n=== 隱含介面：T 必須支援 operator> ===" << std::endl;
    std::cout << "int、double、char、std::string 都有定義 operator>，所以都能用。"
              << std::endl;
    std::cout << "沒有定義 operator> 的自訂型別會編譯失敗 —— 見概念8.cpp。"
              << std::endl;

    return 0;
}

