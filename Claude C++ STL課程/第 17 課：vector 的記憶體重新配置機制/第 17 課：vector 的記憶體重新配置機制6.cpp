// =============================================================================
//  第 17 課-6：【刻意的錯誤示範】erase 之後繼續 ++it —— 使用失效迭代器
// =============================================================================
//
// ⚠️ 本檔是「刻意示範未定義行為」的教學範例，它會照設計崩潰。
//    請不要「修好」它——它的價值就在於呈現錯誤本身。
//    正確寫法在同課的 7.cpp（接住 erase 的回傳值）與 8.cpp（erase-remove）。
//
// 【主題資訊 Information】
//   錯誤樣式：
//     for (auto it = v.begin(); it != v.end(); ++it)
//         if (條件) v.erase(it);            // ← erase 後 it 已失效，++it 是 UB
//   正確樣式：
//     for (auto it = v.begin(); it != v.end(); )
//         if (條件) it = v.erase(it); else ++it;
//   標準依據：[vector.modifiers] —— erase 使「刪除點及其之後」的
//             iterator / pointer / reference 全部失效
//   標頭檔：<vector>
//
// 【詳細解釋 Explanation】
//
// 【1. 這段程式錯在哪裡（精確地說）】
//   v.erase(it) 執行後發生兩件事：
//     (a) it 之後的所有元素往前搬一格；
//     (b) 依標準規定，it 以及它之後的所有迭代器全部失效。
//   接著 for 迴圈的第三格執行 ++it —— 這是對一個「已失效迭代器」
//   做遞增運算，屬於 undefined behavior。
//   迴圈條件 it != v.end() 同樣是在比較一個失效的值，一樣是 UB。
//   注意錯誤不在「值算錯」，而是從 ++it 那一刻起，
//   整個程式的行為就不再受標準約束。
//
// 【2. 為什麼會表現成越界讀取】
//   直覺上的推演是這樣（但這只是「一種可能的展開」，不是保證）：
//   v = {1,2,3,4,5,6,7,8}，刪掉 2 之後變成 {1,3,4,5,6,7,8}，size 從 8 變 7。
//   it 原本指向索引 1，erase 之後那個位置住的是 3，而 ++it 又把它推到索引 2。
//   於是元素被跳過，迴圈也少走了幾輪。更關鍵的是 v.end() 每刪一次就往前縮，
//   而 it 只會往後走——兩者有可能「錯身而過」，
//   使 it != v.end() 永遠成立，迴圈就一路衝出緩衝區外。
//   本機用 AddressSanitizer 觀察到的正是 heap-buffer-overflow。
//
// 【3. UB 沒有「保證的結果」——這點必須講清楚】
//   本機（x86-64 / GCC 15.2 / libstdc++）實測是 segmentation fault，
//   但這只是「這台機器、這個編譯器、這次建置」的觀察值。
//   換最佳化等級、換標準庫、換平台，都可能變成：安靜地跑完、
//   印出莫名其妙的數字、破壞堆積後在無關的地方崩潰。
//   絕不能寫成「這段程式一定會 segfault」——
//   如果 UB 有保證的行為，它就不叫 undefined 了。
//   反過來說，「在我的機器上跑起來沒事」也完全不能證明程式碼正確。
//
// 【4. 為什麼編譯器不報錯】
//   迭代器失效是執行期的狀態變化，型別系統看不到。
//   對編譯器而言 it 只是個普通的 iterator 物件，++it 語法完全合法。
//   要在執行期抓到它，需要額外的檢查工具：
//     g++ -D_GLIBCXX_DEBUG        （libstdc++ 的 debug 模式，會直接中止並說明）
//     g++ -fsanitize=address      （AddressSanitizer，會報 heap-buffer-overflow）
//   這也是為什麼「開發期一定要開 sanitizer」是條硬規則。
//
// 【概念補充 Concept Deep Dive】
//   ▸ erase 內部到底做了什麼
//       把 [pos+1, end) 的元素逐一 move-assign 到 [pos, end-1)，
//       解構最後一個元素，end_ 指標減一。
//     capacity 完全不變，不會重新配置。所以「刪除點之前」的迭代器
//     其實仍然有效——失效的只有刪除點及其之後。
//   ▸ 為什麼 erase 要回傳迭代器
//     正因為傳進去的那個迭代器一定會失效，標準才讓 erase 回傳
//     「下一個有效位置」，好讓刪除迴圈能繼續寫下去。
//     這個回傳值不是方便，是必要——不接住它就沒有安全的方式繼續走訪。
//   ▸ 為什麼這個 bug 在小資料上可能「看起來正常」
//     若容器只有一兩個元素、或符合條件的元素剛好在最後，
//     it 與 end() 可能剛好在同一輪相遇，迴圈正常結束。
//     這種「小測資會過」的特性，正是它能混進正式環境的原因。
//
// 【注意事項 Pay Attention】
//   1. 本檔是刻意的錯誤示範，會照設計崩潰；請勿修正它。
//   2. UB 沒有保證的結果——不可描述成「一定 segfault」或「一定印出 X」。
//   3. 「在我機器上沒事」不能證明正確；請用 sanitizer 驗證。
//   4. erase 只使「刪除點及其之後」失效，之前的迭代器仍然有效。
//   5. 正確寫法見同課 7.cpp（it = v.erase(it)）與 8.cpp（erase-remove，O(n)）。
//   6. C++20 起可直接用 std::erase_if(v, pred) 一行完成，完全避開這個坑。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】使用失效迭代器
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 這段程式碼錯在哪？請精確說明是哪一個運算式造成 undefined behavior。
//        for (auto it = v.begin(); it != v.end(); ++it)
//            if (*it % 2 == 0) v.erase(it);
//     答：v.erase(it) 依標準使 it（及其之後的迭代器）失效，
//         接著 for 第三格的 ++it 是在遞增一個失效迭代器 → UB；
//         下一輪的 it != v.end() 也是在比較失效值 → 同樣 UB。
//         錯誤不是「結果不對」，而是從 ++it 起程式行為不再受標準約束。
//     追問：那要怎麼改？
//         → 把 ++it 移出 for 第三格，改成
//           if (要刪) it = v.erase(it); else ++it;
//           接住 erase 回傳的「下一個有效位置」。
//
// 🔥 Q2. 為什麼編譯器不會對這段程式碼報錯或警告？
//     答：迭代器是否失效是執行期的狀態，型別系統無從得知。
//         對編譯器而言 it 只是個 iterator 物件，++it 語法完全合法。
//         要在執行期抓到，得靠 -D_GLIBCXX_DEBUG 或 -fsanitize=address
//         這類額外的檢查機制。
//     追問：那 -Wall -Wextra 有用嗎？
//         → 沒用。這兩個開關是編譯期的靜態警告，
//           對「執行期物件狀態」無能為力。這也是為什麼
//           光靠 -Wall 不足以保證多數記憶體錯誤被攔下。
//
// ⚠️ 陷阱. 「我在自己機器上跑過，它沒有崩潰，所以這樣寫其實可以。」
//     答：完全不能這樣推論。undefined behavior 的定義就是
//         「標準不對其行為作任何要求」，包含「這次剛好正常」。
//         同一份原始碼換個最佳化等級、換個標準庫版本，
//         或只是資料量變大，行為就可能完全不同。
//     為什麼會錯：把「沒有觀察到症狀」誤當成「沒有錯誤」。
//         UB 最危險的地方正是它經常不會立刻爆炸——
//         它可能默默破壞堆積，然後在幾百行之外一個完全無關的地方崩潰，
//         讓你花好幾天找錯地方。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <vector>

int main() {
    std::vector<int> v = {1, 2, 3, 4, 5, 6, 7, 8};

    std::cout << "刪除前：";
    for (int x : v) std::cout << x << " ";
    std::cout << std::endl;
    std::cout << "接下來執行『erase 後繼續 ++it』的錯誤寫法（未定義行為）..." << std::endl;
    std::cout << std::flush;   // 確保上面的訊息在崩潰前一定已經送出

    // 想刪除所有偶數 —— 錯誤寫法！
    for (auto it = v.begin(); it != v.end(); ++it) {
        if (*it % 2 == 0) {
            v.erase(it);  // erase 之後 it 失效了
            // 接著 ++it 操作一個失效的迭代器 → 未定義行為！
        }
    }

    // 下面這行在本機實測「不會被執行到」，因為程式在迴圈中就已異常結束。
    // 但這同樣是 UB 的觀察結果，不是標準保證——換環境有可能跑到這裡。
    std::cout << "迴圈結束（若你看到這行，代表本次執行的 UB 沒有立即崩潰）" << std::endl;

    return 0;
}

