// =============================================================================
//  第 16 課：vector 的迭代器操作 1  —  迭代器是什麼：泛化的指標
// =============================================================================
//
// 【主題資訊 Information】
//   std::vector<T>::iterator it = v.begin();   // 可讀寫，指向第一個元素
//   *it                                        // 解參考，取得元素
//   ++it / --it                                // 移動到下 / 上一個元素
//   標頭檔：<vector>
//   標準版本：iterator / begin() 自 C++98 就有；auto 型別推導是 C++11
//   複雜度：begin() 為 O(1)；對 vector 而言 *、++、-- 都是 O(1)
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼 STL 需要迭代器，而不是每個容器各自提供 get(i)】
//   假設有 n 種容器與 m 種演算法。若演算法直接依賴容器型別，就得寫 n×m 份
//   實作（sort_vector、sort_list、sort_deque…）。STL 的解法是在中間插入一層
//   共同介面 —— 迭代器：
//       容器   → 負責「產生」迭代器
//       演算法 → 只認識迭代器，完全不認識容器
//   於是實作量降為 n + m。std::sort 從來不知道自己在排 vector 還是原生陣列，
//   它只知道「我收到兩個隨機存取迭代器」。這是 STL 最核心的設計決策。
//
// 【2. 迭代器是「泛化的指標」，不是「一種指標」】
//   迭代器模仿的是指標的「介面」（*、++、--、比較），而不是繼承自指標。
//   任何型別只要提供這組運算子，就能當迭代器用。所以：
//       int arr[5];
//       std::sort(arr, arr + 5);   // 原生指標本身就是合法的隨機存取迭代器
//   指標是迭代器概念的一個特例，反過來並不成立。
//
// 【3. vector<int>::iterator 到底是什麼型別】
//   標準只規定它必須滿足 random access iterator 的需求，「並未規定」它是什麼。
//   libstdc++（本機 g++ 15.2.0）實際上是：
//       __gnu_cxx::__normal_iterator<int*, std::vector<int, std::allocator<int>>>
//   也就是把原生指標包了一層 class。為什麼不直接 typedef 成 int*？
//     (a) 型別安全：包一層後 vector<int>::iterator 與 int* 是不同型別，
//         「接受迭代器」的多載不會被裸指標意外匹配到。
//     (b) 可插入檢查：_GLIBCXX_DEBUG 模式能換成會做邊界／失效檢查的版本，
//         而不必動使用端程式碼。
//     (c) ADL 與特化：class 型別才能安全地特化 iterator_traits、參與 ADL。
//   結論：把它「當成」指標來理解沒問題，但不可以「假設」它就是指標。
//
// 【概念補充 Concept Deep Dive】
//   vector 的元素保證存放在一塊連續記憶體，所以它的迭代器只需要持有一個位址：
//       記憶體： [10][20][30][40][50]
//       位址：    p  p+1 p+2 p+3 p+4
//   ++it 在機器碼層級就是「位址加 sizeof(T)」，最佳化後與手寫指標運算等價
//   （-O2 下通常產生一模一樣的組語）。抽象在此是零成本的 —— 這正是 C++
//   「zero-overhead abstraction」的招牌案例。
//   對照 std::list：節點散落各處，其迭代器內含節點指標，++ 必須走 next 欄位，
//   因此無法支援 it + n 這種跳躍運算（詳見第 6 個檔案）。
//
// 【注意事項 Pay Attention】
//   1. 不要假設 vector<T>::iterator 就是 T*，請用 auto 或完整型別名。
//   2. *v.end() 是未定義行為（UB）—— end() 是哨兵，不指向任何元素。
//   3. 對空 vector，v.begin() == v.end()，此時解參考 begin() 同樣是 UB。
//   4. 迭代器可能因容器改變結構而失效（見第 14 個檔案與 summary.cpp）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】迭代器的本質
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 迭代器和指標有什麼差別？
//     答：指標是語言內建型別；迭代器是一個「概念（concept）」—— 任何提供
//         *、++、比較等運算子的型別都算。原生指標剛好滿足隨機存取迭代器的
//         全部需求，所以指標是迭代器的一個特例；反過來，迭代器不一定是指標
//         （list 的迭代器就不是）。
//     追問：那 std::sort(arr, arr + 5) 為什麼能對原生陣列運作？
//         → 因為 int* 本身就是合法的隨機存取迭代器，std::sort 不需要容器。
//
// 🔥 Q2. std::vector<int>::iterator 是不是 int*？
//     答：標準未規定，不可依賴。libstdc++ 實測是
//         __gnu_cxx::__normal_iterator<int*, vector<int>>，是包了一層的 class；
//         MSVC 在 debug 模式下更是帶檢查資訊的 class。
//         把它當指標「理解」可以，當指標「使用」（宣告成 int*）不行。
//     追問：多包一層不是有額外成本嗎？
//         → 該 class 只含一個指標成員且函式全部 inline，-O2 後與裸指標產生
//           等價機器碼，屬 zero-overhead abstraction。
//
// ⚠️ 陷阱. 迴圈要寫 it++ 還是 ++it？對 vector 有差嗎？
//     答：語意上 it++ 必須先複製一份舊值、遞增、再回傳舊值；++it 直接就地遞增。
//         對 vector 的迭代器（內含一個指標）複製極廉價，且開最佳化後編譯器會
//         把沒用到的回傳值消掉，兩者機器碼通常相同。但對「複製成本高的迭代器」
//         差異是真的，所以 STL 的慣例一律寫 ++it。
//     為什麼會錯：多數人把「++it 比較快」記成放諸四海皆準的效能規則，
//         其實對 vector 是靠最佳化抹平的；它真正的價值是養成一致習慣，
//         讓日後換成重量級迭代器時不會突然變慢。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <vector>

void demo_basic() {
    std::vector<int> v = {10, 20, 30, 40, 50};

    // 用指標的思維來理解迭代器：it 指向第一個元素
    std::vector<int>::iterator it = v.begin();

    std::cout << *it << std::endl;  // 10（解參考，就像指標一樣）
    ++it;
    std::cout << *it << std::endl;  // 20（往前移一個位置）

    // 迭代器持有的是「位置」，透過它可以直接寫回容器
    *it = 99;
    std::cout << "透過迭代器寫入後 v[1] = " << v[1] << std::endl;
}

// -----------------------------------------------------------------------------
// 【日常實務範例】感測器批次讀數：以第一筆樣本當基準值（baseline）
//   情境：溫度感測器每分鐘回傳一批讀數，校正時要整批減掉「第一筆」當基準，
//         以消除感測器的直流偏移（DC offset）。
//   為什麼用到本主題：begin() 取得第一筆的位置，再用同一個迭代器走完整批，
//         且透過 *it 就地寫回，不需要額外的索引變數。
//   注意：必須先檢查非空，否則 *begin() 是 UB。
// -----------------------------------------------------------------------------
std::vector<double> removeBaseline(std::vector<double> samples) {
    if (samples.empty()) return samples;   // 空批次：begin()==end()，不可解參考

    std::vector<double>::iterator it = samples.begin();
    const double baseline = *it;           // 第一筆當基準

    for (; it != samples.end(); ++it) {
        *it -= baseline;                   // 透過迭代器就地修改
    }
    return samples;
}

int main() {
    std::cout << "=== 基本迭代器操作 ===" << std::endl;
    demo_basic();

    std::cout << "\n=== 日常實務：感測器讀數去基準 ===" << std::endl;
    std::vector<double> raw = {21.5, 22.0, 21.8, 23.1, 22.4};
    std::vector<double> fixed = removeBaseline(raw);
    for (double d : fixed) std::cout << d << " ";
    std::cout << std::endl;

    std::vector<double> empty_batch;
    std::cout << "空批次大小 = " << removeBaseline(empty_batch).size()
              << "（安全，未解參考 begin()）" << std::endl;

    return 0;
}

