// =============================================================================
//  第八課 11  —  Lambda 混合捕獲：[=, &b] 與 [&, a]
// =============================================================================
//
// 【主題資訊 Information】
//   語法    : [預設捕獲模式, 例外清單](參數) { 主體 }
//   標準版本: Lambda 為 C++11；本檔語法全部屬於 C++11
//   標頭檔  : 無（lambda 是語言核心特性，不需 #include）
//   規則    : 預設模式只能是 = 或 &；例外項目的捕獲方式必須與預設「不同」
//             [=, &b] 合法（預設值捕獲，b 例外用參考）
//             [&, a]  合法（預設參考捕獲，a 例外用值）
//             [=, b]  非法（b 已經是值捕獲，重複指定）
//             [&, &b] 非法（同理）
//
// 【詳細解釋 Explanation】
//
// 【1. 混合捕獲解決什麼問題】
//   實務上常見的需求是：「大部分變數我只要讀（值捕獲比較安全），但有一兩個
//   我要寫回去（必須參考捕獲）」。如果只能二選一，就得在「全部參考（有懸空
//   風險）」和「全部值捕獲（改不了外部）」之間硬選。混合捕獲讓你精準指定。
//
// 【2. 為什麼「例外必須與預設不同」】
//   若允許 [=, b]，b 的捕獲方式與預設完全一樣，這行程式碼就沒有任何意義，
//   而且極可能是打錯字（本來想寫 &b）。標準直接把它定為 ill-formed，
//   讓編譯器幫你抓錯，而不是默默接受一段無效程式碼。
//
// 【3. 捕獲後在 closure 裡實際變成什麼】
//   [=, &b] 會 desugar 成類似：
//       class __lambda {
//           int a;      // 值捕獲 → 成員副本
//           int& b;     // 參考捕獲 → 成員參考（實作上是指標）
//           int c;      // 值捕獲 → 成員副本
//       public:
//           void operator() () const { b = a + c; }   // const！但 b 是參考，可寫
//       };
//   注意 operator() 是 const，卻仍然能寫 b——因為 const 保護的是「closure 自己
//   的成員」，而 b 這個成員是 int&，const 化之後是 int& const（參考本身不可
//   重綁，但指向的物件可寫）。這正是為什麼值捕獲的 a 不能改、參考捕獲的 b 能改。
//
// 【概念補充 Concept Deep Dive】
//   隱式捕獲（= 或 &）只會捕獲「函數主體中真正用到的」自動儲存期變數。
//   static 變數、全域變數、constexpr 常數都不會被捕獲（它們本來就到處可見），
//   所以 [=] 並不會讓 closure 變大到不合理。
//   本檔 lambda2 寫了 [&, a] 但主體其實沒用到 a——這是「明確列出的例外」，
//   即使沒用到也一定會被捕獲成成員（明確捕獲不受「有用到才捕獲」規則約束）。
//
// 【注意事項 Pay Attention】
// 1. 參考捕獲的變數若在 closure 被呼叫前就結束生命週期，是懸空參考（UB）。
//    UB 不保證崩潰，可能剛好印出看似正常的值，反而更難查。
// 2. 值捕獲的變數預設不可修改（operator() 是 const），要改得加 mutable，
//    但改的仍只是副本。
// 3. 在成員函式內寫 [=]，捕獲的是 this 指標而非成員副本；C++20 已將這種
//    隱式捕獲 this 標為 deprecated，應明寫 [this] 或 [*this]（C++17）。
// 4. 本檔 lambda1 執行後 b 變成 40，這個「a + c」用的是捕獲當下的副本值。
//    若在建立 lambda 後、呼叫前修改 a，lambda 內看到的仍是舊值。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】Lambda 混合捕獲
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. [=, &b] 和 [&, b] 差在哪？為什麼 [=, b] 不能編譯？
//     答：[=, &b] 是「預設值捕獲，b 例外走參考」；[&, b] 是「預設參考捕獲，
//         b 例外走值」。[=, b] 中 b 的捕獲方式與預設相同，屬於冗餘，
//         標準明定為 ill-formed，編譯器會直接報錯。
//     追問：那 [=] 會不會把函式裡「所有」區域變數都捕獲進來？→ 不會，
//         隱式捕獲只捕獲主體中實際使用到的變數；static 與全域則完全不捕獲。
//
// 🔥 Q2. lambda 的 operator() 預設是 const，為什麼參考捕獲的 b 還能被賦值？
//     答：const 限制的是 closure 自身的成員。參考成員 b 的型別是 int&，
//         const 化後不可「重新綁定」，但透過它寫入被指向的物件完全合法。
//         值捕獲的 a 型別是 int，const 化成 const int，因此不可寫。
//     追問：那要修改值捕獲的副本怎麼辦？→ 加 mutable，把 operator() 的
//         const 拿掉；但修改的仍是副本，外部變數不受影響。
//
// ⚠️ 陷阱. 「[&] 只是捕獲方式不同，安全性和 [=] 一樣」——錯在哪？
//     答：[&] 讓 closure 內部持有的是參考（實作上是指標）。只要 closure 的
//         存活時間超過被捕獲變數的生命週期——例如把 lambda return 出去、
//         存進成員變數、丟給 std::thread——就是懸空參考，屬於 UB。
//     為什麼會錯：把 lambda 想成「馬上就會執行完」。在 std::sort、
//         std::count_if 這類立即呼叫的場景，[&] 確實永遠安全，於是養成習慣；
//         一旦 lambda 被儲存起來延後執行，這個心智模型就完全失效。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【LeetCode 實戰範例】從缺。
//   理由：本檔示範的是「捕獲清單語法」本身，屬於語言特性層面。LeetCode 題目
//   即使用到 lambda，關心的也是演算法（例如自訂比較器），與「= 和 & 混用的
//   規則」沒有實質關聯。硬套一題只會模糊焦點，故從缺，改以下方實務範例呈現
//   混合捕獲真正的使用時機。

#include <iostream>
#include <vector>
#include <algorithm>
#include <string>

// -----------------------------------------------------------------------------
// 【日常實務範例】批次資料清洗：唯讀設定 + 可寫統計
//   場景：清洗一批感測器讀數，規則（上下限）來自設定檔、只讀不改；
//         但「被丟棄幾筆」的統計必須寫回呼叫端。
//   為什麼用混合捕獲：min/max 是設定值，值捕獲最安全（不怕外部意外改動）；
//         dropped 是要回傳給呼叫端的統計，必須參考捕獲才寫得回去。
//         這正是 [=, &dropped] 的教科書級用途。
// -----------------------------------------------------------------------------
std::vector<double> cleanReadings(const std::vector<double>& raw,
                                  double lo, double hi, int& dropped) {
    std::vector<double> out;
    // 預設值捕獲（lo、hi 只讀），dropped 例外走參考（要寫回外部）
    std::for_each(raw.begin(), raw.end(), [=, &dropped, &out](double v) {
        if (v < lo || v > hi) {
            ++dropped;          // 參考捕獲：真的寫回外部變數
        } else {
            out.push_back(v);
        }
    });
    return out;
}

int main() {
    int a = 10;
    int b = 20;
    int c = 30;
    
    std::cout << "=== [=, &b] 預設值捕獲，b 走參考 ===" << std::endl;

    // 預設以值捕獲，但 b 以參考捕獲
    auto lambda1 = [=, &b]() {
        // a 是副本，b 是參考，c 是副本
        b = a + c;  // 可以修改 b
        // a = 100;  // 錯誤！a 是值捕獲，不能修改（除非 mutable）
    };
    
    lambda1();
    std::cout << "a = " << a << ", b = " << b << ", c = " << c << std::endl;
    
    std::cout << "\n=== [&, a] 預設參考捕獲，a 走值 ===" << std::endl;

    // 預設以參考捕獲，但 a 以值捕獲
    auto lambda2 = [&, a]() {
        // a 是副本，b 和 c 是參考
        b = 100;
        c = 200;
        // a 不能修改（值捕獲）
    };
    
    lambda2();
    std::cout << "a = " << a << ", b = " << b << ", c = " << c << std::endl;

    std::cout << "\n=== 日常實務: 批次資料清洗 ===" << std::endl;
    std::vector<double> raw = {20.5, -99.0, 23.1, 250.0, 22.8, 21.0, 999.9};
    int dropped = 0;
    std::vector<double> ok = cleanReadings(raw, 0.0, 100.0, dropped);
    std::cout << "保留 " << ok.size() << " 筆，丟棄 " << dropped << " 筆" << std::endl;
    std::cout << "保留值: ";
    for (double v : ok) std::cout << v << " ";
    std::cout << std::endl;

    return 0;
}

