// ============================================================
// 第 22 課 總結：deque 的宣告與初始化
// 編譯：g++ -std=c++17 -o summary summary.cpp
// ============================================================
// 【九種初始化方式】
//   1. 預設建構          deque<int> d;
//   2. 指定數量+預設值   deque<int> d(5);          → 5 個 0
//   3. 指定數量+指定值   deque<int> d(4, 77);      → 4 個 77
//   4. 初始化列表        deque<int> d = {1,2,3};
//   5. 複製建構          deque<int> d2(d1);        → 深複製
//   6. 移動建構          deque<int> d2(move(d1));  → d1 變空
//   7. 迭代器範圍        deque<int> d(v.begin(), v.end());
//   8. assign 重新指定   d.assign({7,8,9});
//   9. 小括號 vs 大括號  d(5,10) → 5個10; d{5,10} → 元素5和10
//
// 【注意：容器複製是淺複製指標成員】
//   vector<int*> v2(v1); → v1 和 v2 的指標指向同一塊記憶體
//   修改 *v2[0] 會影響 *v1[0]，且有 double free 風險
// ============================================================

#include <iostream>
#include <deque>
#include <vector>
using namespace std;

void print(const string& label, const deque<int>& dq) {
    cout << "  " << label << ": ";
    for (int val : dq) cout << val << " ";
    cout << "(size=" << dq.size() << ")" << endl;
}

int main() {
    // ============================================================
    // 1. 預設建構（空 deque）
    // ============================================================
    cout << "===== 初始化方式 =====\n";
    deque<int> d1;
    print("1. 預設建構    ", d1);
    // (size=0)

    // ============================================================
    // 2. 指定數量，預設值為 0
    // ============================================================
    deque<int> d2(5);
    print("2. 五個預設值  ", d2);
    // 0 0 0 0 0 (size=5)

    // ============================================================
    // 3. 指定數量 + 指定值
    // ============================================================
    deque<int> d3(4, 77);
    print("3. 四個 77     ", d3);
    // 77 77 77 77 (size=4)

    // ============================================================
    // 4. 初始化列表
    // ============================================================
    deque<int> d4 = {10, 20, 30, 40, 50};
    print("4. 初始化列表  ", d4);
    // 10 20 30 40 50 (size=5)

    // ============================================================
    // 5. 複製建構（深複製）
    // ============================================================
    deque<int> d5(d4);
    d5[0] = 999;
    print("5a. 修改 d5    ", d5);
    print("5b. d4 不受影響", d4);

    // ============================================================
    // 6. 移動建構
    // ============================================================
    deque<int> d6(move(d5));
    print("6a. d6 移動自d5", d6);
    print("6b. d5 被掏空  ", d5);

    // ============================================================
    // 7. 從其他容器的迭代器範圍建構
    // ============================================================
    vector<int> vec = {100, 200, 300};
    deque<int> d7(vec.begin(), vec.end());
    print("7. 從 vector   ", d7);

    // ============================================================
    // 8. assign 重新指定
    // ============================================================
    d7.assign({7, 8, 9, 10});
    print("8. assign 後   ", d7);

    // ============================================================
    // 9. 小括號 vs 大括號（重要陷阱！）
    // ============================================================
    cout << "\n===== 小括號 vs 大括號 =====\n";
    deque<int> d8(5, 10);    // 5 個 10
    deque<int> d9{5, 10};    // 兩個元素：5 和 10
    print("d8 = deque(5,10)", d8);
    print("d9 = deque{5,10}", d9);

    // ============================================================
    // 指標成員的淺複製陷阱
    // ============================================================
    cout << "\n===== 指標淺複製陷阱 =====\n";
    {
        vector<int*> v1;
        v1.push_back(new int(42));
        v1.push_back(new int(99));

        vector<int*> v2(v1);  // 複製指標，不複製指向的資料

        cout << "  *v1[0] = " << *v1[0] << endl;  // 42
        cout << "  *v2[0] = " << *v2[0] << endl;  // 42

        *v2[0] = 777;
        cout << "  修改 *v2[0] 後：\n";
        cout << "  *v1[0] = " << *v1[0] << endl;  // 777 ← v1 也被影響！
        cout << "  （共享同一塊記憶體 → double free 風險！）\n";

        // 正確清理（只能 delete 一次）
        for (int* p : v1) delete p;
    }

    return 0;
}
