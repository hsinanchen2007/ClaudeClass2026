// =============================================================================
//  第 14 課：vector 元素插入：insert、emplace9.cpp
//    —  安全的連續插入：三種正確做法與它們的取捨
// =============================================================================
//
// 【主題資訊 Information】
//   問題: 需要連續插入多個元素時,第一次 insert 就讓後續要用的 iterator 失效
//   三種解法: (1) 接住回傳值  (2) 改用 index  (3) 一次批次插入
//   標頭檔: <vector>
//   複雜度: 前兩種各為 O(k × n)(k 次插入);第三種是 O(k + n),最優
//
// 【詳細解釋 Explanation】
//
// 【1. 做法一:接住 insert 的回傳值】
//     auto it = v.begin() + 2;
//     it = v.insert(it, 10);      // it 現在指向剛插入的 10
//     it = v.insert(it, 20);      // 在 10 之前插入 20
// 標準保證回傳值指向新插入的元素且有效,所以可以安全連鎖。
// **注意插入的位置語意**:每次都插在「上一個插入的元素之前」,
// 所以 10、20 的順序在結果中是反的 → 得到 {1, 2, 20, 10, 3, 4, 5}。
// 想維持插入順序,要用 `it = v.insert(it, x); ++it;` 讓 it 前進到新元素之後。
//
// 【2. 做法二:改用 index】
//     size_t index = 2;
//     v.insert(v.begin() + index, 10);
//     ++index;                              // 手動維護邏輯位置
//     v.insert(v.begin() + index, 20);
// 整數不會因為記憶體搬家而失效,所以每次都用 `v.begin() + index` 重建 iterator。
// 這種寫法插入順序是正的 → {1, 2, 10, 20, 3, 4, 5}。
// 代價是要自己維護 index 的推進,容易寫錯 ±1。
//
// 【3. 做法三:一次批次插入(最高效,應優先考慮)】
//     v.insert(v.begin() + 2, {10, 20, 30});
// 只搬移一次後段、最多一次 reallocation → O(k + n),
// 而前兩種做法各自都是 O(k × n)。
// **只要能事先湊齊要插入的元素,就該用這種**。
//
// 【4. 怎麼選】
//   * 元素能事先湊齊 → 做法三,沒有懸念。
//   * 插入位置要依前一次結果動態決定 → 做法一(回傳值最直接)。
//   * 位置由外部邏輯以「第幾個」描述 → 做法二(index 語意最自然)。
//
// 【概念補充 Concept Deep Dive】
// 為什麼 index 比 iterator 穩健?因為它們表達的是不同層次的東西:
//   * iterator 是**物理位置**(記憶體位址)—— 記憶體重排就失效
//   * index    是**邏輯位置**(第幾個)  —— 只要「第幾個」的語意不變就有效
// 這也說明 index 不是萬靈丹:若你在索引 2 之前插入了元素,
// 原本索引 2 的那個元素現在變成索引 3,index 本身雖然「有效」,
// 指的卻已經是別的元素。index 免疫的是記憶體搬家,不是邏輯位移。
//
// 這正是 libstdc++ 內部保證 insert 回傳值有效的手法:
//     const size_type idx = pos - begin();   // 降級成 index
//     ... 可能 reallocate ...
//     return begin() + idx;                  // 用新 begin() 升級回 iterator
//
// 【注意事項 Pay Attention】
// 1. 做法一的插入順序與直覺相反(每次都插在前一個之前),
//    要維持順序記得 `++it`。這是最常見的 off-by-one 來源。
// 2. 做法二的 `v.begin() + index`,index 是 size_type(無號),
//    iterator 的加法要的是 difference_type(有號)。混用會觸發
//    -Wsign-conversion 之類的警告,嚴謹的寫法要顯式轉型。
// 3. 三種做法的結果內容不同(見輸出),不是三種等價寫法 ——
//    先想清楚你要的順序,再挑做法。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】連續插入的正確寫法
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 要在 vector 中間連續插入 3 個元素,最好的做法是什麼?為什麼?
//     答：能事先湊齊就用批次插入 v.insert(pos, {a, b, c})。
//         它只搬移一次後段、最多一次 reallocation,複雜度 O(k + n);
//         而逐一插入是 O(k × n),每次都要重新搬移後段。
//     追問：如果插入位置要看前一次的結果才能決定呢?→ 那就只能逐一插入,
//         用 insert 的回傳值接住新的 iterator 繼續操作。
//
// ⚠️ 陷阱. 下面兩段的結果一樣嗎?
//         // A                              // B
//         auto it = v.begin() + 2;          size_t i = 2;
//         it = v.insert(it, 10);            v.insert(v.begin() + i, 10);
//         it = v.insert(it, 20);            ++i;
//                                           v.insert(v.begin() + i, 20);
//     答：不一樣。A 每次都插在「上一個插入的元素之前」→ {…, 20, 10, …};
//         B 的 index 有往後推進 → {…, 10, 20, …}。兩者都正確、都沒有 UB,
//         但插入順序相反。
//     為什麼會錯：以為「只要避開 iterator 失效就沒事」,忽略了 insert 的
//         語意是「插在 pos 之前」。A 要維持順序必須在每次插入後 ++it。
//
// 🔥 Q2. 為什麼把 iterator 換成 index 能避開失效問題?index 有什麼是它擋不住的?
//     答：iterator 是物理位置(記憶體位址),記憶體重排就懸空;
//         index 是邏輯位置(第幾個),不受記憶體搬家影響。
//         但 index 擋不住**邏輯位移** —— 若你在它之前插入了元素,
//         同一個 index 指到的就是另一個元素了。
//     追問：那 vector 有沒有「插入後仍指向同一元素」的穩定 handle?
//         → 沒有。需要穩定參考就得改用 list/deque(reference 穩定),
//         或存元素的鍵值而非位置。
// ═══════════════════════════════════════════════════════════════════════════

#include <vector>
#include <iostream>

int main() {
    std::vector<int> v = {1, 2, 3, 4, 5};

    // 錯誤示範：迭代器失效
    /*
    auto it = v.begin() + 2;
    v.insert(it, 10);
    v.insert(it, 20);  // it 已失效！未定義行為
    */

    // 正確做法 1：使用回傳值更新迭代器
    // 注意：每次都插在「上一個插入的元素之前」，所以順序是反的
    auto it = v.begin() + 2;
    it = v.insert(it, 10);
    it = v.insert(it, 20);  // 在 10 之前插入 20

    std::cout << "做法 1 (回傳值, 順序反): ";
    for (int x : v) {
        std::cout << x << " ";  // 1 2 20 10 3 4 5
    }
    std::cout << std::endl;

    // 正確做法 1b：想維持插入順序，插完之後把 it 往前推一格
    v = {1, 2, 3, 4, 5};
    it = v.begin() + 2;
    it = v.insert(it, 10);
    ++it;                    // 跳過剛插入的 10，指向原本的 3
    it = v.insert(it, 20);   // 插在 3 之前 → 也就是 10 之後

    std::cout << "做法 1b (回傳值 + ++it): ";
    for (int x : v) {
        std::cout << x << " ";  // 1 2 10 20 3 4 5
    }
    std::cout << std::endl;

    // 正確做法 2：使用索引
    v = {1, 2, 3, 4, 5};
    std::size_t index = 2;
    v.insert(v.begin() + static_cast<std::ptrdiff_t>(index), 10);
    ++index;  // 更新索引
    v.insert(v.begin() + static_cast<std::ptrdiff_t>(index), 20);

    std::cout << "做法 2 (index): ";
    for (int x : v) {
        std::cout << x << " ";  // 1 2 10 20 3 4 5
    }
    std::cout << std::endl;

    // 正確做法 3：一次插入多個（最高效，O(k + n) 而非 O(k * n)）
    v = {1, 2, 3, 4, 5};
    v.insert(v.begin() + 2, {10, 20, 30});

    std::cout << "做法 3 (批次, 最優): ";
    for (int x : v) {
        std::cout << x << " ";  // 1 2 10 20 30 3 4 5
    }
    std::cout << std::endl;

    return 0;
}

