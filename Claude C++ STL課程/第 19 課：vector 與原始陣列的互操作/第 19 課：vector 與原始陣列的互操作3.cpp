// =============================================================================
//  第 19 課：vector 與原始陣列的互操作 3  —  data() + size() 成對傳遞的安全介面
// =============================================================================
//
// 【主題資訊 Information】
//   慣用法 : f(v.data(), v.size());
//   標頭檔 : <vector>
//   語意   : data() 給「起點」，size() 給「長度」，兩者合起來才描述一段範圍。
//            標準保證 [data(), data() + size()) 恆為合法範圍，
//            即使 size() == 0（此時是合法的空範圍）。
//   複雜度 : data() 與 size() 皆為 O(1) 且 noexcept。
//   現代替代: C++20 的 std::span<T> 把這兩個值封裝成單一型別，
//            從根本上消除「指標與長度不同步」這類錯誤。
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼「指標 + 長度」是 C 介面的原罪】
//   C 的陣列一旦傳進函式就退化（decay）成指標，長度資訊當場遺失。
//   所以所有 C API 都被迫把長度另外傳一個參數。這帶來三個經典錯誤：
//     (a) 長度傳錯（傳成位元組數 vs 元素數，差 sizeof(T) 倍）
//     (b) 指標與長度來自不同物件（複製貼上後改了其中一個）
//     (c) 取得指標後 vector 又被修改，長度對了但指標失效了
//   把 f(v.data(), v.size()) 寫在同一行，就是為了讓這三件事「不容易寫錯」——
//   兩個值同時取自同一個物件、同一個時間點。
//
// 【2. 空範圍為什麼必須被支援】
//   真實資料常常是空的：查詢 0 筆、檔案 0 位元組、封包無 payload。
//   如果每個呼叫點都要寫 if (!v.empty())，程式碼會被守衛淹沒。
//   比較好的設計是「讓被呼叫端接受空範圍」——函式第一行就檢查 size == 0
//   並直接返回。這樣呼叫端可以無腦寫 f(v.data(), v.size())，
//   空的時候自然什麼都不做。本檔的 process() 就是這種設計。
//
//   注意這是「你自己寫的函式」才能這樣設計。第三方 C API 是否容忍
//   nullptr + 0，要查它的文件；不確定就在呼叫端擋（見本課第 2 個檔案）。
//
// 【3. size() 的單位是「元素個數」，不是位元組】
//   這是最常見的長度傳錯來源。判斷準則看 API 參數的名字與型別：
//       count / n / num_elements  → 傳 v.size()
//       nbytes / len / size_t cb  → 傳 v.size() * sizeof(T)
//   memcpy、fread/fwrite、send/recv、std::ostream::write 全都吃「位元組數」，
//   所以一律要乘 sizeof(T)。忘記乘，就只複製到 1/4 或 1/8 的資料；
//   多乘一次，就會讀寫越界——後者是未定義行為。
//
// 【4. 為什麼 size_t 比 int 好】
//   size() 回傳 size_type（實務上是 size_t，無號）。若 API 用 int 接，
//   資料超過 INT_MAX 就會被截斷成負數，迴圈條件當場失效。
//   自己設計介面時請用 size_t；被迫對接 int 介面時，
//   寫 static_cast<int>(v.size()) 表明意圖，並在資料可能很大時自己先檢查上限。
//
// 【概念補充 Concept Deep Dive】
//
// (A) std::span 如何從根本解決這個問題（C++20）
//   void process(std::span<const double> data);
//   呼叫端直接寫 process(v);——span 自己從 vector 取出 data() 與 size()，
//   兩者永遠同步，不可能傳錯。span 還提供 .size()、range-for、subspan()，
//   而且是「非擁有視圖」，複製 span 不複製資料。
//   本檔用 -std=c++17 編譯所以示範傳統寫法；span 的完整說明見 summary.cpp。
//   （已用 -pedantic-errors 驗證：<span> 在 C++17 下不存在，確實是 C++20 才有。）
//
// (B) 為什麼不乾脆傳 const vector<T>& 就好
//   傳參考當然更安全，但那要求呼叫端「一定是 vector」。
//   一旦你的函式簽名綁死 vector，別人就沒辦法傳原始陣列、std::array、
//   或是從 mmap 拿到的一段記憶體給你。
//   「指標 + 長度」（或 C++20 的 span）是唯一能同時接受這幾種來源的介面形狀。
//
// (C) 空範圍的指標算術是合法的
//   當 size() == 0 時，data() + 0 仍然是合法的指標運算（結果等於 data() 本身）。
//   所以 for (size_t i = 0; i < 0; ++i) 這種迴圈自然一次都不進去，
//   不需要額外的特判就是安全的。真正不能做的是「解參考」那個指標。
//
// 【注意事項 Pay Attention】
//   1. 指標與長度必須來自同一個 vector、同一個時間點，中間不可修改該 vector。
//   2. 分清楚 API 要的是元素數還是位元組數；後者要乘 sizeof(T)。
//   3. 自己寫的函式建議接受空範圍（size == 0 直接返回），呼叫端才乾淨。
//   4. 第三方 C API 不一定容忍 nullptr；不確定就在呼叫端用 empty() 擋。
//   5. 即使 n == 0，把 nullptr 傳給 memcpy 等函式在標準上仍屬未定義行為，
//      雖然常見實作實際上會直接返回。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】data() + size() 介面設計
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼把 vector 傳給 C API 時，一定要 data() 和 size() 一起傳？
//     答：因為 C 陣列傳入函式會退化成指標，長度資訊遺失，被呼叫端無從得知邊界。
//         data() 只提供起點，size() 才提供長度，兩者合起來才構成完整範圍。
//         標準保證 [data(), data() + size()) 恆為合法範圍，這正是安全遍歷的依據。
//     追問：C++20 有更好的做法嗎？
//         → std::span<T>，它把指標與長度綁成一個型別，呼叫端直接傳 vector 即可，
//           不可能出現兩者不同步的錯誤。
//
// 🔥 Q2. process(v.data(), v.size()) 在 v 是空的時候安全嗎？
//     答：這個呼叫本身完全安全——data() 沒有前置條件，size() 回傳 0，
//         被呼叫端的迴圈跑 0 次。前提是被呼叫端不會在檢查長度前就解參考指標。
//         若對接的是不檢查 nullptr 的第三方 C 函式，就要在呼叫端用 empty() 擋。
//     追問：那 memcpy(dst, nullptr, 0) 呢？
//         → 標準上仍是未定義行為（memcpy 要求指標有效），雖然實務上多數
//           實作會直接返回。嚴謹的寫法還是先判斷 size 不為 0。
//
// ⚠️ 陷阱. 「memcpy(dst.data(), src.data(), src.size()) 少複製了資料，為什麼？」
//     答：因為 memcpy 的第三個參數是「位元組數」，不是「元素個數」。
//         src.size() 是元素個數，對 vector<int> 來說只複製了 1/4 的資料
//         （本機 sizeof(int) == 4）。正確寫法是 src.size() * sizeof(int)。
//     為什麼會錯：把 size() 想成「這個容器有多大」的籠統概念，
//         但它精確的定義是「元素個數」。凡是 C 的記憶體層 API
//         （memcpy/memset/fread/fwrite/send/recv）一律以 byte 計，必須乘 sizeof(T)。
//         反過來多乘一次會造成讀寫越界，那是未定義行為，比少複製更危險。
// ═══════════════════════════════════════════════════════════════════════════

#include <cstddef>
#include <iostream>
#include <vector>

// -----------------------------------------------------------------------------
// 好的介面設計：接受空範圍，呼叫端不必額外守衛
// -----------------------------------------------------------------------------
void process(const double* arr, std::size_t size) {
    if (size == 0) {                       // 先看長度，再碰指標
        std::cout << "空陣列，不處理\n";
        return;
    }
    for (std::size_t i = 0; i < size; ++i) {
        std::cout << (i ? " " : "") << arr[i];
    }
    std::cout << "\n";
}

// -----------------------------------------------------------------------------
// 【日常實務範例】把感測器樣本交給舊的 C 統計函式庫
//   情境：資料擷取卡每 100 ms 回報一批電壓樣本，數量不固定，
//         通訊逾時的那一輪會是 0 筆。統計函式 stat_rms() 是 C 寫的。
//   為何是本主題：0 筆是常態不是例外，介面必須天然容忍空範圍，
//         否則每個呼叫點都要包 if。這裡示範「被呼叫端負責擋」的設計。
// -----------------------------------------------------------------------------
double stat_rms(const double* samples, std::size_t n) {  // 假裝這是 C 函式庫
    if (n == 0) return 0.0;                              // 空範圍是合法輸入
    double sumSq = 0.0;
    for (std::size_t i = 0; i < n; ++i) sumSq += samples[i] * samples[i];
    // 這裡刻意不用 <cmath>，用牛頓法示範純運算即可
    double guess = sumSq / n;
    for (int it = 0; it < 40; ++it) {
        if (guess <= 0.0) break;
        guess = 0.5 * (guess + (sumSq / n) / guess);
    }
    return guess;
}

void reportChannel(const char* name, const std::vector<double>& samples) {
    // 指標與長度取自同一物件、同一時間點——這是唯一該養成的習慣
    double rms = stat_rms(samples.data(), samples.size());
    std::cout << "  通道 " << name << "：" << samples.size()
              << " 筆樣本，RMS = " << rms << "\n";
}

int main() {
    // -------------------------------------------------------------------------
    std::cout << "=== 同一段程式碼，空與非空都能跑 ===\n";
    // -------------------------------------------------------------------------
    std::vector<double> v;                  // 空的

    process(v.data(), v.size());            // 呼叫端不必特判

    v = {1.1, 2.2, 3.3};
    process(v.data(), v.size());            // 同一行寫法，直接可用

    // -------------------------------------------------------------------------
    std::cout << "\n=== 空範圍的指標算術是合法的 ===\n";
    // -------------------------------------------------------------------------
    std::vector<double> e;
    const double* begin = e.data();
    const double* end = e.data() + e.size();   // size()==0，等於 data() 本身
    std::cout << "空 vector 的 begin == end : " << (begin == end ? "是" : "否") << "\n";
    std::cout << "兩者相距                  : " << (end - begin) << " 個元素\n";
    std::cout << "→ 合法的空範圍，遍歷迴圈自然跑 0 次，不需要特判\n";

    // -------------------------------------------------------------------------
    std::cout << "\n=== 元素個數 vs 位元組數（最常見的長度傳錯）===\n";
    // -------------------------------------------------------------------------
    std::vector<double> d = {1.0, 2.0, 3.0, 4.0};
    std::cout << "d.size()                 = " << d.size() << " 個元素\n";
    std::cout << "sizeof(double)           = " << sizeof(double)
              << " bytes（實作定義，本機 x86-64 為 8）\n";
    std::cout << "d.size() * sizeof(double)= " << d.size() * sizeof(double)
              << " bytes ← memcpy/fwrite/send 要的是這個\n";

    // -------------------------------------------------------------------------
    std::cout << "\n=== 日常實務：資料擷取卡的多通道樣本 ===\n";
    // -------------------------------------------------------------------------
    std::vector<double> chA = {0.30, -0.40, 0.50, -0.20};
    std::vector<double> chB;                       // 這一輪逾時，0 筆
    std::vector<double> chC = {1.50, 1.50, 1.50};

    reportChannel("A", chA);
    reportChannel("B", chB);                       // 空範圍，天然安全
    reportChannel("C", chC);

    return 0;
}

