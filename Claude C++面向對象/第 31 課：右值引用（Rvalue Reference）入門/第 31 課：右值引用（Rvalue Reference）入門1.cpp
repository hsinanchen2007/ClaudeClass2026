// === 預期輸出 ===
// rref = 100
// srref = "Temp"
// srref = "Temp Modified"
//
// === 綁定結果一覽 ===
//   lref   = 42   (T&       ← 左值 x)
//   clref1 = 42   (const T& ← 左值 x)
//   clref2 = 42   (const T& ← 右值 42，生命週期被延長)
//   rref   = 100  (T&&      ← 右值 42，且已被改成 100)
//   sref   = "Hello"  (T&       ← 左值 str)
//   csref  = "X"      (const T& ← 暫時 string)
//   sref += "!" 之後, str = "Hello!" (同一個物件)
