// =============================================================================
//  第 31 課 -1  —  三種引用的綁定規則：誰能綁誰
// =============================================================================
//
// 【主題資訊 Information】
//   綁定規則 ： T&        只能綁左值
//               const T&  左值、右值都能綁（並延長暫時物件的生命週期）
//               T&&       只能綁右值
//   標準版本 ： T&& 為 C++11 新增；const T& 綁右值的規則 C++98 就有
//   標頭檔   ： <string>（本檔用 std::string 示範類別型別的綁定）
//   複雜度   ： 綁定本身 O(1)，不產生任何執行期成本（引用不是物件）
//
// 【詳細解釋 Explanation】
//
// 【1. 一張表就是本檔的全部】
//       運算式         T&      const T&    T&&
//       x（具名變數）  ✅      ✅          ❌
//       42（字面值）   ❌      ✅          ✅
//       x + 1          ❌      ✅          ✅
//       f() 傳值回傳   ❌      ✅          ✅
//   記法：左邊那一欄「有沒有名字」決定一切。有名字 → 左值 → 只有前兩欄可以；
//   沒名字 → 右值 → 只有後兩欄可以。const T& 是唯一兩邊都吃的。
//
// 【2. 為什麼 int& lref2 = 42; 被禁止】
//   因為引用是「別名」，而 42 是個沒有身分的純右值。若允許綁定，
//   你就能寫 ++lref2 去修改一個馬上要消失的東西 —— 改了也沒人看得到，
//   幾乎必然是筆誤。標準直接在編譯期擋掉。
//
// 【3. 為什麼 const int& clref2 = 42; 反而合法】
//   既然是唯讀，就沒有「改了卻沒人看到」的問題。而且標準額外規定：
//   用 const 左值引用（或右值引用）綁定暫時物件時，該暫時物件的生命週期
//   會延長到這個引用的生命週期結束。所以 clref2 在整個 main 裡都是安全的，
//   不是懸空引用。這條規則正是「函式參數寫 const T&」能安全接受
//   f(x + 1) 這種呼叫的基礎。
//
// 【4. 為什麼 int&& rref2 = x; 被禁止】
//   x 有名字，代表你後面還可能用它。右值引用的語意是「這東西可以被搬空」，
//   若允許綁到具名變數，別人搬空它之後你下一行再用 x 就慘了。
//   要明確表示「我不再用 x 了」，必須自己寫 std::move(x) ——
//   責任回到寫程式的人身上。
//
// 【5. 右值引用綁定之後，它自己是左值，而且可以修改】
//   本檔第 23 行 rref = 100; 與第 37 行 srref += " Modified"; 都合法。
//   這常讓人困惑：「綁到右值的引用，怎麼還能改？」
//   因為 T&& 是「非 const 的引用」，它引用的暫時物件生命週期已被延長，
//   是一塊真實、可寫的記憶體。「右值」描述的是原本那個運算式的值類別，
//   不是「唯讀」的意思。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 生命週期延長只發生在「直接綁定」
//   const std::string& s = std::string("X");        // ✅ 延長，安全
//   const std::string& bad = std::string("X").substr(0,1);
//       // ⚠️ 延長的是 substr 回傳的暫時物件；作為呼叫對象的
//       //    std::string("X") 仍在該完整運算式結束時銷毀。
//   更常見的陷阱是「從函式回傳 const T&」——
//   延長規則不會跨越函式邊界，回傳對區域暫時物件的引用一定是懸空的。
//
// (B) 引用不是物件
//   引用沒有規定要佔空間，編譯器通常實作成指標，但標準並未要求。
//   所以沒有「引用的陣列」、沒有「指向引用的指標」，
//   也不能重新綁定 —— 一旦綁定就終身綁定。
//
// (C) 為什麼字串字面值可以綁到 const std::string&
//   本檔第 30 行 const std::string& csref = std::string("X"); 是明確建構。
//   但即使寫 const std::string& s = "X"; 也合法：
//   "X" 是 const char[2]，會先隱式轉換出一個暫時的 std::string，
//   再由 const 左值引用綁定並延長其生命週期。
//   T& 就辦不到 —— 那個暫時物件是右值。
//
// 【注意事項 Pay Attention】
//   1. 本檔第 12、20、32 行是「刻意保留的錯誤示範」，必須維持註解狀態；
//      取消註解任何一行都會編譯失敗（那正是它們要示範的事）。
//   2. const T& 綁暫時物件會延長生命週期，但這規則不跨函式邊界：
//      千萬不要回傳對區域暫時物件的引用。
//   3. T&& 綁到右值後仍然可寫。「右值」講的是值類別，不是 const。
//   4. rref 本身有名字，所以它是左值 —— 這是第 2 個檔案的主題。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】引用的綁定規則
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼 int& r = 42; 不合法，但 const int& r = 42; 合法？
//     答：42 是純右值，沒有身分。允許非 const 左值引用綁定它，就等於允許
//         你修改一個馬上消失的暫時物件，幾乎必定是筆誤，所以標準禁止。
//         const 版本因為唯讀而沒有這個問題，而且標準規定此時暫時物件的
//         生命週期會延長到該引用結束，所以是安全的。
//     追問：這條規則在實務上為什麼重要？
//         → 它正是「函式參數寫 const T&」可以同時接受 f(x) 和 f(x + 1)
//           的原因。若沒有這條規則，每個函式都得為暫時物件另寫一個重載。
//
// 🔥 Q2. int&& r = 42; 綁定之後，r 可以被修改嗎？
//     答：可以。本檔第 23 行 rref = 100; 就是合法的。T&& 是非 const 引用，
//         它引用的暫時物件生命週期已被延長，是一塊真實可寫的記憶體。
//         「右值」形容的是原本運算式的值類別，跟能不能寫無關。
//     追問：那 const int&& 有什麼用？
//         → 幾乎沒有實際用途。它能綁右值卻不能修改，所以無法搬走資源；
//           它的存在主要是為了讓 std::move(constObj) 這種運算式有型別可言，
//           而後果就是移動會安靜地退化成拷貝。
//
// ⚠️ 陷阱. 「const T& 會延長生命週期，所以回傳 const T& 也安全」——完全相反。
//     答：延長規則只作用在「直接綁定暫時物件的那個引用」上，且不跨越
//         函式邊界。函式回傳 const T& 指向區域暫時物件時，
//         該物件在函式結束時就銷毀，呼叫端拿到的是懸空引用，
//         之後的使用是未定義行為 —— 不保證任何固定結果，
//         很可能在測試環境下看起來完全正常。
//     為什麼會錯：把「延長」理解成「只要用 const& 接住就永遠安全」。
//         實際規則是：延長的期限等於「那個引用本身的生命週期」，
//         而區域引用在函式返回時就結束了，救不了任何東西。
// ═══════════════════════════════════════════════════════════════════════════

// lesson31_binding_rules.cpp
// 編譯：g++ -std=c++17 -Wall -Wextra -o lesson31a lesson31_binding_rules.cpp

#include <iostream>
#include <string>

int main() {
    int x = 42;

    // ──── 左值引用 ────
    int& lref = x;          // ✅ 左值引用 → 左值
    // int& lref2 = 42;     // ❌ 左值引用 → 右值（不行！）

    // ──── const 左值引用 ────
    const int& clref1 = x;  // ✅ const 左值引用 → 左值
    const int& clref2 = 42; // ✅ const 左值引用 → 右值（特殊！）

    // ──── 右值引用 ────
    int&& rref = 42;        // ✅ 右值引用 → 右值
    // int&& rref2 = x;     // ❌ 右值引用 → 左值（不行！）

    // 修改右值引用
    rref = 100;             // ✅ 右值引用本身是可修改的
    std::cout << "rref = " << rref << "\n";  // 100

    // ──── 字串的例子 ────
    std::string str = "Hello";

    std::string& sref = str;                     // ✅ 左值引用 → 左值
    const std::string& csref = std::string("X"); // ✅ const 左值引用 → 右值
    std::string&& srref = std::string("Temp");   // ✅ 右值引用 → 右值
    // std::string&& srref2 = str;               // ❌ 右值引用 → 左值

    std::cout << "srref = \"" << srref << "\"\n";

    // 可以修改右值引用綁定的物件
    srref += " Modified";
    std::cout << "srref = \"" << srref << "\"\n";

    // ── 把上面每個綁定都印出來（順便消除 -Wunused-variable 警告）──
    std::cout << "\n=== 綁定結果一覽 ===\n";
    std::cout << "  lref   = " << lref   << "   (T&       ← 左值 x)\n";
    std::cout << "  clref1 = " << clref1 << "   (const T& ← 左值 x)\n";
    std::cout << "  clref2 = " << clref2 << "   (const T& ← 右值 42，生命週期被延長)\n";
    std::cout << "  rref   = " << rref   << "  (T&&      ← 右值 42，且已被改成 100)\n";
    std::cout << "  sref   = \"" << sref  << "\"  (T&       ← 左值 str)\n";
    std::cout << "  csref  = \"" << csref << "\"      (const T& ← 暫時 string)\n";

    // 修改 sref 會直接改到 str 本身 —— 引用就是別名，不是副本
    sref += "!";
    std::cout << "  sref += \"!\" 之後, str = \"" << str << "\" (同一個物件)\n";

    return 0;
}

