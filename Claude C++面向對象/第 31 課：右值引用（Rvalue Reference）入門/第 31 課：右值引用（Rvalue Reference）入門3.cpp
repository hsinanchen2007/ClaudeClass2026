// =============================================================================
//  第 31 課 -3  —  重載解析：const T& 與 T&& 並存時，編譯器怎麼選
// =============================================================================
//
// 【主題資訊 Information】
//   重載組合 ： void f(const T&);   // 左值、右值皆可綁 —— 但只能讀
//               void f(T&&);        // 只綁右值 —— 可以「偷」
//   選擇規則 ： 傳左值 → 只有 const T& 可行 → 選它
//               傳右值 → 兩者皆可行，但 T&& 是更精確的匹配 → 選 T&&
//   標準版本 ： C++11
//   標頭檔   ： <string>
//   實務對應 ： std::vector::push_back(const T&) / push_back(T&&)
//               就是這個模式最著名的應用
//
// 【詳細解釋 Explanation】
//
// 【1. 這一對重載就是「拷貝／移動」分流的入口】
//   在 C++11 之前，函式只能寫 f(const T&)，於是不管實參之後還要不要用，
//   一律只能複製。這對重載讓函式第一次能分辨：
//       f(name);              → 呼叫端還要用 name → 走 const T& → 老實複製
//       f(name + " King");    → 暫時物件，馬上就死 → 走 T&& → 可以搬走它
//   移動語意的一切（移動建構、移動賦值、emplace、std::vector 擴容）
//   底層都建立在這個重載選擇上。
//
// 【2. 為什麼傳右值時 T&& 會贏過 const T&】
//   兩個候選都能綁定右值，所以編譯器比較「哪一個轉換更好」：
//     * 綁到 T&&      ：直接繫結，不需要加上 const。
//     * 綁到 const T& ：也是直接繫結，但要多加一層 const 限定。
//   標準規定：在其他條件相同時，繫結到右值引用比繫結到左值引用更好
//   （[over.ics.rank]）。所以 T&& 勝出，而且是明確的勝出，不是模稜兩可。
//
// 【3. 逐一看本檔四個呼叫】
//   process(name);
//       name 是具名變數 → 左值 → 只有 const T& 能綁 → [左值版本]
//   process(std::string("Phoenix"));
//       明確建構的暫時物件 → prvalue → [右值版本]
//   process("Knight");
//       "Knight" 是 const char[7]，兩個重載都需要先轉成 std::string。
//       這個轉換產生一個暫時物件（prvalue），是右值 → [右值版本]
//   process(name + " King");
//       operator+ 傳值回傳，結果是暫時物件 → 右值 → [右值版本]
//   重點：值類別是由「整個運算式」決定的，不是由你寫下的字面樣貌決定。
//   name 是左值，但 name + " King" 是右值。
//
// 【4. 只留一個版本會怎樣】
//   只寫 const T&：能編譯、行為正確，但暫時物件的資源永遠白白被複製。
//   只寫 T&&    ：傳左值時直接編譯失敗（左值綁不了 T&&），呼叫端會很痛苦。
//   所以要嘛兩個都寫，要嘛只寫 const T&（正確但較慢）。
//   第三條路是「按值收下 + std::move」（sink parameter），
//   用一份程式碼涵蓋兩種情境 —— 見同課 summary.cpp 的實務範例。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼標準函式庫兩個版本都提供
//   std::vector 同時有 push_back(const T&) 與 push_back(T&&)。
//   前者處理「我還要用這個元素」，後者處理「這個元素給你了」。
//   若只提供按值版本 push_back(T)，對傳左值的情況會多一次移動
//   （複製進參數、再移動進容器）。容器是效能敏感的基礎設施，
//   所以選擇多寫一個重載換取最少的操作次數。
//
// (B) 這個模式的擴展性問題
//   兩個參數就要 4 個重載、n 個參數要 2^n 個。這正是完美轉發
//   （template + T&& + std::forward）被發明出來的原因：
//   一份樣板程式碼涵蓋所有值類別組合。emplace_back 就是這樣實作的。
//
// (C) 傳字面值時到底發生幾件事
//   process("Knight") 需要 const char[7] → std::string 的使用者定義轉換。
//   這個轉換的結果是一個暫時物件。注意兩個重載「都」需要這次轉換，
//   所以轉換成本不構成差別；差別在轉換完成之後，
//   那個暫時物件是右值，於是 T&& 勝出。
//
// 【注意事項 Pay Attention】
//   1. 「右值版本可以移動」不代表它「已經移動」。本檔的 T&& 版本只是印出
//      字串，什麼都沒搬。要真的搬走，函式內必須寫 std::move(s)
//      —— 因為 s 在函式內是左值（見同課第 2 個檔案）。
//   2. 不要同時提供 f(const T&)、f(T&&) 和 f(T)（按值）。
//      按值版本會和另外兩個都構成模稜兩可，直接編譯失敗。
//   3. 值類別由整個運算式決定：name 是左值，name + " King" 是右值。
//   4. 若類別沒有移動建構函數，走到 T&& 版本也不會比較快 ——
//      重載選對了，但接手的操作仍然是深拷貝。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】const T& 與 T&& 的重載解析
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 同時存在 f(const T&) 和 f(T&&) 時，各種實參會選中哪一個？
//     答：傳左值只有 const T& 可行（左值綁不了 T&&）；傳右值時兩者皆可行，
//         但標準規定繫結到右值引用優於繫結到左值引用，所以 T&& 勝出。
//         這正是拷貝與移動分流的機制，std::vector::push_back 的兩個重載
//         就是這樣配對的。
//     追問：那再加一個按值的 f(T) 會怎樣？
//         → 三者互相模稜兩可，任何呼叫都編譯失敗。
//           按值版本是「取代」那兩個重載的另一種設計，不能並存。
//
// 🔥 Q2. process("Knight") 為什麼選中 T&& 版本？
//     答：字串字面值是 const char[7]，兩個重載都需要先做
//         const char* → std::string 的使用者定義轉換。這個轉換產生一個
//         暫時物件（prvalue），而暫時物件是右值，所以 T&& 勝出。
//         關鍵是：值類別看的是「轉換之後的那個運算式」。
//     追問：process(name + " King") 呢？name 明明是左值。
//         → operator+ 是傳值回傳，結果是暫時物件，所以整個運算式是右值。
//           值類別由整個運算式決定，不是由裡面出現的變數決定。
//
// ⚠️ 陷阱. 「呼叫進了 T&& 版本，就代表資源被移動了」——不一定。
//     答：重載選擇只決定「走哪個函式」，不代表函式裡真的搬了東西。
//         本檔的 T&& 版本只是印出字串，一個位元組都沒搬。而且參數 s
//         在函式內是左值，要真的搬走必須寫 std::move(s)。
//         更根本的是：如果該型別根本沒有移動建構函數，
//         就算寫了 std::move 也只會退回深拷貝。
//     為什麼會錯：把「選中了移動的入口」當成「完成了移動」。
//         移動要三件事同時成立 —— 重載選對、函式內明確 move、
//         而且型別真的有移動操作。少一個都不會移動，而且沒有任何警告。
// ═══════════════════════════════════════════════════════════════════════════

// lesson31_overload.cpp
// 編譯：g++ -std=c++17 -Wall -Wextra -o lesson31c lesson31_overload.cpp

#include <iostream>
#include <string>

// 接受左值的版本
void process(const std::string& s) {
    std::cout << "  [左值版本] 收到 \"" << s << "\"（會拷貝）\n";
}

// 接受右值的版本
void process(std::string&& s) {
    std::cout << "  [右值版本] 收到 \"" << s << "\"（可以移動！）\n";
}

int main() {
    std::string name = "Dragon";

    std::cout << "傳入左值：\n";
    process(name);                    // 呼叫 const string& 版本

    std::cout << "\n傳入右值：\n";
    process(std::string("Phoenix"));  // 呼叫 string&& 版本

    std::cout << "\n傳入字面量：\n";
    process("Knight");                // "Knight" 建構暫時 string → 呼叫 string&& 版本

    std::cout << "\n傳入運算結果：\n";
    process(name + " King");          // name + " King" 產生暫時 string → 右值版本

    return 0;
}

