// =============================================================================
//  第 18 課：對象的生命週期 5  —  全域物件的初始化順序問題（Static Init Order Fiasco）
// =============================================================================
//
// 【主題資訊 Information】
//   主題：具有靜態儲存期（static storage duration）的非區域物件，
//         其動態初始化順序在「跨翻譯單元」時是未指定的（unspecified）。
//   保證的部分：同一個翻譯單元（.cpp）內，依「定義出現的順序」初始化。
//   未保證的部分：不同 .cpp 之間的相對順序——連結器決定，可能隨
//         連結順序、編譯器版本、是否為靜態/動態程式庫而改變。
//   標準版本：C++98 起皆然，至今未變（C++20 的 constinit 只解決一部分）。
//   標頭檔：<iostream>、<string>
//   別名：這個問題有個公認的名字叫 “static initialization order fiasco”。
//
// 【詳細解釋 Explanation】
//
// 【1. 靜態物件的初始化其實分兩個階段】
//   要理解這個問題，必須先知道它不是「一次」初始化：
//     階段一：靜態初始化（static initialization）—— 發生在程式載入時
//       ● 零初始化（zero-initialization）：所有靜態物件先被填 0
//       ● 常數初始化（constant initialization）：初值若是編譯期常數，
//         直接把結果寫進 .data 區段，不需執行任何程式碼
//     階段二：動態初始化（dynamic initialization）—— 在 main() 之前執行
//       ● 需要呼叫建構函式、或初值要執行期才算得出來的，都屬於這一類
//   ★ 出問題的永遠是階段二。階段一在程式碼開始跑之前就完成了，不可能有順序問題。
//   本檔的 Config config; 因為有使用者定義的建構函式，屬於動態初始化。
//
// 【2. 為什麼跨檔案的順序無法保證？】
//   編譯器一次只看一個翻譯單元。編譯 a.cpp 時，它根本不知道 b.cpp 存在，
//   更不知道 b.cpp 裡有沒有全域物件、要不要先初始化。
//   實際順序由連結器把各個目標檔的初始化函式（.init_array 區段）
//   串起來的順序決定，而那個順序取決於：
//       連結命令列上 .o 的排列、是否被打包進 .a、動態庫的載入順序……
//   這些都是建置設定，不是語言語意。所以標準只能說「未指定」。
//   ★ 最危險的是：它常常「剛好正確」。你在開發機上測沒問題，
//     改了 CMake 裡的檔案順序、或換成 Release 建置，就突然壞掉——
//     而且壞的形式是讀到一個「已歸零但尚未建構」的物件，
//     看起來像資料錯誤而不像崩潰，極難追查。
//
// 【3. 本檔展示的是「安全的那一半」】
//   本檔把 Config 與 ConnectionPool 都定義在同一個 .cpp：
//       Config config;                 // 先定義
//       ConnectionPool pool(config);   // 後定義，依賴 config
//   同一翻譯單元內，標準保證依定義順序初始化，所以這段是安全的，
//   輸出必定是 [Config] 先於 [ConnectionPool]。
//   ★ 但只要把這兩行搬到不同的 .cpp，保證立刻消失——
//     pool 可能在 config 還是全零的狀態下就被建構，
//     讀到 maxConnections == 0，池子大小變成 0。
//   6.cpp 示範的就是標準解法。
//
// 【4. 三種解法】
//   (a) Construct On First Use（把全域物件包進函式，用靜態區域變數）
//       → 最常用、最推薦，見 6.cpp。初始化順序由「呼叫順序」決定，
//         而呼叫順序是程式邏輯，完全可控。
//   (b) 只用常數初始化（constant initialization）
//       → 若型別是 constexpr 可建構的，就完全跳過動態初始化階段。
//         C++20 的 constinit 關鍵字可以讓編譯器「強制檢查」這一點：
//             constinit int limit = 100;   // 若無法常數初始化就編譯錯誤
//       ★ 注意 constinit 只保證「在階段一完成初始化」，
//         它不是 const，變數之後仍可修改。
//   (c) 明確的初始化函式，由 main() 開頭呼叫
//       → 最笨但最白，順序完全寫在程式碼裡。
//
// 【概念補充 Concept Deep Dive】
//   ● 零初始化一定先發生，所以「未初始化的全域物件」讀起來是 0 而不是垃圾，
//     這和「未初始化的區域變數」（不確定值）完全不同。
//     這也是為什麼此問題症狀是「值是 0」而不是「值是亂數」。
//   ● 還有一個對稱的問題：解構順序（destruction order fiasco）。
//     靜態物件依「建構完成的反序」解構，所以跨檔案的解構順序同樣未指定。
//     若 A 的解構函式用到 B，而 B 先解構，就是使用已銷毀物件。
//   ● 動態程式庫讓情況更複雜：dlopen 載入的程式庫，其全域物件在
//     載入當下才初始化，可能發生在 main() 執行中途。
//   ● 「未指定（unspecified）」不等於「未定義（undefined）」：
//     順序是未指定的（實作可自由選擇且不必說明），
//     但一旦你在 config 尚未初始化時讀它的成員，那才是未定義行為。
//
// 【注意事項 Pay Attention】
//   1. 同一個 .cpp 內：依定義順序初始化，這是有保證的（本檔即是）。
//   2. 跨 .cpp：順序未指定，絕對不可依賴。它「碰巧能跑」是最危險的情況。
//   3. 症狀通常是讀到 0（因為零初始化先發生），不是崩潰，因此很難察覺。
//   4. 解構順序有對稱的問題，且同樣不可依賴。
//   5. 本檔輸出中，兩行初始化訊息會出現在 "=== 程式開始 ===" 之前——
//      因為它們發生在 main() 執行之前。
//   6. C++20 的 constinit 可強制「必須在編譯期完成初始化」，
//      是預防此問題的工具之一；但本檔以 C++17 編譯，僅作說明。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】全域物件的初始化順序
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 什麼是 static initialization order fiasco？為什麼標準無法保證順序？
//     答：不同翻譯單元中，具靜態儲存期物件的「動態初始化」順序是未指定的。
//         因為編譯器一次只看一個 .cpp，不知道別的檔案有什麼全域物件；
//         實際順序由連結器串接 .init_array 的順序決定，
//         而那取決於連結命令列與建置設定，不是語言語意。
//         同一個 .cpp 內則有保證：依定義出現的順序初始化。
//     追問：這個 bug 的典型症狀是什麼？
//         → 讀到 0，而不是亂數。因為所有靜態物件在動態初始化之前
//           已先被零初始化，所以會拿到一個「合法但錯誤」的值，
//           不會崩潰，因此特別難追。
//
// 🔥 Q2. 標準解法是什麼？為什麼它有效？
//     答：Construct On First Use——把全域物件改成函式內的靜態區域變數，
//         回傳其參考（見 6.cpp）。有效的原因是：初始化時機從
//         「程式啟動時、順序由連結器決定」變成「第一次呼叫該函式時」，
//         而呼叫順序是你自己寫的程式邏輯，完全可控。
//         附帶好處：C++11 起這個初始化還是執行緒安全的。
//     追問：C++20 有沒有語言層級的工具？
//         → 有 constinit，強制變數必須在編譯期完成初始化，
//           編譯器會檢查；無法常數初始化就直接編譯錯誤，
//           從根本上避開動態初始化階段。注意它不等於 const。
//
// ⚠️ 陷阱. 「我測過了，順序是對的，所以沒問題」——這個推論錯在哪？
//     答：錯在把「未指定行為」當成「觀察到的行為」。順序取決於連結順序、
//         編譯器版本、最佳化等級、是否打包成靜態庫。今天對，
//         明天改了 CMakeLists 裡兩個檔案的先後就可能反過來，
//         而且不會有任何警告。
//     為什麼會錯：這是把「測試通過」誤當成「行為有保證」的典型。
//         測試只能證明「在這一組建置設定下這次是對的」，
//         無法證明語言給了你保證。凡是標準寫「未指定」的地方，
//         唯一安全的做法是改成不依賴它，而不是驗證它。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

// === 檔案 A 中（概念上）===
class Config {
private:
    int maxConnections;
public:
    Config() : maxConnections(100) {
        cout << "  [Config] 初始化: maxConnections = " 
             << maxConnections << endl;
    }
    int getMax() const { return maxConnections; }
};

// === 檔案 B 中（概念上）===
class ConnectionPool {
private:
    int poolSize;
public:
    // 危險！這裡依賴 config 已經被初始化
    ConnectionPool(const Config& cfg) : poolSize(cfg.getMax()) {
        cout << "  [ConnectionPool] 初始化: poolSize = " 
             << poolSize << endl;
    }
};

// 全域對象
Config config;                    // 在這個檔案中先宣告
ConnectionPool pool(config);      // 依賴 config

// -----------------------------------------------------------------------------
// 【示範】零初始化先發生：所以「還沒建構」讀到的是 0，不是垃圾值
//   Counter 沒有使用者定義的建構函式，且下面這個全域物件是零初始化的。
//   這說明了為什麼 fiasco 的症狀是「拿到 0」而不是「拿到亂數」——
//   這也是它難以被發現的根本原因。
//   注意：這裡印的是「已完成零初始化的靜態物件」，值必定為 0，
//         與「未初始化的區域變數」（不確定值、讀取即 UB）完全不同。
// -----------------------------------------------------------------------------
struct RawCounter {
    int hits;          // 沒有建構函式 → 全域實例走零初始化（階段一）
    long total;
};
RawCounter g_counter;  // 保證在程式載入時就是全零

// -----------------------------------------------------------------------------
// 【日常實務範例】為什麼「日誌系統」最常踩到這個坑
//   情境：Logger 是全域的，而其他模組的全域物件想在自己的建構函式裡寫 log。
//   若 Logger 比它們晚初始化，那些 log 呼叫就會用到尚未建構的 Logger。
//   下面用一個旗標示範「如何自我防衛」：物件明確記錄自己是否已就緒，
//   使用前先檢查。這不是根本解法（根本解法見 6.cpp），
//   但在無法改動架構時，至少能把「安靜的錯誤」變成「明確的警告」。
// -----------------------------------------------------------------------------
class Logger {
    bool ready;                 // 零初始化階段就是 false
    int  lineCount;
public:
    Logger() : ready(true), lineCount(0) {
        cout << "  [Logger] 初始化完成" << endl;
    }
    void write(const string& msg) {
        if (!ready) {
            // 零初始化保證 ready 在建構前一定是 false，所以這個檢查可靠
            cout << "  [Logger] !! 尚未初始化就被使用，訊息遺失: " << msg << endl;
            return;
        }
        ++lineCount;
        cout << "  [Logger] " << msg << endl;
    }
    int count() const { return ready ? lineCount : -1; }
};

Logger g_logger;   // 定義在 g_counter 之後，同檔案內順序有保證

// 註：本檔不加 LeetCode 範例。
//     初始化順序問題屬於「程式啟動期與連結模型」的議題，
//     LeetCode 的單檔評測環境根本不會出現跨翻譯單元的情境，
//     硬掛一題會完全失去這個主題的意義，故從缺。

int main() {
    cout << "\n=== 程式開始 ===" << endl;
    cout << "  在同一個編譯單元中，順序是確定的" << endl;
    cout << "  但如果 config 和 pool 在不同的 .cpp 檔案中..." << endl;
    cout << "  初始化順序就是未定義的！" << endl;

    cout << "\n=== 零初始化：為什麼症狀是「0」而不是亂數 ===" << endl;
    cout << "  g_counter.hits  = " << g_counter.hits  << endl;
    cout << "  g_counter.total = " << g_counter.total << endl;
    cout << "  → 靜態物件在動態初始化之前，已先被填 0（階段一）" << endl;
    cout << "  → 所以讀到「還沒建構」的全域物件會拿到 0，看起來像合法資料" << endl;

    cout << "\n=== 日常實務：Logger 的自我防衛 ===" << endl;
    g_logger.write("服務啟動");
    g_logger.write("載入設定完成");
    cout << "  已寫入行數 = " << g_logger.count() << endl;
    cout << "  註：本檔 g_logger 與 main 在同一單元且已完成初始化，" << endl;
    cout << "      故 ready 為 true；跨 .cpp 時就不再有這個保證" << endl;

    return 0;
}
// 這段程式碼展示了全域對象初始化順序的問題：
// - Config 和 ConnectionPool 是全域對象，且 ConnectionPool 依賴 Config。
// - 如果它們在同一個 .cpp 檔案中，則 Config 會先被初始化，然後 ConnectionPool 會使用它，這是安全的。
// - 但是，如果 Config 和 ConnectionPool 分別在不同的 .cpp 檔案中，則它們的初始化順序是未定義的，
// 可能會導致 ConnectionPool 在 Config 之前被初始化，從而使用未初始化的 Config，造成未定義行為。  

// 編譯: g++ -std=c++17 -Wall -Wextra "第 18 課：對象的生命週期（Object Lifetime）5.cpp" -o life5

// 【輸出說明】前三行（Config / ConnectionPool / Logger 的初始化訊息）
//   出現在 "=== 程式開始 ===" 之前，因為全域物件的動態初始化
//   發生在 main() 執行之前。同一翻譯單元內依定義順序，故順序固定。

// === 預期輸出 ===
//   [Config] 初始化: maxConnections = 100
//   [ConnectionPool] 初始化: poolSize = 100
//   [Logger] 初始化完成
//
// === 程式開始 ===
//   在同一個編譯單元中，順序是確定的
//   但如果 config 和 pool 在不同的 .cpp 檔案中...
//   初始化順序就是未定義的！
//
// === 零初始化：為什麼症狀是「0」而不是亂數 ===
//   g_counter.hits  = 0
//   g_counter.total = 0
//   → 靜態物件在動態初始化之前，已先被填 0（階段一）
//   → 所以讀到「還沒建構」的全域物件會拿到 0，看起來像合法資料
//
// === 日常實務：Logger 的自我防衛 ===
//   [Logger] 服務啟動
//   [Logger] 載入設定完成
//   已寫入行數 = 2
//   註：本檔 g_logger 與 main 在同一單元且已完成初始化，
//       故 ready 為 true；跨 .cpp 時就不再有這個保證
