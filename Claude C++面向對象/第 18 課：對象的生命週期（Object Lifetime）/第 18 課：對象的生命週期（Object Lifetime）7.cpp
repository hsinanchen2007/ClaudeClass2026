// =============================================================================
//  第 18 課：對象的生命週期 7  —  臨時對象與 const 引用的生命週期延長
// =============================================================================
//
// 【主題資訊 Information】
//   主題：臨時物件（temporary object）的存活期間，以及 const 參考
//         如何延長它的生命週期。
//   核心規則：臨時物件在「完整運算式（full-expression）結束時」銷毀，
//             也就是該敘述的分號處。
//   例外規則：若把臨時物件綁定到 const 左值參考（或右值參考），
//             其生命週期延長到該參考的作用域結束。
//   標準版本：生命週期延長 C++98 起即有；
//             ★ C++17 起「保證的複製省略（guaranteed copy elision）」
//               改變了 T x = f(); 的語意——見下方第 4 點。
//   標頭檔：<iostream>、<string>
//
// 【詳細解釋 Explanation】
//
// 【1. 什麼是完整運算式？為什麼臨時物件活到分號】
//   完整運算式大致上就是「一個完整的敘述」。臨時物件在它結束時銷毀，
//   所以下面這行的臨時物件，在分號處才死：
//       Temp("短命鬼").show();       // show() 執行完 → 分號 → 解構
//   這個規則保證了鏈式呼叫的安全：整條鏈跑完之前，臨時物件都活著。
//   ★ 一個真實陷阱：
//       const char* p = std::string("abc").c_str();   // 危險！
//     臨時 string 在分號處就死了，p 立刻變成懸空指標。
//     這是實務上最常見的臨時物件錯誤之一。
//
// 【2. const 參考為什麼能延長生命？】
//       const Temp& ref = Temp("引用續命");
//   如果沒有這條規則，ref 一綁定完就懸空，這個語法將完全無用。
//   標準因此規定：把臨時物件綁到 const 左值參考時，
//   臨時物件的生命週期延長到「該參考的生命週期結束」為止。
//   ★ 這條規則的真正價值不在上面那行，而在函式參數：
//       void print(const std::string& s);
//       print("hello");            // 產生臨時 string，活到 print 返回
//     正因為有它，「傳 const& 可以直接吃字面量」才成立。
//
// 【3. 生命週期延長的三個重要例外（面試高頻）】
//   延長規則有嚴格的邊界，以下三種情況「不會」延長：
//     (a) 透過函式回傳的參考：
//           const std::string& f() { return std::string("x"); }  // 懸空
//         回傳值綁定的臨時物件在 f 結束時就銷毀，呼叫端拿到懸空參考。
//     (b) 建構函式初始化列表中的成員參考：
//           struct S { const std::string& r; S() : r(std::string("x")) {} };
//         臨時物件在建構函式結束時銷毀，成員 r 隨即懸空。
//     (c) 只延長「直接綁定」的那一個臨時物件，不會延長它內部的子物件：
//           const int& r = std::vector<int>{1,2,3}[0];   // vector 仍在分號處死
//   ★ 記憶方式：延長只在「臨時物件直接綁到一個具名的區域參考」時發生。
//     一旦中間隔了函式回傳或成員，就不適用。
//
// 【4. C++17 的保證複製省略，改變了什麼】
//   本檔的 createTemp() 回傳 Temp(n)，呼叫端寫：
//       Temp saved = createTemp("被拯救的");
//   ● C++14 以前：語意上是「建臨時物件 → 複製/搬移到 saved」，
//     編譯器「允許」但不強制省略那次複製（RVO 是最佳化）。
//   ● C++17 起：這不再是最佳化而是語言規則——
//     prvalue 直接在目的地初始化，「根本沒有臨時物件被建立」，
//     因此不需要可用的複製/搬移建構函式。
//   這就是為什麼本檔輸出只看到一次 [+] 被拯救的，沒有任何複製痕跡。
//   ★ 面試時要能區分：C++17 之前是「可以省略」，之後是「必然不存在」。
//
// 【概念補充 Concept Deep Dive】
//   ● 為什麼只有 const 參考能綁臨時物件（非 const 左值參考不行）？
//     因為修改一個即將消失的物件幾乎一定是筆誤——寫進去的值馬上就沒了。
//     標準因此禁止 T& 綁右值。C++11 引入 T&&（右值參考）之後，
//     「我確實想修改這個即將消失的物件（為了搬移它的資源）」
//     才有了正確的表達方式，這正是 move 語意的基礎。
//   ● 延長的實作方式：編譯器把臨時物件放進參考所在作用域的堆疊空間，
//     並在該作用域結束處插入解構呼叫——和一個普通區域變數完全相同。
//   ● 本檔的 show() 回傳 const Temp&，回傳的是 *this（不是臨時物件），
//     所以安全。若它回傳的是函式內建立的臨時物件，就會是上述例外 (a)。
//
// 【注意事項 Pay Attention】
//   1. 臨時物件在「分號」處銷毀，不是在「用完」時銷毀。
//   2. const char* p = std::string(...).c_str(); 是懸空指標，
//      這是實務上最常踩的臨時物件陷阱。
//   3. 生命週期延長不適用於：函式回傳的參考、成員初始化列表中的參考。
//   4. 延長只作用於「直接綁定」的臨時物件本身，不含其子物件。
//   5. C++17 起 T x = f(); 保證不產生臨時物件（不是最佳化，是語言規則）。
//   6. 非 const 左值參考不能綁臨時物件；要修改臨時物件請用右值參考 T&&。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】臨時對象與生命週期延長
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 臨時物件什麼時候被銷毀？const 參考如何改變這件事？
//     答：預設在「完整運算式結束時」，也就是該敘述的分號處。
//         若把臨時物件綁定到 const 左值參考（或右值參考），
//         生命週期會延長到該參考的作用域結束。
//         這條規則讓「傳 const std::string& 可以直接吃字面量」成立。
//     追問：那 const char* p = std::string("abc").c_str(); 安全嗎？
//         → 不安全。這裡延長規則不適用（綁的是指標不是參考），
//           臨時 string 在分號處就銷毀，p 立刻懸空。
//
// 🔥 Q2. 生命週期延長有哪些例外？
//     答：三個。(1) 透過函式回傳的參考——臨時物件在函式結束時就死，
//         呼叫端拿到懸空參考；(2) 建構函式初始化列表中綁到成員參考——
//         臨時物件在建構函式結束時就死；(3) 只延長直接綁定的那個臨時物件，
//         不延長它內部的子物件。
//     追問：一句話怎麼記？
//         → 只有「臨時物件直接綁到具名的區域參考」才會延長；
//           中間一旦隔了函式回傳或類別成員，就不適用。
//
// ⚠️ 陷阱. Temp saved = createTemp("x"); 這行會發生幾次建構、幾次複製？
//     答：在 C++17 中只有一次建構、零次複製，而且這不是最佳化——
//         標準規定 prvalue 直接在 saved 的位置初始化，根本沒有臨時物件產生，
//         連可用的複製/搬移建構函式都不需要。本檔輸出只有一個 [+] 可證。
//     為什麼會錯：多數人記得「編譯器會做 RVO 幫你省掉複製」，
//         於是回答「語意上有一次複製，但被最佳化掉了」。
//         這在 C++14 以前正確，C++17 之後就不對了——
//         差別在於：以前是「允許省略」（複製建構函式仍必須可用），
//         現在是「那個物件從不存在」（即使複製建構函式被 delete 也能編譯）。
//         把最佳化和語言規則混為一談，是這題最常見的失分點。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <vector>
using namespace std;

class Temp {
private:
    string name;
public:
    Temp(const string& n) : name(n) {
        cout << "  [+] " << name << endl;
    }
    ~Temp() {
        cout << "  [-] " << name << endl;
    }
    
    string getName() const { return name; }
    
    // 返回自身引用，用於鏈式調用
    // 注意：返回 const Temp& 可以防止修改臨時對象
    // 這裡的 const Temp& 是為了展示臨時對象的生命週期，實際上在這個例子中並不會有修改行為
    // 返回 const Temp& 可以讓我們在調用 show() 後仍然能夠使用該對象，因為它的生命週期被延長了
    const Temp& show() const {
        cout << "  [=] " << name << " 展示中" << endl;
        return *this;
    }
};

Temp createTemp(const string& n) {
    return Temp(n);   // 返回一個臨時對象
}

// -----------------------------------------------------------------------------
// 【示範】臨時物件確實活到「分號」，而不是「用完就死」
//   兩次 show() 串在同一個敘述裡，臨時物件在整條鏈跑完後才解構。
// -----------------------------------------------------------------------------
void chainDemo() {
    Temp("鏈式呼叫").show().show();      // 兩次 show 之後才到分號
    cout << "  (分號已過，臨時物件已解構)" << endl;
}

// -----------------------------------------------------------------------------
// 【日常實務範例】傳 const& 讓函式直接吃字面量，不必手動建暫存變數
//   情境：一個記錄稽核事件的函式。呼叫端常常直接傳字串字面量。
//   因為參數是 const std::string&，編譯器會產生一個臨時 string，
//   並讓它活到函式返回為止——這正是生命週期延長規則在日常的體現。
//   若參數寫成 std::string&（非 const），"user login" 這種字面量
//   根本無法傳入，編譯就會失敗。
// -----------------------------------------------------------------------------
class AuditLog {
    vector<string> entries;
public:
    // const& 讓呼叫端可以直接傳字面量、也可以傳既有的 string
    void record(const string& action, const string& actor) {
        entries.push_back(actor + " -> " + action);
    }
    void dump() const {
        for (size_t i = 0; i < entries.size(); ++i) {
            cout << "    " << (i + 1) << ". " << entries[i] << endl;
        }
    }
    size_t size() const { return entries.size(); }
};

// 註：本檔不加 LeetCode 範例。
//     臨時物件的生命週期是語言的求值模型，不影響任何題目的演算法；
//     LeetCode 也不會考「臨時物件何時解構」，硬掛一題會失焦，故從缺。

int main() {
    cout << "=== 臨時對象的生命週期 ===" << endl;
    
    // 情境 1：臨時對象在語句結束時死亡
    cout << "\n--- 情境 1：純臨時對象 ---" << endl;
    Temp("短命鬼").show();
    cout << "  (臨時對象已死亡)\n" << endl;
    // Temp("短命鬼") 在這行的分號處就被解構了
    
    // 情境 2：用變數接住，延長生命
    cout << "--- 情境 2：用變數接住 ---" << endl;
    Temp saved = createTemp("被拯救的");
    saved.show();
    cout << "  (saved 還活著)\n" << endl;
    
    // 情境 3：const 引用延長臨時對象的生命
    cout << "--- 情境 3：const 引用延長生命 ---" << endl;
    const Temp& ref = Temp("引用續命");
    ref.show();
    cout << "  (ref 綁定的臨時對象還活著)\n" << endl;
    // 臨時對象的生命被延長到 ref 離開作用域時

    // 情境 4：臨時物件活到「分號」，整條鏈都安全
    cout << "--- 情境 4：鏈式呼叫 ---" << endl;
    chainDemo();
    cout << endl;

    // 情境 5：C++17 保證複製省略 —— 只有一次建構，沒有複製
    cout << "--- 情境 5：C++17 保證複製省略 ---" << endl;
    cout << "  createTemp() 回傳 prvalue，直接在目的地初始化" << endl;
    Temp elided = createTemp("零複製");
    cout << "  上面只出現一個 [+]，代表沒有任何臨時物件被複製" << endl;
    (void)elided;
    cout << endl;

    cout << "=== 日常實務：const& 參數直接吃字面量 ===" << endl;
    {
        AuditLog log;
        log.record("user login", "alice");        // 兩個引數都是字面量
        log.record("delete file", "bob");         // 產生臨時 string，活到函式返回
        string action = "update config";          // 也可以傳既有的 string
        log.record(action, "carol");
        cout << "  稽核記錄（共 " << log.size() << " 筆）：" << endl;
        log.dump();
        cout << "  → 若參數寫成非 const 的 string&，字面量將無法傳入" << endl;
    }
    cout << endl;
    
    cout << "=== main() 結束 ===" << endl;
    return 0;
}
// 這段程式碼展示了 C++ 中臨時對象的生命週期：
// - 在情境 1 中，臨時對象在語句結束時被解構，無法再使用。
// - 在情境 2 中，將臨時對象賦值給  變數 saved，延長了它的生命週期，直到 saved 離開作用域時才被解構。
// - 在情境 3 中，使用 const 引用 ref 綁定臨時對象，延長了它的生命週期，直到 ref 離開作用域時才被解構。 
// 這些情境展示了 C++ 中對象生命週期的不同管理方式，以及如何利用變數和引用來控制對象的存活時間。

// 編譯: g++ -std=c++17 -Wall -Wextra "第 18 課：對象的生命週期（Object Lifetime）7.cpp" -o life7

// 【輸出說明】main() 結束時的解構順序是反向建構順序（LIFO）：
//   零複製 → 引用續命 → 被拯救的，正好與它們的建立順序相反。

// === 預期輸出 ===
// === 臨時對象的生命週期 ===
//
// --- 情境 1：純臨時對象 ---
//   [+] 短命鬼
//   [=] 短命鬼 展示中
//   [-] 短命鬼
//   (臨時對象已死亡)
//
// --- 情境 2：用變數接住 ---
//   [+] 被拯救的
//   [=] 被拯救的 展示中
//   (saved 還活著)
//
// --- 情境 3：const 引用延長生命 ---
//   [+] 引用續命
//   [=] 引用續命 展示中
//   (ref 綁定的臨時對象還活著)
//
// --- 情境 4：鏈式呼叫 ---
//   [+] 鏈式呼叫
//   [=] 鏈式呼叫 展示中
//   [=] 鏈式呼叫 展示中
//   [-] 鏈式呼叫
//   (分號已過，臨時物件已解構)
//
// --- 情境 5：C++17 保證複製省略 ---
//   createTemp() 回傳 prvalue，直接在目的地初始化
//   [+] 零複製
//   上面只出現一個 [+]，代表沒有任何臨時物件被複製
//
// === 日常實務：const& 參數直接吃字面量 ===
//   稽核記錄（共 3 筆）：
//     1. alice -> user login
//     2. bob -> delete file
//     3. carol -> update config
//   → 若參數寫成非 const 的 string&，字面量將無法傳入
//
// === main() 結束 ===
//   [-] 零複製
//   [-] 引用續命
//   [-] 被拯救的
