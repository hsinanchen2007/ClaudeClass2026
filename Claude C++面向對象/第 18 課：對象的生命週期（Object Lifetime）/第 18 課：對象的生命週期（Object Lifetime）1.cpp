// =============================================================================
//  第 18 課 範例 1  —  四種儲存期（storage duration）的生命週期對照
// =============================================================================
//
// 【主題資訊 Information】
//   標準版本：C++98 起有四種儲存期；C++11 起新增 thread_local，
//             且「函式內 static 區域變數的初始化」保證為執行緒安全
//   標頭檔  ：本範例僅需 <iostream>、<string>
//   四種儲存期：
//     static      靜態儲存期  ：全域、namespace 範圍、static 成員、函式內 static
//     automatic   自動儲存期  ：一般的區域變數（放在 stack）
//     dynamic     動態儲存期  ：new 出來的物件（放在 heap），由 delete 結束
//     thread      執行緒儲存期：thread_local 變數（C++11 起，本範例未示範）
//
// 【詳細解釋 Explanation】
//
// 【1. 「生命週期」與「作用域」是兩件不同的事】
//   這是本課最容易混淆、也最重要的一組概念：
//     * 作用域 (scope)     ：這個「名字」在原始碼的哪些地方看得見 —— 編譯期概念。
//     * 生命週期 (lifetime)：這個「物件」在執行期存活多久 —— 執行期概念。
//   函式內的 static 區域物件正好示範兩者的分離：
//   它的「名字」只在函式內看得見（作用域是區域的），
//   但它的「物件」從第一次執行到該行起、一直活到程式結束（生命週期是靜態的）。
//   所以第二次呼叫 func() 時，看不到它再次「誕生」—— 它根本沒死過。
//
// 【2. 四種儲存期各自的起訖點】
//   (a) 全域物件（靜態儲存期）
//       建構：main() 開始「之前」。所以輸出的第一行會在 "=== main() 開始 ===" 之前。
//       解構：main() 結束「之後」，以建構順序的反序進行。
//   (b) 區域物件（自動儲存期）
//       建構：執行到宣告那一行。
//       解構：離開所在 scope（`}`）時，反序解構。
//       每次呼叫 func() 都會產生一個全新的物件，並在函式結束時銷毀。
//   (c) 函式內 static 區域物件（靜態儲存期）
//       建構：「第一次」執行到那一行時才建構（lazy initialization），且只做一次。
//       解構：程式結束時，與全域物件一起、以建構順序的反序解構。
//   (d) 動態物件（動態儲存期）
//       建構：執行 new 時。
//       解構：執行 delete 時 —— 完全由程式設計師決定，這是唯一「不自動」的一種。
//       忘了 delete 就是記憶體洩漏；delete 兩次是未定義行為。
//
// 【3. 程式結束時的解構順序】
//   所有靜態儲存期物件（全域 + 函式內 static）共用同一份「反序」規則：
//   誰先完成建構，誰就後解構。本範例中：
//       建構順序：globalObj（main 之前） → staticLocal（第一次呼叫 func 時）
//       解構順序：staticLocal → globalObj
//   注意 staticLocal 雖然寫在函式裡，卻比 globalObj 晚建構，所以比它早解構。
//
// 【概念補充 Concept Deep Dive】
//   函式內 static 區域變數的實作（俗稱 magic static）：
//   編譯器會為它額外配置一個隱藏的「守衛變數」（guard variable），
//   大致等價於：
//       if (!guard_for_staticLocal) {
//           construct(staticLocal);
//           guard_for_staticLocal = true;
//           register_atexit_destructor(staticLocal);   // 登記程式結束時要解構
//       }
//   C++11 起標準規定這個檢查必須是執行緒安全的：
//   若兩個執行緒同時第一次抵達該行，其中一個負責建構，另一個會「阻塞等待」
//   直到建構完成。注意這是標準保證的阻塞，不是未定義行為，也不是資料競爭。
//   （在 C++11 之前這並無保證，是當年 double-checked locking 那類寫法的由來。）
//   代價是每次讀取都要檢查一次守衛變數，不過在多數平台上這只是一個
//   已載入快取的旗標比較，成本極低。
//
//   記憶體位置上：全域與 static 區域物件放在資料區（.data/.bss），
//   區域物件放在 stack，new 出來的放在 heap。三者的存續規則完全獨立。
//
// 【注意事項 Pay Attention】
// 1. 跨 translation unit 的全域物件，建構順序是「未指定」的
//    （static initialization order fiasco）。若 A.cpp 的全域物件在建構時
//    依賴 B.cpp 的全域物件，可能讀到尚未建構的物件。
//    標準解法：改用「函式內 static 區域物件」（Meyers Singleton），
//    把初始化延後到第一次真正使用時，順序就確定了。
// 2. 動態物件是唯一需要手動負責的：new/delete 必須配對。
//    現代 C++ 應優先使用 unique_ptr / make_unique，讓它退回自動管理。
// 3. 解構函數不會在 std::abort()、std::_Exit() 等情況下執行；
//    靜態物件的解構也不會發生。
// 4. 本範例使用 `using namespace std;`，教學小檔可接受，
//    但標頭檔與大型專案應避免。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】儲存期與物件生命週期
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 函式內的 static 區域變數，什麼時候建構？什麼時候解構？
//     答：第一次執行到它的宣告那一行時才建構（lazy），而且只建構一次；
//         之後每次呼叫都直接沿用同一個物件。解構發生在程式結束時，
//         與其他靜態儲存期物件一起，以「建構順序的反序」進行。
//         它的作用域是區域的，但生命週期是靜態的 —— 兩者分離。
//     追問：多執行緒下同時第一次呼叫這個函式，會不會建構兩次？
//         → 不會。C++11 起標準保證這個初始化是執行緒安全的：
//           一個執行緒負責建構，其他執行緒阻塞等待完成。
//           這是標準規定的阻塞行為，不是資料競爭，也不是未定義行為。
//           C++11 之前則沒有這個保證。
//
// 🔥 Q2. 全域物件的建構發生在 main() 之前，這會帶來什麼問題？
//     答：跨 translation unit 的全域物件，其建構順序是未指定的
//         （static initialization order fiasco）。若某個 TU 的全域物件
//         在自己的建構函數裡使用了另一個 TU 的全域物件，
//         有可能用到尚未建構完成的物件。
//     追問：那要怎麼解？
//         → 改成 Meyers Singleton：把物件包進函式內的 static 區域變數，
//           `T& get() { static T inst; return inst; }`。
//           初始化被延後到第一次呼叫，順序由呼叫關係決定，
//           而且順帶獲得 C++11 的執行緒安全保證。
//
// ⚠️ 陷阱. 「static 區域變數的作用域只在函式內，
//           所以離開函式時它就被銷毀了，下次呼叫再重新建立。」
//     答：錯，而且錯在把「作用域」和「生命週期」混為一談。
//         離開函式時被銷毀的是「自動儲存期」的區域物件；
//         static 區域物件的生命週期是靜態的，它一直活到程式結束。
//         本範例第二次呼叫 func() 時，只會看到 localObj 誕生，
//         看不到 staticLocal 再次誕生 —— 因為它從來沒有死過。
//     為什麼會錯：直覺上把「看不見它」等同於「它不存在」。
//         正確的心智模型是：作用域管的是「名字在哪裡可以使用」（編譯期），
//         生命週期管的是「物件在記憶體中活多久」（執行期），兩者互不相干。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class Probe {
private:
    string name;
public:
    Probe(const string& n) : name(n) {
        cout << "  [誕生] " << name << endl;
    }
    ~Probe() {
        cout << "  [死亡] " << name << endl;
    }
    void hello() const {
        cout << "  [存活] " << name << " 正在工作" << endl;
    }
};

// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】—— 本檔不附
//   理由：本檔主題是「四種儲存期的建構與解構時機」，屬於語言的物件模型，
//   關心的是「何時發生」而不是「算出什麼」。LeetCode 題目不會因為
//   變數放在 stack、heap 還是資料區而有不同答案，硬掛一題只會失焦。
//   依規格「寧缺勿濫」從缺，改附一個真正依賴 static 區域儲存期的實務情境。
// -----------------------------------------------------------------------------

// ====== 1. 靜態存儲期：全域對象 ======
Probe globalObj("全域物件");

void func() {
    // ====== 2. 自動存儲期：局部對象 ======
    // 每次呼叫都建立一個全新的物件，函式結束時銷毀
    Probe localObj("func 局部物件");
    localObj.hello();

    // ====== 3. 靜態存儲期：靜態局部對象 ======
    // 只在「第一次」執行到這一行時建構；之後每次呼叫都沿用同一個物件
    static Probe staticLocal("func 靜態局部物件");
    staticLocal.hello();
}

// -----------------------------------------------------------------------------
// 【日常實務範例】延遲載入的設定檔（Meyers Singleton）
//   情境：整個程式共用一份設定。要求是：
//     (1) 只在第一次真正需要時才去讀檔（啟動時不要無謂地做 I/O）；
//     (2) 只讀一次，之後共用；
//     (3) 不要用全域物件 —— 那會落入跨 TU 建構順序未指定的陷阱。
//   解法：把它放進函式內的 static 區域變數。
//         初始化被延後到第一次呼叫 config()，順序因此變得確定，
//         而且 C++11 起這個初始化本身就是執行緒安全的。
//   這是 C++ 實務中最常見、也最推薦的單例寫法。
// -----------------------------------------------------------------------------
class AppConfig {
    string source;
public:
    explicit AppConfig(const string& src) : source(src) {
        cout << "  [設定] 第一次使用時才載入: " << source << endl;
    }
    ~AppConfig() {
        cout << "  [設定] 釋放設定資源: " << source << endl;
    }
    void dump() const {
        cout << "  [設定] 目前來源 = " << source << endl;
    }
};

AppConfig& config() {
    // 第一次呼叫才建構；之後每次都回傳同一個物件
    static AppConfig instance("/etc/myapp/config.yaml");
    return instance;
}

int main() {
    cout << "\n=== main() 開始 ===" << endl;
    cout << "（注意：全域物件的 [誕生] 出現在這一行之前）" << endl;

    cout << "\n--- 第一次調用 func() ---" << endl;
    func();

    cout << "\n--- 第二次調用 func() ---" << endl;
    func();
    cout << "（staticLocal 沒有再次 [誕生]，因為它從未死過）" << endl;

    cout << "\n--- 動態對象 ---" << endl;
    // ====== 4. 動態存儲期：堆上對象 ======
    // 唯一需要手動負責生命週期的一種
    Probe* heapObj = new Probe("動態物件");
    heapObj->hello();
    delete heapObj;      // 少了這一行就是記憶體洩漏

    cout << "\n--- 日常實務：延遲載入的設定 ---" << endl;
    cout << "  尚未呼叫 config()，設定還沒被載入" << endl;
    config().dump();     // ← 第一次呼叫，此時才建構
    cout << "  再次取用設定：" << endl;
    config().dump();     // ← 沿用同一個物件，不會再載入一次

    cout << "\n=== main() 結束 ===" << endl;
    return 0;
}
