//
// --- 動態對象 ---
//   [誕生] 動態物件
//   [存活] 動態物件 正在工作
//   [死亡] 動態物件
//
// --- 日常實務：延遲載入的設定 ---
//   尚未呼叫 config()，設定還沒被載入
//   [設定] 第一次使用時才載入: /etc/myapp/config.yaml
//   [設定] 目前來源 = /etc/myapp/config.yaml
//   再次取用設定：
//   [設定] 目前來源 = /etc/myapp/config.yaml
//
// === main() 結束 ===
//   [設定] 釋放設定資源: /etc/myapp/config.yaml
//   [死亡] func 靜態局部物件
//   [死亡] 全域物件
