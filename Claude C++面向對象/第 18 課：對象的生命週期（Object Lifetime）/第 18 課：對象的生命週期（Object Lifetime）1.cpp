// staticLocal、config 的 instance、globalObj 都在 main() 結束後才解構，
// 且順序為「建構順序的反序」

// 編譯: g++ -std=c++17 -Wall -Wextra "第 18 課：對象的生命週期（Object Lifetime）1.cpp" -o lifetime1

// ── 輸出但書 ────────────────────────────────────────────────────────────
