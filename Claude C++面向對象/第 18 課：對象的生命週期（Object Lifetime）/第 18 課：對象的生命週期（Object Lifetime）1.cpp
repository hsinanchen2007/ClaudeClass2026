// 2. 請對照三個關鍵觀察：
//    (a)「[誕生] 全域物件」出現在 "=== main() 開始 ===" 之前
//       —— 全域物件在 main() 之前就建構完成。
//    (b) 第二次呼叫 func() 時，只有 localObj 再次 [誕生]，
//       staticLocal 沒有 —— 它只建構一次。
//    (c) 程式結束後的解構順序是建構順序的反序：
//       設定 instance → staticLocal → globalObj。
//       staticLocal 雖然寫在函式裡，卻因為比 globalObj 晚建構而先解構。
// 3. 本檔全域物件只有一個，故不涉及「跨 TU 建構順序未指定」的問題；
