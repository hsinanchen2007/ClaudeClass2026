// =============================================================================
//  第 18 課 範例 10  —  vector 容器中元素的生命週期（暫時物件、複製、擴容）
// =============================================================================
//
// 【主題資訊 Information】
//   標準版本：C++98 起有 push_back；C++11 起有移動語意與 emplace_back
//   標頭檔  ：<vector>、<string>、<iostream>
//   複雜度  ：push_back 攤銷 O(1)；但單次擴容為 O(n)（要搬移全部既有元素）
//
// 【詳細解釋 Explanation】
//
// 【1. 一行 push_back 其實牽涉三個物件事件】
//   `vec.push_back(Element("A"));` 這一行看起來只做一件事，實際上是：
//     (1) 建構一個「暫時物件」Element("A")            → [+] A
//     (2) 把它放進 vector 的緩衝區（複製或移動）      → [拷貝+] A(副本)
//     (3) 完整運算式結束（分號）時，銷毀那個暫時物件  → [-] A
//   所以一次 push_back 會看到三行輸出，而 vector 裡真正留下的是「副本」。
//   這正是 emplace_back 存在的理由：它直接把參數轉發到緩衝區「就地建構」，
//   完全不產生暫時物件，(1) 與 (3) 都省下來。
//
// 【2. 為什麼這裡走的是「複製」而不是「移動」】
//   Element("A") 是一個右值，照理說應該優先選移動建構 —— 但輸出顯示是複製。
//   原因是一條很容易被忽略的語言規則：
//       只要類別「使用者自行宣告了」解構函數、複製建構或複製賦值，
//       編譯器就「不會」再自動產生移動建構與移動賦值。
//   本範例的 Element 同時自訂了複製建構函數與解構函數，
//   因此它根本沒有移動建構函數。多載決議只好退回複製建構
//   （因為 const Element& 可以綁定右值）。
//   這是實務上非常常見的效能陷阱：以為自己在用移動語意，其實一路在深複製。
//
// 【3. 擴容：舊元素會被整批搬到新緩衝區】
//   vector 要求元素連續存放。當 size 觸及 capacity 又要再放一個時，
//   它必須配置更大的緩衝區、把既有元素全部搬過去、再銷毀舊的。
//   所以第二次 push_back 除了自己那一組事件外，
//   還會多出「既有元素 A(副本) 被複製到新位置、舊的 A(副本) 被解構」。
//   本範例中副本的名字會再加一層 "(副本)"，正好讓這個過程無所遁形。
//   提醒：容量成長策略屬實作定義（libstdc++ 為 2 倍），
//   若事先 reserve 足夠容量，這整段搬移都不會發生。
//
// 【概念補充 Concept Deep Dive】
//   vector 的緩衝區是「原始記憶體」，不是一整排已建構好的物件。
//   它內部區分兩件事：
//     * 配置記憶體（operator new）—— 只是要到一塊夠大的空間，沒有任何物件誕生
//     * 在該記憶體上建構物件（placement new）—— 這時才呼叫建構函數
//   銷毀時同樣分兩步：先逐一呼叫解構函數，再把整塊記憶體還回去。
//   這也是為什麼 capacity 可以大於 size：
//   多出來的那段記憶體已經配置好，但上面「還沒有任何物件存在」。
//   離開區塊時，vector 的解構函數會先解構所有現存元素，再釋放緩衝區。
//
// 【注意事項 Pay Attention】
// 1. 本範例的複製建構函數會「修改 name」（加上「(副本)」），這是為了教學
//    可視化而刻意為之。真實程式碼絕不該這樣寫 ——
//    複製建構函數應該產生一個與來源等價的物件，否則會違反直覺並破壞不變量。
// 2. 擴容次數與時機取決於實作的成長策略（libstdc++ 為 2 倍成長，屬實作定義）。
//    因此「第二次 push_back 會觸發擴容」這件事在本機成立，
//    但不是標準保證的通則。
// 3. 想避免這些複製，有三個層次的做法：
//    (a) 事先 reserve() 足夠容量 → 消除擴容造成的搬移；
//    (b) 改用 emplace_back("A") → 消除暫時物件；
//    (c) 為型別提供標了 noexcept 的移動建構 → 讓擴容用移動而非複製。
// 4. 擴容會使所有指向元素的指標、參考、迭代器全部失效，
//    在擴容後繼續使用它們是未定義行為。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】容器中元素的生命週期
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. `vec.push_back(Element("A"));` 這一行會產生幾個物件？分別何時銷毀？
//     答：兩個。一個是暫時物件 Element("A")，一個是放進 vector 的副本。
//         暫時物件在該完整運算式結束（分號）時就銷毀；
//         副本則活到 vector 被銷毀（或該元素被移除／被擴容搬移取代）為止。
//         改用 emplace_back("A") 可以直接在緩衝區就地建構，
//         完全不產生暫時物件。
//     追問：那 emplace_back 是不是永遠比 push_back 好？
//         → 不是。emplace_back 接受的是「建構參數」，會做隱式轉換，
//           因此少了 push_back 那層型別檢查，可能意外呼叫到非 explicit 的
//           建構函數。若你手上已經有一個現成物件，push_back(std::move(obj))
//           語意更清楚，效能也相同。
//
// 🔥 Q2. 為什麼這個範例明明傳入右值，走的卻是複製建構而不是移動建構？
//     答：因為 Element 自行宣告了複製建構函數與解構函數。
//         只要使用者宣告了解構函數／複製建構／複製賦值其中之一，
//         編譯器就不會再隱式產生移動建構與移動賦值。
//         Element 因此根本沒有移動建構，多載決議退回複製建構
//         （const Element& 可以綁定右值），全程深複製。
//     追問：怎麼修？
//         → 要嘛遵循 Rule of Zero，別自訂那些特殊成員（讓編譯器全部自動產生）；
//           要嘛就補齊 Rule of Five，明確寫出移動建構／移動賦值並標上 noexcept。
//           標 noexcept 尤其重要，否則 vector 擴容時仍會退回複製。
//
// ⚠️ 陷阱. 「我保留了指向 vector 元素的指標，之後再 push_back 也沒關係，
//           因為那個元素本身又沒有被我動到。」
//     答：非常危險。push_back 一旦觸發擴容，整個緩衝區會被搬到新位置，
//         舊緩衝區上的元素會被解構、記憶體被釋放。
//         你手上的指標、參考、迭代器全部失效，再使用它們就是未定義行為。
//     為什麼會錯：把 vector 想成「一格一格獨立的盒子」，
//         以為動別格不影響這一格。實際上 vector 是「一整塊連續緩衝區」，
//         擴容是把整塊搬家 —— 每一個元素的位址都變了。
//         需要穩定位址時，應改用 std::deque、std::list，
//         或改存 unique_ptr（指標本身被搬移，但它指向的物件位址不變）。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <vector>
using namespace std;

class Element {
private:
    string name;
public:
    Element(const string& n) : name(n) {
        cout << "  [+] " << name << endl;
    }
    // 拷貝建構函數（vector 操作時會用到）
    // 注意：這裡為了演示，拷貝建構函數會修改 name，讓我們能看到什麼時候被調用。
    //       真實程式碼絕不該這樣寫 —— 副本應與來源等價。
    // 重點：一旦自訂了複製建構函數與解構函數，編譯器就不會再產生移動建構，
    //       所以下面所有的搬移都會走「複製」這條路。
    Element(const Element& other) : name(other.name + "(副本)") {
        cout << "  [拷貝+] " << name << endl;
    }
    ~Element() {
        cout << "  [-] " << name << endl;
    }
};

