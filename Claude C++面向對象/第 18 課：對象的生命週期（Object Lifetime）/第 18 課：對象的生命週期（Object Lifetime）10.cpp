// -----------------------------------------------------------------------------
// 【日常實務範例】批次載入感測器讀數：reserve 的效果
//   情境：從資料來源讀入 N 筆讀數放進 vector。筆數事先已知。
//   要點：不 reserve 時，vector 會在成長過程中反覆配置新緩衝區並搬移舊資料；
//         先 reserve(N) 則一次配置到位，全程零搬移。
//   這是實務上最便宜、最有效的一個優化 —— 只要多寫一行。
//   本範例用「複製建構被呼叫幾次」來量化差異（可重現，不受機器效能影響）。
// -----------------------------------------------------------------------------
struct Reading {
    static inline int copies = 0;      // C++17: inline static
    double value;

    explicit Reading(double v) : value(v) {}
    Reading(const Reading& o) : value(o.value) { ++copies; }   // 只計數，不改內容

    static void reset() { copies = 0; }
};

int main() {
    cout << "=== vector 中的對象生命週期 ===" << endl;

    {
        vector<Element> vec;
        cout << "\n--- push_back ---" << endl;
        vec.push_back(Element("A"));   // 臨時對象 → 拷貝進 vector → 臨時對象死亡

        cout << "\n--- 再 push_back ---" << endl;
        vec.push_back(Element("B"));   // 容量不足 → 觸發擴容
        // 擴容時：既有的 A(副本) 被複製到新緩衝區成為 A(副本)(副本)，
        //         接著舊緩衝區上的 A(副本) 被解構。

        cout << "\n--- 離開區塊 ---" << endl;
    }
    // vector 解構：先逐一解構現存元素，再釋放整塊緩衝區

    cout << "\n=== 日常實務：reserve 對複製次數的影響 ===" << endl;
    {
        const int N = 1000;

        Reading::reset();
        {
            vector<Reading> v;                       // 不 reserve
            for (int i = 0; i < N; ++i) v.push_back(Reading(i * 0.5));
        }
        const int withoutReserve = Reading::copies;

        Reading::reset();
        {
            vector<Reading> v;
            v.reserve(N);                            // 一次配置到位
            for (int i = 0; i < N; ++i) v.push_back(Reading(i * 0.5));
        }
        const int withReserve = Reading::copies;

        cout << "  放入 " << N << " 筆讀數：" << endl;
        cout << "    不 reserve：複製建構被呼叫 " << withoutReserve << " 次" << endl;
        cout << "    先 reserve：複製建構被呼叫 " << withReserve << " 次" << endl;
        cout << "  → 差額 " << (withoutReserve - withReserve)
             << " 次全部來自擴容時的搬移，只要多寫一行 reserve 就能消除" << endl;
        cout << "  （每次 push_back 本身固定要複製 1 次暫時物件進緩衝區，" << endl;
        cout << "    這部分要改用 emplace_back 才省得掉）" << endl;
    }

    cout << "\n--- 完成 ---" << endl;
    return 0;
}

