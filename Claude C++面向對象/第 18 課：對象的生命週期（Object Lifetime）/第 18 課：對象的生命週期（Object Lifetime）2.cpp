//     [開始] 交易
//     [建立] 暫存檔 /tmp/import.csv
//     匯入成功
//     [刪除] 暫存檔 /tmp/import.csv
//     [結束] 交易（COMMIT）
//     [關閉] 資料庫連線 prod-db
//   --- 失敗提前返回 ---
//     [開啟] 資料庫連線 prod-db
//     [開始] 交易
//     [建立] 暫存檔 /tmp/import.csv
//     !! 匯入失敗，提前返回
//     [刪除] 暫存檔 /tmp/import.csv
//     [結束] 交易（ROLLBACK）
//     [關閉] 資料庫連線 prod-db
