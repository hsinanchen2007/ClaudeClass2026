// =============================================================================
//  第 18 課：對象的生命週期 2  —  巢狀作用域與 LIFO 解構順序
// =============================================================================
//
// 【主題資訊 Information】
//   主題：區塊作用域（block scope）中自動儲存期物件的建構與解構順序。
//   規則：同一個作用域內的物件，解構順序與建構順序「完全相反」（LIFO）。
//         內層區塊結束時，內層物件全部先解構，才輪到外層。
//   標準版本：C++98 起即為明文規定，不是實作巧合。
//   標頭檔：<iostream>、<string>
//   複雜度：解構是 O(物件數)；每個物件恰好解構一次。
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼一定是後進先出（LIFO）？】
//   這不是任意選定的規則，而是「唯一安全」的選擇。
//   考慮這種很常見的相依關係：
//       Connection conn("db");        // 先建立連線
//       Transaction tx(conn);         // 交易依賴連線
//   tx 的解構函式很可能要用到 conn（例如 rollback）。
//   若解構順序和建構順序相同，conn 會先死，tx 解構時就會使用
//   已銷毀的物件——未定義行為。
//   反過來 LIFO 則保證：一個物件在建構期間看得到的所有東西，
//   在它解構時仍然活著。這條保證是 RAII 能成立的基石。
//
// 【2. 這和堆疊的關係】
//   自動儲存期的物件配置在呼叫堆疊（call stack）上，
//   進入區塊時依序建構、離開時反序解構，行為與資料結構的堆疊完全一致。
//   本檔的四層巢狀 a→b→c→d，離開時就是 d→c→b→a，
//   輸出會呈現完美的對稱結構。
//
// 【3. 「離開作用域」有哪些方式？全部都會解構】
//   關鍵在於：解構是「離開作用域」觸發的，不是「執行到區塊最後一行」觸發的。
//   以下四種離開方式都保證會解構：
//     (a) 正常執行到區塊結尾
//     (b) return
//     (c) break / continue / goto 跳出區塊
//     (d) 拋出例外（堆疊展開，stack unwinding）
//   唯一不會解構的情況是：呼叫 std::abort()、std::_Exit()，
//   或發生未定義行為導致行程直接死亡。
//   ★ (d) 特別重要：正因為例外傳播時仍會解構，RAII 才能提供例外安全。
//
// 【4. 建構到一半失敗怎麼辦？】
//   若某個物件的建構函式拋出例外，「已經完成建構」的物件會被反序解構，
//   而「正在建構、尚未完成」的那一個不會呼叫解構函式
//   （因為它的生命週期還沒開始）。這是標準明確規定的行為，
//   也是為什麼「建構函式中不該做兩件會失敗的事」的原因。
//
// 【概念補充 Concept Deep Dive】
//   ● 物件生命週期的精確定義：
//       開始 = 建構函式「成功返回」的那一刻（storage 已配置且初始化完成）
//       結束 = 解構函式「開始執行」的那一刻
//     注意結束點是「解構開始」而非「解構結束」——所以在解構函式本體內，
//     物件其實已經處於生命週期之外，此時呼叫虛擬函式不會分派到衍生類別。
//   ● 編譯器實際產生的碼：對每個區塊，編譯器維護一份「已建構物件清單」，
//     在所有離開路徑上插入反序的解構呼叫。若有例外，則透過
//     unwind table（.eh_frame）在展開時執行同樣的清理。
//     這就是為什麼例外處理「不用時零成本」——正常路徑上沒有額外指令。
//   ● 生命週期不等於作用域：
//     static 區域變數的作用域是區塊，生命週期卻是整個程式（見 4.cpp）。
//     兩者是不同的概念，常被混用。
//
// 【注意事項 Pay Attention】
//   1. 解構順序是「反向建構順序」，不是「反向宣告順序」——
//      對區域變數兩者相同，但對「類別成員」則一律依宣告順序建構、
//      反宣告順序解構，與初始化列表寫的順序無關（gcc 用 -Wreorder 警告）。
//   2. 離開作用域的所有路徑都會解構，包含 return、break 與例外傳播。
//   3. 呼叫 std::abort() 或 std::_Exit() 不會執行任何解構函式。
//   4. 解構函式不應拋出例外；若在堆疊展開過程中再拋出，
//      會直接呼叫 std::terminate。C++11 起解構函式預設為 noexcept。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】作用域與解構順序
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼 C++ 規定同一作用域的物件必須「反序解構」？
//     答：因為後建構的物件可能依賴先建構的物件。反序解構才能保證
//         「一個物件解構時，它建構期間看得到的東西都還活著」。
//         若順序相同，Transaction 解構時它依賴的 Connection 已經死了，
//         就是使用已銷毀物件的未定義行為。這條保證是 RAII 的基石。
//     追問：類別成員的解構順序由什麼決定？
//         → 由「成員宣告順序」決定，反向解構；與建構函式初始化列表
//           寫的順序完全無關。寫錯順序 gcc 會用 -Wreorder 警告。
//
// 🔥 Q2. 函式中途 return，或拋出例外，區域物件會被解構嗎？
//     答：都會。解構由「離開作用域」觸發，涵蓋正常結束、return、
//         break/continue/goto，以及例外傳播時的堆疊展開。
//         正是因為例外傳播也會解構，RAII 才能提供例外安全保證。
//     追問：有沒有例外情況？
//         → 有。std::abort()、std::_Exit() 會直接終止行程，
//           不執行任何解構函式；std::exit() 則會解構「靜態物件」
//           但不會解構當時堆疊上的區域物件。
//
// ⚠️ 陷阱. 物件的生命週期是在「解構函式執行完」才結束的嗎？
//     答：不是。標準定義生命週期結束於「解構函式開始執行」的那一刻。
//         也就是說，進入解構函式本體時，物件已經不在生命週期內了。
//     為什麼會錯：直覺上會把「還在跑解構函式」當成「物件還活著」。
//         這個誤解會直接導致另一個經典錯誤——以為在解構函式裡呼叫
//         虛擬函式會分派到衍生類別。實際上衍生部分早已解構完畢，
//         此時虛擬呼叫只會停在當前類別的版本。建構函式中呼叫虛擬函式
//         也是同樣的道理（衍生部分還沒建好）。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <vector>
using namespace std;

class Scope {
private:
    string name;
public:
    Scope(const string& n) : name(n) {
        cout << "  [+] " << name << endl;
    }
    ~Scope() {
        cout << "  [-] " << name << endl;
    }
};

// -----------------------------------------------------------------------------
// 【示範】離開作用域的各種方式，都會觸發解構
//   分別用 return 與 throw 離開，證明「不是只有跑到區塊結尾才解構」。
// -----------------------------------------------------------------------------
void leaveByReturn() {
    Scope s("透過 return 離開");
    cout << "  即將 return..." << endl;
    return;                      // 解構仍然發生
}

void leaveByThrow() {
    Scope s("透過 throw 離開");
    cout << "  即將 throw..." << endl;
    throw runtime_error("模擬錯誤");   // 堆疊展開時仍會解構
}

// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】LeetCode 20. Valid Parentheses
//   題目：判斷只含 ()[]{} 的字串是否為合法的括號序列。
//   為什麼用到本主題：巢狀作用域和巢狀括號是同一個結構——
//         都必須「後開的先關」。本檔輸出的 [+]a [+]b [+]c [+]d
//         [-]d [-]c [-]b [-]a，形狀正是一組合法的巢狀括號；
//         而 C++ 的區塊解構順序，等同於用堆疊做括號配對。
//         換句話說，編譯器替你維護的那份「已建構物件清單」，
//         就是這題裡的那個 stack。
//   複雜度：時間 O(n)，空間 O(n)。
// -----------------------------------------------------------------------------
bool isValidParentheses(const string& s) {
    vector<char> stk;                       // 用 vector 當堆疊
    for (char c : s) {
        if (c == '(' || c == '[' || c == '{') {
            stk.push_back(c);               // 相當於「進入一層作用域」
        } else {
            if (stk.empty()) return false;  // 沒有對應的開括號
            char open = stk.back();
            // 必須和「最後一個尚未關閉」的配對 —— 這就是 LIFO
            if ((c == ')' && open != '(') ||
                (c == ']' && open != '[') ||
                (c == '}' && open != '{')) {
                return false;
            }
            stk.pop_back();                 // 相當於「離開一層作用域」
        }
    }
    return stk.empty();                     // 全部都要關乾淨
}

// -----------------------------------------------------------------------------
// 【日常實務範例】用巢狀 RAII 管理「連線 → 交易 → 暫存檔」的相依資源
//   情境：一支資料匯入程式，必須先連上資料庫、開啟交易，再寫暫存檔。
//   清理順序必須嚴格相反：先關暫存檔、再結束交易、最後關連線。
//   若手動寫 close，任何一條提早 return 的路徑漏掉一個 close 就是資源洩漏；
//   用巢狀 RAII 則由語言保證順序正確，而且完全不會漏。
// -----------------------------------------------------------------------------
class DbConnection {
    string host;
public:
    explicit DbConnection(const string& h) : host(h) {
        cout << "    [開啟] 資料庫連線 " << host << endl;
    }
    ~DbConnection() { cout << "    [關閉] 資料庫連線 " << host << endl; }
};

class Transaction {
    bool committed = false;
public:
    Transaction() { cout << "    [開始] 交易" << endl; }
    void commit() { committed = true; }
    ~Transaction() {
        // 沒 commit 就自動 rollback —— 這個判斷需要連線還活著，
        // 而 LIFO 順序正好保證了這一點
        cout << "    [結束] 交易（" << (committed ? "COMMIT" : "ROLLBACK") << "）" << endl;
    }
};

class TempFile {
    string path;
public:
    explicit TempFile(const string& p) : path(p) {
        cout << "    [建立] 暫存檔 " << path << endl;
    }
    ~TempFile() { cout << "    [刪除] 暫存檔 " << path << endl; }
};

void importData(bool shouldFail) {
    DbConnection conn("prod-db");        // 1. 最先開，最後關
    Transaction  tx;                     // 2.
    TempFile     tmp("/tmp/import.csv"); // 3. 最後開，最先刪

    if (shouldFail) {
        cout << "    !! 匯入失敗，提前返回" << endl;
        return;                          // 三個資源仍會依 3→2→1 反序清理
    }

    cout << "    匯入成功" << endl;
    tx.commit();
}

int main() {
    cout << "=== 作用域嵌套觀察 ===" << endl;
    
    Scope a("a - main 層");
    
    {   // 第一層區塊
        Scope b("b - 第一層");
        
        {   // 第二層區塊
            Scope c("c - 第二層");
            
            {   // 第三層區塊
                Scope d("d - 第三層");
                cout << "  --- 最深處 ---" << endl;
            }   // d 死亡
            
            cout << "  --- 回到第二層 ---" << endl;
        }   // c 死亡
        
        cout << "  --- 回到第一層 ---" << endl;
    }   // b 死亡
    
    cout << "  --- 回到 main ---" << endl;

    cout << "\n=== 離開作用域的各種方式 ===" << endl;
    leaveByReturn();
    try {
        leaveByThrow();
    } catch (const exception& e) {
        cout << "  捕獲例外: " << e.what() << "（物件已在展開時解構）" << endl;
    }

    cout << "\n=== LeetCode 20. Valid Parentheses ===" << endl;
    cout << "  \"()[]{}\"  -> " << (isValidParentheses("()[]{}")  ? "true" : "false") << endl;
    cout << "  \"([{}])\"  -> " << (isValidParentheses("([{}])")  ? "true" : "false") << endl;
    cout << "  \"(]\"      -> " << (isValidParentheses("(]")      ? "true" : "false") << endl;
    cout << "  \"([)]\"    -> " << (isValidParentheses("([)]")    ? "true" : "false") << endl;
    cout << "  \"(\"       -> " << (isValidParentheses("(")       ? "true" : "false") << endl;

    cout << "\n=== 日常實務：巢狀 RAII 保證清理順序 ===" << endl;
    cout << "  --- 成功路徑 ---" << endl;
    importData(false);
    cout << "  --- 失敗提前返回 ---" << endl;
    importData(true);

    return 0;
}   // a 死亡

