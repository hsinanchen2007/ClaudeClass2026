// =============================================================================
//  第 24 課：類別內的靜態成員變數 3  —  兩種存取方式與「共用一份」的本質
// =============================================================================
//
// 【主題資訊 Information】
//   兩種存取語法:
//     Enemy::totalSpawned      // 透過類別名(推薦)
//     e1.totalSpawned          // 透過物件(合法,但會誤導)
//   兩者存取的是「同一個變數」,不是各物件各一份。
//   標準版本：靜態成員為 C++98;本檔的 inline static 初始化是 C++17。
//   複雜度：O(1) 直接定址,不經過物件,也不需要 this。
//   標頭檔：<string>
//
// 【詳細解釋 Explanation】
//
// 【1. 「屬於類別」到底是什麼意思】
//   非靜態成員(name_、alive_)是「每個物件各一份」,配置在物件本身內部。
//   靜態成員(totalSpawned)則是「整個程式一份」,存放在靜態儲存區,
//   生命期從 main 之前到 main 之後,與任何物件的建立/銷毀無關。
//   所以本檔三隻怪物共用同一個 totalSpawned —— 這不是巧合,是定義。
//
// 【2. 為什麼 e1.totalSpawned 合法卻不推薦】
//   標準允許用物件語法存取靜態成員,但這個寫法會誤導讀者,
//   讓人以為「e1 有自己的一份」。本檔刻意印出 e1/e2/e3 的 totalKilled,
//   三個值完全相同,就是要用輸出證明它們是同一個變數。
//   更隱蔽的是:編譯器其實根本不會用到那個物件 ——
//   連 e1 是懸空指標都無所謂,因為存取靜態成員不需要 this。
//   維護時看到 Enemy::totalSpawned 一眼就知道是全域計數,
//   看到 e1.totalSpawned 卻要回頭查宣告,這就是可讀性的差別。
//
// 【3. 計數器模式:建構子 ++、解構子 --】
//   本檔在建構子做 totalSpawned++,是「累計產生過幾隻」的統計。
//   要注意這與「目前存活幾隻」不同 —— 後者需要在解構子做 --。
//   實務上兩種計數都常見,但語意完全不同,命名要說清楚
//   (totalSpawned vs aliveCount),否則很容易誤用。
//
// 【4. 靜態成員是全域可變狀態,要有節制】
//   它方便,但也帶著全域變數的所有問題:任何程式碼都能改、
//   難以單元測試(測試之間狀態會殘留)、多執行緒需要自行同步。
//   本檔把計數器放 public 是為了教學展示;實務上應設為 private,
//   只開放靜態 getter(這正是第 5 號檔案的主題)。
//
// 【概念補充 Concept Deep Dive】
//   * 靜態成員不佔物件空間:sizeof(Enemy) 不含 totalSpawned
//     (第 6 號檔案會用 sizeof 實測)。
//   * 存取靜態成員不需要 this,因此靜態成員函式也能存取它們 ——
//     這是第 25 課的主題。
//   * totalSpawned++ 是非原子的讀-改-寫。多執行緒同時建立 Enemy
//     就是資料競爭(未定義行為),需要 std::atomic<int> 或加鎖。
//   * 單元測試的隱形陷阱:靜態計數器會跨測試案例累加,
//     導致「單獨跑會過、全部一起跑就失敗」。需要提供 reset() 或
//     改用可注入的計數物件。
//
// 【注意事項 Pay Attention】
//   1. 一律用 類別名::成員 存取,不要用 物件.成員(合法但誤導)。
//   2. 靜態成員只有一份;修改它會影響「所有」物件與所有程式碼。
//   3. 建構子 ++ 計的是「累計產生數」,要算「目前存活數」需在解構子 --。
//   4. 多執行緒下遞增靜態計數器需自行同步,否則是資料競爭 → 未定義行為。
//   5. 靜態狀態會跨測試殘留,寫測試時要留意重置。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】靜態成員的存取與本質
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 靜態成員變數和一般成員變數,在儲存上有什麼根本差別?
//     答：一般成員是「每個物件各一份」,配置在物件內部,生命期跟著物件走;
//         靜態成員是「整個程式一份」,放在靜態儲存區,
//         在 main 之前初始化、main 之後才解構,與任何物件無關。
//         所以靜態成員不佔物件空間,sizeof 也不會把它算進去。
//     追問：那存取它需要物件嗎?→ 不需要,也不需要 this;
//         直接 類別名::成員 即可,這也是靜態成員函式能用它的原因。
//
// 🔥 Q2. e1.totalSpawned 和 Enemy::totalSpawned 有什麼差別?
//     答：沒有任何實質差別 —— 存取的是同一個變數,產生的機器碼也相同。
//         差別純粹在可讀性:物件語法會誤導讀者以為「每個物件各有一份」。
//         事實上編譯器根本不會真正使用那個物件,
//         因為存取靜態成員不需要 this。因此一律建議寫 類別名::成員。
//     追問：怎麼證明它們是同一份?→ 像本檔一樣印出 e1/e2/e3 的同一個
//         靜態成員,三個值必然完全相同。
//
// ⚠️ 陷阱. 「建構子裡 ++counter,解構子沒寫 --,那 counter 就是目前的物件數量。」
//     答：不是,那是「累計建立過幾個」。物件被解構後 counter 不會減少,
//         所以它會單調遞增,永遠不等於「目前存活數」。
//         本檔 totalSpawned 就是累計數;要算存活數必須在解構子做 --,
//         而且要注意拷貝建構子也會產生新物件 —— 若只在一般建構子 ++、
//         忘了在拷貝建構子也 ++,計數就會漏。
//     為什麼會錯：把「建立次數」和「目前數量」當成同一件事。
//         兩者只有在「從不銷毀物件」時才碰巧相等,而那通常不成立。
//         命名上直接寫清楚(totalSpawned vs aliveCount)就能避免整類錯誤。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【LeetCode 實戰範例】—— 從缺,理由如下
//   靜態成員的存取語法是語言機制,LeetCode 判題不涉及。
//   但這裡有一個真實相關的實務警訊:LeetCode 的判題程序常在同一個
//   行程內連續跑多筆測資,類別的 static 狀態會跨測資殘留,
//   造成「單筆測資對、整批跑就錯」。詳見本課 summary.cpp。
//
// =============================================================================

#include <iostream>
#include <string>
using namespace std;

class Enemy {
public:
    // C++17 inline static：直接在類別內定義並初始化
    // 靜態成員變數屬於類別本身，而不是任何特定的對象
    inline static int totalKilled = 0;
    inline static int totalSpawned = 0;

private:
    string name_;
    bool alive_;

public:
    Enemy(const string& name) : name_(name), alive_(true) {
        totalSpawned++;
    }

    void kill() {
        if (alive_) {
            alive_ = false;
            totalKilled++;
            cout << "  " << name_ << " 被擊殺！" << endl;
        }
    }

    const string& getName() const { return name_; }
};

// ─────────────────────────────────────────────────────────
// 【日常實務範例】連線池:區分「累計建立數」與「目前存活數」
//   情境：資料庫連線池要同時知道兩件事 ——
//         (a) 從服務啟動到現在總共開過幾條連線（容量規劃、洩漏偵測）
//         (b) 此刻還有幾條連線活著（即時監控、是否逼近上限）
//   這兩個數字都用靜態成員，但語意完全不同：
//         (a) 只在建構子 ++，永遠單調遞增
//         (b) 建構子 ++、解構子 --，會上下浮動
//   實務上最常見的錯誤，就是只寫了 (a) 卻拿它當 (b) 用。
//
//   注意：這個類別必須定義在「檔案作用域」，不能寫在 main 裡面。
//   標準 [class.local]/4 規定「local class 不得有 static data member」，
//   寫在 main 內會直接編譯失敗（GCC 訊息：local class ... shall not have
//   static data member）。這正是靜態成員需要獨立儲存區的直接後果。
// ─────────────────────────────────────────────────────────
struct DbConnection {
    inline static int totalOpened = 0;   // 累計建立數：只增不減
    inline static int aliveNow    = 0;   // 目前存活數：建構 ++、解構 --
    string id;

    explicit DbConnection(const string& name) : id(name) {
        ++totalOpened;
        ++aliveNow;
    }
    // 拷貝也會產生一條新連線 —— 漏寫這個，計數就會失準
    DbConnection(const DbConnection& other) : id(other.id + "-copy") {
        ++totalOpened;
        ++aliveNow;
    }
    ~DbConnection() { --aliveNow; }
};

int main() {
    cout << "=== 靜態成員的訪問方式 ===" << endl;

    Enemy e1("哥布林");
    Enemy e2("骷髏兵");
    Enemy e3("狼人");

    e1.kill();
    e3.kill();

    cout << "\n--- 方式 1：通過類別名訪問（推薦）---" << endl;
    cout << "  生成數：" << Enemy::totalSpawned << endl;
    cout << "  擊殺數：" << Enemy::totalKilled << endl;

    cout << "\n--- 方式 2：通過對象訪問（不推薦）---" << endl;
    cout << "  生成數：" << e1.totalSpawned << endl;
    cout << "  擊殺數：" << e2.totalKilled << endl;

    // 方式 2 的問題：看起來像是對象自己的數據
    // 但實際上 e1.totalSpawned 和 e2.totalSpawned 是同一個變數
    cout << "\n--- 驗證：同一個變數 ---" << endl;
    cout << "  e1.totalKilled = " << e1.totalKilled << endl;
    cout << "  e2.totalKilled = " << e2.totalKilled << endl;
    cout << "  e3.totalKilled = " << e3.totalKilled << endl;
    cout << "  都是同一個值！" << endl;

    // 日常實務：連線池的兩種計數（類別定義在檔案作用域，見上方說明）
    cout << "\n=== 日常實務：連線池的兩種計數 ===" << endl;

    auto report = [] {
        cout << "    累計建立=" << DbConnection::totalOpened
             << "  目前存活=" << DbConnection::aliveNow << endl;
    };

    cout << "  開三條連線：" << endl;
    {
        DbConnection c1("conn-A");
        DbConnection c2("conn-B");
        DbConnection c3("conn-C");
        report();

        cout << "  再拷貝一條（拷貝建構子同樣要計數）：" << endl;
        DbConnection c4 = c1;
        report();

        cout << "  離開作用域，四條連線即將解構..." << endl;
    }

    cout << "  作用域結束後：" << endl;
    report();
    cout << "  ↑ 累計建立數停在 4 不會回頭，存活數則歸 0。" << endl;
    cout << "    若只有累計數而拿它當『目前用量』，就會誤判連線池爆滿。" << endl;

    return 0;
}

