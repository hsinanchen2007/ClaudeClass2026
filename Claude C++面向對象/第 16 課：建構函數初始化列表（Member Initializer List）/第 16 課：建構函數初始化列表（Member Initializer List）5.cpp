// =============================================================================
//  第 16 課：初始化列表 5  —  必用場景之四：呼叫基底類別的建構函數
// =============================================================================
//
// 【主題資訊 Information】
//   語法：  class Dog : public Animal {
//               Dog(const string& n) : Animal("犬科"), name(n) { }
//           };                        └基底類別┘  └自己的成員┘
//   標準版本：C++98 起即有
//   複雜度：O(1)；基底子物件就地建構，不產生臨時物件
//   標頭檔：<string>
//
// 【詳細解釋 Explanation】
//
// 【1. 衍生類別物件的組成：基底是「子物件」不是「另一個物件」】
//   一個 Dog 物件在記憶體中是這樣的：
//       ┌─────────────────────────┐
//       │ Animal 子物件            │ ← species
//       ├─────────────────────────┤
//       │ Dog 自己的成員           │ ← name
//       └─────────────────────────┘
//   基底不是「Dog 裡面放了一個 Animal 指標」，而是 Dog 物件本身的一部分。
//   所以建立 Dog 時，那塊 Animal 子物件也必須被初始化——
//   而初始化基底的唯一語法就是寫在初始化列表。
//
// 【2. 為什麼函數體內無法初始化基底】
//   跟成員的道理完全一樣：進入 Dog 建構函數的函數體之前，
//   Animal 子物件就必須已經建好（否則函數體內呼叫繼承來的
//   getSpecies() 會操作到未初始化的資料）。
//   若你沒在初始化列表寫 Animal(...)，編譯器就會試著呼叫
//   Animal 的**預設建構函數**；而本檔的 Animal 只有帶參版本，
//   所以會直接編譯失敗：
//       error: no matching function for call to 'Animal::Animal()'
//   這是新手最常見的繼承編譯錯誤之一。
//
// 【3. 完整的建構順序（三層）】
//   建立一個 Dog 時：
//       (1) 基底 Animal 的建構函數（含它自己的成員 species）
//       (2) Dog 自己的成員，依**宣告順序**（name）
//       (3) Dog 建構函數的函數體
//   解構順序則完全相反：
//       (3') Dog 解構函數的函數體
//       (2') Dog 自己的成員，依宣告的**反序**
//       (1') 基底 Animal 的解構函數
//   口訣：**建構由內而外（先基底後衍生），解構由外而內**。
//   本檔的輸出可以完整看到這六個階段。
//
// 【4. 基底的初始化永遠排在自己的成員之前】
//   不論你在初始化列表把 Animal(...) 寫在哪個位置，它一定最先執行。
//   所以下面兩種寫法的執行結果完全一樣：
//       : Animal("犬科"), name(n)      // 建議：與實際順序一致
//       : name(n), Animal("犬科")      // 可編譯，但會有 -Wreorder 警告
//   建議永遠把基底寫在最前面，讓程式碼順序等於執行順序。
//
// 【5. 多層繼承與多重繼承】
//   ● 多層（A → B → C）：建構順序是 A、B、C，解構是 C、B、A。
//     每一層只需要負責初始化它的**直接**基底。
//   ● 多重繼承（class D : public A, public B）：
//     基底的建構順序由**繼承列表的書寫順序**決定（A 再 B），
//     與初始化列表的順序無關。
//
// 【概念補充 Concept Deep Dive】
//
//   ● 為什麼建構要「先基底後衍生」
//     因為衍生類別可能會用到基底的成員（本檔 Dog::print() 就呼叫了
//     繼承來的 getSpecies()）。基底先建好，衍生類別才能安全地依賴它。
//     解構反過來，是為了讓衍生類別在自己的資源還在時先收尾，
//     再讓基底拆除它依賴的東西。
//
//   ● 建構期間虛擬函數不會下派
//     在 Animal 的建構函數執行期間，物件的動態型別就是 Animal，
//     即使實際上正在建一個 Dog。所以在基底建構函數裡呼叫虛擬函數，
//     呼叫到的是**基底自己的版本**，不是 Dog 的覆寫版本。
//     道理很直接：那時 Dog 的部分還沒建好，若下派過去就會操作未初始化的成員。
//     解構期間同理（動態型別逐層退回）。這是很常見的面試題。
//
//   ● 繼承要搭配虛擬解構函數
//     若會透過基底指標刪除衍生物件（Animal* p = new Dog; delete p;），
//     基底的解構函數必須是 virtual，否則只會呼叫到 Animal 的解構函數，
//     Dog 的部分不會被清理。本檔沒有多型刪除的情境，但實務上請養成習慣。
//
//   ● C++11 的繼承建構函數
//     若衍生類別只是想沿用基底的所有建構函數，可以寫：
//         class Dog : public Animal { using Animal::Animal; };
//     這會把基底的建構函數「繼承」下來，省去大量轉發樣板碼。
//
// 【注意事項 Pay Attention】
//   1. 基底一定最先初始化，與書寫位置無關；建議寫在初始化列表最前面。
//   2. 沒寫基底就等於呼叫基底的預設建構函數——基底若沒有就是編譯錯誤。
//   3. 建構／解構期間虛擬函數不會下派到衍生類別。
//   4. 打算多型使用（基底指標刪除衍生物件）時，基底解構函數要宣告 virtual。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】基底類別的初始化與建構順序
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 建立一個衍生類別物件時，建構與解構的完整順序是什麼？
//     答：建構是「基底 → 衍生類別的成員（依宣告順序）→ 衍生類別的函數體」；
//         解構完全相反：「衍生類別的函數體 → 衍生類別的成員（反序）→ 基底」。
//         簡記為建構由內而外、解構由外而內。
//     追問：多重繼承時多個基底的順序由什麼決定？
//         → 由繼承列表的書寫順序決定（class D : public A, public B → A 先 B 後），
//           與初始化列表怎麼寫無關。
//
// 🔥 Q2. 為什麼基底類別的建構函數只能在初始化列表呼叫？
//     答：基底是衍生物件的一部分（子物件），必須在進入衍生類別函數體之前
//         就完成初始化，否則函數體內使用繼承來的成員會操作未初始化資料。
//         初始化列表是語言提供的唯一入口；若不寫，就等同要求呼叫基底的
//         預設建構函數，基底沒有的話直接編譯失敗。
//     追問：如果只想沿用基底的建構函數，有沒有省事的寫法？
//         → C++11 起可用繼承建構函數：using Animal::Animal;
//
// ⚠️ 陷阱. 在基底類別的建構函數裡呼叫虛擬函數，會呼叫到衍生類別的覆寫版本嗎？
//     答：不會。建構期間物件的動態型別就是「當前正在建構的那一層」，
//         所以呼叫到的是基底自己的版本。解構期間同理。
//     為什麼會錯：以為虛擬機制從物件一誕生就全程有效。實際上此時衍生類別的
//         成員都還沒初始化，若真的下派過去就會操作到未初始化的資料，
//         所以語言刻意讓虛擬呼叫停在當前層——這是保護，不是缺陷。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class Animal {
private:
    string species;

public:
    // Animal 只有帶參建構函數 → 衍生類別必須明確呼叫它
    Animal(const string& s) : species(s) {
        cout << "  (1) Animal 建構: " << species << endl;
    }

    ~Animal() {
        cout << "  (1') Animal 解構: " << species << endl;
    }

    string getSpecies() const { return species; }
};

class Dog : public Animal {
private:
    string name;

public:
    // 錯誤寫法：
    //   Dog(const string& n) : name(n) { }
    //   → 沒寫 Animal(...)，編譯器嘗試呼叫 Animal 的預設建構函數，
    //     但它不存在 → error: no matching function for call to 'Animal::Animal()'

    // 正確寫法：基底寫在最前面，讓書寫順序等於執行順序
    Dog(const string& n)
        : Animal("犬科"),   // (1) 基底一定最先建構
          name(n)            // (2) 才輪到自己的成員
    {
        cout << "  (3) Dog 建構: " << name << endl;   // (3) 最後才是函數體
    }

    ~Dog() {
        cout << "  (3') Dog 解構: " << name << endl;
    }

    void print() const {
        // 這裡安全地使用繼承來的 getSpecies()，因為基底早已建好
        cout << "  " << name << " (" << getSpecies() << ")" << endl;
    }
};

// -----------------------------------------------------------------------------
// 示範：建構／解構期間，虛擬函數不會下派到衍生類別
// -----------------------------------------------------------------------------
class Base {
public:
    Base() {
        cout << "    Base 建構中，呼叫 who() → ";
        who();      // 此時動態型別是 Base，呼叫的是 Base::who
    }
    virtual ~Base() = default;
    virtual void who() const { cout << "Base::who" << endl; }
};

class Derived : public Base {
public:
    Derived() : Base() {
        cout << "    Derived 建構完成，呼叫 who() → ";
        who();      // 此時物件已完整，才會下派到 Derived::who
    }
    void who() const override { cout << "Derived::who" << endl; }
};

// -----------------------------------------------------------------------------
// 【日常實務範例】例外階層：基底統一保存訊息與錯誤碼
//   情境：服務端要為不同錯誤定義專屬例外型別，但所有例外都共用
//         「訊息 + HTTP 狀態碼」這組資料。把共通部分放進基底，
//         各衍生類別只負責在初始化列表填入自己的語意。
//   重點：這是繼承最典型且最無爭議的用法之一——衍生類別的建構函數
//         幾乎只做一件事：把適當的參數轉交給基底。
// -----------------------------------------------------------------------------
class ApiError {
private:
    string message_;
    int httpStatus_;

public:
    ApiError(const string& message, int httpStatus)
        : message_(message), httpStatus_(httpStatus)
    { }

    virtual ~ApiError() = default;   // 打算多型使用 → 基底解構函數設為 virtual

    void print() const {
        cout << "  HTTP " << httpStatus_ << " - " << message_ << endl;
    }
};

class NotFoundError : public ApiError {
public:
    explicit NotFoundError(const string& resource)
        : ApiError("找不到資源: " + resource, 404)
    { }
};

class RateLimitError : public ApiError {
public:
    explicit RateLimitError(int retryAfterSec)
        : ApiError("請求過於頻繁，請於 " + to_string(retryAfterSec) + " 秒後重試", 429)
    { }
};

int main() {
    cout << "=== 建構順序：基底 → 成員 → 函數體 ===" << endl;
    {
        Dog d("旺財");
        cout << "\n  --- 物件已就緒 ---" << endl;
        d.print();
        cout << "\n  --- 離開作用域，解構順序完全相反 ---" << endl;
    }

    cout << "\n=== 建構期間虛擬函數不會下派 ===" << endl;
    {
        Derived d;
        cout << "    物件完整後，透過基底參考呼叫 who() → ";
        const Base& b = d;
        b.who();        // 現在才是真正的多型
    }

    cout << "\n=== 日常實務：例外階層共用基底資料 ===" << endl;
    NotFoundError e1("/api/v2/users/9527");
    RateLimitError e2(30);
    e1.print();
    e2.print();

    return 0;
}

