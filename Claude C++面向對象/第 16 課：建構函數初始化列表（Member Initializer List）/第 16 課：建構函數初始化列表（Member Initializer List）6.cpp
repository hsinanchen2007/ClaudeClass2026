// =============================================================================
//  第 16 課：初始化列表 6  —  初始化順序由「宣告順序」決定，不是書寫順序
// =============================================================================
//
// 【主題資訊 Information】
//   規則：非靜態成員一律依照在 class 內的**宣告順序**初始化，
//         與初始化列表的書寫順序完全無關。
//   標準版本：C++98 起即有
//   相關診斷：-Wreorder（GCC/Clang，已包含在 -Wall 中）
//   複雜度：O(成員數)
//   標頭檔：<iostream>
//
//   ※ 本檔會**故意**產生兩個 -Wreorder 警告，那正是要教的東西。
//     編譯時看到警告是預期行為，不是這個檔案寫壞了。
//
// 【詳細解釋 Explanation】
//
// 【1. 規則本身】
//   本檔的 OrderDemo 宣告順序是 a、b、c，但初始化列表故意寫成 c、a、b。
//   實際執行順序仍然是 a → b → c。
//   從輸出可以驗證：c 的初值是 val+2 = 12，a 是 val = 10，b 是 val+1 = 11，
//   三個值都正確——因為這個例子的成員之間沒有互相依賴，順序不影響結果。
//   （下一個檔案 7.cpp 就會示範「有依賴時」順序錯掉會出什麼事。）
//
// 【2. 為什麼要有這條規則】
//   關鍵理由是**解構順序必須是建構順序的完全反序**，而且這個反序必須在
//   編譯期就能唯一決定。
//   想像一下，如果初始化順序可以由初始化列表的書寫順序決定：
//     ● 同一個類別有三個建構函數，各自寫不同的順序 → 三種建構順序
//     ● 但解構函數只有一個，它該用哪一種反序來拆？
//   為了讓解構順序唯一且可預測，語言把初始化順序綁定在**類別的宣告**上，
//   而不是綁在個別建構函數上。這樣不論從哪個建構函數建出來的物件，
//   拆除順序都一致。
//
// 【3. 另一個理由：記憶體佈局】
//   成員在物件中的排列順序本來就是宣告順序（可能有 padding，但相對次序不變）。
//   依宣告順序初始化等於「由低位址往高位址依序填」，對快取比較友善，
//   也讓編譯器容易產生穩定的程式碼。
//
// 【4. -Wreorder：編譯器會提醒你】
//   當書寫順序與宣告順序不一致時，g++ 會發出：
//       warning: 'OrderDemo::c' will be initialized after [-Wreorder]
//       warning:   'int OrderDemo::a' [-Wreorder]
//       warning:   when initialized here [-Wreorder]
//   請注意它的意思是「**你寫的順序不是真正發生的順序**」，
//   而不是「編譯器幫你調整了順序」。編譯器沒有改變任何行為，
//   只是提醒你的程式碼可能造成誤解。
//   這個警告已包含在 -Wall 裡，不需要另外開。
//
// 【5. 實務建議】
//   讓初始化列表的書寫順序**永遠等於**宣告順序。
//   這樣做的好處不只是消除警告，而是讓「讀程式碼看到的順序」
//   就是「實際執行的順序」——在成員之間有依賴時，這是避免 bug 的關鍵。
//
// 【概念補充 Concept Deep Dive】
//
//   ● 完整的初始化順序（含繼承）
//     (1) 虛擬基底類別（依繼承圖的深度優先、由左至右）
//     (2) 直接基底類別（依**繼承列表**的書寫順序）
//     (3) 非靜態成員（依**宣告順序**）
//     (4) 建構函數的函數體
//     注意 (2) 看的是繼承列表順序，(3) 看的是宣告順序，兩者都不看初始化列表。
//
//   ● 靜態成員不在此列
//     static 成員屬於類別而非物件，它們在程式啟動階段初始化，
//     與建構函數無關，也不受此規則約束。
//
//   ● 沒寫在初始化列表的成員會怎樣
//     ● 有類別內預設初始值（C++11 NSDMI）→ 用該初值
//     ● 否則若是類別型別 → 呼叫預設建構函數
//     ● 否則若是內建型別 → **不初始化**，值不定，讀取是未定義行為
//     這是 int 成員最容易出事的地方，也是 9.cpp 要談的主題。
//
//   ● 為什麼解構順序是反序
//     因為後建立的成員可能依賴先建立的成員。反序拆除保證
//     「依賴別人的先走，被依賴的後走」，不會有人在依賴對象消失後還被使用。
//     這與函式呼叫堆疊、RAII 的巢狀結構是同一個道理。
//
// 【注意事項 Pay Attention】
//   1. 順序由宣告決定；改動成員宣告的先後，會**默默**改變初始化順序。
//      這是重構時的隱形風險——移動一行宣告就可能改變程式行為。
//   2. 請保持書寫順序 = 宣告順序，並開啟 -Wall 讓 -Wreorder 幫你把關。
//   3. -Wreorder 只提醒「順序不一致」，不會判斷你有沒有真的踩到 bug；
//      成員之間有依賴時才會出事（見 7.cpp）。
//   4. 多重繼承時基底的順序看繼承列表，不看初始化列表。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】成員初始化順序
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 成員的初始化順序由什麼決定？
//     答：由成員在 class 內的**宣告順序**決定，與初始化列表的書寫順序無關。
//         所以 : c(x), a(y), b(z) 若宣告順序是 a、b、c，實際仍是 a → b → c。
//     追問：那為什麼語言要這樣設計，不乾脆照我寫的順序？
//         → 因為解構順序必須是建構順序的反序且唯一。若順序由各建構函數
//           自行決定，同一個類別的多個建構函數會產生不同的建構順序，
//           唯一的解構函數就無法決定該用哪種反序。
//
// 🔥 Q2. -Wreorder 這個警告是什麼意思？編譯器會幫我調整順序嗎？
//     答：它表示「初始化列表的書寫順序與實際執行順序不一致」。
//         編譯器**不會**調整任何順序，實際順序永遠是宣告順序；
//         警告只是提醒你的程式碼容易造成誤解。它包含在 -Wall 中。
//     追問：消除這個警告的正確做法？
//         → 把初始化列表的順序改成與宣告順序一致，而不是關掉警告。
//
// ⚠️ 陷阱. 我把初始化列表寫成 : c(val+2), a(val), b(val+1)，
//          所以 c 會先算好，之後 a、b 才初始化，對嗎？
//     答：不對。實際順序是 a → b → c，c 是**最後**才初始化的。
//         本例三個成員互不依賴，所以看不出差別、結果都正確；
//         但只要有依賴（例如用 length 去配置陣列），順序錯就是真的 bug。
//     為什麼會錯：把初始化列表當成「由上往下執行的敘述序列」。
//         它其實是宣告式的——你只是在指定「每個成員該用什麼初始化」，
//         真正的執行順序由類別的宣告佈局決定。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <vector>
using namespace std;

class OrderDemo {
private:
    int a;    // 第 1 個宣告 → 第 1 個初始化
    int b;    // 第 2 個宣告 → 第 2 個初始化
    int c;    // 第 3 個宣告 → 第 3 個初始化

public:
    // 注意：初始化列表故意寫成 c, a, b 的順序，
    // 但實際初始化順序仍是 a, b, c（按宣告順序）。
    // 本建構函數會產生 -Wreorder 警告，這正是本檔要示範的重點。
    OrderDemo(int val)
        : c(val + 2),     // 寫在第 1 個，但第 3 個執行
          a(val),          // 寫在第 2 個，但第 1 個執行
          b(val + 1)       // 寫在第 3 個，但第 2 個執行
    {
        cout << "  a = " << a << ", b = " << b << ", c = " << c << endl;
    }
};

// -----------------------------------------------------------------------------
// 對照組：書寫順序 = 宣告順序 → 沒有警告，讀起來也不會誤解
// -----------------------------------------------------------------------------
class OrderTidy {
private:
    int a;
    int b;
    int c;

public:
    OrderTidy(int val)
        : a(val),          // 與宣告順序一致
          b(val + 1),
          c(val + 2)
    {
        cout << "  a = " << a << ", b = " << b << ", c = " << c << endl;
    }
};

// -----------------------------------------------------------------------------
// 用會出聲的成員，把「真正的初始化順序」直接印出來
//   三個 Tracer 成員宣告順序是 first、second、third，
//   但初始化列表刻意倒著寫；輸出會證明順序不受書寫影響。
// -----------------------------------------------------------------------------
class Tracer {
public:
    explicit Tracer(const string& tag) : tag_(tag) {
        cout << "      建構 " << tag_ << endl;
    }
    ~Tracer() {
        cout << "      解構 " << tag_ << endl;
    }
private:
    string tag_;
};

class OrderProof {
private:
    Tracer first;    // 宣告第 1
    Tracer second;   // 宣告第 2
    Tracer third;    // 宣告第 3

public:
    // 刻意倒著寫，實際順序仍是 first → second → third
    // 同樣會產生 -Wreorder 警告
    OrderProof()
        : third("third"),
          second("second"),
          first("first")
    {
        cout << "      OrderProof 函數體" << endl;
    }
};

// -----------------------------------------------------------------------------
// 【日常實務範例】重構時的隱形風險：移動一行宣告就改變了初始化順序
//   情境：某個快取類別原本宣告順序是 capacity_ 在前、slots_ 在後，
//         slots_ 依賴 capacity_ 來決定大小。若有人為了「把容器欄位放一起」
//         而把 slots_ 移到 capacity_ 前面，程式仍然編得過，
//         但初始化順序就悄悄反了。
//   重點：本範例採取的防禦做法是——把被依賴的欄位宣告在最前面，
//         並在該欄位上方寫明「順序敏感，勿移動」的註解。
//         這種註解在 code review 時比任何口頭約定都有效。
// -----------------------------------------------------------------------------
class TileCache {
private:
    // ↓↓↓ 順序敏感：capacity_ 必須宣告在 slots_ 之前，勿調換 ↓↓↓
    size_t capacity_;              // 被依賴者：先初始化
    vector<string> slots_;         // 依賴者：用 capacity_ 決定大小
    // ↑↑↑ 順序敏感 ↑↑↑
    size_t used_;

public:
    explicit TileCache(size_t capacity)
        : capacity_(capacity)
        , slots_(capacity, "(空)")   // 安全：capacity_ 此時已完成初始化
        , used_(0)
    { }

    void put(const string& tile) {
        if (used_ < capacity_) slots_[used_++] = tile;
    }

    void print() const {
        cout << "  容量 " << capacity_ << "，已用 " << used_ << "：";
        for (const auto& s : slots_) cout << "[" << s << "]";
        cout << endl;
    }
};

int main() {
    cout << "=== 書寫順序 c,a,b；實際順序仍是 a,b,c ===" << endl;
    OrderDemo d(10);
    cout << "  （三個值都正確，因為本例成員之間沒有互相依賴）" << endl;

    cout << "\n=== 對照：書寫順序 = 宣告順序（無警告）===" << endl;
    OrderTidy t(10);

    cout << "\n=== 用建構訊息證明真正的初始化順序 ===" << endl;
    {
        OrderProof p;
        cout << "    --- 離開作用域，解構為建構的反序 ---" << endl;
    }

    cout << "\n=== 日常實務：把順序相依明確標註出來 ===" << endl;
    TileCache cache(4);
    cache.put("tile_0_0");
    cache.put("tile_0_1");
    cache.print();

    return 0;
}

