// =============================================================================
//  第 16 課：初始化列表 1  —  賦值 vs 初始化：本質區別
// =============================================================================
//
// 【主題資訊 Information】
//   語法：  Class(參數) : 成員1(初值1), 成員2(初值2) { 函數體 }
//                        └────── 成員初始化列表 ──────┘
//   標準版本：C++98 起即有
//   複雜度：對有建構成本的成員，初始化列表少一次「預設建構 + 賦值」
//   標頭檔：<string>
//
// 【詳細解釋 Explanation】
//
// 【1. 兩種寫法看起來一樣，做的事不一樣】
//   ● 方式 A（函數體內賦值）：
//         Demo(const string& n) { name = n; }
//     實際發生兩步：
//       (a) **進入函數體之前**，成員 name 就已經被「預設建構」成空字串
//       (b) 函數體內再用 operator= 把 n 的內容複製進去，蓋掉剛剛那個空字串
//   ● 方式 B（初始化列表）：
//         DemoInit(const string& n) : name(n) { }
//     只有一步：
//       (a) 直接用 n 呼叫 string 的複製建構函數，把 name 建出來
//
//   本檔用一個很直接的方式證明這件事：在方式 A 的函數體第一行就把 name 印出來。
//   如果「賦值前 name 還不存在」，那行根本印不出東西；但它印出了一個空字串
//   ——證明成員早在進入函數體之前就已經被預設建構好了。
//
// 【2. 為什麼「成員在進函數體前就建好了」】
//   這是 C++ 物件生命週期的規定：一個物件在建構函數的函數體開始執行時，
//   必須已經是「所有成員都完成初始化」的狀態。否則函數體內若呼叫某個
//   成員函數，那個成員還沒建好就會出事。
//   所以編譯器一定會在函數體之前把每個成員初始化掉：
//     ● 你在初始化列表指定了 → 照你說的初始化
//     ● 你沒指定 → 用預設建構函數（內建型別則不初始化，值不定）
//   函數體內的 = 因此永遠是「對一個已存在物件做賦值」，而不是初始化。
//
// 【3. 效能差多少】
//   對 int、double 這種內建型別，差異幾乎為零（都只是一次寫入）。
//   對 std::string、std::vector 這類要管理動態記憶體的型別，差別是：
//       方式 A：預設建構（可能配置）→ 賦值（可能釋放舊的、再配置新的）
//       方式 B：複製建構（一次配置）
//   短字串因為 SSO（small string optimization，短字串直接存在物件內、
//   不配置 heap）差異不明顯；長字串就會有實質差異。
//   ※ SSO 的門檻是**實作定義**的，libstdc++ 上一般是 15 個字元（不含結尾），
//     libc++ 與 MSVC 的門檻不同，不可寫死在程式邏輯裡。
//
// 【4. 但真正的重點不是效能，是「有些成員只能這樣寫」】
//   下面三種成員在函數體內根本無法賦值，只能用初始化列表（後續檔案逐一示範）：
//     ● const 成員（2.cpp）           —— 建出來就不能改
//     ● 參考成員 T&（3.cpp）          —— 必須誕生時綁定
//     ● 沒有預設建構函數的成員（4.cpp）—— 進函數體前沒東西可先建
//   還有 base class 的初始化（5.cpp）也只能寫在初始化列表。
//   所以初始化列表不是「效能微調」，而是「表達某些初始化的唯一方式」。
//
// 【概念補充 Concept Deep Dive】
//
//   ● 初始化與賦值在 C 語言就有一樣的區別
//         int x = 10;   // 初始化：誕生時就有值
//         int y; y = 10;// 賦值：先誕生（值不定），再改值
//     C++ 只是把這個區別延伸到類別型別，並用初始化列表提供「初始化」的語法。
//
//   ● 為什麼方式 A 印出來是空字串而不是亂碼
//     因為 std::string 有預設建構函數，標準保證它產生空字串。
//     如果成員是 int 而在函數體賦值前就去讀它，那才是讀取不定值（未定義行為）。
//     本檔刻意只印 string 成員，就是為了避免示範程式本身踩到 UB。
//
//   ● 編譯器會不會幫我最佳化掉那次多餘的預設建構
//     有時可以，有時不行。若 string 的建構／賦值涉及 heap 配置，
//     編譯器通常無法證明可以省略，因為那牽涉到 operator new 的可觀察副作用。
//     不要指望最佳化器替你把方式 A 變成方式 B。
//
// 【注意事項 Pay Attention】
//   1. 初始化列表的實際執行順序由**成員宣告順序**決定，不是書寫順序
//      （6.cpp、7.cpp 專門示範）。
//   2. 函數體內的 = 一律是賦值；想要初始化只有初始化列表一條路。
//   3. 初始化列表裡不要寫複雜流程；需要驗證或計算的值，先用 helper 函數
//      算好再傳進來。
//   4. SSO 門檻、vector 成長倍率這類數字都是**實作定義**，不同標準函式庫不同。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】賦值 vs 初始化
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 在建構函數本體內寫 name = n; 和用初始化列表 : name(n) 差在哪？
//     答：本體內是「先預設建構成員，再用 operator= 賦值」兩步；
//         初始化列表是「直接用 n 複製建構成員」一步。
//         對有建構成本的型別（string、vector）方式 A 多做一次白工。
//     追問：怎麼證明成員在進函數體前就已經被建構了？
//         → 在函數體第一行就把該成員印出來。若它已是合法的空字串，
//           就代表預設建構早已完成——本檔正是這樣示範的。
//
// 🔥 Q2. 既然只是效能差異，為什麼大家都說一定要用初始化列表？
//     答：因為不只是效能。const 成員、參考成員、沒有預設建構函數的成員，
//         以及 base class 的初始化，都**只能**寫在初始化列表，
//         在函數體裡根本寫不出來。效能只是附帶的好處。
//     追問：那什麼東西還是應該寫在函數體？
//         → 需要條件判斷、迴圈、驗證後拋例外這類流程；
//           初始化列表每一項只能是單一運算式。
//
// ⚠️ 陷阱. 反正編譯器會最佳化，兩種寫法產生的機器碼應該一樣吧？
//     答：不能這樣假設。當成員的預設建構或賦值涉及 heap 配置時，
//         那是可觀察的副作用，編譯器通常無法證明能省略，
//         多出來的預設建構就真的會執行。
//     為什麼會錯：把「最佳化器很聰明」當成萬用擋箭牌，忽略了最佳化必須
//         保持程式的可觀察行為；記憶體配置、I/O 這類副作用是不能亂刪的。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

// -----------------------------------------------------------------------------
// 方式 A：函數體內賦值
//   name 先被預設建構（空字串），然後才被賦值 → 兩步
// -----------------------------------------------------------------------------
class Demo {
private:
    string name;
    int value;

public:
    Demo(const string& n, int v) {
        cout << "  [賦值方式]" << endl;
        // 關鍵證據：此時 name 已經是一個合法的空字串了
        // 代表它在進入函數體「之前」就被預設建構完成
        cout << "  賦值前 name = [" << name << "]（已經被預設建構過了）" << endl;
        name = n;     // 這是賦值，不是初始化
        value = v;
    }

    void print() const {
        cout << "  name = " << name << ", value = " << value << endl;
    }
};

// -----------------------------------------------------------------------------
// 方式 B：初始化列表
//   name 直接用 n 建構出來 → 一步到位
// -----------------------------------------------------------------------------
class DemoInit {
private:
    string name;
    int value;

public:
    DemoInit(const string& n, int v)
        : name(n), value(v)
    {
        cout << "  [初始化列表方式]" << endl;
        cout << "  name 直接就是 [" << name << "]（一步完成）" << endl;
    }

    void print() const {
        cout << "  name = " << name << ", value = " << value << endl;
    }
};

// -----------------------------------------------------------------------------
// 用一個會出聲的成員，把「多做的那一步」直接印出來
// -----------------------------------------------------------------------------
class Noisy {
private:
    string tag_;
public:
    Noisy() : tag_("(空)") {
        cout << "      → Noisy 預設建構" << endl;
    }
    Noisy(const string& t) : tag_(t) {
        cout << "      → Noisy 複製建構: " << t << endl;
    }
    Noisy& operator=(const string& t) {
        tag_ = t;
        cout << "      → Noisy 賦值: " << t << endl;
        return *this;
    }
};

class UseAssign {          // 函數體賦值 → 會看到「預設建構 + 賦值」兩行
private:
    Noisy n_;
public:
    UseAssign(const string& t) { n_ = t; }
};

class UseInitList {        // 初始化列表 → 只會看到「複製建構」一行
private:
    Noisy n_;
public:
    UseInitList(const string& t) : n_(t) { }
};

// -----------------------------------------------------------------------------
// 【日常實務範例】HTTP 連線設定：欄位多時，初始化列表省下的白工會累積
//   情境：後端服務每次建立連線都要組一份設定物件（主機、路徑、User-Agent、
//         認證標頭）。四個 string 成員，用函數體賦值就是四次多餘的預設建構。
//   重點：這種「設定物件」在真實服務裡可能每秒建立上千次，
//         而且成員多半是字串，正是初始化列表最有價值的場景。
// -----------------------------------------------------------------------------
class HttpClientConfig {
private:
    string host_;
    string basePath_;
    string userAgent_;
    string authHeader_;
    int timeoutSec_;

public:
    HttpClientConfig(const string& host,
                     const string& basePath,
                     const string& userAgent,
                     const string& authHeader,
                     int timeoutSec)
        : host_(host)
        , basePath_(basePath)
        , userAgent_(userAgent)
        , authHeader_(authHeader)
        , timeoutSec_(timeoutSec)
    { }

    void print() const {
        cout << "  " << host_ << basePath_
             << "  (UA=" << userAgent_
             << ", Auth=" << (authHeader_.empty() ? "無" : "有")
             << ", timeout=" << timeoutSec_ << "s)" << endl;
    }
};

int main() {
    cout << "=== 函數體內賦值 ===" << endl;
    Demo d1("Hello", 42);
    d1.print();

    cout << "\n=== 初始化列表 ===" << endl;
    DemoInit d2("Hello", 42);
    d2.print();

    cout << "\n=== 把多做的那一步印出來：函數體賦值 ===" << endl;
    { UseAssign ua("Hello"); }

    cout << "\n=== 把多做的那一步印出來：初始化列表 ===" << endl;
    { UseInitList ui("Hello"); }

    cout << "\n=== 日常實務：HTTP 連線設定 ===" << endl;
    HttpClientConfig prod("https://api.example.com", "/v2",
                          "myapp/1.4", "Bearer xxxxx", 15);
    HttpClientConfig local("http://127.0.0.1:8080", "/",
                           "myapp-dev/1.4", "", 60);
    prod.print();
    local.print();

    return 0;
}

