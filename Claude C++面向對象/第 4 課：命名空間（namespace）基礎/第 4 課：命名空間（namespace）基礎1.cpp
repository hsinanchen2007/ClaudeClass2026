// =============================================================================
//  第 4 課：命名空間（namespace）基礎（1） — 定義命名空間與限定名稱
// =============================================================================
//
// 【主題資訊 Information】
//   語法：
//       namespace 名稱 { 宣告與定義... }        // 定義
//       名稱::成員                              // 限定名稱(qualified name)存取
//   標準版本：C++98 起;C++17 新增巢狀簡寫 namespace A::B::C { }。
//   標頭檔  ：無需額外標頭,namespace 是語言關鍵字。
//   複雜度  ：零執行期成本。命名空間只影響「名稱查找」,
//             編譯後不存在任何對應的執行期實體。
//
// 【詳細解釋 Explanation】
//
// 【1. 命名空間要解決的問題:名稱碰撞】
//   C 語言只有一個全域名稱空間。若你的專案寫了 int add(int,int),
//   而引入的第三方函式庫也有 add,連結器就會回報 multiple definition,
//   而且你完全無法迴避 —— 除非改名。
//   典型的 C 解法是加前綴:cv_add、gl_add、SDL_add,靠命名紀律硬撐。
//   C++ 的解法是把「前綴」提升為語言機制:
//       namespace math_utils { int add(int, int); }
//   於是完整名稱變成 math_utils::add,和別人的 other::add 不會衝突,
//   而且編譯器認得這個結構(可以有巢狀、可以取別名、可以選擇性引入)。
//
// 【2. 限定名稱(qualified name)是預設且最安全的用法】
//   math_utils::PI 這種寫法叫做「限定名稱」—— 明確指出要哪個命名空間裡的哪個名稱。
//   它的好處是完全沒有歧義:讀者一眼看出這個 PI 來自何處,
//   編譯器也不必猜。代價是稍微囉唆,這正是後面幾份檔案要討論的
//   using 宣告(選擇性引入)與 using 指示(全部引入)的動機。
//   慣例:標頭檔一律用限定名稱;.cpp 內部若嫌長,再考慮局部的 using。
//
// 【3. 命名空間裡可以放什麼】
//   變數、常數、函式、類別、型別別名、巢狀命名空間、樣板 —— 幾乎所有宣告。
//   本檔放了一個 const double 與兩個函式,是最典型的「工具集」用法。
//   注意 PI 宣告為 const:在命名空間範圍(namespace scope)的 const 物件
//   預設具有 internal linkage(內部連結),每個 .cpp 各有一份自己的副本,
//   因此把它寫在標頭檔裡不會造成重複定義。這是 C++ 相對於 C 的重要差異
//   (C 裡的 const 仍是 external linkage)。
//
// 【概念補充 Concept Deep Dive】
//   命名空間對「名稱查找」的影響,以及編譯後的實體:
//   (A) 名稱查找 —— 編譯器看到 math_utils::add(3, 5) 時,直接進入 math_utils
//       這個作用域找 add,不會外洩到全域。若寫的是沒有限定的 add(3, 5),
//       編譯器會依序找:區塊作用域 → 外層區塊 → 全域,找不到就報錯
//       (除非引數型別觸發 ADL,見下)。
//   (B) ADL(Argument-Dependent Lookup,引數依賴查找) —— 若函式的引數型別
//       定義在某個命名空間裡,該命名空間也會被自動納入查找範圍。
//       這就是為什麼 std::string s; std::cout << s; 可以運作,
//       而不必寫 std::operator<<(std::cout, s)。
//   (C) 名稱修飾(name mangling) —— 命名空間會被編碼進符號名稱。
//       math_utils::add(int,int) 在 Itanium ABI(g++/clang 採用)下的符號是
//       _ZN10math_utils3addEii,其中 10math_utils 就是命名空間名稱與其長度。
//       這正是連結期不會撞名的實際機制。
//
// 【注意事項 Pay Attention】
//   1. 命名空間的右大括號後面「不加分號」(class 和 struct 才要加)。
//   2. 命名空間本身不提供存取控制。裡面的東西全是公開的,
//      沒有 private 可言 —— 要隱藏實作細節請用匿名命名空間(見本課第 6 份檔案)。
//   3. 命名空間可以分次「重新開啟」擴充(見本課第 7 份檔案),
//      因此它不是封閉的實體,不要期待它能防止別人往裡面加東西。
//   4. 千萬不要在標頭檔的全域範圍寫 using namespace —— 那會把命名空間的
//      隔離效果整個抵銷掉,而且會傳染給每一個引入該標頭的檔案
//      (詳見本課第 3、4 份檔案)。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】命名空間基礎
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 命名空間解決了什麼問題?它有執行期成本嗎?
//     答：解決全域名稱碰撞 —— 讓不同函式庫可以各自擁有同名的函式或型別,
//         而不會在連結期產生 multiple definition。
//         沒有任何執行期成本:它純粹影響編譯期的名稱查找,
//         以及連結期符號的名稱修飾(g++ 下 math_utils::add 會被編碼成
//         _ZN10math_utils3addEii)。命名空間不會產生任何物件或間接層。
//     追問：那它和 class 裡的 static 成員函式差在哪?
//         → class 是型別,可以實體化、可以繼承、有存取控制(private/protected);
//           namespace 只是名稱的分組,不能實體化、沒有存取控制,
//           但可以分次擴充、可以取別名。單純要「分組一批自由函式」時用 namespace。
//
// ⚠️ 陷阱. 為什麼在標頭檔裡定義 const double PI = 3.14; 不會造成重複定義,
//         但 double PI = 3.14; 就會?
//     答：命名空間範圍(含全域)的 const 物件在 C++ 中預設是
//         internal linkage —— 每個 .cpp 各有一份自己的副本,連結器看不到彼此。
//         非 const 的全域變數則是 external linkage,多個 .cpp 引入同一標頭時
//         就會出現多個定義。
//     為什麼會錯：多數人把 C 的規則直接套過來 —— 在 C 裡 const 仍是
//         external linkage,同樣的寫法會撞。這是 C 與 C++ 少數不相容的細節之一。
//         (要在 C++ 讓標頭中的變數只有一份,C++17 之後可用 inline 變數。)
// ═══════════════════════════════════════════════════════════════════════════
//
// 【本檔不加 LeetCode 範例的理由】
//   命名空間是「程式碼組織」機制,不影響演算法或複雜度,
//   與 LeetCode 的評量面向沒有交集。強行掛一題只會誤導讀者
//   以為兩者有關。本檔改以「工具函式庫」這個最貼近真實用途的形式呈現。

#include <iostream>

// 定義命名空間 math_utils
namespace math_utils {
    const double PI = 3.14159265358979;
    
    int add(int a, int b) {
        return a + b;
    }
    
    int square(int x) {
        return x * x;
    }
}

int main() {
    // 使用命名空間中的成員，需要加上 命名空間:: 前綴
    std::cout << "PI = " << math_utils::PI << std::endl;
    std::cout << "add(3, 5) = " << math_utils::add(3, 5) << std::endl;
    std::cout << "square(4) = " << math_utils::square(4) << std::endl;
    
    return 0;
}

