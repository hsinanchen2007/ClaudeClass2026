// =============================================================================
//  第 4 課：命名空間（namespace）基礎（3） — using 指示:方便,但代價是什麼
// =============================================================================
//
// 【主題資訊 Information】
//   語法：using namespace 命名空間;          // using directive(using 指示)
//         例：using namespace std;
//   對照：using std::cout;                   // using declaration(見第 2 份檔案)
//   標準版本：C++98 起。
//   複雜度  ：零執行期成本。它只影響編譯期的名稱查找,不產生任何指令。
//
//   ★ 本檔可以正常編譯執行 —— 它示範的是「目前沒事,但何時會出事」。
//     出事的條件與實測到的錯誤訊息,寫在下方【4】與末尾但書。
//
// 【詳細解釋 Explanation】
//
// 【1. using 指示做了什麼】
//   它讓指定命名空間裡的「所有」名稱,在目前作用域中都能以非限定形式使用。
//   寫下 using namespace std; 之後,cout、string、vector、count、distance、
//   data、size、swap、begin、end、min、max…… std 裡數千個名稱一次全部可見。
//   好處顯而易見:程式碼變短、教學範例好讀。這也是為什麼幾乎所有入門書
//   和線上題目都這樣寫。
//
// 【2. 它與 using 宣告的本質差異:你知不知道自己引入了什麼】
//   using std::cout;      → 白名單。你點名了什麼,就只有什麼進來。
//   using namespace std;  → 全開。std 未來新增的名稱也會自動生效。
//   第二點特別值得注意:標準函式庫是「會長大」的。C++17 加入了
//   std::size、std::data、std::empty;C++20 加入了大量 ranges 相關名稱。
//   一段在 C++14 編得過的程式碼,只因為換到新標準,就可能突然出現歧義 ——
//   而你什麼都沒改。
//
// 【3. 為什麼它在標頭檔裡是重罪】
//   標頭檔會被 #include 到許多 .cpp。在標頭的全域範圍寫 using namespace std;,
//   等於強迫每一個引入者接受這個決定,而他們往往不知情。
//   於是「A 專案引入你的函式庫之後編不過」變成極難追查的問題 ——
//   問題根本不在他們的程式碼裡。
//   ★ 鐵則:標頭檔的全域範圍,永遠不要寫 using(宣告與指示皆然)。★
//
// 【4. 什麼時候真的會爆:本機實測】
//   下面這段是實際驗證過的最小重現:
//       #include <iostream>
//       #include <algorithm>       // 帶進 std::count
//       using namespace std;
//       int count = 0;             // 全域,與 std::count 撞名
//       int main() { count = 5; }  // ← 這一行編譯失敗
//   g++ 15.2.0 的錯誤是:
//       error: reference to 'count' is ambiguous
//       note: candidates are: template<...> std::count(...)
//                             int count
//   關鍵在於:錯誤發生在「使用」的那一行,不是在宣告的那一行。
//   宣告 int count 時一切正常,直到你去用它,編譯器才發現有兩個候選。
//   這使得問題往往在離錯誤根源很遠的地方才浮現。
//
//   反過來,實測也確認了:寫在「函式內部」的區域變數 int count = 0;
//   完全不受影響 —— 區塊作用域的宣告會遮蔽外層的名稱,不構成歧義。
//   這正是「若真要用 using namespace,至少把它關進函式裡」的理由
//   (見本課第 4 份檔案)。
//
// 【概念補充 Concept Deep Dive】
//   using 指示的查找規則,與直覺不太一樣:
//   (A) 它不會把名稱「複製」到目前作用域,而是讓查找時「也去看」那個命名空間。
//       嚴格說,這些名稱被視為出現在「該命名空間與目前作用域最近的共同外層」。
//   (B) 因此它不會與目前作用域的宣告直接衝突,而是在「使用時」才可能歧義。
//       這與 using 宣告不同 —— using 宣告若與既有宣告撞名,當場就報錯,
//       錯誤位置精準得多。
//   (C) 有一個例外情境常被忽略:即使沒有任何 using,ADL 仍可能把 std
//       帶進候選集合。例如對 std::vector<int> 呼叫非限定的 begin(v),
//       ADL 會找到 std::begin。所以「完全不引入 std」並不代表
//       std 的名稱永遠不參與重載解析。
//
// 【注意事項 Pay Attention】
//   1. 本檔可以正常編譯執行。它不是錯誤示範,而是「風險示範」——
//      危險只在特定條件下觸發(全域同名 + 該名稱真的被使用)。
//   2. 實務建議的優先順序:
//        ① 限定名稱 std::cout(標頭檔只能用這個)
//        ② 函式內的 using 宣告 using std::cout;
//        ③ 函式內的 using namespace std;
//        ④ .cpp 全域的 using namespace std;(小型程式可接受)
//        ⑤ 標頭檔全域的 using namespace std;(絕不可)
//   3. 競賽程式碼(以及本課教材)大量使用 using namespace std;,
//      因為生命週期短、單檔、無人維護。這個取捨在正式專案並不成立。
//   4. 本檔會從標準輸入讀取一個字串,執行方式見編譯說明。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】using namespace std 的風險
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼大家都說不要寫 using namespace std;?具體會出什麼事?
//     答：它把 std 裡數千個名稱一次全部引入,其中 count、distance、data、
//         size、swap、begin、end、min、max 都是極常見的變數/函式名。
//         一旦你的全域名稱與之重疊,使用該名稱時就會出現
//         「reference to 'X' is ambiguous」——而且錯誤發生在「使用處」,
//         不在宣告處,追查起來很費力。
//         更麻煩的是標準函式庫會持續新增名稱(C++17 的 std::size、
//         C++20 的 ranges),原本沒事的程式碼可能因為升級標準而突然編不過。
//     追問：那寫在 .cpp 裡可以嗎?
//         → 小型單檔程式可接受,風險自負且影響僅限該檔。
//           但標頭檔的全域範圍絕對不行 —— 它會傳染給每一個引入者,
//           而那些人完全無從得知問題來源。
//
// 🔥 Q2. 加了 using namespace std; 之後,程式會變慢或變大嗎?
//     答：完全不會。命名空間與 using 都是純編譯期的名稱查找機制,
//         不產生任何執行期實體。它唯一的成本是編譯期的查找範圍變大
//         (影響極小),以及「人」要付出的正確性風險。
//     追問：那為什麼不乾脆全部都用?
//         → 因為代價不在效能,在正確性與可維護性。
//           程式碼的讀者(包括半年後的你)會無法一眼判斷某個名稱來自哪裡。
//
// ⚠️ 陷阱. 我寫了 using namespace std; 又宣告了全域 int count = 0;,
//         編譯器在「宣告那一行」沒有報錯,是不是代表沒問題?
//     答：不是。實測(g++ 15.2.0)顯示宣告行確實不報錯,
//         但只要之後有任何一處使用非限定的 count,就會在「那一行」爆出
//         error: reference to 'count' is ambiguous。
//         using 指示不是把名稱複製進來,而是讓查找時「也去看」std,
//         所以衝突要等到查找真正發生時才被偵測到。
//     為什麼會錯：把「編譯通過的那一刻」當成安全證明。
//         實際上 using 指示埋下的是延遲引爆的地雷 ——
//         可能在你新增一行程式碼、或升級到新標準時才觸發。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【本檔不加 LeetCode 範例的理由】
//   using 指示是名稱查找機制,與演算法及複雜度無關。
//   附帶一提:LeetCode 的提交範本正是 using namespace std; 最合理的使用場景 ——
//   單檔、生命週期只有幾分鐘、沒有人需要維護。
//   把同樣的寫法帶進正式專案,才是問題所在。

#include <iostream>
#include <string>

using namespace std;  // 引入 std 的所有成員（方便，但風險見上方說明）

int main() {
    cout << "Hello" << endl;
    string name = "Alice";

    if (!(cin >> name)) {
        // 讀取失敗（沒有輸入／直接 EOF）時，標準規定 name 會先被清空並設 failbit
        name = "(無輸入)";
    }
    cout << "讀到的名字：" << name << endl;

    // ── 區域變數不受 using namespace std 影響 ──────────────────────
    // 這個 count 在區塊作用域中，會遮蔽外層（含 std）的同名名稱，完全安全。
    // 但若把它移到「全域」，再加上 #include <algorithm>，
    // 使用它的那一行就會變成 error: reference to 'count' is ambiguous（實測）。
    int count = 0;
    for (char c : name) if (c != ' ') ++count;
    cout << "名字中的非空白字元數 = " << count << endl;

    return 0;
}

// 編譯: g++ -std=c++17 -Wall -Wextra "第 4 課：命名空間（namespace）基礎3.cpp" -o ns3
// 執行: echo "Bob" | ./ns3        ← 本檔會讀一行標準輸入

// ─────────────────────────────────────────────────────────────────────────────
// 【輸出但書】
//  1. 本檔會從標準輸入讀取一個字串。以下預期輸出對應的輸入是 echo "Bob"。
//     若以 EOF 結束(./ns3 < /dev/null),標準規定 operator>> 會先清空字串
//     並設 failbit,本檔會補上 "(無輸入)"。
//  2. 「名字中的非空白字元數 = 3」對應輸入 "Bob"。注意 cin >> 以空白分隔,
//     只會讀到第一個詞,所以即使輸入 "Bob Smith" 也只會讀到 "Bob"。
//  3. 本檔的 count 是「區域」變數,實測完全不受 using namespace std 影響。
//     實測反例(另建的最小重現檔,不在本檔內):把 count 移到全域並
//     #include <algorithm>,g++ 15.2.0 在「使用 count 的那一行」報
//         error: reference to 'count' is ambiguous
//         note: candidates are: template<...> std::count(...)
//                               int count
//     ——注意錯誤在使用處而非宣告處,這正是 using 指示難以追查的原因。
//  4. 以下為本機 g++ 15.2.0 (Ubuntu 26.04) 以 echo "Bob" 餵入、
//     連續執行 3 次逐位元組相同的結果。
// ─────────────────────────────────────────────────────────────────────────────

// === 預期輸出 ===
// Hello
// 讀到的名字：Bob
// 名字中的非空白字元數 = 3
