// =============================================================================
//  第 4 課：命名空間（namespace）基礎（2） — using 宣告:只把要用的名稱請進來
// =============================================================================
//
// 【主題資訊 Information】
//   語法：using 命名空間::成員名稱;        // using declaration(using 宣告)
//         例：using std::cout;
//   對照：using namespace 命名空間;        // using directive(using 指示,見第 3 份檔案)
//   標準版本：C++98 起。
//   複雜度  ：零執行期成本 —— 它只是在目前作用域裡新增一筆「名稱查找」的對應。
//
// 【詳細解釋 Explanation】
//
// 【1. using 宣告 vs using 指示:一個字的差別,天差地遠】
//       using std::cout;        // 宣告:只把 cout 這「一個名稱」引入目前作用域
//       using namespace std;    // 指示:讓 std 裡「所有名稱」在此作用域可見
//   前者是白名單(你逐一點名),後者是全開(把整個 std 倒進來)。
//   本檔示範前者,下一份檔案示範後者並說明它的風險。
//   兩者的關鍵差異在「你是否清楚知道自己引入了什麼」——
//   std 裡有數千個名稱,其中不乏 count、distance、data、size、swap、
//   begin、end 這些極容易和自己的變數或函式撞名的常見字。
//
// 【2. 精確引入為什麼比較安全】
//   using std::cout; 只讓 cout 這個名稱可見。如果你自己另外定義了
//   int count = 0;,完全不受影響 —— 因為你根本沒有引入 std::count。
//   相對地,using namespace std; 之後,你的 count 會和 std::count 同時可見,
//   在某些呼叫情境下造成歧義(ambiguous)或意外選到不是你要的那個。
//   結論:引入的名稱越少,可控性越高。這是「最小權限原則」在名稱上的應用。
//
// 【3. 沒有被引入的名稱,仍然要寫完整】
//   本檔只引入了 cout、endl、string 三個,所以第 13 行的 std::cin
//   仍然必須寫完整 —— 這正好證明 using 宣告是「逐一點名」而非「全部放行」。
//   若把 std::cin 寫成 cin,會得到
//       error: 'cin' was not declared in this scope
//   而且 g++ 會貼心地提示 'std::cin'。
//
// 【4. 放在哪裡:作用域決定影響範圍】
//   本檔的三個 using 宣告寫在全域範圍,因此整個檔案都適用。
//   更嚴謹的做法是放進函式內部(見本課第 4 份檔案),
//   讓影響範圍縮到最小。而無論如何:
//   ★ 絕對不要把任何 using 寫在標頭檔的全域範圍 ★
//   因為它會傳染給每一個 #include 該標頭的原始檔,而那些檔案的作者
//   完全不知情 —— 這是實務上最常見、也最難追查的名稱汙染來源。
//
// 【概念補充 Concept Deep Dive】
//   using 宣告有幾個值得知道的性質:
//   (A) 它引入的是「名稱」,不是「單一多載」。using std::swap; 會把 std::swap
//       的所有多載一次引入。這正是「std::swap 慣用法」的基礎:
//           using std::swap;   // 讓 std::swap 進入候選
//           swap(a, b);        // 非限定呼叫 → ADL 會優先找到使用者自訂的版本
//       這種寫法既能用到使用者為自己型別特製的 swap,又保證退無可退時
//       還有 std::swap 可用。若寫死 std::swap(a, b),就永遠用不到自訂版本。
//   (B) 它會參與重載解析。引入之後,該名稱與目前作用域既有的同名函式
//       一起構成候選集合,重載規則照舊。
//   (C) 若目前作用域已經有「同名同參數」的宣告,會直接編譯錯誤,
//       而不是靜靜地覆蓋掉 —— 這比 using 指示的行為明確得多。
//
// 【注意事項 Pay Attention】
//   1. using 宣告不會複製任何東西,也不建立別名物件。
//      cout 和 std::cout 是同一個物件,只是查找路徑多了一條。
//   2. 標頭檔的全域範圍禁用 using(宣告與指示皆然)。.cpp 內可酌情使用。
//   3. using std::endl; 常見但值得留意:endl 除了換行還會 flush 串流。
//      在迴圈中大量使用會嚴重拖慢輸出,單純換行請用 '\n'。
//      本檔沿用 endl 是為了示範 using 宣告,不是推薦寫法。
//   4. 本檔會從標準輸入讀取一個字串。直接執行會等待輸入;
//      要重現末尾的預期輸出,請用管線餵入(見編譯說明)。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】using 宣告
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. using std::cout; 和 using namespace std; 差在哪裡?
//     答：前者是 using declaration,只把 cout 這一個名稱引入目前作用域;
//         後者是 using directive,讓整個 std 命名空間的所有名稱在此可見。
//         前者是白名單、可控;後者是全開,會把 count、distance、data、size
//         這類極易撞名的常見字一併放進來。
//     追問：兩者都寫在函式內,還有差別嗎?
//         → 有。作用域縮小只是限制了「影響範圍」,沒有改變「引入多少名稱」。
//           函式內的 using namespace std; 仍然會讓該函式裡的 count
//           變得有歧義風險,只是不會波及其他函式。
//
// 🔥 Q2. 為什麼標準函式庫推薦「using std::swap; swap(a, b);」
//        而不是直接寫 std::swap(a, b)?
//     答：這是為了同時支援 ADL。非限定的 swap(a, b) 會先透過引數依賴查找,
//         找到使用者為自己型別特製的 swap(通常比通用版快得多);
//         找不到時才退回被 using 引入的 std::swap。
//         若寫死 std::swap(a, b),就直接跳過了使用者的特化版本。
//     追問：這個慣用法還用在哪些地方?
//         → std::begin/std::end(泛型程式碼支援 C 陣列與自訂容器)、
//           以及 C++20 之前處理數學函式多載的場合,模式完全相同。
//
// ⚠️ 陷阱. 本檔已經寫了 using std::cout;,為什麼第 13 行的 cin 還是要寫 std::cin?
//     答：因為 using 宣告是「逐一點名」。你只點了 cout、endl、string 三個名字,
//         cin 從來沒有被引入,自然不在查找範圍內。
//         寫成 cin 會得到 error: 'cin' was not declared in this scope。
//     為什麼會錯：把 using 宣告誤解成「打開了 std 這扇門」。
//         實際上它比較像「把某一個名字抄一份到目前的名冊上」——
//         沒抄到的名字,門依然是關著的。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【本檔不加 LeetCode 範例的理由】
//   using 宣告是名稱查找機制,不影響演算法或複雜度,與 LeetCode 的評量無關。
//   下方改以「命名衝突」的真實情境示範它的價值。

#include <iostream>
#include <string>

using std::cout;    // 只引入 cout
using std::endl;    // 只引入 endl
using std::string;  // 只引入 string

// -----------------------------------------------------------------------------
// 【日常實務範例】為什麼「只引入需要的名稱」在真實專案裡很重要
// 情境：資料處理程式裡,count 是再自然不過的變數名(計數器)。
//       本檔只做了 using std::cout / endl / string,沒有引入 std::count,
//       所以下面這個自訂的 count 完全安全。
//       若當初寫的是 using namespace std;,std::count(<algorithm> 的演算法)
//       也會同時可見,此時再寫非限定的 count(...) 呼叫就可能產生歧義。
// -----------------------------------------------------------------------------
int count = 0;                     // 與 std::count 同名，但這裡完全不受影響

void tally(const string& line) {   // string 已由 using 宣告引入
    if (!line.empty()) ++count;
}

int main() {
    cout << "Hello" << endl;  // 可以直接使用 cout 和 endl
    string name = "Alice";    // 可以直接使用 string

    // 其他成員仍需完整名稱：本檔沒有 using std::cin，所以 cin 必須寫全
    // （若寫成 cin >> name; 會得到 error: 'cin' was not declared in this scope）
    if (!(std::cin >> name)) {
        // 讀取失敗（例如沒有輸入、直接 EOF）時，標準規定 name 會被清空並設 failbit。
        // 這裡明確補上預設值，避免後續使用到空字串。
        name = "(無輸入)";
    }
    cout << "讀到的名字：" << name << endl;

    // 示範：自訂的全域 count 與 std 名稱和平共存
    tally("2026-07-19 [INFO] service started");
    tally("");                       // 空行不計入
    tally("2026-07-19 [WARN] slow response");
    cout << "自訂 count（非 std::count）= " << count << endl;

    return 0;
}

