// =============================================================================
//  第 4 課：命名空間（namespace）基礎（4） — 把 using 關進函式:作用域即防線
// =============================================================================
//
// 【主題資訊 Information】
//   語法：函式(或任意區塊)內部同樣可以寫 using 宣告與 using 指示,
//         其效力在該區塊的右大括號結束。
//             void f() { using namespace std; ... }   // 只在 f 內生效
//   標準版本：C++98 起。
//   複雜度  ：零執行期成本(純編譯期名稱查找)。
//
// 【詳細解釋 Explanation】
//
// 【1. 核心觀念:using 的效力範圍就是它所在的作用域】
//   把 using namespace std; 寫在 processData() 內部,std 的名稱就只在
//   processData 的函式本體中可見。main() 完全不受影響 —— 這就是為什麼
//   本檔 main 裡仍然必須寫 std::cout,即使前面的函式已經「using 過了」。
//   這不是限制,這正是我們要的:把便利性侷限在真正需要的地方,
//   讓風險不會外溢。
//
// 【2. 這個寫法解決了什麼、沒解決什麼】
//   解決了：影響範圍。名稱衝突最多只會發生在這一個函式裡,
//           而且一定看得見肇因(using 就寫在上面幾行),追查成本極低。
//           標頭檔即使被引入,也不會把 std 倒進別人的檔案。
//   沒解決：引入的「數量」。函式內的 using namespace std; 依然是全開 ——
//           這個函式裡若有區域變數叫 data 或 size,一樣可能出問題。
//           真正精確的做法是函式內的 using 宣告:using std::cout;。
//   一句話:縮小作用域是止血,精確引入才是治本。兩者可以疊加使用。
//
// 【3. 為什麼區域變數能安全地遮蔽 std 的同名名稱】
//   C++ 的名稱查找由內而外:先找目前區塊,找到就停。
//   區塊內宣告的 data 會遮蔽(hide)外層一切同名者,連歧義都不會發生。
//   相對地,全域範圍的宣告與 using namespace 引入的名稱處於同一層級,
//   查找時兩者都是候選 → 才會出現 ambiguous(見第 3 份檔案的實測)。
//   這就是「宣告位置越深,越安全」的機制性原因。
//
// 【概念補充 Concept Deep Dive】
//   using 指示的作用域規則有個細節值得知道:
//   它並非「把名稱塞進目前區塊」,而是讓那些名稱在
//   「該命名空間與目前作用域最近的共同外層」被視為可見。
//   對函式內的 using namespace std; 而言,這個共同外層是全域範圍。
//   結果是:std 的名稱在查找上像是位於全域,但這條額外的查找路徑
//   只在該函式的作用域內有效。
//   實務上的推論:即使寫在函式裡,若函式內用到的名稱在全域也有同名者,
//   仍可能歧義。所以「關進函式」降低的是暴露面,不是把風險歸零。
//
// 【注意事項 Pay Attention】
//   1. 標頭檔的「全域範圍」永遠不要寫 using。但標頭檔中「函式本體內部」
//      (例如 inline 函式或樣板的實作)寫 using 是可以接受的 ——
//      因為它不會外洩到引入者的作用域。
//   2. 順序也重要:using 必須出現在使用那些名稱之前,
//      因為名稱查找是由上而下進行的。
//   3. 本檔 main 裡的 std:: 前綴不是多餘的贅字,而是必要的 ——
//      它證明了 processData 內的 using 確實沒有外洩。
//   4. 若同一個函式內同時 using 兩個含有同名成員的命名空間,
//      使用該名稱時一樣會歧義。縮小作用域不等於免疫。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】using 的作用域
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 在函式內寫 using namespace std;,和寫在檔案全域,差別在哪?
//     答：差在「影響範圍」。函式內的版本只在該函式本體中有效,
//         main 或其他函式完全不受影響(本檔 main 仍須寫 std::cout 即為證明)。
//         寫在全域則整個翻譯單元都受影響;若寫在標頭檔的全域範圍,
//         還會傳染給每一個 #include 它的原始檔。
//     追問：那放在函式裡就完全安全了嗎?
//         → 沒有。它依然是「全開」——把 std 的所有名稱引入。
//           該函式內若有變數叫 data、size、count,仍可能出問題。
//           要真正精確,應改用函式內的 using 宣告:using std::cout;。
//
// 🔥 Q2. 為什麼函式內宣告的區域變數 vector<int> data 不會和 std 撞名,
//        但全域的同名變數就會?
//     答：因為名稱查找是由內而外的。區域宣告會「遮蔽」外層一切同名者,
//         找到就停,不會繼續往外找,所以不存在兩個候選。
//         全域宣告則與 using 指示引入的名稱位於同一查找層級,
//         兩者同時是候選 → 產生 ambiguous。
//     追問：那如果我在函式裡故意宣告 int count = 0; 又想呼叫 std::count?
//         → 寫限定名稱 std::count(...) 即可。限定名稱直接指定作用域,
//           完全繞過非限定查找,不受任何遮蔽或 using 影響。
//
// ⚠️ 陷阱. 我在標頭檔裡把 using namespace std; 寫在 #include 之後、
//         所有宣告之前,想說「反正只是我的標頭」——為什麼這是重大錯誤?
//     答：因為 #include 的本質是文字貼上。你的標頭被貼進別人的 .cpp 之後,
//         那行 using namespace std; 就出現在對方檔案的全域範圍,
//         效力涵蓋該檔案「引入你的標頭之後」的所有程式碼。
//         對方的全域 count 因此突然變成歧義,而錯誤訊息指向他自己的檔案,
//         他幾乎不可能想到是你的標頭造成的。
//     為什麼會錯：把標頭檔想成「獨立的模組」,以為裡面的宣告有邊界。
//         在 C++20 modules 之前,#include 沒有任何邊界可言 ——
//         它就是純粹的文字展開。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【本檔不加 LeetCode 範例的理由】
//   本檔談的是作用域與名稱查找的組織問題,與演算法無關。
//   下方改以「函式各自選擇引入策略」的實務對照來呈現。

#include <iostream>
#include <vector>
#include <string>

// 不要在全域範圍使用 using namespace

void processData() {
    // 在函數內部使用是安全的，作用域限制在這個函數內
    using namespace std;

    vector<int> data = {1, 2, 3, 4, 5};
    for (int x : data) {
        cout << x << " ";
    }
    cout << endl;
}

// -----------------------------------------------------------------------------
// 【日常實務範例】同一個檔案裡，不同函式各自選擇引入策略
// 情境：一個小型報表模組。上面的 processData() 用「函式內 using 指示」求方便；
//       下面的 summarize() 改用「函式內 using 宣告」——只點名真正用到的三個名稱。
//       兩者互不影響，這正是把 using 關進函式的價值：
//       每個函式可以獨立決定要多方便、承擔多少風險。
// -----------------------------------------------------------------------------
void summarize(const std::vector<int>& values) {
    using std::cout;      // 只引入需要的三個名稱，比 using namespace std 精確得多
    using std::endl;
    using std::string;

    int total = 0;
    int maxV = values.empty() ? 0 : values[0];
    for (int v : values) {
        total += v;
        if (v > maxV) maxV = v;
    }

    // 這個區域變數叫 size —— 與 std::size（C++17 起存在）同名。
    // 因為本函式沒有 using namespace std，所以完全不構成任何風險。
    const std::size_t size = values.size();

    string label = "報表";
    cout << label << "：筆數=" << size
         << " 總和=" << total
         << " 最大值=" << maxV << endl;
}

int main() {
    // 這裡仍需使用完整名稱
    std::cout << "開始處理..." << std::endl;
    processData();

    // processData() 內的 using 完全沒有外洩到這裡，
    // 所以下面這行一樣必須寫出 std:: 前綴。
    std::vector<int> values = {8, 3, 17, 4, 12};
    summarize(values);

    std::cout << "處理完成" << std::endl;

    return 0;
}

