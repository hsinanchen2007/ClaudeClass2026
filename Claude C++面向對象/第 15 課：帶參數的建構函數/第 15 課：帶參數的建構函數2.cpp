// =============================================================================
//  第 15 課：帶參數的建構函數 2  —  建構函數參數為什麼要用 const 引用
// =============================================================================
//
// 【主題資訊 Information】
//   語法：  Class(const std::string& s);      // 建議寫法
//           Class(std::string& s);            // 只能綁 non-const 左值
//           Class(std::string s);             // 值傳遞，會多一次複製
//   標準版本：C++98 起即有；C++11 之後多了「值傳遞 + std::move」的替代方案
//   複雜度：const& 傳參本身是 O(1)（只傳位址），複製成本發生在函數體內
//   標頭檔：<string>
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼不用值傳遞 Class(std::string s)】
//   值傳遞會在「呼叫的當下」就複製一份 string。若這個 string 很長（例如一整份
//   HTML），就是一次完整的 heap 配置 + memcpy。建構函數往往是熱路徑（每建立
//   一個物件就跑一次），這種隱形成本會累積。
//   const& 只傳遞一個位址（在 64-bit 上就是 8 bytes），不論字串多長都一樣快。
//
// 【2. 為什麼不用非 const 引用 Class(std::string& s)】
//   這是本檔的核心。C++ 的規則是：
//       非 const 左值引用（T&）只能綁定到「具名的、可修改的左值」。
//   而下面這三種東西都不是：
//       Demo d2("直接傳字串");          // const char[N] → 產生臨時 string（右值）
//       Demo d3(string("臨時字串"));    // 明確建立的臨時物件（右值）
//       const string cs = "x"; Demo d(cs);  // const 左值
//   所以只要參數寫成 string&，上面三種呼叫全部編譯失敗。使用者會覺得
//   「我明明只是傳個字串進去，為什麼不能編譯？」——這就是 API 設計失敗。
//
// 【3. const& 為什麼可以綁定臨時物件】
//   標準特別規定：const 左值引用可以綁定到右值，而且會把該臨時物件的生命週期
//   延長到「該引用的生命週期結束為止」。在建構函數參數的情境下，臨時 string
//   活到建構函數返回為止——這段期間足夠我們把它複製進成員變數。
//   語意上也講得通：既然我保證不改它（const），讓我讀一個臨時值並無風險。
//
// 【4. 讀完之後真正發生的複製在哪裡】
//   注意本檔的建構函數體內是 data = s;——這是「賦值」，而不是初始化。
//   流程其實是兩步：
//       (a) 進入函數體前，成員 data 先被「預設建構」成空字串
//       (b) 函數體內再用 operator= 把 s 的內容複製進去
//   用初始化列表 : data(s) {} 可以省掉 (a)，直接以複製建構完成。這是第 16 課
//   的主題，本檔先維持函數體賦值的寫法，好讓兩課的對比更清楚。
//
// 【概念補充 Concept Deep Dive】
//
//   ● 引用在組合語言層面就是指標
//     const string& 在 ABI 上傳的是一個位址。所以「傳引用比較快」的真正意思是
//     「傳 8 bytes 而不是傳整個物件」，不是什麼編譯器魔法。
//
//   ● 值傳遞 + std::move 的現代替代方案（C++11 起）
//         Demo(std::string s) : data(std::move(s)) {}
//     呼叫端傳左值 → 複製一次；傳右值 → 移動一次（幾乎免費）。
//     而 const& 版本則是：傳左值 → 複製一次；傳右值 → 仍然複製一次。
//     所以在「一定會把參數存起來」的建構函數裡，by-value + move 有時更好。
//     代價是永遠會有一次移動，而且會多一個參數物件。兩種寫法都常見，
//     const& 仍然是最安全、最不需要解釋的預設選擇。
//
//   ● 為什麼字面值 "直接傳字串" 會產生臨時物件
//     它的型別是 const char[N]，不是 std::string。編譯器必須先呼叫
//     std::string 的 const char* 建構函數，做出一個臨時 string，才能綁定參數。
//
// 【注意事項 Pay Attention】
//   1. const& 參數不能在函數內修改；要修改請複製一份到區域變數。
//   2. 小型內建型別（int、double、指標）用值傳遞即可，包成 const int& 反而
//      多一層間接，通常更慢。經驗法則：sizeof 大於兩個指標才考慮 const&。
//   3. 不要回傳區域變數的 const&——生命週期延長只適用於「綁定臨時物件到
//      const 引用」的那一刻，不會讓你把它安全地傳出函數。
//   4. 本檔的 data 是 public，純粹為了讓 main 直接印出來看結果；正式程式
//      應該封裝成 private + getter（第 3 課封裝的內容）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】建構函數的參數傳遞方式
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 建構函數參數為什麼建議用 const std::string&，而不是 std::string&？
//     答：非 const 左值引用只能綁定具名的可修改左值，字面值、臨時物件、const
//         物件全部綁不上，呼叫端會大量編譯失敗。const& 則可以綁定右值，
//         而且不需要複製整個字串，只傳一個位址。
//     追問：那為什麼不乾脆用值傳遞就好？
//         → 值傳遞在呼叫當下就複製一份；只有在「參數一定會被存起來、且想吃到
//           右值的移動」時，by-value + std::move 才划算。
//
// 🔥 Q2. const 引用可以綁定臨時物件，那個臨時物件什麼時候被銷毀？
//     答：綁定到 const 左值引用會延長臨時物件的生命週期，延長到該引用本身
//         的生命週期結束。在函數參數的情況下，臨時物件活到函數呼叫結束。
//     追問：如果把這個 const& 存成類別成員，臨時物件會跟著活著嗎？
//         → 不會。生命週期延長不會透過建構函數參數再傳遞下去，
//           存成成員引用會得到懸空引用（dangling reference）。
//
// ⚠️ 陷阱. 把所有參數都改成 const& 就一定比較快嗎？
//     答：不是。對 int、double、指標這類小型型別，const int& 傳的是位址，
//         使用時還要多一次解參考，而且會阻礙暫存器傳參的最佳化，通常更慢。
//     為什麼會錯：把「傳引用避免複製」背成無條件的規則，卻忘了複製一個 int
//         本來就只要一道指令，根本沒有需要避免的成本。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class Demo {
public:
    string data;

    // 如果用普通引用（非 const）
    // Demo(string& s) { data = s; }
    //   → 下面 main 裡的 d2、d3、d4 都會編譯失敗，因為字面值、臨時物件
    //     與 const 左值都綁不到非 const 的 string&。

    // 用 const 引用（推薦）：左值、右值、const 物件全部收得下
    Demo(const string& s) { data = s; }
};

// -----------------------------------------------------------------------------
// 【日常實務範例】設定檔載入器：以 const& 收字串，避免每建一個物件就複製一次
//   情境：程式啟動時讀取設定，key/value 都是字串。設定項可能有上百筆，
//         每筆都值傳遞的話就是上百次多餘的 heap 配置。
// -----------------------------------------------------------------------------
class ConfigEntry {
private:
    string key_;
    string value_;

public:
    // 兩個參數都用 const&：呼叫端可以傳變數，也可以直接傳字面值
    ConfigEntry(const string& key, const string& value)
        : key_(key), value_(value) { }

    void print() const {
        cout << "  " << key_ << " = " << value_ << endl;
    }
};

int main() {
    string name = "測試";

    cout << "=== 情境 1：傳入具名變數（左值）===" << endl;
    // 這是唯一一種「非 const 引用也能收」的情況
    Demo d1(name);
    cout << "d1.data = " << d1.data << endl;

    cout << "\n=== 情境 2：傳入字面值 ===" << endl;
    // "直接傳字串" 的型別是 const char[N]，會先轉成臨時 string（右值）
    // 非 const 引用在此會編譯失敗；const 引用可以綁定
    Demo d2("直接傳字串");
    cout << "d2.data = " << d2.data << endl;

    cout << "\n=== 情境 3：傳入臨時物件 ===" << endl;
    // string("臨時字串") 明確產生一個右值
    Demo d3(string("臨時字串"));
    cout << "d3.data = " << d3.data << endl;

    cout << "\n=== 情境 4：傳入 const 左值 ===" << endl;
    // const 物件同樣綁不到 string&，只有 const string& 收得下
    const string frozen = "不可修改的來源";
    Demo d4(frozen);
    cout << "d4.data = " << d4.data << endl;

    cout << "\n=== 日常實務：設定檔項目 ===" << endl;
    ConfigEntry e1("server.host", "127.0.0.1");   // 兩個都是字面值
    string port = "8080";
    ConfigEntry e2("server.port", port);          // 一個字面值、一個變數
    e1.print();
    e2.print();

    return 0;
}

