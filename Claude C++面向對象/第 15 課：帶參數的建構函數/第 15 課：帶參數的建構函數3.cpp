// =============================================================================
//  第 15 課：帶參數的建構函數 3  —  參數與成員同名造成的自我賦值（錯誤示範）
// =============================================================================
//
// 【主題資訊 Information】
//   主題：name shadowing（名稱遮蔽）導致 name = name; 變成參數自我賦值
//   標準版本：C++98 起就存在；至今仍是最常見的建構函數初學者錯誤之一
//   本檔性質：**錯誤示範**。程式可以編譯、可以執行，但成員從未被真正設定。
//             為了讓輸出可重現、又不在教材中實際執行 UB，示範類別的 int 成員
//             改用 C++11 類別內預設初始化給哨兵值 -1（詳見類別上方說明）。
//   相關診斷：-Wshadow（GCC/Clang 皆有）、-Wself-assign（Clang，-Wall 即開啟）
//   標頭檔：<string>
//
// 【詳細解釋 Explanation】
//
// 【1. 遮蔽（shadowing）是怎麼發生的】
//   C++ 的名稱查找是「由內而外」：編譯器在一個作用域看到識別字 name，
//   會先找最近的宣告。建構函數的參數列是比類別作用域更內層的作用域，
//   所以在函數體內寫 name，找到的是「參數 name」，不是「成員 name」。
//   成員被完全遮蔽了——這就是 shadowing。
//
// 【2. 於是 name = name; 究竟做了什麼】
//   左邊的 name 是參數，右邊的 name 也是參數。這行等於把參數指派給它自己，
//   是一個完全合法、但毫無作用的敘述。成員 name 從頭到尾沒有被碰過。
//   同理 age = age; 也只是參數自我賦值。
//
// 【3. 兩個成員的「沒被設定」後果完全不同——這是本檔最重要的觀念】
//   ● 成員 name 是 std::string：它有預設建構函數。即使建構函數體什麼都沒做，
//     進入函數體之前 name 已經被預設建構成「空字串」。
//     所以印出來是空的——這是**確定**的行為，不是 UB。
//   ● 成員 age 若宣告成 int age;（不給初值）：內建型別沒有預設建構函數，
//     在這種建構方式下它的值是「不定值（indeterminate value）」，
//     讀取它是**未定義行為**。
//   一句話總結：name 是「有值但不是你要的值」，未初始化的 age 是「根本不該讀」。
//   ● 本檔的示範類別把 age 寫成 int age = -1;（C++11 類別內預設初始化），
//     讓它變成確定行為，同時仍然清楚呈現「成員沒收到傳入的 25」這個 bug。
//     這也順帶示範了一個實用的防禦技巧：替內建型別成員寫上類別內初值，
//     忘記賦值時就會得到一個顯眼的哨兵值，而不是無法追查的不定值。
//
// 【4. 為什麼編譯器沒有攔下我】
//   本機以 g++ 15.2 實測：
//       g++ -std=c++17 -Wall -Wextra          → 完全沒有警告
//       g++ -std=c++17 -Wall -Wextra -Wshadow → 兩個 -Wshadow 警告（指出遮蔽）
//       clang++ -std=c++17 -Wall              → 直接指出自我賦值
//                                               （-Wself-assign / -Wself-assign-overloaded）
//   也就是說，**光開 -Wall -Wextra 是抓不到這個 bug 的**。這正是很多人被咬到
//   的原因。實務上請把 -Wshadow 一起打開。
//
// 【概念補充 Concept Deep Dive】
//
//   ● 「不定值」與「垃圾值」的差別
//     很多教材寫「age 是垃圾值」，這個講法會誤導。標準的說法是不定值：
//     編譯器可以假設你不會讀它，並據此最佳化。所以結果不只是「某個隨機數字」，
//     而是整段程式的行為都失去保證——理論上不同的最佳化等級可以印出不同的東西，
//     甚至讓相關的判斷式被整段移除。
//     本檔開發過程有個很好的實證：原本 int age; 版本在 -O0 到 -O3 都印出 1，
//     看起來「很穩定」；但只是在同一個檔案裡多加了幾個修正示範類別、
//     完全沒動到那個類別，同一支編譯器印出來就變成 -1809194880。
//     這說明「我試過都印一樣」完全不能當作保證。
//
//   ● 為什麼 std::string 成員是安全的
//     只要類別有任何使用者定義的建構函數被執行，所有具有預設建構函數的
//     非靜態成員都會在進入函數體前被預設建構。std::string 的預設建構函數
//     保證產生空字串，所以它永遠是有效物件——不是 UB。
//
//   ● 修正方法有四種（下面四個類別逐一示範）
//     (a) 參數改名（最直白）
//     (b) 成員加前綴 m_ 或後綴 _（團隊風格）
//     (c) 用 this->name = name;（明確指定成員）
//     (d) 用初始化列表 : name(name)（最推薦，見第 16 課）
//     其中 (d) 之所以不會遮蔽，是因為初始化列表中「括號外的名字」只會在
//     類別作用域查找，一定是成員；括號內才是參數。
//
// 【注意事項 Pay Attention】
//   1. 本檔的 BadExample 是刻意保留的錯誤示範（自我賦值），請不要把這種
//      寫法帶進正式程式。其 int 成員已改為類別內初始化，因此本檔本身不執行 UB；
//      但若你把它改回 int age; 再印出來，那就是不折不扣的未定義行為。
//   2. 不要把 UB 的實測結果當成規格。任何「我試過就是印 X」的說法都不成立。
//   3. -Wall -Wextra 抓不到這題；請額外加 -Wshadow。
//   4. 成員如果是 std::string、std::vector 這類有預設建構函數的型別，
//      忘記賦值只會得到「空物件」而不是 UB，症狀比較溫和，也因此更難發現。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】參數遮蔽成員與未初始化成員
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 建構函數寫成 BadExample(string name, int age) { name = name; age = age; }
//        會發生什麼事？
//     答：參數遮蔽了同名成員，兩行都是參數對自己賦值，成員完全沒被設定。
//         成員 name 是 std::string，會被預設建構成空字串；成員 age 是 int，
//         若沒有類別內初值則為不定值，讀取它是未定義行為。
//     追問：那為什麼程式跑起來好像「只是印錯」而沒有崩潰？
//         → 因為讀不定值不保證會崩潰。UB 的可怕之處正是它常常「看起來像能跑」，
//           等到換編譯器、換最佳化等級才爆炸。
//
// 🔥 Q2. -Wall -Wextra 能抓到這個錯誤嗎？
//     答：以 g++ 15.2 實測，不能。要加 -Wshadow 才會警告名稱遮蔽；
//         Clang 則在 -Wall 下就會用 -Wself-assign 指出自我賦值。
//     追問：那你平常的編譯旗標會加什麼？
//         → 至少 -Wall -Wextra -Wshadow；再視專案加 -Wconversion、
//           以及在測試階段用 -fsanitize=address,undefined。
//
// ⚠️ 陷阱. 只要把 name = name; 改成 this->name = name; 就全部修好了嗎？
//     答：那一行是修好了，但如果你只改了 name 卻忘了 age，age 依然是不定值。
//         而且 this-> 只是「繞過遮蔽」，遮蔽本身還在，下次有人在函數體其他
//         地方寫 name 仍然拿到參數。
//     為什麼會錯：把問題定義成「這一行寫錯了」，而不是「命名設計讓成員被遮蔽」。
//         真正的修法是消除遮蔽（改名或加前綴），或改用初始化列表。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

// -----------------------------------------------------------------------------
// 錯誤示範：參數與成員同名，函數體內全部是自我賦值
//
//   ※ 關於 age 的寫法，請先讀這段：
//     最原始的錯誤版本是 int age;（不給任何初值）。那樣寫的話，成員 age
//     是不定值，print() 讀它就是未定義行為——結果不可預期，也不可重現。
//     本檔實測過：光是在檔案裡多加幾個類別，印出來的數字就從 1 變成
//     -1809194880，這正好證明「不定值不是固定的垃圾值」。
//     為了讓本檔的輸出可重現、又不要在教材裡執行 UB，這裡改用 C++11 的
//     「類別內預設成員初始化」給一個明顯的哨兵值 -1。
//     這樣 bug 依然看得一清二楚（age 應該是 25，結果還是 -1，代表成員
//     從頭到尾沒被設定過），但行為是完全確定的。
// -----------------------------------------------------------------------------
class BadExample {
private:
    string name;      // 有預設建構函數 → 會被建成空字串（確定行為）
    int age = -1;     // C++11 類別內預設初始化：哨兵值，代表「沒被設定過」
                      // 若寫成 int age;（原始錯誤版），讀取它就是 UB

public:
    // 參數名和成員變數名完全相同 → 函數體內的 name/age 一律指向「參數」
    BadExample(string name, int age) {
        name = name;   // 參數指派給參數自己；成員 name 沒被碰過
        age = age;     // 同上；成員 age 依然維持 -1
    }

    void print() const {
        cout << "  name = [" << name << "], age = " << age << endl;
    }
};

// -----------------------------------------------------------------------------
// 修正 (a)：參數改名 —— 最直白，沒有任何魔法
// -----------------------------------------------------------------------------
class FixedByRename {
private:
    string name;
    int age;

public:
    FixedByRename(const string& inName, int inAge) {
        name = inName;
        age = inAge;
    }
    void print() const {
        cout << "  name = [" << name << "], age = " << age << endl;
    }
};

// -----------------------------------------------------------------------------
// 修正 (b)：成員加前綴 m_ —— 團隊常見風格，一眼看出誰是成員
// -----------------------------------------------------------------------------
class FixedByPrefix {
private:
    string m_name;
    int m_age;

public:
    FixedByPrefix(const string& name, int age) {
        m_name = name;
        m_age = age;
    }
    void print() const {
        cout << "  name = [" << m_name << "], age = " << m_age << endl;
    }
};

// -----------------------------------------------------------------------------
// 修正 (c)：用 this-> 明確指定成員 —— 可行，但遮蔽依然存在
// -----------------------------------------------------------------------------
class FixedByThis {
private:
    string name;
    int age;

public:
    FixedByThis(const string& name, int age) {
        this->name = name;   // 左邊明確是成員，右邊是參數
        this->age = age;
    }
    void print() const {
        cout << "  name = [" << name << "], age = " << age << endl;
    }
};

// -----------------------------------------------------------------------------
// 修正 (d)：初始化列表 —— 最推薦（第 16 課主題）
//   括號外的 name 只在類別作用域查找，必定是成員；括號內才是參數，
//   所以同名完全不衝突，而且是「初始化」而非「先預設建構再賦值」。
// -----------------------------------------------------------------------------
class FixedByInitList {
private:
    string name;
    int age;

public:
    FixedByInitList(const string& name, int age)
        : name(name), age(age) { }

    void print() const {
        cout << "  name = [" << name << "], age = " << age << endl;
    }
};

// -----------------------------------------------------------------------------
// 【日常實務範例】使用者資料匯入：欄位沒設定會造成什麼後果
//   情境：從 CSV 匯入會員資料。若建構函數漏設 age，資料庫寫入的會是不定值；
//         這種 bug 在測試環境常常「剛好是 0」而躲過，上線後才炸。
//   做法：欄位全部走初始化列表，並在建構時就驗證，不合法直接拒絕。
// -----------------------------------------------------------------------------
class MemberRecord {
private:
    string account_;
    int age_;
    bool valid_;

public:
    MemberRecord(const string& account, int age)
        : account_(account), age_(age), valid_(!account.empty() && age > 0 && age < 150) { }

    void print() const {
        cout << "  [" << (valid_ ? "OK  " : "拒絕") << "] "
             << "account=" << account_ << ", age=" << age_ << endl;
    }
};

int main() {
    cout << "=== 錯誤示範：自我賦值，成員完全沒被設定 ===" << endl;
    BadExample b("張三", 25);
    b.print();
    cout << "  ↑ 傳進去的是 (\"張三\", 25)，但 name 是空字串、age 還是哨兵值 -1" << endl;
    cout << "  ↑ name 空字串是確定行為（string 會被預設建構）" << endl;
    cout << "  ↑ age 若改回 int age;（不給初值），讀取它就是 UB，數字不可重現" << endl;

    cout << "\n=== 修正 (a) 參數改名 ===" << endl;
    FixedByRename fa("李四", 30);
    fa.print();

    cout << "\n=== 修正 (b) 成員加 m_ 前綴 ===" << endl;
    FixedByPrefix fb("王五", 35);
    fb.print();

    cout << "\n=== 修正 (c) 使用 this-> ===" << endl;
    FixedByThis fc("趙六", 40);
    fc.print();

    cout << "\n=== 修正 (d) 初始化列表（推薦）===" << endl;
    FixedByInitList fd("孫七", 45);
    fd.print();

    cout << "\n=== 日常實務：會員資料匯入驗證 ===" << endl;
    MemberRecord m1("alice", 28);
    MemberRecord m2("", 28);        // 帳號空白 → 拒絕
    MemberRecord m3("carol", 999);  // 年齡不合理 → 拒絕
    m1.print();
    m2.print();
    m3.print();

    return 0;
}

