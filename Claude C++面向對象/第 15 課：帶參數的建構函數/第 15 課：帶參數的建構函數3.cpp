// ※ 說明（放在預期輸出標記之前）：
//   本檔輸出是完全確定的。BadExample 的 age 使用 C++11 類別內預設初始化
//   （int age = -1;），因此不涉及未定義行為，-1 就是保證會看到的哨兵值。
//   若你自行把它改回 int age;，那一格數字就會變成不定值、無法重現。

// === 預期輸出 ===
// === 錯誤示範：自我賦值，成員完全沒被設定 ===
//   name = [], age = -1
//   ↑ 傳進去的是 ("張三", 25)，但 name 是空字串、age 還是哨兵值 -1
//   ↑ name 空字串是確定行為（string 會被預設建構）
//   ↑ age 若改回 int age;（不給初值），讀取它就是 UB，數字不可重現
//
// === 修正 (a) 參數改名 ===
//   name = [李四], age = 30
//
// === 修正 (b) 成員加 m_ 前綴 ===
//   name = [王五], age = 35
//
// === 修正 (c) 使用 this-> ===
//   name = [趙六], age = 40
//
// === 修正 (d) 初始化列表（推薦）===
//   name = [孫七], age = 45
//
// === 日常實務：會員資料匯入驗證 ===
//   [OK  ] account=alice, age=28
//   [拒絕] account=, age=28
//   [拒絕] account=carol, age=999
