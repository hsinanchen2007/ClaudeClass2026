// =============================================================================
//  第 15 課：帶參數的建構函數 5  —  四種命名風格，如何避開參數／成員同名
// =============================================================================
//
// 【主題資訊 Information】
//   主題：解決建構函數參數遮蔽成員的四種常見命名慣例
//   標準版本：純命名慣例，不涉及語言特性，C++98 起皆適用
//   複雜度：全部 O(1)，四種風格產生的機器碼完全相同
//   標頭檔：<string>
//
// 【詳細解釋 Explanation】
//
// 【0. 為什麼需要「風格」這種東西】
//   本課前面示範過 name = name; 這種自我賦值 bug，根因是參數遮蔽成員。
//   語言本身不會禁止你這樣命名，所以各團隊發展出命名慣例，
//   目的只有一個：**讓「這是成員」和「這是參數」在視覺上一眼可分**。
//   四種風格沒有絕對對錯，重點是「整個專案一致」。以下逐一分析取捨。
//
// 【1. 風格 A：參數加底線前綴（_name）】
//   class X { string name; X(const string& _name) { name = _name; } };
//   優點：成員名保持乾淨；改動最小，看到底線就知道是參數。
//   缺點：**這個風格有貼近標準保留識別字的風險**。C++ 標準保留了
//         「底線開頭接大寫字母」（_Name）與「含有連續兩個底線」（a__b）
//         的識別字給實作使用，全域範圍還額外保留所有底線開頭的名稱。
//         _name（底線接小寫、且是區域參數）在技術上還沒被保留，
//         但很接近紅線，一不小心寫成 _Name 就踩到未定義行為。
//   結論：可以用，但不是最推薦的；許多風格指南直接避開底線開頭。
//
// 【2. 風格 B：成員加 m_ 前綴（m_name）】
//   class X { string m_name; X(const string& name) { m_name = name; } };
//   優點：成員一眼可辨，參數可以用最自然的名字；
//         在長函數中看到 m_ 就知道「這會改到物件狀態」。
//   缺點：名字變長；m_ 是匈牙利命名法的遺緒，有人覺得冗餘。
//   採用者：Qt、Unreal Engine 與大量 C++ 商業程式碼庫。
//
// 【3. 風格 C：成員加底線後綴（name_）】
//   class X { string name_; X(const string& name) { name_ = name; } };
//   優點：與風格 B 同樣清楚，但只多一個字元；底線在**結尾**完全避開了
//         標準保留識別字的規則，沒有風格 A 的疑慮。
//   缺點：底線在字尾比較不顯眼，有人第一眼會漏看。
//   採用者：Google C++ Style Guide 明訂此風格；本課 12.cpp 也採用它。
//
// 【4. 風格 D：直接用 this->（不改名）】
//   class X { string name; X(const string& name) { this->name = name; } };
//   優點：成員與參數都用最自然的名字，介面文件最好讀。
//   缺點：遮蔽依然存在，只是每次都手動繞過；長函數中容易漏寫 this->
//         而誤用參數，且編譯器預設不警告（要 -Wshadow）。
//   結論：可行，但它是「處理症狀」而非「消除原因」。
//
// 【5. 那到底該選哪個】
//   實務上最常見的建議是：**成員加後綴底線（C）或前綴 m_（B），二選一並全專案統一**。
//   理由是它們從「宣告處」就把成員標示出來，任何人讀到 name_ 都知道那是狀態，
//   不需要往回捲去看它是不是被遮蔽了。
//   而不管選哪一種，建構函數本身都建議改用初始化列表（第 16 課）——
//   初始化列表的括號外必定是成員，同名也不衝突，等於從語法層面免疫這個問題。
//
// 【概念補充 Concept Deep Dive】
//
//   ● 標準保留識別字（這是風格 A 唯一真正的技術風險）
//     以下名稱由實作保留，使用者程式不得定義：
//       - 任何以「底線 + 大寫字母」開頭者：_Name、_Foo        （任何範圍）
//       - 任何含有連續兩個底線者：my__var                     （任何範圍）
//       - 任何以底線開頭者：_name、_x                         （限全域範圍）
//     所以區域參數叫 _name 在標準上還算合法，但只要哪天有人把它改成
//     _Name，或搬到全域，就踩線了。避開底線開頭是最省心的做法。
//
//   ● 四種風格的執行期成本
//     完全相同。命名只影響編譯期的名稱查找，成員存取一律是透過 this
//     做固定位移定址。選風格是純粹的可讀性與維護性決策。
//
//   ● 為什麼初始化列表不受遮蔽影響
//     在 : name(name) 中，括號外的 name 只在類別作用域查找 → 必定是成員；
//     括號內的 name 依一般規則查找 → 找到參數。這是標準明文規定的查找規則，
//     所以同名不但不衝突，還是完全合法且推薦的寫法。
//
// 【注意事項 Pay Attention】
//   1. 風格可以爭論，但「同一個專案混用多種風格」一定是壞事。
//   2. 不論選哪種風格，都建議編譯時加上 -Wshadow，讓編譯器幫你守門。
//   3. 本檔四個對照類別刻意都用「函數體內賦值」以突顯命名差異；正式程式
//      請用初始化列表（第 16 課），效率更好也更安全。檔案最後的實務範例
//      則示範了推薦寫法。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】成員命名風格與識別字規則
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 你的專案怎麼區分成員變數和區域變數／參數？為什麼？
//     答：常見做法是成員加 m_ 前綴或加底線後綴（Google 風格）。理由是從宣告處
//         就標示出「這是物件狀態」，讀長函數時不必回頭確認有沒有被遮蔽，
//         也讓建構函數參數可以沿用最自然的名字。
//     追問：那為什麼不乾脆用 this-> 就好？
//         → this-> 只在寫的那一行有效，遮蔽本身還在，漏寫就誤用參數；
//           命名慣例是從源頭消除問題。
//
// 🔥 Q2. 成員變數命名成 _name 有什麼問題？
//     答：底線開頭的名稱在全域範圍是保留給實作的；而「底線 + 大寫字母」
//         （_Name）與「含連續兩底線」（a__b）在任何範圍都保留。
//         _name 當作區域參數技術上還沒違規，但非常貼近紅線，
//         一旦改成 _Name 或搬到全域就會踩到保留識別字。所以多數風格指南避開它。
//     追問：那 name_ 呢？
//         → 底線在結尾完全不在保留規則內，是安全的，這也是 Google 選它的原因。
//
// ⚠️ 陷阱. 建構函數寫成 : name(name) 這樣同名，不會把自己賦值給自己嗎？
//     答：不會。初始化列表中括號外的名字只在類別作用域查找，一定是成員；
//         括號內依一般規則查找，找到的是參數。所以這是正確且推薦的寫法。
//     為什麼會錯：把函數體內 name = name; 的自我賦值經驗直接套到初始化列表，
//         但兩者的名稱查找規則根本不同——函數體是同一個作用域內的普通運算式，
//         初始化列表的「被初始化者」則是專門規定要在類別作用域查找的。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

// -----------------------------------------------------------------------------
// 風格 A：參數加底線前綴
//   成員名乾淨，但底線開頭貼近標準保留識別字的紅線（見上方概念補充）
// -----------------------------------------------------------------------------
class Solution2A {
private:
    string name;
    int age;

public:
    Solution2A(const string& _name, int _age) {
        name = _name;
        age = _age;
    }
    void print() const {
        cout << "  [風格A 參數加底線] name=" << name << ", age=" << age << endl;
    }
};

// -----------------------------------------------------------------------------
// 風格 B：成員加 m_ 前綴（Qt、Unreal 等大型專案常見）
//   成員一眼可辨，參數可用最自然的名字
// -----------------------------------------------------------------------------
class Solution2B {
private:
    string m_name;
    int m_age;

public:
    Solution2B(const string& name, int age) {
        m_name = name;
        m_age = age;
    }
    void print() const {
        cout << "  [風格B 成員m_前綴] name=" << m_name << ", age=" << m_age << endl;
    }
};

// -----------------------------------------------------------------------------
// 風格 C：成員加底線後綴（Google C++ Style Guide）
//   與 B 同樣清楚，且底線在結尾完全避開保留識別字規則
// -----------------------------------------------------------------------------
class Solution2C {
private:
    string name_;
    int age_;

public:
    Solution2C(const string& name, int age) {
        name_ = name;
        age_ = age;
    }
    void print() const {
        cout << "  [風格C 成員底線後綴] name=" << name_ << ", age=" << age_ << endl;
    }
};

// -----------------------------------------------------------------------------
// 風格 D：不改名，靠 this-> 繞過遮蔽
//   介面最自然，但遮蔽仍在，漏寫 this-> 就會誤用參數
// -----------------------------------------------------------------------------
class Solution2D {
private:
    string name;
    int age;

public:
    Solution2D(const string& name, int age) {
        this->name = name;   // 左邊明確是成員
        this->age = age;
    }
    void print() const {
        cout << "  [風格D 使用this->] name=" << name << ", age=" << age << endl;
    }
};

// -----------------------------------------------------------------------------
// 【日常實務範例】日誌設定物件：採 Google 風格（成員底線後綴）+ 初始化列表
//   情境：服務啟動時建立 logger 設定，欄位包含輸出檔名、等級門檻、是否同時
//         輸出到終端。這種設定類別在任何後端服務都看得到。
//   重點：成員一律 xxx_，參數用最自然的名字，並改用初始化列表一步到位——
//         這就是本課建議的最終寫法。
// -----------------------------------------------------------------------------
class LoggerConfig {
private:
    string file_;        // 輸出檔名
    string level_;       // 門檻等級
    bool toConsole_;     // 是否同時輸出到終端

public:
    LoggerConfig(const string& file, const string& level, bool toConsole)
        : file_(file), level_(level), toConsole_(toConsole) { }
    // ↑ 括號外必定是成員、括號內是參數，同名也不衝突

    void print() const {
        cout << "  日誌檔=" << file_
             << ", 等級=" << level_
             << ", 同步輸出終端=" << (toConsole_ ? "是" : "否") << endl;
    }
};

int main() {
    cout << "=== 四種命名風格對照 ===" << endl;
    Solution2A s1("Alice", 25);
    Solution2B s2("Bob", 30);
    Solution2C s3("Charlie", 35);
    Solution2D s4("David", 40);
    s1.print();
    s2.print();
    s3.print();
    s4.print();

    cout << "\n=== 日常實務：服務日誌設定 ===" << endl;
    LoggerConfig prod("/var/log/app.log", "WARN", false);
    LoggerConfig dev("./dev.log", "DEBUG", true);
    prod.print();
    dev.print();

    return 0;
}

