// =============================================================================
//  第 6 課：什麼是面向對象？核心思想介紹1.cpp  —  繼承（Inheritance）的第一個例子
// =============================================================================
//
// 【主題資訊 Information】
//   class Derived : public Base { ... };        // public 繼承（is-a 關係）
//   語法：class 派生類名 : 存取修飾詞 基類名
//   預設值：class 的預設繼承方式是 private；struct 是 public
//           （所以幾乎所有教材都明寫 public，省略會出乎意料）
//   標準版本：C++98 起
//   記憶體：派生類物件內含一份完整的基類子物件（base subobject）
//   標頭檔：語言核心特性，不需要標頭檔
//
// 【詳細解釋 Explanation】
//
// 【1. 繼承到底「繼承」了什麼】
//   Dog 繼承 Animal 之後，每一個 Dog 物件裡面都【實際包含】
//   一個完整的 Animal 子物件 —— 這不是比喻，而是記憶體佈局上的事實。
//   所以 d.name 存取的是那個 Animal 子物件裡的 name，
//   d.eat() 呼叫的是 Animal 定義的成員函式，而 this 指標
//   會被自動調整成指向那個子物件。
//   繼承下來的包含：所有非 private 的資料成員與成員函式。
//   【不會】繼承的有三樣：建構子、解構子、以及 operator=
//   （C++11 起可以用 using Base::Base; 顯式繼承建構子）。
//
// 【2. public 繼承代表的是「is-a」而不是「有這些功能」】
//   public 繼承在設計上是一個【強烈的承諾】：
//   任何需要 Animal 的地方，都必須能安全地放進一個 Dog
//   —— 這就是 Liskov 替換原則（LSP）。
//   常見的誤用是把繼承當成「省打字的程式碼重用」：
//       class Stack : public std::vector<int>   // 危險
//   Stack 並不是一種 vector（不該支援隨機插入），
//   這種情況應該用【組合】（把 vector 當成員）而不是繼承。
//   判準很簡單：問「B 真的可以在任何用到 A 的地方頂替 A 嗎？」
//
// 【3. 為什麼 name 放在 public 是個壞示範】
//   本檔為了語法簡潔把 name 設為 public，
//   於是 d.name = "旺財"; 可以直接寫。
//   但這正是第 20 課（封裝）要修掉的問題：
//   任何人都能把 name 改成空字串或亂碼，類別無法維護自己的不變量。
//   實務上應該是 private 成員 + 建構子設定 + getter，
//   本檔請只當成「繼承語法」的示範，不要當成類別設計的範本。
//
// 【4. 這裡還沒有多型】
//   本檔的 eat() 不是 virtual，Dog 與 Cat 也沒有覆寫它。
//   所有呼叫都在【編譯期】就決定好了（靜態繫結）。
//   要做到「用基類指標呼叫、實際執行派生類版本」，
//   必須把函式宣告成 virtual —— 那是同課 2 號檔的主題。
//   繼承與多型是兩件事：繼承是【結構】的重用，多型是【行為】的分派。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 物件的記憶體佈局
//   單一繼承且沒有虛擬函式時，佈局是「基類成員在前，派生類成員在後」：
//       Dog 物件 = [ Animal::name ][ Dog 自己的成員（本檔沒有）]
//   因為基類子物件放在最前面（offset 0），
//   把 Dog* 轉成 Animal* 通常不需要調整指標值 —— 這讓向上轉型幾乎零成本。
//   （多重繼承或有虛擬基類時就會需要調整偏移量，成本不再為零。）
//   本檔的 Dog 沒有新增資料成員，
//   所以 sizeof(Dog) == sizeof(Animal) == sizeof(std::string)
//   （本機 x86-64 libstdc++ 實測為 32 bytes，此值屬實作定義）。
//
// (B) 成員函式不會佔用物件空間
//   eat()、bark() 這些非虛擬成員函式，編譯後就是普通函式，
//   只是多接收一個隱藏的 this 參數。
//   它們存在於程式碼區段，不會讓每個物件變大 ——
//   一百萬個 Dog 物件也只有一份 bark() 的機器碼。
//   這也是為什麼「加成員函式不影響 sizeof」。
//
// (C) private 成員也被繼承了，只是不能存取
//   常見誤解是「private 成員不會被繼承」。
//   實際上派生類物件裡【確實包含】那些 private 成員（佔用空間、
//   會被基類的成員函式使用），只是派生類的程式碼不能直接存取它們。
//   「繼承」與「存取權限」是兩個獨立的概念。
//
// 【注意事項 Pay Attention】
//   1. class 的預設繼承方式是 private，一定要明寫 public。
//      漏寫會讓所有繼承來的成員在外部都不可存取，錯誤訊息還很難懂。
//   2. 建構子、解構子、operator= 【不會】被繼承。
//   3. 本檔把 name 設為 public 只是為了語法簡潔，
//      這在真實設計中是要避免的（見第 20 課封裝）。
//   4. 本檔沒有 virtual，所以還沒有多型 —— 全部是編譯期靜態繫結。
//   5. 只要基類可能被當成多型基底使用，就必須給它 virtual 解構子；
//      本檔沒有用指標刪除物件，所以還不會出問題（見同課 2 號檔）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】繼承的本質
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. class Dog : Animal 和 class Dog : public Animal 有什麼差別？
//     答：差別很大。class 的預設繼承方式是 【private】，
//         所以第一種寫法中，Animal 的所有 public 成員在 Dog 外部都變成不可存取
//         —— d.eat() 會編譯失敗。
//         而且 private 繼承表達的是「用 Animal 來實作 Dog」（has-a），
//         不是 is-a，無法把 Dog* 隱式轉成 Animal*。
//         （struct 的預設繼承則是 public，這也是兩者少數的實質差異之一。）
//     追問：那什麼時候該用 private 繼承？
//           → 幾乎總是可以改用組合（把基類物件當成員）而且更清楚。
//             少數例外是需要覆寫基類的 virtual 函式、
//             或利用 empty base optimization 節省空間時。
//
// 🔥 Q2. private 成員會被繼承嗎？
//     答：會。派生類物件裡【確實包含】基類的 private 成員 ——
//         它們佔用空間、也會被基類自己的成員函式正常使用。
//         只是派生類的程式碼不能直接存取它們。
//         「有沒有被繼承（是否存在於物件中）」與
//         「能不能存取（存取控制）」是兩個獨立的概念。
//     追問：那 protected 呢？
//           → 派生類可以存取，但外部不行。
//             不過 protected 資料成員通常是設計氣味 ——
//             它讓所有派生類都與基類的內部表示綁死，
//             等於把封裝的破口開給了整個繼承體系。
//
// ⚠️ 陷阱. 「Stack 需要用到 vector 的功能，所以讓 Stack 繼承 vector
//          可以少寫很多程式碼」—— 這個推理錯在哪？
//     答：錯在把 public 繼承當成程式碼重用的工具。
//         public 繼承代表 is-a：承諾任何用到 vector 的地方都能安全地
//         換成 Stack。但 Stack 一旦繼承 vector，
//         使用者就能呼叫 insert()、operator[]、clear()——
//         隨意在中間插入元素，Stack「後進先出」的不變量當場被破壞。
//         正確做法是【組合】：把 vector 當成 private 成員，
//         只對外暴露 push/pop/top。
//     為什麼會錯：把繼承理解成「取得那些功能」，
//         而繼承真正的意義是「承諾可以被當成基類使用」。
//         判準不是「我需不需要這些功能」，
//         而是「我願不願意接受基類的【全部】介面成為我的介面」。
//         需要功能就用組合，需要替換性才用繼承。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

// 基類（父類）
class Animal {
public:
    string name;

    void eat() {
        cout << name << " 正在吃東西" << endl;
    }
};

// 派生類（子類）—— 繼承了 Animal 的一切
class Dog : public Animal {
public:
    void bark() {
        cout << name << " 汪汪叫！" << endl;
    }
};

class Cat : public Animal {
public:
    void meow() {
        cout << name << " 喵喵叫！" << endl;
    }
};

int main() {
    Dog d;
    d.name = "旺財";
    d.eat();   // 繼承自 Animal
    d.bark();  // Dog 自己的行為

    Cat c;
    c.name = "咪咪";
    c.eat();   // 繼承自 Animal
    c.meow();  // Cat 自己的行為

    return 0;
}

