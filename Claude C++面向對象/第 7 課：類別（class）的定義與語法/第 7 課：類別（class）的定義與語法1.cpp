//      不需要任何參數 —— 因為編譯器已經幫它們加上隱藏的 this 指標。
//
// 註 3:本機實測 sizeof(Dog) = 72（x86-64 / g++ 15.2.0 / libstdc++，
//      屬實作定義）：string 32 + int 4 + 【填充 4】 + string 32。
//      那 4 bytes 的填充是為了讓 breed 落在 8 bytes 對齊的位置上。
