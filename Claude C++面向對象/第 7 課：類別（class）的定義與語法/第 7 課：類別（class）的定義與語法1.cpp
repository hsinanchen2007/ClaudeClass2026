//  第 7 課：類別（class）的定義與語法1.cpp  —  類別的基本語法與第一個物件
// =============================================================================
//
// 【主題資訊 Information】
//   class 類別名稱 {
//   存取修飾詞:
//       成員變數;          // 描述「有什麼」（狀態）
//       成員函式() { }     // 描述「能做什麼」（行為）
//   };                     // ← 這個分號是語法的一部分，不可省略
//
//   預設存取：class 是 private；struct 是 public（兩者【唯一】的語言差異）
//   標準版本：C++98 起
//   物件大小：所有非靜態資料成員的大小總和 + 對齊填充
//   標頭檔  ：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. 類別是「型別」，物件才是「東西」】
//   class Dog { ... }; 這一整段【不會產生任何物件、不佔用任何記憶體】，
//   它只是告訴編譯器「Dog 這個型別長什麼樣子、佔多大、有哪些操作」。
//   真正產生東西的是 Dog myDog; 這一行 ——
//   此時才在堆疊上配置一塊足夠大的記憶體。
//   常見的類比是「設計圖 vs 房子」：
//   設計圖畫一百次不會蓋出房子，但每蓋一棟房子都依循同一份設計圖。
//
// 【2. 成員函式為什麼可以直接用 name 而不必傳參數】
//   bark() 裡直接寫 name，看起來像存取全域變數，其實不是。
//   編譯器會把成員函式改寫成一個隱藏多接收一個參數的普通函式：
//       void bark(Dog* this) { cout << this->name << " 說：汪汪！"; }
//   而 myDog.bark() 被改寫成 bark(&myDog)。
//   所以 name 實際上是 this->name —— 每個物件用的是自己那一份資料。
//   這也解釋了為什麼成員函式不會讓物件變大：
//   它就是一個普通函式，只有一份機器碼，存在程式碼區段裡。
//
// 【3. 那個分號為什麼不能省】
//   class Dog { ... };  結尾的分號常被漏掉，而錯誤訊息通常出現在
//   【下一行】甚至下一個函式，非常難懂。
//   原因是 C++ 繼承自 C 的語法：類別定義本身是一個「宣告」，
//   而宣告可以順便定義變數：
//       class Dog { ... } myDog, yourDog;    // 合法！定義類別同時建立兩個物件
//   編譯器讀完 } 之後還在等待可能出現的變數名稱，
//   所以必須有分號才知道宣告結束。
//   —— 這也是為什麼漏掉分號時錯誤訊息會指向下一行。
//
// 【4. 本檔把所有成員都放 public，這是刻意的教學順序】
//   myDog.name = "旺財"; 之所以能寫，是因為 name 是 public。
//   這在真實設計中應該避免 —— 任何人都能把 age 設成 -5，
//   類別無法維護自己的合理性。
//   本課先建立「類別＝資料＋行為」的直覺，
//   存取控制與封裝是第 20 課的主題。
//   讀本檔時請把重點放在語法結構，而不是設計品質。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 物件的大小怎麼算
//   本機實測 sizeof(Dog) = 72（x86-64 / libstdc++，屬實作定義）：
//       string name  32 bytes
//       int    age    4 bytes（+ 4 bytes 對齊填充）
//       string breed 32 bytes
//   注意 age 後面有 4 bytes 的【填充（padding）】，
//   因為 std::string 需要 8 bytes 對齊，而 32+4=36 不是 8 的倍數。
//   把 age 移到最後面可以省下這 4 bytes ——
//   成員宣告順序會影響物件大小，這在大量物件時是實際的考量。
//
// (B) class 與 struct 真的只差預設存取權限
//   在 C++ 中 struct 也可以有成員函式、建構子、繼承、甚至虛擬函式。
//   兩者【唯一】的語言差異是預設存取權（class=private、struct=public）
//   與預設繼承方式。
//   慣例上：純資料聚合用 struct，有不變量要維護的用 class ——
//   這是溝通意圖，不是語法要求。
//
// (C) 沒有寫任何建構子時會發生什麼
//   編譯器會隱式產生一個預設建構子。
//   對 Dog 而言，它會呼叫兩個 string 的預設建構子（產生空字串），
//   但【不會】初始化 age —— int 是內建型別，
//   預設初始化就是「不做任何事」，值不確定。
//   本檔在使用前就設定了 myDog.age = 3，所以沒有問題；
//   但若先讀取再賦值，那就是未定義行為。
//   解法是類內初始化：int age = 0;（C++11 起，見同課 summary）。
//
// 【注意事項 Pay Attention】
//   1. 類別定義結尾的分號不可省略；漏掉時錯誤訊息會指向下一行，很難懂。
//   2. 類別本身不佔記憶體，物件才佔。
//   3. 成員函式不會增加物件大小 —— 它只是多一個隱藏的 this 參數。
//   4. 內建型別成員（如 int age）若沒有初始化就讀取是未定義行為；
//      本檔在讀取前都先賦值了，所以安全。
//   5. 成員的【宣告順序】會影響物件大小（對齊填充），也決定初始化順序。
//   6. 本檔全部使用 public 只是教學順序上的簡化，不是設計範本。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】類別的基本語法
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. class 和 struct 在 C++ 中有什麼差別？
//     答：語言層面【只有兩個】差別：預設成員存取權（class 是 private、
//         struct 是 public）與預設繼承方式（同樣是 private / public）。
//         其餘完全相同 —— struct 一樣可以有成員函式、建構子、
//         繼承、虛擬函式、樣板。
//         慣例上用 struct 表示「純資料聚合」、
//         用 class 表示「有不變量要維護」，這是溝通意圖而非語法限制。
//     追問：那 C 的 struct 和 C++ 的 struct 一樣嗎？
//           → 不一樣。C 的 struct 只能有資料成員。
//             C++ 的 struct 是完整的類別，只是預設 public。
//
// 🔥 Q2. 成員函式裡直接寫 name，編譯器實際上做了什麼？
//     答：編譯器把成員函式改寫成多一個隱藏參數 this 的普通函式，
//         而 name 被解析成 this->name。
//         myDog.bark() 實際上是 bark(&myDog)。
//         所以每個物件存取的都是自己那一份資料，
//         而成員函式的機器碼只有一份、存在程式碼區段，
//         【不會】讓物件變大。
//     追問：那 static 成員函式呢？
//           → 沒有 this 參數，因此不能存取非靜態成員，
//             也不能宣告為 const 或 virtual。它本質上就是一個
//             被放進類別命名空間的自由函式。
//
// ⚠️ 陷阱. sizeof(Dog) 應該是 32 + 4 + 32 = 68 才對，為什麼實測是 72？
//     答：因為【對齊填充（padding）】。std::string 需要 8 bytes 對齊，
//         而 name(32) + age(4) = 36 不是 8 的倍數，
//         編譯器會在 age 後面插入 4 bytes 的填充，
//         讓 breed 從 40 這個 8 的倍數位置開始。
//         最終 32 + 4 + 4(填充) + 32 = 72。
//     為什麼會錯：以為結構體大小就是成員大小的總和。
//         實際上還要考慮每個成員的對齊需求，以及整體大小必須是
//         最大對齊值的倍數。
//         實用的推論是：把成員【由大到小】排列通常能減少填充 ——
//         本檔若改成 name, breed, age 的順序，
//         就變成 32+32+4 = 68，補到 8 的倍數是 72……
//         這個例子剛好一樣，但在成員更多時差異會很明顯。
//         （所有數值皆為本機 x86-64 實測，屬實作定義。）
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

// ========== 類別定義 ==========
class Dog {
public:
    // 成員變數（Member Variables）—— 描述「狗有什麼」
    string name;
    int age;
    string breed;  // 品種

    // 成員函數（Member Functions）—— 描述「狗能做什麼」
    void bark() {
        cout << name << " 說：汪汪！" << endl;
    }

    void sit() {
        cout << name << " 乖乖坐下了" << endl;
    }

    void showInfo() {
        cout << "名字: " << name << endl;
        cout << "年齡: " << age << " 歲" << endl;
        cout << "品種: " << breed << endl;
    }
};  // ← 別忘了分號！

// ========== 主程式 ==========
int main() {
    Dog myDog;              // 根據 Dog 類別建造一個對象
    myDog.name = "旺財";    // 設定屬性
    myDog.age = 3;
    myDog.breed = "柴犬";

    myDog.showInfo();       // 調用成員函數
    myDog.bark();
    myDog.sit();

    return 0;
}

