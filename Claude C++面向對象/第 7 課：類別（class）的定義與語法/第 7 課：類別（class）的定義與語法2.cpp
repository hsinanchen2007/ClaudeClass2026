// =============================================================================
//  第 7 課：類別（class）的定義與語法2.cpp  —  每個物件擁有自己的一份成員變數
// =============================================================================
//
// 【主題資訊 Information】
//   非靜態資料成員：每個物件各有一份獨立的副本
//   非靜態成員函式：全類別共用一份程式碼，靠隱藏的 this 參數區分物件
//   語法：物件.成員     或     指標->成員
//   標準版本：C++98 起
//   複雜度：成員存取 O(1)（編譯期就算好偏移量，執行期只是一次位址加法）
//   標頭檔：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. 本檔要證明的一件事：p1 與 p2 完全獨立】
//   兩個 Person 物件各自擁有一份 name 與 age，
//   修改 p1.name 完全不會影響 p2。
//   但 introduce() 的程式碼【只有一份】——
//   兩次呼叫執行的是同一段機器碼，只是 this 指向不同物件。
//   這就是 OOP 最基本的資源分配方式：
//       資料 → 每個物件一份（狀態是個別的）
//       程式碼 → 全類別一份（行為是共通的）
//
// 【2. this 指標讓「同一段程式碼」產生不同輸出】
//   p1.introduce() 與 p2.introduce() 呼叫的是同一個函式位址，
//   差別只在傳入的 this：
//       introduce(&p1)  → this->name 是 "小明"
//       introduce(&p2)  → this->name 是 "小華"
//   成員存取在編譯期就被轉換成「this + 固定偏移量」，
//   例如 name 在偏移 0、age 在偏移 32。
//   所以成員存取的成本和存取一般區域變數幾乎相同，沒有查表或搜尋。
//
// 【3. 什麼時候資料會被「共用」】
//   如果希望所有物件共用同一份資料，要用 static 成員：
//       class Person {
//       public:
//           static int count;      // 全類別只有一份
//       };
//       int Person::count = 0;     // 必須在類別外定義（C++17 起可用 inline 省略）
//   static 成員不屬於任何物件、不佔用物件空間，
//   而是有靜態儲存期的變數，生命週期橫跨整個程式。
//   典型用途是計數已建立的物件數量。
//
// 【4. 為什麼 introduce() 應該宣告為 const】
//   introduce() 只讀取成員、不修改任何東西，
//   所以更好的寫法是：
//       void introduce() const { ... }
//   加上 const 有三個好處：
//     ① 編譯器會確保這個函式不會意外修改成員
//     ② const Person 物件才能呼叫它（否則會編譯失敗）
//     ③ 對讀者而言是明確的意圖宣告
//   本檔沒有加是教學上的簡化 ——
//   實務規則是【任何不修改狀態的成員函式都該加 const】。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 物件在記憶體中的樣子
//   本機實測 sizeof(Person) = 40（x86-64 / libstdc++，實作定義）：
//       string name  偏移 0，佔 32 bytes
//       int    age   偏移 32，佔 4 bytes
//       （尾端 4 bytes 填充，讓整體大小是 8 的倍數）
//   p1 與 p2 是兩塊各 40 bytes、互不重疊的記憶體。
//
// (B) 成員函式的呼叫成本
//   非虛擬成員函式的呼叫和普通函式一樣，
//   而且因為定義寫在類別內部（隱式 inline），
//   編譯器通常會直接展開，連函式呼叫的開銷都沒有。
//   注意「類別內定義隱式 inline」指的是【連結性質】
//   （允許多個翻譯單元有相同定義），
//   實際上會不會展開仍由編譯器決定。
//
// (C) 為什麼 using namespace std; 在標頭檔中是壞習慣
//   本檔為了簡潔寫了 using namespace std;。
//   在 .cpp 的教學檔中影響有限，但【絕對不要】寫在標頭檔裡 ——
//   它會把整個 std 命名空間倒進每一個包含該標頭的檔案，
//   造成難以預料的名稱衝突（例如自己的 count、distance、
//   left、size 等常見名稱都會與 std 的版本撞名）。
//
// 【注意事項 Pay Attention】
//   1. 每個物件有自己的非靜態資料成員；成員函式的程式碼則是共用的。
//   2. 不修改狀態的成員函式應加 const（本檔為簡化而省略）。
//   3. 要共用資料請用 static 成員 —— 它不佔用物件空間。
//   4. 本檔的 age 沒有類內初始值，若在賦值前讀取就是未定義行為；
//      本檔都先賦值才使用，所以安全。
//   5. using namespace std; 在教學小檔可接受，但不可寫進標頭檔。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】物件的獨立性與 this
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. p1.introduce() 和 p2.introduce() 執行的是同一段程式碼嗎？
//        那為什麼輸出不同？
//     答：是同一段程式碼、同一個函式位址 —— 成員函式在整個程式中只有一份。
//         輸出不同是因為隱藏的 this 參數不同：
//         p1.introduce() 實際上是 introduce(&p1)，
//         函式內的 name 被解析成 this->name。
//         【資料每個物件一份，程式碼全類別共用】——
//         這是 OOP 記憶體模型最核心的一句話。
//     追問：那 sizeof(Person) 會因為多了成員函式而變大嗎？
//           → 不會。非虛擬成員函式不佔物件空間。
//             只有加入【虛擬】函式時，物件才會多一個 vptr（本機 8 bytes）。
//
// 🔥 Q2. 如果希望「所有 Person 物件共用一個計數器」，該怎麼做？
//     答：宣告為 static 資料成員。它不屬於任何物件、不佔用物件空間，
//         而是具有靜態儲存期、全類別只有一份：
//             class Person { public: static int count; };
//             int Person::count = 0;      // 類別外定義（C++17 起可用 inline 省略）
//         可以透過 Person::count 或任何物件存取，指的都是同一份。
//     追問：static 成員函式和一般成員函式差在哪？
//           → static 成員函式【沒有 this】，
//             因此不能存取非靜態成員，也不能宣告為 const 或 virtual。
//             它本質上是被放進類別命名空間的自由函式。
//
// ⚠️ 陷阱. 「introduce() 沒有參數，卻能用到 name 和 age，
//          所以 name 應該是某種全域或靜態的東西」—— 錯在哪？
//     答：錯在忽略了隱藏的 this 參數。name 【不是】全域的，
//         它是 this->name 的簡寫，而 this 指向【當次呼叫的那個物件】。
//         如果 name 真的是全域或靜態的，
//         p1 和 p2 就會共用同一份資料，
//         本檔的輸出將會是兩行一模一樣的「小華」——
//         但實測輸出是「小明」與「小華」，正好反證了這個誤解。
//     為什麼會錯：因為函式簽名上看不到 this，
//         直覺會把「沒有出現在參數列裡的名稱」歸類為外部變數。
//         實際上成員函式的完整簽名多了一個編譯器自動加上的參數，
//         這也是為什麼 static 成員函式（沒有 this）
//         就【真的】不能存取 name —— 兩者一比較，機制就很清楚了。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class Person {
public:
    string name;
    int age;

    void introduce() {
        // 直接使用 name 和 age，不需要傳參數！
        cout << "你好，我叫 " << name << "，今年 " << age << " 歲。" << endl;
    }
};

int main() {
    Person p1;
    p1.name = "小明";
    p1.age = 20;
    p1.introduce();  // 輸出：你好，我叫 小明，今年 20 歲。

    Person p2;
    p2.name = "小華";
    p2.age = 22;
    p2.introduce();  // 輸出：你好，我叫 小華，今年 22 歲。

    return 0;
}

