// 註 1:本檔輸出是【完全確定的】—— 沒有輸入、亂數、執行緒或位址。
//
// 註 2:兩行輸出來自【同一段】introduce() 程式碼，
//      差別只在隱藏的 this 指標指向 p1 還是 p2。
//      這正是「資料每個物件一份、程式碼全類別共用」的直接證據 ——
//      如果 name 是全域或 static 的，兩行就會印出一樣的內容。
//
// 註 3:本機實測 sizeof(Person) = 40（x86-64 / g++ 15.2.0，實作定義）：
//      string 32 + int 4 + 尾端填充 4。p1 與 p2 是兩塊互不重疊的記憶體。
//
// 【LeetCode 實戰範例】從缺 —— 理由：
//      本檔的重點是「物件各自持有狀態」這個最基礎的觀念，
//      程式碼刻意壓到最小。可選清單中的設計題都需要容器或演算法，
//      加進來會讓這個極簡對照失去意義。

// === 預期輸出 ===
// 你好，我叫 小明，今年 20 歲。
// 你好，我叫 小華，今年 22 歲。
