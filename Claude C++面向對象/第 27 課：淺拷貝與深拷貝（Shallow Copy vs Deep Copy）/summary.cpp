// =============================================================================
//  第 27 課 總結  —  淺拷貝與深拷貝（Shallow Copy vs Deep Copy）
// =============================================================================
//
// 【主題資訊 Information】
//   淺拷貝（Shallow Copy）： 逐成員複製；對指標成員只複製「位址值」
//                            → 兩個物件共享同一塊記憶體
//   深拷貝（Deep Copy）：    配置新記憶體 + 複製「內容」
//                            → 兩個物件各自獨立
//   要達成深拷貝需自訂     ： 拷貝建構函數 + 拷貝賦值運算子（+ 解構函數）＝三法則
//   標準版本   ： C++98（三法則）；C++11 後擴充為五法則（見第 32~34 課）
//   標頭檔     ： <cstring>（strlen / strcpy）
//   複雜度     ： 淺拷貝 O(1)（只複製指標）；深拷貝 O(n)（配置 + 複製）
//
// 【詳細解釋 Explanation】
//
// 【1. 淺拷貝的兩大災難】
//   一個持有裸指標 char* m_data 的類別，若不自訂拷貝，
//   編譯器生成的版本是「逐成員拷貝」，對指標而言就是複製位址值：
//       ShallowString(const ShallowString& o) : m_data(o.m_data), m_len(o.m_len) {}
//   於是兩個物件的 m_data 指向同一塊記憶體，後果有二：
//       * 問題 1（共享修改）：改一個會連帶改到另一個。
//       * 問題 2（double free）：兩者解構時各 delete[] 一次同一塊記憶體，
//         對已釋放的記憶體再次釋放是未定義行為。
//   ★ 注意：double free 的結果沒有固定樣貌 —— 它「常常」讓程式崩潰，
//     但標準並不保證一定崩潰，也可能默默寫壞堆積、之後才在別處爆掉。
//     不能把它描述成「一定 crash」。正因如此，本檔的 ShallowString
//     只用「文字說明」示範問題，不真的觸發它（見示範 1）。
//
// 【2. 深拷貝如何解決】
//   拷貝建構函數與拷貝賦值運算子都改成「配置新記憶體 + 複製內容」，
//   讓每個物件擁有自己的一份，互不干擾，也能各自安全解構。
//   本檔的 DeepString 就是完整示範。
//
// 【3. 拷貝賦值運算子的六個步驟】
//   它面對的是「已初始化過」的物件，比拷貝建構多了處理舊資源這件事：
//       ① 自我賦值檢查 if (this == &other)
//       ② 釋放舊資源  delete[] m_data
//       ③ 複製大小    m_len = other.m_len
//       ④ 配置新記憶體 m_data = new char[m_len + 1]
//       ⑤ 複製資料    strcpy
//       ⑥ 回傳 *this  支援鏈式賦值 a = b = c
//   ① 為什麼不能省：s1 = s1 時 other 就是自己，
//   ② 會先把資料 delete 掉，⑤ 再從「已釋放的自己」複製 → 未定義行為。
//   （這與第 26 課 -10 的 this == &other 身分比較是同一個 idiom。）
//
// 【4. 本檔的順序與「例外安全」的取捨】
//   本檔的 operator= 採「先 delete 再 new」的教科書順序，好讀，
//   但若 ④ 的 new 拋 bad_alloc，會留下懸空指標（舊的已刪、新的沒有）。
//   更安全的是「先配置成功再釋放舊的」（強例外保證），
//   最徹底的是 copy-and-swap（見第 33 課 -2）。
//   本檔保留簡單順序以聚焦淺／深拷貝的對比，但讀者應知道這個差別。
//
// 【概念補充 Concept Deep Dive】
//
// (A) ★ 本檔為什麼不印記憶體位址
//   原始版本用 static_cast<void*>(m_data) 印位址來證明「兩塊記憶體不同」。
//   但這在教材裡不可行，原因有二：
//     1) 位址受 ASLR 影響，每次執行都不同（本機實測連跑會得到不同位址），
//        無法寫成可對照的預期輸出。
//     2) 位址會「被重複使用」：原始輸出中，示範 3、4、5 一再出現同一個
//        位址 0x...030，那是因為前一個區塊的物件解構、釋放了記憶體，
//        配置器又把「同一個位址」撥給下一個新物件。
//        於是「位址相同」根本不代表同一個活著的物件 ——
//        位址完全不適合當作獨立性的證據。
//   本檔改用兩種可重現的證據：
//     * 「改一個、印兩個」：改 s2 首字元，s1 不受影響（各自獨立）。
//     * 布林比較 sharesBufferWith()：只暴露「是不是同一塊記憶體」，
//       不洩漏會變動的位址值。
//     * 另配一個「buf#N」緩衝區編號，每次真正配置記憶體時遞增，
//       可讀又可重現。
//
// (B) 為什麼配置要 m_len + 1
//   C 字串以 '\0' 結尾，strlen 回傳的長度「不含」結尾的 '\0'。
//   new char[m_len + 1] 才裝得下那個結尾字元，
//   少算這個 1 是最經典的差一錯誤，會造成一位元組的堆積溢位。
//
// (C) 深拷貝、三法則與後續的五法則
//   DeepString 寫了三法則（解構、拷貝建構、拷貝賦值），但沒有移動操作，
//   而且因為宣告了解構函數，編譯器也不會自動生成移動（見第 32 課 -3）。
//   所以它的所有「移動」都會退回深拷貝。
//   當來源是暫時物件或明確 std::move 時，這是浪費 —— 補上移動建構與
//   移動賦值就成了五法則（第 34 課）。而更根本的做法是 Rule of Zero：
//   把 char* 換成 std::string，三／五法則一個都不用寫。
//
// 【注意事項 Pay Attention】
//   1. 類別有裸指標成員 → 編譯器預設淺拷貝 → 共享修改 + double free。
//   2. double free 是未定義行為，「常」崩潰但不保證，不可描述成「一定 crash」。
//   3. 深拷貝需自訂拷貝建構 + 拷貝賦值（+ 解構）＝三法則。
//   4. 拷貝賦值要有自我賦值檢查，並記得先釋放舊資源、回傳 *this。
//   5. 教材不要印原始位址（ASLR 每次不同，且位址會被重用而誤導）——
//      用「改一個印兩個」或指標布林比較證明獨立性（見【概念補充 A】）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】淺拷貝與深拷貝
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 淺拷貝會造成哪兩個問題？根因是什麼？
//     答：根因是「複製了指標值，兩個物件因此指向同一塊記憶體」。
//         問題 1 是共享修改：改一個會連帶改另一個。
//         問題 2 是 double free：兩者解構時各 delete[] 一次同一塊記憶體，
//         對已釋放的記憶體再次釋放是未定義行為。
//         解法是深拷貝：配置新記憶體、複製內容，讓兩者各自獨立。
//     追問：double free 一定會讓程式當掉嗎？
//         → 不一定。它是未定義行為，常見結果是崩潰，
//           但也可能默默破壞堆積、之後才在別處出錯。
//           不能假設它一定崩潰，也不能假設它一定安全。
//
// 🔥 Q2. 拷貝賦值運算子和拷貝建構函數，實作上多了哪些步驟？為什麼？
//     答：拷貝賦值面對「已經初始化過」的物件，多了兩件事：
//         自我賦值檢查，以及「先釋放自己原有的資源」。
//         拷貝建構的物件還是空的、沒有舊資源，所以都不需要。
//         另外拷貝賦值要回傳 *this 以支援鏈式賦值，拷貝建構沒有回傳型別。
//     追問：自我賦值檢查為什麼不能省？
//         → 若 s1 = s1，沒有檢查時會先 delete 自己的資料，
//           再從「已釋放的自己」複製 → 未定義行為。
//           （採「先配置後釋放」的寫法則可容忍缺檢查，只是多做白工。）
//
// ⚠️ 陷阱. 「印出兩個物件的指標位址，位址不同就證明深拷貝成功、各自獨立。」
//     答：位址不同雖常見於深拷貝，但不是可靠證據，也不該寫進教材。
//         位址受 ASLR 影響每次執行都不同（無法對照）；
//         更關鍵的是位址會被重複使用 ——
//         一個物件解構釋放記憶體後，配置器可能把「同一個位址」
//         再撥給下一個新物件。於是你會看到兩個先後存在的物件位址相同，
//         但它們根本不是同一個。
//     為什麼會錯：把「位址」當成物件的穩定身分。
//         位址只在物件「還活著」的期間代表它；一旦釋放就可被重用。
//         要證明獨立性，該用「改一個不影響另一個」或指標的布林相等比較。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【LeetCode 實戰範例】—— 本檔從缺，理由如下
//   淺／深拷貝是資源管理型類別的正確性議題，屬於語言機制與工程實務。
//   LeetCode 判的是演算法的輸入輸出與時間限制，
//   評測系統只會建立一個實例並呼叫指定方法，
//   既不會拷貝你的物件，也不會做自我賦值來檢驗防護。
//   （名字相近的 138. Copy List with Random Pointer 考的是
//     「圖的深層複製演算法」，與 C++ 拷貝建構函數的語言機制是兩回事。）
//   硬掛題號只會誤導，故從缺 ——
//   本檔以「淺拷貝的問題說明 + 深拷貝的五個示範」完整覆蓋主題。
//
