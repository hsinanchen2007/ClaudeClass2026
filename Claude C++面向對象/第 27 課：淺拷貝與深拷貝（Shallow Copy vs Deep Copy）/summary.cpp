#include <iostream>
#include <cstring>

namespace {
// 緩衝區編號產生器：每次「真正配置一塊新記憶體」時遞增。
// 用它取代原始位址，因為位址受 ASLR 影響且會被重用（見檔頭【概念補充 A】）。
int g_nextBufId = 0;
}  // namespace

// ============================================================
// 淺拷貝問題示範（刻意「不」實際觸發，因為 double free 是未定義行為）
// ============================================================
class ShallowString {
    std::size_t m_len;
    char* m_data;
public:
    ShallowString(const char* str = "") {
        m_len = std::strlen(str);
        m_data = new char[m_len + 1];
        std::strcpy(m_data, str);
    }

    ~ShallowString() { delete[] m_data; }

    // ⚠️ 沒有自訂拷貝建構 → 編譯器生成淺拷貝版本：
    //   ShallowString(const ShallowString& other)
    //       : m_len(other.m_len), m_data(other.m_data) {}
    //   ↑ 只複製指標值，不配置新記憶體！兩個物件共享同一塊記憶體。

    const char* c_str() const { return m_data; }
    void setFirstChar(char ch) { if (m_len > 0) m_data[0] = ch; }
};

// ============================================================
// 深拷貝解決方案
// ============================================================
class DeepString {
    std::size_t m_len;
    char* m_data;
    int m_bufId;
public:
    // 一般建構函數
    DeepString(const char* str = "") {
        m_len = std::strlen(str);
        m_data = new char[m_len + 1];
        m_bufId = ++g_nextBufId;
        std::strcpy(m_data, str);
        std::cout << "  [建構] \"" << m_data << "\" 配置 buf#" << m_bufId << "\n";
    }

    // ★ 拷貝建構函數（深拷貝）
    DeepString(const DeepString& other) {
        m_len = other.m_len;
        m_data = new char[m_len + 1];          // 配置新的記憶體
        m_bufId = ++g_nextBufId;               // 新的一塊 → 新編號
        std::strcpy(m_data, other.m_data);     // 複製資料內容
        std::cout << "  [深拷貝建構] \"" << m_data
                  << "\" 從 buf#" << other.m_bufId
                  << " 複製到「新的」buf#" << m_bufId << "\n";
    }

    // ★ 拷貝賦值運算子（深拷貝）
    DeepString& operator=(const DeepString& other) {
        std::cout << "  [深拷貝賦值] ";
        if (this == &other) {                  // ① 自我賦值檢查（比身分／位址）
            std::cout << "自我賦值，跳過\n";
            return *this;
        }
        delete[] m_data;                       // ② 釋放舊資源
        m_len = other.m_len;                   // ③ 複製大小
        m_data = new char[m_len + 1];          // ④ 配置新記憶體
        m_bufId = ++g_nextBufId;
        std::strcpy(m_data, other.m_data);     // ⑤ 複製資料
        std::cout << "複製為 \"" << m_data << "\"（配置 buf#" << m_bufId << "）\n";
        return *this;                          // ⑥ 回傳 *this（支援鏈式賦值）
    }

    // 解構函數
    ~DeepString() {
        std::cout << "  [解構] \"" << (m_data ? m_data : "nullptr")
                  << "\" 釋放 buf#" << m_bufId << "\n";
        delete[] m_data;
    }

    const char* c_str() const { return m_data; }
    void setFirstChar(char ch) { if (m_len > 0) m_data[0] = ch; }

    // 只暴露「是不是同一塊記憶體」的布林事實，不洩漏會變動的位址。
    bool sharesBufferWith(const DeepString& other) const {
        return m_data == other.m_data;
    }
};

int main() {
    // ============================================================
    // 示範 1：淺拷貝的兩大問題（只說明，不實際觸發 double free）
    // ============================================================
    std::cout << "=== 淺拷貝問題（概念說明，不實際執行避免未定義行為）===\n";
    std::cout << "  ShallowString a(\"hello\");\n";
    std::cout << "  ShallowString b = a;  // 淺拷貝：b.m_data 與 a.m_data 指向同一塊\n";
    std::cout << "  問題 1：b.setFirstChar('X') → a 也被改了（共享記憶體）\n";
    std::cout << "  問題 2：a 解構 delete → b 解構 delete 同一塊 → double free（未定義行為）\n\n";

    // ============================================================
    // 示範 2：深拷貝 — 拷貝建構
    // ============================================================
    std::cout << "=== 深拷貝：拷貝建構 ===\n";
    {
        DeepString s1("Dragon");
        DeepString s2 = s1;  // 呼叫拷貝建構函數（深拷貝）

        std::cout << "  s1 與 s2 共用同一塊記憶體？ " << std::boolalpha
                  << s1.sharesBufferWith(s2) << "\n";
        std::cout << "  （false → 各自獨立！比印位址更可靠，見檔頭【概念補充 A】）\n";

        // 修改 s2 不影響 s1
        s2.setFirstChar('X');
        std::cout << "  修改 s2 後：s1=\"" << s1.c_str()
                  << "\"  s2=\"" << s2.c_str() << "\"\n";
        std::cout << "  （s1 不受影響 → 這才是獨立性的真正證據）\n";
    }
    std::cout << "  （各自安全解構）\n\n";

    // ============================================================
    // 示範 3：深拷貝 — 拷貝賦值
    // ============================================================
    std::cout << "=== 深拷貝：拷貝賦值 ===\n";
    {
        DeepString s1("Knight");
        DeepString s2("Wizard");
        std::cout << "  賦值前：s1=\"" << s1.c_str() << "\" s2=\"" << s2.c_str() << "\"\n";
        s2 = s1;  // 呼叫拷貝賦值運算子
        std::cout << "  賦值後：s1=\"" << s1.c_str() << "\" s2=\"" << s2.c_str() << "\"\n";
        std::cout << "  s1 與 s2 仍是各自獨立的兩塊？ "
                  << (!s1.sharesBufferWith(s2)) << "\n";
    }
    std::cout << "\n";

    // ============================================================
    // 示範 4：自我賦值安全性
    // ============================================================
    std::cout << "=== 自我賦值安全性 ===\n";
    {
        DeepString s1("Phoenix");
        s1 = s1;  // 自我賦值 → if (this == &other) 攔截
        std::cout << "  s1=\"" << s1.c_str() << "\"（安全！）\n";
    }
    std::cout << "\n";

    // ============================================================
    // 示範 5：鏈式賦值
    // ============================================================
    std::cout << "=== 鏈式賦值 ===\n";
    {
        DeepString a("A"), b("B"), c("C");
        c = b = a;  // b=a 回傳 b 的引用（左值）→ c=b
        std::cout << "  a=\"" << a.c_str() << "\" b=\"" << b.c_str()
                  << "\" c=\"" << c.c_str() << "\"\n";
    }

    std::cout << "\n=== 重點整理 ===\n";
    std::cout << "  1. 類別有指標成員 → 編譯器預設淺拷貝 → 危險！\n";
    std::cout << "  2. 深拷貝：new 新記憶體 + 複製資料內容\n";
    std::cout << "  3. 需要自訂：拷貝建構函數 + 拷貝賦值運算子（+解構函數）\n";
    std::cout << "  4. 拷貝賦值要注意：自我賦值檢查 + 先釋放舊資源\n";
    std::cout << "  5. 回傳 *this 支援鏈式賦值 a = b = c\n";

    return 0;
}

