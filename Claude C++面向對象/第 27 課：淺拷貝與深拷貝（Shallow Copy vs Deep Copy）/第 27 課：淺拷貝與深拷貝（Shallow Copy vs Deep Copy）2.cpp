// =============================================================================
//  第 27 課 -2  —  深拷貝：讓每個物件擁有自己的一份資源
// =============================================================================
//
// 【主題資訊 Information】
//   主題       ： 深拷貝（deep copy）—— 拷貝「內容」而非「指標值」
//   對照       ： 淺拷貝（shallow copy）= 編譯器預設的逐成員拷貝，見本課 -1
//   三法則     ： 解構函數 + 拷貝建構函數 + 拷貝賦值運算子（C++11 後為五法則）
//   標準版本   ： C++98
//   標頭檔     ： <cstring>（strlen / strcpy）
//   複雜度     ： 拷貝建構 O(n)、拷貝賦值 O(n)、解構 O(1)
//
// 【詳細解釋 Explanation】
//
// 【1. 深拷貝要解決的問題（承接本課 -1 的淺拷貝災難）】
//   一個持有裸指標 char* m_data 的類別，若使用編譯器預設的拷貝，
//   兩個物件的 m_data 會指向「同一塊記憶體」。後果：
//       * 改一個會連帶改另一個（它們共用同一塊資料）
//       * 兩者解構時各 delete[] 一次同一塊記憶體 → double free
//   深拷貝的做法是「配置一塊新記憶體，把內容逐位元組複製過去」，
//   讓每個物件擁有自己的一份，互不干擾。
//   要完整做到，就必須自己寫三法則（解構、拷貝建構、拷貝賦值）。
//
// 【2. 拷貝建構函數 vs 拷貝賦值運算子：差在「有沒有舊資源要處理」】
//   拷貝建構函數面對的是「還沒初始化」的新物件：
//       配置新記憶體 → 複製內容。兩步就完成。
//   拷貝賦值運算子面對的是「已經初始化過」的物件，多兩件事：
//       ① 自我賦值防護（if this == &other）
//       ② 釋放自己原有的資源，再配置新的、複製內容
//       ④ 回傳 *this 以支援鏈式賦值 a = b = c
//   本檔的 operator= 四個步驟就是照這個順序寫的。
//
// 【3. 自我賦值防護為什麼不能省】
//   若寫 s1 = s1，沒有防護時流程會是：
//       delete[] m_data;                  // 把自己的資料刪了
//       m_data = new char[other.m_len+1]; // other 就是自己
//       strcpy(m_data, other.m_data);     // 從「已釋放的自己」複製 → 未定義行為
//   所以 operator= 一開始就要擋掉 this == &other（比的是位址／身分，
//   不是內容，見第 26 課 -10）。本檔測試 3 驗證的正是這個安全性。
//
// 【4. 鏈式賦值 s5 = s4 = s1 為什麼要回傳參考】
//   = 是右結合，s5 = s4 = s1 等於 s5 = (s4 = s1)。
//   內層 s4 = s1 回傳 s4 的參考（return *this），
//   外層再把這個參考賦值給 s5。若 operator= 回傳的是「值」而不是參考，
//   每一環都會多一次拷貝。本檔測試 4 驗證三者最終都變成 "Dragon"。
//
// 【概念補充 Concept Deep Dive】
//
// (A) ★ 本檔為什麼改掉「印記憶體位址」的做法
//   原始版本用 static_cast<void*>(m_data) 印位址，
//   想證明「新舊兩塊記憶體不同」。但這有兩個問題：
//     1) 位址受 ASLR 影響，每次執行都不同 ——
//        本機實測連跑三次是三組完全不同的位址，無法寫成預期輸出。
//     2) 更微妙的是：位址還會「重複使用」。
//        原始輸出中測試 2 的 s3 拿到的位址，
//        正好等於前面某個已解構物件釋放掉的位址（配置器把剛還回來的記憶體再撥出去）。
//        於是「位址相同」既不代表同一個活著的物件，
//        「位址不同」也未必是好消息 —— 位址根本不適合當作獨立性的證據。
//   本檔改用兩種可重現的證據：
//     * 「改一個、印兩個」：測試 1 改 s2 首字元，s1 不受影響（各自獨立）。
//     * c_str() 指標的「布林比較」：印 (s1.c_str() == s2.c_str())=false，
//       只暴露「是不是同一塊」這個布林事實，不洩漏會變動的位址值。
//
// (B) 深拷貝與移動的關係
//   本類別只有三法則、沒有移動操作，而且因為宣告了解構函數，
//   編譯器也不會自動生成移動操作（見第 32 課 -3）。
//   結果是所有「移動」都會安靜退回深拷貝。
//   要讓「來源不再需要時」享受 O(1) 的資源轉移，
//   就要補上移動建構與移動賦值 —— 那是第 32~34 課的主題。
//
// (C) 成員宣告順序（-Wreorder）
//   本檔的建構函數在函式本體內以指派（m_len = ...; m_data = ...;）初始化，
//   而不是用初始化列表，所以不受 -Wreorder 影響；本機以 -Wreorder 編譯無警告。
//   但仍建議把「被依賴的成員」宣告在前（此處 m_len 用於配置 m_data），
//   一旦日後改用初始化列表就不會踩到順序陷阱
//   （對照第 28 課 -1／-3 修正過的同類問題）。
//
// (D) 為什麼用 strcpy 要 m_len + 1
//   C 字串以 '\0' 結尾，strlen 回傳的長度「不含」結尾的 '\0'。
//   所以配置時要 new char[m_len + 1] 才裝得下那個結尾字元，
//   strcpy 也會把 '\0' 一起複製過去。少算這個 1 就是最經典的差一錯誤，
//   會造成一位元組的堆積溢位。
//
// 【注意事項 Pay Attention】
//   1. 持有裸指標就必須自己寫三法則，否則預設的淺拷貝會導致共享與 double free。
//   2. 拷貝賦值運算子一定要有自我賦值防護；拷貝建構函數不需要（沒有舊資源）。
//   3. operator= 要回傳 *this（參考），鏈式賦值才不會多一次拷貝。
//   4. 配置字串記憶體要 m_len + 1（容納結尾的 '\0'）。
//   5. 教材不要印原始位址（ASLR 每次不同，且位址會被重複使用而誤導）——
//      用「改一個印兩個」或指標的布林比較來證明獨立性（見【概念補充 A】)。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】深拷貝與三法則
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 深拷貝和淺拷貝差在哪裡？什麼樣的類別非做深拷貝不可？
//     答：淺拷貝複製「指標值」，兩個物件因此指向同一塊記憶體；
//         深拷貝配置新記憶體、複製「內容」，讓兩者各自獨立。
//         凡是持有裸指標（或其他需要手動釋放的資源）的類別，
//         都非做深拷貝不可，否則會有共享修改與 double free 的問題。
//     追問：要達成深拷貝，需要寫哪些函式？
//         → 三法則：解構函數、拷貝建構函數、拷貝賦值運算子。
//           需要其中一個通常三個都需要；C++11 之後再加兩個移動操作成為五法則。
//
// 🔥 Q2. 拷貝賦值運算子為什麼要回傳 *this？回傳型別是什麼？
//     答：回傳 ClassName&（自身的參考），目的是支援鏈式賦值 a = b = c。
//         = 是右結合，先算 b = c 得到 b 的參考，再拿它賦值給 a。
//         若回傳「值」而不是參考，每一環都會多一次不必要的拷貝。
//     追問：拷貝建構函數也要回傳 *this 嗎？
//         → 不用。建構函數沒有回傳型別，它「就是」在初始化那個新物件本身。
//           回傳 *this 是賦值運算子專屬的需求。
//
// ⚠️ 陷阱. 「印出兩個物件的指標位址，位址不同就證明深拷貝成功、各自獨立。」
//     答：位址不同確實常見於深拷貝，但它不是可靠的證據，
//         而且不適合寫進教材。原因有二：
//         位址受 ASLR 影響每次執行都不同（無法對照）；
//         更關鍵的是「位址會被重複使用」——
//         一個物件解構釋放記憶體後，配置器可能把「同一個位址」
//         再撥給下一個新物件，於是你會看到兩個先後存在的物件位址相同，
//         但它們根本不是同一個。
//     為什麼會錯：把「位址」當成物件的穩定身分。
//         位址只在物件「還活著」的期間代表它；一旦釋放，那個位址就可被重用。
//         要證明獨立性，該用「改一個不影響另一個」，或指標的布林相等比較。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【LeetCode 實戰範例】—— 本檔從缺，理由如下
//   深拷貝是資源管理型類別的實作要求，屬於工程與正確性議題。
//   LeetCode 判的是演算法的輸入輸出與時間限制，
//   評測系統只會建立一個實例並呼叫指定方法，
//   既不會拷貝你的物件，也不會做自我賦值來檢驗防護。
//   （唯一沾邊的 138. Copy List with Random Pointer 考的是
//     「圖的深層複製演算法」，不是 C++ 拷貝建構函數的語言機制，
//     兩者名字相似但完全不同。）硬掛題號只會誤導，故從缺；
//   本檔改以「使用者設定的可撤銷編輯」這個真實情境呈現深拷貝的價值。
//
