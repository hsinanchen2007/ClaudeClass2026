#include <iostream>
#include <cstring>

class SimpleString {
private:
    // 建議：被依賴的成員宣告在前（m_len 用於配置 m_data），
    // 日後若改用初始化列表也不會踩到 -Wreorder（見檔頭【概念補充 C】）。
    std::size_t m_len;
    char* m_data;

public:
    // ──────── 建構函數 ────────
    SimpleString(const char* str = "") {
        m_len = std::strlen(str);
        m_data = new char[m_len + 1];       // +1 容納結尾的 '\0'
        std::strcpy(m_data, str);
        std::cout << "  [建構] \"" << m_data << "\"（長度 " << m_len << "）\n";
    }

    // ──────── 拷貝建構函數（深拷貝）────────
    // 當用一個已存在的物件來初始化新物件時呼叫
    SimpleString(const SimpleString& other) {
        m_len = other.m_len;
        m_data = new char[m_len + 1];       // ★ 配置新的記憶體
        std::strcpy(m_data, other.m_data);  // ★ 複製內容，而非位址
        std::cout << "  [深拷貝建構] 複製出獨立的一份 \"" << m_data << "\"\n";
    }

    // ──────── 拷貝賦值運算子（深拷貝）────────
    // 當用 = 把一個已存在的物件賦值給另一個已存在的物件時呼叫
    SimpleString& operator=(const SimpleString& other) {
        std::cout << "  [深拷貝賦值] ";

        // ★ 步驟 1：檢查自我賦值（比位址／身分，不是比內容）
        if (this == &other) {
            std::cout << "自我賦值，跳過\n";
            return *this;
        }

        // ★ 步驟 2：釋放舊資源
        delete[] m_data;

        // ★ 步驟 3：配置新記憶體並複製
        m_len = other.m_len;
        m_data = new char[m_len + 1];
        std::strcpy(m_data, other.m_data);
        std::cout << "複製為 \"" << m_data << "\"\n";

        // ★ 步驟 4：回傳 *this 以支援鏈式賦值（a = b = c）
        return *this;
    }

    // ──────── 解構函數 ────────
    ~SimpleString() {
        std::cout << "  [解構] 釋放 \"" << (m_data ? m_data : "nullptr") << "\"\n";
        delete[] m_data;
    }

    // ──────── 存取函數 ────────
    const char* c_str() const { return m_data; }
    std::size_t length() const { return m_len; }

    // 回傳「內部緩衝區指標的比較結果」而不是位址本身：
    // 只暴露「是不是同一塊記憶體」這個布林事實，不洩漏會變動的位址。
    bool sharesBufferWith(const SimpleString& other) const {
        return m_data == other.m_data;
    }

    void setFirstChar(char ch) {
        if (m_len > 0) m_data[0] = ch;
    }
};

// -----------------------------------------------------------------------------
// 【日常實務範例】使用者設定的「可撤銷編輯」
//   情境：編輯器在使用者動手改設定之前，先留一份原始設定。
//         使用者若按「取消」，就把原始那份賦值回去復原。
//   為什麼用到本主題：留存的那份必須是「獨立的深拷貝」——
//         若是淺拷貝，備份與工作副本共用同一塊記憶體，
//         使用者一邊編輯，備份也跟著變，取消時就拿不回原始值了。
//   下面用拷貝建構函數留存，再用拷貝賦值運算子復原。
// -----------------------------------------------------------------------------
void undoableEditDemo() {
    SimpleString working("theme=dark");
    std::cout << "  目前設定：\"" << working.c_str() << "\"\n";

    SimpleString backup = working;          // ★ 深拷貝留存原始設定
    std::cout << "  （已備份）備份與工作副本共用同一塊記憶體？ "
              << std::boolalpha << working.sharesBufferWith(backup)
              << "  ← false 代表各自獨立\n";

    working = SimpleString("theme=light");  // 使用者改了設定
    std::cout << "  使用者修改後：\"" << working.c_str()
              << "\"，備份仍是：\"" << backup.c_str() << "\"\n";

    std::cout << "  使用者按下取消 → 從備份復原\n";
    working = backup;                       // 深拷貝賦值復原
    std::cout << "  復原後：\"" << working.c_str() << "\"\n";
}

int main() {
    std::cout << "=== 深拷貝示範 ===\n\n";

    // ---- 測試 1：拷貝建構 ----
    std::cout << "【測試 1】拷貝建構\n";
    SimpleString s1("Dragon");

    std::cout << "  建立 s2 = s1（拷貝建構）\n";
    SimpleString s2 = s1;   // 呼叫拷貝建構函數

    std::cout << "  s1 與 s2 共用同一塊記憶體？ "
              << std::boolalpha << s1.sharesBufferWith(s2) << "\n";
    std::cout << "  （false → 各自獨立！這比印位址更可靠，見檔頭【概念補充 A】）\n";

    s2.setFirstChar('X');
    std::cout << "  修改 s2 後：s1=\"" << s1.c_str()
              << "\"  s2=\"" << s2.c_str() << "\"\n";
    std::cout << "  （s1 不受影響 → 這才是獨立性的真正證據）\n\n";

    // ---- 測試 2：拷貝賦值 ----
    std::cout << "【測試 2】拷貝賦值\n";
    SimpleString s3("Knight");
    std::cout << "  執行 s3 = s1\n";
    s3 = s1;   // 呼叫拷貝賦值運算子

    std::cout << "  s1=\"" << s1.c_str()
              << "\"  s3=\"" << s3.c_str() << "\"\n\n";

    // ---- 測試 3：自我賦值安全性 ----
    std::cout << "【測試 3】自我賦值\n";
    s1 = s1;   // 應安全處理
    std::cout << "  s1=\"" << s1.c_str() << "\"（安全！）\n\n";

    // ---- 測試 4：鏈式賦值 ----
    std::cout << "【測試 4】鏈式賦值\n";
    SimpleString s4("Wizard");
    SimpleString s5("Archer");
    std::cout << "  執行 s5 = s4 = s1\n";
    s5 = s4 = s1;
    std::cout << "  s1=\"" << s1.c_str() << "\"  s4=\"" << s4.c_str()
              << "\"  s5=\"" << s5.c_str() << "\"\n\n";

    // ---- 測試 5：日常實務 ----
    std::cout << "【測試 5】日常實務：可撤銷的設定編輯\n";
    undoableEditDemo();
    std::cout << "\n";

    std::cout << "=== 離開 main，開始解構 ===\n";
    return 0;
}

