// =============================================================================
//  第 2 課：C 與 C++ 的關鍵差異 11  —  C 沒有預設參數（對照組）
// =============================================================================
//
// 【主題資訊 Information】
//   主題：本檔是「C 沒有預設參數」的對照組，與 12.cpp（C++ 預設參數）配成一對。
//   C 的規則：函式呼叫時，實際引數的個數必須與宣告完全相同，
//             語言層級沒有任何「省略後自動補值」的機制。
//   標頭檔：<stdio.h>
//   複雜度：與主題無關；本檔討論的是 API 設計與呼叫端負擔。
//
// 【詳細解釋 Explanation】
//
// 【1. 問題的本質：呼叫端被迫重複表達「預設」】
//   下面的 greet() 需要 name 和 greeting 兩個參數。實務上 greeting
//   九成九都是 "Hello"，但 C 逼你每次都寫出來：
//       greet("Alice", "Hello");
//       greet("Bob",   "Hello");
//   重複本身還不是最糟的，最糟的是「預設值的知識被散佈到所有呼叫點」。
//   哪天預設招呼語要改成 "Hi"，你得找出全專案每一處呼叫逐一修改，
//   漏一個就是不一致的 bug，而且編譯器完全不會提醒你。
//   有預設參數時，這個知識只存在於函式宣告那一行，改一次就全部生效。
//
// 【2. C 程式設計師的三種替代方案，以及各自的代價】
//   (a) 包裝函式（wrapper）
//         void greet_default(const char* name) { greet(name, "Hello"); }
//       最安全，但每多一個可省略參數，就得多寫一層包裝，
//       n 個可選參數最壞需要 2^n 個包裝函式，顯然不可行。
//   (b) 傳 NULL 當哨兵，由函式內部補值
//         void greet(const char* name, const char* greeting) {
//             if (greeting == NULL) greeting = "Hello";
//             ...
//         }
//       只要一個函式就搞定，但呼叫端變成 greet("Alice", NULL)——
//       讀者看到 NULL 完全猜不出「這代表要用預設招呼語」，可讀性很差；
//       而且如果 NULL 本身是合法的業務值，這招就直接失效。
//   (c) 可變參數（variadic，<stdarg.h>）
//       完全放棄型別檢查，呼叫端傳錯型別是未定義行為，
//       這是最後手段，不是解法。
//
// 【3. 為什麼這是「語言差異」而不是「風格差異」】
//   預設參數不能靠函式庫模擬出來——它必須由編譯器在「呼叫端」動手：
//   編譯器看到 greet("Alice") 時，會自動把預設值當成第二個引數補上去，
//   產生的機器碼跟你手寫 greet("Alice", "Hello") 完全一樣。
//   C 的編譯器沒有這個步驟，所以無論怎麼寫程式庫都補不出來。
//
// 【概念補充 Concept Deep Dive】
//   ● C 的函式呼叫慣例（calling convention）本身沒有「引數個數」的概念：
//     引數就是依序放進暫存器/堆疊。缺一個引數，被呼叫方讀到的就是
//     殘留在該位置的任意資料——所以 C 不可能「安全地」允許少傳。
//     這也是為什麼 C89 的 `void f();`（未指定參數列）非常危險，
//     現代 C 建議一律寫 `void f(void)`。
//   ● C++ 的預設參數完全是編譯期的語法糖，不改變 ABI 的引數傳遞方式，
//     也因此有個重要限制：預設值必須在「呼叫點看得見的宣告」中，
//     通常寫在標頭檔。這帶來一個實務陷阱——改了標頭檔裡的預設值，
//     只重新編譯 .cpp 而沒重編呼叫方，兩邊就會不一致（見 12.cpp）。
//
// 【注意事項 Pay Attention】
//   1. 本檔是刻意保留 C 風格的對照組，不是建議寫法。
//   2. C 中用 NULL 當「請用預設值」的哨兵，會讓 NULL 同時承載兩種語意，
//      日後若 NULL 變成合法輸入就無法區分，屬於設計債。
//   3. printf 的格式字串與引數不符是未定義行為，且 -Wall -Wextra 只能
//      抓到字面量格式字串的情況；動態組出來的格式字串抓不到。
//   4. C11 的 _Generic 可以做「依型別分派」，但那是模擬重載，
//      仍然無法省略引數，跟預設參數是兩回事。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】預設參數（C 的缺席）
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. C 沒有預設參數，工程上通常怎麼補救？各有什麼代價？
//     答：三條路。(1) 寫包裝函式——最安全，但 n 個可選參數最壞要
//         2^n 個包裝，不可擴充。(2) 傳 NULL 當哨兵、函式內補值——
//         只要一個函式，但呼叫端可讀性差，且 NULL 若是合法業務值就失效。
//         (3) variadic 函式——放棄型別檢查，屬於最後手段。
//     追問：那預設參數為什麼不能用函式庫模擬？
//         → 因為補值這個動作發生在「呼叫端」，必須由編譯器在產生
//           呼叫程式碼時插入，函式庫沒有介入呼叫端的能力。
//
// ⚠️ 陷阱. 既然 C 不能少傳引數，那少傳會怎樣？會編譯錯誤還是執行期出錯？
//     答：在現代 C（有完整原型宣告時）是編譯錯誤。但若沿用 C89 的
//         `void f();`——空的括號代表「不指定參數列」而非「沒有參數」——
//         編譯器就不檢查引數個數，少傳會讓被呼叫方讀到殘留資料，
//         是未定義行為，且可能安靜地跑過去。
//     為什麼會錯：多數人以為 C 的 `f()` 和 C++ 的 `f()` 意思一樣。
//         在 C++ 裡 `void f();` 明確代表「不接受任何參數」；
//         在 C89/C99 裡它代表「參數未指定」。同樣的語法、相反的語意，
//         這是 C 移植到 C++ 時最陰險的差異之一。
// ═══════════════════════════════════════════════════════════════════════════

#include <stdio.h>

void greet(const char* name, const char* greeting) {
    printf("%s, %s!\n", greeting, name);
}

/* ---------------------------------------------------------------------------
 * 【日常實務範例】C 風格的補救：包裝函式 vs NULL 哨兵
 *   情境：一個 CLI 工具要印出訊息，訊息前綴（等級標籤）多數時候是 "INFO"。
 *   下面同時示範兩種 C 的補救手法，並讓讀者直接看到呼叫端的差別。
 * ------------------------------------------------------------------------- */

/* 手法 A：包裝函式。呼叫端最乾淨，但每種組合都要多寫一個函式。 */
void log_msg(const char* level, const char* msg) {
    printf("  [%s] %s\n", level, msg);
}

void log_info(const char* msg) {   /* 只是為了「省掉 level」而存在 */
    log_msg("INFO", msg);
}

/* 手法 B：NULL 哨兵。只要一個函式，但呼叫端出現意義不明的 NULL。 */
void log_msg2(const char* level, const char* msg) {
    if (level == NULL) {           /* NULL 被賦予「請用預設值」的第二種語意 */
        level = "INFO";
    }
    printf("  [%s] %s\n", level, msg);
}

/* 註：本檔不加 LeetCode 範例。
 *     「有沒有預設參數」是 API 設計與語言機制問題，不影響任何演算法題的
 *     解法或複雜度；硬掛一題只會讓焦點失真，故從缺。 */

int main() {
    // 每次都必須提供所有參數
    greet("Alice", "Hello");
    greet("Bob", "Hello");  // 即使 greeting 常常是 "Hello"

    printf("\n=== 補救手法 A：包裝函式 ===\n");
    log_info("service started");            /* 呼叫端乾淨，代價是多一個函式 */
    log_msg("ERROR", "connect timeout");    /* 需要自訂等級時走原函式 */

    printf("\n=== 補救手法 B：NULL 哨兵 ===\n");
    log_msg2(NULL, "service started");      /* NULL 是什麼意思？看不出來 */
    log_msg2("ERROR", "connect timeout");

    printf("\n=== 對照 ===\n");
    printf("  C   ：預設值的知識散落在每個呼叫點，或靠 NULL 這種暗號\n");
    printf("  C++ ：預設值只寫在函式宣告一處，改一次全部生效（見 12.cpp）\n");

    return 0;
}

