// =============================================================================
//  第 2 課：C 與 C++ 的關鍵差異 1  —  變數宣告位置（C89 的「區塊開頭宣告」限制）
// =============================================================================
//
// 【主題資訊 Information】
//   語法：C89 要求所有變數宣告必須出現在「區塊（block）的開頭」，
//         也就是在任何一條可執行敘述之前。
//   演進：C89（1989）  必須在區塊開頭宣告
//         C99（1999）  放寬，可在區塊中任意位置宣告
//         C++98 起     一開始就允許任意位置宣告，並允許在 for 的初始化區宣告
//   標頭檔：<stdio.h>（C 風格 I/O）
//   複雜度：與宣告位置無關；這是純粹的語法/作用域規則，不影響執行效率。
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼 C89 會有這個限制？】
//   這不是語言設計者的美學偏好，而是 1970 年代編譯器的現實妥協。
//   早期 C 編譯器是「單遍（one-pass）」的：讀到函式進入點時，就必須立刻決定
//   這個函式的 stack frame 要多大，然後發出一次 `sub rsp, N` 把堆疊指標下移。
//   若允許在函式中段才冒出新變數，編譯器在產生開場（prologue）程式碼時
//   還不知道總共需要幾個 byte，就得回頭修改已經吐出去的機器碼——
//   在只有磁帶、記憶體以 KB 計的年代，這是負擔不起的。
//   把「所有宣告集中在最前面」變成語法規則，等於強迫程式設計師替編譯器
//   先做好尺寸統計。
//
// 【2. 為什麼 C++ 一開始就不要這個限制？】
//   關鍵在於 C++ 的變數不只是「一塊記憶體」，它可能是一個有建構函數的物件。
//       std::string s;        // 這一行會實際執行程式碼（呼叫建構函數）
//   如果強迫所有物件都在區塊開頭宣告，就等於強迫它們在「還沒有資料可以初始化」
//   的時候先被預設建構一次，稍後再用 assignment 覆寫——白白多一次建構 + 一次賦值。
//   C++ 的核心信條是「宣告即初始化」（declare at the point of first use，
//   並且在同一行給它正確的初值），這個信條和 C89 的限制天生衝突。
//   所以宣告位置的自由，在 C 是便利性；在 C++ 是效能與正確性的前提。
//
// 【3. 作用域越小越好（Principle of Least Scope）】
//   把變數宣告在「第一次使用的地方」有三個實質好處：
//     (a) 一定看得到初值。C89 風格常見的 `int sum;` … 20 行後 … `sum = 0;`
//         中間一旦有 return，就是讀取未初始化變數（未定義行為）。
//     (b) 生命週期最短。變數一離開區塊就消失，暫存器配置器（register
//         allocator）更容易把它放進暫存器而不是記憶體。
//     (c) 可讀性。讀者不必往上捲 30 行去查 `k` 到底是什麼型別。
//
// 【4. for 迴圈計數器的作用域差異（最常被考的一點）】
//       for (int i = 0; i < 10; i++) { ... }
//       // C++：i 的作用域「僅限 for 敘述本身」，迴圈結束後 i 不存在。
//   這在 C89 是寫不出來的（必須先在區塊開頭宣告 int i）。
//   因此同一份 C89 程式碼裡，迴圈結束後 i 仍然存在且保有最後的值——
//   有些舊程式碼還故意利用這點在迴圈後讀取 i。移植到 C++ 時，
//   若把宣告移進 for，這種「迴圈後讀 i」的寫法就會編譯失敗。
//
// 【概念補充 Concept Deep Dive】
//   ● 宣告位置不等於配置位置。
//     即使你把 `int sum;` 寫在函式中段，現代編譯器（gcc/clang）通常仍在
//     函式開場時就把整個 frame 一次配置好。也就是說，「在區塊中段宣告」
//     並不會產生一條額外的堆疊調整指令。
//     語法上的自由 ≠ 執行期的成本。這是初學者最常見的誤解之一。
//   ● 唯一真正影響配置時機的是「動態大小」的東西：VLA（C99 變長陣列）
//     或 alloca()，那才會在執行期真的移動堆疊指標。C++ 標準沒有 VLA。
//   ● 對 C++ 的類別型別，宣告位置則會真的影響「建構函數何時執行」——
//     這是執行期可觀察的行為，不只是語法問題。
//
// 【注意事項 Pay Attention】
//   1. 本檔是刻意寫成 C89 風格的「對照組」，用來凸顯限制，不是建議的寫法。
//      實務上請採用「在第一次使用處宣告並同時初始化」。
//   2. `int sum;`（未初始化）之後才 `sum = 0;` 是危險模式：中間若有 return
//      或 goto 跳過賦值，就會讀到不確定值（indeterminate value）。
//      本檔在宣告後立即賦值，故安全。
//   3. C99 起 C 也允許任意位置宣告，所以「C 不能中途宣告」的說法要加上
//      「C89」這個限定詞，否則面試時會被糾正。
//   4. 本檔以 g++ 編譯（副檔名 .cpp），語法上 C++ 完全接受這種 C89 寫法——
//      能編譯不代表是好風格。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】變數宣告位置與作用域
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼 C89 規定變數必須在區塊開頭宣告？C++ 為何取消這個限制？
//     答：C89 的限制源自單遍編譯器必須在函式開場時就決定 stack frame 大小。
//         C++ 取消它的動機不是美觀，而是「宣告即初始化」：C++ 變數可能是
//         有建構函數的物件，強迫提前宣告會導致先預設建構、稍後再賦值，
//         多付一次建構成本，也讓「未初始化就被使用」的空窗期變長。
//     追問：那把宣告寫在函式中段，會不會多一條調整堆疊的指令？
//         → 不會。現代編譯器仍在函式開場一次配置整個 frame，
//           語法自由不等於執行期成本。
//
// 🔥 Q2. C++ 的 `for (int i = 0; ...)` 裡的 i，作用域到哪裡結束？
//     答：僅限 for 敘述本身（包含迴圈本體）。迴圈一結束 i 就不存在，
//         在迴圈後引用 i 會編譯錯誤。C89 因為必須在區塊開頭宣告，
//         i 反而會活到整個區塊結束。
//     追問：若真的需要在迴圈後知道停在哪一格，怎麼寫？
//         → 把 i 宣告在 for 之外（刻意延長作用域），
//           或用一個獨立變數在迴圈內記錄，別依賴計數器殘值。
//
// ⚠️ 陷阱. 「C 語言不能在函式中間宣告變數」——這句話對嗎？
//     答：不完全對，必須限定為「C89 不行」。C99（1999 年）起已經放寬，
//         現在的 C 編譯器預設多半是 C11/C17，早就可以中途宣告。
//     為什麼會錯：多數人把「大學課本教的 C」直接等同於「C 語言」，
//         而那些課本通常停在 C89。把 30 年前的限制當成語言的永久特性，
//         是最常見的知識過期問題。
// ═══════════════════════════════════════════════════════════════════════════

#include <stdio.h>
#include <string.h>   /* strstr：C 風格子字串搜尋（在 C++ 中亦可寫 <cstring>） */

int main() {
    /* C89 要求變數必須在區塊開頭宣告 */
    int i;
    int sum;
    
    sum = 0;
    for (i = 0; i < 10; i++) {
        sum += i;
    }
    
    printf("Sum = %d\n", sum);

    /* ===================================================================
     * 【LeetCode 實戰範例】LeetCode 1480. Running Sum of 1d Array
     *   題目：給一個陣列 nums，回傳 runningSum，其中 runningSum[i] 為
     *         nums[0] 到 nums[i] 的累加和。
     *   為什麼用到本主題：這題的迴圈骨架和上面那段 C89 累加完全相同，
     *         正好拿來對照「宣告位置」的兩種寫法——C89 必須先把 i 提到
     *         區塊開頭，C++/C99 可以直接寫 for (int i = ...)，
     *         讓計數器的作用域縮到最小。
     *   複雜度：時間 O(n)，空間 O(1)（就地寫回 out）。
     * =================================================================== */
    printf("\n=== LeetCode 1480. Running Sum of 1d Array ===\n");
    {
        /* C89 風格：所有變數先在區塊開頭宣告 */
        int nums[4];
        int out[4];
        int k;
        int acc;

        nums[0] = 1; nums[1] = 2; nums[2] = 3; nums[3] = 4;

        acc = 0;
        for (k = 0; k < 4; k++) {
            acc += nums[k];
            out[k] = acc;
        }

        printf("  input : [1,2,3,4]\n");
        printf("  output: [");
        for (k = 0; k < 4; k++) {
            printf("%d", out[k]);
            if (k < 3) printf(",");
        }
        printf("]\n");
    }

    /* ===================================================================
     * 【日常實務範例】統計一段 log 中各嚴重等級出現的次數
     *   情境：維運人員拿到一份 log，想快速知道 ERROR 有幾筆、WARN 有幾筆。
     *   這裡刻意維持 C89 風格（計數器全部宣告在區塊開頭），
     *   讀者可以直接感受到：變數離它被使用的地方越遠，越難確認初值正確。
     * =================================================================== */
    printf("\n=== 日常實務：log 等級統計（C89 風格）===\n");
    {
        const char* lines[5];
        int idx;
        int nError;
        int nWarn;
        int nInfo;

        lines[0] = "2026-07-19 10:00:01 [INFO]  service started";
        lines[1] = "2026-07-19 10:00:07 [WARN]  disk usage 86%";
        lines[2] = "2026-07-19 10:01:22 [ERROR] connect timeout";
        lines[3] = "2026-07-19 10:01:23 [ERROR] retry failed";
        lines[4] = "2026-07-19 10:02:00 [INFO]  recovered";

        /* 三個計數器都必須在這裡就歸零，否則後面讀到的是不確定值 */
        nError = 0;
        nWarn  = 0;
        nInfo  = 0;

        for (idx = 0; idx < 5; idx++) {
            /* strstr 回傳 NULL 代表沒找到 */
            if (strstr(lines[idx], "[ERROR]") != NULL) {
                nError++;
            } else if (strstr(lines[idx], "[WARN]") != NULL) {
                nWarn++;
            } else if (strstr(lines[idx], "[INFO]") != NULL) {
                nInfo++;
            }
        }

        printf("  ERROR = %d\n", nError);
        printf("  WARN  = %d\n", nWarn);
        printf("  INFO  = %d\n", nInfo);
    }

    return 0;
}

