// =============================================================================
//  第 2 課：C 與 C++ 的關鍵差異 12  —  預設參數（Default Arguments）
// =============================================================================
//
// 【主題資訊 Information】
//   語法：void greet(const std::string& name,
//                    const std::string& greeting = "Hello");
//   標準版本：C++98 起支援；C 語言至今（含 C23）沒有這個機制。
//   標頭檔：本檔用 <iostream>、<string>。
//   複雜度：零執行期成本。預設值是「編譯期在呼叫端補上引數」，
//           產生的機器碼與手寫完整引數完全相同。
//
// 【詳細解釋 Explanation】
//
// 【1. 它到底發生在哪一端？（理解一切的鑰匙）】
//   最關鍵的一句話：預設參數是在「呼叫端（caller）」展開的，不是被呼叫端。
//   當編譯器看到
//       greet("Alice");
//   它會查出 greet 的宣告、發現第二個參數有預設值，於是實際產生
//       greet("Alice", "Hello");
//   的呼叫程式碼。函式本體完全不知道「這個引數是使用者給的還是補的」。
//   從這一句話可以直接推導出後面所有的規則與陷阱，不必死背。
//
// 【2. 推論一：預設值必須從右邊開始，不能跳過】
//       void f(int a, int b = 1, int c = 2);   // OK
//       void f(int a = 0, int b, int c);       // 錯誤
//   因為引數是「依位置」對應的。若允許 f(int a = 0, int b)，
//   那 f(5) 的 5 該給 a 還是 b？無從判斷。C++ 沒有 Python 那種
//   具名引數（keyword argument），位置是唯一的對應依據，
//   所以可省略的部分必須連續地落在最右邊。
//
// 【3. 推論二：預設值寫在「宣告」，而且同一個翻譯單元只能給一次】
//       // header.h
//       void f(int a, int b = 10);
//       // impl.cpp
//       void f(int a, int b) { ... }      // 這裡不可以再寫 = 10
//   因為呼叫端只看得到標頭檔的宣告，預設值必須在那裡才能被使用。
//   若在定義處重複給值，同一作用域內會是「重複指定預設參數」的編譯錯誤。
//   ★ 實務地雷：預設值是呼叫端展開的，所以改了標頭檔裡的預設值，
//     必須「重新編譯所有呼叫方」。只重編 .cpp 而沒重編呼叫方，
//     舊的呼叫端會繼續傳舊的預設值，形成難以察覺的不一致——
//     這正是為什麼有些團隊禁止在公開 API 使用預設參數。
//
// 【4. 推論三：預設值在「每次呼叫」時求值，不是只算一次】
//       int counter();
//       void log(int id = counter());     // counter() 每次呼叫 log 都會執行
//   這和 Python 那個惡名昭彰的「可變預設引數只建立一次」正好相反。
//   C++ 的預設值是一段運算式，被貼到每個呼叫點，因此每次都重新求值。
//   本檔的 nextTicket() 範例會實際跑給你看。
//
// 【5. 推論四：預設參數 vs 函數重載】
//   兩者都能達成「用不同引數個數呼叫同一個名字」，選擇原則是：
//     ● 各版本行為一致、只是少填幾個值 → 用預設參數（一份實作，好維護）。
//     ● 各版本行為本質不同、或型別不同 → 用重載。
//   兩者混用要小心產生歧義：
//       void g(int);
//       void g(int, int = 0);
//       g(1);            // 歧義！兩個候選都完全可行 → 編譯錯誤
//
// 【概念補充 Concept Deep Dive】
//   ● 【最經典的陷阱】虛擬函式的預設參數是「靜態綁定」的。
//     虛擬函式本身依物件的動態型別分派（執行期），
//     但預設參數是呼叫端依「指標/參考的靜態型別」補上的（編譯期）。
//     結果就是：跑的是衍生類別的函式本體，用的卻是基底類別的預設值。
//     本檔 main() 有可執行的示範，親眼看過一次就不會忘。
//     實務守則：永遠不要在虛擬函式上使用預設參數。
//   ● 預設參數不參與函式型別。
//     `void (*p)(int, int) = &f;` 即使 f 的第二個參數有預設值，
//     函式指標型別仍是 void(*)(int,int)，透過指標呼叫時
//     不能省略引數——因為指標呼叫沒有宣告可查。
//   ● 預設參數不影響 name mangling（符號名只編碼參數型別），
//     所以加上預設值不會破壞 ABI 的符號相容性，
//     但會破壞「行為相容性」（如上面的重編譯地雷）。
//
// 【注意事項 Pay Attention】
//   1. 只能從最右邊開始給預設值，中間不能有洞。
//   2. 預設值寫在標頭檔的宣告處；改動後必須重編所有呼叫方。
//   3. 預設值每次呼叫都重新求值——若預設值是函式呼叫，會有副作用。
//   4. 絕不要在 virtual 函式上用預設參數（靜態綁定陷阱，本檔有示範）。
//   5. 預設參數與重載併用容易產生歧義，編譯器會直接報錯而非默默挑一個。
//   6. 預設值運算式不能引用同一函式中「位置在它右邊」的參數。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】預設參數
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 預設參數是在編譯期還是執行期處理的？發生在呼叫端還是被呼叫端？
//     答：編譯期，而且發生在「呼叫端」。編譯器看到 greet("Alice")，
//         查出宣告中第二個參數有預設值，就直接產生
//         greet("Alice", "Hello") 的呼叫碼。函式本體無從得知
//         某個引數是使用者傳的還是補的，執行期成本為零。
//     追問：那為什麼預設值只能寫在標頭檔的宣告、不能寫在 .cpp 的定義？
//         → 因為補值的是呼叫端，呼叫端只看得到標頭檔。
//           也因此改了預設值必須重編所有呼叫方，否則行為不一致。
//
// 🔥 Q2. 什麼時候該用預設參數、什麼時候該用函數重載？
//     答：行為相同、只是少填幾個值 → 預設參數（單一實作，維護成本低）。
//         行為或型別本質不同 → 重載。兩者混用時，若某個呼叫同時符合
//         「重載版本」和「補了預設值的版本」，就是歧義、編譯失敗。
//     追問：預設參數會改變函式的型別或符號名嗎？
//         → 都不會。函式指標型別不含預設值，透過函式指標呼叫時
//           必須把引數補齊；name mangling 也只編碼參數型別。
//
// ⚠️ 陷阱. virtual 函式加上預設參數，呼叫時會用哪一個預設值？
//     答：用「指標/參考的靜態型別」上的預設值，但執行「動態型別」的函式本體。
//         也就是說：Base* p = new Derived; p->f();
//         跑的是 Derived::f，卻套用 Base::f 宣告的預設值——
//         這幾乎一定不是你要的結果。本檔 main() 有實際輸出可驗證。
//     為什麼會錯：大家記得「虛擬函式是執行期多型」，就順理成章以為
//         「連預設參數也會跟著動態型別走」。但多型只作用在「呼叫哪個函式本體」，
//         而預設值是編譯器在呼叫端就貼好的常數，它根本沒進入虛擬分派流程。
//         兩件事在不同時間點發生，這是把「動態分派」過度泛化的典型錯誤。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>

void greet(const std::string& name, const std::string& greeting = "Hello") {
    std::cout << greeting << ", " << name << "!" << std::endl;
}

// -----------------------------------------------------------------------------
// 【示範 1】預設值在「每次呼叫」時求值（與 Python 的行為相反）
//   nextTicket() 每被當成預設值使用一次就會遞增，證明它不是只算一次。
// -----------------------------------------------------------------------------
int g_ticket = 0;
int nextTicket() { return ++g_ticket; }

void serve(const std::string& who, int ticket = nextTicket()) {
    std::cout << "  " << who << " 取得號碼牌 #" << ticket << std::endl;
}

// -----------------------------------------------------------------------------
// 【示範 2】⚠️ virtual + 預設參數 = 靜態綁定陷阱（可執行驗證）
//   Base 與 Derived 都宣告了預設值，但兩者不同。
//   透過 Base* 呼叫時，函式本體走 Derived（動態），預設值卻走 Base（靜態）。
// -----------------------------------------------------------------------------
struct Base {
    virtual ~Base() = default;
    virtual void show(const std::string& tag = "Base 的預設值") const {
        std::cout << "  Base::show   tag=" << tag << std::endl;
    }
};

struct Derived : Base {
    void show(const std::string& tag = "Derived 的預設值") const override {
        std::cout << "  Derived::show tag=" << tag << std::endl;
    }
};

// -----------------------------------------------------------------------------
// 【日常實務範例】帶預設值的結構化 log 函式
//   情境：專案裡九成的 log 是 INFO 等級、寫到主 log channel，
//         只有少數需要指定 level 或 channel。
//   有了預設參數，「常見情況」的呼叫端只寫必要資訊，
//   而預設值的知識集中在這一行宣告——要改預設 channel 只需改這裡。
// -----------------------------------------------------------------------------
void logLine(const std::string& msg,
             const std::string& level   = "INFO",
             const std::string& channel = "main") {
    std::cout << "  [" << channel << "][" << level << "] " << msg << std::endl;
}

// 註：本檔不加 LeetCode 範例。預設參數是 API 設計機制，
//     不會改變任何題目的演算法或複雜度，硬掛一題反而失焦，故從缺。

int main() {
    greet("Alice");           // 使用預設值 "Hello"
    greet("Bob");             // 使用預設值 "Hello"
    greet("Charlie", "Hi");   // 使用自訂值 "Hi"

    std::cout << "\n=== 示範 1：預設值每次呼叫都重新求值 ===" << std::endl;
    serve("Alice");           // ticket = nextTicket() → 1
    serve("Bob");             // ticket = nextTicket() → 2
    serve("Charlie", 99);     // 明確給值，不會呼叫 nextTicket()
    serve("Dave");            // ticket = nextTicket() → 3（不是 4，因為上一行沒用到）

    std::cout << "\n=== 示範 2：virtual + 預設參數的靜態綁定陷阱 ===" << std::endl;
    Derived d;
    Base* p = &d;
    std::cout << "  透過 Base* 呼叫（注意：函式本體是 Derived，預設值卻是 Base 的）" << std::endl;
    p->show();                // 跑 Derived::show，但 tag 來自 Base 的宣告
    std::cout << "  直接用 Derived 物件呼叫" << std::endl;
    d.show();                 // 兩者都是 Derived 的

    std::cout << "\n=== 日常實務：帶預設值的 log ===" << std::endl;
    logLine("service started");                          // 全用預設
    logLine("disk usage 86%", "WARN");                   // 指定 level
    logLine("payment failed", "ERROR", "billing");       // 全部指定

    return 0;
}

