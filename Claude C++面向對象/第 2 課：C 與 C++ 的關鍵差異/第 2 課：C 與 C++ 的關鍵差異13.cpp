// =============================================================================
//  第 2 課：C 與 C++ 的關鍵差異 13  —  C 的「傳位址」：用指標修改呼叫端變數
// =============================================================================
//
// 【主題資訊 Information】
//   語法：void swap(int* a, int* b);   呼叫端必須寫 swap(&x, &y);
//   標準版本：C 與 C++ 都支援指標；本檔是 C 的作法，14.cpp 是 C++ 的引用版。
//   標頭檔：<stdio.h>
//   複雜度：O(1) 時間、O(1) 空間；三次搬移。
//
// 【詳細解釋 Explanation】
//
// 【1. C 只有一種傳遞方式：傳值（pass by value）】
//   這是最容易被誤解的一點——「C 有傳值和傳址兩種」的說法其實不精確。
//   C 永遠只有傳值。所謂「傳址」，是把「位址這個值」複製一份傳進去。
//   函式收到的 a 是一個全新的區域指標變數，只是它的內容剛好是 x 的位址。
//   所以：
//       修改 a 本身（a = something）→ 完全不影響呼叫端。
//       修改 *a（解參考後寫入）    → 才會改到呼叫端的 x。
//   理解這一點，就能解釋為什麼「想在函式裡改一個指標」時，
//   必須傳二階指標 int**——因為你要修改的東西是指標，就得傳它的位址。
//
// 【2. 為什麼呼叫端必須寫 & ？】
//   因為型別必須對得上：參數是 int*，而 x 是 int，
//   所以要用取址運算子 & 把 int 變成 int*。
//   這個 & 有一個常被忽略的好處——它是「這個函式可能會改我」的視覺標記。
//   讀者看到 swap(&x, &y) 就知道 x、y 可能被改；
//   看到 process(data) 就知道大概不會被改（雖然沒有強制力）。
//   C++ 的引用把這個標記拿掉了，換來簡潔，代價是呼叫端看不出來（見 14.cpp）。
//
// 【3. 指標的表達力，也是它的風險】
//   指標能做到引用做不到的三件事：
//     (a) 可以是 NULL（表示「沒有值」）。
//     (b) 可以重新指向別的東西（rebind）。
//     (c) 可以做指標算術（p + 1 移動一個元素）。
//   代價是這三件事都可能出錯：
//     (a) 忘記檢查 NULL → 解參考空指標，未定義行為。
//     (b) 不小心改到指標本身而非指向的值。
//     (c) 算錯偏移量 → 越界存取。
//   C++ 的引用刻意放棄這三種能力，換取「一定綁定到有效物件、且永不改綁」
//   的保證。這是典型的「用彈性換安全」的設計取捨。
//
// 【概念補充 Concept Deep Dive】
//   ● 產生的機器碼：引用版和指標版在最佳化後通常完全相同。
//     引用在 ABI 層級就是用指標實作的，兩者不是效能差異，是「語意與安全」差異。
//   ● 為什麼 C 標準函式庫沒有泛型 swap？
//     因為沒有模板。C 只能提供 memcpy 式的 void* + size 版本
//     （像 qsort 那樣），完全喪失型別檢查。C++ 有 std::swap<T>，
//     型別安全又能為特定型別特化（例如 std::vector 的 swap 只交換內部指標，O(1)）。
//   ● 三次搬移 vs XOR 交換：
//     有人愛寫 *a ^= *b; *b ^= *a; *a ^= *b; 來「省一個變數」。
//     這是應該避免的技巧——當 a 和 b 指向同一個物件時，結果會變成 0，
//     而且現代編譯器對 temp 版本的最佳化更好。可讀性與正確性都輸。
//
// 【注意事項 Pay Attention】
//   1. 傳進來的指標可能是 NULL，正式程式碼應檢查（本檔為教學簡化未檢查，
//      但下面的實務範例有示範檢查）。
//   2. 若兩個指標指向同一個位址，本檔的三次搬移版本仍然正確（結果不變），
//      但 XOR 版本會把值清成 0。
//   3. 傳指標不代表「不複製」——複製的是位址本身（64 位元系統上 8 bytes）。
//      對 int 這種小型別，傳指標未必比傳值省。
//   4. C 沒有辦法在型別層級表達「這個指標不可為 NULL」，
//      只能靠註解與文件約定。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】指標傳遞
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. C 語言到底有沒有「傳址呼叫（pass by reference）」？
//     答：嚴格說沒有。C 永遠是傳值。所謂傳址，是把「位址這個值」複製一份
//         傳進函式；函式裡的指標參數是一個全新的區域變數。
//         正因如此，改 a 不影響呼叫端，改 *a 才會。
//     追問：那如果我想在函式裡改變呼叫端的「指標」本身呢？
//         → 要傳二階指標 int**（或在 C++ 用 int*&）。
//           因為你想修改的目標是指標，就得傳指標的位址。
//
// ⚠️ 陷阱. void reset(int* p) { p = NULL; } 呼叫後，呼叫端的指標會變成 NULL 嗎？
//     答：不會。p 只是呼叫端指標的一份複本，把複本設成 NULL
//         對原本的指標毫無影響，呼叫端的指標仍指向原處。
//     為什麼會錯：一看到參數是「指標」，就以為「所有對它的修改都會傳回去」。
//         實際上能傳回去的只有「透過解參考對目標物件的修改」（*p = ...），
//         對指標變數本身的賦值（p = ...）永遠只改到區域複本。
//         分清楚「改指標」與「改指標指向的東西」，這題就再也不會錯。
// ═══════════════════════════════════════════════════════════════════════════

#include <stdio.h>
#include <string.h>   /* strstr / strlen（C++ 中亦可寫 <cstring>） */

void swap(int* a, int* b) {
    int temp = *a;
    *a = *b;
    *b = temp;
}

/* ---------------------------------------------------------------------------
 * 【LeetCode 實戰範例】LeetCode 344. Reverse String
 *   題目：就地（in-place）反轉字元陣列 s，不可額外配置陣列。
 *   為什麼用到本主題：這題的核心就是「反覆交換頭尾兩個元素」，
 *         正是 swap 的標準應用。C 沒有引用，只能傳指標，
 *         所以這裡用 &s[left] / &s[right] 明確取址——
 *         和 14.cpp 的 C++ 引用版對照，可以清楚看到兩者的差別。
 *   複雜度：時間 O(n)，空間 O(1)。
 * ------------------------------------------------------------------------- */
void swapChar(char* a, char* b) {
    char temp = *a;
    *a = *b;
    *b = temp;
}

void reverseString(char* s, int n) {
    int left = 0;
    int right = n - 1;
    while (left < right) {
        swapChar(&s[left], &s[right]);   /* 必須明確取址 */
        left++;
        right--;
    }
}

/* ---------------------------------------------------------------------------
 * 【日常實務範例】解析設定字串 "width=1920" 並「同時回傳兩個值」
 *   情境：C 的函式只能回傳一個值。要同時回傳「解析是否成功」與
 *         「解析出來的數字」，標準作法就是——回傳值給狀態碼，
 *         用指標參數把結果寫回呼叫端。這是 C API 最典型的形狀
 *         （strtol、sscanf、POSIX 的絕大多數函式都是這個模式）。
 *   注意這裡有做 NULL 檢查，這是正式程式碼該有的防禦。
 * ------------------------------------------------------------------------- */
int parseIntSetting(const char* text, const char* key, int* outValue) {
    const char* p;
    size_t keyLen;

    if (text == NULL || key == NULL || outValue == NULL) {
        return 0;                      /* 防禦：任一指標為 NULL 就視為失敗 */
    }

    p = strstr(text, key);
    if (p == NULL) return 0;           /* 找不到 key */

    keyLen = strlen(key);
    if (p[keyLen] != '=') return 0;    /* key 後面不是 '='，格式不符 */

    /* sscanf 本身也是「用指標把結果寫回呼叫端」的經典例子 */
    if (sscanf(p + keyLen + 1, "%d", outValue) != 1) return 0;

    return 1;                          /* 成功 */
}

int main() {
    int x = 10, y = 20;
    swap(&x, &y);  // 必須傳遞位址
    printf("x = %d, y = %d\n", x, y);

    printf("\n=== LeetCode 344. Reverse String ===\n");
    {
        char s[] = "hello";
        printf("  before: %s\n", s);
        reverseString(s, 5);
        printf("  after : %s\n", s);
    }

    printf("\n=== 日常實務：用指標參數回傳解析結果 ===\n");
    {
        int width = 0;
        int height = 0;

        if (parseIntSetting("width=1920;height=1080", "width", &width)) {
            printf("  width  解析成功: %d\n", width);
        } else {
            printf("  width  解析失敗\n");
        }

        if (parseIntSetting("width=1920;height=1080", "height", &height)) {
            printf("  height 解析成功: %d\n", height);
        } else {
            printf("  height 解析失敗\n");
        }

        if (parseIntSetting("width=1920", "depth", &x)) {
            printf("  depth  解析成功: %d\n", x);
        } else {
            printf("  depth  解析失敗（key 不存在，x 未被修改）\n");
        }
    }

    printf("\n=== 對照 ===\n");
    printf("  C   ：swap(&x, &y)  呼叫端有 & 當「我可能被改」的標記\n");
    printf("  C++ ：swap(x, y)    語法更簡潔，但呼叫端看不出來（見 14.cpp）\n");

    return 0;
}

