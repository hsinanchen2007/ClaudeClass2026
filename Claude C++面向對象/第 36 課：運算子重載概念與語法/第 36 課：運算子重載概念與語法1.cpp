// =============================================================================
//  第 36 課：運算子重載概念與語法（1） — 「還沒有重載」的樣子:問題長什麼樣
// =============================================================================
//
// 【主題資訊 Information】
//   本檔是本課的「前情提要」:一個完全沒有任何運算子重載的類別。
//   它可以編譯、可以執行,但 a + b、a == b 這類寫法一律無法通過編譯。
//   標準版本：C++98 起皆同(本檔以 C++17 編譯)
//   標頭檔  ：<iostream>
//
//   ★ 本檔可以正常編譯執行。main 裡被註解掉的那兩行是刻意保留的,
//     一旦取消註解就會編譯失敗 —— 那正是本檔要示範的重點。
//     實際驗證過的錯誤訊息附在檔案末尾。
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼 a + b 對自訂類別是編譯錯誤,而不是執行期錯誤】
//   編譯器看到 a + b 時,會先看兩個運算元的型別,然後做重載解析:
//     - 內建型別(int、double、指標)→ 語言內建規則,直接生成指令。
//     - 類別型別 → 到「候選函式集合」裡找名為 operator+ 的函式。
//   Vector2D 沒有任何 operator+,候選集合是空的 → 重載解析找不到可行解,
//   編譯器在「編譯期」就宣告失敗。這是靜態型別的優點:
//   沒有定義的運算不會被默默猜測,也不會拖到執行期才爆炸。
//   實測的錯誤訊息是:
//       error: no match for 'operator+' (operand types are 'Vector2D' and 'Vector2D')
//   「no match」這三個字就是在說:我找過了,沒有任何 operator+ 適用。
//
// 【2. 為什麼編譯器不能「自己推測」逐分量相加】
//   對人類來說「兩個向量相加 = x 加 x、y 加 y」很直覺,但編譯器不能這樣猜:
//     - 若類別是 Matrix,+ 該是逐元素相加還是矩陣運算?
//     - 若類別是 Date,Date + Date 根本沒有意義(Date + int 才有)。
//     - 若類別是 BankAccount,自動生成的相加會直接製造財務 bug。
//   語意只有型別的作者知道。C++ 的選擇是:預設什麼都不生成,你要就自己寫。
//
// 【3. 沒有重載時,你只能怎麼寫】
//   就是本檔 main 裡示範的樣子:手動把每個分量拆開來算,再自己組回去。
//   兩個分量還可以忍受;等到 Vector3D、Matrix4x4、Complex、Money 都這樣寫,
//   呼叫端就會被大量與業務無關的細節淹沒。運算子重載要解決的正是這件事:
//   把「怎麼算」收進型別裡,讓呼叫端只剩下「算什麼」。
//   請把本檔的 main 和下一份檔案(...語法2.cpp)的 main 並排比較,差異一目了然。
//
// 【概念補充 Concept Deep Dive】
//   C++ 會為類別「自動生成」的成員函式只有這幾個:
//     預設建構子(僅在沒有任何其他建構子時)、解構子、
//     複製建構子、複製賦值、移動建構子、移動賦值。
//   除此之外的所有運算子 —— +、-、*、/、==、<、[]、() —— 一律不會自動生成。
//   Vector2D 這裡自己寫了一個雙參數建構子,連預設建構子也不會被生成了,
//   所以 Vector2D v; 同樣會編譯失敗。這個「你一旦自己寫,編譯器就不再幫忙」
//   的規則,是 C++ 特殊成員函式一致的行為模式。
//   (C++20 起多了一個例外:寫了 operator<=> 可以自動生成 < > <= >=,
//    寫了 == 可以自動生成 !=,但那是你明確要求後才生成,不是憑空推測。)
//
// 【注意事項 Pay Attention】
//   1. main 裡被註解的兩行取消註解後會編譯失敗,這不是本檔的 bug,是教學目的。
//      失敗發生在編譯期,不是執行期 —— 這是靜態型別語言的價值所在。
//   2. 本檔的成員是 public 的,方便示範「手動存取分量」。
//      正式程式碼應該把資料設為 private,再透過運算子或存取器操作。
//   3. Vector2D 因為自訂了建構子,不再有隱式的預設建構子,
//      寫 Vector2D v; 也會編譯失敗(要寫 Vector2D v{0, 0};)。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】沒有運算子重載時會發生什麼
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 對一個沒有定義 operator+ 的類別寫 a + b,錯誤發生在編譯期還是執行期?
//        編譯器的錯誤訊息大致是什麼?
//     答：編譯期。運算子的解析是重載解析的一部分,完全在編譯階段完成。
//         g++ 的訊息是
//         「no match for 'operator+' (operand types are 'Vector2D' and 'Vector2D')」,
//         意思是候選函式集合裡找不到可行的 operator+。
//     追問：為什麼編譯器不乾脆自動幫我逐成員相加?
//         → 因為 + 的語意只有型別作者知道。Date + Date 沒有意義、
//           Matrix + Matrix 有多種可能解釋、BankAccount 自動相加會製造財務 bug。
//           猜錯的代價遠大於「要你多寫一個函式」。
//
// ⚠️ 陷阱. 編譯器會自動生成 operator= 和複製建構子,為什麼不順便生成 operator==?
//     答：因為兩者的「預設語意」可信度差很多。複製一個物件,
//         「逐成員複製」幾乎總是對的;但「相等」在不同型別有截然不同的定義 ——
//         有的要忽略快取欄位、有的要用浮點容差、有的只比 id。
//         猜錯的相等判斷會造成極難追查的邏輯錯誤,所以標準選擇不猜。
//     為什麼會錯：把「編譯器會生成特殊成員函式」過度推廣成
//         「編譯器什麼都會生成」。實際上自動生成的名單很短且固定:
//         預設建構子、解構子、複製/移動建構子、複製/移動賦值,就這些。
//         (C++20 起可以用 = default 明確要求生成 == 和 <=>,但那是你開口要,
//          不是編譯器主動猜。)
// ═══════════════════════════════════════════════════════════════════════════
//
// 【本檔不加 LeetCode／實務範例的理由】
//   本檔的全部內容就是「示範一個編譯錯誤」,有效程式碼不到 10 行。
//   掛上演算法題或實務情境只會稀釋這個唯一的重點。
//   本課的 LeetCode(179. Largest Number)與實務範例(Money 金額型別)
//   放在同目錄的 summary.cpp。

#include <iostream>

class Vector2D {
public:
    double x, y;
    Vector2D(double x, double y) : x(x), y(y) {}
};

int main() {
    Vector2D a(1.0, 2.0);
    Vector2D b(3.0, 4.0);

    // ★ 以下兩行取消註解就會「編譯失敗」（實測錯誤訊息見檔案末尾）
    // Vector2D c = a + b;  // ❌ 編譯錯誤！編譯器不知道如何 "+" 兩個 Vector2D
    // if (a == b) {}       // ❌ 編譯錯誤！編譯器不知道如何比較

    // 沒有運算子重載時，只能像這樣把每個分量拆開來手算：
    std::cout << "=== 沒有運算子重載時，只能手動逐分量處理 ===\n";
    Vector2D sum(a.x + b.x, a.y + b.y);          // 想寫 a + b，只能寫成這樣
    std::cout << "  a        = (" << a.x << ", " << a.y << ")\n";
    std::cout << "  b        = (" << b.x << ", " << b.y << ")\n";
    std::cout << "  手動相加 = (" << sum.x << ", " << sum.y << ")\n";

    bool equal = (a.x == b.x && a.y == b.y);     // 想寫 a == b，只能寫成這樣
    std::cout << "  手動比較 a == b ? " << (equal ? "true" : "false") << "\n";

    std::cout << "\n=== 為什麼這樣不好 ===\n";
    std::cout << "  1. 呼叫端被迫知道 Vector2D 內部有哪些分量（破壞封裝）\n";
    std::cout << "  2. 每多一個分量，所有呼叫端都要跟著改\n";
    std::cout << "  3. 比較邏輯散落各處，改一個地方就會漏掉其他地方\n";
    std::cout << "  → 下一份檔案（...語法2.cpp）用 operator+ 解決這三點\n";

    return 0;
}

