// =============================================================================
//  第 19 課 範例 6  —  delete nullptr 是安全的，delete 兩次不是
// =============================================================================
//
// 【主題資訊 Information】
//   規則    ：對空指標執行 delete / delete[] 是「合法且無效果」的操作
//   標準版本：C++98 起即如此規定（[expr.delete]）
//   標頭檔  ：本範例僅需 <iostream>
//   複雜度  ：delete nullptr 為 O(1)，實際上什麼都不做
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼標準要讓 delete nullptr 合法】
//   因為這讓「清理程式碼」可以寫得單純。設想解構函數要釋放五個可能存在、
//   也可能不存在的資源：若 delete 不接受空指標，每一個都得先寫 if 判斷；
//   而只要規定「空指標就什麼都不做」，那五行 if 全部消失。
//   同樣的設計哲學也出現在 C 的 free(NULL) —— 兩者都是刻意為之的便利規定。
//   結論很明確：
//       if (p) delete p;    // 這個 if 是多餘的
//       delete p;           // 直接寫就好
//
// 【2. 真正危險的是另外兩件事】
//   (a) double free：對同一個非空指標 delete 兩次。
//       第一次 delete 之後，那塊記憶體已經還給配置器，
//       第二次 delete 會去動一塊不再屬於你的記憶體，破壞配置器的內部結構。
//       這是未定義行為 —— 標準不保證任何結果，可能立刻崩潰、
//       可能靜靜地毀損堆積，也可能一切看似正常直到很久以後才爆炸。
//   (b) use after free：delete 之後繼續透過該指標讀寫。同樣是未定義行為。
//   本檔對這兩者「只以文字說明、絕不實際執行」。
//
// 【3. 為什麼「delete 後設為 nullptr」是好習慣】
//   把這兩條規則接起來就是答案：
//       delete 空指標 = 安全  ＋  delete 兩次 = 未定義行為
//       ⇒ delete 之後立刻把指標設為 nullptr，
//          萬一同一個指標被誤刪第二次，那第二次就變成了「安全的無效果操作」。
//   等於用一行賦值，把一個未定義行為轉化成一個無害操作。
//   同理，設為 nullptr 之後，誤用該指標也比較容易當場發現
//   （解參考空指標通常會立即崩潰，比讀到看似合理的垃圾值好除錯得多）。
//
// 【概念補充 Concept Deep Dive】
//   `delete p` 實際上是兩個步驟：
//     (1) 呼叫 p 所指物件的解構函數；
//     (2) 呼叫 operator delete 把記憶體還給配置器。
//   當 p 是空指標時，這兩步都被跳過 —— 所以它不只是「不崩潰」，
//   而是「完全沒有任何效果」，連解構函數都不會被呼叫。
//
//   要注意的是，「delete 後設為 nullptr」只對「你手上這一個指標變數」有效。
//   如果有多個指標指向同一塊記憶體（別名），把其中一個設為 nullptr，
//   其他幾個仍然是懸空指標，照樣可能造成 double free。
//   要從根本解決所有權混亂，答案是 std::unique_ptr（唯一所有權）
//   或 std::shared_ptr（共享所有權、引用計數歸零才釋放）。
//
// 【注意事項 Pay Attention】
// 1. double free 與 use after free 都是未定義行為，
//    標準不保證任何特定結果，不可寫成「一定會崩潰」。
//    本檔全程未執行這兩者。
// 2. new 配 delete、new[] 配 delete[]，混用是未定義行為。
//    對空指標而言兩者都安全，但別因此養成混用的習慣。
// 3. 「delete 後設為 nullptr」無法解決多個指標互為別名的情況。
// 4. 這個習慣在區域變數即將離開 scope 時意義不大（沒有人會再用到它），
//    真正有價值的場合是「類別的成員指標」與「生命週期較長的指標」。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】delete 的安全性
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. delete 一個空指標會發生什麼事？需要先檢查嗎？
//     答：什麼都不會發生，這是標準明確規定的合法操作（[expr.delete]）：
//         空指標時解構函數與 operator delete 都會被跳過。
//         因此 `if (p) delete p;` 中的判斷是多餘的，直接寫 `delete p;` 即可。
//     追問：那 delete 同一個非空指標兩次呢？
//         → 未定義行為。第一次之後記憶體已歸還配置器，
//           第二次會破壞配置器的內部結構，可能立即崩潰、
//           也可能靜默毀損堆積而在很久之後才顯現。標準不保證任何結果。
//
// ⚠️ 陷阱. 「我每次 delete 完都把指標設成 nullptr 了，
//           所以我的程式不可能發生 double free。」
//     答：不成立。設為 nullptr 只保護「你手上這一個指標變數」。
//         如果先前把這個位址複製給了別的指標（或存進容器、傳給了別的物件），
//         那些別名仍然指向已釋放的記憶體，透過它們再 delete 一次
//         照樣是 double free。
//     為什麼會錯：把「指標的值」當成「資源的所有權」。
//         設 nullptr 只是清掉一份值的副本，並沒有告訴其他持有者
//         「這塊記憶體已經沒了」。真正的解法是讓所有權在型別層次上明確：
//         用 std::unique_ptr 表達「只有一個擁有者」（複製會直接編譯失敗），
//         或用 std::shared_ptr 讓引用計數歸零時才釋放。
// ═══════════════════════════════════════════════════════════════════════════
//
// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】—— 本檔不附
//   理由：本檔是一條語言規則的最小示範（delete 空指標的行為），
//   不涉及任何演算法。LeetCode 題目不會考「delete 空指標安不安全」。
//   依規格「寧缺勿濫」從缺；本課的智能指標修法請見同課範例 9。
// -----------------------------------------------------------------------------

#include <iostream>
using namespace std;

int main() {
    cout << "=== delete nullptr ===" << endl;
    
    int* p = nullptr;
    delete p;       // 完全安全！不會崩潰, 也不會有任何效果
    cout << "  delete nullptr 是安全的" << endl;
    
    int* arr = nullptr;
    delete[] arr;   // 也是安全的, 不會崩潰, 也不會有任何效果
    cout << "  delete[] nullptr 也是安全的" << endl;
    
    // 但是 delete 同一個指標兩次是未定義行為！可能會崩潰！
    cout << "\n  // int* q = new int(42);" << endl;
    cout << "  // delete q;   ← 第一次 OK" << endl;
    cout << "  // delete q;   ← 第二次：未定義行為！可能崩潰！" << endl;
    
    // 好習慣：delete 後把指標設為 nullptr, 避免誤刪同一個指標兩次, 這樣即使第二次 delete 也安全（因為是 nullptr）
    int* safe = new int(42);
    delete safe;
    safe = nullptr;    // 設為 nullptr
    delete safe;       // 再次 delete 也安全（因為是 nullptr）
    cout << "\n  好習慣：delete 後設為 nullptr" << endl;
    
    return 0;
}

