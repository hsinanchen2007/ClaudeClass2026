// =============================================================================
//  第 19 課 範例 8  —  用區域物件管理動態記憶體（RAII 的最小可運作示範）
// =============================================================================
//
// 【主題資訊 Information】
//   標準版本：C++98 起即可實作 RAII；本檔以 C++17 編譯
//   標頭檔  ：<iostream>、<string>
//   核心概念：RAII（Resource Acquisition Is Initialization）
//             建構時取得資源、解構時釋放資源，
//             把「資源的生命週期」綁到「物件的生命週期」上
//
// 【詳細解釋 Explanation】
//
// 【1. RAII 解決的是什麼問題】
//   範例 7 示範了四種洩漏，其中「提前 return」與「例外中斷」的共通點是：
//   離開函式的路徑不只一條，而手寫的 delete 只守得住其中一條。
//   RAII 的想法是把釋放動作交給「區域物件的解構函數」，
//   因為編譯器保證每一條離開 scope 的路徑
//   （正常結束、return、break、以及例外造成的堆疊展開）
//   都會執行解構函數 —— 你不可能漏掉任何一條，那不是你在寫的程式碼。
//   本檔的 safeFunction 就是直接驗證這件事：
//   正常結束與提前 return 兩條路徑，都印出了 [解構]。
//
// 【2. 例外安全是免費附贈的】
//   本檔第三段刻意在 MyString 存活期間擲出例外。
//   輸出顯示 [解構] 出現在 catch 的訊息「之前」——
//   這證明堆疊展開時解構函數確實被執行了，記憶體沒有洩漏。
//   對照範例 7 的場景 4（裸指標 + 例外）：那裡的資源就漏掉了。
//   同樣的程式結構，差別只在「資源是由區域物件持有，還是由裸指標持有」。
//
// 【3. MyString 做了什麼】
//   它是 std::string 的極簡版：建構時 new char[]，解構時 delete[]。
//   注意 `new char[length + 1]` 的 +1 是給結尾的 '\0'，
//   而複製迴圈用 `i <= length` 也是為了把那個 '\0' 一起複製進去。
//   這兩處是手寫字串類別最經典的 off-by-one 出錯點。
//
// 【概念補充 Concept Deep Dive】
//   ⚠️ 本檔的 MyString 其實「違反了 Rule of Three」，值得特別留意：
//   它以裸指標持有資源、並自訂了解構函數，卻沒有自訂複製建構函數與
//   複製賦值運算子。編譯器產生的預設版本只會做淺複製（把 data 指標抄一份），
//   於是兩個 MyString 會指向同一塊 char 陣列，各自解構時各 delete[] 一次
//   —— 那是 double free，屬未定義行為。
//   本檔之所以安全，純粹是因為程式碼從頭到尾「沒有複製過任何 MyString」。
//   真要當成可用的類別，必須補上複製建構／複製賦值（Rule of Three），
//   或更好的做法：讓成員改用 std::unique_ptr<char[]>／std::vector<char>，
//   走 Rule of Zero，一個特殊成員都不必寫。
//   （這正是同課範例 9 的主題。）
//
//   另外，解構函數裡的 `data = nullptr;` 在這裡其實沒有實質作用 ——
//   物件馬上就要消失了，沒有人會再讀取這個成員。
//   它是一種防禦性習慣，在較長壽的物件上才有意義。
//
// 【注意事項 Pay Attention】
// 1. new[] 必須配 delete[]，new 必須配 delete。混用是未定義行為。
//    本檔的 data 是 new char[]，因此解構函數用的是 delete[]。
// 2. 本檔的 MyString 違反 Rule of Three（見上），
//    只因程式中未發生任何複製才得以安全運作，不可直接拿去用。
// 3. length 計算的是「位元組數」而不是「字元數」。
//    第三段的中文字串在輸出中顯示長度 18，正是因為 UTF-8 下
//    每個中文字佔 3 個位元組（6 字 × 3 = 18）。
//    這也是 std::string::size() 的行為 —— 它從來就不是「字元數」。
// 4. 例外安全來自「區域物件」這個性質。
//    若把 MyString 本身用 new 建立成動態物件，就又回到了需要手動 delete
//    的處境，RAII 的保護也就消失了。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】RAII 與例外安全
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼說 RAII 讓程式碼「例外安全」？
//     答：因為例外傳播時會進行堆疊展開（stack unwinding），
//         過程中會依反序執行所有已建構區域物件的解構函數。
//         資源既然由區域物件持有，釋放動作就一定會發生 ——
//         不論函式是正常結束、提前 return，還是被例外中斷。
//         手寫 delete 則只守得住你有寫到的那條路徑。
//     追問：那如果解構函數本身擲出例外呢？
//         → C++11 起解構函數預設是 noexcept，例外若逸出會呼叫
//           std::terminate()。注意這是標準規定的結果，不是未定義行為。
//           所以解構函數內必須自己吞掉例外，不可讓它逸出。
//
// ⚠️ 陷阱. 「這個 MyString 有解構函數會自動 delete[]，
//           所以它是一個安全、可以直接拿去用的類別。」
//     答：不是。它以裸指標持有資源又自訂了解構函數，卻沒有處理複製語意，
//         違反 Rule of Three。只要寫一行 `MyString b = a;`，
//         兩個物件就會指向同一塊 char 陣列，各自解構時各 delete[] 一次
//         —— double free，未定義行為。
//         本檔之所以能安全執行，只是因為它從未複製過 MyString。
//     為什麼會錯：把「有解構函數」等同於「資源管理正確」。
//         實際上自訂解構函數正是一個訊號：這個類別擁有資源，
//         因此必須同時決定「被複製時該怎麼辦」。
//         Rule of Three／Five 講的就是這件事，
//         而 Rule of Zero（改用 unique_ptr 等 RAII 成員）能讓你完全不必面對它。
// ═══════════════════════════════════════════════════════════════════════════
//
// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】—— 本檔不附
//   理由：本檔主題是「資源釋放的時機保證與例外安全」，屬於資源管理機制。
//   LeetCode 評測演算法的輸入輸出，不會因為資源在哪一行釋放而有不同答案。
//   依規格「寧缺勿濫」從缺——本檔的 safeFunction（多條離開路徑）
//   與例外安全示範本身就是最貼近真實工程的用例。
// -----------------------------------------------------------------------------

#include <iostream>
#include <string>
using namespace std;

// ============================================================
// 一個簡單的動態字串類別（模擬 std::string 的核心概念）
// ============================================================
class MyString {
private:
    char* data;
    int length;

public:
    // 建構：分配記憶體, 複製字串
    MyString(const char* str = "") {
        length = 0;
        while (str[length] != '\0') length++;
        
        data = new char[length + 1];   // +1 給 '\0'
        for (int i = 0; i <= length; i++) {
            data[i] = str[i];
        }
        
        cout << "  [建構] MyString: \"" << data << "\" (長度: " 
             << length << ")" << endl;
    }
    
    // 解構：自動釋放記憶體, 避免洩漏
    ~MyString() {
        cout << "  [解構] MyString: \"" << data << "\"" << endl;
        delete[] data;     // 自動清理！
        data = nullptr;
    }
    
    void print() const {
        cout << "  \"" << data << "\"" << endl;
    }
    
    int getLength() const { return length; }
};

// 不管怎麼離開這個函數，MyString 都會自動清理
void safeFunction(bool earlyReturn) {
    MyString greeting("Hello, C++!");
    
    if (earlyReturn) {
        cout << "  提前返回..." << endl;
        return;   // greeting 自動解構，記憶體自動釋放, 不會洩漏
    }
    
    greeting.print();
    // greeting 在函數結束時自動解構. 不管是正常結束還是提前返回，都不會洩漏記憶體
}

int main() {
    cout << "=== 用局部對象管理記憶體 ===" << endl;
    
    cout << "\n--- 正常流程 ---" << endl;
    safeFunction(false);
    
    cout << "\n--- 提前返回 ---" << endl;
    safeFunction(true);
    
    cout << "\n--- 異常安全 ---" << endl;
    try {
        MyString msg("即將拋出異常");
        throw runtime_error("boom!");
        // msg 在異常傳播過程中自動解構（堆疊展開），記憶體自動釋放，不會洩漏
    } catch (...) {
        cout << "  異常已捕獲，記憶體已自動清理" << endl;
    }
    
    cout << "\n=== 完成 ===" << endl;
    return 0;
}

