// =============================================================================
//  第 19 課 範例 7  —  記憶體洩漏的四種典型場景
// =============================================================================
//
// 【主題資訊 Information】
//   標準版本：C++98 起；解法（unique_ptr）為 C++11、make_unique 為 C++14
//   標頭檔  ：<iostream>、<string>；解法需要 <memory>
//   定義    ：記憶體洩漏 = 用 new 配置了記憶體，卻永遠失去了釋放它的機會
//
// 【詳細解釋 Explanation】
//
// 【1. 洩漏的代價不只是「記憶體變少」】
//   最常被忽略的一點：物件洩漏時，它的「解構函數永遠不會被呼叫」。
//   如果那個解構函數負責關閉檔案、釋放資料庫連線、解開鎖、
//   送出結算封包，這些副作用就全部不會發生。
//   本檔用 Resource 的 [-] 輸出把這件事具體化：
//   洩漏的物件，你不會看到它的 [-]。
//
// 【2. 四種典型場景】
//   (1) 忘記 delete —— 最單純的一種，程式碼一長就容易漏。
//   (2) 覆蓋指標 —— r 先指向 A，接著 r = new B。
//       A 的位址從此再也沒有人知道，即使你後來 delete r 也只釋放了 B。
//       這種洩漏特別隱蔽，因為程式碼裡「明明有 delete」。
//   (3) 提前 return —— delete 寫在函式尾端，但中間有一條 return 跳過了它。
//       錯誤處理路徑越多，漏掉的機率越高。
//   (4) 例外中斷 —— 例外從 new 與 delete 之間擲出，
//       堆疊展開會跳過後面的 delete。這條路徑最容易被遺忘，
//       因為它在原始碼上「看不見」。
//
// 【3. 為什麼 (3)(4) 特別能說明 RAII 的價值】
//   場景 3 與 4 的共通點是：離開函式的路徑不只一條，
//   而手寫的 delete 只守得住其中一條。
//   RAII 的解法不是「更小心地在每個出口補上 delete」，而是
//   「把釋放動作交給區域物件的解構函數」——
//   因為編譯器保證每一條離開 scope 的路徑（含例外展開）
//   都會執行解構函數，你不可能漏掉任何一條，那本來就不是你在寫的程式碼。
//   把本檔四個場景的 `Resource*` 換成 `std::unique_ptr<Resource>`，
//   四種洩漏會同時消失：
//     (1) 不必寫 delete；
//     (2) 對 unique_ptr 重新賦值會先釋放舊物件；
//     (3)(4) 離開 scope 一律自動釋放。
//
// 【概念補充 Concept Deep Dive】
//   行程結束時，作業系統會回收該行程的全部記憶體，
//   所以「短命的小工具程式漏一點記憶體」通常不會造成實害。
//   但這個說法有三個重要的但書：
//     (a) 長期執行的服務（daemon、遊戲主迴圈、伺服器）會持續累積，
//         最終被 OOM killer 終止；
//     (b) OS 回收的是記憶體，「不會」幫你呼叫解構函數 ——
//         未寫入的檔案緩衝、未提交的交易、未送出的封包就這樣消失了；
//     (c) 洩漏會掩蓋真正的所有權設計問題，讓程式碼越來越難維護。
//   所以「反正結束時會回收」不是可以接受洩漏的理由。
//
// 【注意事項 Pay Attention】
// 1. 本檔是「錯誤示範」，刻意保留四處洩漏，程式可以正常編譯執行。
//    請對照輸出：有 [+] 卻沒有對應 [-] 的，就是洩漏掉的物件。
// 2. leak1 中的 `(void)r;` 只是為了讓 -Wall -Wextra 不發出
//    unused-variable 警告，它不影響洩漏本身（那正是本例要示範的）。
// 3. 場景 4 的例外由 main 的 try-catch 接住，
//    程式因此能正常結束 —— 但那個 Resource 已經漏掉了。
// 4. 真實專案應以 Valgrind、AddressSanitizer（-fsanitize=address）
//    或 LeakSanitizer 自動偵測這類問題，不要靠肉眼比對 [+]/[-]。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】記憶體洩漏
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 除了「忘記寫 delete」，還有哪些情況會造成記憶體洩漏？
//     答：至少三種同樣常見：
//         (a) 覆蓋指標 —— 指標重新指向新物件，舊物件的位址就此遺失，
//             即使後面有 delete 也只釋放得到新的那個；
//         (b) 提前 return —— delete 在函式尾端，中間的錯誤處理路徑跳過了它；
//         (c) 例外中斷 —— 例外在 new 與 delete 之間擲出，堆疊展開跳過 delete。
//         後兩者的共通點是「離開路徑不只一條」，這正是 RAII 要解決的問題。
//     追問：那要怎麼一次解決全部四種？
//         → 改用 std::unique_ptr。不必寫 delete；重新賦值會自動釋放舊物件；
//           離開 scope（含例外展開）一律自動釋放。四種洩漏同時消失。
//
// ⚠️ 陷阱. 「程式結束時作業系統會把記憶體全部收回去，
//           所以小程式漏一點記憶體其實沒差。」
//     答：對「記憶體」而言部分正確，但對「資源」而言是錯的。
//         OS 回收的只是記憶體頁面，它「不會」呼叫你的解構函數。
//         如果那個解構函數要 flush 檔案緩衝、commit 交易、送出最後的封包、
//         解開跨行程的鎖，這些事就全部不會發生 —— 資料就這樣遺失了。
//         此外長期執行的服務會持續累積，最終被 OOM killer 終止。
//     為什麼會錯：把「記憶體洩漏」窄化成「記憶體變少」這一個面向，
//         忽略了 C++ 裡記憶體只是眾多資源的一種。
//         正確的心智模型是：洩漏一個物件 = 跳過它的解構函數 =
//         它負責的所有清理工作全部沒做。
// ═══════════════════════════════════════════════════════════════════════════
//
// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】—— 本檔不附
//   理由：本檔是資源管理的「錯誤示範」，四個場景都不是演算法問題。
//   LeetCode 評測輸入輸出正確性，不檢查是否洩漏（單次執行結束即回收）。
//   依規格「寧缺勿濫」從缺；正確的 RAII 修法請見同課範例 8 與範例 9。
// -----------------------------------------------------------------------------

#include <iostream>
#include <string>
using namespace std;

class Resource {
private:
    string name;
public:
    Resource(const string& n) : name(n) {
        cout << "  [+] " << name << endl;
    }
    ~Resource() {
        cout << "  [-] " << name << endl;
    }
};

void leak1_forget_delete() {
    cout << "\n--- 洩漏 1：忘記 delete ---" << endl;
    Resource* r = new Resource("被遺忘的資源");
    (void)r;   // 僅為避免 -Wunused-variable；刻意「不」delete，這就是洩漏本身
    // 忘記 delete r;
    // r 指向的記憶體永遠無法釋放
    // 程式結束時，r 的記憶體會被 OS 回收，但在此之前它一直佔用著資源，可能導致記憶體洩漏和資源浪費
}

void leak2_overwrite_pointer() {
    cout << "\n--- 洩漏 2：覆蓋指標 ---" << endl;
    Resource* r = new Resource("第一個資源");
    r = new Resource("第二個資源");  // r 指向新對象
    // 第一個資源的地址丟失了，永遠無法 delete！
    delete r;  // 只釋放了第二個
    // 第一個資源永遠無法釋放，造成洩漏
}

void leak3_early_return() {
    cout << "\n--- 洩漏 3：提前返回 ---" << endl;
    Resource* r = new Resource("可能洩漏的資源");
    
    bool error = true;  // 模擬錯誤
    if (error) {
        cout << "  發生錯誤，提前返回！" << endl;
        return;         // 直接返回，忘記 delete！
    }
    
    delete r;   // 這行永遠不會執行
    // r 的記憶體永遠無法釋放，造成洩漏
}

void leak4_exception() {
    cout << "\n--- 洩漏 4：異常中斷 ---" << endl;
    Resource* r = new Resource("異常中洩漏的資源");
    
    // 如果這裡拋出異常...
    throw runtime_error("模擬異常");
    
    delete r;   // 這行永遠不會執行
    // r 的記憶體永遠無法釋放，造成洩漏
}

int main() {
    cout << "=== 記憶體洩漏的常見場景 ===" << endl;
    
    leak1_forget_delete();
    leak2_overwrite_pointer();
    leak3_early_return();
    
    try {
        leak4_exception();
    } catch (const exception& e) {
        cout << "  捕獲異常: " << e.what() << endl;
    }
    
    cout << "\n=== 注意：以上有多個資源沒被解構 ===" << endl;
    return 0;
}

