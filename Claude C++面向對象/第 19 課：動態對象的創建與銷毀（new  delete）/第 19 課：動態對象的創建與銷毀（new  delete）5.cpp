// =============================================================================
//  第 19 課 範例 5  —  new 分配失敗時的兩種處理方式
// =============================================================================
//
// 【主題資訊 Information】
//   語法    ：T* p = new T[n];              // 失敗擲出 std::bad_alloc
//             T* p = new(std::nothrow) T[n]; // 失敗回傳 nullptr，不擲例外
//   標準版本：C++98 起；std::bad_alloc 定義於 <new>
//   標頭檔  ：<new>（bad_alloc、nothrow、set_new_handler）
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼 new 預設是「擲出例外」而不是「回傳 nullptr」】
//   C 的 malloc 失敗回傳 NULL，於是每次呼叫後都得寫一次檢查 ——
//   而真實世界的程式碼十次有九次沒寫。忘記檢查的結果是拿著 NULL 繼續用，
//   崩潰點離真正的錯誤現場十萬八千里，極難除錯。
//   C++ 改用例外，把「失敗」變成一件無法被忽略的事：
//   你不處理，它就一路往上傳播，最壞情況是乾淨俐落地終止程式，
//   而不是帶著壞掉的指標繼續跑。
//   附帶好處是「正常路徑」變乾淨了：new 成功回傳的指標，保證不是 nullptr，
//   所以 `if (p == nullptr)` 對一般的 new 而言是永遠不會成立的死碼。
//
// 【2. nothrow 版本存在的理由】
//   有些場合不能用例外：嵌入式／核心程式關閉了例外機制、
//   或是在解構函數等不允許例外逸出的地方。
//   此時可用 `new(std::nothrow) T[n]`，行為退回 C 風格：失敗回傳 nullptr。
//   代價是你必須自己每次檢查 —— 也就是換回了 malloc 的那個老問題。
//   選擇原則：預設用會擲例外的版本；只有在確實不能用例外時才用 nothrow。
//
// 【3. 分配失敗前，還有一道 new_handler】
//   operator new 找不到記憶體時不會立刻放棄，它會先呼叫「目前的 new handler」
//   （用 std::set_new_handler 設定）。這個 handler 可以試著釋放一些快取、
//   然後返回讓 new 再試一次；若沒有設定 handler，才擲出 std::bad_alloc。
//   這給了大型程式一個「記憶體吃緊時先自救」的掛鉤點。
//
// 【概念補充 Concept Deep Dive】
//   `new T[n]` 在配置前會先計算 n * sizeof(T)。若這個乘法會溢位，
//   標準要求實作必須偵測並擲出例外（C++11 起明確為
//   std::bad_array_new_length，它繼承自 std::bad_alloc），
//   而不是靜靜地配置一塊過小的記憶體 —— 那會是嚴重的安全漏洞。
//   本檔第三段要求 1e12 個 int（約 4 TB），遠超可用位址空間與記憶體，
//   因此在本機被明確拒絕並擲出例外。
//
//   另外要注意 Linux 的「記憶體超額配置（overcommit）」：
//   在預設設定下，核心可能先答應一塊很大的配置、等到真正寫入時才發現不夠，
//   屆時由 OOM killer 直接砍掉行程 —— 那個情況「不會」擲出 bad_alloc。
//   所以「new 成功」不等於「這些記憶體真的都能用」，
//   這是雲端／容器環境下很常見的意外。本檔的 4 TB 之所以能被乾淨地擋下，
//   是因為它大到連位址空間都放不下，在配置階段就失敗了。
//
// 【注意事項 Pay Attention】
// 1. 一般 new 成功時保證回傳非 nullptr，所以檢查 nullptr 是多餘的；
//    只有 nothrow 版本才需要檢查。
// 2. new[] 必須配 delete[]，new 必須配 delete。混用是未定義行為。
// 3. e.what() 的訊息文字屬實作定義（本機為 "std::bad_alloc"），
//    不可用字串比對來判斷錯誤類型，要用型別去 catch。
// 4. catch 應使用 `const std::bad_alloc&`（依參考捕捉），
//    依值捕捉會多做一次複製，且對多型例外會造成物件切片。
// 5. 本檔在記憶體極度吃緊的機器上，第一、二段的小額配置理論上也可能失敗；
//    這是設計上可接受的，因為兩段都有正確的失敗處理路徑。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】new 的失敗處理
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. new 分配失敗會回傳 nullptr 嗎？
//     答：不會。一般的 new 失敗時擲出 std::bad_alloc，成功時保證回傳非空指標。
//         所以 `int* p = new int; if (p == nullptr) ...` 裡的判斷是死碼。
//         只有 `new(std::nothrow)` 版本才會在失敗時回傳 nullptr。
//     追問：那為什麼還要保留 nothrow 版本？
//         → 給不能使用例外的環境用（嵌入式、核心、關閉 -fexceptions 的建置），
//           或是在不允許例外逸出的地方（例如解構函數內）。
//
// 🔥 Q2. new 在擲出 bad_alloc 之前，還會做什麼事？
//     答：會先呼叫目前由 std::set_new_handler 設定的 new handler。
//         handler 可以釋放一些快取記憶體後返回，讓 operator new 再試一次；
//         若沒有設定 handler（或 handler 自己放棄），才擲出 std::bad_alloc。
//         這是大型程式在記憶體吃緊時「先自救」的標準掛鉤點。
//     追問：new handler 如果什麼都不做就直接 return，會怎樣？
//         → 會變成無窮迴圈：operator new 一直重試、一直失敗、一直呼叫 handler。
//           所以 handler 必須要嘛真的釋放出記憶體、要嘛擲出例外、
//           要嘛呼叫 set_new_handler(nullptr) 讓下一輪擲出 bad_alloc。
//
// ⚠️ 陷阱. 「只要 new 沒有擲出例外，就代表這塊記憶體確實可以安全使用。」
//     答：在 Linux 預設的記憶體超額配置（overcommit）策略下不成立。
//         核心可能先答應一塊很大的配置（new 順利返回），
//         等到你真正寫入那些頁面時才發現實體記憶體不夠，
//         此時由 OOM killer 直接終止行程 —— 全程不會有任何 bad_alloc。
//     為什麼會錯：把「配置成功」理解成「記憶體已經到手」。
//         實際上一般的配置只是拿到「虛擬位址空間」的承諾，
//         實體頁面要到第一次寫入時才真正對應。
//         這也是為什麼本檔要求 4 TB 反而能乾淨地收到 bad_alloc ——
//         它大到連位址空間都給不起，在配置階段就失敗了；
//         真正危險的是那些「剛好答應得下、卻用不完」的中等大小配置。
// ═══════════════════════════════════════════════════════════════════════════
//
