// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門8.cpp  —  >> 讀字串只會拿到「一個詞」
// =============================================================================
//
// 【主題資訊 Information】
//   template<class CharT, class Traits, class Alloc>
//   std::basic_istream<CharT,Traits>&
//   operator>>(std::basic_istream<CharT,Traits>& is,
//              std::basic_string<CharT,Traits,Alloc>& str);   // <string>
//
//   語意：跳過前導空白 → 讀取字元直到「遇到空白」或「達到 is.width()」
//         或「到達檔尾」為止；讀到的內容【取代】str 原有內容。
//   標準版本：C++98 起
//   複雜度  ：O(讀入的字元數)（含 string 的配置成本）
//   標頭檔  ：<iostream>、<string>
//
// 【詳細解釋 Explanation】
//
// 【1. 本檔要示範的就是「只拿到一個詞」】
//   輸入 "Hello World Everyone"，印出來的卻是 [Hello]。
//   這不是 bug，而是 operator>> 對 string 的定義本來就如此：
//   它是「格式化輸入」，而格式化輸入的分隔單位是【空白】。
//   剩下的 " World Everyone" 原封不動留在輸入緩衝區裡，
//   如果後面還有 >>，就會接著把 World 拿走。
//
// 【2. 空白到底包含哪些字元】
//   由目前套用的 locale 的 ctype facet 決定，
//   在預設的 "C" locale 下是這六個：
//       空格 ' '、水平定位 '\t'、換行 '\n'、
//       垂直定位 '\v'、換頁 '\f'、歸位 '\r'
//   所以「用 Tab 分隔」或「分成好幾行」對 >> 而言完全一樣。
//
// 【3. 前導空白會被跳過，尾隨空白會被留下】
//   這個不對稱是最容易搞混的地方：
//       輸入 "   Hello World"
//         → >> 先跳過三個空格，讀走 "Hello"，
//           停在 "Hello" 後面那個空格【之前】，
//           緩衝區剩下 " World"（前面那個空格還在）。
//   也就是說 >> 只負責「清掉開頭」，不負責「清掉結尾」。
//   這正是為什麼 >> 之後直接接 getline 會讀到空字串
//   —— 殘留的那個換行符還在緩衝區裡等著。
//
// 【4. 需要整行時該用什麼】
//   std::getline(std::cin, s) 讀到【換行】才停，因此空白會被保留。
//   兩者的分工可以這樣記：
//       >>       ：以「詞」為單位，適合讀數值與單一 token
//       getline  ：以「行」為單位，適合讀姓名、地址、句子
//   同課 9 號檔就是同一支程式改用 getline 的對照版，建議並排閱讀。
//
// 【概念補充 Concept Deep Dive】
//
// (A) width() 是唯一能限制 >> 讀取長度的機制
//   std::cin >> std::setw(10) >> str; 會最多讀 9 個字元（留一格給結尾）。
//   對 std::string 而言這只是截斷；但對 char 陣列而言，
//   不設 width 就是【緩衝區溢位】的經典漏洞：
//       char buf[8]; std::cin >> buf;   // 輸入超過 7 字元即溢位，UB
//   這是 C++ 中 std::string 遠比 char[] 安全的具體理由之一。
//   注意 width 在每次格式化輸入後會被重設為 0，不是永久設定。
//
// (B) 讀取會取代原有內容，不是附加
//   str 原本的內容會被整個換掉（等同 assign），
//   而且若新內容較短，capacity 通常【不會】縮小 ——
//   std::string 不會主動還記憶體給系統。
//   在迴圈中重複讀進同一個 string 因此是有效率的：
//   容量會逐漸長到足夠大，之後就不再配置。
//
// (C) 讀取失敗時 string 會變成空的
//   若在讀取前就已經到達檔尾，>> 會設定 failbit 與 eofbit，
//   並把 str 設為空字串（C++11 起明確規定）。
//   所以對 string 而言不存在「未初始化」問題 ——
//   std::string 的預設建構本來就是合法的空字串。
//   這一點和讀 int 的行為（見同課 6 號檔）完全不同，值得對照記憶。
//
// 【注意事項 Pay Attention】
//   1. >> 讀 string【永遠】只拿一個詞。需要整行請用 std::getline。
//   2. 前導空白被跳過、尾隨空白被留下，這個不對稱是混用 >> 與 getline
//      會出事的根源。
//   3. 對 char 陣列使用 >> 而不設 width 是緩衝區溢位漏洞；
//      對 std::string 則沒有這個風險（會自動增長）。
//   4. 輸入完全為空（直接 EOF）時，word 會是空字串、fail() 為真，
//      本檔沒有檢查，會印出 []。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】operator>> 讀取 std::string 的邊界
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::cin >> str 輸入 "Hello World"，str 的內容是什麼？剩下的去哪了？
//     答：str 是 "Hello"。>> 遇到空白就停止，
//         剩下的 " World"（含前面那個空格）仍留在輸入緩衝區，
//         下一次 >> 會讀到 "World"，或被 getline 一次讀走 " World"。
//         資料沒有消失，只是還沒被取走。
//     追問：那要怎麼一次讀整行？
//           → std::getline(std::cin, str)，它以換行為終止條件，
//             而且會把換行符從緩衝區【取走但不放進字串】。
//
// 🔥 Q2. 為什麼 char buf[8]; std::cin >> buf; 是安全漏洞，
//        但 std::string s; std::cin >> s; 不是？
//     答：>> 對 char 陣列不知道容量上限，會一路寫下去直到遇見空白，
//         輸入超過 7 個字元就寫出陣列邊界 → 緩衝區溢位（UB）。
//         std::string 的多載則會依需要自動增長容量，不會越界。
//         若真的要用 char 陣列，必須寫 std::cin >> std::setw(8) >> buf;。
//     追問：setw 設過一次之後還要再設嗎？
//           → 要。width 在每次格式化輸入／輸出之後就被重設為 0，
//             它是一次性的，不像 setprecision 那樣具有黏性。
//
// ⚠️ 陷阱. 既然 >> 會跳過空白，那 >> 之後接 std::getline 應該也會跳過
//          那個換行符吧？
//     答：不會。「跳過前導空白」是 operator>> 自己的行為，
//         getline【沒有】這個步驟 —— 它從當前位置直接讀到換行為止。
//         >> 停在換行符之前並把它留著，
//         所以緊接的 getline 會立刻讀到那個換行、回傳空字串。
//     為什麼會錯：把「跳過空白」誤認為是輸入串流的通用性質，
//         其實那是格式化輸入（>>）的規則，
//         而 getline 屬於非格式化輸入（unformatted input），
//         兩者遵循完全不同的規範。修法是在中間插入
//         std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
//         詳見同課 11 號檔。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>

int main() {
    std::string word;
    
    std::cout << "請輸入一段文字: ";
    std::cin >> word;
    
    std::cout << "你輸入的是: [" << word << "]" << std::endl;
    
    return 0;
}

