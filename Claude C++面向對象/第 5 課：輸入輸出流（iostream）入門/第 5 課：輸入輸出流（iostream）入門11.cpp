// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門11.cpp  —  混用 >> 與 getline 的換行殘留
// =============================================================================
//
// 【主題資訊 Information】
//   std::istream& ignore(std::streamsize count = 1,
//                        int_type delim = Traits::eof());   // <istream>
//   語意：丟棄至多 count 個字元；若在此之前先遇到 delim，
//         連 delim 一起丟棄後停止。
//   常見慣用法：
//       std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
//   標準版本：C++98 起（std::numeric_limits 在 <limits>）
//   複雜度  ：O(實際丟棄的字元數)
//   標頭檔  ：<iostream>、<string>、<limits>
//
// 【詳細解釋 Explanation】
//
// 【1. 問題是怎麼發生的】
//   輸入若是：
//       25⏎
//       王小明 先生⏎
//   則 std::cin >> age 的行為是：
//     • 跳過前導空白 → 讀走 "25" → 碰到 '\n' 就停
//     • 【'\n' 被留在緩衝區裡】，>> 不會消耗它
//   接著若直接呼叫 std::getline(std::cin, name)：
//     • getline 不跳空白，從當前位置讀起
//     • 當前位置正是那個 '\n' → 立刻視為「這一行結束了」
//     • name 得到【空字串】，而且看起來像「程式沒等我輸入就跳過了」
//   這是 C++ 初學者最常回報的「bug」，但它完全符合規範。
//
// 【2. ignore 的兩個參數各自在做什麼】
//       std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
//                       └────────── count ──────────┘        └ delim ┘
//   • count 給 max()，意思是「數量上不設限」。
//     這裡有一個特別規定：當 count 恰好是
//     std::numeric_limits<std::streamsize>::max() 時，
//     標準明訂【不計數】，純粹讀到 delim 為止。
//     所以這不只是「給一個很大的數字」，而是觸發一條專門的規則。
//   • delim 給 '\n'，表示讀到換行就停，而且【換行本身也被丟掉】。
//   兩者合起來的語意就是：「把這一行剩下的東西全部丟掉，含換行。」
//
// 【3. 為什麼不該用簡化版 std::cin.ignore()】
//   無參數版本等同 ignore(1)，只丟【一個】字元。
//   如果使用者輸入的是 "25   ⏎"（數字後面多打了幾個空格），
//   ignore() 只丟掉第一個空格，getline 仍會讀到剩下的空白 →
//   name 變成 "  "。本檔第 17~18 行把這個簡化版留為註解，
//   正是要讓兩種寫法並排對照。
//   結論：只有在你能【保證】殘留恰好一個字元時才可以用 ignore()，
//   而使用者輸入從來不該被「保證」。
//
// 【4. 有沒有不需要 ignore 的寫法】
//   有，而且通常更好：【全部都用 getline 讀，再自己轉型】。
//       std::string line;
//       std::getline(std::cin, line);
//       int age = std::stoi(line);          // 或用 istringstream
//   這樣輸入流的推進單位始終是「一行」，不會有殘留問題，
//   而且轉型失敗可以用 try/catch 精準處理。
//   混用 >> 與 getline 之所以麻煩，根源就在於兩者的推進單位不一致
//   —— 一個以「詞」為單位、一個以「行」為單位。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼要用 numeric_limits 而不是隨便填 10000
//   除了上述「max() 觸發不計數規則」之外，
//   填一個魔術數字意味著「超過這個長度的垃圾輸入就清不乾淨」。
//   使用者貼上一段超長文字時，殘留的部分會繼續污染後續讀取。
//   用 max() 才是語意正確的「清到行尾為止」。
//
// (B) ignore 會設定 eofbit
//   如果在遇到 delim 之前就到達檔尾，ignore 會設定 eofbit。
//   在互動程式中這通常不影響（使用者總會按 Enter），
//   但在用管線餵資料的測試中，最後一行沒有換行符時
//   就會讓後續讀取全部失敗。寫測試時要留意這一點。
//
// (C) 殘留問題不限於 getline
//   任何「非格式化輸入」接在 >> 後面都會踩到，包括
//   std::cin.get()、std::cin.read()、std::cin.peek()。
//   反過來說，>> 接 >> 完全沒問題，因為第二個 >> 會自己跳過前導空白。
//   判斷準則很簡單：【下一個動作會不會跳過空白？】不會的就要先清。
//
// 【注意事項 Pay Attention】
//   1. >> 之後接 getline 必須先 ignore，否則 getline 會讀到空字串。
//   2. 用 ignore(max(), '\n')，不要用無參數的 ignore()
//      —— 後者只丟一個字元，殘留多個空白時會失效。
//   3. numeric_limits 需要 #include <limits>，本檔已包含。
//   4. 更根本的解法是「一律用 getline 讀行，再自行解析」，
//      從源頭避免兩種推進單位混用。
//   5. ignore 在遇到 EOF 時會設定 eofbit，影響後續讀取。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】混用 >> 與 getline 的緩衝區殘留
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::cin >> age; 之後直接 std::getline(std::cin, name);
//        為什麼 name 會是空的？
//     答：>> 讀完數字後停在換行符【之前】，並把 '\n' 留在緩衝區。
//         getline 是非格式化輸入、不會跳過前導空白，
//         於是它從那個 '\n' 讀起，立刻判定「這一行結束了」，
//         回傳一個空字串。程式看起來像沒等使用者輸入就跳過去了。
//     追問：那要清掉幾個字元？
//           → 不能假設只有一個。使用者可能在數字後面多打空格，
//             所以要用 ignore(numeric_limits<streamsize>::max(), '\n')
//             清到行尾，而不是 ignore()。
//
// 🔥 Q2. ignore 的第一個參數為什麼要寫
//        std::numeric_limits<std::streamsize>::max()？
//     答：因為標準對這個值有【特別規定】：當 count 等於
//         numeric_limits<streamsize>::max() 時，ignore 不做數量限制，
//         純粹讀到 delim 為止。它不只是「一個很大的數」，
//         而是觸發一條專門的不計數規則。填魔術數字（如 10000）
//         則會在超長輸入時清不乾淨。
//     追問：這樣會不會很慢？
//           → 不會。它是丟棄而非複製，實作上通常直接在 streambuf
//             內部用 memchr 找 delim，成本與該行長度成正比。
//
// ⚠️ 陷阱. 那我把 ignore 放在【每次】讀取之前，是不是就一勞永逸了？
//     答：不行，反而會製造新的 bug。ignore 會【無條件】丟棄字元，
//         如果當下緩衝區是乾淨的，它就會把使用者接下來輸入的
//         那一整行吃掉。正確做法是「只在 >> 之後、getline 之前」清，
//         也就是只在【推進單位改變】的接縫處清。
//     為什麼會錯：把 ignore 當成「保險起見的清理」，
//         但它不是冪等操作 —— 沒東西可丟時它會去等新的輸入。
//         真正一勞永逸的做法不是到處加 ignore，
//         而是統一用 getline 讀行、再用 istringstream 解析，
//         讓輸入流永遠以「行」為單位前進。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <limits>

int main() {
    int age;
    std::string name;
    
    std::cout << "請輸入年齡: ";
    std::cin >> age;
    
    // 解決方案一：忽略緩衝區中剩餘的字元直到換行
    // std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n'); // 忽略直到換行符的所有字元
    std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
    
    // 解決方案二（簡化版）：忽略一個字元
    // std::cin.ignore();  // 忽略前一次輸入留下的換行符
    // std::cin.ignore();
    
    std::cout << "請輸入姓名: ";
    std::getline(std::cin, name);
    
    std::cout << "年齡: " << age << std::endl;
    std::cout << "姓名: [" << name << "]" << std::endl;
    
    return 0;
}

