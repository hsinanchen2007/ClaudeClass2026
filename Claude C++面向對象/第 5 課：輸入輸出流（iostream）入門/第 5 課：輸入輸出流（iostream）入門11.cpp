// 執行: printf '25\n王小明 先生\n' | ./io11

// 註 1:姓名刻意用「王小明 先生」（中間有空格）來證明 getline 的價值 ——
//      若改用 std::cin >> name，只會拿到「王小明」，「先生」會被留在緩衝區。
//
// 註 2:把第 14 行的 ignore 註解掉再跑一次，就會看到本課要示範的 bug：
//      姓名輸出變成 []（空字串），程式看起來像「沒等使用者輸入」。
//      本機已實測確認這個對照結果。
//
