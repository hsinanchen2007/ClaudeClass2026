// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門16.cpp  —  setw / left / right / setfill
// =============================================================================
//
// 【主題資訊 Information】
//   /*unspecified*/ setw(int n);               // <iomanip>，設定 width
//   /*unspecified*/ setfill(CharT c);          // <iomanip>，設定填充字元
//   std::ios_base& left (std::ios_base&);      // <ios>，靠左對齊
//   std::ios_base& right(std::ios_base&);      // <ios>，靠右對齊（預設）
//   std::ios_base& internal(std::ios_base&);   // 符號靠左、數值靠右
//
//   關鍵差異：width 是【一次性】的，fill 與對齊旗標是【黏性】的
//   預設值  ：width()=0（不填充）、fill()=' '、對齊為 right
//   標準版本：C++98 起
//   複雜度  ：O(欄位寬度)
//   標頭檔  ：<iostream>、<iomanip>
//
// 【詳細解釋 Explanation】
//
// 【1. 本檔的核心：setw 只對「下一個」輸出有效】
//   這是 iomanip 中最違反直覺、也最常被問的一點。
//       std::cout << std::setw(10) << 42 << "]" << std::endl;
//   setw(10) 只作用在 42 上面，後面的 "]" 完全不受影響。
//   若 setw 是黏性的，"]" 也會被填充成 10 個字元寬 —— 但實際上不會。
//   原因是標準明訂：每次【格式化輸出】完成後，width 會被重設為 0。
//   （注意是格式化輸出；width 對 unformatted 的 put/write 無效。）
//
// 【2. 為什麼偏偏 width 是一次性的】
//   因為它的語意是「這一欄要多寬」，本質上是【針對單一項目】的指示。
//   如果它是黏性的，那麼印完一個對齊欄位之後，
//   後續所有輸出（包括分隔符、換行、下一段文字）都會被莫名其妙地填充，
//   使用者必須記得手動 setw(0) 還原 —— 那才是災難。
//   相對地，fill 與對齊方式描述的是「整份輸出的風格」，
//   所以設計成黏性才合理。
//   記憶法：【描述「這一格」的是一次性；描述「整體風格」的是黏性】。
//
// 【3. 逐行對照本檔的輸出】
//     [        42]   ← setw(10)，預設 right，8 個空格 + "42"
//     [     Hello]   ← setw(10)，字串同樣靠右，5 個空格 + "Hello"
//     [42        ]   ← 加了 std::left，數字靠左
//     [Hello     ]   ← left 是【黏性】的，這行沒有再寫一次 left 也仍然靠左
//     [000042]       ← 改回 right + setfill('0') + setw(6)
//   第 4 行是關鍵證據：程式碼裡確實又寫了一次 std::left，
//   但即使拿掉它，輸出也完全相同 —— 因為對齊旗標具有黏性。
//
// 【4. setfill 的黏性會造成什麼問題】
//   本檔最後一行 std::cout << std::setfill(' '); 是【必要的清理】。
//   若省略它，之後所有用到 setw 的輸出都會被 '0' 填充，
//   例如印出 "0000Hello"。
//   這種「格式狀態污染」在大型程式裡極難追查，
//   因為出問題的地方離設定的地方可能隔了好幾個函式。
//   實務建議：需要臨時改格式時，用 copyfmt 存檔並在離開時還原，
//   或直接改用 C++20 的 std::format（完全不碰串流狀態）。
//
// 【概念補充 Concept Deep Dive】
//
// (A) width 對 std::string 與 char* 都有效，但不會截斷
//   setw(3) 印 "Hello" 會完整印出 "Hello"（5 個字元），不會被截成 "Hel"。
//   width 的語意是【最小】欄寬，不是最大 —— 它只補不砍。
//   要限制最大長度必須自己 substr，或用 C++20 的 std::format("{:.3}", s)。
//
// (B) internal 對齊的實際用途
//   std::internal 會把填充字元插在【符號與數值之間】：
//       std::cout << std::internal << std::setfill('0') << std::setw(6) << -42;
//       → -00042   （而不是 000-42）
//   這正是列印帶號金額或十六進位位址（0x0000ff）時需要的行為。
//
// (C) 為什麼中文字對齊常常會歪掉
//   width 計算的是【字元數（char 個數）】，不是顯示寬度。
//   一個中文字在 UTF-8 下佔 3 個 byte，
//   而 std::string 的每個 char 是 1 byte ——
//   所以 setw(10) 遇到中文會嚴重誤判。
//   本檔全部使用 ASCII 所以看不出問題，
//   但實務上要對齊含中文的表格，必須自己依「顯示寬度」計算，
//   標準庫並未提供這個功能。
//
// 【注意事項 Pay Attention】
//   1. setw 是【一次性】的，只影響下一個格式化輸出；
//      setfill 與 left/right 是【黏性】的，會一直有效。
//   2. width 只補不砍 —— 內容超過欄寬時完整印出，不會被截斷。
//   3. setfill 用完務必還原成 ' '，否則會污染後續所有輸出。
//   4. width 以 char 個數計算，處理中文（UTF-8 多位元組）時對齊會失準。
//   5. 這些狀態是掛在【串流物件】上的，不是全域的 ——
//      改 std::cout 不會影響 std::cerr。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】setw / setfill 的一次性與黏性
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::cout << std::setw(10) << 42 << "]" 中，"]" 會被填充成 10 格嗎？
//     答：不會。setw 設定的 width 是【一次性】的 ——
//         每次格式化輸出完成後，width 就被自動重設為 0。
//         所以只有 42 會被填充成 10 個字元寬，"]" 緊接著原樣印出。
//         這正是本檔輸出中 [        42] 的方括號能緊貼數字的原因。
//     追問：那 setfill 和 std::left 呢？
//           → 它們是【黏性】的，設定後持續有效直到被改變。
//             記憶法：描述「這一格多寬」的是一次性，
//             描述「整體風格」的是黏性。
//
// 🔥 Q2. std::cout << std::setw(3) << "Hello" 會印出什麼？
//     答：完整的 "Hello"，5 個字元。width 是【最小】欄寬不是最大 ——
//         它只在內容不足時補填充字元，內容超過時不會截斷。
//         想限制最大長度必須自己 substr，
//         或用 C++20 的 std::format("{:.3}", s)。
//     追問：那為什麼報表對齊常常還是會歪？
//           → 因為 width 算的是 char 個數。UTF-8 下一個中文字佔 3 個 char，
//             setw(10) 遇到中文會嚴重誤判。
//             標準庫沒有提供「顯示寬度」的概念，必須自己處理。
//
// ⚠️ 陷阱. 本檔最後一行 std::cout << std::setfill(' '); 看起來多餘
//          （後面已經沒有輸出了），可以刪掉嗎？
//     答：以這個只有 main 的小程式來說刪掉確實看不出差別，
//         但這是【非常糟糕的習慣】。setfill 是黏性的，
//         填充字元會一直留在 std::cout 上。
//         在真實程式裡，下一個用到 setw 的函式就會莫名其妙印出 "0000Hello"，
//         而問題根源可能在好幾個檔案之外。
//     為什麼會錯：把 std::cout 想成「每次使用都是乾淨的」。
//         實際上它是一個【全域可變狀態】，格式旗標會跨函式、跨檔案殘留。
//         這也是為什麼 C++20 引入 std::format ——
//         它完全不碰串流狀態，每次格式化都是獨立的，
//         從根本上消除了這類「格式污染」問題。
//         在還沒用上 C++20 的專案裡，正確做法是用
//         std::ios state(nullptr); state.copyfmt(std::cout); … 存檔還原。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <iomanip>

int main() {
    std::cout << "=== 欄位寬度與對齊 ===" << std::endl;
    
    // setw 只對下一個輸出有效
    std::cout << "[" << std::setw(10) << 42 << "]" << std::endl;
    std::cout << "[" << std::setw(10) << "Hello" << "]" << std::endl;
    
    // 靠左對齊
    std::cout << "[" << std::left << std::setw(10) << 42 << "]" << std::endl;
    std::cout << "[" << std::left << std::setw(10) << "Hello" << "]" << std::endl;
    
    // 使用填充字元
    std::cout << "[" << std::right << std::setfill('0') << std::setw(6) 
              << 42 << "]" << std::endl;
    
    // 恢復預設填充字元
    std::cout << std::setfill(' ');
    
    return 0;
}

