// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門7.cpp  —  用 >> 依序讀取不同型別的欄位
// =============================================================================
//
// 【主題資訊 Information】
//   std::istream& operator>>(std::istream&, std::string&);  // <string>
//   std::istream& operator>>(std::istream&, int&);          // <istream>
//   std::istream& operator>>(std::istream&, double&);       // <istream>
//   標準版本：C++98 起（本檔語法無任何新版特性）
//   複雜度  ：每次讀取 O(該欄位字元數)
//   標頭檔  ：<iostream>、<string>
//
// 【詳細解釋 Explanation】
//
// 【1. 同一個 >> 為什麼能讀出三種不同型別】
//   operator>> 是【多載】的：對 int、double、std::string 各有一份實作。
//   編譯器在編譯期就依右運算元的型別選定了要呼叫哪一個，
//   不是在執行期「看內容猜型別」。
//   這帶來一個重要後果：解析規則是【由變數型別決定的】，不是由輸入決定。
//     • 讀 int    → 只吃可構成整數的字元，碰到 '.' 就停
//     • 讀 double → 會吃小數點與指數符號（1e3 也算合法）
//     • 讀 string → 吃到第一個空白為止
//   所以同一份輸入餵給不同型別的變數，切法完全不同。
//
// 【2. 讀 std::string 的 >> 為什麼不能有空白】
//   operator>>(istream&, string&) 的定義是：
//   跳過前導空白 → 一直讀到「下一個空白」或「達到 width()」為止。
//   因此姓名若輸入 "王 小明"，name 只會拿到 "王"，
//   剩下的 "小明" 會留在緩衝區，接著被 >> age 拿去解析 → 失敗。
//   要讀含空白的整行，必須用 std::getline（見同課 9 號檔）。
//
// 【3. 三個提示字串為什麼會連在一起印出來】
//   這是本檔在互動與非互動下差最多的地方：
//     • 互動執行：印「請輸入姓名: 」→ 停下來等你打字 → 你按 Enter →
//       才印下一個提示。看起來一問一答，很自然。
//     • 用管線餵資料（./io7 < input.txt）：所有輸入早就準備好了，
//       三次讀取之間沒有任何等待，於是三個提示【連續】被印出來，
//       看起來像是擠在同一行。
//   同一支程式、同樣的輸出順序，只是「中間有沒有停頓」不同。
//   下方預期輸出是管線模式的結果，請不要以為程式壞掉了。
//
// 【4. 為什麼身高要用 double 而不是 float】
//   float 只有約 7 位十進位有效位數，165.5 尚可，但一旦做累加或比較，
//   誤差會很快浮現。C++ 的浮點字面值預設就是 double，
//   iostream 的預設輸出精度也是 6 位有效數字（不是 6 位小數）。
//   除非有明確的記憶體壓力（例如百萬筆的陣列），一般計算一律用 double。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 預設輸出精度是「有效位數 6」，不是「小數 6 位」
//   std::cout << 165.5 印出 165.5；但 std::cout << 1234567.0 會印成 1.23457e+06，
//   因為有效位數超過 6 就改用科學記號。要固定小數位數必須
//   std::fixed << std::setprecision(n)（見同課 15 號檔）。
//   這是報表輸出最常見的意外。
//
// (B) 混合 >> 與 getline 的經典地雷
//   本檔全部用 >>，所以沒事。但只要在 >> age 之後接一個 getline，
//   getline 會立刻讀到 >> 留在緩衝區的那個換行符而回傳空字串。
//   解法是 std::cin.ignore(...)，見同課 11 號檔。
//
// (C) 為什麼不檢查 cin 的狀態
//   本檔是入門示範，刻意省略錯誤處理。實務上任何一次 >> 都應該檢查，
//   最簡潔的寫法是直接把串流當條件用：
//       if (!(std::cin >> age)) { /* 處理錯誤 */ }
//   因為 istream 有 explicit operator bool()（C++11 起；C++98 是 void*）。
//
// 【注意事項 Pay Attention】
//   1. >> 讀 string 讀不到空白。姓名有空格請改用 getline。
//   2. 沒有任何錯誤檢查：年齡欄輸入 "abc" 時 age 會被設為 0、failbit 立起，
//      之後的 >> height 會【完全不執行】，height 保持未初始化 →
//      印出它是未定義行為。詳見同課 6 號檔的實測探針。
//   3. 提示字串連在一起是「用管線餵輸入」的正常現象，不是 bug。
//   4. 預設輸出是 6 位【有效數字】，不是 6 位小數。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】operator>> 的多載與型別驅動解析
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::cin >> 怎麼知道要把輸入解析成 int 還是 double 還是 string？
//     答：它不「知道」，也不看內容。operator>> 是多載函式，
//         編譯器在【編譯期】依右運算元的靜態型別選定對應的多載，
//         解析規則完全由變數型別決定。所以同一行輸入 "3.14"，
//         讀進 int 會得到 3（停在小數點），讀進 double 會得到 3.14。
//     追問：那 auto x; std::cin >> x; 可以嗎？
//           → 不行，auto 必須有初始值才能推導，
//             而且就算寫成 auto x = 0; 也只是固定成 int，
//             型別依然是編譯期決定的。
//
// 🔥 Q2. 輸入 "王 小明" 給 std::cin >> name 會發生什麼事？
//     答：name 只會拿到 "王"。>> 讀 string 時遇到空白就停，
//         並把空白留在緩衝區。剩下的 "小明" 會被下一個
//         >> age 拿去解析整數 → 失敗、設 failbit、age 設為 0，
//         再之後的 >> height 因 failbit 已立而完全不執行。
//         一個空格造成三個欄位全毀。
//     追問：怎麼修？
//           → 用 std::getline(std::cin, name) 讀整行；
//             若前面已經有 >>，還要先 ignore 掉殘留的換行符。
//
// ⚠️ 陷阱. 為什麼用管線餵輸入時，三個提示字串會擠在一起印出來？
//     答：因為那三次讀取之間沒有任何等待。互動時是「使用者打字的時間」
//         把三個提示隔開的，不是程式做了什麼。
//         輸出的【順序】兩者完全一致，只有【時間間隔】不同。
//     為什麼會錯：把「畫面看起來一問一答」當成程式的行為，
//         於是看到管線模式的輸出就以為 flush 壞了或順序錯了。
//         實際上提示能在讀取前出現，靠的是 cin 與 cout 的 tie
//         （讀取前自動 flush 綁定的 ostream），這個機制在兩種模式下都成立。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>

int main() {
    std::string name;
    int age;
    double height;
    
    std::cout << "請輸入姓名: ";
    std::cin >> name;
    
    std::cout << "請輸入年齡: ";
    std::cin >> age;
    
    std::cout << "請輸入身高(cm): ";
    std::cin >> height;
    
    std::cout << "\n=== 個人資料 ===" << std::endl;
    std::cout << "姓名: " << name << std::endl;
    std::cout << "年齡: " << age << " 歲" << std::endl;
    std::cout << "身高: " << height << " cm" << std::endl;
    
    return 0;
}

// 編譯: g++ -std=c++17 -Wall -Wextra "第 5 課：輸入輸出流（iostream）入門7.cpp" -o io7
// 執行: printf 'Alice 20 165.5\n' | ./io7
//       （本檔需從 stdin 讀取；用管線餵固定輸入才能得到可重現的輸出）

// 註 1:三個提示字串連續出現在第一行，是【用管線餵輸入】的正常結果 ——
//      三次讀取之間沒有等待，提示就一次印完了。互動執行時會一問一答，
//      輸出順序相同、只是中間多了你打字的停頓。
//
// 註 2:輸入必須是「三個以空白分隔的欄位」。姓名【不能含空格】：
//      餵 '王 小明 20 165.5' 會讓 name 只拿到 "王"，
//      接著 >> age 去解析 "小明" 而失敗，height 更是完全不會被寫入
//      （印出它是 UB）。需要含空白的姓名請改用 getline，見同課 9 號檔。
//
// 註 3:身高印出 165.5 而不是 165.500000，是因為 iostream 預設
//      使用【6 位有效數字】並自動去掉尾隨的零。要固定小數位數
//      得用 std::fixed << std::setprecision(2)（同課 15 號檔）。

