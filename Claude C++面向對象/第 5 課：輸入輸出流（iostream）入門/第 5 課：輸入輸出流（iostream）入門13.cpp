// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門13.cpp  —  健全的輸入驗證迴圈（含 EOF 出口）
// =============================================================================
//
// 【主題資訊 Information】
//   組合使用：cin >> x / cin.fail() / cin.eof() / cin.clear() / cin.ignore()
//   標準版本：C++98 起（本檔語法無新版特性）
//   複雜度  ：每輪 O(該行字元數)；總成本取決於使用者重試次數
//   標頭檔  ：<iostream>、<limits>
//
// 【詳細解釋 Explanation】
//
// 【1. 一個健全的驗證迴圈要處理三種結果，不是兩種】
//   多數教材只寫「成功」與「格式錯誤」，於是漏掉第三種：
//       ① 成功且值合法      → break，正常離開
//       ② 格式錯誤          → clear() + ignore()，重新來過
//       ③ 沒有更多輸入(EOF) → 【必須有出口】，否則無限迴圈
//   本檔三種都處理了，這是它與同課 12 號檔最大的差別。
//
// 【2. 為什麼漏掉 EOF 會變成無限迴圈 —— 這是本檔的核心】
//   fail() 對「格式錯誤」與「EOF」都回傳 true。
//   若不先判 eof() 就直接走格式錯誤的處理：
//       clear();    // 狀態清乾淨了
//       ignore(...) // 但緩衝區裡【沒有東西可丟】，也不會有新資料進來
//   下一輪 >> 立刻又因為 EOF 而失敗 → 再 clear、再 ignore → …
//   於是程式在【完全不阻塞】的情況下全速空轉，
//   一邊瘋狂印出「錯誤：請輸入數字！」一邊吃滿一顆 CPU。
//   這在互動測試時不容易發現（使用者不會按 Ctrl-D），
//   一旦程式被放進管線或用檔案重導向，立刻就會炸。
//
// 【3. 三種錯誤要分開處理，因為修法完全不同】
//     • 格式錯誤（輸入 "abc"）
//       → 緩衝區裡有【讀不掉】的垃圾，必須 clear() + ignore() 清掉
//     • 範圍錯誤（輸入 -5 或 0）
//       → 讀取本身是【成功】的，串流狀態乾淨，
//         【不可以】clear/ignore（那會多吃掉使用者下一行輸入）
//       → 只要重新提示即可
//     • EOF
//       → 沒救，只能離開
//   本檔的 if / else if / else 結構正是依這三種情況分流的。
//   把範圍錯誤也一起 clear/ignore 是很常見的錯誤，
//   會導致「輸入 -5 之後下一個正確的數字被吃掉」。
//
// 【4. 迴圈結束後 number 一定有值嗎 —— 答案取決於輸入，而且不總是安全】
//   從 EOF 分支 break 出來時，number 的值來自哪裡？
//   關鍵在於 operator>> 內部的 sentry：
//     • sentry 成功（有東西可讀，但解析失敗）
//         → num_get 真的執行了 → C++11 保證把目標設為 0
//     • sentry 失敗（跳空白時就碰到 EOF，或 eofbit 早已立起）
//         → num_get 【根本沒被呼叫】→ 目標完全不會被寫入
//
//   本機用「先把 number 染成 -777 再跑」的探針逐輪追蹤，證實了兩種情況：
//     餵 'abc\n'：
//         round 1  number=0     fail=1 eof=0   ← 讀到 "abc"，解析失敗 → 寫入 0
//         round 2  number=0     fail=1 eof=1   ← sentry 失敗，維持 round 1 的 0
//         → 印出 0，值是【已定義】的
//     餵空輸入：
//         round 1  number=-777  fail=1 eof=1   ← sentry 立刻失敗，從未寫入
//         → 染色值原封不動 → 在原始程式中 number 是【未初始化】
//           → 印出它是【未定義行為】
//
//   所以精確的結論是：
//     printf 'abc\n' | ./io13  → 印出 0，安全
//     printf ''      | ./io13  → 讀取未初始化的 number，UB
//   而且就算是安全的那條路，0 也不滿足「正整數」這個條件，
//   程式卻照樣把它印出來 —— 這是本檔【刻意保留】的邏輯瑕疵，
//   用來說明「break 的位置決定了後置條件是否成立」。
//   實務上兩個問題都要修：宣告時就寫 int number = 0;（消除 UB），
//   並讓 EOF 分支回傳錯誤碼而不是讓控制流繼續往下走。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼不用 while (std::cin >> number) 這種緊湊寫法
//   那種寫法無法區分「格式錯誤」與「EOF」，
//   而且範圍檢查還是得寫在迴圈裡。
//   本檔展開成完整的 if/else 結構，是為了讓三條路徑各自可見 ——
//   教學上比緊湊更重要。
//
// (B) 更穩健的替代方案：全部用 getline + istringstream
//       std::string line;
//       while (std::getline(std::cin, line)) {
//           std::istringstream iss(line);
//           if (iss >> number && number > 0) break;
//           std::cout << "錯誤\n";
//       }
//   好處是：EOF 由 getline 的回傳值自然處理、
//   永遠不會有緩衝區殘留（每次都吃掉一整行）、
//   也不需要 clear/ignore。實務程式建議用這個模式。
//
// (C) 全速空轉的迴圈為什麼特別危險
//   它不會當掉、不會報錯，只會讓 CPU 使用率衝到 100%
//   並在 log 裡塞進上億行相同訊息。
//   在容器環境中這種行為經常先撐爆磁碟（log 寫滿）才被發現。
//   任何「讀取失敗後重試」的迴圈都必須有明確的終止條件。
//
// 【注意事項 Pay Attention】
//   1. 一定要先判 eof() 再判格式錯誤 —— 兩者的 fail() 都是 true。
//   2. 範圍錯誤【不可以】呼叫 clear()/ignore()，
//      因為那次讀取其實是成功的，緩衝區是乾淨的。
//   3. 從 EOF 分支離開時 number 的值【取決於走到那裡的路徑】：
//        • 曾經有一次「讀到內容但解析失敗」→ 那次寫入了 0，安全
//        • 從頭到尾沒有任何字元可讀（空輸入）→ number 從未被寫入，
//          讀它是【未定義行為】
//      本檔宣告的是 int number;（無初始值），所以第二種路徑真的會踩到 UB。
//      修法很簡單：寫成 int number = 0;。
//   4. 就算走的是安全路徑，0 也不滿足「正整數」的條件，
//      本檔仍照樣印出 —— 這是刻意保留的邏輯瑕疵示範。
//   5. clear() 與 ignore() 的先後順序不可顛倒（見同課 12 號檔）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】輸入驗證迴圈的 EOF 出口
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼輸入驗證迴圈一定要處理 EOF？不處理會怎樣？
//     答：因為 fail() 對「格式錯誤」與「EOF」都回傳 true。
//         若把 EOF 當成格式錯誤處理，程式會 clear() 之後試圖 ignore()，
//         但已經沒有資料可丟、也不會再有新輸入 →
//         下一輪立刻再失敗，形成一個【完全不阻塞的無限迴圈】，
//         全速印錯誤訊息並吃滿 CPU。
//         互動測試時不會發現（人不會按 Ctrl-D），
//         一放進管線或重導向就爆炸。
//     追問：怎麼觸發這個情境來驗證？
//           → printf 'abc\n' | ./程式，或執行後按 Ctrl-D。
//             本檔有 eof() 出口，會印出「輸入結束（EOF），離開。」正常結束。
//
// 🔥 Q2. 輸入 -5 時，為什麼【不應該】呼叫 clear() 和 ignore()？
//     答：因為 >> 這次是【成功】的 —— 它確實讀到了一個合法的整數 -5，
//         串流狀態完全乾淨，緩衝區裡也沒有讀不掉的垃圾。
//         此時呼叫 ignore() 會去丟棄使用者【下一行】的輸入，
//         造成「輸入 -5 之後，接著打的那個正確數字被吃掉」的詭異現象。
//         範圍錯誤只需要重新提示。
//     追問：那怎麼判斷該不該清緩衝區？
//           → 判準是「這次讀取有沒有失敗」，不是「值對不對」。
//             失敗代表有讀不掉的字元卡住，才需要清；
//             成功但值不合邏輯，緩衝區是乾淨的。
//
// ⚠️ 陷阱. 「C++11 起 >> 失敗會把目標設為 0，所以 EOF 出口的 number 一定是 0」
//          —— 這句話錯在哪裡？
//     答：錯在把保證的適用範圍放大了。那個保證屬於【num_get 真的被呼叫】的情況；
//         而 operator>> 會先建立 sentry，若 sentry 就失敗
//         （跳過空白時碰到 EOF，或 eofbit 早已立起），
//         num_get 根本不會執行，目標【完全不會被寫入】。
//         本機染色探針（先把 number 設成 -777）逐輪追蹤的結果：
//             餵 'abc\n' → round1 寫入 0、round2 sentry 失敗維持 0 → 印出 0，安全
//             餵 空輸入  → round1 sentry 就失敗 → 染色值 -777 原封不動
//                          → 原始程式中即為未初始化 → 讀取是 UB
//     為什麼會錯：多數人把「C++11 起失敗會設 0」背成一條無條件的規則，
//         於是推論「不管怎樣都被寫過了，很安全」。
//         但這條規則描述的是【解析階段】的失敗，
//         而 EOF 造成的是【sentry 階段】的失敗 —— 兩者根本沒走到同一步。
//         附帶一提，這個迴圈還有第二層問題：即使走的是安全路徑，
//         0 也不是正整數，程式照樣印出來了。
//         迴圈有【兩個】出口（驗證成功／輸入耗盡），只有前者能保證後置條件；
//         實務上 EOF 分支應該回傳錯誤碼或丟例外，
//         並且一開始就把 number 初始化為 0。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <limits>

int main() {
    int number;
    
    while (true) {
        std::cout << "請輸入一個正整數: ";
        std::cin >> number;
        
        // 檢查輸入是否成功, 如果輸入失敗，則清除錯誤狀態並忽略錯誤的輸入
        if (std::cin.fail()) {
            // ⚠️ 先判 EOF 再處理格式錯誤：兩者都會讓 fail() 為 true，但處理方式完全不同。
            //    EOF（使用者按 Ctrl-D、或用管線/重導向餵完資料）是「沒有更多輸入了」，
            //    此時 clear()+ignore() 不會有新資料進來 → 迴圈會【無限空轉】。
            //    這是驗證迴圈最常見的 bug，一定要有這個出口。
            if (std::cin.eof()) {
                std::cout << "\n輸入結束（EOF），離開。" << std::endl;
                break;
            }
            // 輸入類型錯誤
            std::cout << "錯誤：請輸入數字！" << std::endl;
            std::cin.clear();
            // 忽略錯誤的輸入, 直到換行符為止，確保輸入緩衝區被清空
            std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
        } else if (number <= 0) {
            // 數值範圍錯誤
            std::cout << "錯誤：必須是正整數！" << std::endl;
        } else {
            // 輸入正確
            break;
        }
    }
    
    std::cout << "你輸入的正整數是: " << number << std::endl;
    
    return 0;
}

// 編譯: g++ -std=c++17 -Wall -Wextra "第 5 課：輸入輸出流（iostream）入門13.cpp" -o io13
// 執行: printf 'abc\n-5\n0\n7\n' | ./io13     # 走遍三種錯誤再成功
//       printf 'abc\n'           | ./io13     # 測 EOF 出口

// 註 1:下方兩段預期輸出都是實測結果，涵蓋本檔的兩個出口。
//
// 註 2:第一段的輸入依序是「非數字 → 負數 → 零 → 合法值」，
//      正好走過三種錯誤分支各一次：
//        abc → 格式錯誤（clear + ignore）
//        -5  → 範圍錯誤（不清緩衝區，只重新提示）
//        0   → 範圍錯誤（同上；0 不是正整數）
//        7   → 成功，break
//
// 註 3:第二段示範 EOF 出口。最後印出「你輸入的正整數是: 0」——
//      這個 0 是【第一輪】讀到 "abc" 解析失敗時寫入的
//      （C++11 保證解析失敗把目標設為 0）；
//      第二輪的 >> 因 sentry 失敗而根本沒碰 number，所以維持 0。
//      也就是說這條路徑上的 0 是已定義的值，不是不確定值。
//      但它顯然不滿足「正整數」，這是本檔刻意保留的邏輯瑕疵。
//
// 註 4:⚠️【請勿餵空輸入】：printf '' | ./io13 會讓第一輪的 >> 在
//      sentry 階段就失敗，number 從未被寫入 ——
//      本檔宣告的是 int number;（無初始值），
//      因此最後那行印出 number 是【未定義行為】，不會有可靠的輸出。
//      本機染色探針已證實此路徑下目標完全不被寫入。
//      這也是為什麼上方預期輸出只列 'abc\n' 這條安全路徑。
//
// 註 4:若把 eof() 那個 if 拿掉再餵同樣的輸入，程式會【無限空轉】、
//      全速印出「錯誤：請輸入數字！」並吃滿一顆 CPU（不會自己停）。
//      本機實測（移除出口後）：
//        printf 'abc\n' | timeout 3 ./無出口版本 | wc -l
//      3 秒內印出 6,326,707 行 —— 每秒兩百多萬行錯誤訊息。
//      這個數字每次執行都不同（取決於 CPU 與輸出重導向成本），
//      重點是【數量級】而不是精確值。
//      要重現請務必加上 timeout 或 head 限制輸出，
//      否則在真實環境中 log 會在幾秒內寫滿磁碟。

// === 預期輸出 ===
// --- 走遍三種錯誤：printf 'abc\n-5\n0\n7\n' | ./io13 ---
// 請輸入一個正整數: 錯誤：請輸入數字！
// 請輸入一個正整數: 錯誤：必須是正整數！
// 請輸入一個正整數: 錯誤：必須是正整數！
// 請輸入一個正整數: 你輸入的正整數是: 7
//
// --- EOF 出口：printf 'abc\n' | ./io13 ---
// 請輸入一個正整數: 錯誤：請輸入數字！
// 請輸入一個正整數: 
// 輸入結束（EOF），離開。
// 你輸入的正整數是: 0
