// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門12.cpp  —  偵測輸入失敗並復原串流狀態
// =============================================================================
//
// 【主題資訊 Information】
//   bool fail()  const;   // failbit 或 badbit 任一為真
//   bool good()  const;   // 四個狀態位元全為 0
//   bool eof()   const;   // eofbit
//   bool bad()   const;   // badbit
//   void clear(iostate state = goodbit);   // 【設定】狀態為 state，預設清成 good
//   標準版本：C++98 起
//   複雜度  ：全部 O(1)（clear 不含後續 ignore 的成本）
//   標頭檔  ：<iostream>、<limits>
//
// 【詳細解釋 Explanation】
//
// 【1. 串流的四個狀態位元】
//   iostate 是一組位元旗標，實際上只有三個位元加一個「全零」狀態：
//       goodbit = 0        沒有任何錯誤（不是一個真的位元，就是 0）
//       eofbit             已讀到檔尾
//       failbit            上一次操作失敗，但串流【還可以救】
//       badbit             串流已損毀，通常救不回來（如底層 I/O 錯誤）
//   關鍵區別：
//     • failbit 是「這次解析不成功」——資料還在，只是格式不合
//     • badbit  是「這個串流壞掉了」——例如磁碟錯誤、streambuf 丟例外
//   fail() 對【兩者】都回傳 true，這是它最容易被誤解的地方。
//
// 【2. 為什麼失敗後一定要 clear() 再 ignore()，順序不能顛倒】
//   failbit 一旦立起，【所有】後續的格式化輸入都會立刻返回、什麼都不做。
//   所以：
//     • 先 clear()  → 把狀態清成 good，串流才「活過來」
//     • 再 ignore() → 這時 ignore 才真的會去讀並丟棄那些垃圾字元
//   若順序顛倒（先 ignore 再 clear），ignore 會因為 failbit 還立著
//   而【完全不執行】，垃圾原封不動留在緩衝區，
//   下一次讀取立刻又失敗 —— 這就是驗證迴圈變成無限迴圈的典型原因。
//
// 【3. 失敗時目標變數會變成什麼】
//   自 C++11 起有明文規定（[facet.num.get.virtuals]）：
//     • 格式解析失敗 → 目標設為 0
//     • 數值溢位     → 目標設為該型別的極值（max 或 lowest）
//   C++98 則是「保持原值不動」。
//   但要注意這個保證只適用於【真的被執行到】的那一次 >>；
//   failbit 已立之後的 >> 連目標都不會碰（同課 6 號檔有染色實測）。
//   本檔只讀一個變數，所以 number 在任何情況下都會被寫入，沒有 UB 疑慮。
//
// 【4. clear() 的名字其實有誤導性】
//   它不是「清除錯誤」，而是「【設定】狀態」。
//   預設參數是 goodbit（0），所以 clear() 看起來像在清錯誤；
//   但 clear(std::ios::failbit) 反而是【設定】failbit。
//   要單獨清掉某一個位元得寫成：
//       std::cin.clear(std::cin.rdstate() & ~std::ios::failbit);
//   實務上幾乎都是直接 clear() 全清。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼串流不直接丟例外
//   iostream 預設把錯誤記在狀態位元裡而不丟例外，
//   因為 I/O 失敗（使用者打錯字、檔案讀到底）是【預期內】的常態，
//   不是異常事件；每次都丟例外會讓正常流程充滿 try/catch。
//   需要例外行為時可以自己開啟：
//       std::cin.exceptions(std::ios::failbit | std::ios::badbit);
//   之後任何失敗都會丟 std::ios_base::failure。
//
// (B) 用串流本身當條件是最慣用的檢查方式
//   istream 有 explicit operator bool()（C++11 起，之前是 operator void*），
//   它回傳 !fail()。所以這兩種寫法等價：
//       if (std::cin.fail()) { ... }
//       if (!(std::cin >> number)) { ... }     // 更常見、更緊湊
//   後者的好處是「讀取」與「檢查」寫在一起，不會忘記檢查。
//
// (C) eof() 不能用來預測「還有沒有資料」
//   eofbit 是【已經試圖讀取並碰到底】之後才被設定的事後狀態。
//   所以 while (!std::cin.eof()) 這種寫法必定會多跑一輪
//   （最後一次讀取失敗但迴圈仍執行了本體）。
//   正確做法永遠是把讀取動作本身當條件。
//
// 【注意事項 Pay Attention】
//   1. 順序必須是 clear() 然後 ignore()。顛倒會讓 ignore 失效。
//   2. fail() 對 failbit 與 badbit 都為真。要區分「格式錯」與「串流壞掉」
//      得另外檢查 bad()。
//   3. fail() 也會在 EOF 時為真 —— 使用者按 Ctrl-D 或管線餵完資料時，
//      本檔會走進「輸入錯誤」分支。要區分兩者必須另外檢查 eof()，
//      同課 13 號檔的驗證迴圈就示範了這一點。
//   4. clear() 是「設定狀態」不是「清除錯誤」，預設參數才讓它看起來像清除。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】串流狀態位元與錯誤復原
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 輸入失敗後為什麼要先 clear() 再 ignore()？順序反過來會怎樣？
//     答：failbit 立起之後，所有後續輸入操作都會立刻返回、什麼都不做，
//         ignore 也不例外。所以必須先 clear() 讓串流恢復可用，
//         ignore 才真的會去丟棄緩衝區裡的垃圾。
//         順序顛倒的話 ignore 完全不執行，垃圾還在，
//         下一次讀取立刻又失敗 —— 這就是驗證迴圈無限空轉的典型成因。
//     追問：為什麼不能只 clear() 不 ignore()？
//           → clear 只改狀態旗標，不動緩衝區。那些無法解析的字元還在原地，
//             下次 >> 會再讀到同一批垃圾、再次失敗。兩個動作缺一不可。
//
// 🔥 Q2. failbit 和 badbit 差在哪？fail() 回傳 true 時是哪一種？
//     答：failbit 表示「這次操作失敗但串流還能用」（格式不符、讀不到預期資料）；
//         badbit 表示「串流本身損毀」（底層 I/O 錯誤、streambuf 丟例外），
//         通常無法復原。fail() 對【兩者】都回傳 true，
//         所以偵測到 fail() 之後若要決定「該重試還是該放棄」，
//         必須再看 bad()。
//     追問：clear() 能救回 badbit 嗎？
//           → 狀態位元可以被清掉，但造成 badbit 的底層原因（例如磁碟錯誤）
//             不會因此消失，通常一讀就再次失敗。實務上 badbit 應視為終止條件。
//
// ⚠️ 陷阱. 這段程式用 fail() 判斷「使用者輸入了非數字」，這個判斷完整嗎？
//     答：不完整。fail() 在【EOF】時同樣為真 ——
//         使用者按 Ctrl-D、或用管線餵完資料後，
//         程式會走進「輸入錯誤！不是有效的整數。」這個分支，
//         但實際上並沒有人輸入錯誤，只是沒有更多輸入了。
//         要區分必須先檢查 eof()。
//     為什麼會錯：把 fail() 讀成「格式錯誤」。
//         它真正的語意是「上一次操作沒有成功」，
//         而「沒成功」的原因至少有三種：格式不符、資料耗盡、串流損毀。
//         在單次讀取的程式裡這個混淆無傷大雅；
//         但一旦放進 while 迴圈，沒有 EOF 出口就會【無限空轉】——
//         因為 clear()+ignore() 永遠等不到新資料。
//         同課 13 號檔就是為了修掉這個問題而寫的。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <limits>

int main() {
    int number;
    
    std::cout << "請輸入一個整數: ";
    std::cin >> number;
    
    // 檢查輸入是否成功, 如果輸入失敗，則清除錯誤狀態並忽略錯誤的輸入
    if (std::cin.fail()) {
        std::cout << "輸入錯誤！不是有效的整數。" << std::endl;
        
        // 清除錯誤狀態, 讓 cin 可以繼續使用
        std::cin.clear();
        
        // 忽略錯誤的輸入, 直到換行符為止，確保輸入緩衝區被清空
        std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
    } else {
        std::cout << "你輸入的是: " << number << std::endl;
    }
    
    return 0;
}

