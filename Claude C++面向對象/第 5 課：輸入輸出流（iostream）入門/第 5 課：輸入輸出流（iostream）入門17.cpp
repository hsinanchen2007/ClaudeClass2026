//      才出現 0x 與 0 前綴。請特別注意第 4 行「十進位: 255」
//      【仍然沒有前綴】—— 這不是 bug，
//      而是 C++ 根本沒有十進位前綴這種語法（0x 是十六進位、0 是八進位）。
//
// 註 3:八進位的前綴只是一個 0（0377）。這是 C 留下的歷史設計，
//      也是 010 == 8 這個經典陷阱的來源。
//
// 註 4:程式最後一行的 std::dec << std::noshowbase 是必要的清理 ——
//      dec/hex/oct 與 showbase 都是【黏性】旗標，
//      不還原會讓後續所有整數輸出繼續使用十六進位。

