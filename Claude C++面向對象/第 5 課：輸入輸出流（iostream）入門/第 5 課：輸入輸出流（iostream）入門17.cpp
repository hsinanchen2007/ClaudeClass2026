// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門17.cpp  —  dec / hex / oct 與 showbase
// =============================================================================
//
// 【主題資訊 Information】
//   std::ios_base& dec(std::ios_base&);         // 十進位（預設）
//   std::ios_base& hex(std::ios_base&);         // 十六進位
//   std::ios_base& oct(std::ios_base&);         // 八進位
//   std::ios_base& showbase  (std::ios_base&);  // 顯示進位前綴
//   std::ios_base& noshowbase(std::ios_base&);  // 不顯示（預設）
//   相關：std::uppercase（0XFF 而非 0xff）、std::showpos
//
//   全部都是【黏性】的，設定後持續有效
//   標準版本：C++98 起
//   複雜度  ：O(輸出位數)
//   標頭檔  ：<iostream>（<iomanip> 在本檔非必要，因為未使用帶參數的 manipulator）
//
// 【詳細解釋 Explanation】
//
// 【1. 進位制是三選一的旗標組，不是三個獨立開關】
//   dec / hex / oct 對應到 basefield 這個【欄位遮罩】：
//       std::ios_base::basefield == (dec | oct | hex)
//   設定其中一個會自動清掉另外兩個，所以不可能同時是十六進位又是八進位。
//   這和 boolalpha 那種獨立的開關旗標不同。
//   手動操作的等價寫法是：
//       std::cout.setf(std::ios::hex, std::ios::basefield);
//   第二個參數就是「先清掉這個欄位的所有位元，再設定第一個參數」。
//
// 【2. 為什麼要有 showbase】
//   單看 "ff" 無法判斷它是十六進位的 255 還是一個叫 ff 的字串；
//   單看 "377" 更可能被誤讀成十進位的三百七十七。
//   showbase 會加上 C/C++ 語法中的進位前綴：
//       十六進位 → 0x（本機輸出 0xff）
//       八進位   → 0 （本機輸出 0377）
//       十進位   → 沒有前綴（這是為什麼第 4 行輸出仍是 255）
//   加上前綴的最大好處是【可以直接貼回程式碼裡】，
//   0xff 和 0377 都是合法的 C++ 整數字面值。
//
// 【3. 本檔輸出中最容易被忽略的一行】
//   showbase 開啟後的「十進位: 255」——它【沒有】任何前綴。
//   這不是 bug，而是十進位本來就不需要前綴（也沒有這種語法）。
//   很多人預期看到類似 "0d255" 的東西，那是 Verilog 的寫法，不是 C++。
//
// 【4. 八進位前綴為什麼只是一個 0】
//   這是 C 語言留下的歷史包袱：以 0 開頭的整數字面值就是八進位。
//   於是 010 等於 8 而不是 10 —— 這個設計造成過無數 bug，
//   典型災難是把日期寫成 08、09（八進位裡沒有 8 和 9，直接編譯錯誤）。
//   C++14 為此引入了明確的二進位前綴 0b，
//   而 C++17 之後的新語言（如 Rust）大多改用 0o 來表示八進位。
//   閱讀舊程式碼看到 0 開頭的數字時務必警覺。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 這些旗標只影響【整數】，不影響浮點與字串
//   std::cout << std::hex << 3.14 仍然印出 3.14。
//   浮點的進位格式由 floatfield（fixed/scientific/hexfloat）控制，
//   是完全獨立的另一組旗標。
//   同理，std::hex 對 std::string 也毫無作用。
//
// (B) 負數在 hex 下會印出補數表示
//   std::cout << std::hex << -1 在 32 位元 int 上印出 ffffffff。
//   因為輸出時是把該整數的【位元樣貌】以十六進位呈現，
//   而不是「先取絕對值再加負號」。
//   這在除錯位元運算時非常有用，但初次看到常會嚇一跳。
//   （型別寬度屬實作定義；本機 int 為 32 位元。）
//
// (C) 讀取端也吃這組旗標
//   std::cin >> std::hex >> n; 會把 "ff" 解析成 255。
//   這是解析設定檔中十六進位色碼、遮罩值時很方便的用法，
//   比自己呼叫 std::stoi(s, nullptr, 16) 更能融入串流風格。
//
// 【注意事項 Pay Attention】
//   1. dec/hex/oct 是【互斥】的（同屬 basefield），設一個就清掉其他兩個。
//   2. 三者都是【黏性】的 —— 忘記還原成 dec 是很常見的 bug，
//      本檔最後一行的 std::dec << std::noshowbase 正是為此。
//   3. showbase 對十進位【不會】加任何前綴，因為 C++ 沒有這種語法。
//   4. 這些旗標只作用於整數，對浮點與字串無效。
//   5. 八進位前綴只是一個 0，容易與十進位混淆 ——
//      閱讀 0 開頭的字面值時要特別小心（010 == 8）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】進位制 manipulator 與 basefield
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::cout << std::hex << 255 之後，接著印 std::cout << 100
//        會印出什麼？
//     答：印出 64（100 的十六進位）。因為 hex 是【黏性】旗標，
//         設定後對後續所有整數輸出都有效，直到被 dec 或 oct 取代。
//         這是實務上很常見的 bug —— 除錯時臨時加了 std::hex，
//         結果整支程式後面的數字全變成十六進位。
//     追問：怎麼避免？
//           → 用完立刻 << std::dec 還原；
//             或用 copyfmt 存檔還原；
//             或改用 C++20 的 std::format("{:x}", n)，它不碰串流狀態。
//
// 🔥 Q2. 開啟 showbase 之後，為什麼十進位的 255 還是印成 255 而不是 0d255？
//     答：因為 C++（和 C）根本沒有十進位前綴這種語法。
//         showbase 加的是「能直接貼回原始碼的合法字面值前綴」——
//         十六進位是 0x、八進位是 0，而十進位不需要任何前綴。
//         本檔輸出中那一行「十進位: 255」正是這個規則的體現。
//     追問：那二進位呢？
//           → iostream 【沒有】二進位的 manipulator。
//             要印二進位必須用 std::bitset<N>(n)，
//             或 C++20 的 std::format("{:b}", n)。
//             （C++14 起支援 0b 字面值，但那是「寫進程式碼」，不是「印出來」。）
//
// ⚠️ 陷阱. std::cout << std::hex << -1 印出 ffffffff，
//          是不是代表 hex 把負號弄丟了、輸出是錯的？
//     答：不是錯的。整數以十六進位輸出時，呈現的是該值的【位元樣貌】，
//         而 -1 在二補數表示下所有位元都是 1，
//         32 位元 int 就是 ffffffff。這是正確且有用的行為 ——
//         除錯位元遮罩時正需要看到原始位元。
//         （int 為 32 位元屬實作定義；在 64 位元型別上會是 16 個 f。）
//     為什麼會錯：把 hex 想成「換一種寫法表示同一個數學上的數」，
//         於是預期看到類似 -1 的東西。
//         但 hex/oct 的語意是「把這塊記憶體的整數位元用該進位制展開」，
//         它更接近「看原始資料」而不是「數學表示法轉換」。
//         想要帶號的十六進位必須自己判斷正負再取絕對值印出。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <iomanip>

int main() {
    int num = 255;
    
    std::cout << "十進位: " << std::dec << num << std::endl;
    std::cout << "十六進位: " << std::hex << num << std::endl;
    std::cout << "八進位: " << std::oct << num << std::endl;
    
    // 顯示進位前綴
    std::cout << std::showbase;
    std::cout << "十進位: " << std::dec << num << std::endl;
    std::cout << "十六進位: " << std::hex << num << std::endl;
    std::cout << "八進位: " << std::oct << num << std::endl;
    
    // 恢復預設
    std::cout << std::dec << std::noshowbase;
    
    return 0;
}

