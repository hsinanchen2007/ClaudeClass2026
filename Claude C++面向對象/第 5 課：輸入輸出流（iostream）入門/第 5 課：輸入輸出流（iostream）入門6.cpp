// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門6.cpp  —  用 std::cin 連續讀取多個數值
// =============================================================================
//
// 【主題資訊 Information】
//   std::istream& operator>>(std::istream&, int&);   // C++98 起，標頭 <iostream>
//   語法：std::cin >> a >> b >> c;
//   回傳：istream& —— 這正是能串接（chaining）的原因
//   複雜度：每次讀取 O(讀入字元數)
//   標頭檔：<iostream>（提供 std::cin / std::cout / std::endl）
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼 >> 可以串起來】
//   operator>> 回傳的是「同一個 istream 的參考」，不是 void、也不是副本。
//   所以 std::cin >> a >> b >> c 實際上是：
//       ((std::cin >> a) >> b) >> c
//   第一次讀完回傳 std::cin 本身，第二次接著在它上面讀。
//   如果回傳的是副本，stream 的狀態（位置、錯誤旗標）就會分岔，
//   串接立刻失去意義 —— 這是「回傳參考」在標準庫中的經典理由。
//
// 【2. >> 怎麼看待空白】
//   預設情況下 >> 是「格式化輸入」，開頭會【跳過所有空白】
//   （空格、Tab、換行都算），讀到第一個非空白字元才開始解析，
//   碰到下一個空白就停下、並把那個空白【留在緩衝區裡】。
//   所以下面三種輸入，對這支程式而言完全等價：
//       10 20 30            ← 一行、空格分隔
//       10   20   30        ← 多個空格
//       10                  ← 分成三行輸入
//       20
//       30
//   使用者看到提示字串寫「用空格分隔」，其實換行也可以，
//   因為 >> 根本不區分是哪一種空白。
//
// 【3. 提示字串為什麼和輸入擠在同一行】
//   輸出的 "請輸入三個整數（用空格分隔）: " 沒有換行，
//   而 std::cout 是【行緩衝／滿了才送出】的。
//   但這裡提示仍會先出現，原因是 std::cin 和 std::cout 預設是【綁定】的
//   （cin.tie() 指向 cout）：任何從 cin 讀取之前，
//   標準規定會先自動 flush 綁定的那個 ostream。
//   若把 std::cin.tie(nullptr) 解綁，提示就可能【晚於】使用者輸入才出現 ——
//   這是競賽程式為了加速常做、但互動程式絕對不該做的事。
//
// 【4. 讀取失敗時會怎樣 —— 這裡藏著本檔最嚴重的問題】
//   若使用者輸入 "10 abc 30"：
//     • a 讀到 10，成功
//     • b 解析 "abc" 失敗 → 設定 failbit，且【自 C++11 起】把 b 設為 0
//       （C++98 是「保持原值不動」，這是一個實際的版本差異）
//     • 一旦 failbit 被設起來，後面的 >> c 會【直接跳過、什麼都不做】
//       —— 而「什麼都不做」包含【不寫入 c】。
//       c 來自 int a, b, c; 的預設初始化，值是不確定的，
//       因此把 c 印出來是【未定義行為】。
//
//   本機用「先把三個變數染成 -999 再讀」的探針實測，證實了這個分工：
//       輸入 "10 20 30"            → 10  20   30      （三個都被寫過）
//       輸入 "10 abc 30"           → 10   0  -999     （c 完全沒被碰）
//       輸入 "99999999999999 2 3"  → INT_MAX -999 -999（b、c 都沒被碰）
//   -999 原封不動地留著，就是「這個變數沒有被寫入」最直接的證據。
//
//   所以【不可以】說「失敗時全部會變成 0」。精確的說法是：
//     真正執行到的那一次 >> 一定會寫入目標（成功寫值、格式失敗寫 0）；
//     但 failbit 設起來之後的每一次 >>，連目標都不會碰。
//   本檔完全沒有錯誤檢查，所以輸入不合法時就會落入 UB。
//   正確做法見同課的 12、13 號檔（fail() 檢查與驗證迴圈）。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 「>> 失敗會設成 0」這個保證的【適用範圍】
//   自 C++11 起，operator>> 對算術型別的失敗路徑有明文保證：
//   解析失敗時把目標設為 0、溢位時設為該型別的極值
//   （[facet.num.get.virtuals]）；C++98 則是「保持原值不動」。
//   很多人因此以為「所以變數一定會被寫過，不可能是未初始化」。
//   這個推論【是錯的】，因為它漏掉一個前提：
//     那個保證只適用於【真的被執行到】的那一次 >>。
//   串接中只要有一次失敗，failbit 就會設起來，
//   後續的 >> 是「立刻返回、不碰緩衝區、也不碰目標」的空操作。
//   於是後面那些變數保持在預設初始化的不確定值 —— 讀它們就是 UB。
//
//   結論：本檔在【輸入合法】時三個變數都被寫過，沒有問題；
//   一旦輸入不合法，後半段的變數就會是未初始化狀態。
//   要根治只有兩條路：宣告時就給初值（int a = 0, b = 0, c = 0;），
//   或每次讀取後檢查串流狀態再決定要不要繼續。
//
// (B) std::endl 與 '\n' 的差別
//   std::endl = 寫入 '\n' 再【強制 flush】。
//   flush 是一次系統呼叫（write），在迴圈裡大量使用會明顯拖慢。
//   除非真的需要立刻看到輸出（例如程式可能崩潰、或在管線另一端即時監看），
//   否則用 '\n' 就好。本檔只印幾行，兩者差異可忽略。
//
// (C) 緩衝區裡殘留的換行
//   >> 停在空白處、不吃掉它。所以 >> 之後緊接 std::getline() 會立刻讀到
//   一個空字串（讀到殘留的換行就結束）。這是新手最常踩的坑，
//   解法見同課 11 號檔的 std::cin.ignore(...)。
//
// 【注意事項 Pay Attention】
//   1. 本檔【完全沒有錯誤檢查】。輸入不合法時不但沒有任何警告，
//      還會讀取未被寫入的變數（UB）—— 這是教學上的簡化，
//      不是可以照抄的寫法。要照抄請至少寫成 int a = 0, b = 0, c = 0;。
//   2. 「輸入失敗時目標被設為 0」是 C++11 起的保證、C++98 是保持原值，
//      而且【只對真的執行到的那一次 >> 有效】。
//      failbit 之後的 >> 不會寫入目標，別把它算進「已被初始化」。
//   3. 溢位（例如輸入 99999999999999）同樣會設 failbit，
//      但目標是被設為該型別的極值（std::numeric_limits<int>::max()），
//      不是 0 —— 這和「格式錯誤設 0」是兩條不同的規則。本機已實測。
//   4. 提示字串能先顯示是靠 cin/cout 的 tie，不是靠 endl。
//      別把「有印出來」當成「已經 flush」。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】std::cin 的串接與空白處理
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::cin >> a >> b >> c; 為什麼可以這樣串接？
//     答：因為 operator>> 回傳的是 std::istream&（同一個串流的參考），
//         所以 (cin >> a) 的結果還是 cin，可以繼續 >> b。
//         回傳參考而非副本是關鍵 —— 副本會讓串流狀態分岔，
//         錯誤旗標與讀取位置都無法正確傳遞。
//     追問：那如果中間某一次讀取失敗，後面的還會執行嗎？
//           → 會「執行」但不做事。failbit 一旦設起來，
//             後續 >> 會立即返回、不碰緩衝區也不改變目標值。
//
// 🔥 Q2. 使用者若把三個數字分三行輸入，這支程式還能正確運作嗎？
//     答：可以，而且完全等價。>> 預設會跳過【所有】前導空白，
//         而換行符 '\n' 也是空白的一種。提示文字寫「用空格分隔」
//         只是給人看的建議，不是程式的限制。
//     追問：那要怎麼寫才能「只讀這一行的三個數字」？
//           → 先 std::getline 讀整行，再用 std::istringstream 去解析，
//             這樣換行就變成天然的邊界。
//
// ⚠️ 陷阱. 輸入 "10 abc 30"，印出來的 b 和 c 分別是什麼？
//     答：b 是 0，c 則【沒有定義】。
//         b 是「真的執行到、但解析失敗」→ C++11 保證寫成 0；
//         c 是「因為 failbit 已設而根本沒執行」→ 目標完全沒被寫入，
//         仍是 int a, b, c; 留下的不確定值，讀它就是 UB。
//         本機染色實測（先設成 -999 再讀）得到 10 / 0 / -999，
//         -999 沒被覆寫，正是「c 沒被碰過」的鐵證。
//     為什麼會錯：兩種相反的錯誤模型都很常見 ——
//         一種人記得「沒初始化就是垃圾值」，於是連 b 也說成垃圾值；
//         另一種人（更危險）背了「C++11 起失敗會設成 0」，
//         就以為所有變數都被安全地寫過了。
//         兩者都漏掉同一個關鍵：那個保證屬於【單一次 >> 呼叫】，
//         而 failbit 會讓後續的 >> 整個變成空操作、連目標都不碰。
//         附帶一提，實際跑這支程式常常會印出 "10, 0, 0" ——
//         那個第二個 0 只是堆疊上剛好是 0，不是保證，
//         千萬不要把它當成「預期輸出」。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>

int main() {
    int a, b, c;
    
    std::cout << "請輸入三個整數（用空格分隔）: ";
    std::cin >> a >> b >> c;
    
    std::cout << "你輸入的是: " << a << ", " << b << ", " << c << std::endl;
    std::cout << "總和: " << (a + b + c) << std::endl;
    
    return 0;
}

