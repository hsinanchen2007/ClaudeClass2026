// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門（10）
//      — cin >> 之後接 getline 為什麼會讀到空字串
// =============================================================================
//
// 【主題資訊 Information】
//   std::istream& operator>>(istream&, T&);                       // 格式化輸入
//   std::istream& getline(istream&, string&, char delim = '\n');  // 未格式化輸入
//   std::istream& istream::ignore(streamsize n = 1, int delim = EOF);
//   標準版本：C++98 起（std::getline 的 string 版本定義於 <string>，不是 <iostream>）
//   標頭檔  ：<iostream>（cin）、<string>（std::getline 的 std::string 重載）
//   複雜度  ：兩者都是 O(讀取字元數)。
//
//   ★ 本檔示範的是 C++ 初學者最常踩、也最常在面試中被問到的 I/O 陷阱。
//     它不是 bug，是 >> 與 getline 兩種輸入方式對「分隔字元」處理方式不同
//     所導致的必然結果。
//
// 【詳細解釋 Explanation】
//
// 【1. 根本原因：>> 不吃掉結尾的換行，getline 會】
//   假設使用者輸入 25 然後按 Enter，輸入緩衝區裡實際上是：
//       '2' '5' '\n'
//   std::cin >> age 的行為：
//       - 先跳過前導空白（含換行）
//       - 讀取數字字元直到遇到「不屬於數字的字元」為止
//       - 把那個字元「留在緩衝區裡不取走」
//   於是 age 得到 25，而那個 '\n' 還原封不動地留著。
//   接著 std::getline(std::cin, name) 的行為：
//       - 從目前位置一路讀到下一個 '\n'
//       - 把那個 '\n' 取走並「丟棄」（不放進字串）
//   它立刻就遇到了留下來的 '\n' → 讀到零個字元 → name 是空字串。
//   ★ 關鍵：getline「成功」了，它並沒有失敗。它確實讀到了一整行，
//     只是那一行的內容剛好是空的。這也是為什麼檢查 cin 的狀態
//     不會發現任何異常 —— 沒有任何錯誤旗標被設置。
//
// 【2. 為什麼 >> 要把換行留下來】
//   因為 >> 是「格式化輸入」，它的職責是抽取一個值，
//   而不是決定「一行結束了沒」。輸入 "25 30" 與 "25\n30" 對 >> 來說
//   應該是等價的 —— 兩者都該讀到 25 和 30。若 >> 順手吃掉分隔字元，
//   那麼「一次讀多個值」的常見用法就會變得不可預測。
//   換句話說，這個行為對 >> 自己是完全合理的；
//   問題只發生在「把它和 getline 混用」的時候。
//
// 【3. 三種正確的解法】
//   (a) 讀完 >> 之後，明確清掉那一行的殘留：
//         std::cin >> age;
//         std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
//         std::getline(std::cin, name);
//       這是最穩健的寫法：它會丟棄「直到換行為止」的所有殘留，
//       而不只是一個字元。需要 #include <limits>。
//       常見的簡寫 std::cin.ignore(); 只丟棄「一個」字元 ——
//       在輸入乾淨時可行，但若使用者輸入 "25   " 再按 Enter，
//       那些空白仍會留下，getline 就會讀到一串空白。
//   (b) 調整順序：把所有 getline 放在 >> 之前（本檔第二段的做法）。
//       治標不治本，但在簡單程式裡很實用。
//   (c) 一律只用 getline 讀入整行，再自行解析成需要的型別
//         （例如用 std::stoi 或 std::istringstream）。
//       這是實務上最推薦的做法 —— 輸入的「讀取」與「解析」職責分離，
//       也不必再擔心兩種輸入方式的互動。
//
// 【4. 為什麼本檔第二段（先 getline 再 >>）能正常運作】
//   第二段的順序是：getline 讀姓名 → >> 讀年齡。
//   getline 會把該行結尾的 '\n' 一併取走，所以下一個 >> 從乾淨的位置開始；
//   而 >> 是這一段最後一次讀取，它留下的殘留換行沒有人會再讀到，
//   自然就不會造成問題。
//   ★ 但要注意：如果第二段之後還要再 getline 一次，同樣的問題會再度出現。
//     順序法只是「剛好避開」，不是解決。
//
// 【概念補充 Concept Deep Dive】
//   格式化輸入（>>）與未格式化輸入（getline）的差異整理：
//     跳過前導空白：>> 會（預設）        ／ getline 不會
//     以什麼為終止：任何空白字元          ／ 指定的分隔字元（預設 '\n'）
//     終止字元處理：留在緩衝區            ／ 取走並丟棄
//     讀到空內容時：設 failbit            ／ 不設 failbit（空行是合法的一行）
//   最後一列是本陷阱最惡毒的地方：getline 讀到空行不算失敗，
//   所以任何「檢查串流狀態」的防禦性寫法都攔不到它。
//
//   另外補充 >> 讀取失敗時的行為（C++11 起）：
//   若輸入無法轉換成目標型別（例如對 int 輸入 "abc"），
//   >> 會設定 failbit，並且「把目標變數設為 0」。
//   在 C++11 之前，變數的值是保持不變的 —— 這是少數會影響既有程式碼的行為變更。
//   ★ 但無論哪個版本，都不該依賴這個值。本檔因此把 age 初始化為 0
//     並檢查讀取結果，而不是讓它保持未初始化。
//
// 【注意事項 Pay Attention】
//   1. std::getline 的 std::string 版本宣告在 <string>，不是 <iostream>。
//      許多程式只 include <iostream> 卻能編譯，是靠標頭的間接引入 ——
//      不可依賴這種傳遞性，用到就明確 include。
//   2. std::cin.ignore() 不帶參數時只丟棄「一個」字元。
//      要清乾淨整行請用 ignore(numeric_limits<streamsize>::max(), '\n')。
//   3. 未初始化的區域變數若在被賦值前讀取，是未定義行為。
//      本檔已把 age 初始化為 0 並檢查每次讀取是否成功。
//   4. 本檔需要標準輸入才能執行，且輸出取決於輸入內容。
//      重現末尾預期輸出的輸入資料寫在編譯說明中。
//   5. 混用 >> 與 getline 本身不是錯誤，只要記得清掉殘留即可。
//      但實務上更推薦「全部用 getline 讀行、再自行解析」。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】cin >> 與 getline 混用
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼 cin >> age; 之後緊接著 getline(cin, name); 會讀到空字串？
//     答：因為 >> 讀完數字後，會把終止它的那個字元（使用者按下的 Enter，
//         也就是 '\n'）留在緩衝區裡不取走。接著 getline 一路讀到下一個 '\n'，
//         結果立刻就遇到那個殘留的換行 —— 讀到零個字元，name 就成了空字串。
//         注意 getline 並沒有失敗，它確實讀完了「一行」，只是那行是空的。
//     追問：那要怎麼修？
//         → 在 >> 之後加
//           std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
//           把該行剩下的內容全部丟棄。只寫 cin.ignore() 只丟一個字元，
//           使用者若在數字後多打了空白就會失效。
//           更好的做法是一律用 getline 讀整行，再用 std::stoi
//           或 std::istringstream 解析。
//
// 🔥 Q2. >> 和 getline 對「分隔字元」的處理有什麼根本差異？
//     答：>> 以任意空白為終止並「留下」該字元；
//         getline 以指定分隔字元（預設 '\n'）為終止並「取走丟棄」該字元。
//         此外 >> 會自動跳過前導空白，getline 不會 ——
//         所以 getline 讀到空行時會得到空字串，且不設 failbit。
//     追問：為什麼 >> 要留下終止字元？這不是很反直覺嗎？
//         → 因為 >> 的職責是「抽取一個值」，不是「消耗一行」。
//           對 cin >> a >> b 來說，"1 2" 和 "1\n2" 必須等價。
//           若 >> 順手吃掉分隔字元，多值讀取的行為就會變得不可預測。
//           這個設計對 >> 自己完全合理，問題只出在與 getline 混用時。
//
// ⚠️ 陷阱. 我在 getline 前後都檢查了 cin 的狀態（cin.fail()、cin.good()），
//         全部正常，為什麼 name 還是空的？
//     答：因為 getline「真的成功了」。它讀到了一整行，只是那一行的內容
//         是空字串。空行是完全合法的輸入，標準不會為此設定任何錯誤旗標。
//         換言之，這個 bug 在串流狀態上完全隱形，檢查狀態永遠攔不到它。
//     為什麼會錯：把「讀到空內容」等同於「讀取失敗」。
//         對 getline 而言兩者是不同的事 —— 只有真正無法讀取（例如 EOF）
//         才會設 failbit。相對地，>> 讀到空內容才會設 failbit，
//         兩者的失敗定義並不一致，這也是混用時容易混淆的原因之一。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【本檔不加 LeetCode 範例的理由】
//   本檔的主題是標準輸入的緩衝區行為，屬於 I/O 細節，不含演算法。
//   LeetCode 的評測方式是直接呼叫你的函式、由平台處理輸入輸出，
//   完全不會觸及 cin/getline —— 這也正是「刷題刷再多也不會遇到這個 bug，
//   但實際寫互動式程式第一天就會踩到」的原因。

#include <iostream>
#include <string>   // std::getline 的 std::string 重載宣告在這裡，不是 <iostream>
#include <limits>   // std::numeric_limits —— 正確清空輸入緩衝區需要它

int main() {
    // ★ 一律初始化：未初始化的變數若在賦值前被讀取，是未定義行為。
    //   （C++11 起 >> 讀取失敗會把目標設為 0，但不該依賴這個行為。）
    int age = 0;
    std::string name;

    std::cout << "請輸入年齡: ";
    std::cin >> age;
    std::cout << "請輸入姓名: ";
    std::getline(std::cin, name);  // 問題！會讀到空字串, 
                                   // 因為前一次輸入年齡後，留下了一個換行符在輸入緩衝區中，getline() 會讀取這個換行符，導致 name 變成空字串。

    // 解決方法：在讀取年齡後，清除輸入緩衝區中的換行符
    //std::cin.ignore();  // 忽略前一次輸入留下的換行符
    //std::getline(std::cin, name);
    
    std::cout << "\n年齡: " << age << std::endl;
    std::cout << "姓名: [" << name << "]" << std::endl;
    
    // 另一種解決方法：先讀取年齡，然後再讀取姓名
    std::cout << "\n請輸入姓名: ";
    std::getline(std::cin, name);

    std::cout << "請輸入年齡: ";
    std::cin >> age;

    std::cout << "年齡: " << age << std::endl;
    std::cout << "姓名: [" << name << "]" << std::endl;

    // ========================================================================
    // 【解法三｜推薦】>> 之後明確清掉整行殘留，再 getline
    // ========================================================================
    // 這是真正治本的做法：不依賴呼叫順序，任何時候混用都安全。
    // 注意用的是 ignore(max(), '\n') 而不是無參數的 ignore()：
    //   - cin.ignore()                → 只丟棄「一個」字元
    //   - cin.ignore(max(), '\n')     → 丟棄直到換行為止的「全部」殘留
    // 使用者若輸入 "40   " 再按 Enter，前者會留下空白，後者才清得乾淨。
    std::cout << "\n=== 解法三：ignore 清空緩衝區後再 getline ===\n";
    std::cout << "請輸入年齡: ";
    if (!(std::cin >> age)) {
        std::cout << "（年齡讀取失敗，使用預設值 0）\n";
        age = 0;
        std::cin.clear();   // 清掉錯誤旗標，否則後續讀取全部失敗
    }
    // ★ 關鍵這一行：把 >> 留下的整行殘留（含換行）全部丟棄
    std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');

    std::cout << "請輸入姓名: ";
    if (!std::getline(std::cin, name)) {
        name = "(無輸入)";
    }

    std::cout << "年齡: " << age << std::endl;
    std::cout << "姓名: [" << name << "]" << std::endl;
    std::cout << "→ 這次姓名不再是空字串，因為殘留的換行已被 ignore 清掉\n";

    return 0;
}
    
// 編譯: g++ -std=c++17 -Wall -Wextra "第 5 課：輸入輸出流（iostream）入門10.cpp" -o io10
// 執行: printf '25\nAlice Wang\n30\n40\nBob Chen\n' | ./io10
//       ← 本檔需要標準輸入。上面這組輸入即為下方預期輸出所對應的資料。
//         五行分別餵給：①>>age ②getline(name) ③>>age ④>>age ⑤getline(name)

// ─────────────────────────────────────────────────────────────────────────────
// 【輸出但書】
//  1. 提示字串（「請輸入年齡: 」等）都沒有換行，而輸入來自管線不會被回顯,
//     因此多個提示會擠在同一行 —— 例如第一行是
//     「請輸入年齡: 請輸入姓名: 」。這是管線執行的正常現象,
//     互動執行時因為使用者按 Enter 會換行,畫面看起來不同。
//  2. 第一段的「姓名: []」是空的 —— 這正是本檔要示範的陷阱,不是程式故障。
//     >> 把使用者按下的換行留在緩衝區,緊接著的 getline 立刻讀到它,
//     於是讀到零個字元。★ 注意 getline 並沒有失敗,它成功讀了一「空行」。
//  3. 第二段（先 getline 再 >>）與第三段（>> 後 ignore 再 getline）
//     都正確讀到姓名,證明兩種解法都有效。
//  4. 輸出中「請輸入年齡: 請輸入姓名: 」該行尾端帶一個空白字元(來自提示字串
//     本身),下方預期輸出為避免行尾空白已將其去除,其餘與實際輸出相同。
//  5. 若不給輸入直接執行(./io10 < /dev/null),>> 會失敗;本檔已將 age
//     初始化為 0、檢查讀取結果並呼叫 cin.clear(),不會出現未初始化的值。
//  6. 以下為本機 g++ 15.2.0 (Ubuntu 26.04) 以上述輸入連續執行 3 次、
//     內容完全相同的結果。
// ─────────────────────────────────────────────────────────────────────────────

// === 預期輸出 ===
// 請輸入年齡: 請輸入姓名:
// 年齡: 25
// 姓名: []
//
// 請輸入姓名: 請輸入年齡: 年齡: 30
// 姓名: [Alice Wang]
//
// === 解法三：ignore 清空緩衝區後再 getline ===
// 請輸入年齡: 請輸入姓名: 年齡: 40
// 姓名: [Bob Chen]
// → 這次姓名不再是空字串，因為殘留的換行已被 ignore 清掉
