// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門14.cpp  —  四個串流狀態旗標的實際組合
// =============================================================================
//
// 【主題資訊 Information】
//   bool good() const;   // rdstate() == goodbit（四個位元全為 0）
//   bool eof()  const;   // rdstate() & eofbit
//   bool fail() const;   // rdstate() & (failbit | badbit)   ← 注意含 badbit
//   bool bad()  const;   // rdstate() & badbit
//   iostate rdstate() const;              // 取得原始位元組合
//   標準版本：C++98 起
//   複雜度  ：全部 O(1)
//   標頭檔  ：<iostream>
//
// 【詳細解釋 Explanation】
//
// 【1. 這四個函式不是互斥的，是位元查詢】
//   很多人把 good/eof/fail/bad 想成「串流的四種狀態，只會是其中一種」。
//   實際上底層只有【三個位元】（eofbit、failbit、badbit），
//   而這四個函式是對這三個位元做不同的查詢：
//       good() → 三個位元都是 0
//       eof()  → eofbit  是否為 1
//       fail() → failbit 或 badbit 任一為 1
//       bad()  → badbit  是否為 1
//   所以 eof() 和 fail() 【可以同時為真】，本檔的第三種情境就是如此。
//
// 【2. 本檔三種輸入分別會得到什麼組合】
//   （下方數值皆為本機實測）
//     • 輸入 "5"（成功）
//         good=1 eof=0 fail=0 bad=0
//         —— 一切正常，四個位元全乾淨。
//     • 輸入 "abc"（格式錯誤）
//         good=0 eof=0 fail=1 bad=0
//         —— failbit 立起。注意 eof 是 0：資料還在緩衝區裡，
//            只是解析不成功，並沒有讀到檔尾。
//     • 空輸入（直接 EOF）
//         good=0 eof=1 fail=1 bad=0
//         —— 【eof 與 fail 同時為 1】。這是最關鍵的一格：
//            因為「想讀卻沒東西可讀」既是到達檔尾（eofbit），
//            也是這次操作沒有成功（failbit）。
//   把這三列並排看，就能明白為什麼「用 fail() 判斷格式錯誤」是不夠的。
//
// 【3. 為什麼 fail() 要把 badbit 也算進去】
//   fail() 的語意是「上一次操作沒有成功」，
//   而串流損毀（badbit）當然也算沒成功。
//   這個設計讓最常見的檢查 if (!stream) 只要看一個條件就好
//   —— operator bool 回傳的正是 !fail()。
//   代價是無法用 fail() 區分「可以重試」與「該放棄」，
//   需要區分時必須另外呼叫 bad()。
//
// 【4. bad() 在什麼情況下才會為真】
//   badbit 表示串流的完整性已經無法保證，典型原因包括：
//     • 底層 streambuf 的操作丟出例外（如磁碟 I/O 錯誤）
//     • 對已關閉或無效的檔案串流做操作
//     • 記憶體配置失敗
//   單純「使用者打錯字」或「讀到檔尾」【永遠不會】設 badbit。
//   所以在 std::cin 上要製造 bad()=1 相當困難，
//   本檔的三種情境全都是 bad=0 —— 這本身就是有意義的觀察。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 印出來的是 1 和 0，不是 true 和 false
//   std::cout << bool 預設用 numeric 格式印出 1/0。
//   要印 true/false 得加 std::boolalpha：
//       std::cout << std::boolalpha << std::cin.good();
//   boolalpha 是黏性的（設定後持續有效），和 setw 的一次性不同。
//
// (B) 為什麼本檔不會有未初始化讀取的問題
//   int x; 沒有初始值，但在印出任何狀態之前就先 >> x 了。
//   而本檔只做【一次】讀取 —— 無論成功或失敗，
//   自 C++11 起這一次 >> 都保證會寫入 x（成功寫值、失敗寫 0、溢位寫極值）。
//   而且本檔根本不印 x，只印狀態旗標，所以完全沒有 UB 疑慮。
//   （對照同課 6 號檔：那裡連續三次 >>，一旦中途失敗，
//     後面的變數就不會被寫入，那才是真的 UB。）
//
// (C) 清除狀態的順序與 rdstate
//   clear() 是「設定」狀態不是「清除」；
//   要保留某些位元只清特定一個，得用：
//       cin.clear(cin.rdstate() & ~std::ios::failbit);
//   實務上幾乎都是直接 clear() 全清後再 ignore()。
//
// 【注意事項 Pay Attention】
//   1. eof() 與 fail() 【會同時為真】（讀到檔尾時）。
//      它們不是互斥的狀態，是三個位元的不同查詢。
//   2. fail() 包含 badbit。要區分「格式錯」與「串流壞掉」必須另查 bad()。
//   3. eof() 是【事後】狀態，不能拿來預測「還有沒有下一筆資料」，
//      所以 while (!cin.eof()) 是錯誤的迴圈寫法。
//   4. 印 bool 預設是 1/0；要 true/false 請用 std::boolalpha。
//   5. 本檔沒有提示字串，直接讀取，適合用管線餵資料觀察。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】good / eof / fail / bad 的關係
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. eof() 為真時，fail() 也會是真嗎？
//     答：在「試圖讀取卻沒有資料」的情況下，兩者【同時】為真。
//         因為讀到檔尾既設定了 eofbit（到底了），
//         也讓這次操作失敗而設定 failbit（沒讀到東西）。
//         本機實測空輸入的結果是 good=0 eof=1 fail=1 bad=0。
//         但反過來不成立：格式錯誤時 fail=1 而 eof=0。
//     追問：那有沒有 eof=1 但 fail=0 的情況？
//           → 有。若剛好讀完最後一個字元同時碰到檔尾，
//             這次讀取是成功的（eofbit 已設、failbit 未設）。
//             這也是為什麼不能用 eof() 來判斷「上一次讀取是否成功」。
//
// 🔥 Q2. 為什麼 while (!std::cin.eof()) 是錯誤的迴圈寫法？
//     答：eofbit 是【已經試圖讀取並碰到底之後】才被設定的事後狀態，
//         不是「還有沒有資料」的預測。
//         所以迴圈會在最後一次讀取失敗之後才發現 eof，
//         導致多跑一輪、處理到一筆根本沒讀進來的資料。
//         正確寫法是把讀取動作本身當條件：
//             while (std::cin >> x) { ... }
//             while (std::getline(std::cin, line)) { ... }
//     追問：那 while (std::cin >> x) 為什麼能正確結束？
//           → 因為 >> 回傳 istream&，而 istream 的 explicit operator bool()
//             回傳 !fail()。讀取與判斷發生在同一個表達式裡，
//             不存在「先判斷後讀取」的時間差。
//
// ⚠️ 陷阱. 想偵測「使用者輸入了非數字」，用 if (std::cin.fail()) 夠嗎？
//     答：不夠。fail() 為真至少有三種原因：格式不符、資料耗盡（EOF）、
//         串流損毀（badbit）。本機實測顯示
//         輸入 "abc" 得到 fail=1 eof=0，
//         而空輸入得到 fail=1 eof=1 —— 兩者的 fail() 完全一樣。
//         要精確判斷必須先看 eof()，必要時再看 bad()。
//     為什麼會錯：fail 這個名字讓人聯想到「格式錯誤」，
//         但它真正的語意是「上一次操作沒有成功」。
//         在單次讀取的小程式裡混淆無傷大雅；
//         一旦寫進重試迴圈，把 EOF 誤判成格式錯誤就會造成【無限空轉】
//         —— 同課 13 號檔實測 3 秒內可印出六百多萬行錯誤訊息。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>

int main() {
    int x;
    
    std::cin >> x;
    
    // 檢查輸入狀態, 以確保輸入成功
    // good() 返回 true 如果輸入成功，沒有錯誤
    // eof() 返回 true 如果達到輸入流的結尾, 例如，當用戶按下 Ctrl+D (Unix/Linux) 或 Ctrl+Z (Windows) 時
    // fail() 返回 true 如果輸入失敗（例如，輸入的類型不匹配）, 但流仍然可用
    // bad() 返回 true 如果輸入流處於不可恢復的錯誤狀態, 例如，硬件故障
    std::cout << "good: " << std::cin.good() << std::endl;
    std::cout << "eof: " << std::cin.eof() << std::endl;
    std::cout << "fail: " << std::cin.fail() << std::endl;
    std::cout << "bad: " << std::cin.bad() << std::endl;
    
    return 0;
}

