// =============================================================================
//  summary.cpp（第 5 課 總複習） — iostream:型別安全、可擴充、但有緩衝
// =============================================================================
//
// 【主題資訊 Information】
//   四個標準流物件（<iostream>）：
//     std::cout  標準輸出，有緩衝              對應 C 的 stdout
//     std::cin   標準輸入                      對應 C 的 stdin
//     std::cerr  標準錯誤，unit-buffered（每次操作後即刷出）  對應 stderr
//     std::clog  標準日誌，有緩衝              同樣寫到 stderr
//   核心運算子：
//     std::ostream& operator<<(ostream&, T);   // 插入（輸出）
//     std::istream& operator>>(istream&, T&);  // 提取（輸入）
//     std::istream& getline(istream&, string&, char delim = '\n');  // <string>
//   標準版本：C++98 起。<iomanip> 的操縱器亦同。
//   複雜度  ：O(處理的字元數)。真正的效能差異來自「flush 次數」，
//             而不是字元複製本身。
//
// 【詳細解釋 Explanation】
//
// 【1. 「流」是什麼：一個統一的資料通道抽象】
//   流（stream）把「資料的來源／去處」抽象成同一個介面：
//   螢幕、鍵盤、檔案、記憶體字串（stringstream）、網路，全都用同一套 << >>。
//   這帶來兩個 printf/scanf 沒有的性質：
//     - 型別安全：<< 由重載解析在「編譯期」選定函式；
//                 printf 的 %d 只是執行期才被解讀的字串，型別對不上就是 UB。
//     - 可擴充：自訂類別只要提供 operator<<，就能像內建型別一樣被輸出；
//               printf 無法為新型別新增格式符。
//   代價是 iostream 較冗長、編譯較慢，且格式化語法不如 printf 緊湊
//   （這也是 C++20 引入 std::format 的原因 —— 兼具兩者的優點）。
//
// 【2. 四個流物件的分工，以及為什麼 cerr 不緩衝】
//   cout 有緩衝：字元先累積在記憶體，滿了或 flush 時才寫出，效率高。
//   cerr 是 unit-buffered：每次輸出操作結束後即刷出。理由很實際 ——
//   錯誤訊息最需要被看到的時刻，正是程式即將崩潰的時候；
//   若它被留在緩衝區裡，崩潰時就一起消失了。
//   clog 同樣寫到 stderr，但有緩衝 —— 適合「量大且不急」的日誌。
//   ★ cerr 與 clog 都走 stderr，所以在命令列可以分流：
//       ./summary > out.txt 2> err.txt        （1=stdout，2=stderr）
//
// 【3. endl 與 '\n'：本課最常被誤用的一點】
//   std::endl = 輸出換行 + 呼叫 flush()。'\n' 只做前者。
//   flush 代表一次實際的寫出動作，成本遠高於往緩衝區塞一個字元。
//   所以迴圈中一律用 '\n'；只有在「必須立刻看到」時才用 endl。
//   反過來說，程式「正常結束」時 cout 保證會被 flush，
//   所以不必為了「怕漏字」而到處寫 endl —— 那個保證已經涵蓋正常路徑。
//   （abort／未捕捉訊號終止則不在保證範圍內。）
//
// 【4. >> 與 getline 的互動陷阱】
//   >> 讀完值後，會把終止它的空白字元「留在緩衝區」；
//   getline 則會把分隔字元「取走丟棄」。
//   於是 cin >> age; 之後緊接 getline(cin, name); 會立刻讀到殘留的換行，
//   得到空字串 —— 而且 getline「並沒有失敗」，不會設任何錯誤旗標。
//   正解是在兩者之間清乾淨：
//       std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
//   注意不帶參數的 cin.ignore() 只丟棄「一個」字元，輸入若有多餘空白就失效。
//   （完整示範見本課的「…入門10.cpp」，該檔有可執行的三段對照。）
//
// 【5. 錯誤處理三步驟：fail → clear → ignore】
//   對 int 輸入字母時，>> 會設 failbit，而且「不會取走」那些字元。
//   若不處理就繼續迴圈讀取，會因為那些字元一直卡在緩衝區而陷入無窮迴圈 ——
//   這是初學者最常見的當機模式。正確做法固定為三步：
//       if (std::cin.fail()) {
//           std::cin.clear();                                  // ① 清錯誤旗標
//           std::cin.ignore(numeric_limits<streamsize>::max(), '\n');  // ② 丟壞資料
//           // ③ 重新提示並再試
//       }
//   本檔的 readInt() 就是這個模式的完整封裝。
//
// 【6. iomanip：哪些會「黏住」，哪些不會】
//   這是格式化輸出最容易出錯的地方：
//     sticky（持續生效，直到被改回來）：
//       setprecision、fixed、scientific、setfill、left/right、
//       hex/oct/dec、boolalpha、showbase
//     non-sticky（只影響「下一個」輸出項）：
//       setw
//   所以表格輸出必須「每一欄都重寫一次 setw」，
//   而 setfill('0') 用完若不改回 setfill(' ')，後面所有欄位都會被補零。
//   本檔在每個小節結束時都明確恢復預設值，正是這個原因。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼 cout 印 3.14159265358979 只會得到 3.14159
//   預設浮點格式的有效位數是 6（precision 預設值為 6）。
//   setprecision(n) 在「預設模式」下代表『有效位數』，
//   但在 fixed / scientific 模式下改為代表『小數位數』——
//   同一個操縱器，語意隨模式改變，這是常見的困惑來源。
//   本檔輸出中 setprecision(3) 得到 3.14（3 位有效數字）、
//   fixed+setprecision(2) 得到 3.14（2 位小數），可直接對照。
//
// (B) sync_with_stdio 與 tie：競賽加速的兩行咒語從何而來
//   預設情況下 C++ 串流與 C 的 stdio 是同步的（可安全混用 cout/printf），
//   且 cin 與 cout 是綁定（tie）的（每次讀取前自動 flush cout，
//   確保提示訊息一定先顯示）。這兩個保證都有成本，所以競賽常寫：
//       std::ios::sync_with_stdio(false);
//       std::cin.tie(nullptr);
//   ★ 代價要清楚：關掉同步後就「不可以」再混用 printf/scanf；
//     解除 tie 後，互動式程式的提示可能在等待輸入時還沒顯示出來。
//
// (C) 為什麼 stdout 與 stderr 的相對順序不保證
//   cout 有緩衝、cerr 幾乎沒有。當兩者都被重導到「同一個檔案或管線」時，
//   cout 的內容可能仍在緩衝區裡，而 cerr 已經寫出去了 ——
//   結果就是行序看起來被打亂。這不是 bug，而是兩條獨立的通道。
//   要保證順序，就得在切換前明確 flush，或全部寫到同一個串流。
//
// 【注意事項 Pay Attention】
//   1. setw 只影響「下一個」輸出項，表格每欄都要重設。
//      setfill、setprecision、fixed、hex 等則會一直黏著。
//   2. >> 失敗時「不會消耗」壞字元。忘了 ignore 就重試 = 無窮迴圈。
//   3. std::getline 的 std::string 版本宣告在 <string>，不是 <iostream>。
//   4. 在字串常值中要「顯示」反斜線 n，必須寫 "\\n"；寫 "\n" 會變成真正的換行。
//      ★ 本檔原本第 319 行就犯了這個錯（把一句話從中間切成兩行），已修正。
//   5. cerr／clog 寫到 stderr。本檔末尾的預期輸出是 stdout 與 stderr
//      合併擷取（2>&1）的結果，兩者的相對順序不受標準保證（見概念補充 C）。
//   6. hex/oct 等進位操縱器只影響「整數」，不影響浮點數與字串。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】iostream
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::endl 和 '\n' 差在哪？為什麼說迴圈裡不該用 endl？
//     答：endl 是「換行 + flush」，'\n' 只換行。flush 會觸發一次實際的寫出
//         （系統呼叫），比往緩衝區塞一個字元昂貴得多。迴圈中每行都 flush，
//         等於把批次寫入拆成成千上萬次個別寫入。
//         程式正常結束時 cout 保證會被 flush，所以平常根本不需要手動 flush。
//     追問：那什麼時候真的需要 endl？
//         → 程式可能崩潰而想保住訊息（但這種情況更該用 cerr）、
//           或輸出被導向管線而另一端正在即時讀取。
//           至於「印提示後等輸入」通常不必處理 —— cin 預設與 cout 綁定（tie），
//           讀取前會自動 flush。
//
// 🔥 Q2. 對 int 變數輸入字母後，程式為什麼會陷入無窮迴圈？怎麼修？
//     答：>> 轉換失敗時會設定 failbit，並且「不取走」那些無法轉換的字元 ——
//         它們原封不動留在緩衝區。迴圈再讀一次，又遇到同樣的字元、又失敗，
//         如此永遠循環。修法固定三步：
//           cin.clear();                                      // 清除錯誤旗標
//           cin.ignore(numeric_limits<streamsize>::max(), '\n');  // 丟掉壞資料
//           然後重新提示。
//         只做 clear() 不做 ignore() 仍會無窮迴圈 —— 壞資料還在。
//     追問：C++11 之後 >> 失敗，目標變數的值會變成什麼？
//         → 會被設為 0（C++11 起）。C++11 之前則維持原值不變。
//           但兩種情況都不該依賴 —— 應該檢查讀取是否成功再使用。
//
// 🔥 Q3. iomanip 的操縱器中，哪一個和其他的行為不同？
//     答：setw。它是 non-sticky —— 只影響「緊接的下一個」輸出項，
//         之後立刻失效。其餘如 setprecision、fixed、setfill、left/right、
//         hex/dec、boolalpha 都是 sticky，會一直生效直到被改回來。
//         所以表格輸出必須每一欄都重寫 setw，而 setfill('0') 用完
//         若不改回 setfill(' ')，後續所有欄位都會被補零。
//     追問：setprecision 的語意是固定的嗎？
//         → 不是，它隨浮點模式改變：預設模式下代表「有效位數」，
//           fixed／scientific 模式下代表「小數位數」。
//           本檔輸出中 setprecision(3) 得 3.14、fixed+setprecision(2) 也得 3.14，
//           數字相同但意義完全不同。
//
// ⚠️ 陷阱 1. 我把 stdout 和 stderr 都導到同一個檔案，
//          為什麼錯誤訊息跑到了前面幾行輸出的上面？
