//     答：因為在字串常值中 \n 就是「換行字元」的跳脫寫法，
//         編譯器會把它變成真正的換行，而不是反斜線加 n 兩個字元。
//         要顯示字面上的 \n，必須寫成 "\\n"（雙反斜線）。
//     為什麼會錯：把「引號內的內容」直覺當成「原樣輸出」，
