// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門（1） — cout、<< 與 endl 的真面目
// =============================================================================
//
// 【主題資訊 Information】
//   std::ostream& operator<<(ostream&, const char*);   // 以及數十個重載
//   std::ostream& std::endl(std::ostream& os);         // 換行 + flush
//   標準版本：C++98 起。std::cout 定義於 <iostream>。
//   標頭檔  ：<iostream>
//   複雜度  ：單次輸出 O(輸出長度)。但真正的成本差異不在複製字元，
//             而在「有沒有 flush」——見下方【3】。
//
// 【詳細解釋 Explanation】
//
// 【1. std::cout 是「物件」，不是函式或關鍵字】
//   cout 是 std::ostream 型別的一個全域物件（連接到標準輸出）。
//   所以 std::cout << "Hi" 不是什麼特殊語法，而是一次運算子重載呼叫：
//       operator<<(std::cout, "Hi")
//   它回傳「同一個串流的引用」（ostream&），這正是能寫成
//       std::cout << a << b << c;
//   的原因 —— 它會被解析成 ((cout << a) << b) << c，
//   每一次呼叫都把串流本身傳回去給下一個 <<。
//   若 operator<< 回傳 void，鏈式輸出就會當場編譯失敗。
//
// 【2. << 為什麼看起來「什麼型別都吃」】
//   因為 std::ostream 為各種內建型別各提供了一個 operator<< 重載
//   （int、double、bool、char、const char*、void* …），
//   而 std::string 等標準型別另有非成員的重載。
//   編譯器依引數型別做重載解析，挑出對應的那一個 —— 不是執行期判斷型別。
//   這也是為什麼自訂類別要能被 cout 印出，必須自己提供
//   operator<<(std::ostream&, const T&)（見第 36 課運算子重載）。
//
// 【3. endl 與 '\n' 的差別：這是本檔最重要的一點】
//   std::endl 做「兩件事」：輸出一個換行字元，然後呼叫 os.flush()。
//   '\n' 只做第一件事。
//   std::cout 預設是有緩衝的（buffered）—— 字元先累積在記憶體緩衝區，
//   等到緩衝區滿、程式正常結束、或被明確 flush 時，才真正寫到終端。
//   flush 意味著一次系統呼叫（write），那是相對昂貴的操作。
//   結論：
//     - 一般換行請用 '\n'，讓串流自己決定何時 flush（效能好得多）。
//     - 只有在「必須立刻看到輸出」時才用 endl 或 std::flush，例如：
//         * 程式可能崩潰，希望崩潰前的訊息不要卡在緩衝區裡
//         * 互動式提示（印出提示後要等使用者輸入）
//         * 輸出被導向管線／檔案，而另一端在即時讀取
//   在迴圈中每行都用 endl，是初學者最常見的效能問題來源之一。
//
// 【4. 為什麼程式正常結束時不會漏字】
//   本檔第一行 std::cout << "Hello, World!"; 沒有換行也沒有 flush，
//   但輸出仍完整出現。原因是 std::cout 在程式正常結束時會被自動 flush
//   （標準保證：與 cout 關聯的緩衝區在 main 回傳後、
//   靜態物件解構期間被清空）。
//   ★ 但這個保證只涵蓋「正常結束」。若程式因 std::abort()、
//     未捕捉的訊號、或 _Exit() 而終止，緩衝區內的字元「可能」遺失。
//     這正是「示範崩潰的教材要用 stdbuf -o0 收集輸出」的原因。
//
// 【概念補充 Concept Deep Dive】
//   緩衝與同步的兩個實務細節：
//   (A) std::cout 與 C 的 stdio（printf）預設是「同步」的。
//       這讓 cout 與 printf 混用時輸出順序正確，代價是 cout 無法使用
//       自己更快的緩衝策略。競賽程式常寫
//           std::ios::sync_with_stdio(false);
//       來關掉同步換取速度 —— 但關掉後就不可以再混用 printf/scanf。
//   (B) std::cin 預設與 std::cout「綁定」（tie）。
//       每次從 cin 讀取前，cout 會被自動 flush。
//       這就是為什麼「印出提示 → 等待輸入」即使沒寫 endl 也能正常顯示提示。
//       競賽中常見的 std::cin.tie(nullptr); 就是解除這個綁定。
//
// 【注意事項 Pay Attention】
//   1. endl 不是換行字元，是一個「操縱器」（manipulator，其實是函式）。
//      寫成 std::cout << "endl"; 只會印出字串 endl，兩者完全不同。
//   2. 迴圈內請用 '\n' 而非 endl。差別只在 flush 次數，但量大時很可觀。
//   3. 用雙引號的 "\n"（字串）與單引號的 '\n'（字元）都可以，
//      單引號版本略省一點點（不需處理字串結尾），但差異微不足道。
//   4. 程式正常結束會自動 flush；abort／訊號終止「不保證」會 flush。
//      要在崩潰示範中完整取得輸出，收集時請用 stdbuf -o0。
//   5. std::cout 本身「不是」執行緒安全的整體操作：多執行緒同時輸出
//      不會損壞串流，但各段文字可能交錯。需要完整行請自行加鎖。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】cout 與 endl
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::endl 和 '\n' 有什麼差別？該用哪一個？
//     答：endl 輸出換行「並且」呼叫 flush()，'\n' 只輸出換行。
//         flush 會觸發一次實際的寫出（系統呼叫），成本明顯高於單純
//         往緩衝區塞一個字元。所以一般情況（尤其是迴圈中）應該用 '\n'，
//         讓串流自行決定何時寫出；只有在「必須立刻看到」時才用 endl。
//     追問：什麼情況「必須立刻看到」？
//         → 程式可能崩潰而想保住訊息、印出提示後要等使用者輸入、
//           或輸出被導向管線而另一端正在即時讀取。
//           不過第二種情況通常不必手動處理 —— cin 預設與 cout 綁定（tie），
//           讀取前會自動 flush。
//
// 🔥 Q2. std::cout << a << b 為什麼可以串接？
//     答：因為 operator<< 回傳的是 std::ostream&（同一個串流的引用）。
//         這個運算式被解析成 (std::cout << a) << b：第一次呼叫回傳 cout，
//         第二個 << 就有左運算元可用。若回傳型別是 void，
//         第二個 << 會因為「左運算元是 void」而編譯失敗。
//     追問：那我自己的類別要怎麼支援 cout？
//         → 提供非成員函式 std::ostream& operator<<(std::ostream&, const T&)，
//           並且務必回傳那個 ostream&。它不能是成員函式，
//           因為左運算元是 ostream 而不是你的類別（見第 36 課）。
//
// ⚠️ 陷阱. 我的程式印了很多訊息然後崩潰，為什麼最後幾行沒出現？
//         是不是崩潰前那幾行根本沒執行？
//     答：不一定。std::cout 是有緩衝的：字元先進記憶體緩衝區，
//         等到緩衝區滿、程式正常結束或明確 flush 時才真正寫出。
//         程式若以 abort／未捕捉訊號終止，緩衝區內的內容可能來不及寫出而遺失。
//         也就是說：那幾行「執行了」，只是還沒離開緩衝區。
//     為什麼會錯：把「輸出敘述執行完畢」等同於「文字已經出現在畫面上」。
//         兩者中間隔著一層緩衝區。除錯時可改用 std::cerr
//         （預設不緩衝／立即輸出），或在收集輸出時使用 stdbuf -o0。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【本檔不加 LeetCode 範例的理由】
//   本檔的主題是「輸出串流與緩衝機制」，屬於 I/O 行為，不含任何演算法。
//   （附帶一提：LeetCode 幾乎不考 I/O —— 它直接呼叫你的函式，
//     輸入輸出由平台處理，這也是為什麼刷題不會訓練到這部分的能力。）
//   下方以最小的可觀察實驗來呈現 endl 與 '\n' 的差別，而非硬掛演算法題。

#include <iostream>

int main() {
    std::cout << "Hello, World!";
    std::cout << std::endl;  // 換行並清空緩衝區

    // ── 觀察 1：'\n' 與 endl 的輸出結果完全相同 ──────────────────────
    // 差別不在「看得到什麼」，而在「什麼時候被寫出去」。
    // 兩者的可見輸出一模一樣，所以無法從輸出本身分辨 —— 這正是重點：
    // endl 多做的那件事（flush）是不可見的，卻是有成本的。
    std::cout << "用 '\\n' 換行" << '\n';
    std::cout << "用 endl 換行" << std::endl;

    // ── 觀察 2：<< 可以串接，因為它回傳 ostream& ─────────────────────
    // 下面這一行會被解析成 ((((cout << "a=") << 1) << " b=") << 2) << '\n'
    int a = 1, b = 2;
    std::cout << "a=" << a << " b=" << b << '\n';

    // ── 觀察 3：同一個 << 對不同型別各自挑到不同的重載 ────────────────
    // 這是「編譯期的重載解析」，不是執行期的型別判斷。
    std::cout << "int:    " << 42        << '\n';
    std::cout << "double: " << 3.5       << '\n';
    std::cout << "bool:   " << true      << "   ← 預設印 1，不是 true\n";
    std::cout << "char:   " << 'A'       << '\n';
    std::cout << "字串:   " << "text"    << '\n';

    // ── 觀察 4：明確 flush，與 endl 的效果相同 ───────────────────────
    std::cout << "手動 flush" << '\n' << std::flush;

    return 0;
}

