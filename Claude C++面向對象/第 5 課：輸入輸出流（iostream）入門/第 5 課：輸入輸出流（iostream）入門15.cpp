// =============================================================================
//  第 5 課：輸入輸出流（iostream）入門15.cpp  —  setprecision 與 fixed 的精度控制
// =============================================================================
//
// 【主題資訊 Information】
//   /*unspecified*/ setprecision(int n);        // <iomanip>
//   std::ios_base&  fixed(std::ios_base& str);  // <ios>（由 <iostream> 引入）
//   相關：std::scientific、std::defaultfloat（C++11）、std::hexfloat（C++11）
//
//   語意：precision 的意義【隨浮點格式而變】——
//         預設（defaultfloat）：n = 有效位數（significant digits）
//         fixed / scientific  ：n = 小數點後位數
//   預設值  ：precision() == 6
//   標準版本：C++98（setprecision / fixed）、C++11（defaultfloat / hexfloat）
//   複雜度  ：O(1) 設定；實際輸出成本與位數成正比
//   標頭檔  ：<iostream>、<iomanip>
//
// 【詳細解釋 Explanation】
//
// 【1. 本檔最重要的一課：precision 的意義會變】
//   同樣是 setprecision(3)，在兩種模式下意思完全不同：
//       預設模式  ：3 位【有效數字】→ 3.14159265 印成 3.14
//       fixed 模式：3 位【小數】    → 3.14159265 印成 3.142
//   本檔的輸出正好展示了前者：
//       精度3  → 3.14      （3 位有效數字：3、1、4）
//       精度8  → 3.1415927 （8 位有效數字，第 8 位發生四捨五入）
//   而最後一行切到 fixed 之後：
//       fixed精度2 → 3.14  （2 位小數）
//   這裡有個容易混淆的巧合：「精度3」和「fixed精度2」都印出 3.14，
//   但成因完全不同 —— 一個是 3 位有效數字，一個是 2 位小數。
//
// 【2. 為什麼「精度8」是 3.1415927 而不是 3.1415926】
//   pi 的實際值是 3.14159265358979。
//   取 8 位有效數字要看第 9 位（5）決定進位 → 3.1415926|5… → 進位成 3.1415927。
//   這是【輸出時的四捨五入】，變數本身的值完全沒有改變。
//   iostream 只負責把 double 轉成十進位文字，不會動到原始資料。
//
// 【3. 這些 manipulator 都是「黏性」的】
//   setprecision 與 fixed 設定之後【一直有效】，直到被改變為止。
//   這和 setw（只影響下一個輸出）形成強烈對比 ——
//   是 iomanip 最常見的錯誤來源。
//   本檔第三行設了 setprecision(8)，第四行的 fixed 之後，
//   precision 仍然是被 setprecision(2) 覆蓋的值。
//   若第四行只寫 fixed 而不重設精度，會得到 3.14159265（8 位小數）。
//
// 【4. 怎麼還原成預設狀態】
//   常見的錯誤是以為 std::cout.unsetf(std::ios::fixed) 就好，
//   但更清楚的做法是用 C++11 的 std::defaultfloat：
//       std::cout << std::defaultfloat << std::setprecision(6);
//   若要完整存檔／還原所有格式狀態，可用：
//       std::ios state(nullptr);
//       state.copyfmt(std::cout);      // 存
//       std::cout.copyfmt(state);      // 還原
//   在函式裡臨時改格式時，這是唯一安全的做法
//   —— 否則會把格式污染帶給呼叫者。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼 setprecision 的回傳型別是 /*unspecified*/
//   標準不規定它回傳什麼具體型別，只規定它是個能被 << 接受的物件。
//   實作上通常是一個小的暫存 struct（如 libstdc++ 的 _Setprecision），
//   配合對應的 operator<< 多載去呼叫 os.precision(n)。
//   這個設計讓 manipulator 可以帶參數，而且零額外執行期成本
//   —— 編譯後就只是一次 precision() 的呼叫。
//
// (B) 浮點輸出的精度上限
//   double 有 53 位二進位尾數，約等於 15~17 位十進位有效數字。
//   要能「印出來再讀回去得到完全相同的 double」，
//   需要 std::numeric_limits<double>::max_digits10 位（本機為 17）。
//   而 digits10（本機為 15）代表「任何 15 位十進位數都能無損存進 double」。
//   兩者不同，序列化時要用 max_digits10 才不會失真。
//   （C++17 起更好的做法是 std::to_chars，它保證最短且可往返。）
//
// (C) 為什麼 3.14159265358979 存進 double 後不是精確值
//   二進位浮點無法精確表示大多數十進位小數。
//   pi 的字面值會被轉成最接近的 double，本身就有誤差。
//   所以「印出 3.1415927」是【兩層】近似的結果：
//   先是十進位轉二進位的誤差，再是輸出時的四捨五入。
//   浮點數比較永遠不該用 ==，就是這個原因。
//
// 【注意事項 Pay Attention】
//   1. precision 在預設模式是「有效位數」、在 fixed/scientific 是「小數位數」。
//      這是本主題最大的陷阱。
//   2. setprecision 與 fixed 都是【黏性】的，會一直影響後續輸出；
//      setw 則只影響下一個。不要把兩者的行為混為一談。
//   3. 四捨五入只發生在輸出，變數的值不會被改變。
//   4. 本檔宣告了 int num = 42; 但從未使用，
//      編譯時會出現一個 -Wunused-variable 警告 —— 這是原始教材保留的內容，
//      對執行結果沒有任何影響。
//   5. 要在函式內臨時改格式，請用 copyfmt 存檔還原，
//      否則會污染呼叫者的 std::cout 狀態。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】setprecision 與浮點輸出格式
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::setprecision(3) 到底是「3 位小數」還是「3 位有效數字」？
//     答：兩者都可能 —— 取決於當下的浮點格式。
//         預設（defaultfloat）模式下是【有效數字】：3.14159 印成 3.14。
//         加上 std::fixed 之後變成【小數位數】：3.14159 印成 3.142。
//         本檔的輸出同時展示了這兩種：「精度3」得到 3.14（3 位有效數字），
//         「fixed精度2」也得到 3.14（2 位小數）—— 數字巧合相同，成因不同。
//     追問：那 scientific 模式呢？
//           → 也是小數位數，但指的是尾數部分的小數位，
//             例如 setprecision(2) << scientific << 3.14159 得到 3.14e+00。
//
// 🔥 Q2. setprecision 設定一次之後，還會影響後面的輸出嗎？
//     答：會。setprecision 和 fixed 都是【黏性（sticky）】的，
//         一旦設定就持續有效直到被改變。
//         這和 setw 完全相反 —— setw 只影響緊接著的那一個輸出，
//         之後 width 會被自動重設為 0。
//         把兩者的行為記反了，是 iomanip 最常見的 bug 來源。
//     追問：怎麼還原成預設？
//           → std::cout << std::defaultfloat << std::setprecision(6);（C++11）
//             要完整還原所有格式旗標則用 copyfmt 存檔／還原。
//
// ⚠️ 陷阱. std::cout << std::setprecision(8) << pi 印出 3.1415927，
//          是不是代表 pi 這個變數被四捨五入、精度變差了？
//     答：不是。變數的值【完全沒有改變】。
//         setprecision 只影響「把 double 轉成十進位文字」這個輸出步驟，
//         pi 在記憶體裡仍然是原本那個 double。
//         下一行若改用 setprecision(15) 印同一個變數，
//         會看到更多位數 —— 因為資料一直都在。
//     為什麼會錯：把「顯示格式」和「資料本身」混為一談。
//         這個誤解在實務上很危險：有人會以為
//         「印出來只有 2 位小數，所以存進資料庫也只有 2 位」，
//         結果金額累加後出現對不上的尾差。
//         格式化是【呈現層】的事，任何真正要改變數值精度的動作
//         （四捨五入到分、無條件捨去）都必須明確地用
//         std::round / std::floor 對數值本身操作。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <iomanip>  // 格式化控制器

int main() {
    double pi = 3.14159265358979;
    int num = 42;
    
    // 設定小數精度
    std::cout << "預設: " << pi << std::endl;
    std::cout << "精度3: " << std::setprecision(3) << pi << std::endl;
    std::cout << "精度8: " << std::setprecision(8) << pi << std::endl;
    
    // fixed 固定小數點表示法
    std::cout << "fixed精度2: " << std::fixed << std::setprecision(2) 
              << pi << std::endl;
    
    return 0;
}

