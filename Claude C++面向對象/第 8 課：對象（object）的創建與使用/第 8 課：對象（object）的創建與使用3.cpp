// =============================================================================
//  第 8 課：對象（object）的創建與使用3.cpp  —  堆疊 vs 堆積、. 與 -> 的對照
// =============================================================================
//
// 【主題資訊 Information】
//   堆疊：Cat c1;            自動儲存期，離開作用域自動解構，用 . 存取
//   堆積：Cat* c2 = new Cat(); 動態儲存期，必須 delete，用 -> 存取
//   等價關係：ptr->member  ≡  (*ptr).member
//   標準版本：C++98 起（nullptr 為 C++11）
//   複雜度：堆疊配置約一道指令；堆積配置需經過 allocator，慢上數十倍
//   標頭檔：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. 這兩種方式的真正差別是「誰決定生命週期」】
//   語法差異（. vs ->）只是表面，本質差別在於：
//       堆疊物件 —— 生命週期由【作用域】決定，編譯期就固定了
//       堆積物件 —— 生命週期由【程式設計者】決定，可以跨越作用域
//   所以選擇的判準不是「哪個比較快」，而是：
//       「這個物件需要活得比目前這個作用域更久嗎？」
//   不需要 → 一律用堆疊（更快、更安全、不會洩漏）
//   需要   → 用堆積，而且應該用智慧指標而非裸指標
//
// 【2. -> 只是 (*p). 的語法糖，但沒那麼單純】
//   對裸指標而言 ptr->name 與 (*ptr).name 完全等價，
//   本檔第三行的 (*c2).meow() 就是刻意展示這一點。
//   但要注意 -> 是可以【被多載】的運算子，
//   而且它有一個特殊規則：多載的 operator-> 會【遞迴地】繼續套用，
//   直到得到一個真正的指標為止。
//   這正是 std::unique_ptr、std::shared_ptr、迭代器能寫成 p->member 的原理。
//   相對地，operator* 沒有這個遞迴行為 ——
//   所以對智慧指標而言，-> 並不只是 (*p). 的簡寫。
//
// 【3. 什麼時候真的需要堆積】
//   常見的四種正當理由：
//     ① 生命週期必須超出建立它的作用域（例如工廠函式回傳物件）
//     ② 物件很大，放堆疊會超過上限（本機 8 MiB）
//     ③ 數量或大小在執行期才知道
//     ④ 需要多型 —— 基類指標指向派生類物件
//   注意第 ②③④ 點在現代 C++ 中通常由【容器】處理掉了：
//   std::vector 內部就用堆積，但你完全不必碰 new/delete。
//
// 【4. 本檔的 delete 之後為什麼要設 nullptr】
//   c2 = nullptr; 讓後續誤用 c2 時，症狀從「隨機的未定義行為」
//   變成「明確的空指標解參考」——後者至少會立刻崩潰在正確的位置，
//   而不是安靜地讀到已被回收再利用的資料。
//   同時它也讓重複 delete 變得安全（delete nullptr 是無作用的）。
//   但這只是防禦措施，不是所有權管理 ——
//   真正的解法是不要用裸指標。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼堆疊快這麼多
//   堆疊配置 = 把堆疊指標減去一個編譯期常數（一道指令），
//   而且那塊記憶體幾乎必然還在 CPU 快取裡（剛剛才用過）。
//   堆積配置 = 呼叫 operator new → 進入 allocator →
//   搜尋合適的空閒區塊 → 可能需要向作業系統要新的分頁 →
//   多執行緒環境還要處理同步。
//   除了配置本身，堆積物件在存取時也更容易 cache miss，
//   因為它們散布在記憶體各處，而堆疊物件是連續且熱的。
//
// (B) 釋放後使用（use-after-free）為什麼特別危險
//   delete 之後那塊記憶體會被 allocator 回收，
//   但【內容通常不會被清除】——
//   所以透過懸空指標讀取時，很可能讀到看似合理的舊資料，
//   程式繼續正常執行，直到那塊記憶體被重新配置給別的物件，
//   症狀才在完全無關的地方爆發。
//   這種 bug 極難追查，而 -fsanitize=address 正是為此而生。
//
// (C) 這個 Cat 沒有解構子，但仍然有解構行為
//   Cat 內含一個 std::string，而 string 有自己的解構子
//   （要釋放可能配置在堆積上的字元緩衝區）。
//   所以編譯器產生的隱式解構子並非空的 ——
//   它會呼叫 name 的解構子。
//   這也是為什麼忘記 delete c2 不只漏掉 sizeof(Cat) 的記憶體，
//   還會連帶漏掉 string 內部配置的緩衝區。
//
// 【注意事項 Pay Attention】
//   1. 堆疊物件不可 delete；堆積物件必須 delete（或交給智慧指標）。
//   2. ptr->m 與 (*ptr).m 對裸指標完全等價，
//      但 operator-> 可被多載且會遞迴套用，對智慧指標而言兩者不同。
//   3. 對 nullptr 呼叫 delete 是安全無作用的；
//      重複 delete 一個非 null 指標則是未定義行為。
//   4. 選擇堆疊或堆積的判準是【生命週期需求】，不是效能偏好。
//   5. 現代 C++ 應優先使用堆疊物件與容器，需要動態時用智慧指標。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】堆疊物件與堆積物件
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 什麼時候該用 new 在堆積上建立物件？
//     答：判準是【生命週期】而不是效能：只有當物件必須活得比
//         目前的作用域更久時才需要堆積。
//         常見的四種情況是：工廠函式要回傳物件、物件太大（本機堆疊上限 8 MiB）、
//         數量在執行期才決定、需要多型（基類指標指向派生類）。
//         其餘一律用堆疊 —— 更快、自動釋放、且例外安全。
//     追問：那 std::vector 算堆疊還是堆積？
//           → vector 物件【本身】在堆疊上（本機 sizeof 為 24 bytes，
//             三個指標），但它管理的元素在堆積上。
//             這正是理想的模式：使用者只看到一個自動生命週期的物件，
//             動態記憶體由它內部封裝並保證釋放。
//
// 🔥 Q2. ptr->name 和 (*ptr).name 完全一樣嗎？
//     答：對【裸指標】而言完全等價，-> 只是可讀性更好的簡寫。
//         但對【多載了 operator-> 的型別】（智慧指標、迭代器）就不同了：
//         operator-> 有一個特殊規則，它會【遞迴地】繼續套用，
//         直到得到真正的原生指標為止；operator* 則沒有這個行為。
//         這正是 unique_ptr 能直接寫 p->member 的原理。
//     追問：那 (*ptr).name 對 unique_ptr 還能用嗎？
//           → 能，unique_ptr 也多載了 operator*。
//             只是兩者走的是不同的多載，不再是單純的語法糖關係。
//
// ⚠️ 陷阱. delete c2 之後，如果不小心又用了 c2->meow()，
//          程式一定會立刻崩潰嗎？
//     答：不一定 —— 這正是它危險的地方。
//         delete 只是把記憶體還給 allocator，【內容通常不會被清除】。
//         所以緊接著存取很可能讀到完好的舊資料，程式表面上完全正常。
//         直到那塊記憶體被重新配置給別的物件、內容被覆寫之後，
//         症狀才在【完全無關】的地方爆發。
//         這是未定義行為，任何結果都是允許的，不能假設它會崩潰。
//     為什麼會錯：把「錯誤的程式碼」預期成「會報錯的程式碼」。
//         C++ 的 UB 沒有義務給你任何警示 ——
//         它甚至傾向於在測試環境中「看起來正常」，
//         因為測試程式短、記憶體還沒被重複利用。
//         本檔在 delete 後把 c2 設為 nullptr，
//         就是把「隨機的 UB」換成「必定的空指標解參考」，
//         讓錯誤至少能穩定重現。
//         要真正抓到這類問題，請用 -fsanitize=address 編譯執行。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class Cat {
public:
    string name;

    void meow() {
        cout << name << " 喵～" << endl;
    }
};

int main() {
    // ===== 棧上 =====
    Cat c1;                // 自動分配
    c1.name = "咪咪";
    c1.meow();             // 用 . 存取

    // ===== 堆上 =====
    Cat* c2 = new Cat();   // 手動分配
    c2->name = "花花";
    c2->meow();            // 用 -> 存取

    // c2 也可以用 (*c2).meow()，但 -> 更方便
    (*c2).meow();          // 和上一行等價

    delete c2;             // 手動釋放
    c2 = nullptr;

    return 0;
}   // c1 在這裡自動銷毀

// 編譯: g++ -std=c++17 -Wall -Wextra "第 8 課：對象（object）的創建與使用3.cpp" -o obj3
// 檢查: g++ -std=c++17 -fsanitize=address -g "第 8 課：對象（object）的創建與使用3.cpp" -o obj3_asan

// 註 1:本檔輸出是【完全確定的】—— 沒有輸入、亂數、執行緒或位址。
//
// 註 2:「花花 喵～」出現【兩次】是刻意的：
//        c2->meow();      用 -> 存取
//        (*c2).meow();    先解參考再用 . 存取
//      兩行對裸指標而言完全等價，輸出當然相同。
//      但請注意：對智慧指標（unique_ptr）而言兩者走的是不同的多載，
//      operator-> 會遞迴套用而 operator* 不會，屆時就不再是單純的語法糖。
//
// 註 3:c1 在 main 結束時自動解構，c2 則必須手動 delete。
//      本檔的配對是正確的，但若在 delete 之前丟出例外就會洩漏 ——
//      現代 C++ 應改用 std::make_unique<Cat>()（C++14）。
//
// 註 4:本檔不印任何指標位址。位址每次執行都不同（ASLR），
//      不可能寫進預期輸出，也沒有教學價值。

// === 預期輸出 ===
// 咪咪 喵～
// 花花 喵～
// 花花 喵～
