//
// 註 2:a=3、b=1 這個結果就是「每個物件擁有自己的資料成員」的證明。
//      若把 count 改成 static（全類別共用一份），
//      兩者都會印出 4 —— 本機已實測確認。
//
// 註 3:本機實測 sizeof(Counter) = 4，就只有那個 int；
//      兩個成員函式完全不佔物件空間。
//      若改成 static int count，sizeof 會變成 1
//      （空類別的最小大小，用來保證不同物件有不同位址）。

