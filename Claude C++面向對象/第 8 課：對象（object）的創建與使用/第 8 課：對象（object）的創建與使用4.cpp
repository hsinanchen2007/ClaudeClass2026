// =============================================================================
//  第 8 課：對象（object）的創建與使用4.cpp  —  每個物件的狀態互相獨立
// =============================================================================
//
// 【主題資訊 Information】
//   非靜態資料成員：每個物件各持有一份獨立副本
//   非靜態成員函式：全類別共用一份程式碼，透過隱藏的 this 區分物件
//   對照：static 資料成員 —— 全類別共用一份，不佔用物件空間
//   標準版本：C++98 起（類內初始值 int count = 0; 為 C++11）
//   複雜度：成員存取 O(1)（編譯期算好偏移量）
//   標頭檔：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. 本檔用最小的程式碼證明一件事】
//   a 與 b 都是 Counter，各自呼叫 increment()：
//       a 呼叫三次 → a.count 是 3
//       b 呼叫一次 → b.count 是 1
//   兩者互不影響。這聽起來理所當然，
//   但它其實是【每個物件擁有自己的資料成員】這個規則的直接後果，
//   而不是什麼理所當然的事 —— 換成 static 成員結果就完全不同。
//
// 【2. 同一段程式碼，四次呼叫，四個不同的 this】
//   increment() 的機器碼只有一份。四次呼叫分別是：
//       increment(&a)  → ++(a.count)
//       increment(&a)  → ++(a.count)
//       increment(&a)  → ++(a.count)
//       increment(&b)  → ++(b.count)
//   函式裡的 count++ 實際上是 this->count++，
//   而 this 決定了到底加在哪一塊記憶體上。
//
// 【3. 如果 count 是 static 會發生什麼】
//   把宣告改成 static int count; 之後：
//       class Counter { public: static int count; ... };
//       int Counter::count = 0;        // 必須在類別外定義
//   此時全類別只有一份 count，四次 increment() 全部加在同一個變數上，
//   a.show() 與 b.show() 都會印出 4。
//   而且 sizeof(Counter) 會從 4 變成 1
//   （static 成員不屬於物件，空類別的最小大小是 1 byte）。
//   這正是「計數已建立物件數量」這類需求的正確做法。
//
// 【4. count++ 與 ++count 在這裡沒有差別，但習慣上該用哪個】
//   對 int 而言兩者產生完全相同的機器碼（回傳值沒被使用）。
//   但對【迭代器或使用者自訂型別】，後置版本必須先複製一份舊值再回傳，
//   成本可能明顯較高。
//   所以 C++ 的慣例是：【回傳值用不到時一律用前置 ++】。
//   養成這個習慣的好處是，日後把 int 換成迭代器時不必回頭改。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 物件大小與成員偏移量
//   本機實測 sizeof(Counter) = 4 —— 就只有那個 int，
//   成員函式完全不佔空間。
//   count 的偏移量是 0，所以 a.count 編譯後就是
//   「以 a 的位址為基準加 0 再存取」，成本與存取區域變數相同。
//
// (B) 為什麼 show() 應該是 const
//   show() 只讀不寫，應宣告為 void show() const。
//   加上 const 之後，const Counter& 也能呼叫它，
//   而且編譯器會確保它不會修改成員。
//   本檔為了簡潔而省略 —— 但實務規則是
//   【任何不修改狀態的成員函式都要加 const】，
//   而且這件事必須一開始就做對，事後補會牽連整份程式碼。
//
// (C) 類內初始值讓這個類別不可能出錯
//   int count = 0; 保證了每個新建立的 Counter 都從 0 開始。
//   如果沒有這個初始值，Counter a; 之後 a.count 是不確定的值，
//   increment() 會在一個垃圾值上遞增，show() 印出的是未定義行為。
//   這是類內初始值（C++11）最實際的價值：
//   把「不可能忘記初始化」寫進類別定義本身。
//
// 【注意事項 Pay Attention】
//   1. 非靜態成員每個物件一份；要共用請用 static 成員。
//   2. static 資料成員必須在類別外定義（C++17 起可用 inline 省略）。
//   3. 不修改狀態的成員函式應加 const（本檔為簡化而省略）。
//   4. 回傳值用不到時習慣用前置 ++，對自訂型別可避免不必要的複製。
//   5. 內建型別成員請一律給類內初始值，否則未初始化就使用是 UB。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】物件狀態的獨立性
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 如果把 Counter::count 改成 static，這支程式的輸出會變成什麼？
//     答：a 和 b 都會印出 4。因為 static 成員全類別【只有一份】，
//         四次 increment()（a 三次、b 一次）全部加在同一個變數上。
//         此外 sizeof(Counter) 會從 4 變成 1 ——
//         static 成員不屬於任何物件，
//         而空類別的大小最小是 1 byte（為了讓不同物件有不同位址）。
//     追問：static 成員為什麼要在類別外定義？
//           → 因為類別定義通常在標頭檔中被多個 .cpp 包含，
//             若在類別內定義就會產生多個定義而違反 ODR。
//             類別內的那一行是【宣告】，類別外的才是【定義】
//             （配置實際儲存空間）。C++17 起可以寫 inline static 省略這一步。
//
// 🔥 Q2. increment() 沒有參數，它怎麼知道要加在 a 還是 b 的 count 上？
//     答：靠編譯器自動加上的隱藏參數 this。
//         a.increment() 實際上是 increment(&a)，
//         函式內的 count++ 被解析成 this->count++。
//         整個程式中 increment 的機器碼只有一份，
//         是 this 決定了它作用在哪一塊記憶體上。
//     追問：那 sizeof(Counter) 會因為有兩個成員函式而變大嗎？
//           → 不會，非虛擬成員函式不佔物件空間，
//             本機實測 sizeof(Counter) = 4，就只有那個 int。
//
// ⚠️ 陷阱. 「a 和 b 是同一個類別的物件，所以它們共用類別定義的成員」——
//          這句話哪裡有問題？
//     答：混淆了【共用型別定義】與【共用資料】。
//         a 和 b 確實共用同一份型別資訊與同一份成員函式機器碼，
//         但【資料是各自獨立的】——
//         a.count 與 b.count 是兩塊不同的記憶體。
//         本檔的輸出 a=3、b=1 正是這一點的證明；
//         若真的共用資料，兩者都會是 4。
//     為什麼會錯：「同一個類別」這個說法暗示了某種共享，
//         而 C++ 的規則是：型別與程式碼共用，狀態各自獨立。
//         唯一例外是 static 成員 —— 它才是真正「屬於類別而非物件」的東西。
//         判斷方式很直接：問「這個東西存在物件裡面嗎？」
//         非靜態成員在（會計入 sizeof），
//         成員函式與 static 成員不在（不計入 sizeof）。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class Counter {
public:
    int count = 0;

    void increment() {
        count++;
    }

    void show() {
        cout << "count = " << count << endl;
    }
};

int main() {
    Counter a;
    Counter b;

    a.increment();   // a 的 count 變成 1
    a.increment();   // a 的 count 變成 2
    a.increment();   // a 的 count 變成 3

    b.increment();   // b 的 count 變成 1（和 a 無關！）

    cout << "a: ";
    a.show();

    cout << "b: ";
    b.show();

    return 0;
}

