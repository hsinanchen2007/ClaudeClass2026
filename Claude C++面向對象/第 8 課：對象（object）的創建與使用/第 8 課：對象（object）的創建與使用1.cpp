// =============================================================================
//  第 8 課：對象（object）的創建與使用1.cpp  —  在堆疊上建立物件與自動生命週期
// =============================================================================
//
// 【主題資訊 Information】
//   語法：類別名 物件名;                  // 預設初始化，呼叫預設建構子
//         物件名.成員                      // 用「.」存取
//   儲存期：自動儲存期（automatic storage duration）
//           —— 進入作用域時建構，離開作用域時【自動】解構
//   標準版本：C++98 起（類內初始值 int age = 0; 為 C++11）
//   複雜度  ：建構 O(成員初始化成本)；存取成員 O(1)
//   標頭檔  ：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. Dog d1; 這一行做了三件事】
//   ① 在堆疊上配置 sizeof(Dog) 大小的空間（本機實測 40 bytes）
//   ② 呼叫 Dog 的預設建構子 —— 這裡是編譯器隱式產生的版本，
//      它會依序初始化每個成員：name 呼叫 std::string 的預設建構子（空字串）、
//      age 套用類內初始值 0
//   ③ 把這塊記憶體登記為「離開作用域時要解構」
//   第 ③ 點是 C++ 與 C 最大的分野：物件的清理是【自動且保證】的，
//   即使中途 return 或丟出例外也一樣會執行。
//
// 【2. 為什麼 age 有類內初始值而 name 沒有】
//   name 是 std::string，它有自己的預設建構子（產生空字串），
//   所以不寫初始值也一定是合法狀態。
//   age 是 int —— 內建型別的預設初始化【什麼都不做】，值不確定。
//   所以本檔寫了 int age = 0;（C++11 的類內初始值）。
//   這是一條很實用的規則：
//   【內建型別成員一律給類內初始值，類別型別成員可以依賴其預設建構子】。
//
// 【3. d1 與 d2 完全獨立，但 bark() 只有一份】
//   兩個物件在堆疊上是兩塊互不重疊的記憶體，
//   改 d1.name 不會影響 d2。
//   但 bark() 的機器碼只有一份，靠隱藏的 this 參數區分：
//       d1.bark()  →  bark(&d1)
//       d2.bark()  →  bark(&d2)
//
// 【4. 「d1 和 d2 在這裡自動銷毀」的順序是相反的】
//   檔尾註解說得沒錯，但有個細節值得補充：
//   解構順序是【建構順序的嚴格反序】——
//   先建構 d1、再建構 d2，離開時就是先解構 d2、再解構 d1。
//   這個規則是必要的，因為後建構的物件可能引用先建構的物件
//   （例如 d2 持有指向 d1 的指標），反序解構才能保證引用對象還活著。
//   本檔的兩個 Dog 互不相關，所以看不出差別，
//   但這個規則在 RAII 與資源管理中至關重要。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 堆疊配置為什麼幾乎零成本
//   在堆疊上建立物件，配置動作就只是把堆疊指標（rsp）減去一個常數 ——
//   一道指令，而且大小在編譯期就算好了。
//   對照 new：要進入 allocator、搜尋可用區塊、可能要向作業系統要記憶體、
//   還要處理執行緒同步。兩者差距通常是【數十倍以上】。
//   所以預設就該用堆疊物件，只有在真的需要
//   （生命週期要超出作用域、大小在執行期才知道、多型）時才用堆積。
//
// (B) 堆疊的大小是有限的
//   本機預設堆疊上限為 8 MiB（ulimit -s 顯示 8192 KiB，屬系統設定）。
//   所以不能在堆疊上放巨大的陣列：
//       Dog dogs[1000000];        // 40 MB，堆疊溢位
//   堆疊溢位【不會】丟出 std::bad_alloc，通常是直接 SIGSEGV ——
//   而且錯誤訊息完全看不出真正的原因。大量物件請用 std::vector。
//
// (C) sizeof(Dog) 為什麼是 40
//   本機實測（x86-64 / libstdc++，屬實作定義）：
//       string name   32 bytes（libstdc++ 的 SSO 實作：指標 + 長度 + 16 bytes 緩衝）
//       int    age     4 bytes
//       尾端填充       4 bytes（整體大小須為 8 的倍數）
//   注意 name 的 32 bytes 只是 string 物件【本身】的大小；
//   若字串內容超過 SSO 門檻（libstdc++ 為 15 個字元），
//   實際字元會另外配置在堆積上，但 sizeof 不會因此改變。
//
// 【注意事項 Pay Attention】
//   1. 堆疊物件離開作用域時【自動】解構，不需要也不可以 delete。
//   2. 解構順序是建構順序的嚴格反序。
//   3. 內建型別成員（int age）若沒有初始值，預設初始化【不會】清零，
//      讀取未初始化的值是未定義行為。本檔用了 C++11 的類內初始值。
//   4. 堆疊空間有限（本機 8 MiB），大量或大型物件請改用容器。
//   5. 堆疊溢位通常直接 SIGSEGV，不會丟 std::bad_alloc。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】堆疊物件與自動生命週期
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. Dog d1; 這一行實際上發生了什麼事？
//     答：三件事：① 在堆疊上配置 sizeof(Dog) 的空間
//         （只是把堆疊指標減去一個編譯期常數，幾乎零成本）；
//         ② 呼叫預設建構子，依【宣告順序】初始化每個成員
//         （name 走 std::string 的預設建構子、age 套用類內初始值 0）；
//         ③ 登記在離開作用域時自動解構。
//         第三點是保證的 —— 即使中途 return 或丟出例外也會執行。
//     追問：如果 Dog 沒有寫任何建構子，還會有建構子被呼叫嗎？
//           → 會。編譯器會隱式產生一個預設建構子。
//             它對類別型別成員呼叫各自的預設建構子，
//             對內建型別成員則【什麼都不做】（除非有類內初始值）。
//
// 🔥 Q2. 為什麼堆疊物件不需要（也不可以）delete？
//     答：因為它的生命週期由作用域決定，編譯器已經在離開作用域處
//         插入了解構的程式碼。對堆疊物件呼叫 delete 是未定義行為 ——
//         delete 只能用在 new 回傳的指標上，
//         它會呼叫解構子並把記憶體還給堆積 allocator，
//         而堆疊記憶體根本不屬於 allocator 管理。
//     追問：那怎麼記住哪些要 delete？
//           → 最好的答案是【都不要手動 delete】。
//             現代 C++ 用 RAII：堆疊物件自動管理，
//             需要堆積時用 std::unique_ptr / std::make_unique（C++14），
//             讓解構子替你呼叫 delete。
//
// ⚠️ 陷阱. 「d1 和 d2 都是區域變數，離開 main 時會依照宣告順序
//          d1、d2 依序解構」—— 錯在哪？
//     答：順序是【反的】。解構一律是建構順序的嚴格反序：
//         先建 d1、再建 d2 → 離開時先解構 d2、再解構 d1。
//     為什麼會錯：直覺會把解構想成「再走一次同樣的清單」。
//         但反序才是唯一安全的順序 ——
//         後建立的物件可能引用先建立的物件
//         （例如 d2 內部持有指向 d1 的指標或參考），
//         若照正序解構，d2 解構時 d1 已經是懸空的了。
//         同樣的規則也套用在成員（依宣告順序建構、反序解構）
//         與繼承（基類先建、最後解構）上 ——
//         整個 C++ 的物件生命週期都建立在「反序清理」這個原則之上。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class Dog {
public:
    string name;
    int age = 0;

    void bark() {
        cout << name << " 汪汪！" << endl;
    }
};

int main() {
    Dog d1;           // 建立對象，就像宣告一個普通變數
    d1.name = "旺財";
    d1.age = 3;
    d1.bark();

    Dog d2;           // 再建一個，完全獨立
    d2.name = "小黑";
    d2.age = 5;
    d2.bark();

    return 0;
}   // d1 和 d2 在這裡自動銷毀

