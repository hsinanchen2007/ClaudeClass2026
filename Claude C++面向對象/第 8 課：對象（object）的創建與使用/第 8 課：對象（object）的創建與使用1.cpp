//
// 註 2:兩行輸出來自同一份 bark() 機器碼，靠隱藏的 this 指標區分物件。
//
// 註 3:檔尾註解「d1 和 d2 在這裡自動銷毀」正確，但順序是
//      【先 d2 再 d1】—— 解構一律是建構順序的嚴格反序。
//      本檔的 Dog 沒有自訂解構子，所以觀察不到；
