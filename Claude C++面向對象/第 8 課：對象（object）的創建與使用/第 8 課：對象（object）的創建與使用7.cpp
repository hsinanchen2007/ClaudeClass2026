// =============================================================================
//  第 8 課：對象（object）的創建與使用7.cpp  —  物件陣列與走訪
// =============================================================================
//
// 【主題資訊 Information】
//   Student students[3];        // 陣列的每個元素都會被【預設建構】
//   students[i].member          // 以索引存取後再取成員
//   標準版本：C++98 起（類內初始值為 C++11）
//   複雜度：建構整個陣列 O(n)；索引存取 O(1)
//   標頭檔：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. Student students[3]; 這一行呼叫了三次建構子】
//   陣列的每個元素都是完整的物件，
//   所以這一行會【依序】呼叫三次 Student 的預設建構子
//   （索引 0 → 1 → 2）。
//   而離開作用域時則是【反序】解構（2 → 1 → 0）。
//   本檔的 Student 有類內初始值（name = "未命名"、score = 0），
//   所以三個元素一開始都是合法狀態 ——
//   這是為什麼可以放心地只設定部分成員。
//
// 【2. 物件陣列在記憶體中是連續的】
//   students[0]、students[1]、students[2] 在記憶體中緊鄰排列，
//   每個佔 sizeof(Student) bytes（本機實測 40）。
//   students[i] 的位址就是 students + i * 40，
//   這個乘法在編譯期就展開了，存取成本是 O(1)。
//   連續性帶來的最大好處是【快取友善】——
//   走訪陣列時，讀取 students[0] 會順便把後面的資料載入 cache line，
//   接下來的存取幾乎不需要碰記憶體。
//
// 【3. 為什麼實務上應該用 std::vector 而不是原生陣列】
//   原生陣列有三個實際問題：
//     ① 大小必須是編譯期常數 —— 無法依使用者輸入決定
//     ② 傳給函式時會【退化成指標】，長度資訊完全遺失
//        （所以本檔的迴圈必須硬寫 3，而且改成 4 個學生時
//          要記得同時改三處迴圈條件）
//     ③ 沒有邊界檢查，students[5] 是未定義行為
//   std::vector 三個問題全解，而且成本幾乎相同。
//   本檔用原生陣列是為了聚焦在「物件陣列」這個概念本身。
//
// 【4. 找最高分的迴圈為什麼從 i = 1 開始】
//   maxIdx 先假設為 0，接著只需要和 1、2 比較 ——
//   從 0 開始比較是多餘的（自己和自己比）。
//   這個「先取第一個當基準，再和其餘比較」的模式，
//   正是 std::max_element 的做法：
//       auto it = std::max_element(students, students + 3,
//                   [](const Student& a, const Student& b){ return a.score < b.score; });
//   實務上優先用標準演算法 —— 它已經處理好空範圍等邊界情況。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 陣列退化（array decay）是 C 留下的陷阱
//   把 students 傳給函式時：
//       void print(Student arr[3]);     // 這個 3 完全被忽略！
//       void print(Student* arr);       // 上面那行實際上就是這個意思
//   sizeof(arr) 在函式內會得到指標大小（本機 8）而不是 120。
//   這是 C++ 中最容易誤用的規則之一。
//   若要保留長度資訊，可以用參考到陣列的語法
//   （void print(Student (&arr)[3])）、樣板，或直接用 std::array / std::vector。
//
// (B) 本機實測的大小
//   sizeof(Student) = 40（string 32 + int 4 + 尾端填充 4），
//   sizeof(students) = 120（三個元素連續排列）。
//   這些數值屬【實作定義】。
//   注意 string 的 32 bytes 只是物件本身；
//   若字串內容超過 SSO 門檻（libstdc++ 為 15 字元），
//   字元資料會另外配置在堆積上，但 sizeof 不變。
//
// (C) 走訪陣列的三種寫法
//       for (int i = 0; i < 3; i++)          students[i].show();   // 本檔
//       for (const Student& s : students)    s.show();             // C++11 range-for
//       for (auto& s : students)             s.show();             // 更簡潔
//   range-for 的優點是不可能寫錯邊界、也不需要手動維護長度。
//   注意它只在陣列【尚未退化】時可用 —— 一旦傳進函式變成指標就不能用了，
//   這也從側面說明了退化的代價。
//
// 【注意事項 Pay Attention】
//   1. Student students[3]; 會呼叫三次預設建構子（反序解構）。
//   2. 陣列傳給函式時會退化成指標，長度資訊遺失、sizeof 得到指標大小。
//   3. 原生陣列沒有邊界檢查，students[5] 是未定義行為（不保證崩潰）。
//   4. 實務請用 std::vector（動態大小）或 std::array（固定大小，
//      不會退化且有 size()）。
//   5. 本檔硬寫了三次迴圈邊界 3，人數改變時要記得三處都改 ——
//      這正是原生陣列的維護成本。
//   6. 找極值優先用 std::max_element，它已處理好邊界情況。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】物件陣列與陣列退化
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. Student students[3]; 這一行會呼叫幾次建構子？離開作用域時呢？
//     答：呼叫三次預設建構子，順序是索引 0 → 1 → 2；
//         離開作用域時【反序】解構，2 → 1 → 0。
//         陣列的每個元素都是完整的物件，
//         不是「一塊記憶體然後大家共用」。
//     追問：如果 Student 沒有預設建構子（只有帶參數的）會怎樣？
//           → 編譯失敗，因為無法預設建構陣列元素。
//             解法是提供預設建構子、用初始化列表
//             Student s[3] = { {"a",1}, {"b",2}, {"c",3} }，
//             或改用 std::vector 搭配 emplace_back。
//
// 🔥 Q2. 把 students 傳給 void print(Student arr[3]) 之後，
//        在函式內 sizeof(arr) 是多少？
//     答：是【指標的大小】（本機 8），不是 120。
//         因為陣列參數會【退化成指標】——
//         參數列裡寫的那個 3 會被編譯器完全忽略，
//         void print(Student arr[3]) 與 void print(Student* arr) 是同一個函式。
//         長度資訊在傳遞的瞬間就遺失了。
//     追問：怎麼保留長度？
//           → 三種做法：傳參考到陣列 void print(Student (&arr)[3])、
//             用樣板 template<size_t N> void print(Student (&arr)[N])、
//             或直接改用 std::array / std::vector（有 size() 成員）。
//
// ⚠️ 陷阱. 本檔的迴圈寫 i < 3，如果之後把學生人數改成 4，
//          只要把陣列宣告改成 students[4] 就好了嗎？
//     答：不行，而且這正是原生陣列最大的維護陷阱。
//         本檔有【三處】硬編了 3：
//         陣列宣告、走訪迴圈、找最高分的迴圈。
//         只改宣告的話，後兩個迴圈仍然只處理前三個元素 ——
//         程式不會報錯、不會崩潰，只是安靜地漏掉第四位學生。
//     為什麼會錯：把「陣列大小」當成只在宣告處出現一次的資訊。
//         實際上原生陣列的長度無法從陣列本身取得（傳出去就退化了），
//         所以長度必然散落在程式碼各處，
//         而編譯器完全不會檢查它們是否一致。
//         正解是讓長度【只有一個來源】：
//         用 std::vector 並在迴圈中呼叫 v.size()，
//         或用 range-for（for (auto& s : students)）——
//         後者甚至完全不需要知道長度是多少。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class Student {
public:
    string name = "未命名";
    int score = 0;

    void show() {
        cout << name << ": " << score << " 分" << endl;
    }
};

int main() {
    // 建立包含 3 個 Student 對象的陣列
    Student students[3];

    // 分別設定
    students[0].name = "小明";
    students[0].score = 85;

    students[1].name = "小華";
    students[1].score = 92;

    students[2].name = "小美";
    students[2].score = 78;

    // 用迴圈遍歷
    cout << "===== 成績單 =====" << endl;
    for (int i = 0; i < 3; i++) {
        students[i].show();
    }

    // 找最高分
    int maxIdx = 0;
    for (int i = 1; i < 3; i++) {
        if (students[i].score > students[maxIdx].score) {
            maxIdx = i;
        }
    }
    cout << "\n最高分: " << students[maxIdx].name
         << " (" << students[maxIdx].score << " 分)" << endl;

    return 0;
}

