// 註 1:本檔輸出是【完全確定的】—— 沒有輸入、亂數、執行緒或位址。

// 註 2:Student students[3]; 會依序呼叫三次預設建構子，
//      因為有類內初始值，三個元素一開始都是 "未命名" / 0 的合法狀態。
//      離開作用域時則是【反序】解構。

// 註 3:本機實測 sizeof(Student)=40、sizeof(students)=120
//      （x86-64 / g++ 15.2.0，屬實作定義）。三個元素在記憶體中連續排列，
//      因此走訪時對 CPU 快取非常友善。

// 註 4:⚠️ 本檔有【三處】硬編了數字 3（陣列宣告、走訪迴圈、找最高分迴圈）。
//      人數改變時三處都要改，漏掉任何一處都不會報錯、只會安靜地漏算 ——
//      這正是原生陣列的維護成本。實務請用 std::vector 搭配 size()，
//      或用 range-for 完全不必知道長度。

