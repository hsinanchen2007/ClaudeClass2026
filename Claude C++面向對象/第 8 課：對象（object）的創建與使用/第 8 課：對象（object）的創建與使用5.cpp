// =============================================================================
//  第 8 課：對象（object）的創建與使用5.cpp  —  sizeof、對齊填充與空類別
// =============================================================================
//
// 【主題資訊 Information】
//   sizeof(型別) / sizeof 運算式        // 回傳 std::size_t，單位是 byte
//   alignof(型別)                       // 型別的對齊需求（C++11）
//   規則：物件大小必須是其對齊值的【整數倍】
//   空類別大小：保證 ≥ 1（讓不同物件擁有不同位址）
//   標準版本：sizeof 為 C++98；alignof / alignas 為 C++11
//   複雜度：sizeof 是編譯期常數，執行期成本為零
//   標頭檔：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼空類別不是 0 bytes】
//   sizeof(Empty) 是 1 而不是 0，這是【標準要求】的：
//   任兩個不同的物件必須有不同的位址。
//   若空類別大小為 0，陣列中相鄰的元素就會共用同一個位址，
//   指標比較、pointer arithmetic 全部失效。
//   所以標準規定完整物件的大小最小為 1。
//
// 【2. 為什麼「只有成員函式」的類別也是 1 bytes】
//   sizeof(OnlyFunction) 同樣是 1，
//   因為非虛擬成員函式【不儲存在物件裡】——
//   它編譯後就是一個普通函式（多一個隱藏的 this 參數），
//   存在程式碼區段中，全類別共用一份。
//   物件裡只放【非靜態資料成員】（以及有虛擬函式時的 vptr）。
//   這是判斷 sizeof 最實用的規則：
//   數一數有幾個非靜態資料成員就好，函式一律不算。
//
// 【3. WithData 為什麼是 16 而不是 4+4+8=16 —— 這題剛好沒有填充】
//       int x     偏移 0，佔 4
//       int y     偏移 4，佔 4
//       double z  偏移 8，佔 8（8 需要 8 bytes 對齊，而 8 剛好是 8 的倍數）
//   總計 16，且 16 是最大對齊值（8）的倍數 → 不需要尾端填充。
//   這是少數「加起來剛剛好」的情況，容易讓人誤以為 sizeof 就是總和。
//
// 【4. Mixed 為什麼是 12 而不是 1+4+1=6 —— 這才是重點】
//       char a    偏移 0，佔 1
//       【填充 3 bytes】  ← 因為 int 需要 4 bytes 對齊，
//                            b 必須放在 4 的倍數位置上
//       int  b    偏移 4，佔 4
//       char c    偏移 8，佔 1
//       【尾端填充 3 bytes】← 整體大小必須是最大對齊值（4）的倍數
//   總計 12。整整 6 bytes（50%）是填充！
//   把宣告改成 int b; char a; char c; 就變成
//   4 + 1 + 1 + 尾端填充 2 = 8 bytes ——
//   本機已實測確認。同樣的資料、只是換個順序就省下三分之一空間。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼硬體需要對齊
//   CPU 從記憶體讀取資料是以固定大小的區塊進行的。
//   若一個 4 bytes 的 int 橫跨兩個區塊邊界，
//   就需要兩次記憶體存取再拼接 —— 明顯變慢。
//   在某些架構（早期 ARM、SPARC）上，未對齊存取甚至會直接觸發硬體例外。
//   x86-64 允許未對齊存取但速度較慢，
//   所以編譯器一律插入填充來維持對齊。
//
// (B) 實務上什麼時候該在意這件事
//   單一物件差幾個 byte 無關緊要，但當你有【一百萬個】物件時：
//   Mixed 的陣列會浪費 6 MB。
//   更關鍵的是快取效率 —— 一條 cache line 通常是 64 bytes，
//   物件越小，同一條 cache line 能裝越多個，走訪陣列時的 cache miss 就越少。
//   實用規則：【成員由大到小宣告】通常能自然減少填充。
//
// (C) 不能靠 sizeof 來做序列化
//   因為填充的內容是【不確定的】（編譯器沒有義務清零），
//   而且不同編譯器／平台的填充方式可能不同。
//   所以：
//     • 直接 memcpy 整個結構體寫進檔案 → 檔案內含垃圾資料，且不可攜
//     • 用 memcmp 比較兩個結構體 → 可能因填充內容不同而誤判為不相等
//   正確做法是逐欄位序列化，或使用 protobuf 這類專門的格式。
//
// 【注意事項 Pay Attention】
//   1. sizeof(空類別) 保證 ≥ 1，因為不同物件必須有不同位址。
//   2. 非虛擬成員函式【不佔】物件空間；只有非靜態資料成員（與 vptr）才算。
//   3. sizeof 通常【大於】成員大小的總和，差額是對齊填充。
//   4. 成員【宣告順序】會影響物件大小；由大到小排列通常最省。
//   5. 填充位元組的內容是不確定的 ——
//      不可用 memcmp 比較結構體，也不可直接 memcpy 做序列化。
//   6. 本檔所有數值皆為本機 x86-64 / g++ 15.2.0 實測，【屬實作定義】。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】sizeof 與記憶體對齊
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼空類別的 sizeof 是 1 而不是 0？
//     答：因為標準要求任兩個不同物件必須有不同的位址。
//         如果空類別大小為 0，那麼 Empty arr[2]; 中
//         &arr[0] 與 &arr[1] 就會相等，
//         指標比較與 pointer arithmetic 全部失效。
//         所以標準規定完整物件的大小最小為 1 byte。
//     追問：那有沒有例外？
//           → 有，【空基類最佳化（EBO）】：
//             空類別作為【基類】時可以佔用 0 bytes。
//             這是標準明確允許的，
//             std::unique_ptr 就靠它讓「無狀態的 deleter」不增加大小 ——
//             sizeof(unique_ptr<T>) 因此能和裸指標一樣是 8 bytes。
//
// 🔥 Q2. struct Mixed { char a; int b; char c; }; 的 sizeof 是多少？為什麼？
//     答：本機實測是 12（不是 1+4+1=6）。
//         因為 int 需要 4 bytes 對齊：
//         a 在偏移 0（1 byte），接著【填充 3 bytes】
//         讓 b 落在偏移 4；b 佔 4；c 在偏移 8 佔 1；
//         最後整體大小必須是最大對齊值 4 的倍數，
//         所以【尾端再填充 3 bytes】→ 總計 12。
//         整整一半是填充。
//     追問：怎麼縮小它？
//           → 把成員【由大到小】重排：int b; char a; char c;
//             變成 4 + 1 + 1 + 尾端填充 2 = 8 bytes，省下三分之一。
//             本機已實測確認。
//
// ⚠️ 陷阱. 兩個 Mixed 物件的每個成員值都相同，
//          用 memcmp 比較它們一定會回傳 0（相等）嗎？
//     答：不一定。memcmp 會連【填充位元組】一起比較，
//         而填充的內容是【不確定的】—— 編譯器沒有義務把它清零，
//         裡面可能殘留著那塊堆疊記憶體先前的內容。
//         所以兩個「邏輯上完全相同」的物件，
//         memcmp 可能回傳非零。
//     為什麼會錯：把結構體想成「就是那幾個成員連續排在一起」，
//         忽略了成員【之間】與【尾端】的填充也屬於這個物件的儲存空間。
//         同樣的理由讓「直接 memcpy 整個結構體寫進檔案」也是錯的：
//         寫出去的資料含有不確定的垃圾，
//         而且換一個編譯器或平台，填充方式可能就不同了。
//         正確做法是自己實作 operator== 逐欄位比較，
//         以及逐欄位序列化（或使用專門的序列化格式）。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
using namespace std;

class Empty {
    // 什麼都沒有
};

class OnlyFunction {
public:
    void doSomething() {
        cout << "doing" << endl;
    }
};

class WithData {
public:
    int x;      // 4 bytes
    int y;      // 4 bytes
    double z;   // 8 bytes

    void show() {
        cout << x << ", " << y << ", " << z << endl;
    }
};

class Mixed {
public:
    char a;     // 1 byte
    int b;      // 4 bytes
    char c;     // 1 byte
};

int main() {
    cout << "sizeof(Empty)        = " << sizeof(Empty) << " bytes" << endl;
    cout << "sizeof(OnlyFunction) = " << sizeof(OnlyFunction) << " bytes" << endl;
    cout << "sizeof(WithData)     = " << sizeof(WithData) << " bytes" << endl;
    cout << "sizeof(Mixed)        = " << sizeof(Mixed) << " bytes" << endl;

    return 0;
}

