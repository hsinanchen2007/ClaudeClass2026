// =============================================================================
//  第 10 課：成員函數 7  —  驗證 this 就是物件本身的位址
// =============================================================================
//
// 【主題資訊 Information】
//   語法：  在非 static 成員函式中，this 的值恆等於 &物件。
//   標準：  C++98 起即有。
//   標頭檔：<iostream>、<string>
//   輸出型別：cout << this 會走 operator<<(const void*) 印出十六進位位址。
//
// 【詳細解釋 Explanation】
//
// 【1. 本檔在證明什麼】
//   前一個範例說「this 就是呼叫者的位址」，本檔把它**實際印出來對照**：
//   a.showAddress() 印出的 this，會與 main 裡直接印的 &a 完全相同。
//   這不是巧合，是標準保證的：對非 static 成員函式，this == &物件。
//
// 【2. 為什麼三個物件的位址不同、且通常相鄰】
//   a、b、c 是 main 內的區域變數，配置在 stack 上。
//   在本機實測中三者位址呈固定間隔（sizeof(Widget) 對齊後的距離），
//   但**這是實作定義行為**：
//     - 編譯器可自由決定區域變數的排列順序與是否插入 padding；
//     - 開啟最佳化後變數可能完全不進記憶體（放暫存器）而被優化掉；
//     - 位址本身受 ASLR（位址空間配置隨機化）影響，**每次執行都不同**。
//   ★ 所以底下的預期輸出中，位址數值僅供參考，你跑出來一定不一樣；
//     真正該驗證的是「this 與 & 印出的值相等」這個關係。
//
// 【3. cout << this 為什麼印出十六進位而不是數字】
//   ostream 對 const void* 有專屬的 operator<<，會以實作定義的格式
//   （GCC/libstdc++ 上是 0x 開頭的十六進位）輸出位址。
//   注意 char* 是例外：cout << (char*)p 會被當成 C 字串去讀內容直到 '\0'，
//   想印 char* 的位址必須先轉成 (void*)。這是很常踩的輸出陷阱。
//
// 【概念補充 Concept Deep Dive】
//   this 的值只在物件生命週期內有意義。物件銷毀後，
//   先前記下的 this 就成為懸空指標 —— 拿它做**解參考**是 UB；
//   即使只是**比較**，標準上對已結束生命週期的指標值也只給有限保證。
//
//   另一個常被問到的點：兩個不同物件的 this 一定不同嗎？
//   對於**同時存活**的兩個完整物件，是的 —— C++ 要求不同物件有不同位址
//   （這是 pointer identity 的基礎）。但空的基底子物件可能與衍生物件同址
//   （EBO，empty base optimization），這是繼承章節的細節。
//
// 【注意事項 Pay Attention】
//   1. 位址值**每次執行都不同**（ASLR），不可寫進測試斷言。
//   2. 不要把位址當成物件的「識別碼」持久化；
//      物件銷毀後同一位址可能被新物件重用（ABA 問題）。
//   3. cout << charPtr 印的是字串內容不是位址，要印位址請轉 (void*)。
//   4. 比較不同陣列／不同物件之間的指標大小關係是未指定行為，
//      只有同一陣列（或同一物件內）的指標比較才有明確定義。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】this 與物件位址
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. this 和 &物件 是同一個值嗎？有例外嗎？
//     答：在非 static 成員函式中，this 的值就是該物件的位址，兩者相等。
//         static 成員函式沒有 this，談不上相等。
//         多重繼承時，把衍生物件位址轉成「非第一個基底」的指標，
//         值會被調整偏移量，因此在該基底的成員函式中
//         this 與原始衍生物件位址不同 —— 這是唯一常見的例外。
//     追問：那 dynamic_cast<void*>(p) 有什麼用？
//         → 它會回推到**最終衍生物件**的起始位址，
//           常用來在多重繼承下判斷「這兩個指標是不是同一個物件」。
//
// 🔥 Q2. 兩個同時存活的不同物件，位址可能相同嗎？
//     答：不可能。C++ 要求同時存活的相異物件有相異位址，
//         這是物件識別（pointer identity）的基礎。
//         例外只在空的基底子物件（EBO）—— 它可與衍生物件共用位址，
//         但那並非兩個獨立的完整物件。
//     追問：那 sizeof 空類別為什麼是 1 而不是 0？
//         → 正是為了保證「不同物件位址不同」；
//           若可以是 0，陣列裡相鄰元素就會同址。
//
// ⚠️ 陷阱. cout << ptr 印出的一定是位址嗎？
//     答：不一定。若 ptr 的型別是 char*／const char*，
//         ostream 會走字串重載，把它當 C 字串**印出內容**，
//         一路讀到 '\0' 為止；若那塊記憶體不是字串，就會讀出亂碼甚至越界。
//         要印位址必須寫 cout << static_cast<const void*>(ptr)。
//     為什麼會錯：大家把「指標」當成單一概念，
//         以為 << 對所有指標一視同仁；但 char* 有專門的重載，
//         而重載決議是看型別的，不看你的意圖。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <cstring>
using namespace std;

class Widget {
public:
    int id = 0;

    void showAddress() {
        cout << "對象 id=" << id
             << " 的地址: " << this << endl;
    }
};

// -----------------------------------------------------------------------------
// 【可執行示範 1】用「相等性」而非「位址數值」來驗證 this
//   位址每次執行都不同，無法寫進預期輸出；
//   但 (this == &obj) 這個**關係**是標準保證的，恆為 true，可以穩定驗證。
// -----------------------------------------------------------------------------
class Verifier {
public:
    bool isSameAs(const Verifier* p) const { return this == p; }
};

// -----------------------------------------------------------------------------
// 【可執行示範 2】cout << 指標 不一定印位址：char* 的字串重載陷阱
// -----------------------------------------------------------------------------
void demoCharPtrTrap() {
    const char* msg = "Hello";
    cout << "  cout << msg          -> " << msg << "   (被當字串印內容)" << endl;
    cout << "  cout << (void*)msg   -> " << static_cast<const void*>(msg)
         << "   (轉成 void* 才印位址；每次執行都不同)" << endl;
}

// -----------------------------------------------------------------------------
// 【日常實務範例】自我賦值檢查：比較 this 最經典的真實用途
//   情境：實作有動態資源的類別的 operator= 時，
//         若不先擋掉 v = v（自我賦值），典型的「先釋放舊資源、再複製新資源」
//         流程會先把自己的資料 delete 掉，然後從**已釋放的記憶體**讀取來源 ——
//         這是 use-after-free，屬未定義行為。
//   為什麼用到本主題：唯一可靠的判斷方式就是比較位址 if (this == &other)，
//         因為它問的是「這兩個名字是不是同一個物件」，
//         而不是「內容是否相等」。
//   ★ 現代做法有 copy-and-swap 慣用法可天然免疫自我賦值，
//     但仍需理解這個檢查存在的原因。
// -----------------------------------------------------------------------------
class Buffer {
    size_t m_len;      // 宣告順序：長度在前
    char*  m_data;     // 指標在後 —— 初始化順序依宣告順序，m_len 先就緒
public:
    explicit Buffer(const char* s)
        : m_len(strlen(s)), m_data(new char[m_len + 1]) {   // 用已初始化的 m_len 配置
        memcpy(m_data, s, m_len + 1);
    }

    Buffer(const Buffer& o) : m_len(o.m_len), m_data(new char[m_len + 1]) {
        memcpy(m_data, o.m_data, m_len + 1);
    }

    Buffer& operator=(const Buffer& o) {
        if (this == &o) {                 // ★ 自我賦值檢查：比較的是位址，不是內容
            cout << "    (偵測到自我賦值，直接跳過)" << endl;
            return *this;
        }
        delete[] m_data;                  // 若少了上面的檢查，這行會先毀掉來源
        m_len  = o.m_len;
        m_data = new char[m_len + 1];
        memcpy(m_data, o.m_data, m_len + 1);
        return *this;
    }

    ~Buffer() { delete[] m_data; }

    const char* c_str() const { return m_data; }
};

int main() {
    cout << "=== 基本：印出 this 與 &obj（位址每次執行都不同）===" << endl;
    Widget a, b, c;
    a.id = 1;
    b.id = 2;
    c.id = 3;

    a.showAddress();   // this == &a
    b.showAddress();   // this == &b
    c.showAddress();   // this == &c

    // 驗證：直接取地址比較
    cout << "\n直接取地址驗證:" << endl;
    cout << "&a = " << &a << endl;
    cout << "&b = " << &b << endl;
    cout << "&c = " << &c << endl;

    cout << "\n=== 穩定驗證：用相等關係取代位址數值 ===" << endl;
    Verifier v1, v2;
    cout << "  v1.isSameAs(&v1) = " << boolalpha << v1.isSameAs(&v1)
         << "   <- 標準保證恆為 true" << endl;
    cout << "  v1.isSameAs(&v2) = " << v1.isSameAs(&v2)
         << "  <- 不同物件必為 false" << endl;

    cout << "\n=== 陷阱：cout << 指標 不一定印位址 ===" << endl;
    demoCharPtrTrap();

    cout << "\n=== 日常實務：operator= 的自我賦值檢查 ===" << endl;
    Buffer buf("原始資料");
    cout << "  賦值前: " << buf.c_str() << endl;
    buf = buf;                              // 自我賦值：被檢查擋下
    cout << "  自我賦值後: " << buf.c_str() << "   <- 內容完好" << endl;

    Buffer other("新的資料");
    buf = other;                            // 正常賦值
    cout << "  被 other 賦值後: " << buf.c_str() << endl;

    return 0;
}

