// =============================================================================
//  第 9 課：成員變數（Member Variables）3.cpp  —  組合（Composition）：物件當成員
// =============================================================================
//
// 【主題資訊 Information】
//   組合（composition）：把另一個類別的物件當成資料成員 —— has-a 關係
//   對照繼承（inheritance）：class D : public B —— is-a 關係
//   存取語法：外層物件.內層成員.內層的成員（car.engine.horsepower）
//   標準版本：C++98 起（類內初始值為 C++11）
//   複雜度：巢狀成員存取仍是 O(1)（偏移量在編譯期就疊加好了）
//   標頭檔：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. 組合就是「用小零件拼出大東西」】
//   Car 內含一個 Engine 物件，這表達的是【汽車有一個引擎】（has-a），
//   而不是【汽車是一種引擎】（is-a）。
//   判斷該用組合還是繼承，就問這一句：
//       「A 真的可以在任何用到 B 的地方頂替 B 嗎？」
//   汽車顯然不能拿去當引擎用，所以是組合。
//   實務上這個判斷幾乎總是倒向組合 ——
//   「Prefer composition over inheritance」是被反覆驗證的經驗法則，
//   因為組合的耦合遠比繼承鬆：
//   Engine 改了介面，只有 Car 內部受影響，
//   而基類改了介面，整個繼承體系都會被波及。
//
// 【2. Engine 物件是【內嵌】在 Car 裡面的，不是指標】
//   Car 的記憶體佈局是：
//       [ string brand ][ Engine engine ]
//   engine 不是指向別處的指標，而是實實在在的一塊記憶體，
//   完全包含在 Car 物件之內。
//   所以：
//     • sizeof(Car) 包含了 sizeof(Engine)（本機實測 32 + 40 = 72）
//     • Car 物件建立時，engine 自動被建構；Car 解構時 engine 自動被解構
//     • 不需要 new/delete，不可能洩漏，也不可能是懸空指標
//   這是組合最大的優點：生命週期自動綁定。
//
// 【3. 建構與解構順序】
//   建立 Car car; 時：
//       ① 先建構所有成員（依【宣告順序】）：brand → engine
//          —— engine 的建構會套用它自己的類內初始值（0 與 "汽油"）
//       ② 再執行 Car 的建構子本體
//   解構則完全相反：先執行 ~Car() 本體，再【反序】解構 engine → brand。
//   關鍵推論：在 Car 的建構子本體裡，engine 【已經】是完全建構好的物件，
//   可以安全使用。
//
// 【4. car.engine.horsepower 這種寫法透露的設計問題】
//   本檔為了示範語法，讓外部直接寫 car.engine.horsepower = 150;。
//   這違反了【迪米特法則（Law of Demeter）】——
//   使用者不該需要知道 Car 內部是用什麼零件組成的。
//   更好的介面是：
//       car.setEngineSpec(150, "油電混合");
//   或在建構時就決定。
//   這樣做的好處是：日後 Car 換成「兩顆馬達」的實作時，
//   使用者的程式碼完全不必改。
//   一旦暴露了 car.engine.xxx，那個內部結構就變成了公開介面的一部分，
//   再也改不動了。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 組合 vs 聚合 vs 繼承的實作差異
//       組合（composition）：Engine engine;         生命週期綁定，自動管理
//       聚合（aggregation）：Engine* engine;        外部擁有，Car 只是引用
//       繼承（inheritance）：class Car : public Engine;  is-a，可替換
//   本檔是組合 —— 最簡單也最安全的一種。
//   只有當「零件可以在多個擁有者之間共享」或
//   「零件需要在執行期抽換」時，才需要用指標（且應該是智慧指標）。
//
// (B) 為什麼 Engine 的類內初始值很重要
//   Engine 的兩個成員都有類內初始值（horsepower = 0、fuelType = "汽油"）。
//   這保證了任何 Car 一建立，它的 engine 就是合法狀態 ——
//   即使使用者忘記設定 horsepower，也只會得到 0 而不是垃圾值。
//   對照同課 5、6 號檔沒有初始值的類別，
//   這一行 = 0 就是「有沒有未定義行為」的分界線。
//
// (C) 巢狀存取的成本是零
//   car.engine.horsepower 看起來要「先找到 engine，再找到 horsepower」，
//   但編譯器在編譯期就把兩層偏移量【加起來】了：
//   假設 brand 佔 32、horsepower 在 Engine 內偏移 0，
//   那 car.engine.horsepower 就是「car 的位址 + 32」。
//   一次記憶體存取，與存取單層成員完全相同。
//
// 【注意事項 Pay Attention】
//   1. 組合表達 has-a、繼承表達 is-a；不確定時優先選組合。
//   2. 成員物件是【內嵌】的，不是指標 —— 生命週期自動綁定，不會洩漏。
//   3. 成員依【宣告順序】建構、反序解構；
//      建構子本體執行時所有成員都已建構完畢。
//   4. car.engine.horsepower 這種「鏈式深入」違反迪米特法則，
//      會把內部結構變成公開介面。本檔為示範語法而保留。
//   5. Engine 的類內初始值（C++11）保證了 engine 一建立就是合法狀態。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】組合與物件成員
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 什麼時候用組合、什麼時候用繼承？
//     答：問一句話就能決定：
//         「A 真的可以在任何用到 B 的地方頂替 B 嗎？」
//         可以 → is-a → 繼承；不行 → has-a → 組合。
//         汽車有引擎但不是引擎，所以是組合。
//         實務上絕大多數情況都該選組合，因為耦合鬆得多：
//         Engine 改介面只影響 Car 內部，
//         而基類改介面會波及【整個繼承體系】的所有派生類。
//     追問：那「需要重用 Engine 的程式碼」算是繼承的理由嗎？
//           → 不算。public 繼承的意義是型別替換的承諾，
//             不是程式碼重用的工具。
//             只為了重用而繼承，會讓基類的整個介面
//             變成派生類必須承擔的介面。
//
// 🔥 Q2. Car 物件裡的 engine 是指標還是實體？sizeof(Car) 包含它嗎？
//     答：是【實體】，直接內嵌在 Car 的記憶體裡，
//         所以 sizeof(Car) 包含了 sizeof(Engine)
//         （本機實測 string 32 + Engine 40 = 72）。
//         這帶來三個好處：建立 Car 時 engine 自動建構、
//         解構時自動解構、不需要 new/delete 因此不可能洩漏或懸空。
//     追問：什麼時候該改用指標成員？
//           → 當零件需要在多個擁有者之間【共享】、
//             需要在執行期【抽換】實作（多型）、
//             或需要打破循環相依時。
//             此時應該用 std::unique_ptr / std::shared_ptr 而非裸指標。
//
// ⚠️ 陷阱. car.engine.horsepower = 150; 這行程式碼能編譯、能執行，
//          那它有什麼問題？
//     答：它違反了【迪米特法則】—— 使用者被迫知道
//         「Car 內部有一個叫 engine 的成員，而它有一個叫 horsepower 的欄位」。
//         這等於把 Car 的【內部結構】變成了公開介面的一部分。
//         日後若要把 Car 改成「兩顆電動馬達」的實作，
//         所有寫過 car.engine.xxx 的程式碼全部會編譯失敗。
//     為什麼會錯：把「能不能存取」當成「該不該存取」。
//         能編譯只代表語法合法，
//         而介面設計要問的是「我承諾了什麼、日後改得動嗎」。
//         正確做法是提供意圖明確的方法：
//             car.setEngineSpec(150, "油電混合");
//         如此 Car 內部怎麼實作都與使用者無關。
//         判斷準則很實用：【一行程式碼裡出現兩個以上的點（.）
//         就該警覺】—— 那通常代表你正在穿透別人的內部結構。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class Engine {
public:
    int horsepower = 0;
    string fuelType = "汽油";

    void start() {
        cout << horsepower << " 匹馬力的" << fuelType << "引擎啟動！" << endl;
    }
};

class Car {
public:
    string brand;
    Engine engine;  // Car「擁有」一個 Engine —— 這就是組合（composition）

    void drive() {
        cout << brand << " 開始行駛" << endl;
        engine.start();
    }
};

int main() {
    Car car;
    car.brand = "Toyota";
    car.engine.horsepower = 150;    // 存取內層對象的成員
    car.engine.fuelType = "油電混合";

    car.drive();

    return 0;
}

