//   類內初始值（int horsepower = 0;）為 C++11 起的功能。

// 註 1:本檔輸出是【完全確定的】—— 沒有輸入、亂數、執行緒或位址。
//
// 註 2:Car car; 這一行會【連帶】建構它的 engine 成員 ——
//      成員依宣告順序建構（brand → engine），解構時反序。
//      因為 Engine 有類內初始值，engine 一建立就是
//      「0 匹馬力的汽油引擎」這個合法狀態，
//      即使使用者忘記設定也不會是垃圾值。
//
// 註 3:本機實測 sizeof(Engine) = 40、sizeof(Car) = 72（實作定義）。
//      engine 是【內嵌實體】而非指標，所以 Car 的大小包含了整個 Engine。
//      也因此不需要 new/delete，不可能洩漏或懸空。
//
// 註 4:car.engine.horsepower = 150; 能編譯、能執行，但違反迪米特法則 ——
//      它把「Car 內部有個 engine 成員」變成了公開介面的一部分。
//      本檔為示範語法而保留；實務應提供
//      car.setEngineSpec(150, "油電混合") 這樣的方法。

