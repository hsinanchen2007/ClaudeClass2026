//      所以 c1 即使完全沒被設定過，也印出明確且可重現的預設值。
//      「有沒有那一行 = 100」就是「有沒有 UB」的分界線。
//
// 註 2:c2 只覆蓋了 name 與 hp，其餘三個成員（mp、speed、isAlive）
