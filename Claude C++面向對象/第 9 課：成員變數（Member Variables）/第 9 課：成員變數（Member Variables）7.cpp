// =============================================================================
//  第 9 課：成員變數（Member Variables）7.cpp  —  類內初始值（NSDMI）的正確用法
// =============================================================================
//
// 【主題資訊 Information】
//   類內初始值（Non-Static Data Member Initializer, NSDMI）：
//       class C { int hp = 100; std::string name = "未命名"; };
//   標準版本：【C++11】起（這是本課最重要的版本資訊）
//   語法：可用 = 或 {}；不可用 () —— int x(0); 會被解析成函式宣告
//   優先順序：建構子的初始化列表若有指定該成員，則【覆蓋】類內初始值
//   標頭檔：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. 類內初始值解決的是「重複與遺漏」】
//   在 C++11 之前，要保證成員被初始化只能靠建構子的初始化列表：
//       GameCharacter() : name("未命名"), hp(100), mp(50),
//                         speed(1.0), isAlive(true) {}
//       GameCharacter(std::string n) : name(n), hp(100), mp(50),
//                                      speed(1.0), isAlive(true) {}
//       // 每新增一個建構子，這五個預設值就要重抄一次
//   只要有一個建構子漏掉某個成員，那個路徑就是未定義行為，
//   而且這種遺漏在程式碼審查時極難發現。
//   類內初始值只寫【一次】，所有建構子自動套用 ——
//   「不可能忘記初始化」因此變成類別定義本身的性質。
//
// 【2. 本檔示範的正是「預設值 + 選擇性覆蓋」的模式】
//       GameCharacter c1;          // 全部使用預設值
//       GameCharacter c2;
//       c2.name = "戰士";          // 只覆蓋想改的
//       c2.hp   = 150;
//   c2 的 mp、speed、isAlive 仍然是預設值 50 / 1.0 / true。
//   這個模式的價值在於：新增第六個成員時，
//   既有的所有程式碼都不必修改，也不會有任何路徑漏掉初始化。
//   對照同課 5、6 號檔沒有初始值的類別 ——
//   那裡每一個沒被手動賦值的成員都是一顆未爆彈。
//
// 【3. 為什麼不能寫 int hp(100);】
//   類內初始值只能用 = 或 {}：
//       int hp = 100;      // OK
//       int hp{100};       // OK
//       int hp(100);       // 編譯錯誤
//   因為 int hp(100); 在類別內會被解析成【函式宣告】
//   （一個叫 hp、回傳 int 的函式）——
//   這是 C++ 著名的「最令人困擾的解析（most vexing parse）」的一個變形。
//   用 {} 還有額外好處：它會擋下窄化轉換
//       int x{3.14};       // 編譯錯誤（窄化），而 int x = 3.14; 只會截斷成 3
//
// 【4. 類內初始值與建構子初始化列表的關係】
//   兩者【不衝突】，而是「預設值」與「覆蓋」的關係：
//       class GameCharacter {
//           int hp = 100;                       // 預設值
//       public:
//           GameCharacter() {}                  // hp 是 100
//           GameCharacter(int h) : hp(h) {}     // hp 是 h，類內初始值被覆蓋
//       };
//   最佳實務是：把【共通的預設值】寫成類內初始值，
//   把【依參數而異的】放進初始化列表。
//   如此既沒有重複，也保留了彈性。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 類內初始值會影響「聚合（aggregate）」的判定
//   在 C++11 中，只要有類內初始值，該類別就【不是】聚合，
//   因此不能用 GameCharacter g = {"戰士", 150, ...}; 這種聚合初始化。
//   這個限制在 C++14 被【移除】了 —— 有 NSDMI 的類別仍可以是聚合。
//   這是少見的「標準放寬限制」的例子，
//   也提醒我們談 C++ 特性時要標明版本：
//   同一段程式碼在 C++11 與 C++14 下的合法性可能不同。
//
// (B) 成員初始化的順序永遠由宣告順序決定
//   即使使用類內初始值，初始化順序仍然是【宣告順序】：
//   name → hp → mp → speed → isAlive。
//   所以一個成員的類內初始值若引用了另一個成員，順序就很重要：
//       int a = 1;
//       int b = a + 1;      // OK，a 先初始化
//       // 若順序顛倒，b 會讀到還沒初始化的 a → UB
//   -Wall 包含的 -Wreorder 只檢查建構子初始化列表的順序，
//   對這種類內初始值的相依【不會】警告，要自己小心。
//
// (C) 執行期成本是零
//   類內初始值不會產生任何額外的執行期開銷 ——
//   編譯器只是把它「複製」到每個沒有明確指定該成員的建構子裡。
//   產生的機器碼與手寫初始化列表完全相同。
//   所以這是純粹的「安全性免費升級」，沒有任何理由不用。
//
// 【注意事項 Pay Attention】
//   1. 類內初始值是【C++11】起的功能，C++98 無法使用。
//   2. 只能用 = 或 {}，【不能用 ()】—— int hp(100); 會被當成函式宣告。
//   3. 建構子初始化列表若指定了該成員，會【覆蓋】類內初始值。
//   4. 初始化順序永遠由【宣告順序】決定，與初始化列表的書寫順序無關。
//   5. 在 C++11 中有 NSDMI 的類別不是聚合，C++14 起已放寬。
//   6. 執行期成本為零 —— 產生的機器碼與手寫初始化列表相同。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】類內初始值（NSDMI）
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 類內初始值是哪個標準加入的？它解決了什麼問題？
//     答：【C++11】。它解決的是「多個建構子必須各自重抄一遍預設值」
//         所造成的重複與遺漏 —— 只要有一個建構子漏掉某個成員，
//         那條路徑就是未定義行為，而且極難在審查時發現。
//         類內初始值只寫一次、所有建構子自動套用，
//         把「不可能忘記初始化」變成類別定義本身的性質。
//     追問：它和建構子初始化列表衝突時，以哪個為準？
//           → 以【建構子初始化列表】為準，它會覆蓋類內初始值。
//             最佳實務是把共通預設值寫成 NSDMI，
//             把依參數而異的放進初始化列表。
//
// 🔥 Q2. 為什麼 int hp(100); 不能當作類內初始值？
//     答：因為在類別內部，int hp(100); 會被解析成【函式宣告】——
//         一個名叫 hp、回傳 int、接受一個未命名 int 參數的函式。
//         這是 C++「最令人困擾的解析」的變形：
//         當一段程式碼可以被解析成宣告時，標準規定就解析成宣告。
//         類內初始值只能用 = 或 {}。
//     追問：= 和 {} 有差別嗎？
//           → 有。{} 會【擋下窄化轉換】：
//             int x{3.14}; 編譯失敗，而 int x = 3.14; 只會安靜地截斷成 3。
//             所以 {} 更安全，缺點是遇到有 initializer_list 建構子的型別時
//             會優先匹配它（例如 vector<int> v{5} 是「一個元素 5」
//             而非「五個 0」）。
//
// ⚠️ 陷阱. 有了類內初始值，是不是就完全不需要寫建構子了？
//     答：不是。類內初始值只能提供【固定的預設值】，
//         它無法做三件事：
//         ① 依參數決定初始值（GameCharacter("戰士", 150)）
//         ② 做參數驗證（hp 必須大於 0）
//         ③ 建立成員之間的相依關係（例如 maxHp 決定初始 hp）
//         本檔的 c2 是先建立再逐一賦值 ——
//         這代表物件在「建立完成」與「設定完成」之間
//         有一段【狀態不正確】的空窗期。
//     為什麼會錯：把「成員都有值了」等同於「物件是有效的」。
//         有值不代表有意義 —— c2 剛建立時叫「未命名」、HP 100，
//         那並不是一個真實存在的角色。
//         真正的封裝要求物件【一建立就處於合法且有意義的狀態】，
//         這只能靠建構子做到：
//             GameCharacter(std::string n, int h) : name(std::move(n)), hp(h) {
//                 if (h <= 0) throw std::invalid_argument("HP 必須為正");
//             }
//         類內初始值與建構子是【互補】的：
//         前者保證「不會忘記初始化」，後者保證「初始化成正確的值」。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class GameCharacter {
public:
    string name = "未命名";
    int hp = 100;
    int mp = 50;
    double speed = 1.0;
    bool isAlive = true;

    void show() {
        cout << name << " | HP:" << hp << " MP:" << mp
             << " 速度:" << speed
             << " 狀態:" << (isAlive ? "存活" : "陣亡") << endl;
    }
};

int main() {
    GameCharacter c1;           // 全部使用預設值
    c1.show();

    GameCharacter c2;
    c2.name = "戰士";           // 覆蓋部分預設值
    c2.hp = 150;
    c2.show();

    return 0;
}

