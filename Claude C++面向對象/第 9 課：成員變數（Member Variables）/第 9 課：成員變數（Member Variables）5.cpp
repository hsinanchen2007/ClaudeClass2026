// 【面試題】未初始化的成員變數
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. Dangerous d; 之後讀取 d.x，會發生什麼？
//     答：那是【未定義行為】。d.x 持有不確定值（indeterminate value），
//         讀取它會讓【整個程式】的行為失去約束 ——
//         不只是「拿到一個錯的數字」而已。
//         編譯器可以假設 UB 不會發生並據此最佳化，
//         所以加上 -O2 之後可能出現看起來完全無法解釋的結果。
//         正確的說法是「不確定值 + UB」，不是「隨機的垃圾數字」。
//     追問：那 Dangerous d{}; 呢？
//           → 那是【值初始化】。對沒有自訂建構子的類別，
//             所有成員會被零初始化，x=0、y=0.0、flag=false，完全安全。
//             一對大括號就是有 UB 與沒有 UB 的分界線。
//
// 🔥 Q2. 為什麼 C++ 不乾脆讓所有變數自動歸零？
//     答：因為「不為你沒要求的東西付費」是 C/C++ 的核心設計哲學。
//         宣告一個 1MB 的緩衝區若要先清空，
//         即使你馬上要用資料覆蓋它，那個成本也省不掉。
//         把選擇權留給程式設計者，代價是必須自己負責。
//     追問：那全域變數呢？
//           → 全域與 static 變數（靜態儲存期）是【保證零初始化】的。
//             因為那是在程式載入時由作業系統把整個 .bss 區段清零，
//             一次完成、沒有額外成本。
//             所以「會不會自動歸零」取決於【儲存期】，不是型別。
//
// ⚠️ 陷阱. 「我跑了很多次，x 每次都印出同一個數字，
//          所以這塊記憶體是穩定的，程式沒問題」—— 錯在哪？
//     答：錯得很徹底，而且這正是最危險的誤判。
//         本機實測 20 次，x 確實每次都相同、y 卻有 16 個不同的值。
//         x 的「穩定」純粹是巧合 ——
//         那塊堆疊記憶體剛好被程式啟動流程寫入了固定內容。
//         只要多宣告一個區域變數、換個編譯器、
//         或改變最佳化等級（本機 -O0/-O1/-O2 實測結果都不同），
//         它隨時會變。
//     為什麼會錯：把「可重現」當成「有定義」。
//         這兩件事完全無關 —— UB 沒有義務表現得不穩定，
//         它甚至【傾向於】在測試環境中看起來正常，
//         因為程式剛啟動、呼叫層次淺，堆疊很乾淨。
//         真正的問題會在真實負載下浮現：
//         那時同一塊記憶體早已被前面數十層函式呼叫用過，
//         殘留的是別人的區域變數。
//         這就是為什麼這類 bug 的典型症狀是
//         「開發與測試都正常，上線才出事」。
//         判斷程式對不對的依據是【標準怎麼規定】，
//         永遠不是【我跑起來看起來如何】。
