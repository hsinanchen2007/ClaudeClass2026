//
//  ⚠️⚠️ 本檔是【刻意保留的錯誤示範】，它的行為是【未定義行為（UB）】。
//     它可以編譯（會產生 -Wuninitialized 警告）也可以執行，
//     但它印出來的東西【沒有任何保證】——
//     不保證是 0、不保證每次相同、不保證換個編譯器或最佳化等級還一樣。
//     因此本檔【沒有、也不可能有】確定的「預期輸出」。
//     請把它當成「要避免什麼」的教材，絕對不要當成可依賴的行為來學習。
//
// 【主題資訊 Information】
//   預設初始化（default-initialization）：
//       Dangerous d;      // 內建型別成員【不做任何事】，值不確定
//   對照 值初始化（value-initialization）：
//       Dangerous d{};    // 內建型別成員被【零初始化】
//   標準依據：[dcl.init]/7 —— 讀取不確定值是未定義行為
//   標準版本：C++98 起（{} 值初始化語法為 C++11）
//   標頭檔：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. 「垃圾值」這個說法不夠精確，而且會誤導】
//   原始註解寫「以下輸出是垃圾值 —— 每次執行可能不同」。
//   前半句給人的印象是「會讀到某個隨機但確實存在的數字」，
//   彷彿只是值不對而已。
//   標準的說法嚴格得多：這是【未定義行為】，
//   意思是【整個程式】的行為都不再受任何約束 ——
//   編譯器可以假設這件事不會發生，並據此做最佳化。
//   實務後果是：加上 -O2 之後，整段程式碼可能被刪掉、
//   分支可能被摺疊、甚至產生看起來完全無法解釋的結果。
//   所以正確的描述是「不確定值（indeterminate value）」與「UB」，
//   而不是「一個隨機的數字」。
//
// 【2. 本機實測：哪些看起來穩定，哪些明顯在變】
//   連跑 20 次，本機觀察到：
//       x    每次都是同一個值（看起來「穩定」）
//       y    20 次得到 16 個【互不相同】的值
//       flag 每次都是同一個值
//   這正是 UB 最危險的地方：
//   x 和 flag 的「穩定」純粹是巧合 ——
//   那塊堆疊記憶體剛好被程式啟動流程寫入了固定的內容。
//   換一個編譯器、加一個區域變數、改一下最佳化等級，
//   它們隨時會變。
//   本機以 -O0 / -O1 / -O2 各編一次執行，y 的值也都不同。
//   【看起來穩定的 UB，比每次都亂跳的 UB 更危險】——
//   因為它會讓人誤以為程式是對的，直到上線才爆炸。
//
// 【3. 為什麼 bool 可能印出既不是 0 也不是 1 的值】
//   同課 6 號檔有一個 bool 成員，本機實測印出過
//   96、240、192、16 等值。
//   bool 在記憶體中佔 1 byte，合法的位元樣貌只有 0 與 1；
//   未初始化時那個 byte 可能是任何值。
//   後果不只是「印出奇怪的數字」——
//   這種 bool 會讓 if (c) 與 if (!c) 【可能同時成立或同時不成立】，
//   因為編譯器產生的程式碼假設它只會是 0 或 1。
//   這是最能說明「UB 不只是值不對，而是邏輯崩壞」的例子。
//
// 【4. 三種正確的修法】
//       ① 類內初始值（C++11，最推薦）：
//              int x = 0;  double y = 0.0;  bool flag = false;
//       ② 值初始化：
//              Dangerous d{};      // 對沒有自訂建構子的類別，成員全部零初始化
//       ③ 寫建構子並使用初始化列表：
//              Dangerous() : x(0), y(0.0), flag(false) {}
//   ① 最好，因為它把「不可能忘記初始化」寫進類別定義本身，
//   即使日後新增第五個建構子也不會漏掉。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼 C++ 預設不自動歸零
//   這是 C 傳承下來的「不為你沒要求的東西付費（don't pay for what
//   you don't use）」哲學。
//   若每個區域變數都自動歸零，
//   宣告一個 1MB 的緩衝區就要先付出清空 1MB 的成本 ——
//   即使你接下來馬上要用資料覆蓋它。
//   代價就是程式設計者必須自己負責。
//   注意【靜態儲存期】的變數（全域、static）是【保證零初始化】的，
//   因為那是在程式載入時由作業系統一次完成的，不需要額外成本。
//
// (B) 怎麼在開發階段抓到這類問題
//   本檔的情況 -Wall -Wextra 就會警告（-Wuninitialized），
//   但編譯器的靜態分析能力有限，跨函式的情況經常抓不到。
//   更可靠的工具是：
//       g++ -fsanitize=memory   （Clang 的 MemorySanitizer，偵測未初始化讀取）
//       valgrind --tool=memcheck ./程式
//   實務上應該把 -Wall -Wextra -Werror 設成專案預設，
//   讓這類警告直接變成編譯失敗。
//
// (C) 「我測試過都印 0，所以沒問題」為什麼是錯的推論
//   測試環境的堆疊通常很乾淨（程式剛啟動、呼叫層次淺），
//   容易讀到 0。
//   但在真實負載下，同一塊堆疊記憶體早已被前面數十層函式呼叫用過，
//   殘留的是上一個函式的區域變數。
//   這就是為什麼這類 bug 幾乎總是「開發測試都正常，上線就出事」。
//   UB 不會因為你測不出來就消失。
//
// 【注意事項 Pay Attention】
//   1. 本檔是【錯誤示範】，讀取未初始化成員是【未定義行為】。
//   2. 【不可以】把本檔某次的輸出當成預期結果 ——
//      它沒有、也不可能有確定的輸出。
//   3. 不要說「一定會印出垃圾值」或「一定不是 0」——
//      UB 的任何結果都是允許的，包括看起來完全正常。
//   4. 某些成員看起來每次都一樣，那是巧合而非保證，
//      而且是最危險的一種（會讓人誤以為程式是對的）。
//   5. 修法：類內初始值（推薦）、值初始化 Dangerous d{}、或建構子初始化列表。
//   6. 全域與 static 變數【是】保證零初始化的，區域變數不是。
//
