//
// 註 5:一次修好所有成員的最簡方式是 InitTest t{};（值初始化，
//      對沒有自訂建構子的類別會把全部成員零初始化，ptr 也會變成 nullptr）。
//      更好的做法是給每個內建型別成員類內初始值，
//      讓型別本身保證自己的合法性 —— 見同課 7 號檔。

