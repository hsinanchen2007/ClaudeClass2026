// =============================================================================
//  第 9 課：成員變數（Member Variables）6.cpp
//  ⚠️ 對照示範：哪些成員會自動初始化、哪些不會
// =============================================================================
//
//  ⚠️⚠️ 本檔【前半段是刻意的錯誤示範】：讀取 a、b、c 這三個未初始化的
//     內建型別成員，屬【未定義行為（UB）】，沒有任何保證的輸出。
//     後半段（name、numbers）才是安全的、有確定輸出的部分。
//     本檔的價值就在這個【對照】——
//     同一個類別裡，有些成員一建立就是合法狀態，有些則是不確定值。
//
// 【主題資訊 Information】
//   預設初始化時的行為，取決於成員的【型別種類】：
//       內建型別（int/double/bool/char/指標）→ 【不做任何事】，值不確定
//       有預設建構子的類別型別（string/vector）→ 呼叫其預設建構子，狀態合法
//   標準依據：[dcl.init]/7
//   標準版本：C++98 起（類內初始值為 C++11）
//   標頭檔：<string>、<vector>
//
// 【詳細解釋 Explanation】
//
// 【1. 一條分界線：有沒有自己的預設建構子】
//   本檔把兩類成員並排放在同一個類別裡，對照非常清楚：
//       int a; double b; bool c; char d; int* ptr;
//           → 內建型別，預設初始化【什麼都不做】，值不確定，讀取即 UB
//       string name; vector<int> numbers;
//           → 類別型別，有自己的預設建構子，
//             會被建構成明確的空狀態（空字串、空容器）
//   所以 name 保證是空字串、numbers.size() 保證是 0，
//   這兩行的輸出是【確定的】；而 a、b、c 那三行不是。
//   實用規則：【內建型別成員一律給類內初始值，
//   類別型別成員可以依賴其預設建構子】。
//
// 【2. int* ptr 是本檔最危險的成員】
//   原始註解稱它為「野指標」，而且明智地【沒有】去解參考它。
//   為什麼比其他成員更危險？
//     • 讀取 a、b、c 得到的是不確定的【數值】，
//       印出來雖然是 UB，但至少不會立刻摧毀什麼
//     • 而 *ptr 是拿一個不確定的【位址】去存取記憶體 ——
//       可能讀到別的資料、可能寫壞別的資料、可能 SIGSEGV
//   更陰險的是 if (ptr) 這種檢查【完全無效】：
//   垃圾值幾乎必然非零，所以檢查會通過，然後解參考就出事。
//   指標成員一定要寫 int* ptr = nullptr;。
//
// 【3. bool 未初始化時的特殊危害】
//   本機實測，本檔的 c（bool）印出過 96、240、192、16 等值。
//   bool 佔 1 byte，但合法的位元樣貌只有 0 與 1。
//   當那個 byte 是 96 時，這個 bool 既不是 true 也不是 false —— 
//   而編譯器產生的程式碼【假設】它只會是 0 或 1，
//   於是可能出現 if (c) 與 if (!c) 同時成立（或同時不成立）的情況。
//   這是最能說明「UB 不只是值不對，而是邏輯崩壞」的具體例子。
//
// 【4. 為什麼 string 與 vector 不需要擔心】
//   它們的預設建構子保證把物件設成合法的空狀態，
//   而且【不會配置任何堆積記憶體】（空字串走 SSO、空 vector 三個指標都是 null）。
//   所以「自動初始化」在這裡沒有效能代價 ——
//   這也回應了「C++ 為什麼不乾脆全部自動歸零」：
//   類別型別可以自己決定怎樣才算合法的初始狀態，
//   而內建型別沒有這個能力，只能把選擇權交給程式設計者。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 空 std::string 與空 std::vector 的實際狀態
//   本機實測：sizeof(string) = 32、空字串的 size() = 0、capacity() = 15
//   （libstdc++ 的 SSO 內建緩衝區，不需要堆積配置）。
//   sizeof(vector<int>) = 24（三個指標），空 vector 的三個指標皆為 null，
//   size() = 0、capacity() = 0，同樣不配置堆積。
//   這些數值屬【實作定義】。
//
// (B) 靜態儲存期是唯一的例外
//   如果把 InitTest t; 改成寫在函式外面（全域）或加上 static，
//   那麼 a、b、c、d、ptr 【全部保證零初始化】。
//   因為靜態儲存期的物件是在程式載入時由作業系統把 .bss 清零，
//   一次完成、沒有額外成本。
//   所以「會不會自動歸零」取決於【儲存期】而不是型別 ——
//   這也是「把變數搬成 static 之後 bug 就消失了」這種
//   詭異現象的真正原因（bug 沒有被修好，只是被藏起來了）。
//
// (C) 值初始化可以一次解決所有成員
//       InitTest t{};      // 注意這對大括號
//   對沒有自訂建構子的類別，這會把【所有】成員零初始化，
//   包括 a、b、c、d、ptr（ptr 變成 nullptr）。
//   一對大括號就是有 UB 與沒有 UB 的分界線。
//
// 【注意事項 Pay Attention】
//   1. 本檔的 a、b、c 三行輸出是【未定義行為】，沒有預期結果。
//      name 與 numbers 兩行才是確定的。
//   2. 內建型別成員不會自動歸零；string / vector 等類別型別會。
//   3. int* ptr 未初始化是【野指標】，而且 if (ptr) 這種檢查無效
//      （垃圾值幾乎必然非零）。一定要寫 = nullptr。
//   4. 未初始化的 bool 可能持有既非 0 也非 1 的位元樣貌，
//      會讓 if(c) 與 if(!c) 的行為失去一致性。
//   5. 全域與 static 變數【是】保證零初始化的 —— 差別在儲存期，不在型別。
//   6. 修法：類內初始值（推薦）、InitTest t{}（值初始化）、
//      或建構子初始化列表。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】哪些成員會自動初始化
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼 string name 不寫初始值是安全的，但 int a 不行？
//     答：因為 std::string 是【類別型別】，有自己的預設建構子，
//         預設初始化時會被建構成明確的空狀態（size()==0）。
//         而 int 是內建型別，預設初始化【什麼都不做】，
//         值不確定、讀取即 UB。
//         分界線就是「這個型別有沒有自己的預設建構子」。
//     追問：那 std::string 的自動初始化有效能代價嗎？
//           → 幾乎沒有。空字串走 SSO（本機 libstdc++ 內建 15 字元的緩衝區），
//             【不會】配置任何堆積記憶體；空 vector 也只是把三個指標設成 null。
//             這也解釋了為什麼標準不強制內建型別歸零 ——
//             類別型別能自己定義「合法的空狀態」，內建型別不能。
//
// 🔥 Q2. int* ptr; 沒初始化，用 if (ptr) 檢查一下再用，這樣安全嗎？
//     答：完全不安全，而且這個檢查【毫無作用】。
//         未初始化的指標持有不確定值，
//         那個垃圾值幾乎必然非零 —— 所以 if (ptr) 會通過，
//         接著解參考就是拿一個隨機位址去存取記憶體。
//         更根本地說，讀取那個不確定值本身就已經是 UB 了，
//         檢查發生在 UB 之後，救不了任何東西。
//     追問：那正確做法是什麼？
//           → 宣告時就寫 int* ptr = nullptr;。
//             如此 if (ptr) 才有意義，
//             而且不小心解參考時會是穩定可重現的空指標崩潰，
//             比隨機的記憶體損毀好除錯太多。
//
// ⚠️ 陷阱. 把 InitTest t; 改成全域變數（或加 static）之後，
//          a、b、c 都印出 0 了 —— 所以問題解決了嗎？
//     答：問題【沒有】被解決，只是被藏起來了。
//         靜態儲存期的物件確實保證零初始化（.bss 在程式載入時被清零），
//         所以那些成員真的變成 0 了 —— 這一段沒有 UB。
//         但原本的類別設計缺陷還在：
//         只要有人在別的地方寫出區域的 InitTest t;，UB 立刻回來。
//     為什麼會錯：把「這次沒出事」當成「修好了」。
//         真正的修法是讓【型別本身】保證自己的合法性 ——
//         給每個內建型別成員一個類內初始值，
//         這樣不管它被建立在堆疊、堆積還是全域，都不可能出錯。
//         這也是一個很好的思考練習：
//         「把變數改成 static 之後 bug 就消失了」是很常見的現象，
//         而它幾乎總是代表你藏起了一個未初始化或生命週期的問題，
//         而不是修好了它。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <vector>
using namespace std;

class InitTest {
public:
    // === 不會自動初始化（危險！） ===
    int a;           // 垃圾值
    double b;        // 垃圾值
    bool c;          // 垃圾值
    char d;          // 垃圾值
    int* ptr;        // 野指標！

    // === 會自動初始化（安全） ===
    string name;            // 自動初始化為 ""
    vector<int> numbers;    // 自動初始化為空容器
};

int main() {
    InitTest t;

    cout << "--- 不安全的成員（垃圾值）---" << endl;
    cout << "a   = " << t.a << endl;
    cout << "b   = " << t.b << endl;
    cout << "c   = " << t.c << endl;
    // t.ptr 是野指標，千萬別 dereference！

    cout << "\n--- 安全的成員（自動初始化）---" << endl;
    cout << "name     = [" << t.name << "] (空字串)" << endl;
    cout << "numbers 大小 = " << t.numbers.size() << endl;

    return 0;
}

