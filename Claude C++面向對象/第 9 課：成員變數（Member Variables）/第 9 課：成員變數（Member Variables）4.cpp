//
// 註 2:行列式 = 3×5 − 7×1 = 15 − 7 = 8，是確定的算術結果。
//      印成 8 而非 8.0 是 iostream 對 double 的預設格式所致。
//
// 註 3:本機實測 sizeof(Matrix2x2) = 32（4 個 double × 8，無填充）。
//      data 是【內嵌陣列】而非指標，所以這個型別是 trivially copyable ——
//      可以安全地直接複製、放進容器、當回傳值。
//
// 註 4:⚠️ data 沒有類內初始值。本檔先 set() 再 print() 所以安全，
//      但若把 m.set(3,7,1,5); 那行刪掉，print() 就會讀取未初始化的成員 ——
//      那是【未定義行為】，不保證印出 0，也不保證每次相同。
//      安全寫法是宣告成 double data[2][2] = {};（值初始化，全部歸零）。
//
// 註 5:C++11 之後建議改用 std::array<std::array<double,2>,2> ——
//      不會退化成指標、有 size()、可直接比較，
//      而且 sizeof 完全相同（本機同為 32），沒有任何額外成本。

