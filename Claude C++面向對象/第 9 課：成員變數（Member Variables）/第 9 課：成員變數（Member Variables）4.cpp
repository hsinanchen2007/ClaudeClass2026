// =============================================================================
//  第 9 課：成員變數（Member Variables）4.cpp  —  以固定大小陣列作為成員
// =============================================================================
//
// 【主題資訊 Information】
//   double data[2][2];      // 二維陣列成員，大小在編譯期固定
//   記憶體佈局：C++ 的多維陣列是【列主序（row-major）】——
//               data[0][0], data[0][1], data[1][0], data[1][1] 連續排列
//   標準版本：C++98 起
//   複雜度：元素存取 O(1)（偏移量編譯期算好）
//   標頭檔：語言核心特性
//
// 【詳細解釋 Explanation】
//
// 【1. 陣列成員是【內嵌】的，物件大小包含整個陣列】
//   sizeof(Matrix2x2) = 32（4 個 double × 8 bytes，本機實測，無填充）。
//   data 不是指標，而是實實在在的 32 bytes 直接躺在物件裡。
//   這帶來一個很實用的特性：
//   Matrix2x2 可以直接複製、直接放進 vector、直接當回傳值 ——
//   因為它沒有指標成員，編譯器產生的複製建構子就是正確的
//   （逐位元組複製即可，這稱為 trivially copyable）。
//   若成員改成 double* data 並用 new 配置，
//   預設的複製就會變成兩個物件共用同一塊記憶體 ——
//   雙重釋放與懸空指標接踵而至，這就是「三五法則」要處理的問題。
//
// 【2. 為什麼二維陣列是列主序，這件事什麼時候重要】
//   data[i][j] 的實際位址是 data + (i * 2 + j) * sizeof(double)。
//   也就是說整列（row）的元素是連續的。
//   對 2×2 矩陣無所謂，但對大矩陣就是效能的分水嶺：
//       for (i) for (j) sum += m[i][j];    // 順著記憶體走，快取友善
//       for (j) for (i) sum += m[i][j];    // 每次跳一整列，大量 cache miss
//   同樣的計算、同樣的複雜度，光是迴圈順序就可能差好幾倍。
//   （順帶一提，Fortran 與 MATLAB 是行主序（column-major），
//     移植數值程式碼時這是最常見的錯誤來源。）
//
// 【3. 行列式的計算與這個類別的定位】
//   determinant() 回傳 ad − bc。
//   本檔的 3×5 − 7×1 = 15 − 7 = 8，是確定的算術結果。
//   注意這個類別把【資料】與【對資料的運算】綁在一起 ——
//   這正是成員函式的價值：
//   determinant 需要存取全部四個元素，
//   若寫成自由函式就得把整個矩陣傳進去（或暴露內部資料）。
//
// 【4. 本檔沒有處理的兩件事】
//   ① data 沒有類內初始值。Matrix2x2 m; 之後若不呼叫 set() 就 print()，
//      印出的是不確定的值（未定義行為）。
//      正確寫法是 double data[2][2] = {};（值初始化，全部歸零）。
//      本檔在 print() 之前先呼叫了 set()，所以安全。
//   ② set() 用四個位置參數，很容易把順序搞錯
//      （set(3,7,1,5) 到底哪個是 data[1][0]？）。
//      實務上可以考慮用 std::array<std::array<double,2>,2> 搭配
//      初始化列表，讓寫法本身就表達出結構。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 原生陣列成員 vs std::array 成員
//   double data[2][2] 的缺點是「一離開類別就會退化成指標」——
//   例如把它傳給函式時長度資訊就沒了。
//   std::array<std::array<double,2>,2> 則：
//     • 有 size() 成員、不會退化
//     • 支援 range-for 與標準演算法
//     • 可以直接比較（operator==）與賦值
//     • sizeof 完全相同（本機同為 32），【沒有任何額外成本】
//   C++11 之後沒有理由在類別內使用原生陣列成員。
//
// (B) 為什麼 Matrix2x2 適合傳值
//   一般規則是「大物件用 const&」，但 32 bytes 屬於小物件，
//   而且沒有指標成員、複製就是純粹的位元組搬移。
//   對這種 trivially copyable 的小型別，
//   直接傳值往往比傳參考更快（少一次間接存取，且可以放進暫存器）。
//   實務門檻大約在 16~32 bytes 之間，需要時應以實測為準。
//
// (C) determinant() 與 print() 都應該是 const
//   兩者都不修改物件，應宣告為 double determinant() const;。
//   不加的話，const Matrix2x2& 就無法呼叫它們 ——
//   而數值計算的函式幾乎一定會用 const 參考接收矩陣。
//   本檔為簡化而省略，但這在真實程式碼中會立刻造成問題。
//
// 【注意事項 Pay Attention】
//   1. 陣列成員是內嵌的，sizeof 包含整個陣列（本機 Matrix2x2 為 32）。
//   2. C++ 的多維陣列是【列主序】，走訪順序會顯著影響快取效率。
//   3. data 沒有類內初始值 —— 本檔先呼叫 set() 才 print() 所以安全，
//      但若順序顛倒就是未定義行為。安全寫法是 double data[2][2] = {};。
//   4. C++11 之後建議改用 std::array（不退化、有 size()、
//      sizeof 完全相同、零額外成本）。
//   5. determinant() 與 print() 應宣告為 const 成員函式。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】陣列成員與記憶體佈局
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. sizeof(Matrix2x2) 是多少？data 是指標嗎？
//     答：本機實測 32 bytes —— 4 個 double 各 8 bytes，剛好無需填充。
//         data 【不是指標】，而是直接內嵌在物件裡的 32 bytes。
//         這讓 Matrix2x2 成為 trivially copyable 的型別：
//         可以安全地直接複製、放進容器、當回傳值，
//         編譯器產生的複製建構子就是正確的。
//     追問：如果改成 double* data 並用 new 配置呢？
//           → sizeof 會變成 8（只剩指標），
//             而且編譯器產生的複製建構子只會複製【指標值】——
//             兩個物件指向同一塊記憶體，
//             解構時雙重釋放。這就是「三五法則」要處理的情況：
//             一旦有裸資源成員，就必須自己寫解構子、複製建構子與複製賦值。
//
// 🔥 Q2. C++ 的二維陣列在記憶體中是怎麼排的？這件事什麼時候重要？
//     答：【列主序（row-major）】——
//         data[0][0], data[0][1], data[1][0], data[1][1] 依序連續排列。
//         data[i][j] 的位址是 data + (i * 行數 + j) * sizeof(元素)。
//         對大矩陣而言這決定了走訪效率：
//         先跑 j 再跑 i（順著記憶體）是快取友善的，
//         反過來則每次都跳一整列，造成大量 cache miss ——
//         同樣的複雜度可能差好幾倍。
//     追問：其他語言呢？
//           → Fortran、MATLAB、Julia 是【行主序】。
//             移植數值程式碼時若沒注意，
//             結果會正確但效能大幅下降（或矩陣被轉置）。
//
// ⚠️ 陷阱. Matrix2x2 m; m.print(); —— 不呼叫 set() 直接印，會印出 0 嗎？
//     答：不會，而且這是【未定義行為】。
//         data 是內建型別（double）的陣列且沒有類內初始值，
//         預設初始化【什麼都不做】，內容是不確定的。
//         印出來的可能是 0（如果那塊堆疊記憶體剛好是乾淨的）、
//         可能是上一個函式留下的殘值，也可能因為編譯器的最佳化
//         而產生完全出乎意料的結果。
//     為什麼會錯：兩個常見的錯誤模型 ——
//         一是以為「陣列會自動歸零」（那是【靜態儲存期】變數的規則，
//         全域與 static 變數確實會零初始化，區域變數不會）；
//         二是以為「實測印出 0 就代表安全」——
//         但 UB 沒有義務給你穩定的結果，換個最佳化等級就可能不同。
//         正確做法是寫 double data[2][2] = {};
//         那對大括號會觸發【值初始化】，保證全部歸零。
//         同課 5、6 號檔專門示範讀取未初始化成員的後果。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
using namespace std;

class Matrix2x2 {
public:
    double data[2][2];  // 固定大小的陣列作為成員

    void set(double a, double b, double c, double d) {
        data[0][0] = a;  data[0][1] = b;
        data[1][0] = c;  data[1][1] = d;
    }

    void print() {
        cout << "| " << data[0][0] << "  " << data[0][1] << " |" << endl;
        cout << "| " << data[1][0] << "  " << data[1][1] << " |" << endl;
    }

    double determinant() {
        return data[0][0] * data[1][1] - data[0][1] * data[1][0];
    }
};

int main() {
    Matrix2x2 m;
    m.set(3, 7, 1, 5);

    m.print();
    cout << "行列式 = " << m.determinant() << endl;

    return 0;
}

