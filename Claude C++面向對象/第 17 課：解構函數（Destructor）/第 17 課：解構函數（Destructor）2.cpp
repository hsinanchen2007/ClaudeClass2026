// --- 動態物件（unique_ptr 自動管理）---
//   [建構] 智慧指標物件
// --- 離開區塊，unique_ptr 會自動 delete ---
//   [解構] 智慧指標物件
//
// === 日常實務：用區塊縮短連線持有時間 ===
//   [1] 準備階段（不需要連線）
//     >> 取得連線: postgres://primary/appdb
//        執行: SELECT id FROM pending_orders
//        執行: UPDATE pending_orders SET state='processing'
//     << 歸還連線: postgres://primary/appdb
//   [2] 運算階段（連線已歸還，不佔用連線池）
//   [3] 收尾階段
