// =============================================================================
//  第 17 課：解構函數 2  —  五種儲存期，解構時機各不相同
// =============================================================================
//
// 【主題資訊 Information】
//   主題：全域／區域／區塊／函數內／動態配置物件的解構時機比較
//   標準版本：C++98 起即有；本檔額外提到的 static 區域物件執行緒安全初始化為 C++11
//   複雜度：O(1)
//   標頭檔：<string>
//
// 【詳細解釋 Explanation】
//
// 【1. 儲存期（storage duration）決定生命週期】
//   C++ 有四種儲存期，每一種的解構時機都不同：
//     ● **自動儲存期**（區域變數）：離開作用域即解構 —— 最常見
//     ● **動態儲存期**（new 出來的）：**你呼叫 delete 時**才解構
//     ● **靜態儲存期**（全域、static）：main 結束之後才解構
//     ● **執行緒儲存期**（thread_local，C++11）：該執行緒結束時解構
//   本檔示範前三種，因為它們是日常最常遇到的。
//
// 【2. 全域物件：建構在 main 之前，解構在 main 之後】
//   本檔的 globalObj 宣告在所有函數之外。觀察輸出會發現：
//       [建構] 全域物件          ← 出現在「=== main() 開始 ===」之前
//       ...
//       [解構] 全域物件          ← 出現在最後，連 main 的區域物件都拆完了
//   這是因為全域物件在程式進入 main 之前就完成初始化，
//   而它的解構要等到 main 返回、執行期環境開始收尾時才發生。
//
// 【3. 區塊物件：大括號就是生命線】
//   任何一對 { } 都構成一個作用域。物件在區塊內建立，
//   離開那對大括號的瞬間就解構——不必等到函數結束。
//   這是刻意縮短資源持有時間的常用技巧：
//       {
//           LockGuard g(mtx);   // 只在這個區塊內持有鎖
//           doCriticalWork();
//       }                        // 離開區塊立刻解鎖
//       doOtherWork();           // 這裡已經沒有鎖了
//
// 【4. 動態物件：唯一需要你負責的一種】
//   new 出來的物件不屬於任何作用域，它會一直存在，
//   **直到你呼叫 delete**。忘記 delete 就是記憶體洩漏，
//   解構函數永遠不會被執行（6.cpp 專門示範這個問題）。
//   現代 C++ 的答案是：不要自己管，交給 std::unique_ptr／std::shared_ptr，
//   讓智慧指標的解構函數替你 delete——把動態儲存期重新變回 RAII。
//
// 【5. 全域物件的兩個真實陷阱】
//   ● **初始化順序不確定（static initialization order fiasco）**
//     不同**翻譯單元**（.cpp 檔）之間的全域物件，初始化順序是未指定的。
//     若 A.cpp 的全域物件在建構時用到 B.cpp 的全域物件，
//     就可能用到一個還沒初始化的東西。
//     解法是 **Meyers Singleton**：把全域物件改成「函數內的 static 區域物件」，
//     它保證在第一次呼叫該函數時才初始化，順序完全可控。
//   ● **解構順序同樣不確定**
//     跨翻譯單元的全域物件解構順序也是未指定的，
//     所以不要在某個全域物件的解構函數裡使用另一個全域物件。
//
// 【概念補充 Concept Deep Dive】
//
//   ● 函數內的 static 區域物件
//     它屬於靜態儲存期，但初始化被延後到**第一次執行到該行**時才發生，
//     之後每次呼叫都沿用同一個物件，解構同樣在 main 結束後。
//     C++11 起還保證這個初始化是**執行緒安全**的（俗稱 magic static），
//     這正是 Meyers Singleton 之所以可靠的原因。
//     本檔的 getLogger() 就是這個模式。
//
//   ● 為什麼「全域物件解構在 main 之後」很重要
//     因為此時 std::cout 可能已經被銷毀。標準保證 std::cout 這類
//     iostream 物件在整個程式期間可用（透過 std::ios_base::Init 機制），
//     所以本檔在全域物件的解構函數裡用 cout 是安全的；
//     但若換成你自己的全域資源，就不保證它還活著。
//
//   ● 臨時物件的生命週期
//     沒有名字的臨時物件在**完整運算式**結束時解構（通常就是那個分號）。
//     例外是綁定到 const 參考時會延長生命週期。
//
//   ● 程式異常結束時不會解構
//     呼叫 std::abort()、std::_Exit()，或程式被 SIGKILL 終止時，
//     解構函數**不會**執行。std::exit() 則會解構全域／static 物件，
//     但**不會**解構呼叫堆疊上的區域物件。
//     所以「解構函數一定會跑」這句話的前提是「正常結束生命週期」。
//
// 【注意事項 Pay Attention】
//   1. 跨翻譯單元的全域物件，初始化與解構順序都是未指定的。
//   2. new 出來的物件不會自動解構，忘記 delete 就是洩漏。
//   3. std::exit() 不會解構堆疊上的區域物件；abort() 什麼都不解構。
//   4. 需要「全域唯一」時，優先用函數內 static（Meyers Singleton），
//      而不是真正的全域物件。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】物件生命週期與解構時機
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 全域物件、區域物件、new 出來的物件，解構時機分別是什麼？
//     答：區域物件在離開作用域時解構；new 出來的物件要等到你呼叫 delete
//         才解構（忘記就洩漏）；全域與 static 物件在 main 返回之後、
//         程式收尾時才解構，順序是初始化順序的反序。
//     追問：那 thread_local 呢？
//         → C++11 的執行緒儲存期，在該執行緒結束時解構。
//
// 🔥 Q2. 什麼是 static initialization order fiasco？怎麼解決？
//     答：不同翻譯單元（.cpp）之間的全域物件，初始化順序是**未指定**的。
//         若 A.cpp 的全域物件在建構時用到 B.cpp 的全域物件，
//         可能會用到尚未初始化的物件。解構順序同樣未指定。
//         標準解法是 Meyers Singleton：把它改成函數內的 static 區域物件，
//         初始化延後到第一次呼叫時，順序完全可控；
//         C++11 起還保證這個初始化是執行緒安全的。
//     追問：同一個 .cpp 內的全域物件順序確定嗎？
//         → 確定，依定義出現的先後順序初始化，反序解構。
//
// ⚠️ 陷阱. 只要程式結束，所有物件的解構函數都一定會被呼叫，對嗎？
//     答：不對。前提是「正常結束生命週期」。呼叫 std::abort() 或
//         std::_Exit()、或程式被強制終止時，解構函數完全不會執行。
//         而 std::exit() 會解構全域與 static 物件，
//         卻**不會**解構呼叫堆疊上的區域物件。
//     為什麼會錯：把「RAII 保證釋放」當成無條件成立。RAII 保證的是
//         「正常離開作用域時一定釋放」；異常終止路徑不在這個保證範圍內。
//         這也是為什麼真正關鍵的資料要靠作業系統層級的機制
//         （例如檔案系統的 journal），而不能只靠解構函數。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <memory>
#include <string>
using namespace std;

class Tracker {
private:
    string name;

public:
    Tracker(const string& n) : name(n) {
        cout << "  [建構] " << name << endl;
    }

    ~Tracker() {
        cout << "  [解構] " << name << endl;
    }
};

// ====== 全域物件：建構在 main 之前，解構在 main 之後 ======
Tracker globalObj("全域物件");

void testFunction() {
    cout << "\n--- 進入 testFunction ---" << endl;
    Tracker funcObj("函數局部物件");
    cout << "--- 離開 testFunction ---" << endl;
}  // funcObj 在這裡被解構

// -----------------------------------------------------------------------------
// Meyers Singleton：函數內 static 區域物件
//   初始化延後到「第一次呼叫本函數」時才發生，因此不受跨翻譯單元的
//   初始化順序問題影響；C++11 起還保證此初始化為執行緒安全。
// -----------------------------------------------------------------------------
Tracker& getLogger() {
    static Tracker instance("函數內static物件");   // 只會建構一次
    return instance;
}

// -----------------------------------------------------------------------------
// 【日常實務範例】用區塊縮短資源持有時間
//   情境：批次處理流程中，只有中間一小段需要持有昂貴的資源
//         （資料庫連線、檔案鎖、大型緩衝區）。
//   重點：把資源包在自己的 { } 區塊裡，離開區塊立刻釋放，
//         而不是讓它一路活到函數結束。這在長時間執行的服務中
//         能顯著降低資源佔用與競爭。
// -----------------------------------------------------------------------------
class DbConnection {
private:
    string dsn_;
public:
    explicit DbConnection(const string& dsn) : dsn_(dsn) {
        cout << "    >> 取得連線: " << dsn_ << endl;
    }
    ~DbConnection() {
        cout << "    << 歸還連線: " << dsn_ << endl;
    }
    void query(const string& sql) const {
        cout << "       執行: " << sql << endl;
    }
};

static void batchJob() {
    cout << "  [1] 準備階段（不需要連線）" << endl;

    {   // ← 只有這個區塊需要連線
        DbConnection conn("postgres://primary/appdb");
        conn.query("SELECT id FROM pending_orders");
        conn.query("UPDATE pending_orders SET state='processing'");
    }   // ← 離開區塊立刻歸還連線

    cout << "  [2] 運算階段（連線已歸還，不佔用連線池）" << endl;
    cout << "  [3] 收尾階段" << endl;
}

int main() {
    cout << "\n=== main() 開始 ===" << endl;

    // ====== 區域物件 ======
    Tracker localObj("局部物件");

    // ====== 區塊內物件 ======
    {
        cout << "\n--- 進入區塊 ---" << endl;
        Tracker blockObj("區塊物件");
        cout << "--- 離開區塊 ---" << endl;
    }  // blockObj 在這裡被解構

    cout << "\n--- 區塊已結束 ---" << endl;

    // ====== 函數呼叫 ======
    testFunction();

    // ====== 函數內 static（Meyers Singleton）======
    cout << "\n--- 函數內 static：第一次呼叫才建構 ---" << endl;
    getLogger();
    cout << "--- 第二次呼叫，沿用同一個物件（不會再建構）---" << endl;
    getLogger();

    // ====== 動態物件：手動 delete ======
    cout << "\n--- 動態物件（手動 delete）---" << endl;
    Tracker* heapObj = new Tracker("動態物件");
    cout << "--- 呼叫 delete ---" << endl;
    delete heapObj;  // 解構函數在 delete 時才被呼叫

    // ====== 動態物件：交給 unique_ptr 管理 ======
    cout << "\n--- 動態物件（unique_ptr 自動管理）---" << endl;
    {
        auto smart = make_unique<Tracker>("智慧指標物件");
        cout << "--- 離開區塊，unique_ptr 會自動 delete ---" << endl;
    }

    // ====== 日常實務：用區塊縮短資源持有時間 ======
    cout << "\n=== 日常實務：用區塊縮短連線持有時間 ===" << endl;
    batchJob();

    cout << "\n=== main() 結束 ===" << endl;
    return 0;
}
// localObj 在這裡被解構
// globalObj 與 getLogger() 的 static 物件在 main() 結束之後才被解構
// （靜態儲存期物件的解構順序是其初始化順序的反序）

