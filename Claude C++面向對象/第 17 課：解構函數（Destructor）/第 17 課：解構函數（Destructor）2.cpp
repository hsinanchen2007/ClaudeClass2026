//   [建構] 函數內static物件
// --- 第二次呼叫，沿用同一個物件（不會再建構）---
//
// --- 動態物件（手動 delete）---
//   [建構] 動態物件
// --- 呼叫 delete ---
//   [解構] 動態物件
