// =============================================================================
//  第 17 課：解構函數 1  —  解構函數是什麼、什麼時候被呼叫
// =============================================================================
//
// 【主題資訊 Information】
//   語法：  ~ClassName();      // 沒有回傳型別、沒有參數、不可重載
//   標準版本：C++98 起即有；C++11 起解構函數預設為 noexcept
//   複雜度：取決於要釋放的資源；本檔的示範是 O(1)
//   標頭檔：<string>
//
// 【詳細解釋 Explanation】
//
// 【1. 解構函數的三個「沒有」與一個「唯一」】
//   ● 沒有回傳型別（連 void 都不寫）—— 它不是給你呼叫來取值的
//   ● 沒有參數 —— 所以**不能重載**，一個類別最多只有一個解構函數
//   ● 沒有辦法選擇不呼叫 —— 只要物件正常結束生命週期就一定會執行
//   ● 唯一的名字：~ 加上類別名，例如 ~SimpleObject
//   為什麼不能有參數？因為呼叫它的是**編譯器**，不是你。
//   編譯器在物件生命週期結束時自動插入這個呼叫，它沒有辦法替你決定要傳什麼。
//
// 【2. 它在什麼時候被呼叫】
//   一句話：**物件的生命週期結束時**。具體有五種情況：
//     (a) 區域物件離開作用域（最常見，本檔示範的就是這種）
//     (b) 對 new 出來的物件呼叫 delete
//     (c) 臨時物件在完整運算式結束時
//     (d) 全域／static 物件在 main 結束之後
//     (e) 容器元素被移除，或容器本身被銷毀
//   本檔只示範最基本的 (a)，其餘在 2.cpp 展開。
//
// 【3. 解構順序：與建構順序完全相反（後進先出）】
//   本檔在 main 裡依序建立 a、b，離開時的解構順序是 b、a。
//   這是因為區域物件放在堆疊（stack）上，遵守後進先出（LIFO）。
//   語言保證這個順序的理由很實際：後建立的物件可能依賴先建立的物件，
//   反序拆除才能保證「依賴別人的先走、被依賴的後走」。
//
// 【4. 注意 main 裡最後那行 cout 的位置】
//   本檔的輸出是這樣：
//       === main() 開始 ===
//       [建構] 物件A
//       [建構] 物件B
//       === main() 結束 ===      ← 這行先印
//       [解構] 物件B             ← 然後才解構
//       [解構] 物件A
//   很多人會誤以為「解構訊息應該在 main 結束之前」。實際上
//   cout << "=== main() 結束 ===" 只是一行普通敘述，
//   物件要到 return 0; 之後、真正離開 main 的作用域時才解構。
//
// 【5. 解構函數的正確用途】
//   它的職責是「歸還這個物件借用的東西」：
//     ● 釋放動態記憶體（3.cpp）
//     ● 關閉檔案、socket、資料庫連線（4.cpp）
//     ● 解鎖 mutex、離開臨界區
//     ● 記錄結束時間、輸出統計（5.cpp）
//   這個「取得資源即初始化、離開作用域即釋放」的模式叫 **RAII**
//   （Resource Acquisition Is Initialization），是 C++ 最核心的慣用法。
//
// 【概念補充 Concept Deep Dive】
//
//   ● 編譯器會自動產生解構函數
//     若你不寫，編譯器會生成一個「什麼都不做」的解構函數
//     （它仍然會依序解構每個成員與基底）。
//     所以只有在「這個類別確實持有需要手動歸還的資源」時才需要自己寫。
//     若所有成員都是 std::string、std::vector、std::unique_ptr 這類
//     會自我管理的型別，你根本不需要寫解構函數——這叫 **Rule of Zero**。
//
//   ● 解構函數在 C++11 之後預設是 noexcept
//     這意味著若解構函數拋出例外且未被攔下，程式會直接呼叫 std::terminate。
//     所以**絕對不要讓例外離開解構函數**。若解構過程可能失敗
//     （例如關檔失敗），要嘛在裡面攔下並記錄，要嘛提供一個明確的
//     close() 讓使用者在正常流程中處理錯誤。
//
//   ● 為什麼解構函數不能重載
//     重載需要靠參數型別區分，而解構函數沒有參數。
//     更根本的原因是語意上不需要：一個物件只有一種「結束」方式。
//
//   ● 可以手動呼叫 obj.~SimpleObject() 嗎
//     語法上可以，但除了搭配 placement new 的進階場景外**絕對不要這樣做**。
//     手動呼叫之後，物件離開作用域時還會再被解構一次，
//     形成重複解構（double destruction），那是未定義行為。
//
// 【注意事項 Pay Attention】
//   1. 解構順序是建構順序的完全反序（LIFO）。
//   2. 不要讓例外離開解構函數（C++11 起預設 noexcept）。
//   3. 不要手動呼叫解構函數，除非你在做 placement new 的配對清理。
//   4. 沒有需要手動歸還的資源時，就不要寫解構函數（Rule of Zero）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】解構函數基礎
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 解構函數可以有參數嗎？可以重載嗎？為什麼？
//     答：都不行。解構函數沒有參數也沒有回傳型別，因此無法重載，
//         一個類別最多只有一個。根本原因是它由**編譯器**在物件生命週期
//         結束時自動呼叫，編譯器沒辦法替你決定要傳什麼引數；
//         語意上一個物件也只有一種「結束」方式。
//     追問：那如果我想在銷毀時做不同的事呢？
//         → 把差異放進成員狀態，解構函數依狀態決定行為；
//           或改用不同的類別／策略物件。
//
// 🔥 Q2. 多個區域物件的解構順序是什麼？為什麼要這樣規定？
//     答：與建構順序完全相反（後進先出）。因為區域物件在堆疊上，
//         而且後建立的物件可能依賴先建立的物件；反序拆除才能保證
//         「依賴別人的先消失、被依賴的後消失」，不會有人在依賴對象
//         已被銷毀後還被使用。
//     追問：類別的成員變數呢？
//         → 同樣是反序：依宣告順序建構，依宣告的反序解構。
//
// ⚠️ 陷阱. 程式印出「=== main() 結束 ===」之後才印解構訊息，
//          是不是代表解構函數在 main 結束「之後」才跑，有點晚？
//     答：不晚，這正是正確行為。那行 cout 只是 main 裡的一行普通敘述，
//         而區域物件要到離開 main 的作用域時才解構。
//         真正需要注意的是**全域與 static 物件**，它們才是在 main
//         完全結束之後才解構的（見 2.cpp）。
//     為什麼會錯：把「程式碼寫在最後一行」與「生命週期結束」混為一談。
//         物件的解構時機取決於作用域，不取決於你把 cout 寫在哪裡。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

class SimpleObject {
private:
    string name;

public:
    SimpleObject(const string& n) : name(n) {
        cout << "  [建構] " << name << " 被創建了" << endl;
    }

    // 解構函數：沒有回傳型別、沒有參數、不可重載
    ~SimpleObject() {
        cout << "  [解構] " << name << " 被銷毀了" << endl;
    }
};

// -----------------------------------------------------------------------------
// 示範：類別成員也是「依宣告順序建構、依反序解構」
// -----------------------------------------------------------------------------
class Container {
private:
    SimpleObject first;    // 宣告第 1 → 先建構、後解構
    SimpleObject second;   // 宣告第 2 → 後建構、先解構

public:
    Container()
        : first("成員first"), second("成員second")
    {
        cout << "  [建構] Container 函數體" << endl;
    }

    ~Container() {
        cout << "  [解構] Container 函數體（成員還沒被拆）" << endl;
    }
};

// -----------------------------------------------------------------------------
// 【日常實務範例】RAII 鎖守衛：離開作用域一定會解鎖
//   情境：多執行緒程式中，進入臨界區要上鎖、離開要解鎖。
//         如果靠手動 unlock()，只要中間有 return、break 或拋出例外，
//         就會忘記解鎖而造成死結——這是實務上非常昂貴的 bug。
//   重點：把「解鎖」放進解構函數，就變成語言保證的事：
//         不論從哪條路徑離開作用域，解構函數一定會執行。
//         標準函式庫的 std::lock_guard 就是這個模式，
//         這裡用一個簡化版（不涉及真正的執行緒）呈現原理。
// -----------------------------------------------------------------------------
class FakeMutex {
private:
    string name_;
    bool locked_ = false;

public:
    explicit FakeMutex(const string& name) : name_(name) { }

    void lock() {
        locked_ = true;
        cout << "    >> 上鎖: " << name_ << endl;
    }
    void unlock() {
        locked_ = false;
        cout << "    << 解鎖: " << name_ << endl;
    }
    bool isLocked() const { return locked_; }
};

class LockGuard {
private:
    FakeMutex& mtx_;      // 參考成員：一定要有、不更換（第 16 課主題）

public:
    explicit LockGuard(FakeMutex& mtx) : mtx_(mtx) {
        mtx_.lock();
    }

    // 關鍵：不論怎麼離開作用域，這裡一定會執行
    ~LockGuard() {
        mtx_.unlock();
    }
};

// 用 early return 證明「手動解鎖會漏、RAII 不會」
static void processWithGuard(FakeMutex& mtx, int value) {
    cout << "  處理 value=" << value << endl;
    LockGuard guard(mtx);

    if (value < 0) {
        cout << "    值不合法，提早 return" << endl;
        return;              // 即使從這裡離開，解構函數照樣解鎖
    }
    cout << "    正常處理完成" << endl;
}

int main() {
    cout << "=== main() 開始 ===" << endl;

    SimpleObject a("物件A");
    SimpleObject b("物件B");

    cout << "=== main() 區域物件建立完畢 ===" << endl;

    cout << "\n=== 成員的建構／解構順序 ===" << endl;
    {
        Container c;
        cout << "  --- 即將離開區塊 ---" << endl;
    }

    cout << "\n=== 日常實務：RAII 鎖守衛 ===" << endl;
    FakeMutex mtx("帳戶餘額鎖");
    processWithGuard(mtx, 100);      // 正常路徑
    cout << "  離開後 isLocked = " << (mtx.isLocked() ? "true" : "false") << endl;
    processWithGuard(mtx, -1);       // 提早 return 的路徑
    cout << "  離開後 isLocked = " << (mtx.isLocked() ? "true" : "false") << endl;

    cout << "\n=== main() 結束（下面才會解構 a、b）===" << endl;
    return 0;
}
// a 和 b 在這裡離開作用域，解構函數被自動呼叫（順序與建構相反：先 b 後 a）

