// =============================================================================
//  第 17 課 範例 7  —  用「存活物件計數」觀察解構的時機與順序
// =============================================================================
//
// 【主題資訊 Information】
//   語法    ：~ClassName();          // 無回傳值、無參數、不可多載
//   標準版本：C++98 起即有；C++11 起解構函數預設隱含 noexcept
//   標頭檔  ：本範例僅需 <iostream>、<string>
//   複雜度  ：解構本身 O(1)；但它會連帶解構所有成員，成本取決於成員
//
// 【詳細解釋 Explanation】
//
// 【1. 解構函數什麼時候被呼叫】
//   解構函數不是「你呼叫它」，而是「生命週期結束時編譯器替你呼叫」。
//   四種常見時機：
//     (a) 區域物件：離開其所在的 scope（`}`）時，自動呼叫。
//     (b) 動態物件：對指標執行 delete 時呼叫。
//     (c) 暫時物件：在完整運算式結束（分號）時呼叫。
//     (d) 成員／基底：外層物件被解構時，自動連帶解構。
//   本範例示範的是 (a)：Beta 與 Charlie 在內層區塊的 `}` 就被解構，
//   而不是等到 main 結束。這正是 C++ 與 GC 語言最根本的差異 ——
//   釋放時機是「確定的、可預測的」，不是「某個時候垃圾回收器高興了才做」。
//
// 【2. 解構順序：嚴格的後進先出（LIFO）】
//   同一個 scope 內的區域物件，解構順序與建構順序完全相反。
//       建構：Alpha → Beta → Charlie → Delta
//       解構：Delta → Alpha          （Beta/Charlie 在內層區塊就先走了）
//   為什麼一定是反序？因為後建立的物件可能依賴先建立的物件
//   （例如後者持有前者的參考、或是在前者管理的緩衝區上工作）。
//   反序銷毀能保證「你依賴的東西一定比你晚死」，這是 RAII 能成立的基礎。
//   這個順序是標準規定的，不是實作巧合。
//
// 【3. 為什麼解構訊息裡寫的是 count - 1】
//   這是本範例一個容易被忽略的細節。進入解構函數時，這個物件「還活著」，
//   count 尚未遞減。若直接印 count，會把正在死去的自己也算進去，
//   顯示的數字比實際存活數多 1。所以先用 count - 1 表達「我走之後還剩幾個」，
//   接著才真正遞減。
//   實務上更穩健的寫法是先 --count 再輸出，避免「印出的值」與「狀態」不同步；
//   本範例保留 count - 1 的寫法，是為了凸顯「解構函數執行期間物件仍存在」
//   這個重要事實。
//
// 【4. static 成員為什麼要在類別外定義】
//   類別內的 `static int count;` 只是宣告，沒有配置儲存空間。
//   必須在類別外寫 `int LifeCycle::count = 0;` 才真正定義出那一份唯一的變數，
//   否則連結階段會出現 undefined reference。
//   （C++17 起可改用 `inline static int count = 0;` 就地定義，不必再寫這一行。）
//
// 【概念補充 Concept Deep Dive】
//   區域物件的解構，本質上是編譯器在 scope 結束處自動插入的呼叫。
//   編譯器會為每個 scope 維護一份「已建構物件」的清單，並在所有離開路徑
//   （正常結束、return、break、goto，以及擲出例外時的堆疊展開）
//   都插入反序的解構呼叫。
//   這代表：即使函式中途 return，甚至丟出例外，區域物件的解構函數依然會被執行 ——
//   這就是 RAII 能夠保證資源不洩漏的機制來源，也是它遠優於
//   「記得在每個 return 前手動釋放」的原因：你不可能漏掉任何一條路徑。
//
//   記憶體佈局上，static 成員不屬於任何物件實例，它存放在靜態儲存區，
//   所有實例共用同一份。因此 sizeof(LifeCycle) 不包含 count 的大小。
//
// 【注意事項 Pay Attention】
// 1. 解構函數不可有參數、不可多載、不可回傳值 —— 一個類別只有一個解構函數。
// 2. C++11 起解構函數預設隱含 noexcept。若解構函數真的讓例外逸出，
//    程式會呼叫 std::terminate()。注意這是標準規定的結果，
//    不是未定義行為，兩者要分清楚。
// 3. 若類別會被當成多型基底（透過基底指標 delete），基底的解構函數
//    必須宣告為 virtual，否則是未定義行為。本範例沒有繼承，故不需要。
// 4. 本範例用 `using namespace std;`。在小型教學檔可接受，
//    但在標頭檔或大型專案中應避免 —— 它會把整個 std 灌進全域命名空間，
//    容易與自訂名稱（如 count、distance）撞名。
// 5. 靜態成員 count 在多執行緒下遞增／遞減並非原子操作。
//    本範例為單執行緒；真要跨執行緒統計必須改用 std::atomic<int>。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】解構函數與物件生命週期
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 同一個 scope 裡的多個區域物件，解構順序是什麼？為什麼要這樣規定？
//     答：嚴格反序（後建構的先解構，LIFO）。因為後建立的物件可能依賴
//         先建立的物件（持有它的參考、或使用它管理的資源）。
//         反序銷毀能保證「被依賴者一定活得比依賴者久」，
//         RAII 的正確性就是建立在這個保證上。這是標準規定，不是實作細節。
//     追問：那不同 translation unit 的全域物件，解構順序是什麼？
//         → 未定義的初始化順序問題（static initialization order fiasco）：
//           跨 TU 的全域物件建構順序未指定，解構順序也因此不可靠。
//           解法是用「函式內的 static 區域變數」（Meyers Singleton）延後初始化。
//
// 🔥 Q2. 如果函式中途 return，或是丟出例外，區域物件的解構函數還會執行嗎？
//     答：都會。編譯器在所有離開 scope 的路徑上都會插入反序的解構呼叫，
//         擲出例外時則由堆疊展開（stack unwinding）負責。
//         這正是 RAII 優於「手動在每個 return 前釋放」的根本原因：
//         你不可能漏掉某一條路徑，因為那不是你在寫的。
//     追問：那什麼情況下解構函數「不會」被執行？
//         → 呼叫 std::abort() / std::_Exit()、發生未被處理的例外導致
//           terminate（是否先展開堆疊由實作決定）、以及 new 出來卻沒 delete
//           的動態物件。另外，用基底指標 delete 一個沒有 virtual 解構函數的
//           衍生物件也是未定義行為。
//
// ⚠️ 陷阱. 「物件進到解構函數，就代表它已經被銷毀了，
//           所以此時 count 已經扣掉自己了。」
//     答：錯。進入解構函數時，物件仍然完整存在 —— 成員都還有效，
//         你甚至可以在裡面正常使用它們（這正是解構函數能釋放資源的前提）。
//         count 是程式碼自己維護的，只有執行到 `count--` 那一行才會變。
//         所以本範例才要印 count - 1 來表達「我走之後剩幾個」。
//     為什麼會錯：把「解構函數開始執行」誤當成「物件已經消失」。
//         正確的心智模型是：解構函數是物件生命的「最後遺言」，
//         說完之後成員才依序被銷毀、儲存空間才被回收。
//         成員的解構發生在解構函數本體「執行完畢之後」，而不是之前。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
using namespace std;

// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】—— 本檔不附
//   理由：本檔主題是「解構函數的觸發時機與 LIFO 順序」，
//   屬於物件生命週期與 RAII 的語言機制。LeetCode 評測的是演算法的
//   輸入輸出正確性，題目不會因為「解構在哪一行發生」而有不同結果；
//   設計類題目（146 LRU Cache、155 Min Stack 等）考的也是資料結構操作，
//   而非資源釋放時機。硬掛一題只會模糊焦點，
//   依規格「寧缺勿濫」從缺，改附一個真正依賴解構時機的實務情境。
// -----------------------------------------------------------------------------

class LifeCycle {
private:
    string name;
    static int count;  // 追蹤當前存活的物件數量（所有實例共用同一份）

public:
    LifeCycle(const string& n) : name(n) {
        count++;
        cout << "  [建構] " << name
             << " (目前存活: " << count << " 個)" << endl;
    }

    ~LifeCycle() {
        // 注意：此刻物件「仍然存在」，count 還沒扣掉自己，
        // 所以要印 count - 1 才是「我離開之後的存活數」。
        cout << "  [解構] " << name
             << " (目前存活: " << count - 1 << " 個)" << endl;
        count--;
    }

    static int getCount() { return count; }
};

int LifeCycle::count = 0;  // 靜態成員定義（C++17 起可改用 inline static 就地定義）

// -----------------------------------------------------------------------------
// 【日常實務範例】資料庫交易的 RAII scope guard
//   情境：一段業務邏輯要在交易中執行。規則是「正常走完就 commit，
//         中途 return 或丟例外就必須 rollback」。
//   為什麼一定要用解構函數：離開路徑有很多條（提前 return、驗證失敗、
//         例外），手寫「每個 return 前記得 rollback」遲早會漏掉一條。
//         把 rollback 放進解構函數，編譯器保證每一條離開路徑都會執行它。
//   這正是本課「解構時機是確定的」這個性質最典型的工程應用。
// -----------------------------------------------------------------------------
class Transaction {
    string  id;
    bool    committed = false;
public:
    explicit Transaction(const string& txId) : id(txId) {
        cout << "    BEGIN   " << id << endl;
    }
    void commit() {
        committed = true;
        cout << "    COMMIT  " << id << endl;
    }
    ~Transaction() {
        // 沒有明確 commit 過，一律視為失敗並回滾
        if (!committed) {
            cout << "    ROLLBACK " << id << " (未 commit，自動回滾)" << endl;
        }
    }
};

// 正常完成：走到最後有 commit
static void transferMoney(bool amountValid) {
    Transaction tx("TX-1001");
    cout << "    檢查金額..." << endl;
    if (!amountValid) {
        cout << "    金額不合法，提前 return" << endl;
        return;                 // ← 這條路徑沒有 commit，解構時自動 rollback
    }
    cout << "    扣款、入帳" << endl;
    tx.commit();
}

int main() {
    cout << "=============================" << endl;
    cout << "  物件生命週期觀察" << endl;
    cout << "=============================" << endl;

    LifeCycle a("Alpha");

    {
        LifeCycle b("Beta");
        LifeCycle c("Charlie");

        cout << "\n  --- 區塊內：存活 "
             << LifeCycle::getCount() << " 個 ---\n" << endl;
    }
    // b 和 c 在這裡被解構（反序：先 Charlie 後 Beta）

    cout << "\n  --- 區塊外：存活 "
         << LifeCycle::getCount() << " 個 ---\n" << endl;

    LifeCycle d("Delta");

    cout << "\n  --- 日常實務：交易 scope guard ---" << endl;
    cout << "  情境 1：金額合法，正常完成" << endl;
    transferMoney(true);
    cout << "  情境 2：金額不合法，提前 return" << endl;
    transferMoney(false);

    cout << "\n  --- main() 即將結束 ---" << endl;
    return 0;
}
// d 和 a 在這裡被解構（反序：先 Delta 後 Alpha）

