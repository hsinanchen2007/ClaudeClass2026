// -----------------------------------------------------------------------------
// 【日常實務範例】統計計數器（Metrics）：漏掉初始化如何污染監控資料
//   情境：服務要回報「處理筆數 / 失敗筆數 / 累計延遲」。這種 struct 幾乎
//         一定是「宣告出來就開始 ++」，沒有人會特地寫 constructor。
//         一旦成員沒有 NSDMI，計數器就從一個不確定值開始累加——
//         監控圖表會出現天文數字或負值，而且完全無法重現。
//   正解：計數器型別的每個欄位都給 NSDMI，讓「歸零」成為型別的保證。
// -----------------------------------------------------------------------------
struct MetricsUnsafe {      // ❌ 沒有任何初始化保證
    long processed;
    long failed;
    double totalLatencyMs;
};

struct Metrics {            // ✅ NSDMI：一定從 0 開始
    long   processed      = 0;
    long   failed         = 0;
    double totalLatencyMs = 0.0;

    void record(bool ok, double latencyMs) {
        ++processed;
        if (!ok) ++failed;
        totalLatencyMs += latencyMs;
    }

    double avgLatencyMs() const {
        return processed == 0 ? 0.0 : totalLatencyMs / static_cast<double>(processed);
    }

    void report() const {
        cout << "  處理 " << processed << " 筆，失敗 " << failed
             << " 筆，平均延遲 " << avgLatencyMs() << " ms" << endl;
    }
};

int main() {
    cout << "=== 編譯器自動生成的預設建構函數（區域物件）===" << endl;
    AutoGenerated obj;
    obj.print();
    cout << "  ↑ text 保證為空字串；number / value 的值不確定，" << endl;
    cout << "    每次執行都可能不同，讀取它們本身就是 UB。" << endl;

    cout << "\n=== 同一個類別，但是全域物件 ===" << endl;
    gAuto.print();
    cout << "  ↑ 全域／static 物件有 zero-initialization 保證，所以是 0。" << endl;
    cout << "    這是 storage duration 的效果，不是 constructor 幫你做的。" << endl;

    cout << "\n=== 值初始化：呼叫端自保 ===" << endl;
    AutoGenerated vi{};      // AutoGenerated 沒有使用者宣告的 constructor → 歸零
    vi.print();

    cout << "\n=== 正解：NSDMI（類別作者負責）===" << endl;
    FixedByNSDMI fixed;
    fixed.print();
    cout << "  ↑ 不需要呼叫端配合，怎麼建構都是這個值" << endl;

    cout << "\n=== 日常實務：監控計數器 ===" << endl;
    Metrics m;               // 不必寫 constructor，NSDMI 已保證從 0 開始
    m.record(true,  12.5);
    m.record(true,   8.0);
    m.record(false, 45.5);
    m.report();
    cout << "  若改用 MetricsUnsafe，processed 會從一個不確定值開始累加，" << endl;
    cout << "  監控圖表可能出現天文數字或負值，且無法重現。" << endl;

    return 0;
}

