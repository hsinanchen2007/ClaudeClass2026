// =============================================================================
//  第 25 課：類別內的靜態成員函數 7  —  靜態函式 vs 全域函式 vs 命名空間函式
// =============================================================================
//
// 【主題資訊 Information】
//   三種放置自由函式的方式:
//     (1) 全域函式         int add(int, int);
//     (2) 命名空間函式     namespace N { int add(int, int); }
//     (3) 類別靜態函式     class C { static int add(int, int); };
//   標準版本: 三者皆 C++98；本檔的 inline static 資料成員初始化為 C++17
//   標頭檔: <string>
//
// 【詳細解釋 Explanation】
//
// 【1. 三者的真正差別在「歸屬」與「權限」】
//   ┌──────────────┬──────────┬────────────┬──────────────┐
//   │              │ 全域函式  │ 命名空間    │ 類別靜態函式  │
//   ├──────────────┼──────────┼────────────┼──────────────┤
//   │ 避免名稱衝突  │ 差       │ 好          │ 好           │
//   │ 存取 private │ 不行     │ 不行        │ 可以         │
//   │ 維護私有狀態  │ 不行     │ 不行（註）  │ 可以         │
//   │ 可跨檔案擴充  │ —        │ 可以        │ 不行         │
//   │ 可當 template │ 可以     │ 可以        │ 可以         │
//   └──────────────┴──────────┴────────────┴──────────────┘
//   註：命名空間可以放變數，但那是公開的；
//       要做到「只有這組函式看得到」的私有狀態，
//       得用未具名命名空間（限單一 TU）或類別的 private static。
//
// 【2. 選擇準則（實務上真正的判斷方式）】
//   * 函式需要碰某個類別的 private 實作 → 類別靜態函式。
//   * 純粹的自由函式、與任何類別的內部都無關 → 命名空間。
//     這通常才是正確答案，別為了「看起來物件導向」硬包成類別。
//   * 全域函式 → 幾乎沒有理由，除非是要相容 C 的介面。
//   本檔 Calculator::add 需要維護 operationCount_ 這個私有狀態，
//   所以類別靜態函式是對的；若沒有那個計數器，MathUtils::add 就足夠了。
//
// 【3. 為什麼「用類別當命名空間」常被批評】
//   Java 沒有自由函式，所有東西都得塞進類別，
//   因此不少人把這個習慣帶進 C++，寫出一堆只有靜態函式的類別。
//   在 C++ 這是次佳解：命名空間可以跨檔案擴充、
//   支援 ADL（argument-dependent lookup）、不必寫 = delete 防實例化，
//   也不會被誤繼承。C++ Core Guidelines 的 C.4 / C.5 就是這個方向。
//
// 【概念補充 Concept Deep Dive】
//   * 名稱查找差異：命名空間函式支援 ADL ——
//     若引數型別屬於該命名空間，不必寫命名空間前綴也找得到。
//     類別的靜態函式沒有這個待遇，永遠得寫 C::f()。
//     這正是 operator<< 這類函式通常寫成命名空間內的自由函式的原因。
//   * 全域函式在多個 .cpp 定義同名同簽名的版本會違反 ODR，
//     而且連結器不一定報錯 —— 可能默默選其中一個，造成極難查的 bug。
//     命名空間與類別作用域都能避免這件事。
//   * 若只想讓函式在單一 .cpp 內可見，用未具名命名空間
//     （namespace { ... }），這比 C 風格的 static 函式更符合 C++ 慣例。
//   * operationCount_++ 是非原子的讀-改-寫；
//     若 Calculator::add 會被多執行緒呼叫，這個計數器就是資料競爭。
//
// 【注意事項 Pay Attention】
//   1. 不要為了「物件導向」把純函式硬包成只有靜態成員的類別。
//   2. 類別無法跨檔案擴充，命名空間可以。
//   3. 命名空間函式支援 ADL，類別靜態函式不支援。
//   4. 全域函式跨 TU 同名同簽名會違反 ODR，且未必有錯誤訊息。
//   5. 靜態函式維護的共享計數在多執行緒下需自行同步。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】靜態函式 vs 命名空間函式
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 什麼時候該用類別的靜態函式，什麼時候該用命名空間函式?
//     答：判準是「需不需要碰該類別的私有實作」。
//         需要存取 private 成員、或需要維護只有這組函式看得到的
//         私有狀態（本檔的 operationCount_）→ 類別靜態函式。
//         純粹的自由函式、與任何類別的內部都無關 → 命名空間，
//         它更輕量、可跨檔案擴充、還支援 ADL。
//     追問：那全域函式呢?
//         → 幾乎沒有理由用，除非要提供 C 相容介面。
//         它沒有任何隔離，跨 TU 同名同簽名還可能違反 ODR。
//
// 🔥 Q2. 只有靜態函式的類別，和命名空間相比有什麼缺點?
//     答：三個。第一，類別無法跨檔案擴充，命名空間可以分散在多個標頭檔。
//         第二，類別靜態函式不支援 ADL，呼叫時永遠得寫 C::f()。
//         第三，類別預設可被實例化、可被繼承，
//         要防止就得額外寫 = delete，命名空間根本不需要這道手續。
//     追問：那為什麼還是常看到這種寫法?
//         → 多半是從 Java / C# 帶來的習慣 —— 那些語言沒有自由函式。
//         C++ Core Guidelines 明確建議：不需要類別狀態的函式就寫成自由函式。
//
// ⚠️ 陷阱. 「把工具函式包進類別比放命名空間更封裝、更安全。」
//     答：不成立。封裝的意義是「隱藏實作細節」，
//         而一個只有 public static 函式的類別，什麼都沒藏 ——
//         它和命名空間暴露的東西完全一樣，只是多了一層語法。
//         真正提供封裝的是 private 成員；本檔的 Calculator 有
//         operationCount_ 才算真的用到了類別，
//         若把它拿掉，這個類別就退化成「寫法比較囉嗦的命名空間」。
//     為什麼會錯：把「用了 class 關鍵字」等同於「有封裝」。
//         封裝取決於有沒有隱藏狀態，不取決於用哪個關鍵字。
//         判斷方法很簡單：如果這個類別沒有任何 private 成員，
//         那它就不需要是一個類別。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【LeetCode 實戰範例】—— 從缺，理由如下
//   「函式該放哪裡」是程式組織與命名的設計問題，
//   LeetCode 只要求你在 Solution 類別裡實作指定簽名的函式，不涉及這個選擇。
//   本檔改以「單位換算工具」的實務範例，
//   對照「不需要狀態 → 命名空間」與「需要私有狀態 → 類別靜態函式」的實際分界。
//
// =============================================================================

#include <iostream>
#include <string>
using namespace std;

// ===== 方式 1：全域函數 =====
// 全域函數沒有歸屬感，容易與其他函數名字衝突，且無法訪問類別的 private 成員
// 全域函數適合一些簡單的工具函數，但對於需要封裝和狀態管理的功能來說，並不是最佳選擇
// 全域函數的缺點：名字可能衝突、沒有歸屬感、不能訪問 private
// 全域函數示例
int globalAdd(int a, int b) { return a + b; }
// 問題：名字可能衝突、沒有歸屬感、不能訪問 private

// ===== 方式 2：命名空間函數 =====
// 命名空間函數有了命名空間的隔離，可以避免名字衝突，但仍然沒有歸屬感，且不能訪問類別的 private 成員
// 命名空間函數適合一些需要組織但不需要封裝的工具函數，但對於需要封裝和狀態管理的功能來說，仍然不是最佳選擇
// 命名空間函數示例
namespace MathUtils {
    int add(int a, int b) { return a + b; }
}
// 較好：有命名空間隔離，但不能訪問類別的 private

// ===== 方式 3：類別的靜態函數 =====
// 類別的靜態函數有了類別的歸屬感，可以訪問類別的 private 成員，並且可以維護內部狀態，是最適合封裝功能的方式
// 類別的靜態函數示例
class Calculator {
private:
    inline static int operationCount_ = 0;  // 可以有私有狀態

public:
    static int add(int a, int b) {
        operationCount_++;    // 可以維護內部狀態
        return a + b;
    }

    static int getOperationCount() { return operationCount_; }
};
// 最佳：有歸屬、可以訪問 private、可以維護狀態

// -----------------------------------------------------------------------------
// 【日常實務範例】單位換算工具：兩種寫法的實際分界
//   同一個專案裡的兩組函式，示範什麼時候該用命名空間、什麼時候該用類別。
//
//   (A) ByteFormat：純換算，沒有任何狀態 → 命名空間就夠了。
//       硬包成類別只會多一層語法，什麼也沒隱藏。
//   (B) RateLimiter：需要記住「這個時窗內已經放行幾次」——
//       有私有狀態要隱藏，而且不希望外部直接改動 → 類別 + private static。
//
//   這組對照就是上面陷阱題的實際體現：
//   有沒有 private 狀態，才是「該不該用類別」的判準。
// -----------------------------------------------------------------------------

// (A) 無狀態的純換算 → 命名空間
namespace ByteFormat {
    // 把位元組數換算成人類可讀的字串（保留整數部分即可，教學用簡化版）
    string humanReadable(long bytes) {
        const char* units[] = {"B", "KB", "MB", "GB", "TB"};
        int unitIndex = 0;
        long value = bytes;
        while (value >= 1024 && unitIndex < 4) {
            value /= 1024;
            ++unitIndex;
        }
        return to_string(value) + units[unitIndex];
    }
}

// (B) 有私有狀態 → 類別 + private static
class RateLimiter {
private:
    inline static int allowed_  = 0;    // 私有：外部不能直接改
    inline static int rejected_ = 0;
    inline static int limit_    = 3;    // 每個時窗最多放行幾次

public:
    RateLimiter() = delete;

    // 回傳這次請求是否放行；同時維護內部統計
    static bool tryAcquire() {
        if (allowed_ < limit_) {
            ++allowed_;
            return true;
        }
        ++rejected_;
        return false;
    }

    static void newWindow() { allowed_ = 0; }        // 進入新的時窗
    static int  allowed()   { return allowed_; }
    static int  rejected()  { return rejected_; }
};

int main() {
    cout << "=== 三種方式比較 ===" << endl;

    cout << "  全域函數：" << globalAdd(1, 2) << endl;
    cout << "  命名空間：" << MathUtils::add(3, 4) << endl;
    cout << "  靜態函數：" << Calculator::add(5, 6) << endl;

    Calculator::add(7, 8);
    Calculator::add(9, 10);
    cout << "  Calculator 運算次數：" << Calculator::getOperationCount() << endl;
    cout << "  ↑ 這個計數器是 private static —— 正因為有「要隱藏的狀態」，" << endl;
    cout << "    Calculator 才真的需要是一個類別，而不只是命名空間。" << endl;

    cout << "\n=== 日常實務：什麼時候用命名空間、什麼時候用類別 ===" << endl;

    cout << "\n--- (A) 無狀態純換算 → 命名空間就夠 ---" << endl;
    const long sizes[] = {512, 2048, 5242880, 3221225472};
    for (long s : sizes) {
        cout << "  " << s << " bytes → " << ByteFormat::humanReadable(s) << endl;
    }
    cout << "  ↑ 沒有任何要隱藏的狀態，包成類別只是多一層語法。" << endl;

    cout << "\n--- (B) 有私有狀態 → 類別 + private static ---" << endl;
    cout << "  時窗上限 3 次，連送 5 個請求：" << endl;
    for (int i = 1; i <= 5; ++i) {
        const bool ok = RateLimiter::tryAcquire();
        cout << "    請求 " << i << " → " << (ok ? "放行" : "拒絕（超過上限）") << endl;
    }
    cout << "  統計：放行 " << RateLimiter::allowed()
         << " 次，拒絕 " << RateLimiter::rejected() << " 次" << endl;

    RateLimiter::newWindow();
    cout << "  進入新時窗後再送 2 個請求：" << endl;
    for (int i = 1; i <= 2; ++i) {
        const bool ok = RateLimiter::tryAcquire();
        cout << "    請求 " << i << " → " << (ok ? "放行" : "拒絕（超過上限）") << endl;
    }
    cout << "  累計拒絕數仍為 " << RateLimiter::rejected()
         << "（newWindow 只重置放行數，不清除歷史統計）" << endl;
    cout << "  ↑ allowed_ / limit_ 都是 private，外部無法繞過 tryAcquire()" << endl;
    cout << "    直接把配額改大 —— 這才是類別提供的真正封裝。" << endl;

    return 0;
}

