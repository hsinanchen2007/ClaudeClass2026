// =============================================================================
//  第 33 課 -2  —  統一賦值運算子（copy-and-swap）：一個函式同時處理拷貝與移動
// =============================================================================
//
// 【主題資訊 Information】
//   簽名       ： ClassName& operator=(ClassName other);   // ★ 按「值」傳入
//   核心動作   ： swap(other); return *this;
//   別名       ： copy-and-swap idiom（統一賦值 / 傳值賦值）
//   標準版本   ： C++11 起因為有移動語義才真正划算（C++98 時代只有拷貝路徑）
//   標頭檔     ： <utility>（std::swap、std::move）
//   例外保證   ： 強例外保證（strong exception guarantee）—— 見下方第 4 點
//   複雜度     ： swap 本身 O(1)（只交換指標與長度）；
//                 總成本取決於參數是由拷貝還是移動建構而來
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼「按值傳入」是整個技巧的關鍵】
//   一般寫法要分開寫兩個函式：
//       ClassName& operator=(const ClassName& other);   // 拷貝賦值
//       ClassName& operator=(ClassName&& other);        // 移動賦值
//   統一賦值只寫一個，因為參數是「按值」傳的，
//   於是「這一份參數要怎麼生出來」的問題就交給了重載解析：
//       傳左值   → 參數由「拷貝建構函數」建構
//       傳右值   → 參數由「移動建構函數」建構
//   換句話說，拷貝／移動的分岔在「呼叫端建構參數時」就決定了，
//   函式本體完全不必知道自己走的是哪條路，一律 swap 即可。
//   本檔輸出的前兩段就是證據：同樣呼叫 operator=，
//   一次先印 [拷貝建構]、一次先印 [移動建構]，之後都是同一行 [賦值] swap。
//
// 【2. 為什麼 swap 之後就結束了：舊資源由參數帶走】
//   swap 把「自己的舊資源」和「參數的新資源」對調。
//   函式回傳後，參數 other 離開作用域被解構，
//   它此時持有的正是自己原本的舊資源 —— 於是舊資源被正確釋放。
//   不需要寫 delete，也不需要自我賦值防護（見下一點）。
//   輸出中每段末尾那行 [解構] 就是參數帶著舊資源離場。
//
// 【3. 為什麼不需要 if (this != &other) 自我賦值防護】
//   分開寫的移動賦值必須防自我賦值，否則會先 delete 自己的資源、
//   再從已釋放的自己偷指標（見第 33 課 summary 的示範）。
//   統一賦值天生免疫：參數是「一份獨立的副本」，
//   即使寫 a = a，參數也是從 a 拷貝出來的新物件，
//   和 this 不是同一塊記憶體，swap 完全安全。
//   代價是自我賦值時做了一次白工的拷貝 —— 用正確性換掉一個容易漏寫的防護。
//
// 【4. 最大的好處：強例外保證（strong exception guarantee）】
//   若賦值過程中拋出例外，目標物件會維持原狀，不會處於半新半舊的壞狀態。
//   原因很直接：唯一可能拋例外的動作是「建構參數」（要配置記憶體），
//   而那發生在「進入函式本體之前」。
//   一旦進到函式裡，只剩 swap，而 swap 是 noexcept 的。
//   所以要嘛參數還沒建好就拋了（目標完全沒被碰過），
//   要嘛就一路成功到底 —— 沒有中間狀態。
//   本檔的【日常實務範例】直接把這件事跑給你看。
//
// 【5. 代價：拷貝路徑一定會重新配置記憶體】
//   統一賦值不是沒有缺點。分開寫的拷貝賦值可以「重用既有緩衝區」：
//   若自己的緩衝區夠大，直接 strcpy 覆寫即可，不必 new。
//   統一賦值一定會先建構一份完整的參數副本（含一次配置），再 swap。
//   對「反覆賦值長度相近的字串」這種熱點，分開寫可能明顯較快。
//   取捨：統一賦值換來的是更短的程式碼、免費的強例外保證、
//   以及不會漏寫自我賦值防護；效能敏感處再考慮拆開寫。
//
// 【概念補充 Concept Deep Dive】
//
// (A) ★ 「暫時物件路徑」沒有移動建構，是 C++17 保證的省略
//   本檔原始註解把第三段寫成「暫時物件 → other 由移動建構 → swap」，
//   這在 C++17 是不正確的：輸出裡只有 [建構] "Phoenix"，
//   看不到任何 [移動建構]。
//   原因是 UnifiedString("Phoenix") 是一個純右值（prvalue），
//   而 C++17 保證「純右值初始化」的省略：
//   那個暫時物件不會先造出來再搬進參數，
//   而是「直接就地建構在參數 other 的位置上」—— 從頭到尾只有一個物件。
//   本機四種組合實測，證據非常明確：
//       C++17                              → 無 [移動建構]
//       C++17 + -fno-elide-constructors    → 無 [移動建構]（旗標關不掉，因為是保證的）
//       C++14                              → 無 [移動建構]（編譯器自願省略）
//       C++14 + -fno-elide-constructors    → 出現 [移動建構]（可選的省略被關掉）
//   最後一行才是原始註解描述的情境，那是 C++14 以前的模型。
//   （對照第 28 課 -2：C++17 只保證 prvalue 初始化的省略，NRVO 仍是可選的。）
//
// (B) 為什麼 swap 必須是 noexcept
//   強例外保證的推論依賴「進入函式本體後不會再拋例外」。
//   swap 只交換指標與整數，天生不會拋，標成 noexcept 是誠實的宣告。
//   若 swap 可能拋例外，整個 idiom 的保證就沒了。
//
// (C) 成員宣告順序與初始化順序（-Wreorder）
//   成員的初始化順序由「宣告順序」決定，與初始化列表的書寫順序無關。
//   本類別的 m_data 初始值用到了 m_len（new char[m_len + 1]），
//   所以 m_len 必須宣告在 m_data 之前 —— 檔案中已經是正確的順序。
//   若把兩者的宣告對調，m_data 會先用到還沒初始化的 m_len，
//   配置出錯誤大小的緩衝區，接著 strcpy 就會寫出界（heap buffer overflow）。
//   編譯時加 -Wreorder 可以讓編譯器幫忙抓這類問題。
//
// (D) 統一賦值不能標 noexcept
//   函式本體雖然只有 swap，但「建構參數」這件事是呼叫的一部分，
//   拷貝路徑會配置記憶體、可能拋 bad_alloc。
//   所以不要對統一賦值標 noexcept；
//   而分開寫時的「移動賦值」則應該標（它只搬指標）。
//
// 【注意事項 Pay Attention】
//   1. 參數必須「按值」傳入。寫成 const& 就退化成只有拷貝路徑，
//      整個技巧失效。
//   2. 統一賦值不需要、也不該加自我賦值防護 —— 參數是獨立副本，天生安全。
//   3. 統一賦值不能標 noexcept（建構參數可能配置記憶體）。
//   4. 拷貝路徑一定會重新配置記憶體，無法重用既有緩衝區；
//      這是換取簡潔與強例外保證的代價。
//   5. C++17 起，用暫時物件賦值時看不到移動建構是正常的（保證省略），
//      不是移動語義沒生效 —— 見【概念補充 A】。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】統一賦值運算子（copy-and-swap）
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. operator=(ClassName other) 為什麼參數要「按值」傳？一個函式怎麼同時
//        處理拷貝與移動？
//     答：因為按值傳入時，「參數要怎麼建構」交由重載解析決定：
//         傳左值就用拷貝建構函數生出參數，傳右值就用移動建構函數。
//         分岔在呼叫端建構參數時就完成了，函式本體只要 swap 即可，
//         完全不必知道自己走哪條路。若寫成 const&，就只剩拷貝路徑，
//         整個技巧失效。
//     追問：那舊的資源什麼時候被釋放？
//         → swap 之後舊資源落在參數手上，函式回傳時參數解構，順帶釋放。
//           所以本體裡完全不需要寫 delete。
//
// 🔥 Q2. 統一賦值為什麼不需要自我賦值防護？它的例外保證是怎麼來的？
//     答：參數是一份獨立的副本，即使寫 a = a，
//         參數也是從 a 拷貝出來的新物件，與 this 不同記憶體，swap 安全。
//         強例外保證則來自「唯一可能拋例外的動作（建構參數）
//         發生在進入函式本體之前」——
//         進到本體後只剩 noexcept 的 swap，不可能失敗。
//         所以要嘛還沒開始就拋、目標完全沒被碰過，要嘛一路成功。
//     追問：那它有什麼代價？
//         → 拷貝路徑一定會重新配置記憶體，無法像分開寫那樣重用既有緩衝區。
//           對反覆賦值相近長度資料的熱點，分開寫可能較快。
//
// ⚠️ 陷阱. 「b = UnifiedString("Phoenix") 這行，暫時物件會先建構、
//           再移動建構到參數裡，所以應該看到兩次建構。」
//     答：在 C++17 不會。輸出只有一次 [建構]，沒有 [移動建構]。
//         UnifiedString("Phoenix") 是純右值，C++17 保證省略這種初始化，
//         它直接就地建構在參數的位置上，從頭到尾只有一個物件。
//         本機實測：C++17 加不加 -fno-elide-constructors 都看不到移動建構；
//         必須退到 C++14 並加上 -fno-elide-constructors 才會出現。
//     為什麼會錯：沿用 C++14 以前的心智模型 ——
//         「暫時物件是一個真實存在的物件，要搬進參數」。
//         C++17 重新定義了 prvalue：它不再代表一個物件，
//         而是「一份尚未具體化的初始化配方」，直接用來初始化目標。
//         （本檔原始註解就是照舊模型寫的，已在【概念補充 A】更正。）
// ═══════════════════════════════════════════════════════════════════════════
//
// 【LeetCode 實戰範例】—— 本檔從缺，理由如下
//   copy-and-swap 是資源管理型類別的實作技巧，
//   價值在例外安全與程式碼簡潔，屬於工程與 API 設計層面。
//   LeetCode 判的是演算法的輸入輸出與時間限制，
//   評測系統只會建立一個實例並呼叫指定方法，
//   從不對你的物件做賦值，更不會在賦值中途注入例外來檢驗保證強度。
//   硬掛設計類題號只會誤導，故從缺；
//   改以「設定熱更新時拋出例外，目標仍保持原狀」這個
//   真正能驗證強例外保證的情境呈現。
//
