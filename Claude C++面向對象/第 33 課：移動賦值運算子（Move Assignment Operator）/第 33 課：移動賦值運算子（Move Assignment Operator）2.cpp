#include <iostream>
#include <cstring>
#include <utility>
#include <string>
#include <stdexcept>

class UnifiedString {
private:
    // ⚠️ 成員的初始化順序由「宣告順序」決定，與初始化列表的書寫順序無關。
    //    m_data 的初始值用到了 m_len，所以 m_len 必須宣告在前。
    std::size_t m_len;
    char* m_data;

public:
    // 建構
    UnifiedString(const char* str = "")
        : m_len(std::strlen(str))
        , m_data(new char[m_len + 1])
    {
        std::strcpy(m_data, str);
        std::cout << "  [建構] \"" << m_data << "\"\n";
    }

    // 解構
    ~UnifiedString() {
        std::cout << "  [解構] \"" << (m_data ? m_data : "null") << "\"\n";
        delete[] m_data;
    }

    // 拷貝建構
    UnifiedString(const UnifiedString& other)
        : m_len(other.m_len)
        , m_data(new char[m_len + 1])
    {
        std::strcpy(m_data, other.m_data);
        std::cout << "  [拷貝建構] \"" << m_data << "\"\n";
    }

    // 移動建構
    UnifiedString(UnifiedString&& other) noexcept
        : m_len(other.m_len), m_data(other.m_data)
    {
        other.m_data = nullptr;
        other.m_len = 0;
        std::cout << "  [移動建構] \"" << m_data << "\"\n";
    }

    // ★ 統一的賦值運算子（同時處理拷貝和移動）
    //   按值傳入 → 參數由拷貝或移動建構，分岔在呼叫端就決定了。
    //   本體只有 swap，因此天生具備強例外保證，也不需要自我賦值防護。
    //   注意：不能標 noexcept —— 建構參數時可能配置記憶體而拋 bad_alloc。
    UnifiedString& operator=(UnifiedString other) {  // 按值傳入
        std::cout << "  [賦值] swap\n";
        swap(other);
        return *this;
    }
    // 舊資源在函式回傳時隨參數 other 解構而釋放，本體不必寫 delete。

    // swap
    void swap(UnifiedString& other) noexcept {
        std::swap(m_data, other.m_data);
        std::swap(m_len, other.m_len);
    }

    const char* c_str() const { return m_data ? m_data : "(null)"; }
};

// -----------------------------------------------------------------------------
// 【日常實務範例】設定熱更新：更新失敗時，舊設定必須原封不動
//   情境：服務收到 SIGHUP 後重新解析設定檔並覆蓋目前設定。
//         若新的設定檔內容不合法（欄位缺漏、格式錯誤），
//         解析過程會拋出例外。此時最重要的要求是：
//         服務必須「繼續使用原本那份可用的設定」，
//         絕不能變成「舊的已經被清掉、新的又沒裝好」的半殘狀態。
//   為什麼用到本主題：統一賦值天生提供強例外保證。
//         唯一會拋例外的是「建構參數」，而那發生在進入 operator= 之前；
//         一旦進到本體就只剩 noexcept 的 swap。
//         所以例外拋出時，目標物件根本還沒被碰過。
//   下面刻意讓解析失敗，再確認目標設定完全沒變。
// -----------------------------------------------------------------------------
namespace practice {

class Setting {
private:
    std::string m_value;

public:
    explicit Setting(std::string v) {
        if (v.empty()) {
            throw std::invalid_argument("設定值不可為空");
        }
        m_value = std::move(v);
    }

    // 統一賦值：與上面的 UnifiedString 是同一個 idiom
    Setting& operator=(Setting other) {
        m_value.swap(other.m_value);
        return *this;
    }

    Setting(const Setting&) = default;
    Setting(Setting&&) noexcept = default;
    ~Setting() = default;

    const std::string& value() const { return m_value; }
};

// 模擬「解析設定檔」：內容不合法時拋例外
Setting parseConfig(const std::string& raw) {
    return Setting(raw);          // raw 為空時建構函數會拋
}

void demo() {
    Setting current("timeout=30;retries=3");
    std::cout << "  啟動設定        ：" << current.value() << "\n";

    // 第一次熱更新：成功
    current = parseConfig("timeout=15;retries=5");
    std::cout << "  更新成功後      ：" << current.value() << "\n";

    // 第二次熱更新：設定檔壞掉，解析拋例外
    try {
        current = parseConfig("");        // 建構參數時就拋了
        std::cout << "  （不會執行到這裡）\n";
    } catch (const std::invalid_argument& e) {
        std::cout << "  更新失敗，例外：" << e.what() << "\n";
    }

    // ★ 關鍵驗證：目標物件完全沒被破壞，仍是上一份可用的設定
    std::cout << "  失敗後目前設定  ：" << current.value()
              << "   ← 原封不動，這就是強例外保證\n";
}

}  // namespace practice

int main() {
    UnifiedString a("Dragon");
    UnifiedString b("Knight");

    std::cout << "\n--- 拷貝賦值路徑 ---\n";
    b = a;                          // a 是左值 → 參數由拷貝建構 → swap

    std::cout << "\n--- 移動賦值路徑 ---\n";
    b = std::move(a);               // 右值 → 參數由移動建構 → swap

    std::cout << "\n--- 暫時物件路徑 ---\n";
    // ★ C++17：UnifiedString("Phoenix") 是純右值，保證省略 ——
    //   它直接就地建構在參數的位置上，所以「看不到」[移動建構]。
    //   （原始註解寫「由移動建構」是 C++14 以前的模型，已更正，
    //     詳見檔頭【概念補充 A】的四組實測數據。）
    b = UnifiedString("Phoenix");

    std::cout << "\n--- 日常實務：設定熱更新的強例外保證 ---\n";
    practice::demo();

    std::cout << "\n--- 結束 ---\n";
    return 0;
}

