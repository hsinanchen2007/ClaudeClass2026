// =============================================================================
//  第 20 課 summary  —  封裝（Encapsulation）完整規格
// =============================================================================
//
// 【主題資訊 Information】
//   語法       ：class / struct 內以 public、protected、private 劃分存取區段
//   預設值     ：class 成員預設 private；struct 成員預設 public
//                （繼承的預設存取權限亦同：class 為 private、struct 為 public）
//   標準版本   ：存取控制 C++98；本檔提到的 std::clamp 為 C++17、
//                std::optional 為 C++17、inline static 資料成員為 C++17
//   標頭檔     ：無（語言層級特性）；本檔用到 <algorithm> 的 min/max
//   執行期成本 ：零。存取控制純粹在編譯期檢查，不產生任何執行期指令，
//                也不影響 sizeof 與物件佈局
//   核心概念   ：class invariant（不變式）—— 物件在任何外部可觀察時刻
//                都必須成立的條件
//
// 【詳細解釋 Explanation】
//
// 【1. 封裝要解決的根本問題：責任的歸屬】
//   封裝常被說成「把資料藏起來」，但那只是手段。真正要解決的問題是：
//   「誰負責保證這個物件永遠處於合法狀態？」
//   資料全 public 時，答案是「每一個碰它的人」——也就是沒有人。
//   專案裡任何一處寫 hero.hp = ... 的地方，都必須自己記得檢查上下限；
//   只要有一處忘記，整個系統的假設就破了，而且不會有任何警告。
//   把資料設為 private、只留有語意的操作當入口之後，答案變成
//   「這個 class 自己」——規則只寫一次，而且不可能被繞過。
//   這帶來一個極重要的性質：局部推理（local reasoning）。
//   你只要讀完這一個 class，就能確信不變式成立，不必讀完整個專案。
//
// 【2. 不變式為什麼一定要「跨成員」才有意義】
//   單一成員的合法性通常型別本身就管得住（unsigned 不會是負的）。
//   真正需要封裝的，是那些橫跨多個成員的條件：
//       0 <= hp_ <= maxHp_                    // 兩個成員的關係
//       min_.back() == data_ 的最小值          // 兩個容器的同步
//       balance_ == 所有交易紀錄的總和          // 匯總欄位與明細的一致性
//       -1 <= top_ <= MAX_SIZE - 1            // 索引與容量的關係
//   這類條件的共同特徵是：「單獨看任一成員都看不出對錯」。
//   hp_ = 99999 本身是個完全合法的 int。
//   而 public 成員允許每個欄位被「單獨」修改，物件根本無從得知有人動了它，
//   也就沒有任何時機可以檢查。這就是為什麼 public 資料與不變式互斥。
//
// 【3. 封裝的三層防護（對應本檔第三部分）】
//   第一層 存取控制：private/protected/public 決定「誰碰得到」。
//                    這是機制，本身不含任何規則。
//   第二層 資料驗證：入口處檢查參數是否合法（傷害必須為正、金額必須為正）。
//   第三層 不變式維護：確保「操作完成後」物件仍然合法
//                    （hp 夾在 [0, maxHp]、min_ 與 data_ 同步）。
//   三層缺一不可。只做第一層就是「有 private 但 setter 不檢查」，
//   等於沒有封裝；只做第二層而漏掉第三層，則會出現
//   「每個參數都合法、但組合起來狀態不一致」的 bug。
//
// 【4. 為什麼是動詞（takeDamage）而不是設值（setHp）】
//   這是本課最實用的一條判準。比較兩種介面：
//       setHp(int v)         // 「把血量設成 v」——不知道呼叫端想幹嘛
//       takeDamage(int d)    // 「受到 d 點傷害」——知道這是一次攻擊
//   setHp 只是一個賦值管道，它無從判斷 v 合不合理，因為它不知道這次修改
//   代表什麼事件。takeDamage 知道，所以它能集中套用規則，
//   而且順帶處理連帶效果（歸零時觸發死亡事件）。
//   實務判準：「這個 setter 對應到領域裡的哪一個事件？」
//   如果答不出來，它多半就不該存在。
//
// 【5. 介面穩定，實作可換（對應本檔第九部分）】
//   封裝的第二個價值是「把可能改變的東西藏起來」。
//   PlayerList 從陣列改成 vector、HashSet 從線性搜尋改成雜湊桶，
//   只要 public 介面不變，所有使用端一行都不用改。
//   反過來說，一旦把內部容器透過非 const 參考暴露出去，
//   實作就被凍結了——這正是下面陷阱題 2 要講的事。
//
// 【概念補充 Concept Deep Dive】
//   * 存取控制是「編譯期」的，不是執行期的安全機制。private 成員在記憶體裡
//     就躺在那裡，用指標運算或 reinterpret_cast 一樣碰得到（那是未定義行為，
//     但編譯器攔不住）。封裝防的是「意外」與「維護成本」，不是惡意攻擊。
//   * 存取控制不影響 sizeof，也不產生額外指令。把 public 改成 private
//     不會讓物件變大變小。（注意：標準只保證「同一存取區段內」的成員
//     位址遞增；不同存取區段之間的相對順序是實作定義的。
//     實務上主流編譯器仍照宣告順序排列。）
//   * 成員初始化順序由「宣告順序」決定，與初始化列表的書寫順序無關。
//     寫成 : maxHp_(m), hp_(maxHp_) 而 hp_ 宣告在前，就會讀到尚未初始化的
//     maxHp_，是未定義行為。請開啟 -Wreorder。
//   * C 語言也能做封裝，做法是 opaque pointer：標頭檔只宣告
//     struct Foo;（不完整型別），使用者只拿得到指標。
//     代價是必須動態配置、無法直接放在 stack 上。C++ 的 private
//     是把同樣概念做進語言，且沒有這些代價。
//   * protected 常被誤用。它讓所有衍生類別都能碰到資料，
//     等於把「可能出錯的範圍」從一個 class 擴大到整個繼承樹。
//     多數情況下 private + protected 的存取函式是更好的選擇。
//
// 【注意事項 Pay Attention】
//   1. 加了 getter/setter 但沒有檢查，等於沒有封裝，只是多繞一層。
//   2. 回傳 private 成員的「非 const 參考或指標」會讓封裝完全失效。
//   3. 存取控制是編譯期概念，不能當成執行期的安全防護。
//   4. struct 與 class 在 C++ 只差預設存取權限，其餘能力完全相同。
//   5. 成員初始化順序依宣告順序，不依初始化列表順序；請開 -Wreorder。
//   6. 「悄悄修正」（遊戲數值）與「大聲失敗」（金額）兩種防禦策略
//      各有適用場合，要依領域選擇，不要無條件套用其中一種。
//   7. 不要為了追求「完整」而替每個成員都配一組 getter/setter；
//      沒有語意的存取函式只是雜訊。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】封裝（Encapsulation）
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 什麼是 class invariant？為什麼 public 資料成員無法維護它？
//     答：不變式是「物件在任何外部可觀察時刻都必須成立的條件」，
//         例如 0 <= hp <= maxHp、或 balance 等於所有交易紀錄的總和。
//         關鍵在於它通常「跨越多個成員」——單看 hp = 99999 是個合法的 int，
//         只有跟 maxHp 一起看才知道錯。public 成員允許每個欄位被單獨修改，
//         物件無從得知有人動了它，也就沒有任何時機可以檢查跨欄位條件。
//     追問：那什麼時候不需要不變式？→ 純資料傳輸物件（DTO）、
//         數學上的點／向量這類「任意組合都合法」的型別，
//         用 struct + public 反而更清楚，硬加存取函式是雜訊。
//
// 🔥 Q2. C++ 的 struct 和 class 有什麼差別？
//     答：只有兩個預設值不同——struct 的成員與繼承預設是 public，
//         class 預設是 private。除此之外能力完全相同：struct 一樣可以有
//         建構子、解構子、虛擬函式、繼承、樣板。所以選用哪一個是在宣告意圖：
//         純資料聚合用 struct，有不變式要維護的用 class。
//     追問：那 C 的 struct 跟 C++ 的 struct 一樣嗎？→ 不一樣。
//         C 的 struct 只能有資料成員，沒有成員函式、存取控制或繼承。
//
// 🔥 Q3. 封裝除了「保護資料」，還有什麼實際好處？
//     答：主要有三個。(1) 局部推理：不變式只可能在這個 class 內被破壞，
//         除錯的搜尋範圍從整個專案縮小到一個檔案。
//         (2) 介面穩定、實作可換：內部從陣列換成 vector、從線性搜尋換成
//         雜湊表，只要 public 介面不變，使用端一行都不用改。
//         (3) 降低耦合：使用端只依賴介面，不依賴內部細節，
//         模組之間才能各自獨立演進。
//     追問：封裝有沒有成本？→ 存取控制本身零成本（純編譯期）。
//         真正的成本是驗證邏輯的執行成本，那是刻意付出的代價；
//         以及設計介面所需的思考時間，那通常是划算的投資。
//
// ⚠️ 陷阱 1. 把成員改成 private 再加上 getter/setter，這樣就算封裝了嗎？
//     答：不算。如果 setHp(int v) { hp_ = v; } 沒有任何檢查，
//         它的保護能力跟直接 public「完全等價」——別人照樣能設成 -500，
//         只是多打了幾個字。這種寫法只把「欄位」換成「兩個函式」，
//         沒有換來任何保證。真正的封裝是用動詞取代設值：
//         提供 takeDamage(30) 而不是 setHp(getHp() - 30)。
//     為什麼會錯：很多教材把封裝教成「成員一律 private，每個都配 get/set」，
//         變成機械式的樣板動作。但封裝的目的是「維護不變式」，
//         不是「讓成員變數不可見」。判準很簡單：
//         如果你的 setter 全都沒有檢查，那你只是寫了一堆沒有用的程式碼。
//
// ⚠️ 陷阱 2. 這個 getter 為什麼讓封裝完全失效？
//         class Inventory {
//             std::vector<Item> items_;
//         public:
//             std::vector<Item>& getItems() { return items_; }   // 非 const
//         };
//     答：它回傳內部容器的「非 const 參考」，等於把完整寫入權交給呼叫端：
//             inv.getItems().clear();                  // 清空整個背包
//             inv.getItems().push_back(cheatItem);     // 繞過所有規則
//         這比直接把 items_ 宣告成 public 更糟，因為它「看起來」有封裝，
//         讓人誤以為受到保護。若 Inventory 還有「總重量」之類的快取欄位，
//         這樣改完之後快取就與實際內容不一致了。
//         而且它同時凍結了實作：日後想把 vector 換成別的容器都不可能。
//     為什麼會錯：多數人把 getter 理解成「唯讀存取」，
//         但唯讀是由 const 保證的，不是由「名字叫 get」保證的。
//         正確做法是回傳 const 參考、或只回傳需要的資訊（size()、contains()），
//         真的要修改就提供 addItem() / removeItem() 這類有規則的操作。
// ═══════════════════════════════════════════════════════════════════════════

