/*
 * ============================================================================
 *  第一部分：封裝的基本概念
 * ============================================================================
 *
 *  封裝（Encapsulation）是 OOP（物件導向程式設計）四大核心支柱之一：
 *
 *    ┌─────────────┐
 *    │   封裝       │ ← 本課主題（第 20～26 課）
 *    │ Encapsulation│
 *    ├─────────────┤
 *    │   繼承       │ ← 第八階段
 *    │ Inheritance  │
 *    ├─────────────┤
 *    │   多型       │ ← 第九階段
 *    │ Polymorphism │
 *    ├─────────────┤
 *    │   抽象       │ ← 第十階段
 *    │ Abstraction  │
 *    └─────────────┘
 *
 *  封裝的核心思想：
 *    將數據和操作數據的函數綁定在一起，並控制外部對數據的訪問。
 *
 *  現實類比 —— 手機：
 *    外部介面（public）：觸控螢幕、音量按鈕、充電接口
 *    內部實現（private）：CPU 電路、記憶體晶片、天線模組、電池管理系統
 *    你不需要知道天線怎麼運作，只需要透過「撥號」介面打電話。
 *
 * ============================================================================
 */

/*
 * ============================================================================
 *  第二部分：沒有封裝的災難（反面教材）
 * ============================================================================
 *
 *  當使用 struct（預設全部 public）且不做任何保護時，
 *  外部程式可以隨意修改所有成員，導致數據被篡改為不合理的值。
 *
 *  以下 CharacterBad 展示了六種典型災難：
 *    災難 1：生命值可以超過最大值（hp = 99999，但 maxHp = 100）
 *    災難 2：生命值可以是負數（hp = -500）
 *    災難 3：最大生命值可以被設成 0（maxHp = 0）
 *    災難 4：攻擊力可以隨便改（attack = 999999，作弊）
 *    災難 5：等級與經驗不一致（level = 99 但 exp = 0）
 *    災難 6：金幣可以無限刷（gold = 2147483647）
 *
 *  結論：任何人都可以把數據改成不合理的值，毫無防護！
 * ============================================================================
 */

// 沒有封裝的角色結構體 —— 全部 public，外部可以任意修改
struct CharacterBad {
    string name;
    int hp;         // 生命值
    int maxHp;      // 最大生命值
    int attack;     // 攻擊力
    int level;      // 等級
    int exp;        // 經驗值
    int gold;       // 金幣
};

// 展示沒有封裝時，數據被任意篡改的災難
void demo_noEncapsulation() {
    cout << "=== 第二部分：沒有封裝的災難 ===" << endl;

    CharacterBad hero;
    hero.name = "勇者";
    hero.hp = 100;
    hero.maxHp = 100;
    hero.attack = 25;
    hero.level = 1;
    hero.exp = 0;
    hero.gold = 50;

    cout << hero.name << " HP:" << hero.hp << "/" << hero.maxHp << endl;

    // 災難 1：生命值可以超過最大值
    // 外部直接修改 hp，沒有任何機制阻止 hp 超過 maxHp
    hero.hp = 99999;
    cout << "災難1 超量回血：HP = " << hero.hp << "/" << hero.maxHp << endl;

    // 災難 2：生命值可以是負數
    // 生命值不應該為負數，但 struct 無法阻止這種操作
    hero.hp = -500;
    cout << "災難2 負數 HP：HP = " << hero.hp << "/" << hero.maxHp << endl;

    // 災難 3：最大生命值可以被設成 0
    // maxHp = 0 沒有任何意義，但外部可以自由設定
    hero.maxHp = 0;
    cout << "災難3 最大HP歸零：HP = " << hero.hp << "/" << hero.maxHp << endl;

    // 災難 4：攻擊力可以隨便改
    // 玩家可以作弊，直接把攻擊力改成天文數字
    hero.attack = 999999;
    cout << "災難4 作弊攻擊力：" << hero.attack << endl;

    // 災難 5：等級與經驗不一致
    // 99 級但經驗值是 0，邏輯上不合理但無法阻止
    hero.level = 99;
    hero.exp = 0;
    cout << "災難5 等級 " << hero.level << "，經驗 " << hero.exp << endl;

    // 災難 6：金幣可以無限刷
    // 金幣被設為 int 最大值，破壞遊戲經濟系統
    hero.gold = 2147483647;
    cout << "災難6 無限金幣：" << hero.gold << endl;

    cout << "\n所有數據都被任意篡改，毫無防護！\n" << endl;
}

/*
 * ============================================================================
 *  第三部分：封裝的三層防護
 * ============================================================================
 *
 *  第一層：訪問控制（Access Control）
 *    → 使用 private / protected / public 關鍵字限制誰能碰數據
 *    → 編譯器強制執行，嘗試訪問 private 成員會直接編譯錯誤
 *
 *  第二層：數據驗證（Data Validation）
 *    → 在 setter / 操作函數中檢查數據是否合法
 *    → 例如：傷害值必須 > 0、治療量必須 > 0 且角色必須活著
 *
 *  第三層：不變量維護（Invariant Maintenance）
 *    → 確保對象始終處於合法狀態
 *    → 例如：hp 永遠在 [0, maxHp] 範圍內
 *    → 每個 public 函數執行完畢後，不變量都必須成立
 *
 * ============================================================================
 */

/*
 * ============================================================================
 *  第四部分：完整封裝範例 —— 遊戲角色類別（Character）
 * ============================================================================
 *
 *  這個類別展示了封裝的完整應用：
 *    - private 成員變數：外部無法直接訪問
 *    - private 輔助函數：內部邏輯隱藏（如 expToNextLevel、checkLevelUp）
 *    - 建構函數：確保初始狀態合法（防止無效值）
 *    - public 介面：提供受控的操作方式
 *    - const 成員函數：只讀查詢，保證不修改對象狀態
 *
 *  Character 類別的不變量（Invariant）：
 *    1. hp_ >= 0               （生命值不能是負數）
 *    2. hp_ <= maxHp_           （生命值不超過上限）
 *    3. maxHp_ > 0              （最大生命值必須為正）
 *    4. attack_ > 0             （攻擊力必須為正）
 *    5. level_ >= 1             （等級至少為 1）
 *    6. exp_ >= 0               （經驗不能是負數）
 *    7. exp_ < expToNextLevel() （經驗不超過升級門檻）
 *    8. 0 <= gold_ <= 999999    （金幣在合法範圍）
 * ============================================================================
 */

class Character {
private:
    // ====== 私有數據：外部無法直接碰 ======
    // 這些成員變數被封裝在類內，外部程式無法直接訪問或修改它們，
    // 必須通過公開的函數來操作，確保數據的合法性和一致性。
    string name_;
    int hp_;        // 當前生命值
    int maxHp_;     // 最大生命值
    int attack_;    // 攻擊力
    int level_;     // 等級
    int exp_;       // 當前經驗值
    int gold_;      // 金幣

    // ====== 私有輔助函數 ======
    // 這些函數是內部實現細節，外部使用者不需要知道它們的存在。
    // 這就是封裝的「介面層」—— 只暴露必要的操作，隱藏實現細節。

    // 計算升級所需的經驗值：每級需要 level * 100 經驗
    int expToNextLevel() const {
        return level_ * 100;
    }

    // 檢查是否可以升級，如果經驗足夠則自動升級
    // 使用 while 迴圈處理一次獲得大量經驗時可能連升多級的情況
    void checkLevelUp() {
        while (exp_ >= expToNextLevel()) {
            exp_ -= expToNextLevel();   // 扣除升級所需經驗
            level_++;                   // 等級 +1
            maxHp_ += 20;              // 最大生命值 +20
            hp_ = maxHp_;              // 升級回滿血
            attack_ += 5;             // 攻擊力 +5
            cout << "  ★ " << name_ << " 升級！Lv." << level_
                 << " (HP+" << 20 << " ATK+" << 5 << ")" << endl;
        }
    }

public:
    // ====== 建構函數：確保初始狀態合法 ======
    // 建構函數負責創建對象時的初始化，確保所有成員變數都被賦予合理的初始值。
    // 使用初始化列表（initializer list）進行初始化，並加入防禦性檢查：
    //   - maxHp 如果 <= 0，則預設為 100
    //   - attack 如果 <= 0，則預設為 10
    // 這保證了對象從創建那一刻起就處於合法狀態。
    Character(const string& name, int maxHp, int attack)
        : name_(name)
        , hp_(maxHp > 0 ? maxHp : 100)         // 防止無效值
        , maxHp_(maxHp > 0 ? maxHp : 100)
        , attack_(attack > 0 ? attack : 10)
        , level_(1)
        , exp_(0)
        , gold_(0)
    {
        cout << "  [創建] " << name_ << " HP:" << hp_
             << " ATK:" << attack_ << endl;
    }

    // ====== 公開介面：受控的操作 ======
    // 這些函數提供了受控的方式來操作角色的狀態，
    // 每個函數都包含數據驗證，確保不變量始終成立。

    // ------ 受傷 —— 有保護的 HP 減少 ------
    // 防護措施：
    //   1. 檢查傷害值是否有效（必須 > 0）
    //   2. HP 不會低於 0（使用 max(0, hp_ - damage)）
    //   3. HP 歸零時自動觸發倒下提示
    void takeDamage(int damage) {
        if (damage <= 0) {
            cout << "  " << name_ << "：無效的傷害值" << endl;
            return;     // 拒絕無效操作
        }
        hp_ = max(0, hp_ - damage);   // HP 不會低於 0，維護不變量 hp_ >= 0
        cout << "  " << name_ << " 受到 " << damage
             << " 點傷害 (HP: " << hp_ << "/" << maxHp_ << ")" << endl;

        if (hp_ == 0) {
            cout << "  " << name_ << " 倒下了！" << endl;
        }
    }

    // ------ 治療 —— 有保護的 HP 增加 ------
    // 防護措施：
    //   1. 治療量必須 > 0
    //   2. 已死亡（hp_ == 0）的角色無法治療
    //   3. HP 不會超過 maxHp_（使用 min(hp_ + amount, maxHp_)）
    void heal(int amount) {
        if (amount <= 0 || hp_ == 0) {
            cout << "  " << name_ << "：無法治療" << endl;
            return;     // 拒絕無效操作
        }
        int oldHp = hp_;
        hp_ = min(hp_ + amount, maxHp_);   // 不超過上限，維護不變量 hp_ <= maxHp_
        cout << "  " << name_ << " 恢復 " << (hp_ - oldHp)
             << " 點生命 (HP: " << hp_ << "/" << maxHp_ << ")" << endl;
    }

    // ------ 獲得經驗 —— 自動檢查升級 ------
    // 防護措施：
    //   1. 經驗量必須 > 0
    //   2. 自動調用 checkLevelUp() 處理升級邏輯
    // 使用者不需要知道升級的內部邏輯，只需調用 gainExp() 即可。
    // 這就是封裝的好處 —— 簡化使用，隱藏複雜的內部邏輯。
    void gainExp(int amount) {
        if (amount <= 0) return;
        exp_ += amount;
        cout << "  " << name_ << " 獲得 " << amount << " 經驗值" << endl;
        checkLevelUp();   // 內部自動處理升級邏輯
    }

    // ------ 獲得金幣 —— 有上限保護 ------
    // 防護措施：
    //   1. 金幣量必須 > 0
    //   2. 金幣上限為 999999（維護不變量 gold_ <= 999999）
    void earnGold(int amount) {
        if (amount <= 0) return;
        gold_ = min(gold_ + amount, 999999);   // 金幣上限
        cout << "  " << name_ << " 獲得 " << amount
             << " 金幣 (總計: " << gold_ << ")" << endl;
    }

    // ------ 花費金幣 —— 檢查是否足夠 ------
    // 防護措施：
    //   1. 花費量必須 > 0
    //   2. 花費量不能超過擁有的金幣數
    //   3. 返回 bool 告訴呼叫者操作是否成功
    bool spendGold(int amount) {
        if (amount <= 0 || amount > gold_) {
            cout << "  " << name_ << "：金幣不足！"
                 << "(需要 " << amount << "，擁有 " << gold_ << ")" << endl;
            return false;   // 操作失敗
        }
        gold_ -= amount;
        cout << "  " << name_ << " 花費 " << amount
             << " 金幣 (剩餘: " << gold_ << ")" << endl;
        return true;        // 操作成功
    }

    // ------ 顯示狀態 —— 只讀，不修改 ------
    // 使用 const 修飾符，保證此函數不會修改任何成員變數
    void printStatus() const {
        cout << "  ┌─────────────────────┐" << endl;
        cout << "  │ " << name_ << " (Lv." << level_ << ")" << endl;
        cout << "  │ HP:  " << hp_ << " / " << maxHp_ << endl;
        cout << "  │ ATK: " << attack_ << endl;
        cout << "  │ EXP: " << exp_ << " / " << expToNextLevel() << endl;
        cout << "  │ Gold: " << gold_ << endl;
        cout << "  └─────────────────────┘" << endl;
    }

    // ------ 查詢函數（只讀 getter）------
    // 只提供必要的只讀訪問，不提供 setter，防止外部繞過驗證邏輯
    bool isAlive() const { return hp_ > 0; }
    const string& getName() const { return name_; }
};

// 展示封裝後的角色類別如何安全運作
void demo_encapsulatedCharacter() {
    cout << "=== 第四部分：完整封裝範例 —— 遊戲角色 ===" << endl;

    // --- 創建角色 ---
    // 建構函數保證初始狀態合法，即使傳入無效參數也會使用預設值
    cout << "\n--- 創建角色 ---" << endl;
    Character hero("勇者", 100, 25);
    hero.printStatus();

    // --- 正常戰鬥流程 ---
    // 所有操作都經過封裝的函數，數據始終保持合法
    cout << "\n--- 正常戰鬥 ---" << endl;
    hero.takeDamage(30);    // HP: 100 -> 70
    hero.takeDamage(50);    // HP: 70 -> 20
    hero.heal(40);          // HP: 20 -> 60

    // --- 嘗試非法操作 —— 全部被攔截 ---
    // 封裝的第二層防護（數據驗證）在此發揮作用
    cout << "\n--- 嘗試非法操作（全部被攔截）---" << endl;
    hero.takeDamage(-100);     // 負傷害？拒絕！
    hero.heal(-50);            // 負治療？拒絕！

    // --- 經驗與升級 —— 自動管理 ---
    // 使用者只需調用 gainExp()，升級邏輯由內部自動處理
    cout << "\n--- 經驗與升級 ---" << endl;
    hero.gainExp(80);     // 累計 80 經驗
    hero.gainExp(50);     // 累計 130 經驗 >= 100（1級升級門檻），觸發升級
    hero.printStatus();

    // --- 金幣系統 —— 有保護 ---
    cout << "\n--- 金幣系統 ---" << endl;
    hero.earnGold(200);       // 獲得 200 金幣
    hero.spendGold(150);      // 花費 150，剩餘 50
    hero.spendGold(100);      // 需要 100 但只有 50，被攔截！

    // --- 致命傷害 ---
    // HP 歸零後，治療操作也會被攔截
    cout << "\n--- 致命傷害 ---" << endl;
    hero.takeDamage(9999);    // 巨量傷害，HP 歸零（不會變成負數）
    hero.heal(50);            // 已死亡，無法治療

    hero.printStatus();
    cout << endl;
}

/*
 * ============================================================================
 *  第五部分：封裝 vs 不封裝對比 —— 堆疊（Stack）範例
 * ============================================================================
 *
 *  這個範例用堆疊（Stack）資料結構來對比封裝與不封裝的差異：
 *
 *  不封裝（RawStack）：
 *    - 使用者必須自己記得初始化 top = -1
 *    - 使用者可以直接修改 top 造成越界
 *    - 沒有任何安全檢查
 *
 *  封裝（SafeStack）：
 *    - 建構函數自動初始化 top_ = -1
 *    - push 時檢查是否已滿
 *    - pop 時檢查是否為空
 *    - 外部無法直接修改 top_ 或 data_（private）
 *    - 嘗試訪問 private 成員會直接編譯錯誤
 * ============================================================================
 */

// 不封裝版本的堆疊 —— 所有成員都是 public
struct RawStack {
    int data[100];
    int top;         // 應該在 [-1, 99] 範圍內，但無人保證
};

// 外部函數操作 RawStack，沒有邊界檢查
void rawPush(RawStack& s, int val) {
    // 如果忘記檢查 top 是否越界呢？直接造成記憶體存取錯誤
    s.data[++s.top] = val;
}

// 封裝版本的堆疊 —— 安全可靠
class SafeStack {
private:
    static const int MAX_SIZE = 100;    // 最大容量
    int data_[MAX_SIZE];                // 存儲數據的陣列
    int top_;                           // 棧頂索引

public:
    // 建構函數：自動初始化 top_ = -1（空棧）
    SafeStack() : top_(-1) {}

    // 推入元素 —— 有邊界檢查
    // 如果棧已滿（top_ >= MAX_SIZE - 1），拒絕操作並返回 false
    bool push(int val) {
        if (top_ >= MAX_SIZE - 1) {
            cout << "  棧已滿，無法推入 " << val << endl;
            return false;
        }
        data_[++top_] = val;
        return true;
    }

    // 彈出元素 —— 有邊界檢查
    // 如果棧為空（top_ < 0），拒絕操作並返回 false
    // 透過引用參數 out 返回彈出的值
    bool pop(int& out) {
        if (top_ < 0) {
            cout << "  棧為空，無法彈出" << endl;
            return false;
        }
        out = data_[top_--];
        return true;
    }

    // 只讀查詢函數
    bool isEmpty() const { return top_ < 0; }
    int size() const { return top_ + 1; }
};

// 展示封裝 vs 不封裝堆疊的對比
void demo_stackComparison() {
    cout << "=== 第五部分：封裝 vs 不封裝（Stack）===" << endl;

    // --- 不封裝的 RawStack ---
    cout << "\n--- 不封裝的 RawStack ---" << endl;
    RawStack raw;
    raw.top = -1;       // 使用者必須自己記得初始化！如果忘了就是災難
    rawPush(raw, 10);
    rawPush(raw, 20);
    cout << "  Top = " << raw.top << endl;

    // 直接越界修改 top —— 沒人阻止，可能造成程式崩潰
    raw.top = 999;
    cout << "  直接修改 top = " << raw.top << " (越界！危險！)" << endl;

    // --- 封裝的 SafeStack ---
    cout << "\n--- 封裝的 SafeStack ---" << endl;
    SafeStack safe;       // 建構函數自動初始化，不需要手動設定
    safe.push(10);
    safe.push(20);
    cout << "  Size = " << safe.size() << endl;

    // 以下操作會產生編譯錯誤（已註解），因為 private 不可訪問：
    // safe.top_ = 999;    // 編譯錯誤！private 成員
    // safe.data_[0] = 0;  // 編譯錯誤！private 成員

    // 正常彈出兩個元素
    int val;
    safe.pop(val);      // 彈出 20
    safe.pop(val);      // 彈出 10
    // 嘗試從空棧彈出 —— 被安全攔截
    safe.pop(val);      // 空棧，安全攔截

    cout << endl;
}

/*
 * ============================================================================
 *  第六部分：不變量（Invariant）的概念
 * ============================================================================
 *
 *  不變量（Invariant）是指「對象在任何時刻都必須滿足的條件」。
 *  這是封裝最重要的核心概念。
 *
 *  Character 類別的不變量：
 *    1. hp_ >= 0               （生命值不能是負數）
 *    2. hp_ <= maxHp_           （生命值不超過上限）
 *    3. maxHp_ > 0              （最大生命值必須為正）
 *    4. attack_ > 0             （攻擊力必須為正）
 *    5. level_ >= 1             （等級至少為 1）
 *    6. exp_ >= 0               （經驗不能是負數）
 *    7. exp_ < expToNextLevel() （經驗不超過升級門檻）
 *    8. 0 <= gold_ <= 999999    （金幣在合法範圍）
 *
 *  封裝的目的就是維護不變量。每個 public 成員函數都必須保證：
 *
 *    函數開始前：不變量成立
 *      ↓
 *    函數執行中：可以暫時違反（例如計算過程中 hp 可能暫時超出範圍）
 *      ↓
 *    函數結束後：不變量必須恢復成立
 *
 *  如果數據是 public 的，任何外部代碼都可以直接修改數據，
 *  不變量就無法維護，對象就可能進入非法狀態。
 *
 * ============================================================================
 */

/*
 * ============================================================================
 *  第七部分：封裝的四大好處
 * ============================================================================
 *
 *  好處一：數據保護
 *    → 防止外部代碼破壞對象的合法狀態
 *    → 維護不變量
 *    → 例如：hp 永遠不會變成負數或超過 maxHp
 *
 *  好處二：介面穩定
 *    → 外部代碼只依賴 public 介面
 *    → 內部實現可以自由修改，不影響使用者
 *    → 例如：從陣列改成 vector，使用者的代碼完全不需要修改
 *
 *    介面穩定性範例：
 *      版本 1 內部用 string names[100]（陣列）
 *      版本 2 改用 vector<string> names（動態容器）
 *      但 public 介面（addPlayer、removePlayer、getCount）完全不變！
 *      使用者的代碼不需要做任何修改。
 *
 *  好處三：降低耦合
 *    → 使用者不需要知道內部結構
 *    → 減少代碼之間的依賴關係
 *    → 修改內部實現時不會影響其他模組
 *
 *  好處四：簡化使用
 *    → 使用者只需要知道「做什麼」，不需要知道「怎麼做」
 *    → 就像開車不需要懂引擎原理
 *    → 例如：調用 gainExp() 就好，升級邏輯由內部自動處理
 *
 * ============================================================================
 */

/*
 * ============================================================================
 *  第八部分：封裝的四個層次
 * ============================================================================
 *
 *  層次 1：語法層封裝（Access Control）
 *    → 使用 private / protected / public 關鍵字
 *    → 編譯器強制執行，違反會直接編譯錯誤
 *    → 最基本的封裝層次
 *
 *  層次 2：邏輯層封裝（Data Validation）
 *    → 在操作函數中加入驗證邏輯
 *    → 例如：檢查 damage > 0、amount > 0
 *    → 維護不變量
 *
 *  層次 3：介面層封裝（Interface Design）
 *    → 只暴露必要的操作
 *    → 隱藏實現細節（如 checkLevelUp、expToNextLevel）
 *    → 使用者不需要知道內部如何運作
 *
 *  層次 4：模組層封裝（Module / Namespace）
 *    → 用命名空間和頭文件組織代碼
 *    → 控制哪些符號對外可見
 *    → 大型專案中的封裝策略
 *
 * ============================================================================
 */

/*
 * ============================================================================
 *  第九部分：封裝設計原則 —— 暴露最少介面
 * ============================================================================
 *
 *  一個好的封裝設計原則：暴露盡可能少的介面。
 *
 *  不好的設計（暴露太多 public）：    好的設計（最少暴露）：
 *  ┌────────────┐                   ┌────────────┐
 *  │  public:   │                   │  public:   │
 *  │  func1()   │                   │  doTask()  │  ← 只暴露高層操作
 *  │  func2()   │                   │            │
 *  │  func3()   │                   │  private:  │
 *  │  func4()   │                   │  step1()   │  ← 內部步驟隱藏
 *  │  func5()   │                   │  step2()   │
 *  │  func6()   │                   │  step3()   │
 *  │            │                   │  helper()  │
 *  │  private:  │                   │  data_     │
 *  │  data_     │                   └────────────┘
 *  └────────────┘
 *
 *  Character 類別的設計就遵循了這個原則：
 *    public：takeDamage、heal、gainExp、earnGold、spendGold、printStatus
 *    private：expToNextLevel、checkLevelUp（內部邏輯隱藏）
 *    沒有提供 setHp、setLevel 等 setter（避免繞過驗證）
 *
 * ============================================================================
 */

