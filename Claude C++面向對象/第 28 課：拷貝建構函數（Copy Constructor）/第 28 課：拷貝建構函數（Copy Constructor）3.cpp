// * 本檔輸出完全決定性：只有整數運算與計數，沒有位址、沒有耗時、
//   沒有執行緒（本機實測連跑 5 次逐位元組相同）。
// * 刻意「不列印任何指標位址」：位址會因 ASLR 每次執行都不同。
//   要證明兩個物件的記憶體各自獨立，改用「改一個、印兩個」——
//   arr1 不受 arr2[0]=999 影響，這個結果才是可重現的證據。
// * LeetCode 1480 那段是本檔的成本對照，傳值版總共出現「兩次」[拷貝建構]：
//       第 1 次：呼叫時把 arr1 深拷貝進參數 a
//       第 2 次：return a; —— 參數「不適用」NRVO
//       之後那行 [解構] 就是參數 a 離開函式時銷毀
//   傳參考版則完全沒有 [拷貝建構]，只有 result 的一次 [建構]。
