// 【日常實務範例】感測器讀數的「快照」與回復
//   情境：資料擷取程式持續把感測器讀數寫進一個緩衝區。
//         套用校正演算法之前，要先留一份原始讀數的快照，
//         萬一校正結果不合理（例如超出量測範圍）就整批回復。
//   為什麼用到本主題：快照必須是「獨立的一份資料」，
//         淺拷貝會讓快照與工作緩衝區指向同一塊記憶體 ——
//         一邊被校正改動，另一邊也跟著變，回復時就拿不回原始值了。
//         深拷貝（拷貝建構函數）正是讓快照真正獨立的機制。
// -----------------------------------------------------------------------------
bool calibrationLooksSane(const IntArray& buf) {
    for (std::size_t i = 0; i < buf.size(); ++i) {
        if (buf[i] < 0 || buf[i] > 1000) return false;   // 合理量測範圍
    }
    return true;
}

int main() {
    std::cout << "=== 建立 arr1 ===\n";
    IntArray arr1(5);
    for (std::size_t i = 0; i < arr1.size(); ++i) {
        arr1[i] = static_cast<int>((i + 1) * 10);  // 10, 20, 30, 40, 50
    }
    arr1.print("arr1");

    std::cout << "\n=== 拷貝建構 arr2 = arr1 ===\n";
    IntArray arr2 = arr1;  // 拷貝建構
    arr2.print("arr2");

    std::cout << "\n=== 修改 arr2，驗證獨立性 ===\n";
    arr2[0] = 999;
    arr1.print("arr1");  // 不受影響
    arr2.print("arr2");  // 只有 arr2 改變

    std::cout << "\n=== 拷貝賦值 arr3 = arr1 ===\n";
    IntArray arr3(3);     // 先建構一個不同大小的
    arr3.print("arr3 (before)");
    arr3 = arr1;          // 拷貝賦值：釋放舊的 3 個元素，配置新的 5 個
    arr3.print("arr3 (after)");

    std::cout << "\n=== LeetCode 1480：傳值 vs 傳參考的拷貝成本 ===\n";
    std::cout << "  --- 傳值版（注意多出來的 [拷貝建構]）---\n";
    IntArray sum1 = runningSumByValue(arr1);
    sum1.print("runningSum");
    std::cout << "  --- 傳 const 參考版（沒有 [拷貝建構]）---\n";
    IntArray sum2 = runningSumByRef(arr1);
    sum2.print("runningSum");

    std::cout << "\n=== 日常實務：感測器讀數的快照與回復 ===\n";
    IntArray working(4);
    working[0] = 100; working[1] = 200; working[2] = 300; working[3] = 400;
    working.print("原始讀數");

    IntArray snapshot = working;      // ★ 深拷貝：快照與工作緩衝區完全獨立
    std::cout << "  （已建立快照）\n";

    for (std::size_t i = 0; i < working.size(); ++i) {   // 套用一個過頭的校正
        working[i] = working[i] * 3;
    }
    working.print("校正後");

    if (!calibrationLooksSane(working)) {
        std::cout << "  校正結果超出量測範圍 → 從快照回復\n";
        working = snapshot;           // 拷貝賦值
        working.print("回復後");
    }

    std::cout << "\n=== 結束 ===\n";
    return 0;
}

