// =============================================================================
//  第 28 課 -3  —  自訂動態陣列的深拷貝：拷貝建構函數 + 拷貝賦值運算子
// =============================================================================
//
// 【主題資訊 Information】
//   拷貝建構函數  ： IntArray(const IntArray& other);
//   拷貝賦值運算子： IntArray& operator=(const IntArray& other);
//   解構函數      ： ~IntArray();
//   —— 三者合稱「三法則（Rule of Three）」：只要需要其中一個，
//      通常三個都需要（C++11 後擴充為五法則，見第 34 課）
//   標準版本   ： C++98 起；本檔的 new int[n]{} 值初始化語法需 C++11
//   標頭檔     ： <algorithm>（std::copy）
//   複雜度     ： 拷貝建構 O(n)；拷貝賦值 O(n)；operator[] O(1)
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼一定要自己寫拷貝建構函數】
//   IntArray 持有一個裸指標 m_data。編譯器自動生成的拷貝建構函數
//   做的是「逐成員拷貝」，對指標而言就是把「位址值」複製一份 ——
//   於是兩個物件的 m_data 指向同一塊記憶體（淺拷貝，見第 27 課）。
//   後果有兩個，而且都很嚴重：
//       * 改其中一個會連帶改到另一個（本檔第三段就是在驗證這件事沒發生）
//       * 兩個物件解構時各 delete[] 一次同一塊記憶體 → double free
//   自己寫的版本「配置新記憶體 + 複製內容」，這就是深拷貝。
//
// 【2. 拷貝賦值運算子的三個必要動作，順序很重要】
//   operator= 面對的是「已經初始化過」的物件，所以比拷貝建構多一件事：
//   要先處理掉自己原有的資源。本檔的順序是：
//       ① if (this == &other) return *this;   自我賦值防護
//       ② delete[] m_data;                    釋放舊資源
//       ③ 配置新記憶體 + 複製內容
//   ① 為什麼不能省：若寫 arr = arr，沒有防護時 ② 會先把資料 delete 掉，
//   ③ 再從「已經釋放的自己」複製 —— 讀取已釋放記憶體是未定義行為。
//   （這與第 26 課 -10 的 this == &other 是同一個 idiom。）
//
// 【3. 更安全的順序：先配置、後釋放】
//   本檔採「先 delete 再 new」的教科書順序，好讀但有一個弱點：
//   若 ③ 的 new 拋出 bad_alloc，物件會處於「舊的已刪、新的沒有」的壞狀態，
//   m_data 成了懸空指標，解構時會再 delete 一次 → 未定義行為。
//   更安全的寫法是先配置成功再釋放舊的：
//       int* newData = new int[other.m_size];          // 失敗就直接拋，自己還完好
//       std::copy(other.m_data, other.m_data + other.m_size, newData);
//       delete[] m_data;                                // 到這裡才動舊資源
//       m_data = newData;  m_size = other.m_size;
//   這樣就有了強例外保證。更徹底的做法是 copy-and-swap（見第 33 課 -2）。
//   本檔保留原始順序以聚焦深拷貝本身，但讀者應知道這個差別。
//
// 【4. 本檔驗證「獨立性」的方式】
//   第三段把 arr2[0] 改成 999，再同時印出 arr1 與 arr2。
//   arr1 維持 [10, 20, 30, 40, 50]，arr2 變成 [999, ...] ——
//   兩者互不影響，證明它們各自擁有獨立的記憶體。
//   若拷貝建構函數沒寫（淺拷貝），這一步會看到兩個都變成 999，
//   而且程式結束時很可能因 double free 而異常終止。
//
// 【概念補充 Concept Deep Dive】
//
// (A) ★ 成員宣告順序必須與初始化列表順序一致（-Wreorder）
//   成員的初始化順序「只」由宣告順序決定，與初始化列表的書寫順序無關。
//   本檔原始版本的宣告是 int* m_data; 在前、std::size_t m_size; 在後，
//   初始化列表卻寫成 : m_size(size), m_data(new int[size]{})，
//   兩者不一致 —— 編譯時會產生 -Wreorder 警告（本機實測確實會報）。
//   目前之所以還沒出事，是因為兩個初始值用的都是「參數 size」
//   或「other.m_size」，沒有依賴同一個物件的兄弟成員。
//   但這個寫法非常脆弱：只要有人把它改成很自然的
//       m_data(new int[m_size])
//   就會在 m_size 還沒初始化時拿它當長度，配置出錯誤大小的緩衝區，
//   接著 std::copy 寫出界 —— 典型的 heap buffer overflow。
//   本檔已把宣告順序改為 m_size 在前、m_data 在後，
//   與初始化列表一致，警告消除，也讓上面那種改法自動變安全。
//   （課程中其他檔案的 m_len / m_data 也是採這個順序，並附有相同提醒。）
//
// (B) new int[size]{} 的值初始化
//   結尾的 {} 讓陣列元素被「值初始化」為 0。
//   若寫成 new int[size]（沒有 {}），元素是「預設初始化」——
//   對 int 這種內建型別即為不確定值，讀取它是未定義行為。
//   本檔印出 arr3 (before) 是 [0, 0, 0]，正是靠這個 {}。
//   注意拷貝建構函數裡刻意用 new int[other.m_size]（不加 {}），
//   因為緊接著 std::copy 會覆寫每一個元素，先歸零是多餘的成本。
//
// (C) 這個類別只有三法則，沒有移動操作
//   IntArray 寫了解構函數，所以編譯器不會自動生成移動建構與移動賦值
//   （見第 32 課 -3）。結果是所有「移動」都會安靜退回深拷貝：
//   把它放進 std::vector 並觸發擴容時，既有元素是被逐一深拷貝過去的。
//   要補齊，就是第 34 課的五法則。
//
// (D) operator[] 為什麼要提供 const 與非 const 兩個版本
//   非 const 版回傳 int&，讓 arr[0] = 999 這種寫法成立；
//   const 版回傳 const int&，讓 const IntArray& 也能被讀取。
//   少了 const 版，print() 這種 const 成員函式就無法用 [] 讀資料。
//   兩者靠「const 成員函式」這個特徵重載，是標準庫容器的通用做法。
//   注意兩者都沒有做邊界檢查（與 std::vector::operator[] 一致）；
//   要檢查應另外提供 at()，越界時丟 std::out_of_range。
//
// 【注意事項 Pay Attention】
//   1. 成員宣告順序決定初始化順序。宣告順序與初始化列表不一致時
//      務必修正（用 -Wreorder 讓編譯器抓），否則很容易演變成
//      「用未初始化的長度去配置記憶體」的堆積溢位。
//   2. 拷貝賦值一定要有自我賦值防護，否則 arr = arr 會先刪後讀。
//   3. 「先 delete 再 new」在 new 拋例外時會留下懸空指標；
//      要強例外保證請改成「先 new 成功再 delete」或用 copy-and-swap。
//   4. operator[] 不做邊界檢查（與 std::vector 一致），越界是未定義行為。
//   5. 本類別只有三法則，沒有移動操作，所有「移動」都會退回深拷貝。
//   6. 實務上請優先用 std::vector<int>——本檔是為了理解深拷貝的機制而手寫。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】深拷貝、三法則與成員初始化順序
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 為什麼持有裸指標的類別一定要自己寫拷貝建構函數？不寫會怎樣？
//     答：編譯器生成的版本是逐成員拷貝，對指標而言只複製「位址值」，
//         兩個物件因此指向同一塊記憶體（淺拷貝）。
//         後果一是改一個會連帶改另一個；
//         後果二是兩者解構時各 delete[] 一次同一塊記憶體 → double free。
//         自己寫的版本要配置新記憶體再複製內容，這才是深拷貝。
//     追問：那還要寫哪些？
//         → 三法則：解構函數、拷貝建構函數、拷貝賦值運算子。
//           需要其中一個通常就三個都需要；C++11 之後再加上兩個移動操作
//           成為五法則（第 34 課）。
//
// 🔥 Q2. 成員的初始化順序由什麼決定？
//     答：只由「成員在類別中的宣告順序」決定，
//         與建構函數初始化列表的書寫順序完全無關。
//         寫成不同順序不會改變實際執行順序，只會得到 -Wreorder 警告。
//     追問：這在什麼情況下會變成真正的 bug？
//         → 當某個成員的初始值用到了另一個成員時。
//           例如宣告是 m_data 在前、m_size 在後，卻寫
//           m_data(new int[m_size])，此時 m_size 尚未初始化，
//           會用不確定值當長度去配置，接著複製資料就寫出界 ——
//           典型的 heap buffer overflow。解法是讓宣告順序與依賴順序一致。
//
// ⚠️ 陷阱. 「拷貝賦值運算子裡先 delete[] 舊的、再 new 新的，
//           邏輯很清楚，這樣寫沒問題。」
//     答：正常情況沒問題，但不具備強例外保證。
//         若 new 拋出 bad_alloc，此時舊資源已經被刪掉、新的又沒配置成功，
//         m_data 成了懸空指標，物件處於壞狀態；
//         之後解構函數再 delete 一次，就是對已釋放記憶體再次釋放，
//         屬於未定義行為。
//         正確順序是「先配置成功，再釋放舊的」，或直接用 copy-and-swap。
//     為什麼會錯：只想到「正常流程」的正確性，
//         沒把「中途拋例外」當成必然會發生的路徑來設計。
//         例外安全看的是「失敗時物件停在什麼狀態」，不是成功時的結果。
// ═══════════════════════════════════════════════════════════════════════════

// lesson28_int_array.cpp

#include <iostream>
#include <algorithm>  // std::copy

class IntArray {
private:
    // ⚠️ 宣告順序決定初始化順序，必須與初始化列表一致（見檔頭【概念補充 A】）。
    //    m_size 宣告在前，之後若把配置改寫成 new int[m_size] 也不會出事。
    std::size_t m_size;
    int* m_data;

public:
    // ──────── 一般建構函數 ────────
    explicit IntArray(std::size_t size)
        : m_size(size)
        , m_data(new int[size]{})  // 值初始化為 0（結尾的 {} 不可省，否則是不確定值）
    {
        std::cout << "  [建構] 大小=" << m_size << "\n";
    }

    // ──────── 拷貝建構函數（深拷貝）────────
    IntArray(const IntArray& other)
        : m_size(other.m_size)
        , m_data(new int[other.m_size])   // 配置新記憶體（不加 {}，下一行會全部覆寫）
    {
        std::copy(other.m_data, other.m_data + m_size, m_data);  // 複製內容
        std::cout << "  [拷貝建構] 大小=" << m_size << "\n";
    }

    // ──────── 拷貝賦值運算子（深拷貝）────────
    IntArray& operator=(const IntArray& other) {
        std::cout << "  [拷貝賦值]\n";
        if (this == &other) return *this;   // 自我賦值防護：少了它會先刪後讀

        // 註：此處採教科書順序「先釋放再配置」，好讀但不具強例外保證 ——
        //     若下面的 new 拋出 bad_alloc，m_data 會成為懸空指標。
        //     要強例外保證請改成「先配置成功再釋放舊的」，或用 copy-and-swap
        //     （第 33 課 -2）。詳見檔頭【詳細解釋 3】。
        delete[] m_data;                          // 釋放舊資源
        m_size = other.m_size;
        m_data = new int[m_size];                 // 配置新記憶體
        std::copy(other.m_data, other.m_data + m_size, m_data);

        return *this;
    }

    // ──────── 解構函數 ────────
    ~IntArray() {
        std::cout << "  [解構] 大小=" << m_size << "\n";
        delete[] m_data;
    }

    // ──────── 存取 ────────
    // 兩個版本靠 const 重載：非 const 版可寫，const 版讓 const 物件能讀。
    // 與 std::vector::operator[] 一致，兩者都不做邊界檢查。
    int& operator[](std::size_t index) { return m_data[index]; }
    const int& operator[](std::size_t index) const { return m_data[index]; }
    std::size_t size() const { return m_size; }

    void print(const char* label) const {
        std::cout << "  " << label << ": [";
        for (std::size_t i = 0; i < m_size; ++i) {
            if (i > 0) std::cout << ", ";
            std::cout << m_data[i];
        }
        std::cout << "]\n";
    }
};

// -----------------------------------------------------------------------------
// 【LeetCode 實戰範例】LeetCode 1480. Running Sum of 1d Array
//   題目：給定陣列 nums，回傳 runningSum，其中 runningSum[i] = nums[0]+...+nums[i]。
//   為什麼放在本課：題目本身是一行前綴和，重點不在演算法，
//         而在於它讓「拷貝建構函數什麼時候會被呼叫」變得具體可數。
//         下面刻意提供兩個版本，簽名只差一個 &：
//             runningSumByValue(IntArray a)        → 傳值，進函式就深拷貝一次
//             runningSumByRef  (const IntArray& a) → 傳參考，零拷貝
//         執行時看 [拷貝建構] 出現幾次，就知道傳值的代價在哪裡。
//         這正是本課「深拷貝要花 O(n) 配置 + 複製」的直接後果。
//   複雜度：兩者演算法同為 O(n)；差別在傳值版多一次 O(n) 的配置與複製。
// -----------------------------------------------------------------------------
IntArray runningSumByValue(IntArray a) {          // ← 傳值：呼叫時就會拷貝建構
    for (std::size_t i = 1; i < a.size(); ++i) {
        a[i] += a[i - 1];
    }
    return a;
}

IntArray runningSumByRef(const IntArray& nums) {  // ← 傳 const 參考：不拷貝
    IntArray result(nums.size());
    if (nums.size() > 0) {
        result[0] = nums[0];
        for (std::size_t i = 1; i < nums.size(); ++i) {
            result[i] = result[i - 1] + nums[i];
        }
    }
    return result;
}

