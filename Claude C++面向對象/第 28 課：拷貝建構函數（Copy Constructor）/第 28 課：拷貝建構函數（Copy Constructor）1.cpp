#include <iostream>
#include <cstring>
#include <vector>

namespace {
// 緩衝區編號產生器：每次「真正配置一塊新記憶體」時遞增。
// 用它取代原始位址，因為位址受 ASLR 影響、每次執行都不同（見檔頭【概念補充 C】）。
int g_nextBufId = 0;
}  // namespace

class SimpleString {
private:
    // ⚠️ 宣告順序決定初始化順序，必須與初始化列表一致（見檔頭【概念補充 B】）。
    std::size_t m_len;
    char* m_data;
    int m_bufId;

public:
    // ──────── 一般建構函數 ────────
    SimpleString(const char* str = "") {
        m_len = std::strlen(str);
        m_data = new char[m_len + 1];
        m_bufId = ++g_nextBufId;
        std::strcpy(m_data, str);
        std::cout << "  [一般建構] \"" << m_data << "\" 配置 buf#" << m_bufId << "\n";
    }

    // ──────── 拷貝建構函數 ────────
    SimpleString(const SimpleString& other)
        : m_len(other.m_len)                     // 用初始化列表複製長度
        , m_data(new char[other.m_len + 1])      // 配置新記憶體
        , m_bufId(++g_nextBufId)                 // 新的一塊 → 新的編號
    {
        std::strcpy(m_data, other.m_data);       // 複製內容
        std::cout << "  [拷貝建構] \"" << m_data
                  << "\" 從 buf#" << other.m_bufId
                  << " 複製到「新的」buf#" << m_bufId << "\n";
    }

    // ──────── 拷貝賦值運算子 ────────
    SimpleString& operator=(const SimpleString& other) {
        std::cout << "  [拷貝賦值] ";
        if (this == &other) {                    // 自我賦值防護
            std::cout << "自我賦值，跳過\n";
            return *this;
        }
        delete[] m_data;
        m_len = other.m_len;
        m_data = new char[m_len + 1];
        m_bufId = ++g_nextBufId;                 // 又配置了一塊 → 新編號
        std::strcpy(m_data, other.m_data);
        std::cout << "\"" << m_data << "\" 配置 buf#" << m_bufId << "\n";
        return *this;
    }

    // ──────── 解構函數 ────────
    ~SimpleString() {
        std::cout << "  [解構] \"" << (m_data ? m_data : "nullptr")
                  << "\" 釋放 buf#" << m_bufId << "\n";
        delete[] m_data;
    }

    const char* c_str() const { return m_data; }
    int bufId() const { return m_bufId; }
};

// ──────── 測試用的函數 ────────

// 按值傳參 → 觸發拷貝建構
void passByValue(SimpleString s) {
    std::cout << "    函數內：s = \"" << s.c_str() << "\"（buf#" << s.bufId() << "）\n";
}
// 函數結束時 s 被解構

// 按 const 引用傳參 → 不觸發拷貝建構
void passByConstRef(const SimpleString& s) {
    std::cout << "    函數內：s = \"" << s.c_str() << "\"（buf#" << s.bufId()
              << "  ← 沿用呼叫端那一塊，沒有新配置）\n";
}

// 按值返回 → 可能觸發拷貝建構（也可能被優化掉）
SimpleString createString() {
    std::cout << "  createString() 內部：\n";
    SimpleString local("Created Inside");
    std::cout << "  即將 return local\n";
    return local;
}

int main() {
    std::cout << "===== 情境 1：直接初始化 =====\n";
    SimpleString a("Alpha");
    SimpleString b(a);         // 拷貝建構
    std::cout << "\n";

    std::cout << "===== 情境 2：拷貝初始化（= 語法）=====\n";
    SimpleString c = a;        // 拷貝建構（不是賦值！c 尚不存在）
    std::cout << "\n";

    std::cout << "===== 情境 3：按值傳參 =====\n";
    std::cout << "  呼叫 passByValue(a):\n";
    passByValue(a);            // 拷貝建構 → 函數結束時解構副本
    std::cout << "\n";

    std::cout << "===== 情境 4：按 const 引用傳參 =====\n";
    std::cout << "  呼叫 passByConstRef(a):\n";
    passByConstRef(a);         // 沒有拷貝建構！
    std::cout << "\n";

    std::cout << "===== 情境 5：按值返回 =====\n";
    SimpleString d = createString();
    std::cout << "  d = \"" << d.c_str() << "\"（buf#" << d.bufId()
              << "  ← 沒有 [拷貝建構]，NRVO 把它直接建在這裡）\n";
    std::cout << "\n";

    std::cout << "===== 情境 6：賦值（不是拷貝建構！）=====\n";
    SimpleString e("Echo");
    std::cout << "  e = a（拷貝賦值，不是拷貝建構）:\n";
    e = a;                     // 拷貝賦值運算子，因為 e 已經存在
    std::cout << "\n";

    // -------------------------------------------------------------------------
    // 【日常實務範例】把設定物件存進快取容器
    //   情境：服務把每個租戶的設定物件放進 std::vector 當快取。
    //   為什麼用到本主題：push_back(cfg) 會「拷貝建構」一份放進容器 ——
    //         這是情境 1~6 之外、實務上最常忽略的拷貝來源。
    //         而且容量不足時擴充還要把既有元素再搬一次；
    //         本類別沒有移動操作（見【概念補充 D】），那次搬移也是深拷貝。
    //   對策：先 reserve 足夠容量，避免擴充造成的額外深拷貝。
    // -------------------------------------------------------------------------
    std::cout << "===== 情境 7（日常實務）：存進容器也會拷貝建構 =====\n";
    SimpleString tenantCfg("tenant=acme;region=tw");
    std::vector<SimpleString> cache;
    cache.reserve(2);          // ★ 先預留，避免擴充時再深拷貝一次既有元素
    std::cout << "  push_back(tenantCfg):\n";
    cache.push_back(tenantCfg);            // 拷貝建構一份進容器
    std::cout << "  容器內第 1 筆 = \"" << cache[0].c_str()
              << "\"（buf#" << cache[0].bufId() << "  ← 與來源不同塊）\n";
    std::cout << "\n";

    std::cout << "===== 開始解構 =====\n";
    return 0;
}

