//       情境 5       → 沒有 [拷貝建構]，NRVO 直接把 local 建在 d 的位置
//       情境 6       → 印的是 [拷貝賦值] 不是 [拷貝建構]（e 已經存在）
//       情境 7       → push_back 也是拷貝建構的觸發點
// * 情境 5 沒有拷貝是 NRVO 的結果，而 NRVO 是「可選」的最佳化，
//   標準不保證；C++17 只保證純右值初始化的省略（見第 28 課 -2）。
//   其他編譯器在這裡多印一次 [拷貝建構] 也是合法的。
// * 結尾 [解構] 的順序是區域物件宣告順序的反向：
//   cache（內含 1 筆）、tenantCfg、e、d、c、b、a。
// * 本檔已修正原始版本的 -Wreorder 警告（成員宣告順序）；
//   該警告是尚未被觸發的潛在缺陷，修正前後執行結果相同。
// ─────────────────────────────────────────────────────────────────────────

// === 預期輸出 ===
// ===== 情境 1：直接初始化 =====
//   [一般建構] "Alpha" 配置 buf#1
//   [拷貝建構] "Alpha" 從 buf#1 複製到「新的」buf#2
//
// ===== 情境 2：拷貝初始化（= 語法）=====
//   [拷貝建構] "Alpha" 從 buf#1 複製到「新的」buf#3
//
// ===== 情境 3：按值傳參 =====
//   呼叫 passByValue(a):
//   [拷貝建構] "Alpha" 從 buf#1 複製到「新的」buf#4
//     函數內：s = "Alpha"（buf#4）
//   [解構] "Alpha" 釋放 buf#4
//
// ===== 情境 4：按 const 引用傳參 =====
//   呼叫 passByConstRef(a):
//     函數內：s = "Alpha"（buf#1  ← 沿用呼叫端那一塊，沒有新配置）
//
