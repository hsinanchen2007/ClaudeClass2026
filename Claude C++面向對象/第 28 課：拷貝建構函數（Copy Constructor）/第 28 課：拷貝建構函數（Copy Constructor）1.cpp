// =============================================================================
//  第 28 課 -1  —  拷貝建構函數：它到底在什麼時候會被呼叫？
// =============================================================================
//
// 【主題資訊 Information】
//   簽名     ： ClassName(const ClassName& other);
//   為什麼是 const&：
//               * 用 & ——若傳值就會為了建構參數再呼叫一次拷貝建構函數，無限遞迴
//               * 用 const ——才能接住暫時物件，也才能拷貝 const 物件
//   標準版本 ： C++98
//   標頭檔   ： <cstring>（strlen / strcpy）
//   複雜度   ： O(n)，要配置新記憶體並複製內容
//   對照     ： 拷貝賦值運算子 operator=（見第 29 課）—— 兩者的差別見下方第 2 點
//
// 【詳細解釋 Explanation】
//
// 【1. 本檔的主軸：拷貝建構函數的六個觸發時機】
//   拷貝建構函數不是「寫了 = 就會呼叫」，它的觸發條件是
//   「用一個既有物件去『初始化』一個尚不存在的物件」。本檔逐一驗證：
//       情境 1  直接初始化      SimpleString b(a);
//       情境 2  拷貝初始化      SimpleString c = a;      ← 是初始化，不是賦值
//       情境 3  按值傳參        passByValue(a);          ← 最常被忽略的一個
//       情境 4  按 const& 傳參  passByConstRef(a);       ← 不觸發，這是對照組
//       情境 5  按值返回        return local;            ← 可能被省略掉
//       情境 6  賦值            e = a;                   ← 不觸發，走的是 operator=
//
// 【2. 情境 2 vs 情境 6：= 這個符號的兩種身分】
//   這是本課最容易混淆的地方，關鍵在「左邊那個物件存不存在」：
//       SimpleString c = a;   // c 尚不存在 → 這是「初始化」→ 拷貝建構函數
//       e = a;                // e 已經存在 → 這是「賦值」  → 拷貝賦值運算子
//   同一個 = 符號，做的是完全不同的兩件事。
//   從輸出可以直接看出來：情境 2 印 [拷貝建構]，情境 6 印 [拷貝賦值]。
//   差別不只是名稱：拷貝賦值多了「要先處理掉自己原有的資源」這件事，
//   所以它需要自我賦值防護，拷貝建構則不需要（物件還是空的，沒有舊資源）。
//
// 【3. 情境 3：按值傳參是效能問題的常見來源】
//   passByValue(a) 會為了建構參數而完整深拷貝一次，
//   函式結束時再解構那份副本 —— 一次 O(n) 配置 + 複製 + 釋放。
//   對照情境 4 的 const&：完全沒有任何拷貝。
//   守則：只讀不改的參數一律用 const&。
//   只有在「函式本來就需要一份自己的副本」時，傳值才是合理的
//   （那時傳值反而更好，因為呼叫端傳右值時可以直接移動進來）。
//
// 【4. 情境 5：為什麼看不到拷貝】
//   createString() 回傳一個區域變數，照理說要拷貝建構到 d，
//   但輸出裡沒有 [拷貝建構]。這是 NRVO（具名回傳值最佳化）：
//   編譯器直接把 local 建構在呼叫端 d 的位置上，從頭到尾只有一個物件。
//   注意 NRVO 是「可選」的最佳化，標準並不保證；
//   C++17 只保證「純右值初始化」的省略。詳見第 28 課 -2 的四組實測對照。
//   也因此，不要在拷貝建構函數裡放「一定要發生」的副作用 ——
//   它可能被合法地省略掉。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼拷貝建構函數的參數不能傳值
//   若寫成 SimpleString(SimpleString other)，
//   那麼為了建構參數 other，又要呼叫一次拷貝建構函數，
//   而那次呼叫又要建構它自己的參數…… 無限遞迴。
//   所以標準規定拷貝建構函數的參數必須是參考。
//   加上 const 則有兩個好處：能接住暫時物件，也能拷貝 const 物件。
//
// (B) ★ 成員宣告順序必須與初始化列表一致（-Wreorder）
//   本檔原始版本的宣告是 char* m_data; 在前、std::size_t m_len; 在後，
//   拷貝建構函數的初始化列表卻寫成
//       : m_len(other.m_len), m_data(new char[other.m_len + 1])
//   兩者不一致 → 編譯時產生 -Wreorder 警告（本機實測確實會報）。
//   目前還沒出事，是因為配置長度用的是 other.m_len（別的物件的成員），
//   不是自己還沒初始化的 m_len。
//   但只要有人把它改成很自然的 new char[m_len + 1]，
//   就會用「尚未初始化的長度」去配置記憶體，
//   接著 strcpy 寫出界 —— 典型的 heap buffer overflow。
//   本檔已把宣告順序改為 m_len 在前、m_data 在後，警告消除。
//
// (C) ★ 本檔為什麼不印記憶體位址
//   原始版本印的是 static_cast<void*>(m_data) 這種原始位址，
//   用意是證明「新舊兩塊記憶體不同」。但位址受 ASLR 影響、
//   每次執行都不同（本機實測三次得到三組完全不同的位址），
//   無法寫成可對照的預期輸出。
//   本檔改用「buf#N」緩衝區編號：每次真正配置記憶體時遞增。
//   於是「深拷貝配置了新的一塊」變成可重現的證據 ——
//   情境 1、2、3 各自產生新編號，而情境 4 完全沒有新編號。
//   （這與第 32 課 -1 採用同一套表示法，方便兩課對照。）
//
// (D) 這個類別只有三法則
//   SimpleString 寫了解構、拷貝建構、拷貝賦值，符合三法則，
//   但沒有移動操作 —— 而且因為宣告了解構函數，
//   編譯器也不會自動生成（見第 32 課 -3）。
//   後果是所有「移動」都會安靜退回深拷貝。要補齊就是第 34 課的五法則。
//
// 【注意事項 Pay Attention】
//   1. = 有兩種身分：初始化（拷貝建構）與賦值（拷貝賦值）。
//      看的是「左邊的物件存不存在」，不是看符號本身。
//   2. 按值傳參會完整深拷貝一次；只讀不改的參數請用 const&。
//   3. 情境 5 看不到拷貝是 NRVO 的結果，而 NRVO 是「可選」的最佳化，
//      不保證在所有編譯器上都發生。不要依賴拷貝建構函數裡的副作用。
//   4. 成員宣告順序決定初始化順序，務必與初始化列表一致（用 -Wreorder 檢查）。
//   5. 教材輸出不要印原始位址（ASLR 每次不同）——見【概念補充 C】。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】拷貝建構函數的觸發時機
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. SimpleString c = a; 和 e = a; 分別呼叫什麼？
//     答：前者呼叫「拷貝建構函數」，後者呼叫「拷貝賦值運算子」。
//         判準是左邊的物件存不存在：c 是新物件，這行是初始化；
//         e 已經存在，這行是賦值。同一個 = 符號，兩件不同的事。
//     追問：兩者實作上最大的差別是什麼？
//         → 拷貝賦值必須先處理掉自己原有的資源，因此需要自我賦值防護
//           （if (this == &other)）；拷貝建構的物件還是空的，沒有舊資源，
//           所以不需要。
//
// 🔥 Q2. 拷貝建構函數的參數為什麼一定要是 const 參考，不能傳值？
//     答：不能傳值是因為「為了建構參數就要再呼叫一次拷貝建構函數」，
//         而那次呼叫又要建構它的參數…… 無限遞迴，
//         所以標準規定必須是參考。
//         加 const 則讓它能接住暫時物件，也能拷貝 const 物件。
//     追問：那按值傳參給「一般函式」有什麼代價？
//         → 每次呼叫都完整深拷貝一次，函式結束再解構那份副本。
//           本檔情境 3 與情境 4 的對照就是證據：
//           傳值多了一次 [拷貝建構] 與一次 [解構]，傳 const& 完全沒有。
//
// ⚠️ 陷阱. 「情境 5 按值返回區域變數，一定會呼叫拷貝建構函數。」
//     答：不一定，本檔的輸出裡就完全沒有。
//         編譯器套用了 NRVO，直接把 local 建構在呼叫端 d 的位置上，
//         從頭到尾只有一個物件，沒有任何拷貝發生。
//         拷貝省略是標準明文允許「改變可觀察行為」的例外 ——
//         連拷貝建構函數裡的 std::cout 都會一起消失。
//     為什麼會錯：把「語意上有一次拷貝」當成「執行期一定有一次拷貝」。
//         標準描述的是語意，編譯器只要結果等價就能省略中間步驟。
//         真正的陷阱是反過來的：不能在拷貝建構函數裡放計數、log、
//         資源註冊這類「一定要發生」的副作用，因為它可能被合法地省略。
// ═══════════════════════════════════════════════════════════════════════════
//
// 【LeetCode 實戰範例】—— 本檔從缺，理由如下
//   本檔的內容是「拷貝建構函數在哪些語法情境下會被呼叫」，
//   屬於語言機制與物件生命週期的觀察。
//   LeetCode 只驗證演算法的輸入輸出與時間限制，
//   評測系統既不會拷貝你的物件，也無從觀察建構函數被呼叫幾次。
//   硬掛題號只會誤導，故從缺；改以「按值傳參 vs const& 傳參」
//   與「存進容器」這兩個天天寫到的情境呈現拷貝成本。
//
