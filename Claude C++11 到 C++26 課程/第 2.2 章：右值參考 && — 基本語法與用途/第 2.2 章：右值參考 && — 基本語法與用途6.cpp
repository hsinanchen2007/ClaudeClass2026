// =============================================================================
// 主題: 全章最重要的一條規則 —— 有名字的右值參考，本身是左值
// =============================================================================
//
// 【主題資訊 Information】
//   規則    ：參數型別是 T&&，但「該參數的名字」作為表達式時是 lvalue
//   語法    ：void f(T&& s) { g(s); g(std::move(s)); }  // 兩者選到不同重載
//   標準版本：右值參考 / std::move   C++11
//   標頭檔  ：<utility>（std::move）
//   複雜度  ：純編譯期的重載決議，執行期零成本
//   本檔宣告的標準：C++17
//
// 【詳細解釋 Explanation】
//
// 【1. 型別 ≠ 值類別（value category）】
//   這是整個移動語意最容易混淆的一點，本檔用最短的程式碼證明它：
//       void outer(std::string&& s) {
//           inner(s);              // s 的型別是 string&&，但表達式是 lvalue
//                                  //   → 選中 inner(const std::string&)
//           inner(std::move(s));   // std::move(s) 是 xvalue（右值）
//                                  //   → 選中 inner(std::string&&)
//       }
//   「型別」描述這個名字宣告成什麼；
//   「值類別」描述這個表達式在此處出現時的性質。兩者是獨立的兩件事。
//
// 【2. 為什麼標準要這樣設計：因為 s 可能被用很多次】
//   假設具名的 && 自動就是右值，那麼：
//       void outer(std::string&& s) {
//           consume(s);   // 若這裡自動被移動 → s 已被掏空
//           log(s);       // 這裡就會印出空字串（悄悄的 bug）
//       }
//   標準選擇「安全優先」：只要它有名字，預設就當左值（不會被偷走）。
//   要放棄它，你必須自己明寫 std::move —— 把責任交還給程式設計者。
//
// 【3. 實務準則：std::move 只寫在「最後一次使用」的地方】
//   判斷方法很簡單：問自己「這行之後，我還會讀取它嗎？」
//     * 還會用   → 不要 move
//     * 不會了   → 可以 move
//   本檔的 outer 先用 inner(s) 讀一次、最後才 inner(std::move(s)) 交出去，
//   正是這個準則的示範。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 同樣的規則適用於「所有」具名參考
//     不只函式參數，任何具名的右值參考變數都一樣：
//         std::string&& r = std::string("x");
//         inner(r);              // 仍然選 const T&，因為 r 有名字
//         inner(std::move(r));   // 這才是 T&&
//
// (B) 這條規則與 std::forward 的關係
//     在「模板」中我們無法預先知道引數原本是左值還是右值，
//     所以不能無條件 std::move（那會把左值也偷走）。
//     std::forward<T> 的作用正是「依 T 推導的結果決定要不要轉成右值」——
//     它是這條規則在泛型情境下的解法（見第 2.7 章完美轉發）。
//
// (C) 一個判斷口訣
//     「有名字 → 左值」。這條口訣涵蓋本章幾乎所有陷阱：
//     具名參數是左值、具名的 && 變數是左值、
//     而 std::move(x)、函式回傳值、字面值 42 這些沒有名字的才是右值。
//
// 【注意事項 Pay Attention】
//   1. 型別是 T&& 不代表表達式是右值；有名字就是左值。
//   2. 要把具名的 && 參數當右值傳下去，必須明寫 std::move。
//   3. std::move 只該寫在「最後一次使用」處，否則後續讀取會拿到被掏空的物件。
//   4. 模板中的 T&& 是轉發引用，該用 std::forward 而非 std::move。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】具名右值參考的值類別
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. void f(std::string&& s) 裡直接呼叫 g(s)，會選到 g 的哪個重載？
//     答：會選 g(const std::string&)。因為 s 有名字，它作為表達式是 lvalue，
//         即使它的型別是 std::string&&。
//         要選中 g(std::string&&) 必須寫 g(std::move(s))。
//     追問：為什麼標準要這樣設計？→ 安全。s 在函式內可能被使用多次，
//         若自動當右值，第一次使用就被掏空，後續全部讀到空物件。
//
// 🔥 Q2. 那什麼時候該在函式內對參數寫 std::move？
//     答：只在「這是它最後一次被使用」時。
//         判斷方式：這行之後我還會不會讀取它？會 → 不要 move；不會 → 可以 move。
//     追問：如果是模板參數 T&& 呢？→ 那是轉發引用，要用 std::forward<T>(x)，
//         因為引數原本可能是左值，無條件 move 會把呼叫端的物件偷走。
//
// ⚠️ 陷阱. 「函式參數宣告成 && 就代表傳進來的東西可以被我自由移動」對嗎？
//     答：一半對。它確實保證了「呼叫端傳的是右值、資源可以被拿走」，
//         但在函式內部，你必須自己寫 std::move 才會真的觸發移動 ——
//         直接使用參數名只會走複製路徑。
//     為什麼會錯：把「參數的型別」與「使用參數時的值類別」混為一談。
//         正確心智模型：&& 描述的是「我能接受什麼」，
//         而函式內要不要真的搬走，由你寫不寫 std::move 決定。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <utility>

// 參數不具名：本檔只關心「選中了哪個重載」，不需要參數的值
void inner(const std::string& /*s*/) { std::cout << "  inner: const T&\n"; }
void inner(std::string&& /*s*/)      { std::cout << "  inner: T&&\n"; }

void outer(std::string&& s) {
    // s 的「型別」是 std::string&&（右值參考）
    // 但 s 這個「表達式」是左值（有名字）

    std::cout << "直接傳 s:\n";
    inner(s);              // 呼叫 const T& 版本！因為 s 是左值

    std::cout << "傳 std::move(s):\n";
    inner(std::move(s));   // 呼叫 T&& 版本，因為 std::move(s) 是右值
}

int main() {
    outer(std::string("Hello"));
    return 0;
}

