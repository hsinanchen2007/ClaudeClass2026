// =============================================================================
//  第 2.2 章 之 1  —  右值參考 T&& 的基本宣告與綁定
// =============================================================================
//
// 【主題資訊 Information】
//   語法：   T&&  name = <rvalue expression>;
//   標準：   C++11 引入（本機以 -std=c++03 -pedantic-errors 實測會直接語法錯誤，
//            訊息為 "expected unqualified-id before '&&' token"）
//   標頭檔： 無 — 這是核心語言（core language）特性，不是函式庫
//   成本：   O(1)。綁定參考不複製、不配置記憶體，只是替既有物件取一個別名
//
// 【詳細解釋 Explanation】
//
// 【1. 為什麼 C++11 非得再發明一種參考不可？】
//   C++98 只有兩種參考：
//       T&        只能綁 lvalue          → 可讀可寫，但接不到臨時物件
//       const T&  lvalue / rvalue 都能綁 → 接得到臨時物件，但「唯讀」
//   注意這張表有個空缺：「能接臨時物件，而且可以改它」這一格是空的。
//
//   這個空缺正是移動語意（move semantics）的死結。假設有個函式收到一個
//   馬上就要被丟掉的 std::string 臨時物件，最有效率的做法是把它內部的
//   heap 指標「偷」過來，而不是逐位元組複製。但偷 = 修改來源（要把來源的
//   指標設成 nullptr，否則兩邊都會 delete 同一塊記憶體）。const T& 唯讀，
//   偷不了；T& 又根本綁不到臨時物件。
//
//   T&& 就是專門補這一格的：**只接右值，而且可寫**。它的本質不是「一種新的
//   參考」，而是「型別系統層級的一張許可證」——它向函式保證：這個物件的
//   來源端已經放棄它了，你可以放心把它掏空。
//
// 【2. 「型別是右值參考」和「運算式是右值」是兩件不同的事】
//   這是本章最容易翻車、也是面試最愛問的地方。看這一行：
//       int&& rref = 42;
//   rref 的**宣告型別**是 int&&（右值參考）。
//   但 rref 這個**運算式**的 value category 是 **lvalue**（左值）。
//
//   判準只有一句話：**凡是有名字、能取址的，運算式就是 lvalue。**
//   所以下面兩行都是合法的，而且正是本檔最後幾段示範的：
//       rref = 100;      // 可以指派 → 它是 lvalue
//       &rref;           // 可以取址 → 它是 lvalue
//   反過來說，你不能寫 `int&& r2 = rref;`，因為 rref 是 lvalue。
//   （若真的要，得寫 `int&& r2 = std::move(rref);`。）
//
// 【概念補充 Concept Deep Dive】
//
// (A) `int&& rref = 42;` 之後，那個 42 到底存在哪裡？
//   42 是一個 prvalue（純右值），它本身沒有身分、沒有位址。當它被拿去綁定
//   到一個參考時，會發生 **temporary materialization（臨時物件實體化）**：
//   編譯器產出一個真正的 int 臨時物件，把 42 放進去，再讓 rref 綁上它。
//   這個臨時物件的生命週期被**延長**到 rref 這個參考的作用域結束為止
//   （見下方 (B)）。所以 `&rref` 拿到的是那個實體化臨時物件的位址，
//   它活在 main 的 stack frame 上，完全合法可用。
//
// (B) 生命週期延長（lifetime extension）——以及它的邊界
//   把臨時物件**直接綁定**到一個參考時，臨時物件的壽命會被延長到該參考的
//   壽命：
//       const T& r = make();   // C++98 起就有這條規則
//       T&&      r = make();   // C++11 把同一條規則擴充到右值參考
//   本機以 -std=c++98 實測，const 版本可正常延長；而 T&& 版本在 C++98 下
//   根本無法編譯（&& 不存在），確認它確實是 C++11 才有的。
//
//   但這條規則有一個**不會穿透的邊界：函式回傳**。
//       const std::string& f() { return std::string("tmp"); }  // ✗ 不延長
//   臨時物件的壽命只延長到「那個參考本身」，而 f 內部的參考在 return 當下
//   就結束了，回傳出去的是一個指向已死物件的參考。GCC 會明確警告
//   "returning reference to temporary [-Wreturn-local-addr]"（本機實測）。
//   詳見本章第 8 個範例檔。
//
// (C) 為什麼 `const int& clref = 42;` 早在 C++98 就合法？
//   因為 const 承諾了「唯讀」，語言可以安全地讓它接臨時物件——你不能透過
//   它改變任何東西，也就不會出現「改了一個馬上要消失的東西」這種無意義的
//   操作。而 non-const 的 `int& r = 42;` 之所以被禁，正是因為它允許你寫入
//   一個下一刻就蒸發的物件，那幾乎必然是筆誤。
//
// 【注意事項 Pay Attention】
//   1. `int&& r = a;`（a 是具名變數）**不合法**——右值參考不接 lvalue。
//      要強制綁定必須寫 `int&& r = std::move(a);`（需 #include <utility>）。
//   2. 綁定完成後，rref 這個「變數」是 lvalue。這不是矛盾，是刻意設計，
//      目的是防止資源在函式內被意外重複偷走（詳見本章第 6 個範例檔）。
//   3. 本檔印出的位址（&rref）是**本機實測**值，受 ASLR 影響，
//      **每次執行都不同**，不要把它當成固定結果背下來。
//   4. std::move 只是一個 static_cast，它**不移動任何東西**，
//      只是把運算式的 value category 改標成 rvalue。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】右值參考的宣告與綁定
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. `int&& r = 42;` 這行之後，r 是左值還是右值？為什麼 `&r` 可以編譯？
//     答：r 的「型別」是 int&&，但 r 這個「運算式」是 **lvalue**。
//         判準是「有名字、可取址就是 lvalue」，與宣告型別無關。
//         所以 &r 合法，r = 100 也合法。
//     追問：那我可以寫 `int&& r2 = r;` 嗎？
//         → 不行，r 是 lvalue，右值參考接不了。必須 `int&& r2 = std::move(r);`。
//
// 🔥 Q2. C++98 已經有 `const T&` 能接臨時物件了，為什麼還要發明 `T&&`？
//     答：因為 const T& 是**唯讀**的。移動語意的本質是「把來源掏空」——
//         必須把來源的指標改成 nullptr，否則兩個物件會 double free。
//         const T& 改不了來源，T& 又綁不到臨時物件，中間缺一格，
//         T&& 就是補上「可接右值 **且** 可寫」這一格。
//     追問：那 T&& 除了移動，還能幹嘛？
//         → 搭配 template 的型別推導會變成 forwarding reference，
//           是完美轉發（perfect forwarding）的基礎。
//
// ⚠️ 陷阱. `int&& r = 42;` 之後那個 42 存在哪？r 是不是懸空（dangling）了？
//     答：沒有懸空。綁定時發生 temporary materialization，編譯器產出一個
//         真的 int 臨時物件，其生命週期被**延長**到 r 的作用域結束。
//         r 在整個作用域內都是安全可用的。
//     為什麼會錯：很多人腦中把 `42` 想成「一個立刻消失的字面值」，
//         推論出 r 綁完就懸空。實際上「綁定到參考」本身就會觸發生命週期延長，
//         這正是 const T& / T&& 能安全接住臨時物件的根本機制。
//         但要記住這條規則**不會穿透函式回傳**（見第 8 個範例檔）。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <utility>

int main() {
    // ===== 左值參考（複習）=====
    std::cout << "=== 1. 左值參考 T& ===\n";
    int a = 10;
    int& lref = a;        // OK：左值參考綁定到左值
    // int& lref2 = 42;   // 錯誤！左值參考不能綁定到右值
    lref = 20;            // 透過參考改動的就是 a 本身
    std::cout << "  lref = " << lref << "，a = " << a << "（同一個物件的兩個名字）\n";

    // ===== const 左值參考 =====
    std::cout << "\n=== 2. const 左值參考 const T& ===\n";
    const int& clref = 42; // OK：const 左值參考可以綁定右值（C++98 就有的規則）
    // clref = 1;          // 錯誤！const → 唯讀，這正是它「安全」的原因
    std::cout << "  clref = " << clref << "（綁在一個被延長壽命的臨時物件上）\n";

    // ===== 右值參考（C++11 新增）=====
    std::cout << "\n=== 3. 右值參考 T&& ===\n";
    int&& rref = 42;       // OK：右值參考綁定到右值
    // int&& rref2 = a;   // 錯誤！右值參考不能綁定到左值

    std::string&& sref = std::string("Hello");  // OK：綁定到臨時物件

    // 右值參考綁定後，會延長臨時物件的生命週期
    std::cout << "  rref  = " << rref  << "\n";   // 42
    std::cout << "  sref  = " << sref  << "\n";   // Hello

    // 右值參考本身是左值！（有名字、有地址）
    std::cout << "\n=== 4. 具名的右值參考，其運算式是「左值」===\n";
    rref = 100;            // 可以修改 → 證明它是 lvalue
    std::cout << "  rref  = " << rref  << "（可以指派 → 是 lvalue）\n";
    std::cout << "  &rref = " << &rref << "（可以取址 → 是 lvalue）\n";
    std::cout << "  ↑ 位址為本機實測值，受 ASLR 影響，每次執行都不同\n";

    // 想把一個 lvalue 綁到右值參考，唯一的方式是 std::move
    std::cout << "\n=== 5. 用 std::move 把 lvalue 轉成 rvalue 運算式 ===\n";
    int&& rref2 = std::move(a);   // std::move(a) 是 xvalue，屬於 rvalue
    rref2 = 55;                   // 改到的其實還是 a 本身
    std::cout << "  rref2 = " << rref2 << "，a = " << a << "\n";
    std::cout << "  ↑ std::move 沒有搬動任何東西，只是換一個 value category\n";

    return 0;
}

