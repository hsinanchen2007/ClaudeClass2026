// =============================================================================
// 主題: 臨時物件的生命週期延長 —— const T& 與 T&& 都做得到
// =============================================================================
//
// 【主題資訊 Information】
//   語法    ：const T& r = 產生臨時物件的表達式;   // 延長壽命，但唯讀
//             T&&      r = 產生臨時物件的表達式;   // 延長壽命，且可修改
//   標準版本：const T& 的生命週期延長   C++98
//             T&& 的生命週期延長        C++11
//   標頭檔  ：<string>
//   複雜度  ：綁定本身零成本（不產生複製）
//   本檔宣告的標準：C++17
//
// 【詳細解釋 Explanation】
//
// 【1. 預設規則：臨時物件在「完整表達式」結束時銷毀】
//       make_object();          // 產生的臨時物件在這個分號就銷毀了
//   本檔情境 1 示範這件事：建構完立刻解構，中間什麼都做不了。
//
// 【2. 例外：綁定到參考可以延長它的壽命】
//       const Verbose& ref  = make_object();   // 活到 ref 離開作用域
//       Verbose&&      rref = make_object();   // 同樣延長
//   規則的用意是讓「回傳暫時物件的函式」可以被安全地取用結果，
//   不必為了保命而多做一次複製。
//
// 【3. const T& 與 T&& 的差別：能不能改】
//   兩者都能延長壽命，但：
//       ref  = ...   ❌ 不行，const 禁止修改
//       rref = ...   ✅ 可以，這正是情境 3 示範的
//   這個「可修改」的能力，就是移動建構子必須用 T&&（而非 const T&）的原因 ——
//   移動的本質是「搬走資源後把來源設成空」，那需要修改來源。
//
// 【4. 解構順序：後建構的先解構】
//   情境 3 中 rref 被重新賦值時，會先產生「新物件」這個臨時物件、
//   賦值給 rref 所綁定的那個物件，然後該臨時物件立刻銷毀 ——
//   輸出中可以清楚看到這個過程。
//   而 ref 與 rref 綁定的物件則活到 main 結束才解構。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 生命週期延長「不會傳遞」
//     這條規則只在「臨時物件直接綁定到參考」時生效。一旦中間隔了一層，
//     延長就不會發生：
//         const std::string& f() { return std::string("x"); }  // ❌ 回傳即懸空
//         const std::string& r = f();                          // 救不回來
//     函式回傳的參考不受延長規則保護 —— 這是最常見的誤解，
//     也是本章第 8 個範例（懸空參考）要示範的問題。
//
// (B) 延長不等於「搬移」
//     ref 與 rref 綁定的仍然是「原本那個」臨時物件，沒有發生任何複製或移動，
//     只是它的解構被推遲了。這也是為什麼綁定本身是零成本的。
//
// (C) 為什麼 T&& 綁定臨時物件後「本身是左值」
//     rref 有名字，所以 rref 這個表達式是 lvalue。
//     要把它當右值傳給別人，仍須寫 std::move(rref)。
//     這與「它綁定的是臨時物件」無關 —— 判準永遠是「表達式有沒有名字」。
//
// 【注意事項 Pay Attention】
//   1. 生命週期延長只對「直接綁定」有效，不適用於函式回傳的參考。
//   2. const T& 延長後唯讀；T&& 延長後可修改，這是兩者唯一的實質差別。
//   3. rref 雖然型別是 T&&，但它有名字，所以表達式仍是 lvalue。
//   4. 延長不會複製或移動物件，只是推遲解構時機。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】臨時物件的生命週期延長
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. const T& 綁定臨時物件後，臨時物件何時銷毀？
//     答：延長到該參考本身離開作用域為止，而不是在原本的完整表達式結束時。
//         這是 C++98 就有的規則，目的是讓「回傳暫時物件的函式」可被安全取用，
//         不必為了保命多做一次複製。
//     追問：T&& 也有這個效果嗎？→ 有，而且還多了「可以修改它」的能力，
//         這正是移動建構子必須用 T&& 而非 const T& 的原因。
//
// 🔥 Q2. 生命週期被延長時，物件有被複製或移動嗎？
//     答：都沒有。參考綁定的仍是原本那個臨時物件，只是解構時機被推遲。
//         所以這個機制本身是零成本的。
//     追問：那為什麼不乾脆一律用 const T& 接所有回傳值？→ 可以且常見，
//         但若你要修改它、或要把資源移走，就必須用 T&&。
//
// ⚠️ 陷阱. const std::string& f() { return std::string("x"); } 的回傳值，
//          綁到 const string& r 上，生命週期會被延長嗎？
//     答：不會。生命週期延長只適用於「臨時物件直接綁定到參考」。
//         這裡臨時物件在 f() 內就已經結束生命，回傳的是懸空參考，
//         之後使用它屬未定義行為（其後果不固定，不可依賴任何一次觀察結果）。
//     為什麼會錯：把「延長」想成一種會沿著回傳值傳遞的屬性。
//         正確理解是：它是綁定當下的一次性規則，跨越函式邊界就失效。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>

class Verbose {
public:
    Verbose(const std::string& n) : name_(n) {
        std::cout << "  [建構] " << name_ << "\n";
    }
    ~Verbose() {
        std::cout << "  [解構] " << name_ << "\n";
    }
    const std::string& name() const { return name_; }
private:
    std::string name_;
};

Verbose make_object() {
    return Verbose("臨時物件");
}

int main() {
    std::cout << "--- 情境 1：沒有綁定，臨時物件立即銷毀 ---\n";
    make_object();  // 臨時物件在這行結束就銷毀
    std::cout << "  （繼續執行）\n\n";

    std::cout << "--- 情境 2：const T& 延長生命週期 ---\n";
    const Verbose& ref = make_object();
    std::cout << "  ref 仍然有效: " << ref.name() << "\n";
    std::cout << "  （ref 離開作用域前）\n\n";

    std::cout << "--- 情境 3：T&& 延長生命週期 ---\n";
    Verbose&& rref = make_object();
    std::cout << "  rref 仍然有效: " << rref.name() << "\n";
    rref = Verbose("新物件");  // 還可以修改！（const T& 做不到）
    std::cout << "  rref 修改後: " << rref.name() << "\n";
    std::cout << "  （rref 離開作用域前）\n";

    std::cout << "\n--- 離開 main ---\n";
    return 0;
}

