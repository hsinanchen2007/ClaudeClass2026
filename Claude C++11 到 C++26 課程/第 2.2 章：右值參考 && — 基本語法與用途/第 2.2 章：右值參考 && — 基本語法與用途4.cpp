// =============================================================================
//  第 2.2 章 之 4  —  重載決議：const T& 與 T&& 之間到底怎麼選
// =============================================================================
//
// 【主題資訊 Information】
//   語法：   void f(const T& s);   /   void f(T&& s);
//   標準：   T&& 多載為 C++11
//   標頭檔： std::move 需要 <utility>
//   成本：   決議完全發生在**編譯期**，執行期零額外開銷（沒有分支、沒有虛擬呼叫）
//
// 【詳細解釋 Explanation】
//
// 【1. 兩條規則就能解釋全部結果】
//   規則一：T&& **不接受 lvalue**。傳 lvalue 時它連候選名單都進不去。
//   規則二：實參是 rvalue 且兩者都可行時，**T&& 排名較高**，勝出。
//
//   套用到本檔三行呼叫：
//       f(s);                // s 是 lvalue → T&& 出局 → 只剩 const T&
//       f(std::move(s));     // xvalue（rvalue）→ 兩者皆可 → T&& 勝
//       f(std::string("x")); // prvalue（rvalue）→ 兩者皆可 → T&& 勝
//
//   為什麼 rvalue 時 T&& 會贏？因為綁到 const T& 必須多做一次
//   qualification conversion（把非 const 的東西當成 const 來看），
//   而綁到 T&& 不需要任何轉換。標準把「不需要轉換」排在「需要加 const」之前，
//   所以結果是**確定的、不會 ambiguous**。
//
// 【2. 少寫一個多載會發生什麼事——這才是重點】
//   * 只有 `f(const T&)`：所有呼叫都能編譯（它什麼都接），
//     但右值也走複製路徑。程式**正確但慢**，而且不會有任何警告。
//     C++98 的世界就長這樣。
//   * 只有 `f(T&&)`：傳 lvalue 會**編譯失敗**。這其實常常是好事——
//     它強迫呼叫端寫出 std::move，把「我放棄這個物件了」變成程式碼裡看得見的宣告。
//     unique_ptr 的移動建構子就是刻意只留右值版本。
//   * 兩個都有：呼叫端寫什麼，就自動走對應的最佳路徑。這是標準庫的做法。
//
//   所以「要不要補上 T&& 多載」不是風格問題，而是「這個型別的複製貴不貴」
//   的效能決策。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 重載決議的三個階段（理解「為什麼 lvalue 不會選到 T&&」）
//   1. 名稱查找（name lookup）：找出所有叫 f 的函式。
//   2. 篩選可行候選（viable functions）：實參**能不能**轉換成形參型別？
//      lvalue → std::string&& 是不可能的轉換，所以 T&& 版本在這一步就被淘汰，
//      **根本不參與比較**。這就是為什麼傳 lvalue 時不會出現 ambiguous。
//   3. 在可行候選中排名（ranking）：比較各參數的轉換序列優劣。
//      rvalue 綁 T&&（identity）優於 rvalue 綁 const T&（加 const）。
//
//   關鍵在於第 2 步和第 3 步是**分開**的。很多人以為「兩個都能接、
//   編譯器隨便挑一個」，其實 lvalue 的情況連比都沒得比。
//
// (B) 為什麼具名的 T&& 參數在函式內是 lvalue？（設計動機）
//   假設語言反過來設計，讓 T&& 參數在函式內也是 rvalue，那麼：
//       void f(std::string&& s) {
//           g(s);      // 若 s 是 rvalue，g 會把它掏空
//           h(s);      // 這裡 s 已經是空殼了，而你完全看不出來
//       }
//   資源會在你毫無察覺的情況下被第一個呼叫偷走。
//   因此語言選擇：**進來之後降級成 lvalue，想再往下傳就必須自己寫 std::move**。
//   把「移動」變成一個顯眼、必須手動打出來的動作，而不是隱形的預設行為。
//   代價是初學者常忘記寫 move 導致靜默退化成複製；但反過來的錯誤
//   （資源被意外偷走）遠比效能損失難除錯。
//
// (C) 這裡的 std::move 有沒有真的搬走 s？
//   沒有。本檔的 f 只是把參考綁上去、印一行字，從頭到尾沒有任何人接手 s 的資源。
//   所以 `f(std::move(s));` 執行完之後，s 仍然是 "test"（本檔會實際印出來驗證）。
//   **std::move 只是宣告「我允許你偷」，不是「你已經偷了」。**
//
// 【注意事項 Pay Attention】
//   1. 傳 lvalue 時 T&& 版本不是「輸了」，而是根本不可行、連候選都不是。
//   2. 只提供 const T& 的話，右值也會走複製 —— 正確但慢，且無任何警告。
//   3. 若同時提供 f(T&)、f(const T&)、f(T&&)，傳 non-const lvalue 會選 f(T&)。
//      多載愈多愈難推理，非必要不要三個都寫。
//   4. std::move 之後物件不一定被改變，取決於有沒有人真的接手（見上方 (C)）。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】重載決議
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. `void f(const std::string&)` 和 `void f(std::string&&)` 同時存在，
//         `std::string s; f(s);` 選哪個？為什麼不會 ambiguous？
//     答：選 const T&。因為 s 是 lvalue，而 T&& 無法綁定 lvalue，
//         在「篩選可行候選」階段就被淘汰，根本沒有進入排名比較。
//         沒有兩個候選在競爭，自然不會 ambiguous。
//     追問：那 `f(std::move(s))` 呢？
//         → 兩者都可行，但 rvalue 綁 T&& 不需轉換、綁 const T& 要加 const，
//           T&& 排名較高而勝出。
//
// ⚠️ 陷阱. 如果我只寫 `void f(const std::string&)`，`f(std::move(s))` 會編譯失敗嗎？
//     答：不會，編譯完全正常。const T& 什麼都接，包括右值。
//         結果是：**你以為在移動，實際上走的是複製路徑**，
//         而編譯器一個字都不會提醒你。
//     為什麼會錯：大家以為「寫了 std::move 就代表發生移動」。
//         std::move 只是把運算式標成 rvalue，
//         真正決定「複製還是移動」的是**有沒有一個右值多載去接它**。
//         沒有右值多載，std::move 就是一行昂貴的空話。
//
// ⚠️ 陷阱. `f(std::move(s));` 執行完，s 的內容還在嗎？
//     答：在。本檔的 f 只是綁定參考、印字，沒有任何人接手 s 的資源，
//         所以 s 仍然是 "test"（程式會實際印出來驗證）。
//     為什麼會錯：把 std::move 當成動作。它是「授權」不是「執行」；
//         真正掏空來源的是接手方的移動建構子／移動指派。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <utility>

void f(const std::string& s) { std::cout << "const T& 版本 ← \"" << s << "\"\n"; }
void f(std::string&& s)      { std::cout << "T&& 版本      ← \"" << s << "\"\n"; }

// 只有 const T& 的版本：什麼都接得到，但右值也走複製路徑（C++98 的世界）
void only_const_ref(const std::string& s) {
    std::cout << "only_const_ref 收到 \"" << s << "\"（即使傳右值也走這裡）\n";
}

int main() {
    std::cout << "=== 1. 兩個多載並存時的選擇 ===\n";
    std::string s = "test";

    f(s);                // const T&（s 是左值，T&& 不可行）
    f(std::move(s));     // T&&（兩者都可行，但 T&& 排名較高）
    f(std::string("x")); // T&&（同上）

    std::cout << "\n=== 2. std::move 只是授權，不是執行 ===\n";
    std::cout << "  上面呼叫過 f(std::move(s))，現在 s = \"" << s << "\"\n";
    std::cout << "  ↑ 內容還在！因為 f 只是綁定參考，沒有任何人接手 s 的資源\n";

    std::cout << "\n=== 3. 少了 T&& 多載會怎樣 ===\n";
    only_const_ref(s);                     // lvalue
    only_const_ref(std::move(s));          // rvalue 也照樣走 const T&，無警告
    only_const_ref(std::string("temp"));   // rvalue 也照樣走 const T&，無警告
    std::cout << "  ↑ 三行都編譯成功、都走同一條路：正確但慢，且完全沒有警告\n";

    return 0;
}

