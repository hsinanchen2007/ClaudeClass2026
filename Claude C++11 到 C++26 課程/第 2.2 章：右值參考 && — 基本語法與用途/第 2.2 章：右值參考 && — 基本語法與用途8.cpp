// =============================================================================
// 主題: 絕不要回傳區域變數的右值參考 —— 懸空參考與未定義行為
// =============================================================================
//
// 【主題資訊 Information】
//   錯誤寫法：std::string&& f() { std::string s = "x"; return std::move(s); }
//   正確寫法：std::string   f() { std::string s = "x"; return s; }
//   標準版本：右值參考 / std::move   C++11
//             保證的複製省略          C++17（讓「回傳值」更沒有理由多寫 move）
//   標頭檔  ：<string>、<utility>
//   複雜度  ：正確版本因 RVO/NRVO 通常是零次複製、零次移動
//   本檔宣告的標準：C++17
//
// 【詳細解釋 Explanation】
//
// 【1. 錯在哪裡：參考指向已經不存在的東西】
//   bad_function() 內的 local 是區域變數，配置在該函式的堆疊框（stack frame）上。
//   函式一返回，那個堆疊框就結束了，local 也隨之解構。
//   回傳的 std::string&& 指向的正是那塊已經失效的記憶體 ——
//   呼叫端拿到的是一個「指向廢墟的門把」。
//
// 【2. 為什麼「生命週期延長」救不了它】
//   很多人會想：「不是說綁定到參考會延長臨時物件壽命嗎？」
//   那條規則只適用於「臨時物件『直接』綁定到參考」，例如：
//       const std::string& r = std::string("x");   // ✅ 延長
//   但 local 不是臨時物件，它是具名的區域變數；
//   而且中間隔了一層「函式回傳」，延長規則完全不適用。
//   一句話：延長規則不會跨越函式邊界。
//
// 【3. std::move 在這裡幫了倒忙】
//   return std::move(local); 只是把 local 轉成 xvalue，
//   讓它「能夠」綁定到回傳型別 std::string&& 上 ——
//   換句話說，std::move 在這裡的唯一作用是「讓這段錯誤的程式碼通過編譯」。
//   它完全沒有延長任何東西的壽命。
//
// 【4. 正確作法：回傳「值」，讓編譯器做省略最佳化】
//       std::string good_function() { std::string local = "safe"; return local; }
//   這樣寫不但安全，而且通常更快：
//   編譯器可套用 NRVO（具名回傳值最佳化），直接在呼叫端的記憶體上建構 local，
//   連一次移動都不做。
//   ⚠️ 千萬不要寫成 return std::move(local); —— 那會阻止 NRVO，強迫多做一次移動。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼本檔把危險程式碼關在 #ifdef 後面
//     這段程式碼是「未定義行為」。UB 沒有標準規定的結果 ——
//     可能印出亂碼、可能印出空字串、可能剛好看起來正確、也可能直接崩潰，
//     而且結果會隨編譯器、最佳化等級、堆疊內容而變。
//     本機實測時它會直接 segfault，導致後面的 good_function 示範完全跑不到，
//     因此預設關閉。要親眼觀察請自行加上巨集重新編譯：
//         g++ -std=c++17 -DDEMONSTRATE_UB 本檔.cpp
//     請注意：無論你觀察到什麼結果，那都「不是」這段程式的行為定義，
//     只是這一次執行碰巧發生的事，不可依賴、也不可寫進文件當作結論。
//
// (B) 編譯器擋得住嗎？不完全
//     GCC 對這種直接的情況會給警告，本檔編譯時就會看到：
//         warning: reference to local variable ‘local’ returned [-Wreturn-local-addr]
//     這個警告是「刻意保留」的教材內容 —— 它正是我們要示範的錯誤，
//     不是程式缺陷（bad_function 只被定義、預設不被呼叫，故執行時不會觸發 UB）。
//     但要注意：只要多繞一層（例如經由另一個函式或條件分支回傳），
//     編譯器就可能偵測不到。靜態分析與警告是輔助，
//     不能取代對生命週期的正確理解。
//
// (C) 位址輸出為何每次執行都不同
//     本檔會印出區域變數的位址。這些數值取決於堆疊配置與位址空間隨機化（ASLR），
//     每次執行都不同；預期輸出中的位址僅為某一次執行的樣本，不具重現性。
//
// 【注意事項 Pay Attention】
//   1. 絕不要回傳區域變數的參考或右值參考 —— 那是懸空參考，使用它屬未定義行為。
//   2. 未定義行為沒有固定結果；不可把任何一次觀察到的現象當成「它的行為」。
//   3. 生命週期延長規則不跨越函式邊界，救不了回傳的參考。
//   4. 回傳區域變數時直接 return local;，不要加 std::move（會阻止 NRVO）。
//   5. 本檔輸出的位址每次執行都不同，屬非決定性內容。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】回傳區域變數的右值參考
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::string&& f() { std::string s = "x"; return std::move(s); } 有什麼問題？
//     答：s 是區域變數，函式返回時即銷毀，回傳的參考指向已失效的堆疊記憶體，
//         呼叫端使用它是未定義行為。
//         std::move 在這裡只是讓它「能編譯」，沒有延長任何壽命。
//     追問：那生命週期延長規則呢？→ 不適用。那條規則只在「臨時物件直接綁定
//         到參考」時生效，不會跨越函式邊界。
//
// 🔥 Q2. 正確寫法是什麼？會不會比較慢？
//     答：正確寫法是回傳值：std::string f() { std::string s = "x"; return s; }
//         不但不慢，通常更快 —— 編譯器可套用 NRVO，直接在呼叫端的記憶體建構，
//         連一次移動都省掉。
//     追問：那要不要寫 return std::move(s); 幫編譯器一把？→ 千萬不要。
//         那會阻止 NRVO，反而強迫多做一次移動建構。
//
// ⚠️ 陷阱. 這段 UB 程式在我的機器上跑出了正確的字串，是不是代表它其實沒問題？
//     答：不代表。未定義行為的意思是「標準不對結果做任何保證」，
//         而不是「一定會出錯」。那塊堆疊記憶體可能還沒被覆寫，
//         所以這次看起來正常；換個編譯器、換個最佳化等級，
//         或在它與下一次函式呼叫之間多做一件事，結果就可能完全不同。
//     為什麼會錯：把「這次沒出事」當成「這樣寫可以」。
//         UB 最危險的地方正是它常常「暫時能跑」，等到上線才以隨機的形式爆炸。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <utility>

// ===== 危險版本 =====
std::string&& bad_function() {
    std::string local = "danger";
    std::cout << "  函式內 local 的地址: " << &local << "\n";
    return std::move(local);
    // local 在這之後被解構
}

// ===== 安全版本 =====
std::string good_function() {   // 回傳值，不是參考
    std::string local = "safe";
    std::cout << "  函式內 local 的地址: " << &local << "\n";
    return local;  // 編譯器會套用 RVO 或移動建構
}

int main() {
    // ⚠️ 以下 bad_function 區塊是「刻意示範的未定義行為」（懸空參考）。
    //    它在本機實測會直接 segfault，導致後面的 good_function 示範完全跑不到，
    //    因此預設關閉。要親眼看它出事，請用下列指令編譯：
    //        g++ -std=c++17 -DDEMONSTRATE_UB 本檔.cpp
    //    未定義行為沒有「保證」的結果：可能印出亂碼、空字串、剛好正確，也可能當機。
#ifdef DEMONSTRATE_UB
    std::cout << "=== bad_function ===\n";
    std::string&& ref = bad_function();
    std::cout << "  回傳的參考指向地址: " << &ref << "\n";
    std::cout << "  嘗試讀取: \"" << ref << "\"\n";
    // ↑ 這就像打開一扇門，門後的房間已經被拆除了
    std::cout << "\n";
#endif

    std::cout << "=== good_function ===\n";
    std::string value = good_function();
    std::cout << "  value 的地址: " << &value << "\n";
    std::cout << "  讀取: \"" << value << "\"\n";  // 永遠安全

    return 0;
}

