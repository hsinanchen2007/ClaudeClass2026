// =============================================================================
//  第 2.8 章 範例 2  —  錯誤示範：違反 Rule of Five 造成 double free
// =============================================================================
//
// ⚠️ 本檔是「刻意示範未定義行為」的教學範例：違反 Rule of Five 造成 double free。
//    程式預期會異常結束，這是示範的目的，不是本檔的 bug。
//    未定義行為沒有保證的結果，只是 glibc 通常會偵測到並主動 abort
//    （本機實測：free(): double free detected in tcache 2，退出碼 134 = SIGABRT）。
//    ★ 未定義行為不保證任何特定結果：它可能 abort、可能靜默毀損堆積、
//      也可能「看起來正常」跑完。不同編譯器、最佳化等級、標準函式庫都可能不同。
//      本檔的下方輸出是「本機這一次執行的觀察」，不是語言標準的保證。
//
// 【主題資訊 Information】
//   標頭檔：<iostream>（僅用於觀察，原始最小示範不需要任何標頭檔）
//   本檔違反的規則：Rule of Three / Rule of Five
//     只要類別自訂了「解構子、複製建構子、複製賦值」三者之一，
//     通常三者都需要自訂（C++11 起再加上移動建構子與移動賦值，合稱 Rule of Five）。
//   編譯：可以正常編譯，-Wall -Wextra 也沒有任何警告——這正是問題所在。
//
// 【詳細解釋 Explanation】
//
// 【1. 錯誤的具體機制：淺拷貝（shallow copy）】
//   Broken 自訂了解構子（delete data_），卻沒有自訂複製建構子。
//   於是編譯器產生一個「隱式複製建構子」，它做的是逐成員複製：
//       b.data_ = a.data_;      // 只複製了「指標的值」，沒有複製指向的內容
//   結果是兩個物件的 data_ 指向同一塊堆積記憶體。
//   離開 main 時，b 先解構、delete 該記憶體；a 再解構、delete 同一塊——
//   同一個位址被釋放兩次，這就是 double free。
//
// 【2. 為什麼編譯器不警告？】
//   因為「兩個物件共用同一個指標」在語言層面完全合法——
//   shared_ptr、觀察者指標、intrusive 容器都靠這個。
//   編譯器無從得知 data_ 是「獨佔擁有」還是「借用觀察」。
//   所有權語意是設計者的意圖，不是型別系統能表達的資訊（除非你用
//   unique_ptr 把意圖寫進型別裡——那正是 Rule of Zero 的價值）。
//
// 【3. Rule of Three / Five / Zero 的關係】
//   Rule of Three（C++98）：解構子、複製建構子、複製賦值——需要一個就需要三個。
//   Rule of Five（C++11）：再加上移動建構子、移動賦值。
//   Rule of Zero（最推薦）：不要自己管理資源，改用 RAII 成員
//     （string / vector / unique_ptr / shared_ptr），
//     這樣五個特殊函式全部交給編譯器自動生成，而且必定正確。
//   本檔只要把 int* data_ 換成 std::unique_ptr<int>，
//   問題就從「執行期崩潰」變成「編譯期錯誤」——因為 unique_ptr 不可複製，
//   Broken b = a; 會直接編不過。把錯誤從執行期前移到編譯期，是最大的價值。
//
// 【4. 這個錯誤在真實專案裡的樣子】
//   本檔的 main 只有兩行，一眼就看得出問題。真實情境中，複製往往是隱含的：
//     * 把物件放進 std::vector（擴容時會複製）
//     * 用傳值方式傳參數或回傳
//     * 存進 std::map、當成 lambda 的複製捕捉
//   這些地方都不會出現 = 號，卻全部會呼叫複製建構子。
//   崩潰點也常常離「真正的錯誤」很遠——通常是在某次擴容或函式返回時。
//
// 【概念補充 Concept Deep Dive】
//   (A) 為什麼 glibc 偵測得到？現代 glibc 的 malloc 在釋放時會檢查
//       tcache（thread-local cache）與 chunk 的中繼資料。
//       同一個 chunk 被放進 tcache 兩次會被偵測到，於是主動 abort，
//       訊息即 "free(): double free detected in tcache 2"。
//       這是實作提供的保護，不是語言保證——關掉 tcache 或換 allocator 就未必偵測得到。
//   (B) 用工具偵測：Valgrind 與 AddressSanitizer 都能明確指出
//       double free 的釋放點與配置點。實務上這類 bug 應該靠工具找，
//       而不是等它在生產環境崩潰。
//   (C) 為什麼程式會 abort 而不是拋出例外？因為堆積毀損時，
//       程式的狀態已經不可信任，繼續執行只會造成更難追查的後果。
//       主動 abort 是最負責任的處理方式。
//
// 【注意事項 Pay Attention】
//   1. UB 沒有「一定會發生什麼」。本檔在本機觀察到 abort，
//      但換平台、換最佳化等級、換標準函式庫都可能不同。
//   2. -Wall -Wextra 對此完全沉默，不能依賴編譯器警告發現這類問題。
//   3. 崩潰位置通常不是錯誤的根源位置，除錯時要往「誰複製了它」回溯。
//   4. 正解不是「補上五個函式」，而是優先用 Rule of Zero 讓問題不存在。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】Rule of Five 與淺拷貝
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 這個類別只寫了解構子，為什麼會崩潰？
//     答：自訂解構子代表這個類別「擁有」某項資源，但複製建構子沒有跟著自訂，
//         編譯器產生的隱式版本做逐成員複製，只複製了指標值本身。
//         兩個物件因此指向同一塊記憶體，各自解構時都 delete 一次 → double free。
//     追問：為什麼編譯器不警告？
//         → 因為共用指標本身是合法的設計（觀察者、shared_ptr 都這樣）。
//           所有權是設計意圖，型別系統看不出來——除非你用 unique_ptr 寫進型別裡。
//
// 🔥 Q2. 最好的修法是什麼？補上五個特殊成員函式嗎？
//     答：優先選 Rule of Zero：把 int* 換成 std::unique_ptr<int>，
//         五個特殊函式全部由編譯器正確生成，而且 Broken b = a;
//         會變成編譯期錯誤（unique_ptr 不可複製），錯誤從執行期前移到編譯期。
//         只有在實作低階資源包裝（檔案描述符、OS handle）時才手寫 Rule of Five。
//
// ⚠️ 陷阱. 「我的程式碼裡沒有寫 = 號，所以不會發生複製」——為什麼危險？
//     答：複製最常發生在看不見 = 的地方：把物件 push_back 進 vector、
//         vector 擴容時搬移既有元素、以傳值方式傳參數或回傳、
//         存進 map、被 lambda 以複製方式捕捉。
//         這些全都會呼叫複製建構子，而崩潰點往往離錯誤根源很遠。
//     為什麼會錯：大家把「複製」與「賦值運算子」畫上等號，
//         但 C++ 的複製是一種「隱式發生的基礎操作」，
//         語言中大量的值語意情境都會觸發它——這正是 Rule of Five
//         之所以是規則、而不是建議的原因。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>

class Broken {
    int* data_;
public:
    Broken(int val) : data_(new int(val)) {}
    ~Broken() { delete data_; }
    // 只寫了解構子，其他四個都沒寫

    // 僅供觀察用：讓我們在不印出「位址」這種每次都不同的值的前提下，
    // 確定性地證明兩個物件確實共用同一塊記憶體。
    const int* raw() const { return data_; }
};

int main() {
    Broken a(42);
    Broken b = a;    // 淺拷貝！a.data_ 和 b.data_ 指向同一塊記憶體

    // 確定性觀察：不印位址（每次執行都不同），只印「是否相等」這個布林結果。
    // 用 std::endl 強制沖刷——程式即將 abort，緩衝區內容會來不及輸出。
    std::cout << "兩個物件的 data_ 指向同一塊記憶體嗎? "
              << (a.raw() == b.raw() ? "是" : "否") << std::endl;
    std::cout << "→ 這就是淺拷貝：接下來兩者解構時會各 delete 一次同一個位址"
              << std::endl;
}   // a 和 b 都 delete 同一個指標 → double free（未定義行為，實務上多半直接 abort）

