// ============================================================================
// 第 1.4 章：統一初始化 (Uniform Initialization) — 大括號初始化語法
// 綜合總結檔案 (summary.cpp)
// ============================================================================
//
// 【主題資訊 Information】
//   語法    ：T obj{...};        // 直接列表初始化
//             T obj = {...};     // 複製列表初始化
//             T obj{};           // 值初始化（歸零／呼叫預設建構子）
//   標準版本：統一初始化 {}                 C++11
//             std::initializer_list        C++11
//             聚合初始化擴充至含基底類別     C++17
//             指定初始化 .x = 1             C++20
//   標頭檔  ：<initializer_list>（明確使用 initializer_list 型別時）
//   複雜度  ：語法特性；聚合初始化為零成本，容器初始化視元素數而定
//   本檔宣告的標準：C++11
//
// 【詳細解釋 Explanation】
//
// 【1. C++11 之前有幾種初始化語法？答案是「太多種」】
//     int a = 5;              // 複製初始化
//     int b(5);               // 直接初始化
//     int arr[] = {1,2,3};    // 聚合初始化（只能用在陣列/struct）
//     std::vector<int> v;     // 預設建構
//     Widget w();             // ← 這其實是函式宣告，不是物件！
//   四種語法各有適用範圍、彼此不能互換，還附帶 Most Vexing Parse 這種陷阱。
//   C++11 的目標是：讓 {} 在所有情境都能用，這就是「統一（uniform）」的意思。
//
// 【2. {} 真正多做的兩件事】
//   統一語法只是表面，{} 有兩個「行為上」的差異：
//   (a) 禁止窄化轉換（narrowing conversion）
//         int x = 3.14;   // 靜默截斷成 3，沒有任何提示
//         int x{3.14};    // 編譯錯誤 — 在編譯期就攔截
//       這是 {} 最有價值的地方：把「資料悄悄失真」變成「編譯不過」。
//   (b) 永遠不會被解析成函式宣告
//         Widget w();     // 函式宣告（Most Vexing Parse）
//         Widget w{};     // 一定是物件
//
// 【3. 代價：initializer_list 建構子的絕對優先權】
//   重載決議對 {} 有一條特例規則：
//   只要類別有任何 initializer_list 建構子「可行」，它就勝過所有其他建構子，
//   即使其他建構子是更精確的匹配。
//       std::vector<int> v1(5, 10);   // (count, value) → 5 個 10
//       std::vector<int> v2{5, 10};   // initializer_list → 2 個元素 [5, 10]
//   這不是 bug，是為了讓 {} 的「列表語意」永遠可預測而付出的代價。
//   實務準則：想指定「大小/容量」用 ()，想指定「內容元素」用 {}。
//
// 【4. 值初始化 T{} 的意義】
//     int x{};           // 0（不是未定義值！）
//     std::string s{};   // 空字串
//     Widget w{};        // 呼叫預設建構子
//   對內建型別，區塊作用域中的 T x; 是「未初始化」（讀取即 UB），
//   而 T x{}; 保證歸零。這讓「忘記初始化」這類 bug 有了統一的預防寫法。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 聚合初始化的「部分指定」規則
//     struct Aggregate { int a; double b; std::string c; };
//     Aggregate agg{100};      // a=100，b 與 c 被「值初始化」→ 0.0 與 ""
//     沒有寫出來的成員不是垃圾值，而是保證被值初始化。
//     ※ 本檔在此處會產生 -Wmissing-field-initializers 警告，
//       這是刻意保留的：GCC 提醒你「有成員沒寫」，而我們正是要示範
//       「沒寫的那些會被值初始化」這條規則。警告本身即是教材的一部分，
//       不是缺陷（程式仍可正常編譯與執行）。
//
// (B) 什麼型別算「聚合（aggregate）」
//     C++11 的條件：沒有使用者提供的建構子、沒有 private/protected 非靜態成員、
//     沒有虛擬函式、沒有基底類別。
//     C++17 放寬為允許有 public 基底類別；C++20 又收緊了「使用者宣告」的定義。
//     一旦型別不是聚合，{} 就會走建構子重載決議，而不是逐成員初始化。
//
// (C) 窄化轉換的精確定義
//     標準列舉了哪些屬於窄化：浮點→整數、整數→浮點（若無法精確表示）、
//     長整數→短整數（若值不可表示）等。
//     關鍵例外：若是「編譯期常數且值可被目標型別精確表示」則不算窄化。
//         int  x{3.0};       // ❌ 錯誤：double → int 一律算窄化
//         char c{65};        // ✅ 可以：65 是常數且 char 表示得下
//         char c{300};       // ❌ 錯誤：300 超出 char 範圍
//
// (D) 為什麼 {} 沒有完全取代 ()
//     因為 initializer_list 的優先權規則讓兩者語意不同（見第 3 點）。
//     此外，對「只有 initializer_list 建構子」的型別，用 {} 就無法呼叫到別的建構子。
//     因此社群共識不是「一律用 {}」，而是
//     「預設用 {}，但需要呼叫特定建構子（尤其是大小/容量）時用 ()」。
//
// 【注意事項 Pay Attention】
//   1. vector<int> v(5,10) 與 v{5,10} 結果完全不同，不是寫法差異而是語意差異。
//   2. {} 禁止窄化轉換，這是它與 = 和 () 最重要的行為差別。
//   3. auto x{42}; 在 C++17 起是 int；C++11/14 是 initializer_list<int>（N3922）。
//      本檔為 C++11，故此處以 C++11 規則說明。
//   4. 聚合的部分初始化會把其餘成員值初始化（歸零），不是留下垃圾值。
//   5. 本檔刻意保留 2 個 -Wmissing-field-initializers 警告作為教材，非缺陷。
//   6. 空的 {} 對容器是「零個元素」；Widget w{}; 呼叫的是預設建構子，
//      不是 initializer_list 版本。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】統一初始化
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. std::vector<int> v1(5, 10); 和 v2{5, 10}; 分別產生什麼？
//     答：v1 呼叫 (count, value) 建構子 → 5 個元素、每個都是 10。
//         v2 因為 {} 讓 initializer_list 建構子取得絕對優先權 → 2 個元素 [5, 10]。
//     追問：那 v3{5}; 呢？→ 1 個元素 [5]，不是 5 個 0。
//         這正是為什麼「指定容器大小」必須用 ()。
//
// 🔥 Q2. {} 相對於 = 和 () 多做了什麼事？
//     答：兩件。(1) 禁止窄化轉換，把靜默的精度遺失變成編譯錯誤；
//         (2) 永遠不會被解析成函式宣告，因此可避開 Most Vexing Parse。
//     追問：既然更安全，為什麼不全部改用 {}？→ 因為它會改變重載決議：
//         對有 initializer_list 建構子的型別，語意完全不同。
//
// 🔥 Q3. int x{3.14}; 為什麼編譯不過，但 int x = 3.14; 可以？
//     答：{} 明確禁止窄化轉換，double → int 屬於窄化（會遺失小數部分）。
//         = 與 () 允許隱式轉換，只做靜默截斷（可能連警告都沒有）。
//     追問：char c{65}; 會不會也算窄化？→ 不會。65 是編譯期常數且 char
//         表示得下，標準對此有明確豁免；但 char c{300}; 就會編譯錯誤。
//
// ⚠️ 陷阱. Widget w{}; 和 Widget w(); 一樣嗎？
//     答：完全不同。Widget w(); 是「宣告一個回傳 Widget 的無參數函式」
//         （Most Vexing Parse），根本沒有建立物件；
//         Widget w{}; 才是建立物件並呼叫預設建構子。
//     為什麼會錯：直覺認為「() 裡沒東西 = 沒有參數 = 預設建構」，
//         但 C++ 文法規定：任何「可以被解析成宣告」的東西一律優先當成宣告。
//         這也是 {} 被引入的重要理由之一。
// ═══════════════════════════════════════════════════════════════════════════
//
// 編譯指令: g++ -std=c++11 -Wall -Wextra -o summary summary.cpp
//   註：本檔會出現 2 個 -Wmissing-field-initializers 警告，屬刻意示範
//       「聚合部分初始化」的教材內容，非程式缺陷。
// 說明: 本檔案彙整第 1.4 章所有核心觀念與範例，閱讀此檔即可完整複習。
