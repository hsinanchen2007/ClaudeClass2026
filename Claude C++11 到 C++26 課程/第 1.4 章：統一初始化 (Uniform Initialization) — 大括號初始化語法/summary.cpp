// ============================================================================
// 【前言】統一初始化 (Uniform Initialization) 概述
// ============================================================================
// C++11 引入了「大括號初始化」語法 {}，又稱為「統一初始化」或
// 「列表初始化 (List Initialization)」。
//
// 在 C++11 之前，初始化方式不統一：
//   - 基本型別用 =         例如: int x = 10;
//   - 類別用 ()             例如: Point p(1, 2);
//   - 陣列用 {}             例如: int arr[] = {1, 2, 3};
//   - 某些情境無法初始化    例如: STL 容器無法在宣告時直接給值
//
// C++11 的大括號初始化讓「所有」型別都可以用 {} 來初始化，語法一致。
// 同時還提供兩大安全保障：
//   1. 防止窄化轉換 (Narrowing Conversion)
//   2. 避免 Most Vexing Parse（最令人困擾的語法歧義）
// ============================================================================

// ============================================================================
// 【輔助類別定義】用於後續範例展示
// ============================================================================

// --- Point 類別：示範基本類別的大括號初始化 ---
class Point
{
public:
    int x;
    int y;

    // 預設建構子：使用大括號初始化成員
    Point() : x{0}, y{0}
    {
        std::cout << "  Point() 預設建構\n";
    }

    // 雙參數建構子：使用大括號初始化成員
    Point(int x_, int y_) : x{x_}, y{y_}
    {
        std::cout << "  Point(" << x << ", " << y << ") 建構\n";
    }

    void print() const
    {
        std::cout << "  Point(" << x << ", " << y << ")\n";
    }
};

// --- NumberList 類別：示範 initializer_list 建構子的優先順序 ---
// 當類別同時擁有「一般建構子」與「initializer_list 建構子」時，
// 使用 {} 會優先匹配 initializer_list 建構子。
class NumberList
{
    std::vector<int> data_;

public:
    // 一般建構子：指定大小和初始值
    NumberList(std::size_t size, int value)
        : data_(size, value)
    {
        std::cout << "  NumberList(size=" << size << ", value=" << value << ")\n";
    }

    // initializer_list 建構子：接收初始化列表
    NumberList(std::initializer_list<int> init)
        : data_(init)
    {
        std::cout << "  NumberList(initializer_list, size=" << init.size() << ")\n";
    }

    void print() const
    {
        std::cout << "  內容: ";
        for (int n : data_)
        {
            std::cout << n << " ";
        }
        std::cout << "\n";
    }
};

// --- 聚合類別 (Aggregate)：無使用者定義建構子、無私有成員 ---
// 聚合類別可以直接用 {} 按順序初始化成員。
struct Aggregate
{
    int a;
    double b;
    std::string c;
};

