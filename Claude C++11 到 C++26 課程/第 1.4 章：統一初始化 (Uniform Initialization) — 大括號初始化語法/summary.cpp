// ===== 【日常實務 1】用 {} 定義路由表 =====
//   共載入 5 條路由：
//     / → IndexHandler  [公開]
//     /login → LoginHandler  [公開]
//     /api/orders → OrdersHandler  [需驗證]
//     /api/profile → ProfileHandler  [需驗證]
//     /health → HealthHandler  [公開]
//   巢狀 {} 讓設定在程式碼裡長得像資料表，維護時一眼可讀。
//
// ===== 【日常實務 2】{} 防窄化保護設定值 =====
//   預設設定: port=8080, timeout=30, retries=3, verbose=false
//   若把 timeout 寫成 30.5：
//     ServerConfig{8080, 30.5, 3, false}  → 編譯錯誤（{} 擋下窄化）
//     int t = 30.5;                        → 靜默變成 30（錯誤被藏起來）
//   執行期值 7200 是否合法: false（超過 3600 上限）
//   執行期值 45 是否合法:   true → timeout=45
//   提醒：{} 只在編譯期常數時擋得住，執行期資料仍須自行檢查。
