// =============================================================================
// 主題: 移動建構子的四種觸發時機（以及哪些其實被編譯器省略了）
// =============================================================================
//
// 【主題資訊 Information】
//   四種時機：① std::move  ② 接收臨時物件  ③ 函式回傳值  ④ 容器操作
//   標準版本：移動建構子 / 右值參考        C++11
//             保證的複製省略                C++17（讓 ② 完全不呼叫移動）
//   標頭檔  ：<utility>（std::move）、<vector>
//   複雜度  ：移動 O(1)；被省略時成本為 0
//   本檔宣告的標準：C++17
//
// 【詳細解釋 Explanation】
//
// 【1. 四種時機的本質是同一件事】
//   移動建構子會被選中，唯一的條件是「初始化的來源是一個右值」。
//   四種時機只是產生右值的四種方式：
//       ① std::move(a)          → 把左值轉成 xvalue
//       ② Tracker("temp")       → 臨時物件本身就是 prvalue
//       ③ return t;             → 回傳值是 prvalue（但常被省略，見下）
//       ④ push_back(臨時物件)   → 同 ②
//
// 【2. 時機 ② 在 C++17 其實「不會」呼叫移動建構子】
//   Tracker c(Tracker("temp"));
//   C++17 起「保證的複製省略」規定：用 prvalue 初始化同型別物件時，
//   根本不存在中間的臨時物件 —— 直接就在 c 的位置建構。
//   所以輸出只會看到一次 [建構]，看不到 [移動]。
//   這比移動更快（成本為零），不是程式碼有問題。
//
// 【3. 時機 ③ NRVO：也常常看不到移動】
//   make_tracker() 內回傳具名的區域變數 t，編譯器可套用 NRVO
//   （具名回傳值最佳化），直接在呼叫端的記憶體上建構 t。
//   ⚠️ NRVO 是「允許但不強制」的最佳化（不同於 ② 的強制省略），
//      所以不同編譯器/最佳化等級的輸出可能不同。
//      本檔的預期輸出是本機 GCC 15.2 於 -O0 的實測結果。
//   ⚠️ 絕對不要為了「幫編譯器」而寫 return std::move(t); ——
//      那反而會阻止 NRVO，強迫真的做一次移動。
//
// 【4. 時機 ④ 為什麼要先 reserve】
//   本檔在 push_back 前呼叫了 vec.reserve(2)。
//   若不 reserve，第二次 push_back 觸發擴容時，vector 會把既有元素
//   搬到新緩衝區 —— 那會產生額外的移動，讓輸出變得難以對照。
//   reserve 讓示範聚焦在「push_back 本身的移動」上。
//
// 【概念補充 Concept Deep Dive】
//
// (A) 為什麼 Tracker 的移動建構子要改寫 label_
//     本檔的移動建構子刻意在標籤後面加上 "(moved)"、
//     並把來源設成 "(empty)"，是為了讓輸出「看得見」發生了什麼。
//     真實程式碼不會這樣做 —— 移動建構子應該只搬資源、不做額外加工。
//     這裡是為了教學而刻意加的觀察點。
//
// (B) 被移動後的來源：本檔為何是 "(empty)" 而非空字串
//     這是本類別「自己決定」的移動後狀態。
//     標準對 std::string 只保證「有效但未指定」，
//     但對「你自己寫的類別」，移動後的狀態完全由你的移動建構子決定 ——
//     你有責任讓它處於一個可安全解構、可重新賦值的狀態。
//
// (C) 解構順序
//     區域物件依「建構順序的反序」解構。
//     vector 內的元素則在 vector 解構時一併銷毀。
//     輸出末尾可以清楚看到這個順序。
//
// 【注意事項 Pay Attention】
//   1. C++17 起，用 prvalue 初始化同型別物件會「保證省略」，連移動都不會發生。
//   2. NRVO 是允許但不強制的最佳化，不同編譯器/最佳化等級可能有不同輸出。
//   3. 絕不要寫 return std::move(區域變數); —— 那會阻止 NRVO，反而變慢。
//   4. 本檔在移動建構子內修改標籤只是為了讓輸出可觀察，真實程式碼不應如此。
//   5. push_back 前先 reserve，避免擴容產生額外的移動干擾示範。
//
// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】移動建構子的觸發時機
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. Tracker c(Tracker("temp")); 會呼叫幾次建構子？
//     答：在 C++17 起只會呼叫「一次」一般建構子，不會呼叫移動建構子。
//         因為「保證的複製省略」規定：用 prvalue 初始化同型別物件時，
//         根本不會產生中間的臨時物件，直接就在 c 的位置建構。
//     追問：那 C++11/14 呢？→ 標準「允許」但不強制省略，
//         所以理論上可能看到一次移動；實務上主流編譯器早就都會省略。
//
// 🔥 Q2. 函式回傳區域變數時，要不要寫 return std::move(t); ？
//     答：不要。直接 return t; 讓編譯器套用 NRVO，
//         可以直接在呼叫端的記憶體建構，一次搬移都不做。
//         寫了 std::move 反而會阻止 NRVO，強迫真的執行一次移動建構。
//     追問：為什麼 std::move 會阻止 NRVO？→ 因為 NRVO 的前提是
//         「回傳的就是那個具名區域變數本身」；
//         加上 std::move 之後回傳的是一個 xvalue 表達式，不再符合條件。
//
// ⚠️ 陷阱. 我在輸出裡沒看到 [移動]，是不是移動建構子沒被正確定義？
//     答：不一定。更可能是編譯器把它「省略」了 ——
//         C++17 對 prvalue 初始化是強制省略，NRVO 則是允許的最佳化。
//         省略比移動更快（成本為零），是好事而不是問題。
//     為什麼會錯：把「看到移動建構子被呼叫」當成正確性的指標。
//         正確的驗證方式是用 std::is_nothrow_move_constructible<T>
//         在編譯期檢查，而不是數輸出行數。
// ═══════════════════════════════════════════════════════════════════════════

#include <iostream>
#include <string>
#include <vector>
#include <utility>

class Tracker {
    std::string label_;
public:
    explicit Tracker(const std::string& l) : label_(l) {
        std::cout << "  [建構] " << label_ << "\n";
    }
    Tracker(const Tracker& o) : label_(o.label_ + "(copy)") {
        std::cout << "  [複製] " << label_ << "\n";
    }
    Tracker(Tracker&& o) noexcept : label_(std::move(o.label_)) {
        label_ += "(moved)";
        o.label_ = "(empty)";
        std::cout << "  [移動] " << label_ << "\n";
    }
    ~Tracker() {
        std::cout << "  [解構] " << label_ << "\n";
    }
    const std::string& label() const { return label_; }
};

Tracker make_tracker() {
    Tracker t("factory");
    return t;  // 可能觸發 NRVO 或移動
}

int main() {
    std::cout << "--- 時機 1：std::move ---\n";
    Tracker a("A");
    Tracker b(std::move(a));  // 移動建構

    std::cout << "\n--- 時機 2：接收臨時物件 ---\n";
    Tracker c(Tracker("temp"));  // 可能被 RVO 省略，也可能移動

    std::cout << "\n--- 時機 3：函式回傳值 ---\n";
    Tracker d = make_tracker();  // NRVO 或移動

    std::cout << "\n--- 時機 4：容器操作 ---\n";
    std::vector<Tracker> vec;
    vec.reserve(2);
    vec.push_back(Tracker("V1"));          // 移動（臨時物件）
    vec.push_back(std::move(b));           // 移動（明確轉換）

    std::cout << "\n--- 離開 main ---\n";
    return 0;
}

