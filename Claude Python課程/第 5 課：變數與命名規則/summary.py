# 【面試題】動態型別、強型別與記憶體管理
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. Python 是動態型別還是靜態型別？強型別還是弱型別？
#     答：**動態型別 + 強型別**。動態＝變數不需宣告型別、可重新綁定不同型別；
#     強型別＝不做跨型別的隱式轉換。實測 `1 + '1'` →
#     `TypeError: unsupported operand type(s) for +: 'int' and 'str'`
#     （JavaScript 會給 `'11'`，那才叫弱型別）。
#     追問：那 `1 + True` 為什麼可以？（bool 是 int 的子類，不是弱型別）
#
# 🔥 Q2. Python 如何管理記憶體？
#     答：物件放在直譯器管理的私有堆（private heap）。CPython 主要靠
#     **參考計數**即時回收，另有**分代垃圾回收器**處理循環參考。
#     注意：參考計數是 CPython 的實作策略，不是語言規範。
#     追問：`del x` 會刪掉物件嗎？（不會，只解除名稱綁定、計數減一，歸零才回收）
#
# ⚠️ 陷阱. 變數命名可以用中文嗎？該用嗎？
#     答：語法上**可以**（Python 3 識別字支援 Unicode），但實務上不建議——
#     跨平台編碼、輸入法切換、與多數團隊規範衝突。
#     為什麼會錯：把「不會報錯」直接當成「這樣寫沒問題」，
#     忽略了可維護性與協作成本這種語法檢查不出來的代價。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】什麼是變數？
# 變數 = 用來儲存資料的容器，用 = 賦值。
# ============================================================

name = "小明"          # 儲存文字（字串 str）
age = 18               # 儲存整數（int）
height = 175.5         # 儲存小數（浮點數 float）
is_student = True      # 儲存布林值（bool）

print(name)            # 輸出：小明
print(age)             # 輸出：18

# ============================================================
# 【重點 2】使用變數的好處 — 可讀性 & 可維護性
# 只需修改變數值，所有引用該變數的地方都會自動更新。
# ============================================================

# --- 不用變數（重複寫名字，改名要改多處） ---
print("學生姓名：陳小明")
print("陳小明 的成績是 95 分")

# --- 用變數（只需改一處） ---
name = "陳小明"
print("學生姓名：", name)
print(name, "的成績是 95 分")

# ============================================================
# 【重點 3】變數可重新賦值
# 變數的值可以隨時更改。
# ============================================================

score = 60
print("原本的分數：", score)          # 60
score = 85
print("補考後的分數：", score)        # 85

# ============================================================
# 【重點 4】用變數做計算
# ============================================================

a = 10
b = 3
print(a + b)            # 13 — 加法
print(a - b)            # 7  — 減法
print(a * b)            # 30 — 乘法
print(a / b)            # 3.333... — 除法

# --- 儲存計算結果 ---
price = 100
quantity = 5
total = price * quantity
print("總價：", total)   # 總價：500

# --- 自我累加 ---
count = 10
count = count + 1        # 取出舊值 + 1，再存回去
print(count)             # 11

# ============================================================
# 【重點 5】多變數同時賦值
# ============================================================

# --- 方法一：一行同時賦值多個不同值 ---
a, b, c = 1, 2, 3
print(a, b, c)           # 1 2 3

# --- 方法二：多個變數同時設為相同值 ---
x = y = z = 0
print(x, y, z)           # 0 0 0

# ============================================================
# 【重點 6】變數交換
# ============================================================

a = 5
b = 10

# --- 方法一：傳統做法（用暫存變數 temp） ---
temp = a
a = b
b = temp

# --- 方法二：Python 特有寫法（一行搞定） ---
a, b = b, a              # ★ 推薦！
print("a =", a, "b =", b)

# ============================================================
# 【重點 7】命名規則
# 1. 只能包含字母、數字、底線（_），不能以數字開頭
# 2. 不能使用 Python 保留字（if, for, while 等）
# 3. 區分大小寫（name ≠ Name ≠ NAME）
# 4. 應具描述性，讓人一眼看出用途
# ============================================================

# ✅ Python 推薦風格：snake_case（小寫 + 底線）
my_name = "小明"
student_age = 18
total_score = 95
is_passed = True

# ❌ 不好的命名
a = 18                   # 看不出是什麼
the_total_price_of_all_items_in_the_shopping_cart = 500  # 太長

# ✅ 適中的命名
age = 18
cart_total = 500

# 駝峰式（camelCase）—  JavaScript 常用
myName = "小明"

# 帕斯卡式（PascalCase）— Python 用於類別名稱
MyClassName = "SomeClass"

# ============================================================
# 【重點 8】⚠️ 印出變數名 vs 印出變數值
# ============================================================

name = "小明"
print("name")            # 印出文字 "name"（字串）
print(name)              # 印出變數值 "小明"

# ============================================================
# 【重點 9】type() — 查看變數的資料型別
# ============================================================

name = "小明"
age = 18
height = 175.5
is_student = True

print(type(name))        # <class 'str'>
print(type(age))         # <class 'int'>
print(type(height))      # <class 'float'>
print(type(is_student))  # <class 'bool'>

# ============================================================
# 小結：
# 1. 變數用 = 賦值，可儲存字串、數字、布林值等
# 2. 變數可重新賦值、可做計算
# 3. a, b, c = 1, 2, 3 同時賦值，a, b = b, a 一行交換
# 4. 命名用 snake_case，有描述性，不能以數字開頭
# 5. type() 可查看變數的資料型別
