# Python 中最基本、最常用的函數。
# 用來在螢幕上顯示文字或資料。

# ============================================================
# 1.1 基本輸出
# ============================================================

print("Hello World")                          # 印出字串
print(123)                                    # 印出整數
print(3.14)                                   # 印出浮點數
print(True)                                   # 印出布林值
print(None)                                   # 印出 None

# 每個 print 結尾自動換行
print("第一行")
print("第二行")
# 輸出：
# 第一行
# 第二行

# ============================================================
# 1.2 多個參數（預設用空格分隔）
# ============================================================

print("姓名", "小明", "年齡", 18)
# 輸出：姓名 小明 年齡 18

print("1 + 1 =", 1 + 1)                      # 1 + 1 = 2（可放表達式）

# ============================================================
# 1.3 sep — 自訂分隔符（預設空格）
# ============================================================

print("A", "B", "C", sep="-")                # A-B-C
print("A", "B", "C", sep="★")               # A★B★C
print("A", "B", "C", sep="")                 # ABC（無分隔）
print("A", "B", "C", sep="\n")               # 每個佔一行

# 實用：印日期
print(2025, 3, 1, sep="/")                   # 2025/3/1

# ============================================================
# 1.4 end — 改變結尾（預設換行 \n）
# ============================================================

print("Hello", end=" ")
print("World")                                # Hello World（同一行）

print("載入中", end="")
print("...", end="")
print("完成！")                                # 載入中...完成！

# sep + end 組合
print("蘋果", "香蕉", "橘子", sep="、", end="。\n")
# 輸出：蘋果、香蕉、橘子。

# ============================================================
# 1.5 轉義字元
# ============================================================

print("第一行\n第二行")                         # \n → 換行
print("姓名\t年齡")                             # \t → Tab（對齊用）
print("C:\\Users\\Documents")                   # \\ → 顯示反斜線
print('It\'s OK')                               # \' → 單引號
print("他說：\"你好\"")                          # \" → 雙引號

# ============================================================
