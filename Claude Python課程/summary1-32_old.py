# 十五、字典 dict                                 【第 29~30 課】
# ════════════════════════════════════════════════════════════════

# 有序(3.7+) ‧ key 不重複 ‧ 可變
d = {"name": "小明", "age": 18}
d["name"]                                     # "小明"（[] 存取）
d.get("score", 0)                             # 0（★ 安全存取）
"name" in d                                   # True（⚠️ 檢查的是 key）

d["city"] = "台北"                              # 新增/修改
