# 【重點 4】處理二維資料（串列的串列）
# ============================================================

scores = [
    [85, 72, 90],        # 小明的成績
    [68, 95, 78],        # 小華的成績
    [92, 88, 85],        # 小美的成績
]
names = ["小明", "小華", "小美"]
subjects = ["國文", "英文", "數學"]

for i in range(len(names)):              # 外層：每個學生
    print(f"\n{names[i]} 的成績：")
    student_total = 0
    for j in range(len(subjects)):       # 內層：每個科目
        print(f"  {subjects[j]}：{scores[i][j]} 分")
        student_total += scores[i][j]
    average = student_total / len(subjects)
    print(f"  → 平均：{average:.1f} 分")

# ============================================================
# 【重點 5】⚠️ break 在巢狀迴圈中只跳出「內層」
# ============================================================

for i in range(1, 4):
    print(f"外層 i = {i}")
    for j in range(1, 4):
        if j == 2:
            break                        # ★ 只跳出內層迴圈！
        print(f"  內層 j = {j}")
    print(f"  （內層結束）")             # 外層繼續執行
# 每次外層執行時，內層只印 j=1，j=2 時 break

