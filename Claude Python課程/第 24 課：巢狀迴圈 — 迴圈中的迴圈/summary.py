# ============================================================
# 【重點 1】巢狀迴圈基本概念
# 外層迴圈每執行一次，內層迴圈就完整跑一遍。
# 總執行次數 = 外層次數 × 內層次數
# ============================================================

for floor in range(1, 4):            # 外層：1~3 樓
    for room in range(1, 5):         # 內層：1~4 號房
        print(f"  {floor} 樓 {room} 號房")
    print(f"--- {floor} 樓巡完了 ---\n")
# 外層 3 次 × 內層 4 次 = 12 次

# ============================================================
# 【重點 2】★ 九九乘法表 — 巢狀迴圈的經典範例
# ============================================================

# --- 完整版（9×9） ---
for i in range(1, 10):               # 被乘數 1~9
    for j in range(1, 10):           # 乘數 1~9
        print(f"{i}×{j}={i*j:2d}", end="  ")
    print()                          # 每列結束換行

# --- 三角形版（j 的上限隨 i 變化） ---
for i in range(1, 10):
    for j in range(1, i + 1):        # j 只到 i
        print(f"{i}×{j}={i*j:<2d}", end="  ")
    print()

