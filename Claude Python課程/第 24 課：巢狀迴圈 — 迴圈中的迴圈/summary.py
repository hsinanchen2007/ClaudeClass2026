# 【重點 6】★ 跳出所有迴圈的方法
# ============================================================

# --- 方法 1：用旗標變數 ---
found = False

for i in range(5):
    for j in range(5):
        if i == 2 and j == 3:
            print(f"找到目標！位置 ({i}, {j})")
            found = True
            break                    # 跳出內層
    if found:
        break                        # 跳出外層

# --- 方法 2：包在函數裡用 return（更優雅） ---
def search():
    for i in range(5):
        for j in range(5):
            if i == 2 and j == 3:
                return (i, j)        # return 直接結束整個函數
    return None

result = search()
print(f"找到位置：{result}")

# ============================================================
# 【重點 7】continue 在巢狀迴圈中只影響「內層」
# ============================================================

for i in range(3):
    for j in range(3):
        if i == j:
            continue                 # 跳過 i == j 的組合
        print(f"({i},{j})", end=" ")
    print()
# 輸出：
# (0,1) (0,2)
# (1,0) (1,2)
# (2,0) (2,1)

# ============================================================
# 【重點 8】while 巢狀迴圈
# ============================================================

i = 1
while i <= 3:
    j = 1
    while j <= 3:
        print(f"({i},{j})", end=" ")
        j += 1
    print()
    i += 1
# (1,1) (1,2) (1,3)
# (2,1) (2,2) (2,3)
# (3,1) (3,2) (3,3)

