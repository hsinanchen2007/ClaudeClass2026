# 🔥 Q1. 為什麼 `0.1 + 0.2` 不等於 `0.3`？
#     答：`float` 是 IEEE 754 雙精度**二進位**浮點，0.1 與 0.2 在二進位下
#     都是無限循環，只能存近似值。實測 `0.1 + 0.2` → `0.30000000000000004`。
#     追問：那要怎麼比較？（`math.isclose()`；金額請用 `decimal.Decimal`，
#     而且要用字串建構 `Decimal('0.1')`，否則誤差在建構時就已經產生了）
#
# 🔥 Q2. `divmod()` 有什麼用？
#     答：一次回傳商與餘數的 tuple，`divmod(7, 2)` → `(3, 1)`，
#     等價於 `(7//2, 7%2)` 但只做一次除法。
#     常用在時間換算、進位轉換這類同時需要兩個結果的場合。
#     追問：對負數呢？（一樣遵循 floor 語意，`divmod(-7,2)` → `(-4, 1)`）
#
# ⚠️ 陷阱. `2 ** 3 ** 2` 等於多少？
#     答：實測 **512**，不是 64。`**` 是 Python 中極少數的**右結合**運算子，
#     所以它等於 `2 ** (3 ** 2)` = `2 ** 9`。
#     為什麼會錯：預設所有運算子都左結合。
#     另一個同源陷阱：`-2 ** 2` 實測是 **-4**，因為 `**` 的優先序**高於**
#     一元負號，等於 `-(2 ** 2)`。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】加法 +
# ============================================================

print(10 + 3)               # 13
print(1.5 + 2.3)            # 3.8
print(10 + 3.0)             # 13.0 ← int + float → float

# --- 字串「相加」是拼接，不是數學加法 ---
print("Hello" + " " + "World")  # Hello World

# --- ⚠️ 數字和字串不能相加 ---
# print(10 + "3")            # ❌ TypeError!
print(10 + int("3"))         # ✅ 13

# ============================================================
# 【重點 2】減法 -
# ============================================================

print(10 - 3)               # 7
print(3 - 10)               # -7（可以得到負數）

# --- 負號也是用 - ---
x = -5
print(-x)                   # 5（負負得正）

# ============================================================
# 【重點 3】乘法 *
# ============================================================

print(10 * 3)               # 30
print(2.5 * 4)              # 10.0（有 float → 結果是 float）

# --- 字串「相乘」是重複 ---
print("哈" * 3)              # 哈哈哈
print("-" * 30)              # ----- 畫分隔線
print("Go! " * 3)           # Go! Go! Go!
# print("Hi" * 2.5)         # ❌ TypeError! 字串只能乘以整數

# ============================================================
# 【重點 4】除法 /（永遠回傳 float！）
# ============================================================

print(10 / 3)               # 3.3333333333333335
print(10 / 2)               # 5.0 ← 即使整除，結果也是 float！
print(type(10 / 2))         # <class 'float'>

# --- ⚠️ 除以 0 會報錯 ---
# print(10 / 0)             # ❌ ZeroDivisionError

# ============================================================
# 【重點 5】整除 //（地板除法）
# 砍掉小數部分，得到整數結果。
# ============================================================

print(10 // 3)              # 3（3.33 → 砍掉小數 → 3）
print(7 // 2)               # 3（3.5 → 砍掉小數 → 3）

# --- ⚠️ 負數的整除：往更小的方向取整 ---
print(-7 // 2)              # -4（不是 -3！-3.5 往下取整 → -4）
print(-10 // 3)             # -4（-3.33 往下取整 → -4）

# --- 有 float 參與，結果是 float（但沒小數） ---
print(10.0 // 3)            # 3.0
print(7 // 2.0)             # 3.0

# ============================================================
# 【重點 6】取餘數 %
# ============================================================

print(10 % 3)               # 1（10 ÷ 3 = 3 餘 1）
print(7 % 2)                # 1（7 ÷ 2 = 3 餘 1）
print(8 % 4)                # 0（整除，無餘數）
print(3 % 5)                # 3（3 ÷ 5 = 0 餘 3）

# --- 驗證：商 × 除數 + 餘數 = 被除數 ---
a, b = 10, 3
print(b * (a // b) + a % b) # 10 ✅

# --- ★ 判斷奇偶數 ---
print(7 % 2)                # 1 → 奇數
print(8 % 2)                # 0 → 偶數

# --- ★ 判斷能否整除 ---
print(15 % 5)               # 0 → 可以整除 ✅
print(17 % 5)               # 2 → 不能整除 ❌

# --- 讓數字在 0~2 循環：0, 1, 2, 0, 1, 2... ---
for i in range(9):
    print(i % 3, end=" ")   # 0 1 2 0 1 2 0 1 2
print()

# --- 取出數字的某幾位數 ---
num = 12345
print(num % 10)             # 5（取個位數）
print(num % 100)            # 45（取最後兩位數）

# ============================================================
# 【重點 7】指數 **
# ============================================================

print(2 ** 3)               # 8（2³ = 2×2×2）
print(5 ** 2)               # 25（5²）
print(3 ** 0)               # 1（任何數的 0 次方 = 1）

# --- 開根號 ---
print(9 ** 0.5)             # 3.0（√9 = 3）
print(27 ** (1/3))          # 3.0（∛27 = 3）

# --- 負的次方 ---
print(2 ** -1)              # 0.5（2⁻¹ = 1/2）
print(2 ** -3)              # 0.125（2⁻³ = 1/8）

# --- 複利計算 ---
principal = 10000           # 本金
rate = 0.05                 # 年利率 5%
years = 10
result = principal * (1 + rate) ** years
print(f"10 年後：{result:.2f} 元")  # 16288.95 元

# ============================================================
# 【重點 8】int 與 float 的混合規則
# 只要有一邊是 float，結果就是 float
# 唯一例外：/ 永遠回傳 float
# ============================================================

print(10 + 3)               # 13   — int + int = int
print(10 + 3.0)             # 13.0 — int + float = float
print(10 / 2)               # 5.0  — / 永遠回傳 float

# ============================================================
# 【重點 9】⚠️ 浮點數精度問題
# ============================================================

print(0.1 + 0.2)            # 0.30000000000000004 ← 不是 0.3！
print(0.1 + 0.2 == 0.3)     # False！

# --- 解決方式 ---
print(round(0.1 + 0.2, 1))               # 0.3 ✅
print(round(0.1 + 0.2, 10) == 0.3)       # True ✅

# ============================================================
# 【重點 10】⚠️ 字串的 + 和 * 不是數學運算！
# ============================================================

print("3" + "4")            # "34"（拼接，不是 7！）
print("3" * 4)              # "3333"（重複，不是 12！）

# ============================================================
# 【重點 11】實作範例
# ============================================================

# --- 時間轉換器 ---
total_seconds = 3725
hours = total_seconds // 3600
remaining = total_seconds % 3600
minutes = remaining // 60
seconds = remaining % 60
print(f"{total_seconds} 秒 = {hours} 時 {minutes} 分 {seconds} 秒")

# --- 硬幣找零 ---
change = 87
coins_50 = change // 50;  change = change % 50
coins_10 = change // 10;  change = change % 10
coins_5  = change // 5;   change = change % 5
coins_1  = change
print(f"50元:{coins_50}, 10元:{coins_10}, 5元:{coins_5}, 1元:{coins_1}")

# ============================================================
# 小結：
# 1. + 加、- 減、* 乘、/ 除（永遠 float）
# 2. // 整除、% 取餘數、** 指數
# 3. 字串 + 是拼接，* 是重複（只能乘整數）
# 4. int + float → float
# 5. 除以 0 → ZeroDivisionError
# 6. 負數 // → 往更小方向取整（-7//2 = -4）
# 7. % 常用：判斷奇偶、整除、循環、取位數
