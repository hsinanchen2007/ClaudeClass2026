# 【面試題】多重條件的組織方式
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. 為什麼建議用 guard clause（早期回傳）取代深層巢狀？
#     答：把「不合格的情況」先擋掉並提早結束，主要邏輯就能維持在最外層，
#     縮排不會越陷越深。可讀性、可測試性都比五層巢狀好得多。
#     追問：在沒有函式可 return 的腳本裡怎麼做？
#     （改用扁平的 `if/elif` 逐條排除，效果相同）
#
# 🔥 Q2. `if-elif-else` 的 else 一定要寫嗎？
#     答：語法上不需要，但實務上**強烈建議寫**——它是你對「其他所有情況」
#     的明確表態。少了 else，非預期的輸入會安靜地什麼都不做，
#     這種 bug 最難查。
#     追問：真的沒有預設行為時怎麼辦？（寫 `else: raise ValueError(...)`，
#     讓錯誤大聲說出來，而不是靜靜溜過）
#
# ⚠️ 陷阱. 條件裡寫 `if x == 1 or 2:` 為什麼永遠成立？
#     答：因為它被解析成 `if (x == 1) or 2:`，而 `2` 是 truthy，
#     整條 `or` 永遠為真，x 是什麼都無所謂。
#     為什麼會錯：用自然語言的方式讀成「x 等於 1 或 2」。
#     正確寫法是 `if x == 1 or x == 2:`，
#     或更 Pythonic 的 `if x in (1, 2):`。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】if-else — 二選一
# 條件為 True 走 if，為 False 走 else。
# ============================================================

age = int(input("請輸入你的年齡："))

if age >= 18:
    print("你已成年，可以投票！")
else:
    print("你還未成年，再等幾年吧！")

# --- 判斷奇偶數 ---
number = int(input("輸入一個整數："))

if number % 2 == 0:
    print(f"{number} 是偶數")
else:
    print(f"{number} 是奇數")

# --- 密碼驗證（有成功/失敗回饋） ---
password = input("請輸入密碼：")

if password == "python123":
    print("密碼正確，歡迎進入！")
else:
    print("密碼錯誤，拒絕存取！")

# ============================================================
# 【重點 2】⚠️ else 後面不能加條件！
# else 代表「其他所有情況」。
# ============================================================

score = 75

# ❌ 錯誤：else 後面不能寫條件
# if score >= 60:
#     print("及格")
# else score < 60:         # SyntaxError!
#     print("不及格")

# ✅ 正確：else 就是「否則」
if score >= 60:
    print("及格")
else:                       # 不需要寫條件
    print("不及格")

# ============================================================
# 【重點 3】巢狀 if-else 的問題 — 可讀性差！
# ============================================================

# --- ❌ 巢狀寫法（越來越深，難以閱讀） ---
score = 85

if score >= 90:
    print("A")
else:
    if score >= 80:
        print("B")
    else:
        if score >= 70:
            print("C")
        else:
            if score >= 60:
                print("D")
            else:
                print("F")

# ============================================================
# 【重點 4】★ if-elif-else — 多重條件的正確寫法！
# 從上到下依序判斷，命中一個就停止，不再往下檢查。
# ============================================================

score = int(input("請輸入成績："))

if score >= 90:
    print("等級：A（優秀）")
elif score >= 80:
    print("等級：B（良好）")
elif score >= 70:
    print("等級：C（中等）")
elif score >= 60:
    print("等級：D（及格）")
else:
    print("等級：F（不及格）")

# ============================================================
# 【重點 5】⚠️ 條件順序很重要！— 從最嚴格開始
# ============================================================

score = 95

# --- ❌ 錯誤順序：從最寬鬆開始 ---
if score >= 60:              # 95 >= 60 → True → 命中！
    print("D")               # 輸出 D... 但 95 分應該是 A！
elif score >= 70:            # ← 根本不會到這裡
    print("C")
elif score >= 80:
    print("B")
elif score >= 90:
    print("A")

# --- ✅ 正確順序：從最嚴格開始 ---
if score >= 90:              # 最嚴格的先判斷
    print("A")               # ✅ 95 >= 90 → True → 輸出 A
elif score >= 80:
    print("B")
elif score >= 70:
    print("C")
elif score >= 60:
    print("D")
else:
    print("F")

# ============================================================
# 【重點 6】if-elif 不一定要有 else
# 可以只有 if-elif，沒有 else（不匹配時什麼都不做）。
# ============================================================

day = input("今天星期幾？(Sat/Sun)：")

if day == "Sat":
    print("星期六！去爬山")
elif day == "Sun":
    print("星期日！在家耍廢")
# 沒有 else → 輸入其他值時不做任何事

# ============================================================
# 【重點 7】用 in 搭配列表做多值匹配
# ============================================================

month = int(input("請輸入月份 (1-12)："))

if month in [3, 4, 5]:
    print("春天")
elif month in [6, 7, 8]:
    print("夏天")
elif month in [9, 10, 11]:
    print("秋天")
elif month in [12, 1, 2]:
    print("冬天")
else:
    print("無效的月份！")

# ============================================================
# 【重點 8】★ 多個獨立 if vs if-elif（超重要差別！）
# ============================================================

score = 95

# --- 寫法 A：多個獨立 if → 每個都會判斷！ ---
if score >= 90:
    print("A")               # ✅ 會印
if score >= 80:
    print("B")               # ✅ 也會印！因為 95 也 >= 80
if score >= 70:
    print("C")               # ✅ 也會印！因為 95 也 >= 70
# 輸出：A B C（全部都印了！）

# --- 寫法 B：if-elif → 命中就停止！ ---
if score >= 90:
    print("A")               # ✅ 命中！
elif score >= 80:
    print("B")               # ❌ 不會印（上面已經命中了）
elif score >= 70:
    print("C")               # ❌ 不會印
# 輸出：A（只印一個）

