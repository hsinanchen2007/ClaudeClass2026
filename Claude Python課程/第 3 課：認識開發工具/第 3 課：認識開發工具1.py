# 🔥 Q1. REPL（互動模式）與執行 .py 腳本有什麼差別？
#     答：REPL 會自動把每個運算式的結果回顯出來；腳本模式**不會**，
#     想看到東西一定要自己呼叫 print()。這是初學者「在 IDLE 有輸出、
#     存成檔案跑卻一片空白」的主因。
#     追問：那腳本裡寫一行 `1 + 1` 會有輸出嗎？（不會，值被算完就丟掉）
#
# 🔥 Q2. print() 不給任何參數會印出什麼？
#     答：印出一個空行——因為 `end` 參數預設是 `"\n"`，即使沒有內容，
#     換行符還是會輸出。常用來做版面留白。
#     追問：要讓兩次 print 印在同一行怎麼做？（`print(..., end="")`）
#
# ⚠️ 陷阱. 在編輯器裡「看起來對齊」就代表縮排正確嗎？
#     答：不一定。Python 把縮排當**語法**，混用 tab 與空格即使畫面上對齊，
#     仍可能拋 TabError／IndentationError。PEP 8 規定一律用 4 個空格。
#     為什麼會錯：肉眼只看得到「寬度」，看不到那到底是 tab 還是空格。
