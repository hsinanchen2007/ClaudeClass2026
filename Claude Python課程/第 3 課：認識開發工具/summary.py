# 本課介紹如何使用開發工具撰寫和執行 Python 程式。
# 常見開發工具：VS Code、PyCharm、IDLE、Jupyter Notebook 等。
#
# 【重點 1】print() 輸出基礎排版
# 可以用字串和 print() 做出簡單的文字排版效果。
# ============================================================

# --- 用 print() 做簡單的文字排版 ---
print("=================")
print("   歡迎學習 Python   ")
print("=================")

# --- 用 print() 印出空行，讓版面更整齊 ---
print()                              # 印出一個空白行

# --- 用底線(_) 代替待填內容 ---
print("我的名字是：_____")
print("我開始學 Python 了！")

# ============================================================
# 小結：
# 1. 選一個適合你的開發工具（推薦 VS Code）
# 2. print() 可以做出簡單文字排版
# 3. print() 不帶參數可印出空行
