# 【面試題】print() 的參數與文字排版
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. print() 的 sep 與 end 各自控制什麼？
#     答：`sep` 是「多個參數之間」的分隔字串（預設一個空格）；
#     `end` 是「整行結束時」附加的字串（預設 `"\n"`）。
#     例：`print("A","B",sep="-")` → `A-B`。
#     追問：想印出不換行的進度條要改哪一個？（end）
#
# 🔥 Q2. print(123) 與 print("123") 有什麼不同？
#     答：畫面上看起來一樣，但前者傳入的是 int、後者是 str。
#     print 會對每個參數取字串表示再輸出，所以型別差異在畫面上被抹平了，
#     一旦拿去做運算就完全不同（`123+1` 可以，`"123"+1` 會 TypeError）。
#     追問：那要怎麼確認變數真正的型別？（`type(x)`）
#
# ⚠️ 陷阱. 用底線、等號排版時，中文字會對齊嗎？
#     答：不一定。等寬字型下一個中文字通常佔兩個英文字元的寬度，
#     用固定數量的 `=` 去「對齊」中英混排的標題，在不同字型／終端機下
#     很可能歪掉。要精確排版應該用格式化寬度而不是手數符號。
#     為什麼會錯：把「字元數」誤當成「顯示寬度」。
# ═══════════════════════════════════════════════════════════════════════════


