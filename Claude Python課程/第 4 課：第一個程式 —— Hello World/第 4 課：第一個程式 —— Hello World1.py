# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. 單引號與雙引號定義字串有差別嗎？
#     答：功能上完全等價，Python 不像某些語言區分字元與字串。
#     實務上的用途是「交替使用」以避免跳脫：
#     字串內含 `'` 就用 `"` 包，內含 `"` 就用 `'` 包。
#     追問：兩種引號都要用怎麼辦？（用 `\` 跳脫，或改用三引號）
#
# 🔥 Q2. `\n` 是幾個字元？
#     答：**一個**。`\n` 是跳脫序列，在原始碼裡寫兩個符號，
#     但在字串物件裡是單一的換行字元，`len("a\nb")` 是 3 不是 4。
#     追問：想讓反斜線保持原樣（例如 Windows 路徑）怎麼寫？（raw string `r"..."`）
#
# ⚠️ 陷阱. print("第一行\n第二行") 與兩次 print 的輸出一樣嗎？
#     答：畫面上一樣，但機制不同——前者是一次呼叫、字串內含換行字元；
#     後者是兩次呼叫、各自由 `end="\n"` 補上換行。
#     為什麼會錯：只看畫面就以為兩者可以任意互換；一旦改了 `end` 或
#     把輸出接到管線／檔案做逐行處理，差別就會顯現。
# ═══════════════════════════════════════════════════════════════════════════

print("Hello World")

# 當你要印的文字本身包含引號時，就需要交替使用單引號和雙引號，這樣 Python 就能正確地解析字符串。
print("It's a beautiful day")

# 如果你需要在字符串中使用相同類型的引號，可以使用反斜杠（\）來轉義引號，這樣 Python 就會將其視為普通字符，而不是字符串的結束符。
print('It\'s a beautiful day')

# 在 Python 中，字符串可以使用單引號（'）或雙引號（"）來定義。這兩種方式在功能上是等效的，你可以根據需要選擇使用哪一種。
print('Hello World')
print("Hello World")

# 如果你需要在字符串中包含引號，可以使用交替的引號來定義字符串，這樣就不需要使用轉義字符了。
print('他說："你好"')
print("她說：'你好'")

print("第一行")
print("第二行")
print("第三行")

print("上面的文字")
print()
print("下面的文字")

print("第一行\n第二行\n第三行")

print(123)
print(456)
print(3.14159)
print(True)
print(False)
print(None)

print("123")    # 這是文字，不是數字
print(123)      # 這是數字
print("3.14159")  # 這是文字，不是數字
print(3.14159)    # 這是數字

# 在 Python 中，print() 函數可以接受多個參數，並且會自動將它們轉換為字符串並用空格分隔開來。
# 你也可以使用 sep 參數來指定分隔符，默認是空格。
print("我的名字是", "小明")
print("今年", 18, "歲")
print("圓周率是", 3.14159)
print("Hello", "World", sep="-")
print("Hello", end=" ")
print("World")

# 在 Python 中，數字類型包括整數（int）和浮點數（float）。你可以直接使用這些數字進行計算，Python 會自動處理類型轉換。
print(1 + 1)
print(10 - 3)
print(4 * 5)
print(20 / 4)
print(10 // 3)  # 整除
print(10 % 3)   # 取餘數

# 你也可以將數字和字符串結合在一起使用 print() 函數，Python 會自動將數字轉換為字符串。
print("1 + 1 =", 1 + 1)
print("10 - 3 =", 10 - 3)
print("4 x 5 =", 4 * 5)
print("20 ÷ 4 =", 20 / 4)
print("10 // 3 =", 10 // 3)
print("10 % 3 =", 10 % 3)   

# 在 Python 中，字符串可以使用單引號（'）或雙引號（"）來定義。這兩種方式在功能上是等效的，你可以根據需要選擇使用哪一種。
print("================")
