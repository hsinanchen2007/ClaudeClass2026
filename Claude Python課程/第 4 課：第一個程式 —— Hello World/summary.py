# 🔥 Q1. Python 3 的 print 是函式還是語句？
#     答：**函式**。Python 2 的 `print` 是語句（可寫 `print "hi"`），
#     Python 3 改成內建函式，所以括號是必要的。
#     這也讓它能被當成一般物件傳遞、能吃 sep/end/file 等關鍵字參數。
#     追問：`print` 可以被賦值給別的名字嗎？（可以，它就是個物件）
#
# 🔥 Q2. print 能印出 None 嗎？印出來長什麼樣？
#     答：可以，畫面顯示 `None`。要注意 `print(None)` 是「印出 None 這個物件」，
#     而 `print()` 沒有參數時印的是空行，兩者完全不同。
#     追問：函式沒有 return 時回傳什麼？（None）
#
# ⚠️ 陷阱. 三引號字串是「多行註解」嗎？
#     答：**不是**。它是一個正常的字串運算式，只是沒被使用就被丟棄。
#     真正的註解是 `#`，在編譯期就被丟掉。若三引號出現在模組／函式的
#     第一個位置，它會變成 docstring 存進 `__doc__`，語意又完全不同。
#     為什麼會錯：把「執行後沒有效果」誤當成「等同註解」。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】字串的定義 — 單引號 vs 雙引號
# Python 中字串可用 ' 或 " 定義，功能完全等效。
# ============================================================

print("Hello World")        # 用雙引號
print('Hello World')        # 用單引號 — 效果完全一樣

# --- 當字串本身包含引號時，交替使用即可 ---
print("It's a beautiful day")       # 字串含 ' → 外面用 "
print('他說："你好"')                 # 字串含 " → 外面用 '

# --- 也可以用反斜杠 \ 轉義引號 ---
print('It\'s a beautiful day')       # 用 \' 轉義單引號
print("他說：\"你好\"")              # 用 \" 轉義雙引號

# ============================================================
# 【重點 2】print() 進階用法
# ============================================================

# --- 印出空行 ---
print("上面的文字")
print()                              # 空行
print("下面的文字")

# --- 用 \n 換行（在同一個 print 中產生多行） ---
print("第一行\n第二行\n第三行")
# 輸出：
# 第一行
# 第二行
# 第三行

# --- print() 可以印出各種資料類型 ---
print(123)          # 整數
print(3.14159)      # 浮點數
print(True)         # 布林值 True
print(False)        # 布林值 False
print(None)         # None（什麼都沒有）

# --- ⚠️ 字串 vs 數字的差別 ---
print("123")        # 這是文字（字串）
print(123)          # 這是數字（整數）

# ============================================================
# 【重點 3】print() 多個參數、sep、end
# ============================================================

# --- 多個參數用逗號分隔，預設用空格連接 ---
print("我的名字是", "小明")          # 輸出：我的名字是 小明
print("今年", 18, "歲")             # 輸出：今年 18 歲
print("圓周率是", 3.14159)          # 輸出：圓周率是 3.14159

# --- sep 參數：自訂分隔符號（預設是空格） ---
print("Hello", "World", sep="-")    # 輸出：Hello-World

# --- end 參數：改變結尾（預設是換行 \n） ---
print("Hello", end=" ")             # 不換行，結尾改為空格
print("World")                      # 輸出：Hello World（同一行）

# ============================================================
# 【重點 4】基本數學運算（直接在 print 中計算）
# ============================================================

print(1 + 1)        # 2   — 加法
print(10 - 3)       # 7   — 減法
print(4 * 5)        # 20  — 乘法
print(20 / 4)       # 5.0 — 除法（結果永遠是 float）
print(10 // 3)      # 3   — 整除（捨去小數）
print(10 % 3)       # 1   — 取餘數

# --- 混合文字與計算結果 ---
print("1 + 1 =", 1 + 1)             # 輸出：1 + 1 = 2
print("4 x 5 =", 4 * 5)             # 輸出：4 x 5 = 20

# ============================================================
