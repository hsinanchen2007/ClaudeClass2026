# 【重點 4】while 經典應用 — 猜數字遊戲
# ============================================================

import random

answer = random.randint(1, 10)     # 產生 1~10 的隨機數
guess = 0
attempts = 0

print("=== 猜數字遊戲（1~10）===")

while guess != answer:
    guess = int(input("猜一個數字："))
    attempts += 1

    if guess > answer:
        print("太大了！往小的猜")
    elif guess < answer:
        print("太小了！往大的猜")
    else:
        print(f"答對了！你猜了 {attempts} 次")

# ============================================================
# 【重點 5】while 經典應用 — 累加直到滿足條件
# ============================================================

total = 0
n = 0

while total < 100:
    n += 1
    total += n

print(f"從 1 加到 {n}，總和首次超過 100")
print(f"總和是：{total}")

# ============================================================
# 【重點 6】while True + break — 無窮迴圈搭配跳出
# ============================================================

while True:
    user_input = input("請輸入（輸入 'quit' 離開）：")
    if user_input == "quit":
        break                      # ★ 跳出迴圈
    print(f"你輸入了：{user_input}")

# ============================================================
# 【重點 7】while 搭配選單 — 簡易計算機
# ============================================================

print("=== 簡易計算機 ===")
choice = ""

while choice != "q":
    print("\n選項：")
    print("1. 加法")
    print("2. 減法")
    print("q. 離開")
    choice = input("請選擇：")

    if choice == "1":
        a = float(input("第一個數："))
        b = float(input("第二個數："))
        print(f"結果：{a + b}")
    elif choice == "2":
        a = float(input("第一個數："))
        b = float(input("第二個數："))
        print(f"結果：{a - b}")
    elif choice == "q":
        print("再見！")
    else:
        print("無效選項！")

# ============================================================
# 【重點 8】while...else — 迴圈正常結束時執行 else
# 注意：如果被 break 跳出，else 不會執行。
# ============================================================

count = 1

while count <= 3:
    print(f"第 {count} 次")
    count += 1
else:
    print("迴圈正常結束！")       # ← 條件變 False 時才執行
# 輸出：
# 第 1 次
# 第 2 次
# 第 3 次
# 迴圈正常結束！

# ============================================================
# 【重點 9】★ for vs while 的選擇
# ============================================================

# --- for 版本（知道次數或有明確範圍時用 for） ---
for i in range(1, 6):
    print(i)

# --- while 版本（需要自己處理初始化和更新） ---
i = 1
while i <= 5:
    print(i)
    i += 1

# ★ for 更簡潔安全，while 更靈活
# 用 for：已知次數、遍歷序列
# 用 while：不確定次數、需要等待某條件成立

# ============================================================
# 【重點 10】條件一開始就是 False → 迴圈一次都不執行
# ============================================================

count = 0

while count > 0:            # 0 > 0 → False → 直接跳過
    print(count)
    count -= 1
# 什麼都不會印

