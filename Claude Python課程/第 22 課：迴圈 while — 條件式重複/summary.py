# 【重點 12】綜合應用 — ATM 系統
# ============================================================

balance = 10000

print("=== 歡迎使用 Python ATM ===")
choice = ""

while choice != "4":
    print(f"\n目前餘額：{balance} 元")
    print("1. 存款")
    print("2. 提款")
    print("3. 查詢餘額")
    print("4. 離開")
    choice = input("請選擇功能：")

    if choice == "1":
        amount = int(input("存款金額："))
        if amount > 0:
            balance += amount
            print(f"已存入 {amount} 元 ✅")
        else:
            print("金額必須大於 0！")
    elif choice == "2":
        amount = int(input("提款金額："))
        if amount > balance:
            print("餘額不足！❌")
        elif amount <= 0:
            print("金額必須大於 0！")
        else:
            balance -= amount
            print(f"已提出 {amount} 元 ✅")
    elif choice == "3":
        print(f"你的餘額是 {balance} 元")
    elif choice == "4":
        print("感謝使用，再見！")
    else:
        print("無效選項，請重新選擇！")

