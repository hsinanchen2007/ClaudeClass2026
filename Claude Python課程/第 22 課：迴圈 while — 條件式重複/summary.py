# 【面試題】while 迴圈的控制與模式
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. `while...else` 的 else 什麼時候執行？
#     答：迴圈**正常結束**（條件變成假）才執行；被 `break` 中斷則跳過。
#     `for...else` 同理，典型用途是「找遍了都沒找到」的失敗處理。
#     追問：為什麼這個 else 反直覺？
#     （命名不佳——它的語意其實是 `nobreak`，跟 if 的 else 完全不同）
#
# 🔥 Q2. 迴圈中用 `return` 會觸發 else 區塊嗎？
#     答：**不會**。`return` 直接離開整個函式，
#     `else` 與後續程式碼都不會執行。`break` 也不會觸發 else，
#     只有「條件自然變假」才會。
#     追問：那 `finally` 呢？（會執行——finally 的保證比 return 更強）
#
# ⚠️ 陷阱. 用 while 讀使用者輸入時，為什麼常常一開始就結束？
#     答：多半是條件變數的**初始值**就不滿足。
#     例如先 `answer = ""` 再 `while answer != "q":` 沒問題，
#     但若初始化成 `answer = "q"`，迴圈一次都不會跑。
#     為什麼會錯：只檢查「迴圈內有沒有更新變數」，
#     忘了 while 是**先判斷再執行**（pre-test），
#     初始值錯了就直接零次。這也是 `while True` + `break` 更常見的原因。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】while 基本語法 — 三要素
# ① 初始化：給條件一個起始狀態
# ② 條件：決定什麼時候停下來
# ③ 更新：讓條件有機會變成 False（否則無窮迴圈！）
# ============================================================

count = 5               # ① 初始化

while count > 0:         # ② 條件
    print(f"{count}...")
    count -= 1           # ③ 更新

print("發射！")
# 輸出：5... 4... 3... 2... 1... 發射！

# ============================================================
# 【重點 2】⚠️ 無窮迴圈 — 忘記更新變數是最常見的錯誤！
# ============================================================

# --- ❌ 錯誤 1：忘記更新變數 ---
# count = 5
# while count > 0:
#     print(count)
#     # 忘記 count -= 1 → count 永遠是 5 → 無窮迴圈！

# --- ❌ 錯誤 2：更新方向錯誤 ---
# x = 10
# while x > 0:
#     print(x)
#     x += 1              # 應該是 -= 1，寫成 += 1 越來越大 → 無窮迴圈！

# --- ❌ 錯誤 3：條件永遠不會為 False ---
# x = 1
# while x != 10:
#     print(x)
#     x += 2              # 1, 3, 5, 7, 9, 11... 永遠跳過 10！

# ============================================================
# 【重點 3】while 經典應用 — 輸入驗證（重複直到正確）
# ============================================================

# --- 密碼驗證 ---
password = input("請輸入密碼：")

while password != "python123":
    print("密碼錯誤，請重試！")
    password = input("請輸入密碼：")   # ← 迴圈內也要 input！

print("密碼正確，歡迎進入！✅")

# --- 年齡驗證（限制範圍） ---
age = int(input("請輸入年齡（1-150）："))

while age < 1 or age > 150:
    print("輸入不合理！請重新輸入。")
    age = int(input("請輸入年齡（1-150）："))

print(f"你的年齡是 {age} 歲")

