# ============================================================
# 第 13 課：比較運算子 — 大於、小於、等於、不等於 — 重點總結
# ============================================================

# ============================================================
# 【重點 1】六個比較運算子 — 結果都是布林值
# ============================================================

# ==  等於
print(5 == 5)               # True
print(5 == 3)               # False
print(1.0 == 1)             # True ← Python 認為值相等
print(True == 1)            # True ← True 的數值是 1
print(False == 0)           # True ← False 的數值是 0

# !=  不等於
print(5 != 3)               # True
print(5 != 5)               # False

# >   大於
print(10 > 5)               # True
print(5 > 5)                # False（等於不算大於！）

# <   小於
print(3 < 10)               # True
print(5 < 5)                # False（等於不算小於！）

# >=  大於或等於
print(5 >= 5)               # True（等於也算！✅）
print(3 >= 5)               # False

# <=  小於或等於
print(5 <= 5)               # True（等於也算！✅）
print(10 <= 5)              # False

# ============================================================
# 【重點 2】⚠️ = 和 == 的差別（超經典錯誤！）
# ============================================================

x = 10           # = 是「賦值」：把 10 存到 x
print(x == 10)   # == 是「比較」：x 等於 10 嗎？→ True

# ❌ 在 if 中寫錯
# if x = 10:     # SyntaxError! 應該用 ==

# ✅ 正確
if x == 10:
    print("x 是 10")

# ============================================================
# 【重點 3】⚠️ 符號順序不能反！
# ============================================================

# ✅ 正確寫法
print(5 >= 3)    # 大於等於
print(5 <= 3)    # 小於等於
print(5 != 3)    # 不等於

# ❌ 錯誤寫法（符號順序反了）
# print(5 =>)    # SyntaxError!
# print(5 =<)    # SyntaxError!
# print(5 =!)    # SyntaxError!

# ============================================================
# 【重點 4】不同型別之間的比較
# ============================================================

# --- int 和 float 可以直接比較 ---
print(1 == 1.0)             # True
print(5 > 4.9)              # True
print(10 >= 10.0)           # True

# --- 字串比較：按字母順序（ASCII/Unicode 編碼） ---
print("apple" == "apple")   # True
print("apple" == "Apple")   # False ← 大小寫不同！
print("apple" < "banana")   # True  ← a 排在 b 前面
print("cat" > "car")        # True  ← 前兩字一樣，t > r

# --- ASCII 編碼順序 ---
# 數字 0-9（48-57）< 大寫 A-Z（65-90）< 小寫 a-z（97-122）
print("Z" < "a")            # True（Z=90 < a=97）
print("9" < "A")            # True（9=57 < A=65）

# --- 長度不同的字串比較 ---
print("app" < "apple")      # True（較短的字串較「小」）

# --- ⚠️ 字串和數字不能比大小 ---
# print("5" > 3)             # ❌ TypeError!
print("5" == 5)              # False（型別不同，直接 False）

# --- ✅ 先轉型再比較 ---
print(int("10") > 5)         # True

# ============================================================
# 【重點 5】★ 連鎖比較（Python 特有功能！）
# 可以像數學一樣寫 1 < x < 10
# ============================================================

x = 5
print(1 < x < 10)           # True（等同於 1 < x and x < 10）
print(1 < x < 3)            # False

# --- 實用：判斷範圍 ---
age = 25
print(18 <= age <= 65)       # True（18 到 65 之間）

score = 85
print(60 <= score <= 100)    # True（60 到 100 之間）

# --- 判斷三個數是否依序排列 ---
a, b, c = 1, 2, 3
print(a < b < c)             # True
a, b, c = 1, 3, 2
print(a < b < c)             # False（3 不小於 2）

# ============================================================
# 【重點 6】把比較結果存到變數
# ============================================================

age = 20
score = 85

is_adult = age >= 18         # True
is_pass = score >= 60        # True
is_excellent = score >= 90   # False

print(f"已成年：{is_adult}")       # True
print(f"及格：{is_pass}")          # True
print(f"優秀：{is_excellent}")     # False

# ============================================================
# 【重點 7】⚠️ 常見陷阱
# ============================================================

# --- 字串比較大小寫敏感 → 統一轉小寫 ---
user_input = "Yes"
# ❌ 可能匹配不到
if user_input == "yes":
    pass

# ✅ 統一轉小寫再比較
if user_input.lower() == "yes":
    print("你選了是")

# --- 浮點數比較不精確 ---
print(0.1 + 0.2 == 0.3)           # False 😱
print(round(0.1 + 0.2, 10) == round(0.3, 10))  # True ✅

# ============================================================
# 【重點 8】實作範例
# ============================================================

# --- 年齡判斷 ---
age = int(input("請輸入年齡："))
if age >= 18:
    print("你已成年")
else:
    print("你未成年")

# --- 成績等級 ---
score = float(input("請輸入成績："))
if score >= 90:
    print("等級：A")
elif score >= 80:
    print("等級：B")
elif score >= 70:
    print("等級：C")
elif score >= 60:
    print("等級：D")
else:
    print("等級：F")

# --- 體溫判斷（連鎖比較） ---
temp = float(input("請輸入體溫："))
if 36.0 <= temp <= 37.5:
    print("體溫正常")
elif temp > 37.5:
    print("體溫偏高")
else:
    print("體溫偏低")

# ============================================================
# 小結：
# 1. 六個比較運算子：== != > < >= <=
# 2. = 賦值 vs == 比較 — 超經典錯誤！
# 3. >= <= != 符號順序不能反（= 在後面）
# 4. int 和 float 可直接比較，字串和數字不行
# 5. 字串比較按 ASCII 編碼，大小寫敏感
# 6. Python 支援連鎖比較：18 <= age <= 65
# 7. 比較結果可存到變數，之後在 if 中使用
# 8. 浮點數比較用 round() 處理
