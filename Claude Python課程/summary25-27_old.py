# ============================================================
# 一、串列的建立                                   【第 25 課】
# ============================================================

# --- 用 [] 直接建立 ---
empty = []                                    # 空串列
nums = [10, 20, 30]                           # 整數串列
fruits = ["蘋果", "香蕉", "芒果"]              # 字串串列
mixed = ["小明", 18, 175.5, True]             # ★ 可混合不同型別

