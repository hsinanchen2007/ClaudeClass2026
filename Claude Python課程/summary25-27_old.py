# ============================================================
# 五、遍歷串列                                     【第 25 課】
# ============================================================

# --- 直接走訪 ---
for fruit in fruits:
    print(fruit)

# --- ★ enumerate() 同時取索引和元素（比 range(len()) 更好） ---
for i, fruit in enumerate(fruits):
    print(f"第 {i} 號：{fruit}")               # 索引從 0 開始

for i, fruit in enumerate(fruits, 1):
    print(f"第 {i} 個：{fruit}")               # 索引從 1 開始


# ============================================================
# 六、新增元素                                     【第 26 課】
# ============================================================

# --- append(x)：尾端加入一個元素 ---
a = [1, 2, 3]
a.append(4)                                   # [1, 2, 3, 4]

# ⚠️ append 加入串列 → 整個當一個元素（巢狀）
a.append([5, 6])                              # [1, 2, 3, 4, [5, 6]]

# --- extend(iterable)：把元素逐一加入 ---
b = [1, 2, 3]
b.extend([4, 5])                              # [1, 2, 3, 4, 5]

# ★ append vs extend：
# append([4,5]) → [..., [4,5]]   長度 +1
# extend([4,5]) → [..., 4, 5]    長度 +2

# --- insert(index, x)：在指定位置插入 ---
c = ["紅", "黃", "藍"]
c.insert(1, "橙")                             # ['紅', '橙', '黃', '藍']
c.insert(0, "白")                             # ['白', '紅', '橙', '黃', '藍']

# --- + 運算子：產生新串列（原串列不變） ---
x = [1, 2]
y = x + [3, 4]                                # y = [1, 2, 3, 4]，x 不變


# ============================================================
# 七、刪除元素                                     【第 26 課】
# ============================================================

# --- remove(value)：刪除第一個符合的值 ---
d = ["蘋果", "香蕉", "芒果", "香蕉"]
d.remove("香蕉")                               # ['蘋果', '芒果', '香蕉']
# ★ 只刪第一個！第二個 "香蕉" 還在

# ⚠️ 元素不存在 → ValueError
# d.remove("草莓")                             # ❌ ValueError
# ✅ 安全做法：先檢查
if "草莓" in d:
    d.remove("草莓")

# --- del list[i]：根據索引刪除（不回傳） ---
e = [10, 20, 30, 40, 50]
del e[2]                                      # [10, 20, 40, 50]
del e[1:3]                                    # [10, 50]（切片刪除）

# --- pop(index)：刪除並回傳 ---
f = ["蘋果", "香蕉", "芒果", "葡萄"]
last = f.pop()                                # "葡萄"（刪除最後一個）
second = f.pop(1)                             # "香蕉"（刪除索引 1）

# ★ del vs pop：
# del → 刪除，不回傳
# pop → 刪除並回傳被刪的值

# --- clear()：清空串列 ---
g = [1, 2, 3]
g.clear()                                     # []（串列還在，但是空的）


# ============================================================
# 八、修改元素                                     【第 26 課】
# ============================================================

# --- 用索引修改 ---
h = ["蘋果", "香蕉", "芒果"]
h[1] = "草莓"                                  # ['蘋果', '草莓', '芒果']

# --- 用切片修改（可改變長度！） ---
n = [1, 2, 3, 4, 5]
n[1:3] = [20, 30]                             # [1, 20, 30, 4, 5]（等量替換）
n[1:3] = [200, 300, 400]                      # [1, 200, 300, 400, 4, 5]（多量替換）

# --- 用切片插入（start == end → 不刪除） ---
m = [1, 2, 5, 6]
m[2:2] = [3, 4]                               # [1, 2, 3, 4, 5, 6]


# ============================================================
# 九、★★★ 別名陷阱與複製                         【第 26 課】
# ============================================================

# ⚠️ 賦值是別名（指向同一個物件），不是複製！
a = [1, 2, 3]
b = a                                         # b 和 a 指向同一個串列
b.append(4)
print(a)                                      # [1, 2, 3, 4] ← a 也被影響！

# ★ 正確複製的三種方式
original = [1, 2, 3]
copy1 = original[:]                           # 方法一：切片（最常用）
copy2 = original.copy()                       # 方法二：.copy()
copy3 = list(original)                        # 方法三：list()

# 修改副本不影響原串列
copy1.append(99)
print(original)                               # [1, 2, 3]（不受影響）

# ⚠️ 一邊走訪一邊刪除 → 結果不正確
nums = [1, 2, 3, 4, 5, 6]
for n in nums:
    if n % 2 == 0:
        nums.remove(n)
print(nums)                                   # [1, 3, 5, 6] ← 6 沒被刪！

# ✅ 安全做法一：推導式建新串列
odds = [n for n in nums if n % 2 != 0]

# ✅ 安全做法二：走訪副本
for n in nums[:]:
    if n % 2 == 0:
        nums.remove(n)


# ============================================================
# 十、排序                                         【第 27 課】
# ============================================================

# --- sort()：原地排序（回傳 None） ---
nums = [38, 15, 72, 4, 56]
nums.sort()                                   # [4, 15, 38, 56, 72]
nums.sort(reverse=True)                       # [72, 56, 38, 15, 4]

# --- sorted()：回傳新串列（原串列不變） ---
nums = [38, 15, 72, 4, 56]
new_list = sorted(nums)                       # [4, 15, 38, 56, 72]
print(nums)                                   # [38, 15, 72, 4, 56] ← 不變

# ⚠️ sort() 回傳 None，不能接回傳值
result = nums.sort()                          # result = None（不是排序後的串列！）
# ✅ 要接結果用 sorted()

# --- ★ key 參數：自訂排序依據 ---
["banana", "kiwi", "fig"].sort(key=len)       # 依長度排序
[-5, 3, -1, 8].sort(key=abs)                  # 依絕對值排序

# --- ★ lambda + key 排序元組 ---
students = [("小明", 85), ("小華", 92), ("小美", 78)]
students.sort(key=lambda x: x[1], reverse=True)
# → [('小華', 92), ('小明', 85), ('小美', 78)]

# ⚠️ 混合型別不能排序
# [3, "hello", 1].sort()                      # ❌ TypeError


# ============================================================
# 十一、反轉                                       【第 27 課】
# ============================================================

nums = [1, 2, 3, 4, 5]

# --- reverse()：原地反轉（回傳 None） ---
nums.reverse()                                # [5, 4, 3, 2, 1]

# --- reversed()：回傳迭代器（原串列不變） ---
nums = [1, 2, 3, 4, 5]
rev = list(reversed(nums))                    # [5, 4, 3, 2, 1]，nums 不變

# --- [::-1]：切片反轉（最簡潔，原串列不變） ---
rev = nums[::-1]                              # [5, 4, 3, 2, 1]，nums 不變


