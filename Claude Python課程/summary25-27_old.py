# ============================================================
# 十四、★★★ 串列推導式（List Comprehension）      【第 27 課】
# ============================================================
# 語法：[表達式 for 變數 in 可迭代 if 條件]

# --- 基本推導式 ---
[n ** 2 for n in range(1, 6)]                 # [1, 4, 9, 16, 25]

# --- 轉換 ---
[f.upper() for f in ["apple", "banana"]]      # ['APPLE', 'BANANA']

# --- 帶條件篩選 ---
[n for n in range(1, 11) if n % 2 == 0]       # [2, 4, 6, 8, 10]

# --- 同時篩選 + 轉換 ---
[s + 5 for s in [45, 72, 88, 55] if s >= 60]  # [77, 93]

# --- 等效的傳統寫法 ---
# result = []
# for n in range(1, 6):
#     result.append(n ** 2)
# 推導式一行搞定：[n ** 2 for n in range(1, 6)]


# ============================================================
# 十五、字串與串列互轉 — join() 與 split()         【第 27 課】
# ============================================================

# --- join()：串列 → 字串 ---
# ★ join 是字串的方法，在分隔符上呼叫
" ".join(["Hello", "World"])                   # "Hello World"
"-".join(["A", "B", "C"])                      # "A-B-C"
"".join(["H", "e", "l", "l", "o"])            # "Hello"

# ⚠️ 元素必須是字串
# "-".join([1, 2, 3])                          # ❌ TypeError
"-".join(str(n) for n in [1, 2, 3])            # "1-2-3"

# --- split()：字串 → 串列 ---
"Python 是 程式語言".split()                    # ['Python', '是', '程式語言']
"85,92,78".split(",")                          # ['85', '92', '78']

# ★ split + 推導式 → 字串轉數字串列
[int(s) for s in "85,92,78".split(",")]        # [85, 92, 78]


