# ║                  ★ List 方法速查表 ★                          ║
# ╚════════════════════════════════════════════════════════════════╝
#
# 【建立】
#   []                空串列
#   [1, 2, 3]         直接建立
#   list("abc")       轉換建立 → ['a', 'b', 'c']
#   list(range(5))    range 轉串列 → [0, 1, 2, 3, 4]
#   [0] * n           重複建立
#
# 【存取】
#   list[i]           索引（0 開始，-1 最後）
#   list[i:j]         切片（不含 j）
#   list[::step]      步長切片
#   list[::-1]        反轉
#   list[i][j]        二維存取
#
# 【新增】                        回傳值    修改原串列
#   .append(x)       尾端加一個    None      ✅
#   .extend(iter)    逐一加入      None      ✅
#   .insert(i, x)    指定位置插入  None      ✅
#   list1 + list2    合併          新串列    ❌
#
# 【刪除】                        回傳值    修改原串列
#   .remove(x)       按值刪第一個  None      ✅（不存在→ValueError）
#   del list[i]      按索引刪除    —         ✅
#   del list[i:j]    切片刪除      —         ✅
#   .pop()           刪最後一個    被刪的值  ✅
#   .pop(i)          刪索引 i      被刪的值  ✅
#   .clear()         清空          None      ✅
#
# 【修改】
#   list[i] = val    單一修改
#   list[i:j] = [..] 切片修改（可改變長度）
#   list[i:i] = [..] 切片插入（不刪除）
#
# 【排序】                        回傳值    修改原串列
#   .sort()          原地排序      None      ✅
#   .sort(reverse=True) 降冪排序   None      ✅
#   .sort(key=func)  自訂依據排序  None      ✅
#   sorted(list)     排序          新串列    ❌
#
# 【反轉】                        回傳值    修改原串列
#   .reverse()       原地反轉      None      ✅
#   list(reversed()) 反轉          新串列    ❌
#   list[::-1]       切片反轉      新串列    ❌
#
# 【搜尋與計數】
#   .index(x)        第一次出現位置（不存在→ValueError）
#   .index(x, start) 從 start 開始找
#   .count(x)        出現次數（不存在→0）
#   x in list        是否存在 → True/False
#
# 【統計】
#   len(list)        長度
#   sum(list)        總和
#   max(list)        最大值
#   min(list)        最小值
#
# 【串列推導式】
#   [expr for x in iter]            基本推導
#   [expr for x in iter if cond]    帶條件篩選
#
# 【字串互轉】
#   "sep".join(list)    串列→字串
#   "str".split("sep")  字串→串列
#
# 【複製】（避免別名陷阱）
#   list[:]          切片複製
#   list.copy()      copy 方法
#   list(original)   list 建構
#
# 【常見錯誤】
#   ⚠️ b = a 是別名，不是複製！修改 b 會影響 a
#   ⚠️ append/sort/reverse 回傳 None，不能接回傳值
#   ⚠️ remove/index 元素不存在會報錯，先用 in 檢查
#   ⚠️ 一邊走訪一邊刪除 → 結果不正確，要走訪副本
#   ⚠️ 索引超出範圍 → IndexError，但切片不會
#   ⚠️ 混合型別不能排序
#   ⚠️ join 是字串方法，不是串列方法
#
