# █  第六章：遍歷串列 — for 迴圈的各種寫法                             █
# █                                                                  █
# ████████████████████████████████████████████████████████████████████

fruits = ["蘋果", "香蕉", "芒果", "葡萄"]

# ============================================================
# 6.1 直接走訪元素（最簡潔）
# ============================================================

for fruit in fruits:
    print(fruit)
# 蘋果
# 香蕉
# 芒果
# 葡萄

# ============================================================
# 6.2 ★★★ enumerate() — 同時取得索引和元素（推薦！）
# ============================================================

# 預設從 0 開始
for i, fruit in enumerate(fruits):
    print(f"第 {i} 號：{fruit}")
# 第 0 號：蘋果
# 第 1 號：香蕉
# ...

# 可以指定起始編號
for i, fruit in enumerate(fruits, 1):
    print(f"第 {i} 個：{fruit}")
# 第 1 個：蘋果
# 第 2 個：香蕉
# ...

# ★ enumerate 比 range(len(...)) 更 Pythonic
# ❌ 不好的寫法
for i in range(len(fruits)):
    print(f"{i}: {fruits[i]}")

# ✅ 好的寫法
for i, fruit in enumerate(fruits):
    print(f"{i}: {fruit}")

# ============================================================
# 6.3 用索引遍歷 — range(len(...))
# 當你確實需要索引來操作多個串列時才用
# ============================================================

names = ["小明", "小華", "小美"]
scores = [85, 72, 93]

for i in range(len(names)):
    print(f"{names[i]}：{scores[i]} 分")

# ============================================================
# 6.4 走訪二維串列
# ============================================================

grades = [
    [85, 92, 78],
    [88, 76, 95],
    [91, 85, 89],
]
names = ["小明", "小華", "小美"]
subjects = ["國文", "英文", "數學"]

for i, student_grades in enumerate(grades):
    print(f"\n{names[i]} 的成績：")
    for j, score in enumerate(student_grades):
        print(f"  {subjects[j]}：{score} 分")
    avg = sum(student_grades) / len(student_grades)
    print(f"  → 平均：{avg:.1f} 分")


