# 假設你要管理 5 位學生的成績。
# 不用串列的話，你必須建立 5 個獨立變數：

score1 = 85
score2 = 92
score3 = 78
score4 = 96
score5 = 88

# 如果要算平均，你得一個一個寫：
avg = (score1 + score2 + score3 + score4 + score5) / 5

# 如果有 100 位學生呢？1000 位呢？這種做法不可行。

# ★ 串列讓你用一個變數存放一整組資料：
scores = [85, 92, 78, 96, 88]
avg = sum(scores) / len(scores)
print(f"平均：{avg}")                # 平均：87.8

# 而且你可以用迴圈一次處理所有資料：
for score in scores:
    print(f"  {score} 分 → {'及格' if score >= 60 else '不及格'}")


