# ============================================================
# 10.1 ⚠️ 賦值是別名，不是複製！
# ============================================================

a = [1, 2, 3]
b = a                                # b 和 a 指向同一個串列！

b.append(4)
print(a)                             # [1, 2, 3, 4] ← a 也被影響了！
print(a is b)                        # True（確實是同一個物件）

# 這是因為 Python 的變數是「標籤」，不是「盒子」。
# b = a 只是給同一個串列多貼了一張標籤，
# 不管你拉哪張標籤，操作的都是同一個串列。

# ============================================================
# 10.2 ★ 淺複製 — 三種方式
# ============================================================

original = [1, 2, 3]

# 方式一：切片（最常用）
copy1 = original[:]

# 方式二：.copy() 方法
copy2 = original.copy()

# 方式三：list() 建構
copy3 = list(original)

# 修改副本不影響原串列
copy1.append(99)
print(original)                      # [1, 2, 3] ← 不受影響
print(copy1)                         # [1, 2, 3, 99]

# ============================================================
# 10.3 ⚠️ 淺複製的限制 — 內層可變物件仍共享
# ============================================================

original = [1, 2, [3, 4]]
copied = original.copy()

# 修改第一層 → 不影響
copied[0] = 999
print(original[0])                   # 1 ← 安全

# ⚠️ 修改內層串列 → 原串列也被影響！
copied[2].append(5)
print(original)                      # [1, 2, [3, 4, 5]] ← 被改了！

# ============================================================
# 10.4 ★ 深複製 — 完全獨立
# ============================================================

import copy

original = [1, 2, [3, 4]]
deep = copy.deepcopy(original)

deep[2].append(5)
print(original)                      # [1, 2, [3, 4]] ← 安全！

# ★ 選擇指引：
# 元素都是不可變型別（str, int, float, bool） → copy() 就夠了
# 元素包含可變型別（list, dict） → 用 deepcopy()

# ============================================================
# 10.5 ⚠️ 一邊走訪一邊刪除 → 結果不正確
# ============================================================

nums = [1, 2, 3, 4, 5, 6]
for n in nums:
    if n % 2 == 0:
        nums.remove(n)
print(nums)                          # [1, 3, 5, 6] ← 6 沒被刪掉！

# 原因：走訪時串列在縮短，索引會跳過元素。

# ✅ 安全做法一：推導式建新串列（最 Pythonic）
nums = [1, 2, 3, 4, 5, 6]
odds = [n for n in nums if n % 2 != 0]
print(odds)                          # [1, 3, 5]

# ✅ 安全做法二：走訪副本
nums = [1, 2, 3, 4, 5, 6]
for n in nums[:]:                    # nums[:] 是副本
    if n % 2 == 0:
        nums.remove(n)
print(nums)                          # [1, 3, 5]

# ✅ 安全做法三：倒序走訪（刪除不影響前面的索引）
nums = [1, 2, 3, 4, 5, 6]
for i in range(len(nums) - 1, -1, -1):
    if nums[i] % 2 == 0:
        del nums[i]
print(nums)                          # [1, 3, 5]


