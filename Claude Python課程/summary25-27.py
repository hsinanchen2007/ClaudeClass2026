# ============================================================
# 8.1 remove(value) — 按值刪除第一個出現的
# ============================================================

fruits = ["蘋果", "香蕉", "芒果", "香蕉", "葡萄"]
fruits.remove("香蕉")
print(fruits)                        # ['蘋果', '芒果', '香蕉', '葡萄']
# ★ 只刪除第一個 "香蕉"！第二個還在

# ⚠️ 元素不存在 → ValueError
# fruits.remove("草莓")             # ❌ ValueError: list.remove(x): x not in list

# ✅ 安全做法：先檢查
target = "草莓"
if target in fruits:
    fruits.remove(target)
else:
    print(f"{target} 不在串列中")

# ============================================================
# 8.2 del list[i] — 按索引刪除（不回傳）
# ============================================================

numbers = [10, 20, 30, 40, 50]
del numbers[2]                       # 刪除索引 2
print(numbers)                       # [10, 20, 40, 50]

# 切片刪除
del numbers[1:3]                     # 刪除索引 1~2
print(numbers)                       # [10, 50]

# del numbers 會刪除整個變數
# del numbers
# print(numbers)                     # ❌ NameError: name 'numbers' is not defined

# ============================================================
# 8.3 ★ pop(index) — 刪除並回傳
# ============================================================

fruits = ["蘋果", "香蕉", "芒果", "葡萄"]

# 不給參數 → 刪除最後一個
last = fruits.pop()
print(last)                          # 葡萄
print(fruits)                        # ['蘋果', '香蕉', '芒果']

# 給索引 → 刪除指定位置
second = fruits.pop(1)
print(second)                        # 香蕉
print(fruits)                        # ['蘋果', '芒果']

# ★ del vs pop 差異：
# del → 刪除，不回傳值
# pop → 刪除並回傳被刪的值

# ============================================================
# 8.4 clear() — 清空串列
# ============================================================

nums = [1, 2, 3, 4, 5]
nums.clear()
print(nums)                          # []（串列還在，但是空的）

# ============================================================
# 刪除方式比較表：
#
#   方式              按什麼刪    回傳值       不存在時
#   ──────────────────────────────────────────────────
#   remove(x)         按值        None         ValueError
#   del list[i]       按索引      —            IndexError
#   del list[i:j]     按切片      —            不報錯
#   pop()             最後一個    被刪的值      IndexError(空串列)
#   pop(i)            按索引      被刪的值      IndexError
#   clear()           全部        None         不適用
# ============================================================


