# ★ 串列是可變的（mutable），可以直接修改內容。
# 以下方法都是「原地修改」，不會建立新串列。

# ============================================================
# 7.1 append(x) — 在尾端加入一個元素
# ============================================================

fruits = ["蘋果", "香蕉"]
fruits.append("芒果")
print(fruits)                        # ['蘋果', '香蕉', '芒果']

fruits.append("葡萄")
print(fruits)                        # ['蘋果', '香蕉', '芒果', '葡萄']

# ⚠️ append 加入串列時，整個串列當成「一個元素」
a = [1, 2, 3]
a.append([4, 5])
print(a)                             # [1, 2, 3, [4, 5]] ← 巢狀了！長度只 +1

# ============================================================
# 7.2 extend(iterable) — 把多個元素逐一加入
# ============================================================

b = [1, 2, 3]
b.extend([4, 5])
print(b)                             # [1, 2, 3, 4, 5] ← 每個元素獨立加入

# ★★★ append vs extend — 超重要差異！
# append([4, 5]) → [1, 2, 3, [4, 5]]     長度 +1（整個串列當一個元素）
# extend([4, 5]) → [1, 2, 3, 4, 5]       長度 +2（逐一加入）

# extend 也可以用其他可迭代物件
c = [1, 2]
c.extend("ABC")                      # 字串的每個字元逐一加入
print(c)                             # [1, 2, 'A', 'B', 'C']

c.extend(range(3))
print(c)                             # [1, 2, 'A', 'B', 'C', 0, 1, 2]

# ============================================================
# 7.3 insert(index, x) — 在指定位置插入
# ============================================================

colors = ["紅", "黃", "藍"]

# 在索引 1 的位置插入
colors.insert(1, "橙")
print(colors)                        # ['紅', '橙', '黃', '藍']

# 在最前面插入
colors.insert(0, "白")
print(colors)                        # ['白', '紅', '橙', '黃', '藍']

# 在最後面插入（等同 append）
colors.insert(len(colors), "紫")
print(colors)                        # ['白', '紅', '橙', '黃', '藍', '紫']

# ============================================================
# 7.4 + 運算子 — 產生新串列（原串列不變）
# ============================================================

a = [1, 2]
b = [3, 4]
c = a + b
print(a)                             # [1, 2] ← 不變
print(c)                             # [1, 2, 3, 4] ← 新串列

# ★ append/extend/insert 是原地修改（改自己）
# ★ + 是產生新串列（不改原本的）

# ============================================================
# 7.5 ⚠️ append/extend/insert 回傳 None！
# ============================================================

nums = [1, 2, 3]
result = nums.append(4)
print(result)                        # None ← 不是 [1,2,3,4]！
print(nums)                          # [1, 2, 3, 4]（串列本身已改）

# ✅ 正確寫法：不需要接回傳值
nums.append(5)


