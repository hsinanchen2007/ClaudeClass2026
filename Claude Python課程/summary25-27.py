# ============================================================
# 方式 1：用 [] 大括號直接建立（最常見）
# ============================================================

# 空串列
empty = []
print(empty)                         # []
print(type(empty))                   # <class 'list'>

# 存放整數
numbers = [10, 20, 30, 40, 50]

# 存放字串
fruits = ["蘋果", "香蕉", "芒果", "葡萄"]

# 存放布林值
flags = [True, False, True, True]

# ★ 串列可以混合不同型別（但通常不建議，保持同類型更好讀）
mixed = ["小明", 18, 175.5, True]
# 索引：  0       1     2       3

# ★ 最後一個元素後加逗號是合法的（推薦！方便之後新增）
colors = [
    "紅",
    "橙",
    "黃",
    "綠",   # ← 尾端逗號
]

# ============================================================
# 方式 2：用 list() 從其他型別轉換
# ============================================================

# 字串 → 串列（每個字元變成一個元素）
chars = list("Hello")
print(chars)                         # ['H', 'e', 'l', 'l', 'o']

# range → 串列
nums = list(range(1, 6))
print(nums)                          # [1, 2, 3, 4, 5]

# 元組 → 串列
from_tuple = list((10, 20, 30))
print(from_tuple)                    # [10, 20, 30]

# ============================================================
# 方式 3：用 * 重複建立
# ============================================================

zeros = [0] * 5
print(zeros)                         # [0, 0, 0, 0, 0]

lockers = ["空"] * 8
print(lockers)                       # ['空', '空', '空', '空', '空', '空', '空', '空']

# ⚠️ 陷阱：如果重複的是可變物件，所有元素會共享同一個物件！
bad = [[]] * 3
bad[0].append(1)
print(bad)                           # [[1], [1], [1]] ← 全部被改了！
# 因為三個 [] 是同一個物件

# ✅ 正確做法：用推導式
good = [[] for _ in range(3)]
good[0].append(1)
print(good)                          # [[1], [], []] ← 各自獨立

# ============================================================
# 方式 4：二維串列（串列的串列）
# ============================================================

grades = [
    [85, 92, 78],                    # 第 0 位同學
    [88, 76, 95],                    # 第 1 位同學
    [91, 85, 89],                    # 第 2 位同學
]
print(grades)
print(grades[1])                     # [88, 76, 95]（第 1 位同學的整列成績）
print(grades[1][2])                  # 95（第 1 位同學的第 2 科）

# ============================================================
# 方式 5：串列推導式（List Comprehension）
# 語法：[表達式 for 變數 in 可迭代 if 條件]
# （詳見第十二章）
# ============================================================

squares = [n ** 2 for n in range(1, 6)]
print(squares)                       # [1, 4, 9, 16, 25]


