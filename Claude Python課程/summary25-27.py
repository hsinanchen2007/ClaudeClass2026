print(new_list)                      # [4, 15, 38, 56, 72]
print(nums)                          # [38, 15, 72, 4, 56] ← 不變

sorted(nums, reverse=True)           # [72, 56, 38, 15, 4]

# ★ sort() vs sorted()：
# sort()   → 原地修改，回傳 None      → 當你不需要保留原始順序
# sorted() → 回傳新串列，原串列不變    → 當你需要保留原始資料

