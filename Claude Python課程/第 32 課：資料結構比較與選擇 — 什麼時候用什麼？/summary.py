# 小結
# ════════════════════════════════════════════════════════════════════
# 1. 四大容器各有定位，沒有「最好」的，只有「最適合當下需求」的。
# 2. 選擇三問：要順序/索引嗎？會修改嗎？要查名稱/判存在嗎？
# 3. 效能關鍵：「查找」與「去重」就交給 set / dict（O(1)），
#    不要傻傻地用 list 配 in（O(n)），資料一大就會卡。
# 4. 真實程式幾乎都是容器「組合搭配」：
#    list of dict（資料表）、dict of list（分組）、dict of set（去重歸類）、
#    tuple 當 key（座標 / 複合鍵）。
# 5. 一句話記住：
#       list  → 一排可改的資料
#       tuple → 一筆不可改的紀錄
#       dict  → 一本查號簿
#       set   → 一個投票箱（去重 + 問有沒有）
