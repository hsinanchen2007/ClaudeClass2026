# 真實世界很少只用單一容器，而是互相組合。常見組合：

# ------------------------------------------------------------
# 9.1 list of dict：一張資料表（最常見的資料格式，像 JSON / CSV）
# ------------------------------------------------------------
students = [
    {"id": "S001", "name": "Alice", "dept": "資工", "score": 85},
    {"id": "S002", "name": "Bob",   "dept": "電機", "score": 92},
    {"id": "S003", "name": "Cindy", "dept": "資工", "score": 78},
    {"id": "S004", "name": "David", "dept": "機械", "score": 96},
]
# 篩選：資工系學生（list comprehension）
cs = [s["name"] for s in students if s["dept"] == "資工"]
print("資工系：", cs)   # ['Alice', 'Cindy']
# 找最高分：max + key
top = max(students, key=lambda s: s["score"])
print("最高分：", top["name"], top["score"])   # David 96

# ------------------------------------------------------------
# 9.2 dict 當查表（list → dict 加速查找）
# ------------------------------------------------------------
# 原本要在 list 裡用迴圈找學號 → O(n)
# 先做成 {學號: 整筆資料} 的字典 → 之後查找 O(1)
lookup = {s["id"]: s for s in students}
print("查 S003：", lookup["S003"]["name"])   # Cindy

# ------------------------------------------------------------
# 9.3 dict + list 分組（setdefault 經典用法）
# ------------------------------------------------------------
by_dept = {}
for s in students:
    by_dept.setdefault(s["dept"], []).append(s["name"])
print("依科系分組：", by_dept)
# {'資工': ['Alice', 'Cindy'], '電機': ['Bob'], '機械': ['David']}

# ------------------------------------------------------------
# 9.4 dict + set：每位學生選了哪些課（自動去重）
# ------------------------------------------------------------
enroll = [("S001", "Python"), ("S001", "資料結構"), ("S001", "Python"),  # 重複選
          ("S003", "Python"), ("S003", "機器學習")]
courses = {}
for sid, c in enroll:
    courses.setdefault(sid, set()).add(c)   # set 自動去掉重複的 Python
print("S001 選課：", courses["S001"])   # {'Python', '資料結構'}

