print(list(d.values()))      # [1, 2, 3]                      拿 value
print(list(d.items()))       # [('a', 1), ('b', 2), ('c', 3)] 拿 (key, value)
print(set(d))                # {'a', 'b', 'c'}                key 變集合

# ============================================================
# 6.5 ★ 兩個串列 → 字典：zip + dict（超常用）
# ============================================================
keys = ["name", "age", "city"]
vals = ["小明", 18, "台北"]
person = dict(zip(keys, vals))
print(person)   # {'name': '小明', 'age': 18, 'city': '台北'}

# ============================================================
# 6.6 ★ 去重但要「排序後的串列」
# ============================================================
