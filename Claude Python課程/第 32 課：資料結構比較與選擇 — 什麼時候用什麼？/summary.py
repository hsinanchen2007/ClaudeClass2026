# 問題：統計一句話裡每個單字出現幾次。
text = "the cat sat on the mat the cat"
words = text.split()

# ------------------------------------------------------------
# 寫法 A：用串列硬幹（笨拙、慢，O(n²)）—— 反例
# ------------------------------------------------------------
# 每次都要 index() 從頭找，資料一多就很慢。
uniq = []
counts = []
for w in words:
    if w in uniq:                 # in 串列：O(n)
        counts[uniq.index(w)] += 1  # index：又一次 O(n)
    else:
        uniq.append(w)
        counts.append(1)
print("串列版：", dict(zip(uniq, counts)))

# ------------------------------------------------------------
# 寫法 B：用字典（正確選擇，O(n)）—— ★ 推薦
# ------------------------------------------------------------
counter = {}
for w in words:
    counter[w] = counter.get(w, 0) + 1   # get：O(1)
print("字典版：", counter)

# ------------------------------------------------------------
# 寫法 C：只要「不重複的單字」，不要次數 → 用集合
# ------------------------------------------------------------
print("集合版（不重複單字）：", set(words), "共", len(set(words)), "種")

# ------------------------------------------------------------
# 寫法 D：標準函式庫 Counter（實務上最常用）
# ------------------------------------------------------------
from collections import Counter
print("Counter 版：", Counter(words))
print("最常出現：", Counter(words).most_common(2))  # [('the', 3), ('cat', 2)]

# ★ 心得：選對容器，程式碼從「雙層迴圈」變成「一行」。


