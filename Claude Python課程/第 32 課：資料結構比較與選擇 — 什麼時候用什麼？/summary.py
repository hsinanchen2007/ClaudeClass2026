# 一個小系統，剛好把 list / tuple / dict / set 全用上，
# 並示範「該用哪個就用哪個」。

# --- 資料 ---
# list of dict：學生名冊（有序、會增減 → list；每筆是欄位對應 → dict）
roster = [
    {"id": "S001", "name": "Alice", "dept": "資工"},
    {"id": "S002", "name": "Bob",   "dept": "電機"},
    {"id": "S003", "name": "Cindy", "dept": "資工"},
    {"id": "S004", "name": "David", "dept": "機械"},
    {"id": "S005", "name": "Eva",   "dept": "電機"},
]
# tuple 串列：選課紀錄（每筆是固定的 (學號, 課名) → tuple）
enrollments = [
    ("S001", "Python"), ("S001", "資料結構"), ("S001", "線性代數"),
    ("S002", "Java"),   ("S002", "Python"),
    ("S003", "Python"), ("S003", "資料結構"), ("S003", "機器學習"),
    ("S004", "材料力學"), ("S004", "Python"),
    ("S005", "Java"),   ("S005", "Python"),
]

print("=" * 50)
print("課程管理系統")
print("=" * 50)

# 1) dict 查表：用學號 O(1) 找學生
lookup = {s["id"]: s for s in roster}
info = lookup.get("S003")
print(f"\n查詢 S003：{info['name']}（{info['dept']}）")

# 2) dict + set：每位學生選了哪些課（去重）
student_courses = {}
for sid, course in enrollments:
    student_courses.setdefault(sid, set()).add(course)
print("\n各學生選課：")
for sid, cs in student_courses.items():
    print(f"  {lookup[sid]['name']}：{', '.join(sorted(cs))}")

# 3) set 集合運算：比較兩位學生的共同 / 獨有課程
a = student_courses["S001"]
c = student_courses["S003"]
print(f"\nAlice 與 Cindy 共同選課：{a & c}")
print(f"只有 Alice 選：{a - c}")
print(f"只有 Cindy 選：{c - a}")

# 4) dict 計數 + sorted 排行：各課程修課人數排名
count = {}
for _, course in enrollments:
    count[course] = count.get(course, 0) + 1
print("\n課程熱門度排行：")
for rank, (course, n) in enumerate(sorted(count.items(),
                                          key=lambda x: x[1],
                                          reverse=True), 1):
    print(f"  {rank}. {course:8s} {n} 人 {'■' * n}")

# 5) dict + list 分組：依科系列出學生
groups = {}
for s in roster:
    groups.setdefault(s["dept"], []).append(s["name"])
print("\n各科系學生：")
for dept, names in groups.items():
    print(f"  {dept}：{', '.join(names)}")

# 6) set 差集：找出「開了但沒人選」的課
all_courses = {"Python", "Java", "資料結構", "線性代數",
               "機器學習", "材料力學", "演算法", "作業系統"}
chosen = {c for _, c in enrollments}
print(f"\n沒人選的課：{all_courses - chosen or '全部都有人選'}")


