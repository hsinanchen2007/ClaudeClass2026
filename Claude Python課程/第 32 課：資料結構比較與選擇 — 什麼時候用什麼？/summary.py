# ============================================================
# 反例 1：在大型 list 裡反覆用 in 檢查 → 應該用 set
# ============================================================
# ❌ 慢：blacklist 是 list，每次 in 都 O(n)
# blacklist = ["spam1", "spam2", ...]          # 假設一萬筆
# for email in all_emails:                      # 假設五萬封
#     if email in blacklist:  # O(n) × O(m) = O(n·m) 超慢
#         block(email)
#
# ✅ 快：把 blacklist 改成 set，in 變 O(1)
# blacklist = {"spam1", "spam2", ...}
# for email in all_emails:
#     if email in blacklist:  # O(1) × O(m) = O(m)
#         block(email)

# ============================================================
# 反例 2：用 list 一筆筆找對應值 → 應該用 dict
# ============================================================
# ❌ 每次查找都要遍歷
students_list = [("小明", 85), ("小華", 92), ("小美", 78)]
def find_score_slow(name):
    for n, s in students_list:   # O(n)
        if n == name:
            return s
    return None

# ✅ 用字典，一步到位
students_map = {"小明": 85, "小華": 92, "小美": 78}
print(students_map.get("小華"))   # 92（O(1)）

# ============================================================
# 反例 3：用 dict 模擬「連號索引」 → 多此一舉，應該用 list
# ============================================================
# ❌ key 是 0,1,2,3... 其實就是索引，幹嘛用字典
faux = {0: "a", 1: "b", 2: "c"}
print(faux[2])   # c
# ✅ 直接用串列
real = ["a", "b", "c"]
print(real[2])   # c

# ============================================================
# 反例 4：用迴圈 + in 去重（O(n²)）→ 應該用 set
# ============================================================
data = [1, 2, 3, 1, 2, 4, 5, 3]
# ❌ 慢
uniq_slow = []
for x in data:
    if x not in uniq_slow:   # 每次 O(n)
        uniq_slow.append(x)
# ✅ 不在乎順序：直接 set
print(list(set(data)))
# ✅ 要保持順序：用 dict.fromkeys（3.7+ 保序）或 seen set
print(list(dict.fromkeys(data)))   # [1, 2, 3, 4, 5]（保留首次出現順序）

# ============================================================
# 反例 5：需要修改卻用了 tuple / 不會改卻用 list
# ============================================================
# tuple 不可變，硬要改會報錯：
# t = (1, 2, 3); t[0] = 99   # ❌ TypeError
# 規則：之後要 append / 改值 → list；確定固定不動 → tuple（更安全、可當 key）


