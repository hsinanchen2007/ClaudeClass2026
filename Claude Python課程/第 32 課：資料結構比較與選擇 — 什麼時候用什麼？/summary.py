empty_tuple = ()          # 空元組
empty_dict  = {}          # 空字典（★ {} 是字典，不是集合！）
empty_set   = set()       # 空集合（★ 一定要用 set()）
print(type(empty_dict))   # <class 'dict'>
print(type(empty_set))    # <class 'set'>


