# 這是「選錯容器」最常見、最致命的效能問題。
#
# 「x in 容器」這個動作：
#   list / tuple → 從頭找到尾，最差要比對 n 次          → O(n)
#   set / dict   → 用雜湊（hash）直接算出位置，一步到位 → O(1)
#
# 資料量越大，差距越誇張：100 萬筆時可能差好幾千倍。

import time

size = 1_000_000
big_list = list(range(size))               # 串列
big_set  = set(range(size))                # 集合
big_dict = {i: True for i in range(size)}  # 字典

target = 999_999   # 故意找最後一個（串列的最差情況）

start = time.time()
_ = target in big_list      # O(n)：要掃到最後一個才找到
list_time = time.time() - start

start = time.time()
_ = target in big_set       # O(1)：直接定位
set_time = time.time() - start

start = time.time()
_ = target in big_dict      # O(1)：查 key 跟集合一樣快
dict_time = time.time() - start

print(f"串列 in：{list_time:.6f} 秒   ← 慢")
print(f"集合 in：{set_time:.6f} 秒   ← 快")
print(f"字典 in：{dict_time:.6f} 秒   ← 快")

# ★ 結論：
#   只要你的程式裡有「大量資料 + 反覆判斷某值在不在」，
#   就把資料放進 set 或 dict，不要放在 list 裡用 in 檢查。


