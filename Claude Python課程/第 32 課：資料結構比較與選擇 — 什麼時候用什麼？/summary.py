# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. 拿到一個需求，怎麼選資料結構？
#     答：問三個問題——①要不要**改**？（不改優先 tuple）
#     ②要不要**保持順序／用索引取**？（要就 list／tuple）
#     ③主要操作是**查找**還是**走訪**？（頻繁查找用 dict／set）
#     再看要不要**去重**（set）、要不要**鍵值對應**（dict）。
#     追問：需要「有序又要 O(1) 查找」怎麼辦？（用 dict——3.7+ 保證插入序，
#     兩個需求同時滿足）
#
# 🔥 Q2. 各結構的常見操作複雜度？
#     答：list 索引 O(1)、尾端 append 均攤 O(1)、`insert(0,x)` 與 `in` 是 O(n)；
#     dict／set 的查找、插入、刪除平均 O(1)（最壞會因碰撞退化）；
#     tuple 與 list 的索引同為 O(1)。
#     追問：要頻繁在**開頭**插入怎麼辦？（用 `collections.deque`，
#     兩端操作都是 O(1)）
#
# ⚠️ 陷阱. 「dict 和 set 都是雜湊結構，所以都無序」——這句話對嗎？
#     答：**不對**。dict 自 **3.7 起保證插入序**（語言規範，不是實作細節）；
#     set **至今仍不保證任何順序**。兩者的順序保證完全不同。
#     為什麼會錯：從「底層都是雜湊表」推論出「行為一定一樣」。
#     實際上 dict 的插入序是官方明訂的承諾，
#     而 set 的迭代順序會隨 hash 值與 PYTHONHASHSEED 變動。
