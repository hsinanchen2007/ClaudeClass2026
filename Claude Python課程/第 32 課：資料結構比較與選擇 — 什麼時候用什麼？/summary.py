# ╔══════════════════════════════════════════════════════════════════╗
# ║  你的需求                                    → 選這個             ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  有序、會增減、可重複（最通用）              → list               ║
# ║  固定一筆紀錄、不可變、想當 dict key         → tuple              ║
# ║  用「名稱 / 鍵」查資料、建立對應關係         → dict               ║
# ║  去除重複                                    → set                ║
# ║  快速判斷「某值在不在」（大量資料）          → set 或 dict        ║
# ║  聯集 / 交集 / 差集 等集合運算               → set                ║
# ║  需要用 [索引] 取第 n 個                     → list 或 tuple      ║
# ║  需要計數（每個元素出現幾次）                → dict / Counter     ║
# ║  需要分組                                    → dict（值放 list）  ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║                  效能重點（時間複雜度）                            ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  x in list / tuple   → O(n)  慢（資料多時很有感）                 ║
# ║  x in set / dict     → O(1)  快                                   ║
# ║  list[i] 索引取值     → O(1)                                       ║
# ║  dict[key] 取值       → O(1)                                       ║
# ║  list.append         → O(1)（攤銷）                               ║
# ║  list 中間 insert/del → O(n)                                       ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║                  ⚠️ 最常見的選錯地雷                              ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  大 list 反覆 in 檢查      → 改 set（O(n)→O(1)）                  ║
# ║  list 一筆筆找對應值       → 改 dict 查表                          ║
# ║  迴圈 + in 去重            → 改 set / dict.fromkeys               ║
# ║  {} 當空集合              → 它是空字典！要用 set()                ║
# ║  拿 list 當 dict 的 key   → 不行，改用 tuple                      ║
# ║  該變動卻用 tuple         → 改 list；固定不動用 tuple 更安全      ║
# ╚══════════════════════════════════════════════════════════════════╝


