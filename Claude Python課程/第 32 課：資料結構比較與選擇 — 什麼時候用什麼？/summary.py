# 同樣一組資料，用四種容器裝裝看，感受差異：

my_list  = [1, 2, 3, 2, 1]       # 串列：保留順序，也保留重複
my_tuple = (1, 2, 3, 2, 1)       # 元組：跟串列像，但「鎖住」不能改
my_dict  = {"a": 1, "b": 2}      # 字典：用 key 找 value
my_set   = {1, 2, 3}             # 集合：自動去重、無序

print(my_list)    # [1, 2, 3, 2, 1]
print(my_tuple)   # (1, 2, 3, 2, 1)
print(my_dict)    # {'a': 1, 'b': 2}
print(my_set)     # {1, 2, 3}

# ★ 直覺比喻（記住這個就不會選錯）：
#   list  ≈ 排隊隊伍   → 有先後順序，可以有同名的人，可以插隊/離開
#   tuple ≈ 身分證     → 有欄位順序，但內容一旦發了就不能改
#   dict  ≈ 電話簿     → 用「名字（key）」查「號碼（value）」
#   set   ≈ 投票箱     → 不管投入順序，同一人只算一票（去重）

