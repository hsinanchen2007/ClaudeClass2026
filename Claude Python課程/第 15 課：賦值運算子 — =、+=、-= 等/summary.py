# 【面試題】複合賦值與名稱綁定
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. 有哪些複合賦值運算子？它們有什麼共同特性？
#     答：`+=`、`-=`、`*=`、`/=`、`//=`、`%=`、`**=` 等。
#     共同特性是「先取值、運算、再綁回同一個名稱」，
#     而且對可變物件會優先嘗試**就地修改**（`__iadd__` 之類），
#     只有在型別不支援時才退回「建新物件再綁定」。
#     追問：`/=` 用在整數上會怎樣？（結果變 float，因為 `/` 恆回傳 float）
#
# 🔥 Q2. 為什麼 Python 沒有 `++` 與 `--`？
#     答：因為變數是名稱綁定而非可變的記憶體格子，
#     「自增」這個概念不自然；而且 `++x` 在 Python 裡是合法語法——
#     它會被解析成兩個一元正號，結果就是 x 本身，不會有任何遞增效果。
#     追問：那該怎麼寫？（`x += 1`）
#
# ⚠️ 陷阱. 在函式裡對全域變數寫 `x += 1` 會發生什麼？
#     答：拋 **UnboundLocalError**。只要函式內對某個名稱有**賦值動作**，
#     該名稱在整個函式內就被視為區域變數，`+=` 需要先讀取它，
#     於是在還沒賦值前就讀取而失敗。
#     為什麼會錯：以為「有讀有寫」會自動沿用全域的那個。
#     要改全域得先寫 `global x`（改外層函式的變數則用 `nonlocal`）。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】基本賦值 =
# 把右邊的值存進左邊的變數。
# ============================================================

x = 100
name = "信安"
is_student = True

# --- 右邊可以是表達式，先算完再存 ---
total = 100 + 200              # total = 300
area = 3.14 * 5 ** 2           # area = 78.5
greeting = "Hello" + " " + "World"

# --- 自我更新（取出舊值 → 計算 → 存回） ---
count = 0
count = count + 1              # 0 + 1 = 1
count = count + 1              # 1 + 1 = 2
print(count)                   # 2

# ============================================================
# 【重點 2】★ 複合賦值運算子（簡化自我更新）
# ============================================================

# ---- += 加法賦值（最常用！） ----
score = 0
score += 10                    # score = score + 10 → 10
score += 25                    # score = score + 25 → 35
print(score)                   # 35

# --- += 也適用於字串拼接 ---
message = "Hello"
message += " "                 # "Hello "
message += "World"             # "Hello World"
message += "!"                 # "Hello World!"
print(message)

# 逐步組合文字
result = ""
result += "姓名：信安\n"
result += "年齡：25\n"
result += "城市：台北\n"
print(result)

# ---- -= 減法賦值 ----
health = 100
health -= 20                   # 100 - 20 = 80
health -= 35                   # 80 - 35 = 45
print(health)                  # 45

# ---- *= 乘法賦值 ----
money = 1000
money *= 2                     # 翻倍 → 2000
money *= 1.1                   # 加 10% → 2200.0
print(money)

# --- *= 也適用於字串重複 ---
pattern = "★"
pattern *= 5                   # "★★★★★"
print(pattern)

# ---- /= 除法賦值（結果永遠是 float！） ----
pizza = 8
pizza /= 2                    # 8 / 2 = 4.0 ← 注意是 float！
print(pizza)                   # 4.0

# ---- //= 整除賦值 ----
candies = 25
candies //= 4                 # 25 // 4 = 6
print(candies)                 # 6

# ---- %= 取餘數賦值 ----
x = 27
x %= 10                       # 27 % 10 = 7（取個位數）
print(x)                      # 7

# ---- **= 指數賦值 ----
base = 2
base **= 3                    # 2 ** 3 = 8
print(base)                   # 8
base **= 2                    # 8 ** 2 = 64
print(base)                   # 64

# ============================================================
# 【重點 3】多變數同時賦值
# ============================================================

# --- 一行賦不同值 ---
x, y, z = 10, 20, 30
print(x, y, z)                # 10 20 30

name, age, city = "信安", 25, "台北"
print(f"{name}，{age}歲，住在{city}")

# --- 多變數同一值 ---
a = b = c = 0
print(a, b, c)                # 0 0 0

wins = losses = draws = 0      # 統計勝負平

# ============================================================
# 【重點 4】★ 變數交換（Python 魔法）
# ============================================================

a = 10
b = 20

# 一行搞定！（先打包右邊，再分配左邊）
a, b = b, a
print(a, b)                   # 20 10

# --- 也可以輪轉多個變數 ---
a, b, c = 1, 2, 3
a, b, c = c, a, b             # 輪轉！
print(a, b, c)                # 3 1 2

# --- 從串列取值（解包） ---
coordinates = [10, 20]
x, y = coordinates
print(x, y)                   # 10 20

# ============================================================
# 【重點 5】⚠️ 常見錯誤
# ============================================================

# --- ❌ 變數未初始化就使用 += ---
# total += 100                # NameError! total 還不存在

# ✅ 要先初始化
total = 0
total += 100                  # 正確！

# --- ❌ += 和 =+ 的差別 ---
x = 10
x += 5                        # x = x + 5 → 15 ✅
# x =+ 5                     # ⚠️ 這是 x = (+5) → 5（不是加法！）

x = 10
x -= 3                        # x = x - 3 → 7 ✅
# x =- 3                     # ⚠️ 這是 x = (-3) → -3（不是減法！）

# --- ⚠️ /= 結果永遠是 float ---
x = 10
x /= 2
print(x)                      # 5.0（不是 5！）

# 如果需要整數結果，用 //=
x = 10
x //= 2
print(x)                      # 5（整數）

# --- ⚠️ = 和 == 別搞混 ---
x = 10       # ✅ 賦值
x == 10      # ✅ 比較 → True
# if x = 5:  # ❌ SyntaxError!

