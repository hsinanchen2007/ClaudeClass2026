# ============================================================
# 小結：
# 1. = 基本賦值，右邊先算完再存到左邊
# 2. += -= *= /= //= %= **= 複合賦值運算子
# 3. += 最常用，可用於數字累加和字串拼接
# 4. /= 結果永遠是 float，要整數用 //=
# 5. x, y, z = 1, 2, 3 同時賦值
# 6. a, b = b, a 一行交換
# 7. ⚠️ += 和 =+ 不一樣！+= 是累加，=+ 是賦正值
# 8. ⚠️ 使用 += 前要先初始化變數
