# 【面試題】for 迴圈的走訪對象與慣用法
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. 什麼樣的物件可以被 for 走訪？
#     答：實作了**迭代協定**的物件——也就是能被 `iter()` 取得迭代器的。
#     list、tuple、str、dict、set、range、檔案物件、generator 都可以。
#     走訪 dict 時預設拿到的是 **key**。
#     追問：走訪字串會得到什麼？（一個一個字元，因為字串本身就是序列）
#
# 🔥 Q2. 為什麼建議用 `for x in items:` 而不是 `for i in range(len(items)):`？
#     答：前者直接表達意圖、不會有索引越界問題，而且對任何可迭代物件都成立
#     （包括不支援索引的 set 與 generator）。
#     需要索引時用 `enumerate`，需要並行走訪多個序列時用 `zip`。
#     追問：什麼時候真的需要 range(len(...))？
#     （需要**修改**原 list 的對應位置、或要往前往後跳著存取時）
#
# ⚠️ 陷阱. 邊走訪 list 邊刪除元素會發生什麼？
#     答：會**跳過元素**。實測 `n=[2,4,6]`，在迴圈中對每個偶數 `n.remove(x)`，
#     最後得到 **`[4]`** 而不是 `[]`——刪除會讓後面的元素前移，
#     但迭代器的索引仍繼續往前，於是漏掉一個。
#     為什麼會錯：以為迭代器持有的是「元素清單的快照」。
#     實際上它持有的是「原 list + 目前索引」。
#     修法：對副本迭代 `for x in n[:]`，或直接用推導式重建。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】for 迴圈基本語法
# 對一個「可迭代物件」中的每個元素，依序執行區塊程式碼。
# 語法：for 變數 in 可迭代物件:
#           程式碼（縮排 4 格）
# ============================================================

# --- 不用迴圈（重複又累贅） ---
print("我不會再遲到了")
print("我不會再遲到了")
print("我不會再遲到了")

# --- ✅ 用 for 迴圈（一行搞定） ---
for i in range(100):
    print("我不會再遲到了")

# ============================================================
# 【重點 2】遍歷列表（list）
# ============================================================

fruits = ["蘋果", "香蕉", "櫻桃"]

for fruit in fruits:
    print(fruit)
# 輸出：
# 蘋果
# 香蕉
# 櫻桃

# --- 對每個元素做計算 ---
numbers = [10, 20, 30, 40, 50]

for num in numbers:
    print(f"{num} 的兩倍是 {num * 2}")
# 輸出：
# 10 的兩倍是 20
# 20 的兩倍是 40
# ...

# --- 列表可包含不同型別 ---
mixed = ["信安", 25, True, 3.14]

for item in mixed:
    print(f"值：{item}，型別：{type(item).__name__}")

# ============================================================
# 【重點 3】遍歷字串
# 字串也是可迭代的，每次取出一個字元。
# ============================================================

word = "Python"

for char in word:
    print(char)
# 輸出：P y t h o n（每個字元一行）

