# --- 當不需要使用迴圈變數時，用 _ ---
for _ in range(3):
    print("重複三次！")
# _ 表示「我不在乎這個變數的值」

# ============================================================
# 【重點 5】for + if — 迴圈中搭配條件判斷
# ============================================================

scores = [85, 62, 93, 47, 78]

for score in scores:
    print(f"分數：{score}")
    if score >= 60:
        print("  → 及格 ✅")
    else:
        print("  → 不及格 ❌")
    print()                  # 印空行做分隔

# ============================================================
# 【重點 6】★ 三大常見模式
# ============================================================

# --- 模式 1：累加器（Accumulator）— 求和 ---
numbers = [10, 20, 30, 40, 50]
total = 0                        # ← 先初始化為 0

for num in numbers:
    total += num                 # 每一輪累加
    print(f"  加上 {num}，目前累計：{total}")

print(f"總和：{total}")           # 150

# --- 模式 2：計數器（Counter）— 計算符合條件的數量 ---
scores = [85, 62, 93, 47, 78, 55, 91]
pass_count = 0                   # ← 先初始化為 0

for score in scores:
    if score >= 60:
        pass_count += 1          # 符合條件就 +1

print(f"全班 {len(scores)} 人，及格 {pass_count} 人")

# --- 模式 3：追蹤器（Tracker）— 找最大/最小值 ---
numbers = [34, 67, 12, 89, 45, 23]
max_value = numbers[0]           # ← 先假設第一個是最大的

for num in numbers:
    if num > max_value:
        max_value = num          # 發現更大的就更新

print(f"最大值是：{max_value}")   # 89

# ============================================================
# 【重點 7】用索引遍歷 — range(len(...))
# 當你需要同時存取多個列表的對應元素時。
# ============================================================

students = ["小明", "小華", "小美", "小傑", "小芳"]
scores = [85, 62, 93, 47, 78]

total = 0
pass_count = 0
highest = scores[0]
highest_student = students[0]

print("=== 成績報告 ===\n")

for i in range(len(students)):       # i = 0, 1, 2, 3, 4
    name = students[i]
    score = scores[i]
    total += score

    if score >= 60:
        status = "及格 ✅"
        pass_count += 1
    else:
        status = "不及格 ❌"

    if score > highest:
        highest = score
        highest_student = name

    print(f"{name}：{score} 分 → {status}")

average = total / len(students)

print(f"\n=== 統計結果 ===")
print(f"全班平均：{average:.1f} 分")
print(f"及格人數：{pass_count} / {len(students)} 人")
print(f"最高分：{highest_student}（{highest} 分）")

