# ============================================================
# 第 10 課：輸入與輸出 — print() 與 input() 完全攻略 — 重點總結
# ============================================================

# ============================================================
# 【重點 1】print() — 基本輸出
# ============================================================

print("Hello!")            # 字串
print(123)                 # 整數
print(3.14)                # 浮點數
print(True)                # 布林值

# --- 多個參數用逗號分隔（自動加空格） ---
name = "信安"
age = 25
print("我叫", name, "，今年", age, "歲")
# 輸出：我叫 信安 ，今年 25 歲（注意逗號前後有空格）

# --- 字串拼接方式（需要手動轉型，但沒有多餘空格） ---
print("我叫" + name + "，今年" + str(age) + "歲")
# 輸出：我叫信安，今年25歲

# ============================================================
# 【重點 2】sep 參數 — 控制分隔符號
# 預設是空格 " "。
# ============================================================

print("A", "B", "C")               # A B C（預設空格）
print("A", "B", "C", sep="-")      # A-B-C
print("A", "B", "C", sep="★")      # A★B★C
print("A", "B", "C", sep="")       # ABC（無分隔）
print("A", "B", "C", sep="\n")     # 每個佔一行

# --- 實用：印出日期 ---
year, month, day = 2025, 3, 1
print(year, month, day, sep="/")    # 2025/3/1
print(year, month, day, sep="-")    # 2025-3-1

# ============================================================
# 【重點 3】end 參數 — 控制結尾
# 預設是換行 "\n"。
# ============================================================

# --- 預設每個 print 會換行 ---
print("Hello")
print("World")
# 輸出：
# Hello
# World

# --- 用 end 改變結尾，讓多個 print 在同一行 ---
print("Hello", end=" ")
print("World")
# 輸出：Hello World

print("Hello", end="→")
print("World")
# 輸出：Hello→World

print("載入中", end="")
print("...", end="")
print("完成！")
# 輸出：載入中...完成！

# --- 倒數計時 ---
print("倒數：", end="")
print("3", end="...")
print("2", end="...")
print("1", end="...")
print("發射！")
# 輸出：倒數：3...2...1...發射！

# --- sep + end 組合使用 ---
print("蘋果", "香蕉", "橘子", sep="、", end="。\n")
# 輸出：蘋果、香蕉、橘子。

print("A", "B", "C", sep=" + ", end=" = ?\n")
# 輸出：A + B + C = ?

# ============================================================
# 【重點 4】特殊字元（轉義字元）
# ============================================================

# \n  — 換行
print("第一行\n第二行\n第三行")

# \t  — Tab（對齊）
print("姓名\t年齡\t城市")
print("信安\t25\t台北")
print("小明\t30\t高雄")

# \\  — 顯示反斜線
print("檔案路徑：C:\\Users\\Documents")

# \"  — 在雙引號字串中顯示雙引號
print("他說：\"你好！\"")

# --- 三引號也可以做多行文字 ---
print("""
============================
     歡迎使用 Python 程式
============================
""")

# ============================================================
# 【重點 5】input() — 取得使用者輸入
# ⚠️ input() 回傳的永遠是字串！
# ============================================================

name = input("請輸入你的名字：")
print("你好，" + name + "！")

# --- input 回傳字串，需要轉型才能做數學運算 ---
num = input("請輸入數字：")      # 假設輸入 42
print(type(num))                  # <class 'str'>
print(num + num)                  # "4242"（字串拼接，不是加法！）
print(int(num) + int(num))        # 84（轉型後才是數學加法）

# --- 常用寫法：一行搞定 ---
age = int(input("請輸入年齡："))          # 直接轉整數
height = float(input("請輸入身高（cm）：")) # 直接轉浮點數

# ============================================================
# 【重點 6】input().strip() — 去除多餘空格
# 使用者可能不小心打了前後空格。
# ============================================================

name = input("請輸入名字：").strip()
print("【" + name + "】")  # 不會有多餘空格

# ============================================================
# 【重點 7】★ f-string（格式化字串）— 最推薦的方式！
# 語法：f"文字 {變數或表達式}"
# ============================================================

name = "信安"
age = 25

# 方法一：字串拼接（麻煩，需要 str()）
print("我叫" + name + "，今年" + str(age) + "歲")

# 方法二：逗號分隔（簡單，但會多空格）
print("我叫", name, "，今年", age, "歲")

# ★ 方法三：f-string（最推薦！簡潔又好讀）
print(f"我叫{name}，今年{age}歲")

# --- f-string 中可以放表達式 ---
a, b = 10, 3
print(f"{a} + {b} = {a + b}")           # 10 + 3 = 13
print(f"{a} × {b} = {a * b}")           # 10 × 3 = 30
print(f"{a} ÷ {b} = {a / b:.2f}")       # 10 ÷ 3 = 3.33（保留兩位小數）

# --- f-string 搭配 input ---
name = input("你的名字：")
age = int(input("你的年齡："))
print(f"你好，{name}！你明年就 {age + 1} 歲了。")

# ============================================================
# 【重點 8】⚠️ 常見錯誤
# ============================================================

name = "信安"

# ❌ 忘記加 f → 大括號原樣輸出
print("你好，{name}")      # 輸出：你好，{name}

# ✅ 加上 f
print(f"你好，{name}")     # 輸出：你好，信安

# ❌ sep/end 必須放在最後面
# print(sep="-", "A", "B")  # SyntaxError!

# ✅ 正確寫法
print("A", "B", sep="-")   # A-B

# ============================================================
# 【重點 9】綜合應用 — 成績計算器
# ============================================================

chinese = float(input("國文成績："))
english = float(input("英文成績："))
math_score = float(input("數學成績："))

total = chinese + english + math_score
average = total / 3

print(f"  總分：{total}")
print(f"  平均：{average:.1f}")    # :.1f → 保留一位小數

# ============================================================
# 小結：
# 1. print() 可印各種資料，多參數用逗號分隔
# 2. sep 控制分隔符（預設空格），end 控制結尾（預設換行）
# 3. input() 永遠回傳字串 → 數學運算前要 int() 或 float()
# 4. input().strip() 去除使用者輸入的前後空格
# 5. ★ f-string 是最推薦的格式化方式：f"文字{變數}"
# 6. f-string 中可放表達式，:.2f 保留小數位數
# ============================================================
