# 4.1 add(x) — 加入一個元素
# ============================================================

fruits = {"蘋果", "香蕉"}

fruits.add("芒果")
print(fruits)                        # {'蘋果', '香蕉', '芒果'}

# ★ 加入已存在的元素 → 沒有任何效果（不報錯）
fruits.add("香蕉")
