# 集合（set）是 Python 的四大內建容器之一（list, tuple, dict, set）。
# 它源自數學上的「集合」概念，有三個核心特性：
#
#   ★ 無序（Unordered）   → 元素沒有固定順序，不能用索引存取
#   ★ 不重複（Unique）     → 相同的元素只會保留一個
#   ★ 可變（Mutable）      → 可以新增或刪除元素（但元素本身必須不可變）
#
# 直覺比喻：
#   串列 ≈ 排隊（有順序，可以有同名的人）
#   元組 ≈ 身分證（有順序，不能改）
#   字典 ≈ 電話簿（用名字查號碼）
#   集合 ≈ 投票箱（不管投入順序，每個人只算一票）

# 什麼時候用集合？
# 1. 你只關心「有哪些元素」，不關心順序
# 2. 你需要快速判斷「某個值存不存在」
# 3. 你需要去除重複
# 4. 你需要做數學集合運算（聯集、交集、差集）


