# ============================================================
# 5.1 remove(x) — 刪除指定元素（不存在 → 報錯）
# ============================================================

fruits = {"蘋果", "香蕉", "芒果"}
fruits.remove("香蕉")
print(fruits)                        # {'蘋果', '芒果'}

# ⚠️ 元素不存在 → KeyError
# fruits.remove("草莓")             # ❌ KeyError: '草莓'

# ============================================================
# 5.2 ★ discard(x) — 安全刪除（不存在 → 不報錯）
# ============================================================

fruits = {"蘋果", "香蕉", "芒果"}
fruits.discard("香蕉")
print(fruits)                        # {'蘋果', '芒果'}

fruits.discard("草莓")               # 不存在也沒關係
print(fruits)                        # {'蘋果', '芒果'}（安全，不報錯）

# ★ remove vs discard
# remove(x)  → 不存在時拋 KeyError → 適合「確定元素存在」的場景
# discard(x) → 不存在時靜默忽略   → 適合「不確定是否存在」的場景
# 實務上 discard 更常用，因為更安全。

# ============================================================
# 5.3 pop() — 隨機刪除一個元素並回傳
# ============================================================

fruits = {"蘋果", "香蕉", "芒果", "葡萄"}
removed = fruits.pop()
print(f"被移除：{removed}")          # 隨機的一個
print(f"剩餘：{fruits}")

# ⚠️ 空集合 pop → KeyError
# set().pop()                        # ❌ KeyError: 'pop from an empty set'

# ★ 注意：集合的 pop() 跟串列不同！
# 串列 pop() → 刪除最後一個（有序，可預測）
# 集合 pop() → 隨機刪除一個（無序，不可預測）

# ============================================================
# 5.4 clear() — 清空集合
# ============================================================

fruits = {"蘋果", "香蕉", "芒果"}
fruits.clear()
print(fruits)                        # set()（空集合）

# ============================================================
# 刪除方式比較表：
#
#   方式           不存在時       回傳值        用途
#   ────────────────────────────────────────────────
#   remove(x)      KeyError       None          確定存在時
#   discard(x)     不報錯         None          不確定時（★推薦）
#   pop()          KeyError(空)   被刪的值       隨機取一個
#   clear()        不適用         None          清空全部
# ============================================================


