# ============================================================
# 8.1 ★ 去重 — 集合最常見的用途
# ============================================================

# --- 基本去重（不保持順序） ---
names = ["小明", "小華", "小美", "小明", "小華", "小芳", "小美"]
unique = list(set(names))
print(unique)                        # 順序不保證

# --- ★ 保持順序的去重（超常用！） ---
names = ["小明", "小華", "小美", "小明", "小華", "小芳", "小美"]
seen = set()
unique_ordered = []
for name in names:
    if name not in seen:             # 集合的 in 是 O(1)，非常快
        seen.add(name)
        unique_ordered.append(name)
print(unique_ordered)                # ['小明', '小華', '小美', '小芳']

# --- 用 dict.fromkeys() 保持順序去重（Python 3.7+ 字典保持插入順序） ---
unique_ordered2 = list(dict.fromkeys(names))
print(unique_ordered2)               # ['小明', '小華', '小美', '小芳']

# ============================================================
# 8.2 ★ 快速存在性檢查
# ============================================================

# 帳號是否已被使用
registered = {"alice", "bob", "cindy", "david"}
new_user = "eve"

if new_user in registered:           # O(1)，瞬間完成
    print("帳號已被使用")
else:
    print("帳號可用！")
    registered.add(new_user)

# 有效選項驗證
valid_options = {"A", "B", "C", "D"}
answer = "B"
if answer in valid_options:
    print(f"你選了 {answer}")
else:
    print("無效選項！")

# ============================================================
# 8.3 ★★ 比較兩份名單的差異
# ============================================================

old_members = {"小明", "小華", "小美", "小強", "小芳"}
new_members = {"小華", "小美", "小偉", "小芳", "小玲"}

joined = new_members - old_members
print(f"新加入的：{joined}")         # {'小偉', '小玲'}

left = old_members - new_members
print(f"退出的：{left}")             # {'小明', '小強'}

stayed = old_members & new_members
print(f"留下的：{stayed}")           # {'小華', '小美', '小芳'}

all_ever = old_members | new_members
print(f"曾經參加過的：{all_ever}")   # 全部 7 個人

changed = old_members ^ new_members
print(f"有變動的：{changed}")        # {'小明', '小強', '小偉', '小玲'}

