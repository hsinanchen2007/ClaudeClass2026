# 這跟串列的 append 不同！
# 串列 append 同一個值 → 會有兩個
# 集合 add 同一個值 → 只有一個

# ============================================================
# 4.2 update(iterable) — 加入多個元素
# ============================================================

fruits = {"蘋果", "香蕉"}

# 從串列加入
fruits.update(["芒果", "葡萄"])
print(fruits)                        # {'蘋果', '香蕉', '芒果', '葡萄'}

# 從另一個集合加入
fruits.update({"西瓜", "蘋果"})       # 蘋果已存在，自動忽略
print(fruits)                        # {'蘋果', '香蕉', '芒果', '葡萄', '西瓜'}

# 從字串加入（每個字元變一個元素）
chars = set()
chars.update("hello")
