# frozenset 就是「不可變版本的集合」。
# 跟 tuple 是「不可變版本的 list」一樣的道理。

# ============================================================
# 9.1 建立 frozenset
# ============================================================

fs = frozenset([1, 2, 3, 4, 5])
print(fs)                            # frozenset({1, 2, 3, 4, 5})
print(type(fs))                      # <class 'frozenset'>

# ============================================================
# 9.2 不能修改
# ============================================================

# fs.add(6)                          # ❌ AttributeError
# fs.remove(1)                       # ❌ AttributeError
# fs.discard(1)                      # ❌ AttributeError
# fs.pop()                           # ❌ AttributeError
# fs.clear()                         # ❌ AttributeError

# ============================================================
# 9.3 ✅ 可以做集合運算（回傳新的 frozenset）
# ============================================================

fs2 = frozenset([4, 5, 6, 7])
print(fs & fs2)                      # frozenset({4, 5})
print(fs | fs2)                      # frozenset({1, 2, 3, 4, 5, 6, 7})
print(fs - fs2)                      # frozenset({1, 2, 3})

# ============================================================
# 9.4 ★ frozenset 的用途 — 可以當字典 key 和放入集合
# ============================================================

# 因為 frozenset 是不可變的（hashable），所以：

# ✅ 可以當字典的 key
d = {frozenset([1, 2]): "pair_a", frozenset([3, 4]): "pair_b"}
print(d[frozenset([1, 2])])          # pair_a

# ✅ 可以放入另一個集合
s = {frozenset([1, 2]), frozenset([3, 4])}
print(s)

# ❌ 普通 set 不行
# {set([1, 2])}                      # TypeError: unhashable type: 'set'

# ★ set 對應 frozenset，就像 list 對應 tuple
#   set       → 可變，不能當 key
#   frozenset → 不可變，可以當 key


