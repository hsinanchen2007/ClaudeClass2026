# 這是集合跟其他容器最大的不同！
# 集合可以做數學上的聯集、交集、差集、對稱差集。
# 每種運算都有「運算子」和「方法」兩種寫法。

A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}

# ============================================================
# 6.1 聯集（Union）— 所有元素合在一起（去重）
# 數學符號：A ∪ B
# ============================================================

#   A: {1, 2, 3, 4, 5}
#   B:          {4, 5, 6, 7, 8}
#   ──────────────────────────────
#   A ∪ B: {1, 2, 3, 4, 5, 6, 7, 8}

print(A | B)                         # {1, 2, 3, 4, 5, 6, 7, 8}
print(A.union(B))                    # {1, 2, 3, 4, 5, 6, 7, 8}

# union() 可以接受非集合的可迭代物件
print(A.union([10, 11]))             # {1, 2, 3, 4, 5, 10, 11}

# 多個集合聯集
C = {10, 11}
print(A | B | C)                     # {1, 2, 3, 4, 5, 6, 7, 8, 10, 11}
print(A.union(B, C))                 # 同上

# ============================================================
# 6.2 交集（Intersection）— 兩個集合共有的元素
# 數學符號：A ∩ B
# ============================================================

#   A: {1, 2, 3, [4, 5]}
#   B:          {[4, 5], 6, 7, 8}
#   ──────────────────────────────
#   A ∩ B:      {4, 5}

print(A & B)                         # {4, 5}
print(A.intersection(B))             # {4, 5}

# ============================================================
# 6.3 差集（Difference）— 在 A 但不在 B
# 數學符號：A - B（有方向性！）
# ============================================================

#   A: {[1, 2, 3], 4, 5}
#   B:             {4, 5, 6, 7, 8}
#   ──────────────────────────────
#   A - B: {1, 2, 3}              ← 在 A 但不在 B

print(A - B)                         # {1, 2, 3}
print(A.difference(B))               # {1, 2, 3}

# ★★ 方向不同，結果不同！
print(B - A)                         # {6, 7, 8}   ← 在 B 但不在 A
print(B.difference(A))               # {6, 7, 8}

# ============================================================
# 6.4 對稱差集（Symmetric Difference）— 只在一邊出現的
# 數學符號：A △ B
# 等同於 (A - B) ∪ (B - A)，也等同於 (A ∪ B) - (A ∩ B)
# ============================================================

#   A: {[1, 2, 3], 4, 5}
#   B:             {4, 5, [6, 7, 8]}
#   ──────────────────────────────
#   A △ B: {1, 2, 3, 6, 7, 8}     ← 去掉共有的 4, 5

print(A ^ B)                         # {1, 2, 3, 6, 7, 8}
print(A.symmetric_difference(B))     # {1, 2, 3, 6, 7, 8}

# ============================================================
# 6.5 ★ 運算子 vs 方法的差異
# ============================================================

# 運算子（|, &, -, ^）：兩邊都必須是 set
# 方法（union, intersection...）：參數可以是任何可迭代物件

# A | [1, 2]                         # ❌ TypeError
print(A.union([1, 2]))               # ✅ 可以

# ★ 如果你確定兩邊都是 set，用運算子更簡潔。
# 如果參數可能是串列或其他型別，用方法更安全。

# ============================================================
# 6.6 原地運算 — 修改自身而非產生新集合
# ============================================================

# 每種集合運算都有對應的「原地版本」：

A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}

# --- 原地聯集 ---
A_copy = A.copy()
A_copy |= B                         # 等同 A_copy.update(B)
print(A_copy)                        # {1, 2, 3, 4, 5, 6, 7, 8}

# --- 原地交集 ---
A_copy = A.copy()
A_copy &= B                         # 等同 A_copy.intersection_update(B)
print(A_copy)                        # {4, 5}

# --- 原地差集 ---
A_copy = A.copy()
A_copy -= B                         # 等同 A_copy.difference_update(B)
print(A_copy)                        # {1, 2, 3}

# --- 原地對稱差集 ---
A_copy = A.copy()
A_copy ^= B                         # 等同 A_copy.symmetric_difference_update(B)
print(A_copy)                        # {1, 2, 3, 6, 7, 8}

# ============================================================
# 集合運算速查表：
#
#   運算          運算子    方法                     回傳
#   ─────────────────────────────────────────────────────────
#   聯集          A | B     A.union(B)               新集合
#   交集          A & B     A.intersection(B)        新集合
#   差集          A - B     A.difference(B)          新集合
#   對稱差集      A ^ B     A.symmetric_difference(B) 新集合
#
#   原地聯集      A |= B    A.update(B)              修改 A
#   原地交集      A &= B    A.intersection_update(B) 修改 A
#   原地差集      A -= B    A.difference_update(B)   修改 A
#   原地對稱差集  A ^= B    A.symmetric_difference_update(B)  修改 A
# ============================================================


