# ★ 什麼時候用集合？
# 1. 需要去除重複           → set
# 2. 需要快速查找           → set（或 dict）
# 3. 需要聯集/交集/差集     → set
# 4. 不在乎順序             → set

# ★ 什麼時候不用集合？
# 1. 需要保持順序           → list
# 2. 需要索引存取           → list 或 tuple
# 3. 需要存放重複元素       → list
# 4. 需要鍵值對應           → dict


