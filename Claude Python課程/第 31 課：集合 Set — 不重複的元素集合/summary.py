# ============================================================
# 7.1 子集（Subset）— A 的所有元素都在 B 中
# ============================================================

print(A <= B)                        # True  （A 是 B 的子集）
print(A.issubset(B))                 # True

print(A <= C)                        # True  （相等也算子集）

# ============================================================
# 7.2 真子集（Proper Subset）— 是子集但不相等
# ============================================================

print(A < B)                         # True  （A 是 B 的真子集）
print(A < C)                         # False （A == C，不是「真」子集）

# ============================================================
# 7.3 超集（Superset）— B 包含 A 的所有元素
# ============================================================

print(B >= A)                        # True  （B 是 A 的超集）
print(B.issuperset(A))               # True
print(B > A)                         # True  （B 是 A 的真超集）

# ============================================================
# 7.4 不相交（Disjoint）— 完全沒有共同元素
# ============================================================

X = {1, 2, 3}
Y = {4, 5, 6}
Z = {3, 4, 5}

print(X.isdisjoint(Y))              # True  （完全沒有交集）
print(X.isdisjoint(Z))              # False （有共同的 3）


