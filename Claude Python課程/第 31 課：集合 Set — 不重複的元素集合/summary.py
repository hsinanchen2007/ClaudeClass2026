# ============================================================
# 陷阱 1：{} 是空字典，不是空集合
# ============================================================

a = {}                               # dict！
b = set()                            # set

# ============================================================
# 陷阱 2：集合沒有索引
# ============================================================

# fruits = {"蘋果", "香蕉"}
# fruits[0]                          # ❌ TypeError

# ============================================================
# 陷阱 3：set() 轉字串 → 拆成字元，不是整個字串
# ============================================================

# 你想要 {"hello"}，但寫成了 set("hello")
print(set("hello"))                  # {'h', 'e', 'l', 'o'} ← 不是你要的！
print({"hello"})                     # {'hello'} ← 這才是

# ============================================================
# 陷阱 4：轉集合去重會丟失順序
# ============================================================

data = [3, 1, 4, 1, 5, 9]
unique = list(set(data))
# 可能是 [1, 3, 4, 5, 9]，原始順序 [3, 1, 4, 5, 9] 不保證

# ✅ 要保持順序，用 seen set + list 的方式（見 8.1）

# ============================================================
# 陷阱 5：不能放入可變物件
# ============================================================

# {[1, 2, 3]}                        # ❌ 串列不行
# {{"a": 1}}                         # ❌ 字典不行
{(1, 2, 3)}                          # ✅ 元組可以

# ============================================================
# 陷阱 6：True/1 和 False/0 被視為相同
# ============================================================

print({True, 1, 0, False})           # {True, 0} 或 {0, True}
# 因為 True == 1 且 hash(True) == hash(1)

# ============================================================
# 陷阱 7：集合推導式 vs 字典推導式
# ============================================================

s = {x for x in range(5)}            # set（沒冒號）
d = {x: x for x in range(5)}         # dict（有冒號）

# ============================================================
# 陷阱 8：pop() 是隨機的，不像串列的 pop()
# ============================================================

# 串列 pop() → 刪最後一個（可預測）
# 集合 pop() → 隨機刪一個（不可預測）


