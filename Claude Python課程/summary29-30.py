# 假設你要管理一個通訊錄。
# 用串列的話，你必須「記住」每個人在第幾個位置：

phones_list = ["0912-111-111", "0923-222-222", "0934-333-333"]
# 小明是 0，小華是 1，小美是 2... 你要自己記！
# 如果你插入或刪除了一個人，所有位置都會亂掉。

# 用字典的話，你用「名字」直接查：

phones_dict = {
    "小明": "0912-111-111",
    "小華": "0923-222-222",
    "小美": "0934-333-333",
}
print(phones_dict["小明"])     # 0912-111-111 ← 直覺、不怕位置亂

# ★ 字典的核心概念：
#   - 用「鍵」(key) 來查找「值」(value)
#   - 就像查字典：你用「單字」查「解釋」
#   - 就像查電話簿：你用「名字」查「電話號碼」
#   - 查找速度極快（O(1)），不管字典有多大


