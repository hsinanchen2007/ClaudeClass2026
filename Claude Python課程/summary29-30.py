# ============================================================
# 選擇指引
# ============================================================

# 【串列 list】
#   - 有順序、可重複、可修改
#   - 用途：同類資料的集合（一堆成績、一堆名字）
#   - 存取方式：用位置（索引）
#   - 例：scores = [85, 92, 78]

# 【元組 tuple】
#   - 有順序、可重複、不可修改
#   - 用途：固定結構的紀錄（座標、日期、一筆紀錄的欄位）
#   - 存取方式：用位置（索引）
#   - 例：point = (25.03, 121.56)

# 【字典 dict】
#   - 無順序（3.7+ 保持插入序）、key 不可重複、可修改
#   - 用途：用名稱查找的資料（通訊錄、設定、統計結果）
#   - 存取方式：用 key（名稱）
#   - 例：contact = {"name": "小明", "phone": "0912"}

# ★ 簡單判斷法：
#   需要用「名字」查東西 → 字典
#   需要有序的同類集合 → 串列
#   需要不可變的固定紀錄 → 元組
#   需要用字典的 key → 元組（串列不行）

# ★ 常見的組合搭配：
#   串列裡放字典 → [{"name": "小明", "age": 18}, {"name": "小華", "age": 20}]
#   字典裡放串列 → {"小明": [85, 92, 78]}
#   串列裡放元組 → [("小明", 85), ("小華", 92)]


