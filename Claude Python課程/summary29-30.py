# ============================================================
# 方式 1：用 {} 大括號直接建立（最常見、最直覺）
# 語法：{key1: value1, key2: value2, ...}
# ============================================================

# 空字典
empty = {}
print(empty)                   # {}
print(type(empty))             # <class 'dict'>

# 通訊錄
contacts = {
    "小明": "0912-345-678",
    "小華": "0923-456-789",
    "小美": "0934-567-890",
}
# ★ 最後一個元素後面加逗號是合法的（推薦！方便之後新增）

# 值可以是任何型別
student = {
    "name": "小明",                  # 字串
    "age": 18,                       # 整數
    "height": 175.5,                 # 浮點數
    "is_active": True,               # 布林值
    "scores": [85, 92, 78],          # 串列
    "address": {                     # 巢狀字典
        "city": "台北",
        "district": "大安區",
    },
}

# ============================================================
# 方式 2：用 dict() 建構函式 + 關鍵字參數
# ★ 限制：key 必須是合法的 Python 變數名（不能有空格、不能以數字開頭）
# ============================================================

person = dict(name="小明", age=18, city="台北")
print(person)
# {'name': '小明', 'age': 18, 'city': '台北'}

# ⚠️ key 會自動變成字串，但不能用非法變數名
# dict(1st="bad")               # ❌ SyntaxError（數字開頭）
# dict(my name="bad")           # ❌ SyntaxError（有空格）

# ============================================================
# 方式 3：用元組串列（list of tuples）
# 每個元組是一對 (key, value)
# ============================================================

pairs = [("name", "小華"), ("age", 20), ("city", "高雄")]
person2 = dict(pairs)
print(person2)
# {'name': '小華', 'age': 20, 'city': '高雄'}

# ============================================================
# 方式 4：★ 用 zip() 把兩個串列合併成字典
# 這是非常實用的技巧！
# ============================================================

keys = ["name", "age", "city"]
values = ["小美", 19, "台中"]

person3 = dict(zip(keys, values))
print(person3)
# {'name': '小美', 'age': 19, 'city': '台中'}

# zip() 會把兩個串列的元素一一配對：
#   ("name", "小美"), ("age", 19), ("city", "台中")
# 然後 dict() 把每對元組轉成 key: value

# ⚠️ 如果兩個串列長度不同，zip 會以較短的為準
keys = ["a", "b", "c"]
values = [1, 2]
print(dict(zip(keys, values)))   # {'a': 1, 'b': 2}（"c" 被忽略）

# ============================================================
# 方式 5：dict.fromkeys() — 批量建立，所有 key 用同一個預設值
# ============================================================

subjects = ["國文", "英文", "數學"]

# 所有科目初始化為 0 分
scores = dict.fromkeys(subjects, 0)
print(scores)                    # {'國文': 0, '英文': 0, '數學': 0}

# 不給預設值就是 None
empty_scores = dict.fromkeys(subjects)
print(empty_scores)              # {'國文': None, '英文': None, '數學': None}

# ⚠️ 陷阱：如果預設值是可變物件，所有 key 會共享同一個物件！
bad = dict.fromkeys(["a", "b", "c"], [])
bad["a"].append(1)
print(bad)                       # {'a': [1], 'b': [1], 'c': [1]} ← 全部被改了！
# 因為三個 key 指向的是同一個 []

# ============================================================
# 方式 6：字典推導式（Dictionary Comprehension）
# 語法：{key_expr: value_expr for item in iterable}
# （詳見第九章）
# ============================================================

squares = {n: n ** 2 for n in range(1, 6)}
print(squares)                   # {1: 1, 2: 4, 3: 9, 4: 16, 5: 25}


# ████████████████████████████████████████████████████████████████████
# █                                                                  █
# █  第三章：存取值 — 讀取字典中的資料                                 █
# █                                                                  █
# ████████████████████████████████████████████████████████████████████

contacts = {"小明": "0912", "小華": "0923", "小美": "0934"}

# ============================================================
# 方法 1：dict[key] — 直接用方括號
# key 存在 → 回傳值
# key 不存在 → ❌ KeyError（程式會崩潰！）
# ============================================================

print(contacts["小明"])          # 0912
print(contacts["小華"])          # 0923

# ❌ key 不存在 → 直接報錯
# print(contacts["小強"])        # KeyError: '小強'

# ============================================================
# 方法 2：★ dict.get(key, default) — 安全存取
# key 存在 → 回傳值
# key 不存在 → 回傳 None（或你指定的預設值），不報錯
# ============================================================

# 不存在 → 回傳 None
result = contacts.get("小強")
print(result)                    # None

# 不存在 → 回傳自訂預設值
result = contacts.get("小強", "查無此人")
print(result)                    # 查無此人

# 存在 → 正常回傳值
result = contacts.get("小明")
print(result)                    # 0912

# ★ 什麼時候用哪個？
# 1. 你「確定」key 一定存在 → 用 dict[key]（程式碼更簡潔）
# 2. key「可能」不存在 → 用 dict.get(key, default)（更安全）
# 3. key 不存在時你想要程式報錯提醒你 → 用 dict[key]

# ============================================================
# 方法 3：★ dict.setdefault(key, default)
# key 存在 → 回傳現有值，字典不變
# key 不存在 → 新增 key:default 到字典，並回傳 default
# ============================================================

contacts = {"小明": "0912", "小華": "0923"}

# key 存在：回傳現有值，不修改
result = contacts.setdefault("小明", "0000")
print(result)                    # 0912（回傳現有值）
print(contacts)                  # {'小明': '0912', '小華': '0923'}（沒被改）

# key 不存在：新增並回傳
result = contacts.setdefault("小美", "0934")
print(result)                    # 0934（回傳新設定的值）
print(contacts)                  # {'小明': '0912', '小華': '0923', '小美': '0934'}

# ★★★ get() vs setdefault() 超重要的差異：
d = {"a": 1}

d.get("b", 99)
print(d)                         # {'a': 1} ← get() 不修改字典

d.setdefault("b", 99)
print(d)                         # {'a': 1, 'b': 99} ← setdefault() 會修改字典！

# ★★★ setdefault 的殺手級用法：分組收集
# 這是 setdefault 最常見也最重要的應用場景
students = [
    ("小明", "資工"), ("小華", "電機"),
    ("小美", "資工"), ("小強", "機械"),
    ("小芳", "電機"), ("小偉", "資工"),
]

groups = {}
for name, dept in students:
    groups.setdefault(dept, []).append(name)
    # 翻譯：
    #   如果 dept 這個 key 不存在 → 建立一個空串列 []
    #   不管是新建還是已存在 → 回傳那個串列
    #   最後在串列上呼叫 .append(name)

print(groups)
# {'資工': ['小明', '小美', '小偉'], '電機': ['小華', '小芳'], '機械': ['小強']}


# ████████████████████████████████████████████████████████████████████
# █                                                                  █
# █  第四章：檢查 key 是否存在 — in / not in                          █
# █                                                                  █
# ████████████████████████████████████████████████████████████████████

contacts = {"小明": "0912", "小華": "0923"}

# --- 基本用法 ---
print("小明" in contacts)            # True
print("小強" in contacts)            # False
print("小強" not in contacts)        # True

# ★★★ 超重要：in 檢查的是 key，不是 value！
print("0912" in contacts)            # False ← 0912 是 value，不是 key！

# 如果要檢查 value 是否存在，用 .values()
print("0912" in contacts.values())   # True

# --- 實用模式：查詢前先檢查 ---
name = "小明"
if name in contacts:
    print(f"{name} 的電話是 {contacts[name]}")
else:
    print(f"找不到 {name} 的資料")

# --- 實用模式：用 get() 取代 if-else（更簡潔） ---
name = "小強"
phone = contacts.get(name, "未登記")
print(f"{name} 的電話是 {phone}")     # 小強 的電話是 未登記


