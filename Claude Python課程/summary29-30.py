# █  第八章：複製字典 — 別名陷阱、淺複製、深複製                       █
# █                                                                  █
# ████████████████████████████████████████████████████████████████████

# ============================================================
# 8.1 ⚠️ 賦值是別名，不是複製！（跟串列一樣的問題）
# ============================================================

original = {"a": 1, "b": 2}
alias = original                 # alias 和 original 指向同一個字典

alias["c"] = 3
print(original)                  # {'a': 1, 'b': 2, 'c': 3} ← original 也被改了！
print(alias is original)         # True（確實是同一個物件）

# ============================================================
# 8.2 淺複製（Shallow Copy）— 三種方式
# 第一層是獨立的，但內層的可變物件仍然共享
# ============================================================

original = {"a": 1, "b": [2, 3], "c": {"x": 10}}

# 方式一：.copy()
copy1 = original.copy()

# 方式二：dict()
copy2 = dict(original)

# 方式三：{**original}（解包語法）
copy3 = {**original}

# 修改第一層 → 原字典不受影響
copy1["a"] = 999
print(original["a"])             # 1 ← 安全

# ⚠️ 修改內層（可變物件）→ 原字典也被影響！
copy1["b"].append(999)
print(original["b"])             # [2, 3, 999] ← 被改了！

copy1["c"]["x"] = 999
print(original["c"])             # {'x': 999} ← 也被改了！

# 因為淺複製只複製了第一層的「參考」(reference)，
# 內層的串列和字典仍然是同一個物件。

# ============================================================
# 8.3 ★ 深複製（Deep Copy）— 完全獨立
# 用 copy 模組的 deepcopy()
# ============================================================

import copy

original = {"a": 1, "b": [2, 3], "c": {"x": 10}}
deep = copy.deepcopy(original)

# 修改內層 → 原字典完全不受影響
deep["b"].append(999)
deep["c"]["x"] = 999
print(original["b"])             # [2, 3] ← 安全！
print(original["c"])             # {'x': 10} ← 安全！

# ★ 選擇指引：
# 值都是不可變型別（str, int, float, bool, tuple） → copy() 就夠了
# 值包含可變型別（list, dict, set）→ 用 deepcopy() 更安全


