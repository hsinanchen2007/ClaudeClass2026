# ╔══════════════════════════════════════════════════════════════════╗
# ║                       建立字典                                   ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  {}                        空字典                                ║
# ║  {"k": "v", ...}           直接建立                              ║
# ║  dict(k="v", ...)          關鍵字引數（key 須為合法變數名）        ║
# ║  dict([("k","v"), ...])    元組串列                              ║
# ║  dict(zip(keys, vals))     合併兩個串列                          ║
# ║  dict.fromkeys(keys, val)  批量建立（共享同一預設值）             ║
# ║  {k: v for k, v in ...}    推導式                               ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║                       存取值                                     ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  d[k]                      取值（不存在→KeyError）               ║
# ║  d.get(k)                  取值（不存在→None）                   ║
# ║  d.get(k, default)         取值（不存在→default）                ║
# ║  d.setdefault(k, default)  取值＋自動新增（不存在→新增並回傳）    ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║                       檢查                                      ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  k in d                    key 是否存在                          ║
# ║  k not in d                key 是否不存在                        ║
# ║  v in d.values()           value 是否存在                        ║
# ║  len(d)                    鍵值對數量                            ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║                       新增 / 修改                                ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  d[k] = v                  新增或修改                            ║
# ║  d.update(other)           批量合併（重複key被覆蓋）              ║
# ║  d | other                 合併→新字典（Python 3.9+）            ║
# ║  d |= other                原地合併（Python 3.9+）               ║
# ║  d.setdefault(k, v)        不存在才新增                          ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║                       刪除                                      ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  del d[k]                  按key刪（不回傳，不存在→KeyError）     ║
# ║  d.pop(k)                  按key刪並回傳值（不存在→KeyError）     ║
# ║  d.pop(k, default)         按key刪並回傳值（不存在→default）      ║
# ║  d.popitem()               刪最後一對，回傳(k,v)元組              ║
# ║  d.clear()                 清空字典                               ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║                       遍歷                                      ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  for k in d:               走訪 key                             ║
# ║  for k in d.keys():        走訪 key（明確寫法）                  ║
# ║  for v in d.values():      走訪 value                           ║
# ║  for k, v in d.items():    走訪 key+value（★推薦）              ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║                       複製                                      ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  d.copy()                  淺複製                                ║
# ║  dict(d)                   淺複製                                ║
# ║  {**d}                     淺複製（解包語法）                     ║
# ║  copy.deepcopy(d)          深複製（完全獨立）                     ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║                       排序                                      ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  sorted(d)                 按 key 排序→回傳 key 串列             ║
# ║  sorted(d.items())         按 key 排序→回傳 (k,v) 串列          ║
# ║  sorted(d.items(),         按 value 排序                        ║
# ║    key=lambda x: x[1])                                          ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║                    ⚠️ 常見陷阱                                   ║
# ╠══════════════════════════════════════════════════════════════════╣
# ║  d[k] 不存在 → KeyError          用 get() 或 in 檢查            ║
# ║  in 查的是 key，不是 value        查 value 用 in d.values()      ║
# ║  {} 是 dict，不是 set            空集合要用 set()                ║
# ║  走訪時不能增刪 → RuntimeError    走訪 list(d.keys()) 的副本     ║
# ║  賦值 = 是別名                   複製用 .copy() 或 deepcopy()   ║
# ║  copy() 是淺複製                 內層可變物件仍共享              ║
# ║  推導式忘記冒號 → set             {k: v for ...} 才是 dict       ║
# ║  fromkeys() 共享可變預設值        用推導式 {k: [] for ...}       ║
# ║  True==1, False==0 同 key        設計 key 時注意                 ║
