# 這是字典最經典的應用之一
# ============================================================

# --- 統計每個字元出現幾次 ---
text = "mississippi"
counter = {}
for char in text:
    counter[char] = counter.get(char, 0) + 1
    # 解析：
    #   counter.get(char, 0) → 如果 char 已存在，回傳目前的次數；否則回傳 0
    #   + 1 → 次數 +1
    #   存回 counter[char]

print(counter)                   # {'m': 1, 'i': 4, 's': 4, 'p': 2}

# --- 統計每個單字出現幾次 ---
sentence = "the cat sat on the mat the cat"
word_count = {}
for word in sentence.split():
    word_count[word] = word_count.get(word, 0) + 1
print(word_count)
# {'the': 3, 'cat': 2, 'sat': 1, 'on': 1, 'mat': 1}

# --- 找出出現最多次的字元 ---
most_common = max(counter, key=counter.get)
print(f"出現最多次的字元：'{most_common}'，共 {counter[most_common]} 次")
# 出現最多次的字元：'i'，共 4 次

