# ============================================================
# 5.1 新增與修改 — dict[key] = value
# key 不存在 → 新增
# key 已存在 → 修改（覆蓋舊值）
# ============================================================

contacts = {"小明": "0912"}

# 新增
contacts["小華"] = "0923"
print(contacts)                  # {'小明': '0912', '小華': '0923'}

# 修改
contacts["小明"] = "0999"
print(contacts)                  # {'小明': '0999', '小華': '0923'}

# ============================================================
# 5.2 update() — 批量新增/修改
# 可以一次合併另一個字典的內容
# 重複的 key 會被覆蓋
# ============================================================

contacts = {"小明": "0912", "小華": "0923"}
new_data = {"小美": "0934", "小明": "0999"}

contacts.update(new_data)
print(contacts)
# {'小明': '0999', '小華': '0923', '小美': '0934'}
# 小明被覆蓋成 0999，小美是新增的

# update() 也可以用關鍵字引數
contacts.update(小強="0945", 小玲="0956")
print(contacts)

# ============================================================
# 5.3 | 與 |= 合併運算子（Python 3.9+）
# ============================================================

a = {"x": 1, "y": 2}
b = {"y": 3, "z": 4}

# | 產生新字典（原字典不變）
c = a | b
print(c)                         # {'x': 1, 'y': 3, 'z': 4}（重複 key 取右邊的）
print(a)                         # {'x': 1, 'y': 2}（a 沒變）

# |= 原地合併（等同 a.update(b)）
a |= b
print(a)                         # {'x': 1, 'y': 3, 'z': 4}（a 被修改了）

# ============================================================
# 5.4 刪除 — 四種方式
# ============================================================

contacts = {"小明": "0912", "小華": "0923", "小美": "0934", "小強": "0945"}

# ── 方式 1：del dict[key] ──
# 根據 key 刪除，不回傳值
# key 不存在 → KeyError
del contacts["小強"]
print(contacts)                  # {'小明': '0912', '小華': '0923', '小美': '0934'}
# del contacts["不存在"]         # ❌ KeyError

# ── 方式 2：★ pop(key, default) ──
# 刪除並回傳被刪除的值
# 可提供 default 避免 KeyError
removed = contacts.pop("小華")
print(removed)                   # 0923（回傳被刪除的值）
print(contacts)                  # {'小明': '0912', '小美': '0934'}

# key 不存在且沒給 default → KeyError
# contacts.pop("不存在")         # ❌ KeyError

# key 不存在但有給 default → 回傳 default，不報錯
result = contacts.pop("不存在", "查無此人")
print(result)                    # 查無此人

# ── 方式 3：popitem() ──
# 刪除最後插入的鍵值對，並回傳 (key, value) 元組
# 空字典呼叫 → KeyError
contacts = {"小明": "0912", "小華": "0923", "小美": "0934"}
last = contacts.popitem()
print(last)                      # ('小美', '0934')
print(contacts)                  # {'小明': '0912', '小華': '0923'}

# {}.popitem()                   # ❌ KeyError: 'popitem(): dictionary is empty'

# ── 方式 4：clear() ──
# 清空字典中所有鍵值對（字典本身還在，只是變成空的）
contacts.clear()
print(contacts)                  # {}

# ============================================================
# 刪除方式比較表：
#
#   方式           回傳值       key不存在時          用途
#   ───────────────────────────────────────────────────────
#   del d[k]       無           KeyError            只刪除不需要值
#   d.pop(k)       被刪的值     KeyError            需要用到被刪的值
#   d.pop(k, def)  被刪的值     回傳 def            安全刪除
#   d.popitem()    (k, v) 元組  KeyError (空字典)    刪除最後一對
#   d.clear()      None         不適用               清空全部
# ============================================================


# ████████████████████████████████████████████████████████████████████
# █                                                                  █
# █  第六章：遍歷字典 — keys(), values(), items()                     █
# █                                                                  █
# ████████████████████████████████████████████████████████████████████

student = {"name": "小明", "age": 18, "city": "台北", "score": 85}

# ============================================================
# 6.1 for 直接走訪 — 預設走訪的是 key
# ============================================================

for key in student:
    print(f"{key} → {student[key]}")
# name → 小明
# age → 18
# city → 台北
# score → 85

# ============================================================
# 6.2 .keys() — 取得所有的鍵
# ============================================================

print(student.keys())            # dict_keys(['name', 'age', 'city', 'score'])

for key in student.keys():
    print(key, end=" ")          # name age city score
print()

# keys() 可以轉成串列
key_list = list(student.keys())
print(key_list)                  # ['name', 'age', 'city', 'score']

# ============================================================
# 6.3 .values() — 取得所有的值
# ============================================================

print(student.values())          # dict_values(['小明', 18, '台北', 85])

for value in student.values():
    print(value, end=" ")        # 小明 18 台北 85
print()

# ============================================================
# 6.4 ★★★ .items() + 拆包 — 同時取得 key 和 value（最推薦！）
# ============================================================

print(student.items())
# dict_items([('name', '小明'), ('age', 18), ('city', '台北'), ('score', 85)])

# ★ 搭配拆包，優雅地走訪
for key, value in student.items():
    print(f"{key}: {value}")
# name: 小明
# age: 18
# city: 台北
# score: 85

# ============================================================
# 6.5 帶索引遍歷 — enumerate + items()
# ============================================================

for i, (key, value) in enumerate(student.items(), 1):
    print(f"  {i}. {key} = {value}")
# 1. name = 小明
# 2. age = 18
# 3. city = 台北
# 4. score = 85


