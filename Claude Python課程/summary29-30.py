# ============================================================
# 規則 1：key 必須是不可變型別（hashable）
# ============================================================

# ✅ 可以當 key 的型別（不可變）
d1 = {"name": "小明"}                     # 字串 ✅
d2 = {1: "一月", 2: "二月", 3: "三月"}     # 整數 ✅
d3 = {3.14: "圓周率"}                     # 浮點數 ✅
d4 = {True: "真", False: "假"}            # 布林值 ✅
d5 = {(25.03, 121.56): "台北"}            # 元組 ✅
d6 = {None: "空值"}                       # None ✅

# ❌ 不能當 key 的型別（可變）
# {[1, 2, 3]: "串列"}                     # ❌ TypeError: unhashable type: 'list'
# {{"a": 1}: "字典"}                      # ❌ TypeError: unhashable type: 'dict'
# {{1, 2}: "集合"}                        # ❌ TypeError: unhashable type: 'set'

# ★ 為什麼？
# 字典在內部用「雜湊表」(hash table) 實作。
# key 會被算出一個雜湊值(hash value)，用來快速定位。
# 如果 key 是可變的，修改後雜湊值就會變，找不到原來的位置。
# 所以 Python 規定：key 必須是不可變的。

# ============================================================
# 規則 2：key 不能重複，重複會被覆蓋
# ============================================================

data = {"a": 1, "b": 2, "a": 3}
print(data)                      # {'a': 3, 'b': 2}
print(len(data))                 # 2（只有兩個 key，"a" 的值被覆蓋成 3）

# ============================================================
# 規則 3：value 可以是任何型別，也可以重複
# ============================================================

flexible = {
    "string": "hello",
    "number": 42,
    "float": 3.14,
    "bool": True,
    "list": [1, 2, 3],
    "tuple": (4, 5, 6),
    "dict": {"nested": True},
    "none": None,
    "also_42": 42,               # value 可以重複
}

# ============================================================
# 規則 4：Python 3.7+ 字典保持插入順序
# ============================================================

# Python 3.6 及之前：字典是無序的
# Python 3.7 開始：字典保持插入順序（這是語言規範的一部分）

ordered = {}
ordered["c"] = 3
ordered["a"] = 1
ordered["b"] = 2
print(ordered)                   # {'c': 3, 'a': 1, 'b': 2}（保持插入順序）

# ============================================================
# ⚠️ 特殊注意：True/False 和 1/0 是相同的 key
# ============================================================

# 因為 True == 1 且 False == 0，所以它們被視為相同的 key
d = {True: "布林真", 1: "整數一"}
print(d)                         # {True: '整數一'}（key 保留 True，但值被 1 覆蓋）
print(len(d))                    # 1

d = {0: "零", False: "假"}
print(d)                         # {0: '假'}


