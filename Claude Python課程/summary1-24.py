# --- 索引（index）— 用位置存取字元 ---
# 正索引從 0 開始，負索引從 -1（最後一個）開始
text = "Python"
#       P  y  t  h  o  n
# 正：  0  1  2  3  4  5
# 負： -6 -5 -4 -3 -2 -1

