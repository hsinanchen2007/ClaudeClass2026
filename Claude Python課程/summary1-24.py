# 🔥 Q1. 變數賦值到底發生了什麼事？
#     答：是**名稱綁定**，不是「把值放進盒子」。
#     `a = [1]; b = a` 之後兩個名字指向**同一個物件**——
#     實測 `b.append(2)` 後 `a` 也變成 `[1,2]`、`a is b` → True。
#     追問：那 `b = b + [2]` 呢？（建立新物件並重新綁定 b，`a` 不受影響；
#     但 `b += [2]` 是就地修改，`a` 會跟著變）
#
# 🔥 Q2. `and`／`or` 回傳什麼？falsy 的值有哪些？
#     答：回傳**運算元本身**而非布林值（短路求值）——
#     實測 `0 or 'a'` → `'a'`、`'x' and 'y'` → `'y'`。
#     falsy 的有 `0`、`0.0`、`''`、`[]`、`{}`、`set()`、`None`、`False`。
#     追問：`x = a or default` 在 `a = 0` 時有什麼 bug？
#     （0 是 falsy 會誤用 default，應寫 `a if a is not None else default`）
#
# ⚠️ 陷阱. 迴圈變數在迴圈結束後還在嗎？推導式呢？
#     答：普通 for 迴圈**會洩漏**——實測 `for i in range(3): pass` 之後
#     `i` 仍是 **2**；但推導式**不會**，實測 `x='outer'; [x for x in range(3)]`
#     之後 `x` 仍是 `'outer'`（Python 3 給了推導式獨立作用域）。
#     為什麼會錯：以為兩者的作用域規則一致。
#     這是 Python 2 → 3 的重要改變，只改了推導式，沒改普通迴圈。
