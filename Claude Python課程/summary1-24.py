# ============================================================
# 2.3 ★ sep 參數 — 自訂分隔符號（預設是空格）
# ============================================================

print("A", "B", "C")             # A B C（預設空格）
print("A", "B", "C", sep="-")   # A-B-C
print("A", "B", "C", sep="★")   # A★B★C
print("A", "B", "C", sep="")    # ABC（無分隔）
print("A", "B", "C", sep="\n")  # 每個佔一行

# 實用：印出日期
year, month, day = 2025, 3, 15
print(year, month, day, sep="/") # 2025/3/15
print(year, month, day, sep="-") # 2025-3-15

# ============================================================
