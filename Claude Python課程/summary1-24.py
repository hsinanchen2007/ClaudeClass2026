print(5 == 3)                       # False

# --- ★★★ Truthy 和 Falsy — 哪些值被視為 False？ ---
# 以下值被視為 False（Falsy）：
print(bool(0))                      # False — 數字 0
print(bool(0.0))                    # False — 浮點數 0.0
print(bool(""))                     # False — 空字串
print(bool(None))                   # False — None
print(bool([]))                     # False — 空串列
print(bool({}))                     # False — 空字典
print(bool(False))                  # False — False 本身

# 其他所有值都被視為 True（Truthy）：
print(bool(1))                      # True
print(bool(-5))                     # True — 負數也是 True！
print(bool("hello"))                # True
print(bool(" "))                    # True — ⚠️ 空格也算有內容！
print(bool("0"))                    # True — ⚠️ 字串 "0" 不是空的！
print(bool("False"))                # True — ⚠️ 字串 "False" 也不是空的！

# --- 布林值可參與數學運算（True=1, False=0） ---
print(True + True)                  # 2
