# ============================================================
# 2.1 基本輸出 — 印出各種資料
# ============================================================

print("Hello World")             # 印出文字（字串）
print(123)                       # 印出整數
print(3.14)                      # 印出浮點數
print(True)                      # 印出布林值
print(None)                      # 印出 None（代表「什麼都沒有」）

# ============================================================
# 2.2 多個參數 — 逗號分隔，自動加空格
# ============================================================

print("姓名", "小明", "年齡", 18)
# 輸出：姓名 小明 年齡 18
# ★ 多個參數之間會自動用空格分隔

# 可以混合不同型別
print("圓周率是", 3.14159)       # 圓周率是 3.14159
