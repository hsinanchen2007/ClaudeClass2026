# --- 也可以用反斜杠 \ 轉義 ---
print('It\'s a beautiful day')   # \' 轉義單引號
print("他說：\"你好\"")           # \" 轉義雙引號

# ============================================================
# 3.2 ★ 轉義字元 — 在字串中表示特殊字元
# ============================================================

# \n  — 換行（最常用！）
print("第一行\n第二行\n第三行")
# 輸出：
# 第一行
# 第二行
# 第三行

# \t  — Tab（水平對齊）
print("姓名\t年齡\t城市")
print("小明\t18\t台北")
print("小華\t25\t高雄")
# 輸出會對齊：
# 姓名    年齡    城市
# 小明    18      台北
# 小華    25      高雄

# \\  — 顯示一個反斜線
print("C:\\Users\\Documents")     # C:\Users\Documents

# \'  — 在單引號字串中顯示單引號
# \"  — 在雙引號字串中顯示雙引號

# ============================================================
# 3.3 ★ 原始字串 r"..." — 忽略所有轉義字元
# 常用於檔案路徑和正規表示式
# ============================================================

# 沒有 r 前綴：\n 會被當成換行
print("C:\new\test")             # C: 然後換行 ew 然後 Tab est（亂掉了）

# 加上 r 前綴：所有字元原樣輸出
print(r"C:\new\test")            # C:\new\test ← 正確！

