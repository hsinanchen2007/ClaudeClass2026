# ============================================================
# 3.1 字串的定義 — 單引號 vs 雙引號
# 兩者完全等效，選一種風格統一使用即可。
# ============================================================

print("Hello World")             # 雙引號
print('Hello World')             # 單引號 — 效果完全一樣

# --- 當字串本身包含引號時，交替使用 ---
print("It's a beautiful day")    # 字串含 ' → 外面用 "
