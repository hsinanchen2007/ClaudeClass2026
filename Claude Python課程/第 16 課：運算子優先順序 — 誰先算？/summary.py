# ============================================================
# 第 16 課：運算子優先順序 — 誰先算？ — 重點總結
# ============================================================

# ============================================================
# 【重點 1】★ 完整優先順序（由高到低）
#   1. ()     括號 — 最優先
#   2. **     指數 — 從右到左計算！
#   3. +x -x  正負號
#   4. * / // %  乘、除、整除、取餘 — 同級，從左到右
#   5. + -    加、減 — 同級，從左到右
#   6. == != > < >= <=  比較運算子
#   7. not    邏輯非
#   8. and    邏輯且
#   9. or     邏輯或
#   10. =     賦值（最後才做）
# ============================================================

# ============================================================
# 【重點 2】括號 () — 最優先，可改變計算順序
# ============================================================

# 沒括號：先乘後加
print(3 + 4 * 2)             # 11（先 4*2=8，再 3+8=11）

# 有括號：先算括號裡的
print((3 + 4) * 2)           # 14（先 3+4=7，再 7*2=14）

# 更多範例
print(10 - 2 * 3)            # 4 （先 2*3=6，再 10-6=4）
print((10 - 2) * 3)          # 24（先 10-2=8，再 8*3=24）

# --- 多層括號：從最內層開始 ---
result = ((2 + 3) * (10 - 4)) / 3
# 步驟 1：(2+3)=5, (10-4)=6
# 步驟 2：5 * 6 = 30
# 步驟 3：30 / 3 = 10.0
print(result)                # 10.0

# ============================================================
# 【重點 3】** 指數 — 優先級很高，且從右到左計算！
# ============================================================

print(2 * 3 ** 2)            # 18（先 3**2=9，再 2*9=18）
print(2 ** 3 * 4)            # 32（先 2**3=8，再 8*4=32）

# --- ★ 連續次方從右到左算！ ---
print(2 ** 3 ** 2)           # 512
# 不是 (2**3) ** 2 = 64
# 而是 2 ** (3**2) = 2 ** 9 = 512

# 用括號明確順序
print((2 ** 3) ** 2)         # 64（先 2³=8，再 8²=64）
print(2 ** (3 ** 2))         # 512（先 3²=9，再 2⁹=512）

# ============================================================
# 【重點 4】⚠️ 負號 vs 指數
# ** 比負號 - 更優先！
# ============================================================

print(-3 ** 2)               # -9（先 3²=9，再加負號 → -9）
print((-3) ** 2)             # 9 （先取 -3，再平方 → 9）

# ============================================================
# 【重點 5】同級運算子 — 從左到右計算
# ============================================================

# --- 乘除同級，從左到右 ---
print(10 * 2 / 4)            # 5.0（先 10*2=20，再 20/4=5.0）
print(10 / 2 * 4)            # 20.0（先 10/2=5.0，再 5.0*4=20.0）
print(20 // 3 % 2)           # 0（先 20//3=6，再 6%2=0）

# --- 加減同級，從左到右 ---
print(10 - 3 + 2)            # 9（先 10-3=7，再 7+2=9）

# --- 乘除 比 加減 優先 ---
print(5 + 10 // 3)           # 8（先 10//3=3，再 5+3=8）
print(5 + 10 % 3)            # 6（先 10%3=1，再 5+1=6）

# ============================================================
# 【重點 6】★ 混合運算的計算順序
# 先算「算術」→ 再算「比較」→ 最後算「邏輯」
# ============================================================

result = 3 + 2 > 4 and 10 - 5 == 5

# 步驟 1：算術運算
#   (3 + 2) > 4 and (10 - 5) == 5
#     5     > 4 and    5     == 5

# 步驟 2：比較運算
#   (5 > 4) and (5 == 5)
#    True   and  True

# 步驟 3：邏輯運算
#   True and True → True

print(result)                # True

# ============================================================
# 【重點 7】邏輯運算子之間的順序：not > and > or
# ============================================================

print(True or False and not False)
# 步驟 1：not False → True
# 步驟 2：False and True → False
# 步驟 3：True or False → True
# 結果：True

# ============================================================
# 【重點 8】複雜運算的分步拆解
# ============================================================

result = 2 + 3 * 4 ** 2 - 10 / 5
# 步驟 1：** 最先算 → 4**2 = 16
#   2 + 3 * 16 - 10 / 5
# 步驟 2：* 和 / 同級，從左到右
#   2 + 48 - 2.0
# 步驟 3：+ 和 - 同級，從左到右
#   50 - 2.0 = 48.0
print(result)                # 48.0

# ============================================================
# 【重點 9】★ 最佳實踐：有疑慮就加括號！
# ============================================================

# ❌ 不清楚，要靠記住優先順序
result = 10 + 20 * 3 / 2

# ✅ 加上括號，一目了然
result = 10 + ((20 * 3) / 2)
print(result)                # 40.0

# ❌ 邏輯運算不加括號，難理解
# result = a > 5 and b < 10 or c == 0

# ✅ 加括號，清楚表達意圖
# result = (a > 5 and b < 10) or (c == 0)

# ============================================================
# 小結：
# 1. 優先順序：() > ** > +x -x > * / // % > + - > 比較 > not > and > or
# 2. ** 指數從右到左計算！（2**3**2 = 2**(3**2) = 512）
# 3. ** 比負號優先！（-3**2 = -9，不是 9）
# 4. 同級運算子從左到右
# 5. 先算術 → 再比較 → 最後邏輯
# 6. 邏輯順序：not > and > or
# 7. ★ 最重要的規則：有疑慮就加括號！
# ============================================================
