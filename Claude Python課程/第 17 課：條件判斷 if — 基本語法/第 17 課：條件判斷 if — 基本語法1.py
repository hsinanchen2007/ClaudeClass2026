# if 條件判斷
# if 是 Python 中的條件判斷語句，用於根據條件的真假來執行不同的程式碼區塊。
# 基本語法如下：

# ═══════════════════════════════════════════════════════════════════════════
# 【面試題】條件判斷 if 的基本語法
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. Python 為什麼用縮排而不是大括號？
#     答：縮排是**語法的一部分**，強制讓「視覺結構」與「執行結構」一致，
#     從語言層面消滅了 C 系語言常見的「縮排騙人」bug。
#     混用 tab 與空格會拋 TabError／IndentationError；PEP 8 規定用 4 個空格。
#     追問：某個區塊還沒想好要寫什麼，但語法上需要一行怎麼辦？（`pass`）
#
# 🔥 Q2. `if` 條件裡可以放非布林值嗎？
#     答：可以，Python 會對它取真假值（truthiness）。
#     falsy 的有 `0`、`0.0`、`''`、`[]`、`{}`、`set()`、`None`、`False`，
#     其餘皆為 truthy。所以 `if my_list:` 就是慣用的「非空判斷」。
#     追問：`if len(x) > 0:` 與 `if x:` 哪個好？（後者更 Pythonic，
#     但若 x 可能是 None，兩者行為不同——前者會炸，後者安靜走 else）
#
# ⚠️ 陷阱. `if x = 5:` 會發生什麼？
#     答：**SyntaxError**。Python 刻意讓賦值不是運算式，
#     以杜絕 C 語言那種把 `==` 誤寫成 `=` 的經典 bug。
#     為什麼會錯：C/Java 背景的人習慣「賦值有值」。
#     Python 3.8 起確實有海象運算子 `:=` 可在條件中賦值，
#     但它是**獨立的運算子**、必須明確寫出來，不會被誤打誤撞觸發。
# ═══════════════════════════════════════════════════════════════════════════

age = 20

if age >= 18:
    print("你已經成年了！")
# 在這個例子中，我們定義了一個變數 age，然後使用 if 語句來判斷 age 是否大於或等於 18。
# 如果條件為真（即 age 大於或等於 18），則會執行 print("你已經成年了！") 這行程式碼，輸出 "你已經成年了！"。

age = 15

if age >= 18:
    print("你已經成年了！")

print("程式繼續執行...")
# 在這個例子中，當 age 為 15 時，條件 age >= 18 為假，因此 if 區塊內的程式碼不會被執行，輸出將是 "程式繼續執行..."。


if True:
    print("第一行，屬於 if")    # ✅ 縮排 4 格
    print("第二行，也屬於 if")  # ✅ 縮排 4 格
print("第三行，不屬於 if")      # 沒有縮排，獨立執行
# 在這個例子中，第一行和第二行都屬於 if 區塊，因為它們有相同的縮排（4 格）。
# 第三行沒有縮排，因此不屬於 if 區塊，無論條件是否為真都會執行。

