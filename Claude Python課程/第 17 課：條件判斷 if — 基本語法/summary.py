# 【面試題】條件判斷的寫法與風格
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. 什麼是三元運算式？Python 怎麼寫？
#     答：`值A if 條件 else 值B`——條件寫在**中間**，
#     這和 C 的 `條件 ? A : B` 順序不同。
#     例：`status = "成年" if age >= 18 else "未成年"`。
#     追問：可以省略 else 嗎？（不行，三元運算式必須是完整運算式，一定要有值）
#
# 🔥 Q2. 判斷一個值是否落在區間內，最好的寫法是什麼？
#     答：用**鏈式比較** `if 0 <= score <= 100:`，
#     比 `if score >= 0 and score <= 100:` 更短、更接近數學寫法，
#     而且中間的 `score` 只求值一次。
#     追問：如果 score 是個函式呼叫呢？（鏈式寫法只呼叫一次，
#     and 版本會呼叫兩次——這時鏈式不只好讀，還更有效率）
#
# ⚠️ 陷阱. `if x == True:` 與 `if x:` 有什麼不同？
#     答：不等價。`x = 1` 時兩者都成立，但 `x = 2` 時
#     `x == True` 是 **False**（因為 `True == 1`，而 2 != 1），
#     `if x:` 卻是 True。`x = [1,2]` 時差異更明顯。
#     為什麼會錯：把「真假值判斷」誤當成「與 True 相等」。
#     PEP 8 明確建議不要寫 `== True`／`== False`，直接用 `if x:` 或 `if not x:`。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】if 基本語法
# 條件為 True 時，執行縮排區塊內的程式碼。
# 語法：if 條件:
#           程式碼（縮排 4 格）
# ============================================================

age = 20

if age >= 18:
    print("你已經成年了！")
# age = 20，20 >= 18 為 True → 輸出 "你已經成年了！"

# --- 條件為 False 時，if 區塊不執行，直接跳過 ---
age = 15

if age >= 18:
    print("你已經成年了！")     # ← 不會執行（15 < 18）

print("程式繼續執行...")         # ← 這行不在 if 區塊內，照常執行
# 輸出：程式繼續執行...

# ============================================================
# 【重點 2】★ 縮排（Indentation）— Python 的核心規則！
# 用「縮排」決定程式碼是否屬於 if 區塊。
# 標準縮排是 4 個空格。
# ============================================================

if True:
    print("第一行，屬於 if")     # ✅ 縮排 4 格 → 屬於 if
    print("第二行，也屬於 if")   # ✅ 縮排 4 格 → 屬於 if
print("第三行，不屬於 if")       # 沒有縮排 → 獨立執行（不管 if 結果）

# --- ❌ 常見縮排錯誤 ---

# 錯誤 1：if 後面沒有縮排
# if age >= 18:
# print("成年了")               # ❌ IndentationError: expected an indented block

# 錯誤 2：縮排不一致
# if age >= 18:
#     print("成年了")
#       print("可以投票")        # ❌ IndentationError: unexpected indent

# ============================================================
# 【重點 3】⚠️ if 語句結尾要加冒號 :
# ============================================================

# ❌ 忘記冒號
# if age >= 18                  # SyntaxError: expected ':'
#     print("成年了")

# ✅ 正確
if age >= 18:
    print("成年了")

# ============================================================
# 【重點 4】if 區塊可以有多行程式碼
# 只要維持相同的縮排，都屬於 if 區塊。
# ============================================================

wallet = 600

if wallet >= 500:
    print("太棒了！錢夠吃大餐")
    print(f"你有 {wallet} 元")
    remaining = wallet - 500
    print(f"吃完大餐還剩 {remaining} 元")
# 以上 4 行都屬於 if 區塊，只有 wallet >= 500 時才執行

# ============================================================
# 【重點 5】if 搭配邏輯運算子 and / or
# ============================================================

# --- and：兩個條件都要滿足 ---
age = 25
has_id = True

if age >= 18 and has_id:
    print("可以進入酒吧")

# --- or：任一條件滿足即可 ---
day = "Saturday"

if day == "Saturday" or day == "Sunday":
    print("今天是週末！")

# ============================================================
# 【重點 6】if 搭配 input()
# ============================================================

# --- 密碼驗證 ---
password = input("請輸入密碼：")

if password == "python123":
    print("密碼正確，歡迎進入！")

# --- 年齡判斷（搭配 int() 轉型） ---
age = int(input("請輸入你的年齡："))

if age >= 18:
    print(f"你 {age} 歲了，已經成年！")

# ============================================================
# 【重點 7】★ Truthy / Falsy 值直接當條件
# 非布林值也能放在 if 中，Python 會自動判斷真假。
# 回顧：0, 0.0, "", None, [] 是 Falsy，其他都是 Truthy。
# ============================================================

# --- 非空字串 → True ---
name = "信安"
if name:                     # "信安" 是 Truthy → True
    print(f"你好，{name}！")

# --- 數字 0 → False ---
money = 0
if money:                    # 0 是 Falsy → False
    print("你有錢！")        # ← 不會執行

# --- 空串列 → False ---
items = []
if items:                    # [] 是 Falsy → False
    print("清單有東西")      # ← 不會執行

# ============================================================
# 【重點 8】判斷「屬於 if」vs「不屬於 if」
# ============================================================

x = 0
if x:                        # 0 是 Falsy → False
    print("A")               # ← 不執行
print("B")                   # ← 不在 if 區塊，照常執行
# 輸出：B

# ============================================================
# 小結：
# 1. if 條件: → 條件為 True 時執行縮排區塊
# 2. ★ 縮排是 Python 的核心語法，標準 4 格空格
# 3. if 結尾要加冒號 :
# 4. if 區塊可多行，只要縮排一致
# 5. 搭配 and / or 可做多條件判斷
# 6. 搭配 input() + int()/float() 做互動式判斷
# 7. Truthy/Falsy 值可直接當條件（0, "", [], None → False）
# 8. 沒有縮排的程式碼不屬於 if，無論如何都會執行
