# 3. 字串裡的 # 不是註解
# 4. # 後面加空格，保持好閱讀
# 5. 好註解解釋「為什麼」，壞註解是廢話
# 6. ⚠️ 註解和程式碼不一致比沒有註解更糟
# 7. TODO / FIXME 標記方便追蹤待辦事項
