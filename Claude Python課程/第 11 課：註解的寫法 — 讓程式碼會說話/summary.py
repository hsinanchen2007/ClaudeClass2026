# 【面試題】註解的用途與慣例
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. 好的註解應該寫什麼？
#     答：寫 **why**（為什麼這樣做、當初的取捨、外部限制），
#     不要複述 **what**（程式碼本身已經說得很清楚的事）。
#     `i += 1  # i 加一` 是零價值註解；
#     `# 這裡不能用 set，因為下游依賴插入順序` 才有價值。
#     追問：註解與程式碼不一致時該信哪個？（信程式碼——所以過時的註解
#     比沒有註解更糟）
#
# 🔥 Q2. 行內註解的格式規範是什麼？
#     答：PEP 8 規定行內註解與程式碼之間至少空 **兩個空格**，
#     且 `#` 後面接一個空格。整行註解則與所註解的程式碼同縮排。
#     追問：為什麼 PEP 8 說要少用行內註解？（容易讓行變長、
#     也容易在改程式時忘記同步）
#
# ⚠️ 陷阱. 被 `#` 註解掉的程式碼，裡面的語法錯誤會被抓到嗎？
#     答：**不會**。註解在詞法分析階段就被丟棄，內容再怎麼壞都不影響編譯。
#     但這也代表：你把一段壞掉的程式註解起來「先擺著」，
#     編譯器再也不會提醒你，它可能就這樣爛在專案裡。
#     為什麼會錯：以為註解掉的程式碼「還在被檢查」。
#     正解是——不要留註解掉的程式碼，那是版本控制該做的事。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】單行註解 — 用 # 開頭
# Python 會忽略 # 後面的所有文字。
# ============================================================

# 這是一行註解
radius = 5
area = 3.14159 * radius ** 2    # 面積 = π × r²（行尾註解）
print(area)

# ============================================================
# 【重點 2】多行註解 — 用 ''' 或 """ 三引號
# 嚴格來說是多行字串，但常用來當多行註解。
# ============================================================

"""
程式名稱：BMI 計算器
作者：信安
日期：2025-03-01
說明：根據身高體重計算 BMI 指數
"""

# 也可以用多個 # 來做多行註解（更傳統的做法）
# ================================
# 程式名稱：BMI 計算器
# 作者：信安
# 日期：2025-03-01
# ================================

# ============================================================
# 【重點 3】⚠️ 字串裡的 # 不是註解
# ============================================================

print("# 這不是註解")                # ← 字串裡的 # 會正常印出
# 輸出：# 這不是註解

print("請輸入密碼：# 至少 8 個字元")  # 字串中的 # 不影響
# 輸出：請輸入密碼：# 至少 8 個字元

# ============================================================
# 【重點 4】註解的好習慣
# ============================================================

# --- ✅ # 後面加一個空格，比較好讀 ---
# 計算總金額
total = 100 * 5

# ❌ 擠在一起，不好讀
#計算總金額

# ============================================================
# 【重點 5】暫時停用程式碼 — 用 # 註解掉
# ============================================================

