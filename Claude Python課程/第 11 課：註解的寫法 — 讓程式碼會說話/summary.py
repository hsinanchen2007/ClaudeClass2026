# ============================================================
# 【重點 7】用註解做分隔線 — 分區整理程式碼
# ============================================================

# ===== 第一步：取得使用者輸入 =====
name = input("姓名：")
age = int(input("年齡："))
height = float(input("身高（cm）："))

# ===== 第二步：進行計算 =====
birth_year = 2025 - age
