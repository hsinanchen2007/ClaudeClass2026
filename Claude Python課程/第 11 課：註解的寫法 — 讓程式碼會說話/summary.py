# ============================================================
# 【重點 8】好的註解 vs 壞的註解
# ============================================================

# --- ❌ 壞的註解：廢話（程式碼已經很清楚了） ---
age = 25            # 把 25 存到 age（廢話！）
name = "信安"       # 把 "信安" 存到 name（廢話！）

# --- ✅ 好的註解：解釋「為什麼」或補充背景 ---
age = 25            # 預設年齡，用於測試

# 使用 0.85 是因為要扣除 15% 的稅金
net_income = 100000 * 0.85

# --- ❌ 最危險的情況：註解和程式碼不一致！ ---
# 計算稅後收入（稅率 10%）   ← 註解說 10%
income = 1000 * 0.85           # ← 程式碼是 15%！到底哪個對？

# ============================================================
# 【重點 9】好的註解風格範例
# ============================================================

# ❌ 壞的風格
x = 10          # 設定 x 為 10（廢話）
y = x * 2       # y 等於 x 乘以 2（廢話）

# ✅ 好的風格
# 商品定價策略：基本價加倍後加上 5 元包裝費
base_price = 10
selling_price = base_price * 2
final_price = selling_price + 5     # 包裝費固定 5 元

# ============================================================
# 【重點 10】格式化小技巧 — f-string 中的格式
# （本課範例中用到）
# ============================================================

meal_cost = 500.0
tip_rate = 0.20
tip = meal_cost * tip_rate

print(f"餐費：{meal_cost:.0f} 元")        # 500（不顯示小數）
print(f"小費（{tip_rate:.0%}）：{tip:.0f} 元")  # 小費（20%）：100 元
# :.0f  → 浮點數不顯示小數
# :.0%  → 自動轉成百分比顯示（0.20 → 20%）

# ============================================================
