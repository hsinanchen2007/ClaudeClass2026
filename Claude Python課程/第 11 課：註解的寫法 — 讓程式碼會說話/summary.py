# ============================================================
# 【重點 3】⚠️ 字串裡的 # 不是註解
# ============================================================

print("# 這不是註解")                # ← 字串裡的 # 會正常印出
# 輸出：# 這不是註解

print("請輸入密碼：# 至少 8 個字元")  # 字串中的 # 不影響
# 輸出：請輸入密碼：# 至少 8 個字元

