# ============================================================
# 第 26 課：串列 List（二）— 新增、刪除、修改 — 重點總結
# ============================================================
# ★ 串列是可變的（mutable），可以直接修改內容，記憶體位址不變。
# 相比之下，字串是不可變的（immutable）。
# ============================================================

# ============================================================
# 【重點 1】新增元素 — append()、extend()、insert()
# ============================================================

# --- append()：在尾端加入一個元素 ---
fruits = ["蘋果", "香蕉"]
fruits.append("芒果")
print(fruits)                # ['蘋果', '香蕉', '芒果']

# ⚠️ append 加入串列時，整個串列當成「一個元素」
a = [1, 2, 3]
a.append([4, 5])
print(a)                     # [1, 2, 3, [4, 5]] ← 巢狀了！

# --- extend()：把另一個串列的元素逐一加入 ---
b = [1, 2, 3]
b.extend([4, 5])
print(b)                     # [1, 2, 3, 4, 5] ← 每個元素獨立加入

# ★ append vs extend 差異：
# append([4, 5]) → [1, 2, 3, [4, 5]]    長度 +1
# extend([4, 5]) → [1, 2, 3, 4, 5]      長度 +2

# --- insert(index, element)：在指定位置插入 ---
colors = ["紅", "黃", "藍"]
colors.insert(1, "橙")       # 在索引 1 插入
print(colors)                # ['紅', '橙', '黃', '藍']

colors.insert(0, "白")       # 在最前面插入
print(colors)                # ['白', '紅', '橙', '黃', '藍']

# --- + 運算子：產生新串列，原串列不變 ---
a = [1, 2]
b = [3, 4]
c = a + b
print(a)                     # [1, 2] ← 不變
print(c)                     # [1, 2, 3, 4] ← 新串列

# ============================================================
# 【重點 2】刪除元素 — remove()、del、pop()、clear()
# ============================================================

# --- remove(value)：刪除第一個出現的指定值 ---
fruits = ["蘋果", "香蕉", "芒果", "香蕉"]
fruits.remove("香蕉")
print(fruits)                # ['蘋果', '芒果', '香蕉']
# ★ 只刪除第一個 "香蕉"，第二個還在！

# ⚠️ 元素不存在 → ValueError
# fruits.remove("草莓")     # ❌ ValueError

# ✅ 安全做法：先檢查再刪除
target = "草莓"
if target in fruits:
    fruits.remove(target)
else:
    print(f"{target} 不在串列中")

# --- del：根據索引刪除（不回傳值） ---
numbers = [10, 20, 30, 40, 50]
del numbers[2]               # 刪除索引 2
print(numbers)               # [10, 20, 40, 50]

# del 也可以刪除一段範圍（切片刪除）
del numbers[1:3]
print(numbers)               # [10, 50]

# del numbers                # 刪除整個變數（之後存取會 NameError）

# --- pop(index)：刪除並回傳指定位置的元素 ---
fruits = ["蘋果", "香蕉", "芒果", "葡萄"]

last = fruits.pop()          # 不給參數 → 刪除最後一個
print(last)                  # 葡萄
print(fruits)                # ['蘋果', '香蕉', '芒果']

second = fruits.pop(1)       # 刪除索引 1
print(second)                # 香蕉
print(fruits)                # ['蘋果', '芒果']

# ★ del vs pop 差異：
# del numbers[0]             → 刪除，不回傳
# removed = numbers.pop(0)   → 刪除並回傳被刪的值

# --- clear()：清空串列（串列本身還在） ---
nums = [1, 2, 3, 4, 5]
nums.clear()
print(nums)                  # []（空串列，但變數還在）

# ============================================================
# 【重點 3】修改元素 — 用索引或切片
# ============================================================

# --- 用索引修改單一元素 ---
fruits = ["蘋果", "香蕉", "芒果"]
fruits[1] = "草莓"
print(fruits)                # ['蘋果', '草莓', '芒果']

# --- 用切片修改一段範圍 ---
nums = [1, 2, 3, 4, 5]

# 等量替換
nums[1:3] = [20, 30]
print(nums)                  # [1, 20, 30, 4, 5]

# 不等量替換（串列長度會改變！）
nums[1:3] = [200, 300, 400, 500]
print(nums)                  # [1, 200, 300, 400, 500, 4, 5]

# --- 用切片插入（不刪除原有元素） ---
nums = [1, 2, 5, 6]
nums[2:2] = [3, 4]           # start == end → 在索引 2 位置插入
print(nums)                  # [1, 2, 3, 4, 5, 6]

# ============================================================
# 【重點 4】★★★ 串列是可變的（mutable） — 別名陷阱
# ============================================================

# --- 串列修改後，記憶體位址不變 ---
fruits = ["蘋果", "香蕉"]
print(id(fruits))            # 例如 140234567890
fruits.append("芒果")
print(id(fruits))            # 同一個位址！

# --- ⚠️ 賦值只是建立別名，不是複製！ ---
a = [1, 2, 3]
b = a                        # b 和 a 指向同一個串列！

b.append(4)
print(a)                     # [1, 2, 3, 4] ← a 也被影響了！
print(a is b)                # True（同一個物件）

# ============================================================
# 【重點 5】★ 如何正確複製串列
# ============================================================

original = [1, 2, 3]

# 方法一：切片複製（最常用）
copy1 = original[:]

# 方法二：list() 建構
copy2 = list(original)

# 方法三：.copy() 方法
copy3 = original.copy()

# 修改副本不會影響原始串列
copy1.append(99)
print(original)              # [1, 2, 3]（不受影響）
print(copy1)                 # [1, 2, 3, 99]

# ❌ 以為是複製，其實是別名
a = [1, 2, 3]
b = a                        # ← 這不是複製！
b[0] = 999
print(a)                     # [999, 2, 3] ← a 被改了！

# ✅ 真正的複製
a = [1, 2, 3]
b = a.copy()
b[0] = 999
print(a)                     # [1, 2, 3] ← a 沒被影響

# ============================================================
# 【重點 6】⚠️ 常見錯誤與陷阱
# ============================================================

# --- ❌ 一邊走訪一邊刪除 → 結果不正確 ---
nums = [1, 2, 3, 4, 5, 6]
for n in nums:
    if n % 2 == 0:
        nums.remove(n)
print(nums)                  # [1, 3, 5, 6] ← 6 沒被刪掉！

# ✅ 安全做法一：建立新串列（列表推導式）
nums = [1, 2, 3, 4, 5, 6]
odds = [n for n in nums if n % 2 != 0]
print(odds)                  # [1, 3, 5]

# ✅ 安全做法二：走訪副本
nums = [1, 2, 3, 4, 5, 6]
for n in nums[:]:            # nums[:] 是副本
    if n % 2 == 0:
        nums.remove(n)
print(nums)                  # [1, 3, 5]

# --- ❌ append/extend/insert 沒有回傳值（回傳 None） ---
nums = [1, 2, 3]
result = nums.append(4)
print(result)                # None（不是 [1,2,3,4]！）
print(nums)                  # [1, 2, 3, 4]（串列本身已改）

# ✅ 正確寫法：不需要接回傳值
nums.append(5)               # 直接呼叫即可

# ============================================================
# 【重點 7】新增、刪除、修改方法速查表
# ============================================================

# 新增：
# .append(x)        → 尾端加入一個元素
# .extend(iterable) → 把多個元素逐一加入
# .insert(i, x)     → 在索引 i 處插入
# list1 + list2     → 產生新串列（原串列不變）
#
# 刪除：
# .remove(x)        → 刪除第一個出現的 x（不存在 → ValueError）
# del list[i]       → 根據索引刪除（不回傳）
# del list[i:j]     → 切片刪除
# .pop()            → 刪除最後一個並回傳
# .pop(i)           → 刪除索引 i 並回傳
# .clear()          → 清空串列
#
# 修改：
# list[i] = value   → 用索引修改
# list[i:j] = [...]  → 用切片修改（可改變長度）
# list[i:i] = [...]  → 用切片插入（不刪除）
#
# 複製：
# list[:]           → 切片複製（最常用）
# list.copy()       → 複製方法
# list(original)    → list() 建構

# ============================================================
# 小結：
# 1. 新增：append（尾端加一個）、extend（逐一加入）、insert（指定位置）
# 2. 刪除：remove（按值）、del（按索引）、pop（按索引並回傳）、clear（清空）
# 3. 修改：直接用索引 list[i] = value，或用切片批量修改
# 4. ★★★ 賦值是別名（b = a 指向同一個），不是複製！
# 5. ★ 複製用 a[:] 或 a.copy() 或 list(a)
# 6. ⚠️ 一邊走訪一邊刪除會出錯 → 走訪副本或建新串列
# 7. ⚠️ append/extend/insert 回傳 None，不能接回傳值
# 8. ★ 串列可變（mutable），字串不可變（immutable）
# ============================================================
