# 🔥 Q1. 巢狀條件的執行順序是什麼？
#     答：外層條件先判斷，只有成立時才會進入內層——
#     內層條件在外層為假時**完全不會被求值**。
#     這代表把「成本高」或「可能出錯」的判斷放內層是有意義的優化。
#     追問：那外層條件應該放哪一個？（放最能快速排除大多數情況的那個）
#
# 🔥 Q2. 巢狀條件與 elif 鏈可以互相改寫嗎？
#     答：語意上經常可以，但**結構意義不同**：
#     巢狀表達的是「階層關係」（先有票，才談身高），
#     elif 鏈表達的是「平行互斥」（不是 A 就是 B 就是 C）。
#     追問：兩者都能寫時該選哪個？（選能反映領域邏輯的那個，
#     不要只看縮排少）
#
# ⚠️ 陷阱. 巢狀寫法裡，內層的 else 到底配對到哪個 if？
#     答：配對到**同縮排層級**最近的那個 if。
#     Python 靠縮排決定，不會有 C 語言的 dangling-else 歧義——
#     但也因此，**縮排錯一格，else 就換了一個主人**，而且語法完全合法、
#     不會報錯，只會安靜地算出錯誤結果。
#     為什麼會錯：以為「語法沒錯」就代表「配對如我所想」。
#     這是縮排即語法的雙面刃。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】巢狀 if — 在 if 區塊裡面再放 if
# 先判斷外層條件，成立後才繼續判斷內層條件。
# ============================================================

has_ticket = input("你有票嗎？(y/n)：")

if has_ticket == "y":
    height = int(input("你的身高是幾公分？"))
    if height >= 140:
        print("歡迎搭乘雲霄飛車！")
    else:
        print("抱歉，身高不足 140 公分，無法搭乘")
else:
    print("請先去購票處買票！")
# 結構：外層判斷「有沒有票」→ 內層判斷「身高是否足夠」

# ============================================================
# 【重點 2】巢狀 if 的優勢 — 能區分不同的錯誤原因
# ============================================================

# --- ✅ 巢狀寫法：能分別提示帳號錯 or 密碼錯 ---
username = input("帳號：")

if username == "admin":
    password = input("密碼：")
    if password == "1234":
        print("登入成功！")
    else:
        print("密碼錯誤！")          # ← 知道是密碼的問題
else:
    print("查無此帳號！")              # ← 知道是帳號的問題

