# 【重點 3】巢狀 if 搭配 if-elif-else — 會員折扣系統
# ============================================================

is_member = input("是否為會員？(y/n)：")
total = int(input("消費金額："))

if is_member == "y":
    # 會員折扣規則
    if total >= 1000:
        discount = total * 0.8
        print(f"會員 + 滿千 → 打 8 折，應付 {discount:.0f} 元")
    elif total >= 500:
        discount = total * 0.9
        print(f"會員 + 滿五百 → 打 9 折，應付 {discount:.0f} 元")
    else:
        discount = total * 0.95
        print(f"會員基本優惠 → 打 95 折，應付 {discount:.0f} 元")
else:
    # 非會員折扣規則
    if total >= 1000:
        discount = total * 0.9
        print(f"非會員 + 滿千 → 打 9 折，應付 {discount:.0f} 元")
    else:
        discount = total
        print(f"非會員原價，應付 {discount:.0f} 元")

# ============================================================
# 【重點 4】★ 巢狀 vs and 的選擇
# ============================================================

# --- 用 and 簡化（兩個條件都要滿足，且不需區分錯誤原因） ---
age = 25
has_id = True

# 巢狀寫法
if age >= 18:
    if has_id:
        print("可以進入")

# 用 and 簡化（效果完全一樣）
if age >= 18 and has_id:
    print("可以進入")

# --- 什麼時候用巢狀？ ---
# ✅ 需要區分不同錯誤原因
# ✅ 內層判斷依賴外層的結果（如：先確認有票再問身高）
# ✅ 不同層有不同的處理邏輯

# --- 什麼時候用 and？ ---
# ✅ 只需要知道「全部通過」或「沒通過」
# ✅ 條件之間沒有依賴關係

