height = 155
has_heart_disease = False
is_pregnant = False

if not has_ticket:
    print("請先購票")
elif age < 12:
    print("年齡不足")
elif height < 140:
    print("身高不足")
elif has_heart_disease:
    print("心臟病患者不能搭乘")
elif is_pregnant:
    print("孕婦不能搭乘")
else:
    print("歡迎搭乘！")
