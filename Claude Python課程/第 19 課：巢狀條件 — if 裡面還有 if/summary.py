# 【重點 6】綜合應用 — 電影票價系統
# 外層判斷平日/週末，內層判斷年齡/學生身分。
# ============================================================

print("=== 電影票價系統 ===")
day = input("今天星期幾？(weekday/weekend)：")
age = int(input("請輸入年齡："))
is_student = input("是否為學生？(y/n)：")

if day == "weekend":
    # 週末票價
    if age < 6:
        price = 0
        print("6 歲以下免費！")
    elif age >= 65:
        price = 150
        print("敬老票：150 元")
    else:
        price = 350
        print(f"週末全票：{price} 元")
else:
    # 平日票價
    if age < 6:
        price = 0
        print("6 歲以下免費！")
    elif age >= 65:
        price = 100
        print("敬老票：100 元")
    elif is_student == "y":
        price = 180
        print("平日學生票：180 元")
    else:
        price = 280
        print(f"平日全票：{price} 元")

print(f"\n最終票價：{price} 元")

