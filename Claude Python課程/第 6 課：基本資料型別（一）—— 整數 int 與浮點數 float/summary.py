# ═══════════════════════════════════════════════════════════════════════════
# 【面試題】數值型別的邊界情況
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. Python 的整數會溢位嗎？
#     答：不會。`int` 是任意精度，`2 ** 1000` 照樣算得出來，
#     上限只有記憶體。這和 C/Java 的固定寬度整數（會 wrap around）完全不同。
#     追問：那代價是什麼？（大整數運算比機器原生整數慢、也更佔記憶體）
#
# 🔥 Q2. `int(-3.99)` 與 `-7 // 2` 的取整方向一樣嗎？
#     答：**不一樣**。實測 `int(-3.99)` → `-3`（**向零截斷**），
#     `-7 // 2` → `-4`（**向下取整 floor**）。兩者對負數的行為剛好相反。
#     追問：`-7 % 2` 是多少？（實測 `1`——Python 的模運算結果符號跟**除數**走，
#     與 C/Java 給 `-1` 不同；這保證了 `a == (a//b)*b + a%b` 恆成立）
#
# ⚠️ 陷阱. `a = 256; b = 256; a is b` 與換成 257 結果一樣嗎？
#     答：這題本身就是陷阱。寫死字面值時，**編譯器常數摺疊**常讓兩者都是 True；
#     要看真實快取行為必須繞過摺疊。實測用 `int(str(v))` 在執行期建立：
#     256 → True、257 → False。
#     為什麼會錯：這是 **CPython 的實作細節**（small integer cache，
#     本機實測範圍 -5..256），**不是語言保證**，其他實作可能不同。
#     面試正解只有一句：**數值比較永遠用 `==`，不要用 `is`**。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】整數 int — 沒有小數部分的數字
# Python 的整數沒有大小限制，可以非常大。
# ============================================================

age = 18
year = 2024
temperature = -5         # 負整數
count = 0                # 零

print(type(age))         # <class 'int'>

# --- Python 整數可以非常大 ---
big_number = 99999999999999999999999999999999999999
print(big_number)

# --- 用底線分隔增加可讀性（不影響數值） ---
population = 23_000_000  # 等同於 23000000
print(population)        # 23000000

# ============================================================
# 【重點 2】浮點數 float — 有小數部分的數字
# ============================================================

pi = 3.14159
height = 175.5
score = 100.0            # 整數寫成小數形式也是 float

print(type(height))      # <class 'float'>

# --- 科學記號表示法 ---
speed_of_light = 3e8     # 3 × 10^8 = 300000000.0
tiny_number = 1e-9       # 1 × 10^-9 = 0.000000001
print(speed_of_light)    # 300000000.0
print(tiny_number)       # 1e-09

# ============================================================
# 【重點 3】int 與 float 混合運算
# 規則：只要有一邊是 float，結果就是 float
# ============================================================

print(10 + 5)            # 15    — int + int = int
print(3.5 + 2.5)         # 6.0   — float + float = float
print(10 + 3.5)          # 13.5  — int + float = float ★

# --- / 除法永遠回傳 float（即使整除） ---
print(10 / 2)            # 5.0（不是 5！）
print(type(10 / 2))      # <class 'float'>

# ============================================================
# 【重點 4】各種算術運算
# ============================================================

print(10 + 5)            # 15   — 加法
print(10 - 3)            # 7    — 減法
print(4 * 3)             # 12   — 乘法
print(10 / 3)            # 3.33 — 除法（結果是 float）
print(10 // 3)           # 3    — 整除（捨去小數）
print(10 % 3)            # 1    — 取餘數
print(2 ** 3)            # 8    — 指數（2 的 3 次方）

# ============================================================
# 【重點 5】整除 // 與取餘數 %
# ============================================================

# --- 整除 // ---
print(7 // 2)            # 3（3.5 → 取整 → 3）

# --- 取餘數 % ---
print(10 % 3)            # 1（10 = 3×3 + 1，餘 1）
print(12 % 4)            # 0（整除，無餘數）

# --- 判斷奇偶數：看除以 2 的餘數 ---
number = 17
if number % 2 == 0:
    print("偶數")
else:
    print("奇數")         # 17 % 2 = 1 → 奇數

# --- 餘數的循環特性 ---
# 0, 1, 2, 0, 1, 2, 0...
for i in range(7):
    print(i % 3, end=" ")  # 0 1 2 0 1 2 0
print()

# ============================================================
# 【重點 6】指數運算 **
# ============================================================

print(2 ** 3)            # 8     — 2³
print(5 ** 2)            # 25    — 5²
print(10 ** 0)           # 1     — 任何數的 0 次方 = 1
print(9 ** 0.5)          # 3.0   — 開根號（√9 = 3）
print(27 ** (1/3))       # 3.0   — 開三次根號
print(2 ** -1)           # 0.5   — 2⁻¹ = 1/2

# ============================================================
# 【重點 7】int ↔ float 型別轉換
# ============================================================

# --- float → int：直接截斷小數（不是四捨五入！） ---
print(int(3.9))          # 3（不是 4！）
print(int(-3.9))         # -3（往零的方向截斷）

# --- int → float ---
print(float(18))         # 18.0

# ============================================================
# 【重點 8】運算子優先順序
# 括號 () > 指數 ** > 乘除餘 * / % // > 加減 + -
# ============================================================

print(2 + 3 * 4)         # 14（先乘後加）
print((2 + 3) * 4)       # 20（括號優先）
print(2 ** 3 * 2)        # 16（先次方再乘）

# 建議：有疑慮就加括號，更清楚！
result = 10 + ((20 * 3) / 2)
print(result)            # 40.0

# ============================================================
# 【重點 9】⚠️ 浮點數精度問題
# 因為電腦用二進制存浮點數，某些十進制數無法精確表示。
# ============================================================

print(0.1 + 0.2)         # 0.30000000000000004（不是 0.3！）

# --- 解決方案 1：用 round() ---
print(round(0.1 + 0.2, 1))  # 0.3

# --- 解決方案 2：比較時用容差值 ---
a = 0.1 + 0.2
b = 0.3
tolerance = 0.0001
print(abs(a - b) < tolerance)  # True

# --- 解決方案 3：用 Decimal 模組（金額推薦） ---
from decimal import Decimal
price = Decimal('19.99')
tax = Decimal('0.15')
total = price + tax
print(total)             # 20.14

# --- 解決方案 4：用「分」代替「元」 ---
price_cents = 150        # 1.50 元 = 150 分
total_cents = price_cents + 15   # 165 分
print(total_cents / 100) # 1.65 元

# ============================================================
# 【重點 10】內建數學函數 & math 模組
# ============================================================

# --- 內建函數 ---
print(abs(-10))          # 10    — 絕對值
print(round(3.14159, 2)) # 3.14  — 四捨五入
print(max(10, 20, 5))    # 20    — 最大值
print(min(10, 20, 5))    # 5     — 最小值
print(pow(2, 3))         # 8     — 次方（同 2**3）

# --- math 模組 ---
import math

print(math.pi)           # 3.141592653589793 — 圓周率
print(math.e)            # 2.718281828459045 — 自然對數底
print(math.sqrt(16))     # 4.0   — 平方根
print(math.floor(3.7))   # 3     — 無條件捨去
print(math.ceil(3.2))    # 4     — 無條件進位
print(math.trunc(3.7))   # 3     — 取整數部分

# ============================================================
# 【重點 11】實作範例
# ============================================================

# --- 圓的面積與周長 ---
radius = 5
area = math.pi * radius ** 2
circumference = 2 * math.pi * radius
print(f"面積：{round(area, 2)}")          # 78.54
print(f"周長：{round(circumference, 2)}") # 31.42

# --- 攝氏轉華氏 ---
celsius = 28
fahrenheit = celsius * 9 / 5 + 32
print(f"{celsius}°C = {round(fahrenheit, 1)}°F")  # 82.4°F

# --- 秒數轉換為 時:分:秒 ---
total_seconds = 3725
hours = total_seconds // 3600          # 1
remaining = total_seconds % 3600       # 125
minutes = remaining // 60              # 2
seconds = remaining % 60               # 5
print(f"{hours} 時 {minutes} 分 {seconds} 秒")  # 1 時 2 分 5 秒

# --- BMI 計算 ---
height_cm = 175
weight_kg = 70
height_m = height_cm / 100
bmi = weight_kg / (height_m ** 2)
print(f"BMI：{round(bmi, 1)}")         # 22.9

# ============================================================
# 小結：
# 1. int 無大小限制，可用 _ 增加可讀性
# 2. float 有精度問題，金額用 Decimal 或以「分」計算
# 3. int + float → float，/ 永遠回傳 float
# 4. // 整除、% 取餘數、** 指數
# 5. int() 截斷（非四捨五入），float() 轉浮點
# 6. round(), abs(), max(), min() 為常用內建函數
# 7. math 模組：pi, sqrt, floor, ceil, trunc 等
