# ============================================================
# 10.1 ★ namedtuple — 有名字的元組（讓欄位有語義）
# ============================================================

# 普通元組用索引存取，看不出每個位置是什麼：
student = ("小明", 18, "資工系")
print(student[0])                    # 小明（0 是什麼？看不出來）
print(student[2])                    # 資工系（2 是什麼？）

# ★ namedtuple 讓你用名字存取：
from collections import namedtuple

Student = namedtuple("Student", ["name", "age", "dept"])

s = Student("小明", 18, "資工系")
print(s.name)                        # 小明 ← 清楚多了！
print(s.age)                         # 18
print(s.dept)                        # 資工系

# 仍然可以用索引（完全向下相容）
print(s[0])                          # 小明
print(s[-1])                         # 資工系

# 仍然可以拆包
name, age, dept = s
print(f"{name}，{age} 歲，{dept}")

# 仍然不可變
# s.name = "小華"                    # ❌ AttributeError

# ★ namedtuple 是在你覺得「元組索引太難讀」時的完美解決方案。
# 它有元組的所有優點（不可變、可當 key、省記憶體），
# 又有「用名字存取」的可讀性。

# --- 實用：建立一組紀錄 ---
Point = namedtuple("Point", ["x", "y"])
Color = namedtuple("Color", ["r", "g", "b"])

origin = Point(0, 0)
red = Color(255, 0, 0)
print(f"原點座標：({origin.x}, {origin.y})")
print(f"紅色 RGB：({red.r}, {red.g}, {red.b})")

# ============================================================
# 10.2 元組作為函數參數 — *args
# ============================================================

# 在函數定義中，*args 會把多餘的位置參數收集成元組：
def show_args(*args):
    print(type(args))                # <class 'tuple'>
    for arg in args:
        print(f"  {arg}")

show_args("hello", 42, True)
#   hello
#   42
#   True

# 反過來，用 * 可以把元組「展開」成函數的多個參數：
def add(a, b, c):
    return a + b + c

nums = (10, 20, 30)
print(add(*nums))                    # 60（等同 add(10, 20, 30)）

# ============================================================
# 10.3 元組的比較 — 按元素逐一比較
# ============================================================

# 元組比較是「字典序」：從第一個元素開始比，相同就比下一個。
print((1, 2, 3) < (1, 2, 4))        # True （前兩個相同，3 < 4）
print((1, 2, 3) < (1, 3, 0))        # True （第二個 2 < 3，後面不看了）
print((1, 2, 3) == (1, 2, 3))       # True
print((1, 2) < (1, 2, 3))           # True （較短的「小」）

# 實用：按多個欄位排序
students = [
    ("小美", 92),
    ("小明", 85),
    ("小華", 92),
]
# 先按分數降冪，同分按名字升冪
students.sort(key=lambda s: (-s[1], s[0]))
for name, score in students:
    print(f"{name}：{score}")
# 小美：92（分數一樣，小美 < 小華）
# 小華：92
# 小明：85

# ============================================================
# 10.4 元組推導式？不存在，但有生成器表達式
# ============================================================

# [x for x in range(5)] → 串列推導式 → 回傳串列
# (x for x in range(5)) → 生成器表達式 → 回傳生成器（不是元組！）

gen = (x ** 2 for x in range(5))
print(type(gen))                     # <class 'generator'>

# 如果需要元組，用 tuple() 包起來：
t = tuple(x ** 2 for x in range(5))
print(t)                             # (0, 1, 4, 9, 16)

# ============================================================
# 10.5 元組與串列的互轉
# ============================================================

# 串列 → 元組
my_list = [1, 2, 3]
my_tuple = tuple(my_list)
print(my_tuple)                      # (1, 2, 3)

# 元組 → 串列
my_tuple = (4, 5, 6)
my_list = list(my_tuple)
print(my_list)                       # [4, 5, 6]

# ★ 轉換是建立新物件，原物件不受影響。


