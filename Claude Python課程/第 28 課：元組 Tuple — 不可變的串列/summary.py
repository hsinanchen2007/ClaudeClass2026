# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. 什麼是 tuple 拆包（unpacking）？
#     答：把 tuple 的元素一次配給多個名稱：`x, y = (1, 2)`。
#     `a, b = b, a` 的交換寫法就是先組成 tuple 再拆包，所以不需要暫存變數。
#     還可以用 `*` 收集剩餘元素：`first, *rest = [1,2,3]`。
#     追問：左右個數不符會怎樣？（拋 ValueError，訊息會說
#     not enough／too many values to unpack）
#
# 🔥 Q2. `() is ()` 為 True，`[] is []` 為 False，為什麼？
#     答：實測確實如此。因為空 tuple 不可變，**CPython 把它實作成單例**共用；
#     空 list 可變，每次都必須是新物件。
#     **但這是 CPython 的實作細節、不是語言保證**，其他實作可能不同。
#     實測本機對這種寫法還會發出
#     `SyntaxWarning: "is" with 'tuple' literal. Did you mean "=="?`
#     追問：這說明什麼原則？（只有不可變物件才可能被安全地共享／快取——
#     這與小整數快取、字串 interning 是同一套邏輯）
#
# ⚠️ 陷阱. `t = (1, [2,3], 4)` 執行 `t[1] += [100]` 會發生什麼？
#     答：**兩件事同時發生**——實測拋出
#     `TypeError: 'tuple' object does not support item assignment`，
#     **但那個 list 已經被改掉了**，例外之後 `t` 是 `(1, [2, 3, 99, 100], 4)`。
#     原因：`t[1] += [100]` 等價於 `t[1] = t[1].__iadd__([100])`——
#     `__iadd__` **先成功就地修改**了 list，接著「寫回 tuple」這步才失敗。
#     為什麼會錯：以為拋了例外就代表「什麼都沒發生」。
#     這裡是「做了一半才失敗」，是最難查的一種狀態。
