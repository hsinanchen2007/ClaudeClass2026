# ============================================================
# 2.1 用 () 建立（最常見）
# ============================================================

# 空元組
empty = ()
print(empty)                         # ()
print(type(empty))                   # <class 'tuple'>

# 多個元素
colors = ("紅", "橙", "黃", "綠", "藍")
numbers = (10, 20, 30, 40, 50)

# 混合型別（元組常這樣用，用來表示「一筆紀錄」）
person = ("小明", 18, 175.5, True)
# 每個位置有固定意義：(姓名, 年齡, 身高, 是否為學生)

# ============================================================
# 2.2 ★★★ 單元素元組必須加逗號！— 最經典的陷阱
# ============================================================

# 你以為這是元組，但它不是：
not_tuple = ("hello")
print(type(not_tuple))               # <class 'str'> ← 只是一個字串加了括號！

# ★ 加上逗號才是元組：
is_tuple = ("hello",)
print(type(is_tuple))                # <class 'tuple'> ✅

# 數字也一樣：
not_tuple2 = (42)
print(type(not_tuple2))              # <class 'int'> ← 只是數字加括號！

is_tuple2 = (42,)
print(type(is_tuple2))               # <class 'tuple'> ✅

# 為什麼？因為 Python 的小括號 () 有兩種用途：
# 1. 數學分組：(3 + 4) * 2  → 這裡的 () 是數學括號
# 2. 建立元組：(3, 4)       → 有逗號才是元組
#
# 所以 (42) 被 Python 當成數學括號，等同於 42。
# 而 (42,) 因為有逗號，Python 才知道這是元組。

# ============================================================
# 2.3 ★ 逗號才是關鍵 — 括號可以省略
# ============================================================

# Python 看到逗號分隔的值，自動視為元組：
coordinates = 25.03, 121.56
print(coordinates)                   # (25.03, 121.56)
print(type(coordinates))             # <class 'tuple'>

data = "小明", 18, "台北"
print(data)                          # ('小明', 18, '台北')

# 甚至單元素也行：
single = "hello",
print(type(single))                  # <class 'tuple'>

# ★ 但加括號可讀性更好，所以實務上通常還是加 ()。
# 特別是在 return 語句和函數呼叫中，加括號更清楚。

# ============================================================
# 2.4 用 tuple() 從其他型別轉換
# ============================================================

# 串列 → 元組
from_list = tuple([1, 2, 3, 4, 5])
print(from_list)                     # (1, 2, 3, 4, 5)

# 字串 → 元組（每個字元變成一個元素）
from_str = tuple("Hello")
print(from_str)                      # ('H', 'e', 'l', 'l', 'o')

# range → 元組
from_range = tuple(range(1, 6))
print(from_range)                    # (1, 2, 3, 4, 5)

# ============================================================
# 2.5 元組的串接 + 和重複 *
# ============================================================

a = (1, 2, 3)
b = (4, 5, 6)

# + 產生新元組
c = a + b
print(c)                             # (1, 2, 3, 4, 5, 6)

# * 重複
d = a * 3
print(d)                             # (1, 2, 3, 1, 2, 3, 1, 2, 3)

# ★ 這些操作都是產生新元組，原元組不變（因為元組不可變）。

# ============================================================
# 2.6 尾端逗號（trailing comma）— 好習慣
# ============================================================

# 多行元組建議加尾端逗號（方便之後新增、避免 git diff 多行改動）
student = (
    "小明",
    18,
    "資工系",   # ← 尾端逗號
)


