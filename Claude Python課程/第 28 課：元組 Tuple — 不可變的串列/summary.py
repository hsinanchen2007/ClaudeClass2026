# 拆包（Unpacking）讓你把元組的元素一次賦值給多個變數。
# 這是 Python 中非常常用、非常優雅的語法。
# 雖然串列也能拆包，但元組是最典型的使用場景。

# ============================================================
# 6.1 基本拆包 — 變數數量 = 元素數量
# ============================================================

person = ("小明", 18, "台北")

# 不拆包的話，要一個一個取：
name = person[0]
age = person[1]
city = person[2]

# ★ 用拆包，一行搞定：
name, age, city = person
print(name)                          # 小明
print(age)                           # 18
print(city)                          # 台北

# ⚠️ 變數數量必須與元素數量一致，否則報錯：
# x, y = (10, 20, 30)
# → ValueError: too many values to unpack (expected 2)

# x, y, z, w = (10, 20, 30)
# → ValueError: not enough values to unpack (expected 4, got 3)

# ============================================================
# 6.2 ★ 變數交換 — Python 最優雅的語法之一
# ============================================================

a = 10
b = 20
print(f"交換前：a={a}, b={b}")       # a=10, b=20

# 傳統做法（需要暫存變數）
# temp = a
# a = b
# b = temp

# ★ Python 一行搞定：
a, b = b, a
print(f"交換後：a={a}, b={b}")       # a=20, b=10

# 原理：
# 右邊 b, a 先打包成元組 (20, 10)
# 然後拆包賦值給左邊 a, b
# → a = 20, b = 10

# 三個變數也能一行輪轉：
a, b, c = 1, 2, 3
a, b, c = c, a, b
print(a, b, c)                       # 3 1 2

# ============================================================
# 6.3 ★★ 用 * 收集剩餘元素（Extended Unpacking）
# ============================================================

# 當你不知道有多少元素，或者只想取前幾個時，用 * 收集。
# ★ 收集到的永遠是串列（list），即使原始資料是元組。

nums = (1, 2, 3, 4, 5, 6, 7)

# --- 取前兩個，其餘收集 ---
first, second, *rest = nums
print(first)                         # 1
print(second)                        # 2
print(rest)                          # [3, 4, 5, 6, 7] ← ★ 是串列！

# --- 取頭尾，中間收集 ---
first, *middle, last = nums
print(first)                         # 1
print(middle)                        # [2, 3, 4, 5, 6]
print(last)                          # 7

# --- 取最後兩個，其餘收集 ---
*others, second_last, last = nums
print(others)                        # [1, 2, 3, 4, 5]
print(second_last)                   # 6
print(last)                          # 7

# --- 只取第一個，忽略其餘 ---
head, *_ = nums
print(head)                          # 1
# _ 是慣例用來表示「不在乎的變數」

# ⚠️ 只能有一個 *
# a, *b, *c = nums                   # ❌ SyntaxError

# --- 如果剩餘元素是零個，* 變數會是空串列 ---
a, b, c, *rest = (1, 2, 3)
print(rest)                          # []

# ============================================================
# 6.4 ★★★ 走訪時直接拆包 — 超實用的日常模式
# ============================================================

# 當串列裡的每個元素都是元組時，走訪時可以直接拆包：

students = [
    ("小明", 85),
    ("小華", 92),
    ("小美", 78),
    ("小強", 96),
]

# ❌ 不拆包（要用索引）
for student in students:
    print(f"{student[0]}：{student[1]} 分")

# ✅ 拆包（清楚多了！）
for name, score in students:
    status = "✅ 優秀" if score >= 90 else "📘 普通"
    print(f"{name}：{score} 分 {status}")
# 小明：85 分 📘 普通
# 小華：92 分 ✅ 優秀
# 小美：78 分 📘 普通
# 小強：96 分 ✅ 優秀

# --- 更複雜的例子 ---
employees = [
    ("Alice", "Engineering", 95000),
    ("Bob", "Marketing", 72000),
    ("Carol", "Engineering", 102000),
]

for name, dept, salary in employees:
    print(f"{name:10s} {dept:15s} ${salary:,}")
# Alice      Engineering     $95,000
# Bob        Marketing       $72,000
# Carol      Engineering     $102,000

# --- 搭配 enumerate ---
for i, (name, score) in enumerate(students, 1):
    print(f"第 {i} 名：{name}（{score} 分）")

# ============================================================
# 6.5 ★★★ 函數回傳多個值 — 其實就是回傳元組
# ============================================================

# Python 函數只能回傳一個值，但那個值可以是元組！
# return a, b 其實就是 return (a, b)

def min_max(numbers):
    """回傳串列中的最小值和最大值"""
    return min(numbers), max(numbers)

data = [23, 45, 12, 67, 34]
result = min_max(data)
print(result)                        # (12, 67) ← 是元組
print(type(result))                  # <class 'tuple'>

# ★ 用拆包接收更優雅：
lo, hi = min_max(data)
print(f"最小：{lo}，最大：{hi}")     # 最小：12，最大：67

# --- 更實用的例子 ---
def analyze_scores(scores):
    """分析成績，回傳平均、最高、最低、及格率"""
    avg = sum(scores) / len(scores)
    highest = max(scores)
    lowest = min(scores)
    pass_rate = sum(1 for s in scores if s >= 60) / len(scores) * 100
    return avg, highest, lowest, pass_rate

scores = [85, 47, 93, 55, 78, 62, 91]
avg, highest, lowest, pass_rate = analyze_scores(scores)
print(f"平均：{avg:.1f}")
print(f"最高：{highest}")
print(f"最低：{lowest}")
print(f"及格率：{pass_rate:.0f}%")

# --- 只取部分回傳值 ---
# 只要平均，其他不管：
avg, *_ = analyze_scores(scores)
print(f"平均：{avg:.1f}")

# 只要及格率（最後一個）：
*_, pass_rate = analyze_scores(scores)
print(f"及格率：{pass_rate:.0f}%")

