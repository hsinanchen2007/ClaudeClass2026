# ============================================================
# 8.1 優勢一：保護資料不被意外修改
# ============================================================

# 如果你有一組「應該是常數」的資料，用元組可以防止意外修改。

# ★ 一週的天數 — 這是事實，不該被改
DAYS_OF_WEEK = ("一", "二", "三", "四", "五", "六", "日")

# 如果用串列，某段程式碼可能不小心：
# days = ["一", "二", "三", "四", "五", "六", "日"]
# days[0] = "Monday"   # 沒有報錯！但已經搞亂了

# 用元組就安全了：
# DAYS_OF_WEEK[0] = "Monday"   # ❌ TypeError → 被攔住！

# ★ 其他適合用元組的常數
DIRECTIONS = ("北", "東", "南", "西")
MONTHS = ("Jan", "Feb", "Mar", "Apr", "May", "Jun",
          "Jul", "Aug", "Sep", "Oct", "Nov", "Dec")
RGB_RED = (255, 0, 0)
RGB_GREEN = (0, 255, 0)
RGB_BLUE = (0, 0, 255)

# ============================================================
# 8.2 優勢二：可以當字典的 key（串列不行！）
# ============================================================

# 字典的 key 必須是「不可變」的（hashable）。
# 元組是不可變的 → ✅ 可以當 key
# 串列是可變的   → ❌ 不能當 key

# ✅ 元組當 key — GPS 座標對應城市名
location_names = {
    (25.03, 121.56): "台北",
    (22.63, 120.30): "高雄",
    (24.15, 120.67): "台中",
}
print(location_names[(25.03, 121.56)])  # 台北

# ✅ 元組當 key — 棋盤座標
chess_board = {}
chess_board[(0, 0)] = "車"
chess_board[(0, 1)] = "馬"
chess_board[(0, 4)] = "王"
print(chess_board[(0, 4)])            # 王

# ❌ 串列不能當 key
# bad_dict = {[25.03, 121.56]: "台北"}
# → TypeError: unhashable type: 'list'

# ⚠️ 注意：元組裡如果包含可變物件，也不能當 key
# bad_key = {(1, [2, 3]): "value"}
# → TypeError: unhashable type: 'list'

