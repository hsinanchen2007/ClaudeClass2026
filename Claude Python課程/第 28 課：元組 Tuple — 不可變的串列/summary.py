# █                                                                  █
# █  第五章：★★★ 不可變性（Immutability）— 元組的核心特性              █
# █                                                                  █
# ████████████████████████████████████████████████████████████████████

# 這是元組和串列最根本的差異。
# 元組一旦建立，就不能修改、不能新增、不能刪除任何元素。

fruits = ("蘋果", "香蕉", "芒果")

# ============================================================
# 5.1 ❌ 不能做的事
# ============================================================

# 不能修改元素
# fruits[1] = "草莓"
# → TypeError: 'tuple' object does not support item assignment

# 不能新增元素
# fruits.append("葡萄")     # → AttributeError: 'tuple' object has no attribute 'append'
# fruits.extend(["葡萄"])   # → AttributeError
# fruits.insert(0, "草莓")  # → AttributeError

# 不能刪除元素
# fruits.remove("香蕉")     # → AttributeError
# fruits.pop()              # → AttributeError
# del fruits[0]             # → TypeError

# 不能排序
# fruits.sort()             # → AttributeError
# fruits.reverse()          # → AttributeError

# ============================================================
# 5.2 ✅ 可以做的事
# ============================================================

# ★ 可以重新賦值整個變數（但這不是「修改元組」，而是「建立新元組」）
fruits = ("蘋果", "草莓", "芒果")
# 舊的 ("蘋果", "香蕉", "芒果") 被丟棄，fruits 現在指向新元組

# ★ 可以用 + 串接產生新元組
bigger = fruits + ("葡萄",)
print(bigger)                        # ('蘋果', '草莓', '芒果', '葡萄')
# 注意：("葡萄",) 需要逗號，否則 ("葡萄") 只是字串

# ★ 可以用 sorted() 回傳排序後的串列
nums = (38, 15, 72, 4, 56)
sorted_list = sorted(nums)
print(sorted_list)                   # [4, 15, 38, 56, 72] ← 注意：回傳的是串列！
sorted_tuple = tuple(sorted(nums))   # 轉回元組
print(sorted_tuple)                  # (4, 15, 38, 56, 72)

# ============================================================
# 5.3 需要修改時：轉串列 → 修改 → 轉回元組
# ============================================================

original = ("蘋果", "香蕉", "芒果")
print(f"原始：{original}")

# 步驟 1：轉成串列（可變）
temp = list(original)

# 步驟 2：在串列上做任何修改
temp[1] = "草莓"
temp.append("葡萄")
temp.sort()

# 步驟 3：轉回元組（不可變）
modified = tuple(temp)
print(f"修改後：{modified}")
# 修改後：('芒果', '蘋果', '草莓', '葡萄')

# ============================================================
# 5.4 ⚠️⚠️⚠️ 最大陷阱：元組裡的可變物件仍然可變！
# ============================================================

# 元組的「不可變」指的是：
# 元組的「哪個位置指向哪個物件」不能變。
# 但如果那個物件本身是可變的（如串列），它的內容可以被改！

tricky = (1, 2, [3, 4])
print(tricky)                        # (1, 2, [3, 4])

# ❌ 不能把 tricky[2] 指向新物件
# tricky[2] = [5, 6]                 # TypeError

# ⚠️ 但可以修改 tricky[2] 所指向的串列的內容！
tricky[2].append(5)
print(tricky)                        # (1, 2, [3, 4, 5])

tricky[2][0] = 999
print(tricky)                        # (1, 2, [999, 4, 5])

# 這就像：元組保證「這個房間不會換人住」，
# 但住在裡面的人可以自己移動家具。

# ★ 實務建議：如果你要一個「完全不可變」的結構，
# 確保元組裡的所有元素也都是不可變的（str, int, float, bool, tuple）。

fully_immutable = (1, "hello", (3, 4), True)
# 裡面全是不可變型別 → 完全安全


