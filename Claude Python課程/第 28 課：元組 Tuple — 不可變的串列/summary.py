# 8.3 優勢三：佔用空間較小、速度較快
# ============================================================

import sys

my_list = [1, 2, 3, 4, 5]
my_tuple = (1, 2, 3, 4, 5)

print(f"串列佔用：{sys.getsizeof(my_list)} bytes")
print(f"元組佔用：{sys.getsizeof(my_tuple)} bytes")
# 元組通常比串列少 10-20 bytes

# 為什麼？因為串列需要額外空間來支援「可變」的功能：
# - 動態陣列的容量管理
# - 預留空間給未來的 append
# 元組不需要這些，所以更精簡。

# 效能差異在小資料上不明顯，但在大量資料的場景下有意義。
# 例如：100 萬筆座標資料，用元組比用串列省很多記憶體。


