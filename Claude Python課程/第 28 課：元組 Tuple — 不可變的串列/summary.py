# ============================================================
# 陷阱 1：單元素忘加逗號
# ============================================================

a = (42)
print(type(a))                       # <class 'int'> ← 不是元組！

b = (42,)
print(type(b))                       # <class 'tuple'> ← 這才是

# ★ 串接時也容易忘：
# t = (1, 2) + (3)                   # ❌ TypeError: can only concatenate tuple to tuple
t = (1, 2) + (3,)                    # ✅ (1, 2, 3)

# ============================================================
# 陷阱 2：拆包數量不一致
# ============================================================

point = (10, 20, 30)

# x, y = point                      # ❌ ValueError: too many values
x, y, z = point                      # ✅ 正確
x, *rest = point                     # ✅ 用 * 收集剩餘

# ============================================================
# 陷阱 3：元組裡的可變物件仍然可變
# ============================================================

t = (1, 2, [3, 4])
# t[2] = [5, 6]                     # ❌ TypeError
t[2].append(5)                       # ⚠️ 可以修改裡面的串列！
print(t)                             # (1, 2, [3, 4, 5])

# ============================================================
# 陷阱 4：以為 sorted() 回傳元組
# ============================================================

nums = (5, 3, 1, 4, 2)
result = sorted(nums)
print(type(result))                  # <class 'list'> ← 是串列！

# 要元組得自己轉：
result_tuple = tuple(sorted(nums))

# ============================================================
# 陷阱 5：意外建立元組（逗號陷阱）
# ============================================================

# 不小心多打了一個逗號
x = 42,
print(type(x))                      # <class 'tuple'> ← 驚不驚喜？

# 在 return 語句中特別危險：
def get_value():
    return 42,                       # 多了一個逗號！
    # 回傳的是 (42,) 而不是 42

result = get_value()
print(result)                        # (42,)
print(type(result))                  # <class 'tuple'>

# ============================================================
# 陷阱 6：混淆函數呼叫和元組
# ============================================================

# print(1, 2, 3) → 這是函數呼叫，三個參數
# (1, 2, 3)      → 這是元組

# 想把元組傳給函數，要用 * 展開：
nums = (1, 2, 3)
print(*nums)                        # 1 2 3（三個參數）
print(nums)                         # (1, 2, 3)（一個參數：元組本身）


