# 元組（tuple）和串列（list）非常相似，它們都是：
# - 有序的（有索引，可以用 [i] 存取）
# - 可以存放任意型別的元素
# - 可以巢狀
# - 可以用 for 走訪

# 但它們有一個根本性的差異：
#
#   ★ 串列是可變的（mutable）   → 可以增刪改元素
#   ★ 元組是不可變的（immutable）→ 建立後就不能動了
#
# 這不是缺點，而是一種「保護機制」。
# 很多時候，你就是不希望資料被改動。

# 一個直覺的比喻：
#   串列 ≈ 購物車    → 隨時加東西、刪東西
#   元組 ≈ 身分證    → 出生日期、血型不會改變
#   串列 ≈ 便利貼    → 寫了可以撕掉重寫
#   元組 ≈ 刻在石頭上 → 刻了就不能改


