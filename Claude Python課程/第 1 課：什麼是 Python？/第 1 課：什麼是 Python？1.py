# 🔥 Q1. Python 是編譯型還是直譯型語言？
#     答：兩者都有。原始碼先被「編譯」成 bytecode，再由 PVM（Python 虛擬機）
#     「直譯」執行。說成「純直譯語言」並不精確。
#     追問：那 .pyc 檔是什麼時候產生的？
#
# 🔥 Q2. CPython、PyPy、Jython 差在哪裡？
#     答：都是 Python 語言的不同「實作」。CPython 是官方參考實作（用 C 寫）；
#     PyPy 帶 JIT，長時間運算通常較快；Jython 跑在 JVM 上。
#     語言規範與某個實作的行為必須分清楚。
#     追問：那你怎麼知道某個行為是語言保證還是實作細節？
#
# ⚠️ 陷阱. GIL 是「Python 語言」的規定嗎？
#     答：不是。GIL 是 **CPython 的實作細節**，不是語言規範；Jython、IronPython
#     就沒有 GIL。本機實測為標準 GIL 建置：`sys._is_gil_enabled()` → True。
#     為什麼會錯：多數人把「CPython 的行為」直接當成「Python 的定義」，
#     於是講出「Python 不能多執行緒平行」這種過度斷言。
