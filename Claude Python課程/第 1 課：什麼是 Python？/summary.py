# Python 是一種高級程式語言，特色：簡潔易讀、適合初學者。
# 應用領域：網頁開發、資料分析、人工智慧、機器學習等。
# 本課程將涵蓋：變數、資料型別、條件語句、迴圈、函數等基礎。
#
# 【重點 1】print() 是 Python 最基本的輸出函數
# 用來在螢幕上顯示文字或資料。
# ============================================================

# --- 基本用法：用 print() 輸出文字 ---
print("你好")                        # 輸出：你好
print("歡迎來到 Python 課程")         # 輸出：歡迎來到 Python 課程

# --- print() 可以一次輸出一行文字 ---
print("Python 是一種高級程式語言，具有簡潔易讀的語法，適合初學者學習。")
print("Python 可以用於網頁開發、資料分析、人工智慧、機器學習等多種領域。")

# --- 多個 print() 會依序輸出，每個 print 自動換行 ---
print("第一行")
print("第二行")
print("第三行")
# 輸出：
# 第一行
# 第二行
# 第三行

# ============================================================
# 小結：
# 1. Python 是簡潔易讀的高級語言，適合初學者
# 2. print("文字") 用來輸出文字到螢幕
# 3. 每個 print() 結尾會自動換行
