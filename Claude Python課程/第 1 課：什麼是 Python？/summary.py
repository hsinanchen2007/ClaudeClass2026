# 🔥 Q1. .pyc 與 __pycache__ 是什麼？為什麼不該進版控？
#     答：`.pyc` 是原始碼編譯後的 bytecode 快取，放在 `__pycache__/`，
#     目的是省下重複編譯的時間。它可由原始碼重新產生、且綁定 Python 版本，
#     屬於「可重生的產物」，因此應該寫進 .gitignore。
#     追問：刪掉 __pycache__ 會怎樣？（下次執行自動重建，只是稍慢）
#
# 🔥 Q2. 為什麼 print() 不用 import 就能用？
#     答：它是 builtins 模組裡的內建函式，直譯器啟動時就已載入，
#     位於名稱查找的最後一層（LEGB 的 B）。
#     追問：如果自己定義一個叫 print 的變數會怎樣？（遮蔽掉內建的那個）
#
# ⚠️ 陷阱. print() 的回傳值是什麼？
#     答：**None**（本機實測）。它的作用是「副作用」——把東西寫到 stdout，
#     不是「產生一個值」。所以 `x = print("hi")` 之後 x 是 None。
#     為什麼會錯：在 REPL 裡輸入運算式會自動回顯結果，容易讓人誤以為
#     print 也「回傳了那段文字」。實際上回顯與 print 是兩回事。
# ═══════════════════════════════════════════════════════════════════════════


# --- 基本用法：用 print() 輸出文字 ---
print("你好")                        # 輸出：你好
print("歡迎來到 Python 課程")         # 輸出：歡迎來到 Python 課程

# --- print() 可以一次輸出一行文字 ---
print("Python 是一種高級程式語言，具有簡潔易讀的語法，適合初學者學習。")
print("Python 可以用於網頁開發、資料分析、人工智慧、機器學習等多種領域。")

# --- 多個 print() 會依序輸出，每個 print 自動換行 ---
print("第一行")
print("第二行")
print("第三行")
# 輸出：
# 第一行
# 第二行
# 第三行

