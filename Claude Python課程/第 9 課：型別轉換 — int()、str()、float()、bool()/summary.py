# ============================================================
# 【重點 7】常見考題
# ============================================================

print(int(9.99))           # 9（截斷，不是四捨五入）
print(float("10"))         # 10.0
print(str(True))           # "True"（字串）
print(bool(""))            # False（空字串）
print(bool("0"))           # True（⚠️ 非空字串）
print(int(True) + int(False))  # 1 + 0 = 1
print(int("3" + "4"))      # int("34") = 34（先字串拼接再轉整數）

# ============================================================
# 小結：
# 1. input() 永遠回傳字串 → 必須轉型才能做數學運算
# 2. int()：截斷小數（非四捨五入），字串含小數點會報錯
# 3. float()：幾乎都能轉，逗號不行
# 4. str()：什麼都能轉，最安全
# 5. bool()：0, 0.0, "", None, [] → False，其他 → True
# 6. 常用寫法：int(input("..."))  float(input("..."))
