# ═══════════════════════════════════════════════════════════════════════════
# 【面試題】轉型的細節與取整方向
# ───────────────────────────────────────────────────────────────────────────
# 🔥 Q1. `int()` 有哪些容易踩雷的細節？
#     答：實測三點——①`int(3.99)` → 3、`int(-3.99)` → **-3**（**向零截斷**，
#     不是四捨五入也不是 floor）；②`int('abc')` → ValueError；
#     ③`int('10', 2)` → **2**，第二個參數是**進位基底**不是預設值。
#     追問：那 `int('10', 2)` 和 `int('10')` 差在哪？（前者當二進位讀，後者十進位）
#
# 🔥 Q2. `str()` 與 `repr()` 差在哪？
#     答：`str()` 給人看（友善），`repr()` 給開發者看（盡量精確、可還原）。
#     字串上差別最明顯：`str('a')` → `a`，`repr('a')` → `'a'`（帶引號）。
#     print 用的是 str()，在 REPL 直接回顯用的是 repr()。
#     追問：f-string 裡怎麼取 repr？（`f"{x!r}"`）
#
# ⚠️ 陷阱. 轉成 bool 時，哪些「看起來有內容」的值其實是 False？
#     答：實測 `bool(0)`、`bool(0.0)`、`bool('')`、`bool([])`、`bool({})`、
#     `bool(set())` 全是 False。反過來 `bool('0')`、`bool('False')`、
#     `bool([0])` 全是 **True**——只要是非空字串／非空容器就 truthy。
#     為什麼會錯：把「值的語意」與「容器是否為空」混為一談。
#     `'0'` 是長度 1 的字串，內容是什麼跟真假無關。
# ═══════════════════════════════════════════════════════════════════════════


# ============================================================
# 【重點 1】⚠️ input() 回傳的永遠是字串！
# 這是型別轉換最常見的使用場景。
# ============================================================

age = input("請輸入年齡：")
print(type(age))           # <class 'str'> ← 永遠是字串！
# print(age + 10)          # ❌ TypeError! 字串不能和數字相加

# ============================================================
# 【重點 2】int() — 轉換成整數
# ============================================================

# --- 字串 → int ---
a = "123"
b = int(a)
print(b + 10)              # 133 ✅

# --- float → int：截斷小數（不是四捨五入！） ---
print(int(3.9))            # 3（不是 4！直接去掉小數）
print(int(3.1))            # 3
print(int(-2.7))           # -2（往零的方向截斷）
print(int(-2.1))           # -2

# --- bool → int ---
print(int(True))           # 1
print(int(False))          # 0

# --- ❌ 以下轉換會報 ValueError ---
# int("abc")               # 內容不是數字
# int("3.14")              # ⚠️ 有小數點的字串不能直接轉 int
# int("")                  # 空字串
# int("12 34")             # 有空格

# --- ✅ 有小數點的字串：先轉 float 再轉 int ---
text = "3.14"
result = int(float(text))  # 先 float("3.14") → 3.14，再 int(3.14) → 3
print(result)              # 3

# ============================================================
# 【重點 3】float() — 轉換成浮點數
# ============================================================

# --- 字串 → float ---
print(float("3.14"))       # 3.14
print(float("100"))        # 100.0（整數字串也行）
print(float("1e3"))        # 1000.0（科學記號）

# --- int → float ---
print(float(5))            # 5.0
print(float(-10))          # -10.0

# --- bool → float ---
print(float(True))         # 1.0
print(float(False))        # 0.0

# --- ❌ 以下會報 ValueError ---
# float("abc")
# float("")
# float("12,345")          # ⚠️ 逗號不行

# ============================================================
# 【重點 4】str() — 轉換成字串
# 幾乎什麼都能轉成字串，不會報錯。
# ============================================================

print(str(123))            # "123"
print(str(3.14))           # "3.14"
print(str(-50))            # "-50"
print(str(True))           # "True"
print(str(False))          # "False"

# --- 實用：拼接字串與數字 ---
age = 25
# print("我今年" + age + "歲")      # ❌ TypeError!
print("我今年" + str(age) + "歲")    # ✅ 我今年25歲

# ============================================================
# 【重點 5】bool() — 轉換成布林值
# ============================================================

# --- Falsy 值（轉成 False） ---
print(bool(0))             # False
print(bool(0.0))           # False
print(bool(""))            # False — 空字串
print(bool(None))          # False
print(bool([]))            # False — 空串列

# --- Truthy 值（轉成 True） ---
print(bool(1))             # True
print(bool(-5))            # True — 任何非零數字
print(bool("hello"))       # True — 有內容的字串
print(bool("0"))           # True ← ⚠️ 字串 "0" 不是空的！
print(bool(" "))           # True ← ⚠️ 空格也算有內容！
print(bool("False"))       # True ← ⚠️ 字串 "False" 也不是空的！

# ============================================================
# 【重點 6】最常用的實際應用：input() + 型別轉換
# ============================================================

# --- 計算年齡 ---
