// =============================================================================
//  01_basic_operators.cpp  —  位元基本運算子 & | ^ ~ << >>
// =============================================================================
//  參考：https://en.cppreference.com/w/cpp/language/operator_arithmetic
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 一、六個位元運算子                                         │
//  └────────────────────────────────────────────────────────────┘
//
//   &   AND          兩位都 1 才是 1
//   |   OR           任一位是 1 就是 1
//   ^   XOR          兩位不同才是 1（同則 0）
//   ~   NOT          反向整位
//   <<  左移         所有位向左 → 等同乘以 2^k（最高位被推出去）
//   >>  右移         所有位向右
//                    * unsigned：邏輯右移（補 0）
//                    * signed   ：算術右移（補符號位） — C++20 起標準保證
//                                 (C++20 之前是「實作定義」)
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 二、優先級陷阱 — 容易犯的 3 個錯誤                          │
//  └────────────────────────────────────────────────────────────┘
//
//   1) `==` 比 `&` 高 → `n & 1 == 0` 等於 `n & (1 == 0)` = `n & 0` 永遠是 0
//      正確：(n & 1) == 0。寫位元判斷時「永遠加括號」。
//
//   2) `<<` 比 `+` 低？ 反了。`<<` 跟 `>>` 比 `+` 低。
//      `1 << 2 + 3` = `1 << (2 + 3)` = 32。要乘 4 加 3 寫 `(1 << 2) + 3`。
//
//   3) `&|^` 比邏輯 `&&||` 低，但比比較運算符 `<,>` 還低。
//      `a & b > c` 等於 `a & (b > c)`，幾乎一定不是你要的意思。
//
//   結論：寫位元運算「凡是混到比較或加減」就無腦加括號。
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 三、unsigned vs signed                                    │
//  └────────────────────────────────────────────────────────────┘
//
//  位元運算建議「絕對用 unsigned」，理由：
//   * signed 整數的「左移到符號位」是 UB（C++20 之前）
//   * signed 右移的填補方式 historical 上是「實作定義」
//   * 表達 mask、bitfield 概念上就是無號 — 用 signed 邏輯混亂
//
//  範例：std::uint32_t / std::uint64_t 是首選；長度明確、範圍不會驚喜。
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 四、本檔示範                                               │
//  └────────────────────────────────────────────────────────────┘
//
//   * Demo 1：六種運算子各示範一次
//   * Demo 2：優先級陷阱實例
//   * Demo 3：用 std::bitset 印出 32-bit 二進位（除錯神器）
// =============================================================================

/*
補充筆記：basic_operators
  - basic_operators 屬於位元操作；位元運算適合旗標、遮罩、集合狀態、低階資料格式和某些整數技巧。
  - 位移前要確認位數合法；位移負數或位移量大於等於型別寬度會造成未定義行為。
  - signed integer 的位元表示和右移行為有細節；做遮罩和位元技巧時通常使用 unsigned 型別較清楚。
  - x & (x - 1) 可清掉最低位的 1，常用於計算 set bits 或判斷 power of two；但 x=0 要另外處理。
  - C++20 <bit> 提供 popcount、has_single_bit、rotl、bit_width 等標準工具，能取代許多手寫技巧。
  - bit_cast 是按位元重新解讀且要求大小相同與 trivially copyable；它不是任意型別轉換。
  - 位元 AND、OR、XOR、NOT 是逐 bit 運算，和邏輯 &&、||、! 不同；前者處理整數每一位，後者處理 true/false。
  - 左移常被用來建立遮罩，例如 1u << k，但 k 必須小於型別 bit 數，否則是未定義行為。
  - 做位元運算時優先用 unsigned，避免 signed 負數表示法和右移補位規則干擾理解。
*/
#include <bitset>
#include <cstdint>
#include <iostream>

// 把任意 unsigned 整數印成 32-bit 二進位（除錯方便）
template <class T>
static std::string bin32(T x) {
    return std::bitset<32>(static_cast<std::uint32_t>(x)).to_string();
}

// 前置宣告：附加的 LeetCode 題與實用範例
static void demo_lc_461_hamming_distance();
static void demo_byte_parity();

int main() {
    std::uint32_t a = 0b11001010u;   //  202
    std::uint32_t b = 0b10101100u;   //  172

    // ─────────────────────────────────────────────────────────
    // Demo 1：六種運算子
    // ─────────────────────────────────────────────────────────
    std::cout << "[Demo1]\n";
    std::cout << "a     = " << bin32(a)       << " (" << a << ")\n";
    std::cout << "b     = " << bin32(b)       << " (" << b << ")\n";
    std::cout << "a & b = " << bin32(a & b)   << '\n';
    std::cout << "a | b = " << bin32(a | b)   << '\n';
    std::cout << "a ^ b = " << bin32(a ^ b)   << '\n';
    std::cout << "~a    = " << bin32(~a)      << '\n';
    std::cout << "a<<2  = " << bin32(a << 2)  << '\n';
    std::cout << "a>>2  = " << bin32(a >> 2)  << '\n';

    // ─────────────────────────────────────────────────────────
    // Demo 2：優先級陷阱
    // ─────────────────────────────────────────────────────────
    int n = 6;
    // 「壞寫法」用括號明寫成等價的 n & (1==0)，避免 -Wparentheses warning
    bool wrong = (n & (1 == 0));            // = n & 0 = 0 → false（永遠 false）
    bool right = ((n & 1) == 0);            // 真正想表達「n 是偶數」
    std::cout << "[Demo2] (n=6) BAD: n&(1==0) = " << wrong
              << "; GOOD: (n&1)==0 = " << right << '\n';

    // ─────────────────────────────────────────────────────────
    // Demo 3：左移 / 右移做乘除
    //   注意：如果 n << k 把資訊推出去了，結果不再是 n*2^k
    // ─────────────────────────────────────────────────────────
    std::uint32_t x = 5;
    std::cout << "[Demo3] 5 << 3 = " << (x << 3) << " (= 40)\n";
    std::cout << "[Demo3] 40 >> 2 = " << (40u >> 2) << " (= 10)\n";

    // ─────────────────────────────────────────────────────────
    // 課堂知識補充
    // ─────────────────────────────────────────────────────────
    //  Q1：為什麼編譯器自帶 << >> 已經比手寫 *、/ 還快嗎？
    //    A：對 unsigned 整數來說，`x*2`、`x/2` 編譯器一般也會優化成 shift；
    //       只有「除以非常數」或「signed 不確定符號」時手動 shift 才有差。
    //       可讀性優先 — 該寫乘除就寫乘除，shift 留給「真的在做位元運算」的場合。
    //
    //  Q2：~ 跟邏輯非 ! 差在哪？
    //    A：~ 是按位反轉（全部 bit 翻），! 是邏輯反（!0 = 1, !非0 = 0）。
    //       ~0u = 0xFFFFFFFF；!0u = 1。看混了一定 bug。
    //
    //  Q3：bitset 比手寫 mask 慢嗎？
    //    A：除錯時用 bitset 印很方便；運算 hot path 用原生整數通常一樣快或
    //       更快（編譯器把 std::bitset 操作優化得很好）。可讀性 + 印出方便
    //       是 bitset 的真正優勢。
    //
    demo_lc_461_hamming_distance();
    demo_byte_parity();
    return 0;
}

// =============================================================================
//  附加 1：LeetCode 461. Hamming Distance（簡單）
// =============================================================================
//  題意：給兩個整數 x、y，回傳「二進位下不同位的個數」（漢明距離）。
//  思路：x ^ y 把「相同位」歸 0、「不同位」歸 1；再對結果 popcount 即答案。
//  這直接展示了 ^（XOR）和「逐 bit 比較」的本質用途。
//
//  例：x = 1 (0001), y = 4 (0100)
//      x ^ y = 0101 → popcount = 2
// =============================================================================
static int hammingDistance(int x, int y) {
    unsigned diff = static_cast<unsigned>(x) ^ static_cast<unsigned>(y);
    int cnt = 0;
    while (diff) { diff &= diff - 1; ++cnt; } // Brian Kernighan
    return cnt;
}
static void demo_lc_461_hamming_distance() {
    std::cout << "[LC461] hamming(1,4) = " << hammingDistance(1, 4) << " (= 2)\n";
    std::cout << "[LC461] hamming(3,1) = " << hammingDistance(3, 1) << " (= 1)\n";
}

// =============================================================================
//  附加 2：實用範例 — 位元組奇偶校驗 (byte parity)
// =============================================================================
//  通訊 / 序列協定常用「奇偶位元」做最低成本錯誤偵測：對一個 byte 做 XOR 累
//  積，最後得到 1 表示有奇數個 1。把多個 bit 結合用一個 byte 表達狀態，是
//  位元 OR / AND / XOR 在工作上的常見應用。
// =============================================================================
static bool byteParityOdd(std::uint8_t b) {
    // 把 byte 的每個 bit XOR 起來
    b ^= b >> 4;
    b ^= b >> 2;
    b ^= b >> 1;
    return (b & 1u) != 0;
}
static void demo_byte_parity() {
    std::cout << "[parity] 0b10110100 odd? " << byteParityOdd(0b10110100) << " (4 個 1 → false)\n";
    std::cout << "[parity] 0b10110101 odd? " << byteParityOdd(0b10110101) << " (5 個 1 → true)\n";
}
