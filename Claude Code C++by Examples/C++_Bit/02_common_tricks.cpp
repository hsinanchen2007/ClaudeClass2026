// 【面試題】位元經典技巧
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. n & (n - 1) 和 n & -n 各是什麼？為什麼成立？
//     答：n & (n - 1) 清除「最低位的 1」——n - 1 會把最低位的 1 變 0、其右方全部 0 變 1，
//     AND 之後該位與其右方全為 0。n & -n 取出「最低位的 1 及其後的 0」（lowbit）——
//     補數下 -n == ~n + 1，~n 讓最低位 1 右邊的 0 全變 1、該 1 變 0，+1 進位後最低位的 1
//     復原、而其左邊各位與 n 恰好相反，故 AND 只剩下那一位。
//     用途：前者用於 popcount（Brian Kernighan）、判 2 的冪、LC 338／201；後者用於
//     Fenwick Tree、逐一枚舉所有 set bit、LC 260 分組。
//     追問：還有哪些同類技巧？（n | (n + 1) 設定最低位的 0；n & (n + 1) 清除末尾連續的
//     1，因此 (n & (n + 1)) == 0 可判斷 n 是否形如 0..01..1；~n & (n + 1) 取出最低位的 0）
//
// 🔥 Q2. 不用臨時變數的 XOR 交換為什麼實務上是壞主意？
//     答：a ^= b; b ^= a; a ^= b; 有三個問題：① aliasing——若 &a == &b（例如
//     swap(v[i], v[i])），第一步就把那塊記憶體清成 0，值永久遺失，而 std::swap 沒有這個
//     問題 ② 效能反而更差——三個運算彼此有資料相依，無法指令級平行，現代 CPU 上比
//     「temp 變數 + 三次 mov」慢，暫存器重新命名讓 temp 版幾乎零成本 ③ 可讀性差且只
//     適用於整數型別。正解永遠是 std::swap(a, b)。
//     追問：加減法版 a = a + b; b = a - b; a = a - b; 呢？（更糟，有號溢位是 UB，
//     而且同樣有 aliasing 問題）
//
// Q3. 用位移做乘除法還有意義嗎？
//     答：幾乎沒有。編譯器對「乘／除以 2 的冪」早已自動轉成位移，對非 2 冪的除法也會用
//     「乘上魔術數再位移」來最佳化，比手寫更好；而手寫 x >> 1 取代 x / 2 對有號負數還是
//     錯的。仍有意義的場合是雜湊函式、位圖操作、對齊計算（向上對齊到 2 的冪 a 寫成
//     (x + a - 1) & ~(a - 1)，取模 2 的冪寫成 x & (a - 1)，兩者都僅限無號或非負）。
//
// ⚠️ 陷阱. lowbit 寫成 n & -n，對 INT_MIN 安全嗎？
//     答：不安全。對有號型別而言 -n 在 n == INT_MIN 時是溢位，屬 UB（C++20 只規定了
//     補數表示法與位移語意，並沒有改變算術溢位仍是 UB 這件事）。正解是先轉成無號再做：
//     unsigned u = n; u & -u;（無號的一元負號有明確定義的模運算語意）。這也是「位運算
//     一律用 unsigned」這條原則的具體理由之一。
//     為什麼會錯：以為位運算「只是搬位元、不可能溢位」，但 -n 是算術運算不是位運算。
// ═══════════════════════════════════════════════════════════════════════════

#include <bitset>
#include <cstdint>
#include <iostream>

template <class T>
static std::string bin8(T x) {
    return std::bitset<8>(static_cast<std::uint8_t>(x)).to_string();
}

// 前置宣告：附加範例
static void demo_lc_371_sum_of_two_integers();
static void demo_xor_swap();

int main() {
    // ─────────────────────────────────────────────────────────
    // T1：n & (n-1) 清掉最低位 1 — 順便算 popcount
    // ─────────────────────────────────────────────────────────
    {
        std::uint8_t n = 0b10110;
        std::cout << "[T1] n     = " << bin8(n) << '\n';
        std::cout << "[T1] n-1   = " << bin8(static_cast<std::uint8_t>(n - 1)) << '\n';
        std::cout << "[T1] n&n-1 = " << bin8(static_cast<std::uint8_t>(n & (n - 1))) << '\n';

        // popcount via Brian Kernighan
        int cnt = 0;
        for (auto v = n; v; v &= (v - 1)) ++cnt;
        std::cout << "[T1] popcount(0b10110) = " << cnt << " (= 3)\n";
    }

    // ─────────────────────────────────────────────────────────
    // T2：n & -n 取最低位 1
    //   注意：負號要對 signed 做才有兩補意義；用 unsigned 時可以寫
    //         n & (~n + 1)，效果一樣但避開「對 unsigned 取負」的 warning
    // ─────────────────────────────────────────────────────────
    {
        std::int32_t n = 0b10110;
        std::int32_t lowbit = n & -n;
        std::cout << "[T2] lowbit(0b10110) = " << lowbit << " (= 2)\n";
    }

    // ─────────────────────────────────────────────────────────
    // T3：XOR 找「孤獨」元素（LeetCode 136 預演）
    // ─────────────────────────────────────────────────────────
    {
        int arr[] = {2, 3, 5, 3, 2};
        int x = 0;
        for (int v : arr) x ^= v;
        std::cout << "[T3] single = " << x << " (= 5)\n";
    }

    // ─────────────────────────────────────────────────────────
    // T4：判斷「全 1」
    // ─────────────────────────────────────────────────────────
    {
        std::uint32_t a = 0b0111;
        std::uint32_t b = 0b1010;
        std::cout << "[T4] 0b0111 全 1? "
                  << ((a & (a + 1)) == 0 ? "yes" : "no") << '\n';
        std::cout << "[T4] 0b1010 全 1? "
                  << ((b & (b + 1)) == 0 ? "yes" : "no") << '\n';
    }

    // ─────────────────────────────────────────────────────────
    // T5：(n-1) ^ n 取「最低位 1 以下含全 1」mask
    // ─────────────────────────────────────────────────────────
    {
        std::uint8_t n = 0b10100;
        std::uint8_t mask = static_cast<std::uint8_t>((n - 1) ^ n);
        std::cout << "[T5] n     = " << bin8(n)    << '\n';
        std::cout << "[T5] mask  = " << bin8(mask) << " (最低位 1 以下含)\n";
    }

    // ─────────────────────────────────────────────────────────
    // 課堂知識補充
    // ─────────────────────────────────────────────────────────
    //  Q1：「Brian Kernighan」popcount 跟 std::popcount 哪個快？
    //    A：std::popcount (C++20) 直接對應 CPU 指令 (popcnt) → 一個 cycle
    //       完成。Kernighan 版要跑「位元 1 的個數」次迭代，較慢但跨平台。
    //
    //  Q2：n & -n 對 0 也安全嗎？
    //    A：0 & -0 = 0，不會炸；只是「沒有最低位 1」。實務上常需要在前面加
    //       一個 if (n) 守護避免邏輯誤判。
    //
    //  Q3：XOR 0 vs OR 0 vs AND 0？
    //    A：XOR 0 = self（不變）；OR 0 = self；AND 0 = 0。記住這三條，多
    //       數位元演算法的「邊界 case 」就不會出錯。
    //
    demo_lc_371_sum_of_two_integers();
    demo_xor_swap();
    return 0;
}

// =============================================================================
//  附加 1：LeetCode 371. Sum of Two Integers（不能用 + 和 -）  // 難度: medium
// =============================================================================
//  題意：給 a、b，回傳 a + b，但「不能用 + 和 -」。
//  思路：把加法拆成「位元層級」 —
//    * 不含進位的加 = a ^ b
//    * 進位         = (a & b) << 1
//  不斷把「相加 + 進位」回灌，直到進位為 0。負數透過 unsigned 兩補碼自動處理。
// =============================================================================
static int getSum(int a, int b) {
    while (b != 0) {
        unsigned carry = (static_cast<unsigned>(a) & static_cast<unsigned>(b)) << 1;
        a = static_cast<int>(static_cast<unsigned>(a) ^ static_cast<unsigned>(b));
        b = static_cast<int>(carry);
    }
    return a;
}
static void demo_lc_371_sum_of_two_integers() {
    std::cout << "[LC371] 1 + 2 = " << getSum(1, 2) << '\n';
    std::cout << "[LC371] -2 + 3 = " << getSum(-2, 3) << '\n';
}

// =============================================================================
//  附加 2：實用範例 — XOR swap（不用暫存交換兩變數）
// =============================================================================
//  經典 hack：a ^= b; b ^= a; a ^= b; → a 和 b 互換值，且不需 temp。
//  原理：
//    第一行：a 變成 a^b
//    第二行：b 變成 (a^b)^b = a （原本的 a）
//    第三行：a 變成 (a^b)^a = b （原本的 b）
//  注意：a 與 b 是「同一個變數別名」時會把它清成 0；只用於兩個不同位址。
//  實務上請優先用 std::swap（編譯器會優化更好），這只是經典面試題。
// =============================================================================
static void xorSwap(int& a, int& b) {
    if (&a == &b) return;       // 防呆：避免同一變數情況
    a ^= b;
    b ^= a;
    a ^= b;
}
static void demo_xor_swap() {
    int a = 7, b = 11;
    xorSwap(a, b);
    std::cout << "[xor_swap] after swap: a=" << a << " b=" << b << " (= 11, 7)\n";
}

