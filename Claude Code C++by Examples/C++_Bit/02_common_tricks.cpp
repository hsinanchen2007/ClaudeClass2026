//  參考：
//    - https://en.cppreference.com/w/cpp/language/operator_arithmetic (位元運算子)
//    - https://en.cppreference.com/w/cpp/header/bit                   (C++20 <bit>)
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 5 個必背技巧                                               │
//  └────────────────────────────────────────────────────────────┘
//
//   T1. n & (n - 1)        清掉最低位的 1
//   T2. n & -n              取出最低位的 1 (最低位 1 的 mask)
//   T3. n ^ n = 0           同值 XOR 為 0、xor 用來「成對消除」
//   T4. n & (n + 1)         判斷「最後 k 位是否全為 1」: n&(n+1)==0
//   T5. (n - 1) ^ n         得到「最低位 1 以下全 1」的 mask
//
//  這些技巧把「需要寫迴圈的位元工作」壓成 O(1) 表達式。它們是 LeetCode
//  位元題、面試以及效能 hot path 的常客。
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ T1 詳解：n & (n - 1)                                       │
//  └────────────────────────────────────────────────────────────┘
//
//  例子：n = 0b10110
//        n - 1 = 0b10101
//        n & (n-1) = 0b10100   ← 把最右邊那個 1 變 0
//
//  原理：減 1 會把「最低位 1」翻成 0、它後面的 0 都翻成 1；AND 之後就剛好
//  把那個位剃掉，後面的 0 也保持 0。
//
//  典型應用：
//   * 計算位元 1 的個數（迭代 popcount）— 每次把最低位 1 拔掉，計 1
//   * 判斷是否為 2 的次方：`n != 0 && (n & (n-1)) == 0`
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ T2 詳解：n & -n                                           │
//  └────────────────────────────────────────────────────────────┘
//
//  例子：n = 0b10110
//        -n = ~n + 1 = 0b...01010 (兩補數)
//        n & -n = 0b00010   ← 最低位 1 的「位置」(以 mask 形式)
//
//  典型應用：
//   * Fenwick tree (BIT) 的 lowbit
//   * 找「最右邊那個 1 在哪一位」 → __builtin_ctz / std::countr_zero
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ T3 詳解：XOR 配對消除                                      │
//  └────────────────────────────────────────────────────────────┘
//
//  XOR 性質：
//   * a ^ a = 0
//   * a ^ 0 = a
//   * 交換律、結合律
//
//  推論：把陣列裡所有元素 XOR 起來，所有「成對」的元素互相消除，剩下「孤
//  獨那個」 — 這就是 LC136 的解法。
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ T4 / T5：較少用，列出供參                                  │
//  └────────────────────────────────────────────────────────────┘
//
//   T4：n & (n + 1) == 0 ⇔ n 的二進位表示全是 1（如 0、1、3、7、15...）
//   T5：(n - 1) ^ n 結果是「最低位 1 以下（含）全為 1」的 mask
//
// =============================================================================

/*
補充筆記：common_tricks
  - common_tricks 屬於位元操作；位元運算適合旗標、遮罩、集合狀態、低階資料格式和某些整數技巧。
  - 位移前要確認位數合法；位移負數或位移量大於等於型別寬度會造成未定義行為。
  - signed integer 的位元表示和右移行為有細節；做遮罩和位元技巧時通常使用 unsigned 型別較清楚。
  - x & (x - 1) 可清掉最低位的 1，常用於計算 set bits 或判斷 power of two；但 x=0 要另外處理。
  - C++20 <bit> 提供 popcount、has_single_bit、rotl、bit_width 等標準工具，能取代許多手寫技巧。
  - bit_cast 是按位元重新解讀且要求大小相同與 trivially copyable；它不是任意型別轉換。
  - x & (x - 1) 會清掉最低位的 1，這個技巧可用來計算 1 的數量或逐步移除集合中的元素。
  - x & -x 可取出最低位的 1，但要理解二補數表示；教材可用 unsigned 寫法降低 signed overflow 風險。
  - 位元技巧看起來短，但可讀性成本高；工作程式若有標準函式如 std::popcount，通常應優先使用標準函式。
*/

