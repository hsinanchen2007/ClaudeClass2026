// =============================================================================
//  09_lc_231_power_of_two.cpp  —  LeetCode 231. Power of Two
// =============================================================================
//  參考：
//    - https://en.cppreference.com/w/cpp/language/operator_arithmetic (位元運算子)
//    - https://en.cppreference.com/w/cpp/numeric/has_single_bit  (C++20 has_single_bit)
//    - https://leetcode.com/problems/power-of-two/
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 題意                                                       │
//  └────────────────────────────────────────────────────────────┘
//
//  輸入：整數 n
//  輸出：n 是不是 2 的次方？（1, 2, 4, 8, 16, ...）
//
//  注意：n 可能是負數或 0 — 兩者都 false。
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 思路 1：n & (n - 1) == 0                                   │
//  └────────────────────────────────────────────────────────────┘
//
//  2 的次方在二進位中只有「一個 1」，例如：
//      1   = 0b0001
//      2   = 0b0010
//      4   = 0b0100
//      8   = 0b1000
//      16  = 0b10000
//
//  把這個性質轉成位元判斷：
//      n & (n - 1) 把最低位 1 拔掉
//      若 n 只有一個 1 → 拔掉後變 0
//
//  正確寫法（要排除 0）：
//      n > 0 && (n & (n - 1)) == 0
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 思路 2：std::has_single_bit (C++20)                        │
//  └────────────────────────────────────────────────────────────┘
//
//  C++20 直接提供：
//      std::has_single_bit(static_cast<uint32_t>(n))
//
//  注意：has_single_bit 只接受 unsigned；signed 要先 cast。當 n ≤ 0 時不
//  能直接 cast（負數轉 uint 會變大正數），所以仍要先 if (n > 0)。
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 思路 3：n & -n == n                                         │
//  └────────────────────────────────────────────────────────────┘
//
//  n & -n 取出最低位 1（以 mask 形式）。若 n 本身就只有一個 1，那 mask
//  剛好等於 n。同樣要先排除 0。
//
// =============================================================================

/*
補充筆記：power_of_two
  - power_of_two 屬於位元操作；位元運算適合旗標、遮罩、集合狀態、低階資料格式和某些整數技巧。
  - 位移前要確認位數合法；位移負數或位移量大於等於型別寬度會造成未定義行為。
  - signed integer 的位元表示和右移行為有細節；做遮罩和位元技巧時通常使用 unsigned 型別較清楚。
  - x & (x - 1) 可清掉最低位的 1，常用於計算 set bits 或判斷 power of two；但 x=0 要另外處理。
  - C++20 <bit> 提供 popcount、has_single_bit、rotl、bit_width 等標準工具，能取代許多手寫技巧。
  - bit_cast 是按位元重新解讀且要求大小相同與 trivially copyable；它不是任意型別轉換。
  - 負數不是 2 的冪；使用 signed int 時必須先判斷 n > 0。
  - 1 是 2^0，因此正確判斷應接受 1，而不是只接受偶數。
*/

// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】LeetCode 231（判斷 2 的冪）
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. 怎麼判斷一個數是不是 2 的冪？
//     答：n > 0 && (n & (n - 1)) == 0。2 的冪的二進位形如 1000...0，減 1 後變成
//     0111...1，AND 起來是 0。等價寫法是 n > 0 && (n & -n) == n（lowbit 等於自己）。
//     C++20 可用 std::has_single_bit(static_cast<unsigned>(n))，但它不處理負數語意，
//     需要自己保證非負。
//     追問：怎麼判斷是 4 的冪？（在 2 的冪的基礎上，再要求那個唯一的 1 落在偶數位：
//     n > 0 && (n & (n-1)) == 0 && (n & 0x55555555)）怎麼求它是 2 的幾次方？
//     （std::countr_zero 或 __builtin_ctz）
//
// 🔥 Q2. 為什麼 std::has_single_bit 只吃無號型別反而是好事？
//     答：因為「2 的冪」這個概念在補數表示的負數上根本沒有意義，而手寫的
//     (n & (n-1)) == 0 對 INT_MIN 會誤判成 true。強制無號等於用型別系統把負數這個
//     錯誤輸入擋在門外，剩下的唯一邊界只有 0——而 has_single_bit(0) 正確回傳 false。
//
// ⚠️ 陷阱. 忘了寫 n > 0 會怎樣？
//     答：兩個誤判。① n == 0 時 (0 & 0xFFFFFFFF) == 0 成立 → 誤判 0 是 2 的冪
//     ② n == INT_MIN（0x80000000）時 (n & (n-1)) == 0 也成立 → 誤判負數是 2 的冪。
//     所以 n > 0 這個守衛不是防禦性冗餘，而是正確性的一部分。
//     為什麼會錯：只用正數測試，而這個位元技巧的兩個反例恰好都在測試資料的邊界之外。
// ═══════════════════════════════════════════════════════════════════════════

#include <cstdint>
#include <iostream>

#if __cplusplus >= 202002L
#  include <bit>
#endif

// 前置宣告：附加範例
static void demo_lc_342_power_of_four();
static void demo_next_pow2_capacity();

static bool isPow2_v1(int n) {
    return n > 0 && (n & (n - 1)) == 0;
}

static bool isPow2_v2(int n) {
#if __cplusplus >= 202002L
    return n > 0 && std::has_single_bit(static_cast<std::uint32_t>(n));
#else
    return isPow2_v1(n);
#endif
}

static bool isPow2_v3(int n) {
    return n > 0 && (n & -n) == n;
}

int main() {
    int cases[] = {1, 2, 3, 4, 16, 18, 0, -2, 1024};
    for (int n : cases) {
        std::cout << "n=" << n
                  << " | v1=" << isPow2_v1(n)
                  << " v2=" << isPow2_v2(n)
                  << " v3=" << isPow2_v3(n)
                  << '\n';
    }

    // ─────────────────────────────────────────────────────────
    // 課堂知識補充
    // ─────────────────────────────────────────────────────────
    //  Q1：負數呢？
    //    A：兩補碼下，例如 -2 的二進位是 0xFFFFFFFE，有多個 1 → 直接套
    //       (n & (n-1)) == 0 不會通過。但 INT_MIN 在 signed int 下有特例
    //       （0x80000000 在 signed 是負，但只有一個 1）— 我們的 n > 0 判
    //       斷會把它擋掉，所以安全。
    //
    //  Q2：為什麼 std::has_single_bit 才比較對？
    //    A：has_single_bit 把「只有一個 1」這個語意明寫出來，比 (n & (n-1))
    //       == 0 直觀許多。如果你的工具鏈是 C++20，優先用它。
    //
    //  Q3：4 的次方（LC342）呢？
    //    A：除了 (n & (n-1)) == 0（先確認是 2 的次方），再檢查「位元 1 落
    //       在偶數位」：n & 0x55555555 != 0。
    //
    demo_lc_342_power_of_four();
    demo_next_pow2_capacity();
    return 0;
}

// =============================================================================
//  附加 1：LeetCode 342. Power of Four
// =============================================================================
//  題意：判斷 n 是否為 4 的次方（1, 4, 16, 64, ...）。
//  思路：n 是 4^k ⇔ (a) n 是 2 的次方，且 (b) 唯一的 1 落在「偶數 bit 位置」。
//        mask 0x55555555 把偶數位置標記為 1（bit0、bit2、bit4...），用 AND 檢查。
//  從 LC 231 延伸：「powers of 2」中再篩出「powers of 4」，純位元判斷一行解。
// =============================================================================
static bool isPowerOfFour(int n) {
    return n > 0
        && (n & (n - 1)) == 0
        && (n & 0x55555555) != 0;
}
static void demo_lc_342_power_of_four() {
    int cases[] = {1, 4, 16, 8, 64, 0, -16};
    for (int n : cases) {
        std::cout << "[LC342] isPow4(" << n << ") = " << isPowerOfFour(n) << '\n';
    }
}

// =============================================================================
//  附加 2：實用範例 — Hash table / ring buffer 容量向上取 2 的次方
// =============================================================================
//  許多容器（unordered_map、ring buffer）內部用 2 的次方容量，因為 `x mod N`
//  在 N 為 2 次方時可以化簡為 `x & (N - 1)` —— 比除法快很多。
//  下面 nextPow2 把任意 want 向上取到最近的 2 次方（用迴圈版本，相容 C++17）。
//  C++20 有 std::bit_ceil 直接做這件事。
// =============================================================================
static std::uint32_t nextPow2(std::uint32_t want) {
    if (want <= 1) return 1;
    std::uint32_t p = 1;
    while (p < want) p <<= 1; // 不會 overflow：want 上限應 < 2^31
    return p;
}
static void demo_next_pow2_capacity() {
    std::cout << "[nextPow2] 7 -> " << nextPow2(7) << '\n';
    std::cout << "[nextPow2] 8 -> " << nextPow2(8) << '\n';
    std::cout << "[nextPow2] 1000 -> " << nextPow2(1000) << '\n';
}
