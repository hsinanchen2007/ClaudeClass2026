// =============================================================================
//  05_digit_separator.cpp  —  千位分隔符 ' (C++14)
// =============================================================================
//  參考：https://en.cppreference.com/w/cpp/language/integer_literal
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 一、語法                                                   │
//  └────────────────────────────────────────────────────────────┘
//
//      1'000'000              = 1000000     (一百萬)
//      1'000'000'000LL        = 10 億 (long long)
//      0xFF'FF'FF'FF          = 4294967295 (32-bit max)
//      0b1111'1111'0000'0000  = 0xFF00
//      3.141'592'653'5        = π 近似
//
//  單引號 `'` 純粹是「視覺分隔符」 — 編譯器把它去掉後解析。
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 二、好處                                                   │
//  └────────────────────────────────────────────────────────────┘
//
//   * 大數字一目瞭然 — 「1'000'000」 vs 「1000000」 vs 「10000000」
//   * 配合二進位 / 十六進位字面量極好讀（ex. 0xFF'FF'00'00 比 0xFFFF0000 好讀）
//   * 跟「常數查表 / 平台 ABI」常數搭配時可讀性大增
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 三、限制                                                   │
//  └────────────────────────────────────────────────────────────┘
//
//   * 必須在「數字之間」 — 不能放開頭、結尾、或字面量後綴前
//   * 開頭：'1234   ❌（變字元字面量）
//   * 結尾：1234'   ❌
//   * 後綴前：123'LL  ❌（必須是 123LL 或 1'2'3LL）
//
//  ┌────────────────────────────────────────────────────────────┐
//  │ 四、本檔示範                                               │
//  └────────────────────────────────────────────────────────────┘
// =============================================================================

/*
補充筆記：digit_separator
  - digit_separator 是現代 C++ 語法或標準庫特性；學習時要把「少寫字」和「語意更精確」分開看。
  - auto 讓型別由初始化式推導，但會丟掉 top-level const/reference；需要保留引用語意時要寫 auto&、const auto& 或 decltype(auto)。
  - brace initialization 能減少未初始化與 narrowing，但遇到 initializer_list overload 可能選到不同建構子。
  - constexpr、static_assert、if constexpr 把部分錯誤和計算提前到編譯期，能讓 template 和常數邏輯更清楚。
  - 屬性如 [[nodiscard]]、[[maybe_unused]]、[[fallthrough]] 是對編譯器和讀者的意圖標記，不應拿來掩蓋設計問題。
  - string_view、optional、variant、structured binding 等特性改善介面表達力，但也帶來生命週期或狀態檢查責任。
  - 單引號 digit separator 只改善可讀性，不改變數值。
  - 分隔位置不能亂放在開頭、結尾或連續出現；它是 lexical 語法的一部分。
*/

// ═══════════════════════════════════════════════════════════════════════════
// 【面試題】數字分隔符 '（C++14）
// ───────────────────────────────────────────────────────────────────────────
// 🔥 Q1. C++ 的數字分隔符是什麼符號？屬於哪個標準？可以放哪裡？
//     答：是「單引號 '」，C++14 引入（不是底線；底線留給使用者自訂字面值後綴）。
//         規則：只能夾在數字「之間」——不能放開頭、不能放結尾、不能連續兩個、
//         也不能緊貼在後綴之前（123'LL 會被 GCC 判為 digit separator outside
//         digit sequence）。整數、浮點數、0x、0b 都能用。
//     追問：為什麼選單引號而不是底線？（底線已被 UDL 後綴用掉，
//           如 3.5_km，再拿來當分隔符會產生無法消解的歧義）
//
// ⚠️ 陷阱. 加了分隔符之後，數值會變嗎？在 C++11 編譯器上會怎樣？
//     答：值完全不變——分隔符只是 lexical 層的視覺輔助，編譯器移除後才解析，
//         所以 1'000'000 == 1000000、0b1010'1010 == 0xAA 都成立（可用
//         static_assert 直接驗）。但把同一份碼丟給 C++11 模式編譯，GCC 不會說
//         「這是 C++14 特性」，而是報 multi-character character constant——
//         因為它把 '000' 當成了「多字元的字元字面值」。
//     為什麼會錯：分隔符看起來是純裝飾，容易假設「舊編譯器頂多警告一下」。
//         實際上單引號在 C++11 的文法裡是字元字面值的界定符，語意被整個誤讀，
//         錯誤訊息也完全指不到真正原因——是版本分界題裡很好用的一題。
// ═══════════════════════════════════════════════════════════════════════════

#include <cstdint>
#include <iostream>

int main() {
    constexpr long long pop      = 8'100'000'000LL;        // 世界人口 ~80 億
    constexpr int       million  = 1'000'000;
    constexpr std::uint32_t mask = 0xFF'FF'00'00u;         // 高 16 bit
    constexpr int       binary   = 0b1010'0101'1111'0000;  // 4-bit 一組

    std::cout << "[Demo] pop     = " << pop     << '\n';
    std::cout << "[Demo] million = " << million << '\n';
    std::cout << "[Demo] mask    = 0x" << std::hex << mask << std::dec << '\n';
    std::cout << "[Demo] binary  = " << binary << '\n';

    // 跟 0b 字面量結合，「4-bit 一組」非常直觀
    constexpr std::uint32_t REGISTER = 0b1100'0011'0101'1010u;
    std::cout << "[Demo] reg     = " << REGISTER << '\n';

    // ─────────────────────────────────────────────────────────
    // 課堂知識補充
    // ─────────────────────────────────────────────────────────
    //  Q1：分隔位置可以隨意嗎？
    //    A：除了「不能放開頭/結尾/緊鄰後綴」之外都可以。慣例是「數值類三
    //       位一組」、「16-進位兩位一組」、「二進位四位一組」。
    //
    //  Q2：跟「字元字面量」會混淆嗎？
    //    A：'A' 是字元字面量；1'234 是數字。編譯器靠「前面有沒有非字元
    //       字面量字元」判斷。實務不會撞，編譯錯訊息一旦出現也很清楚。
    //
    //  Q3：浮點數也可以加嗎？
    //    A：可以。3.141'592'653'589'793 完全合法。對科學記號常數可讀性
    //       大增。
    //
    // ─────────────────────────────────────────────────────────
    // 實用範例：常見「魔術數字」用千位分隔符更清楚
    //   工作上常見：timeout 設定、平台 limit、檔案大小門檻
    // ─────────────────────────────────────────────────────────
    constexpr int   TIMEOUT_MS    = 30'000;            // 30 秒
    constexpr long  MAX_FILE_SIZE = 100'000'000L;      // 100 MB
    constexpr int   USERS_LIMIT   = 1'000'000;         // 一百萬使用者
    constexpr double PI           = 3.141'592'653'589'793;
    constexpr double LIGHT_SPEED  = 299'792'458.0;      // 公尺/秒

    std::cout << "[Demo2] TIMEOUT_MS    = " << TIMEOUT_MS << '\n';
    std::cout << "[Demo2] MAX_FILE_SIZE = " << MAX_FILE_SIZE << " bytes\n";
    std::cout << "[Demo2] USERS_LIMIT   = " << USERS_LIMIT << '\n';
    std::cout << "[Demo2] PI            = " << PI << '\n';
    std::cout << "[Demo2] LIGHT_SPEED   = " << LIGHT_SPEED << " m/s\n";

    return 0;
}
